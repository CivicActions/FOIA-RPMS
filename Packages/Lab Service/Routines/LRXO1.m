LRXO1 ; IHS/DIR/AAB - Lab Orders 10:31 ; [ 4/18/91 ]
 ;;5.2;LR;**1002**;JUN 01, 1998
 ;
 ;;5.2;LAB SERVICE;**75,100,128**;Sep 27, 1994
EN ;Entry for lab orders
 D EN1,END1
 Q
EN1 Q:'$D(ORACTION)!('$D(ORVP))  S DFN=+ORVP,LREND=0,LRSX=1
 I ORACTION D EN^LRXO0 Q
 D:'$D(LRPRAM)!('$D(LROUTINE)) INIT^LRXO00 G:LREND END
 D SETUP^LRXO00 G:LREND END
 D SAMP G END:LREND
 D DATE G END:LREND
 D MAX G END:LREND
 D URG
 D INCRM^LRXO4 ;Check for valid ORSX
 S LRSCH="" D COM S LRORGST=LROST D SCHED S LROST=LRORGST
 G END
 Q
TEST ;Get Lab test
 ;S:$D(ORSPU) LRSPU=1
 K DIC W !!,"Select LABORATORY TEST: " R X:DTIME I X["^"!(X="") S:X="^^" DIROUT=1 S LRTST="" Q
 I $E(X)="*" S XQORSPEW=$S($G(XQORSPEW):0,1:1),X=$E(X,2,255)
 S LRINPUT=X,X=$P(X,"=",2) S LRASK=$S($G(XQORSPEW):1,1:0),LRASKU=$S(X["*":1,$D(^LAB(69.9,1,9,+OROLOC,0)):$S($P(^(0),"^",5):1,1:0),1:0) S:$E(X)="*" X=$E(X,2,999) S LRECUR=$S(X["X":$E(X,$F(X,"X"),$F(X,"X")+1),1:0)
 I 'LRECUR S LRECUR=$S(X["X":+$E(X,$F(X,"X")-3,$F(X,"X")-2),1:X)
 S X=$P(LRINPUT,"="),DIC="^LAB(60,",DIC(0)="EMQ",DIC("S")="I ""NO""'[$P(^(0),""^"",3)" D ^DIC K DIC I Y<1 S LRTST="" W !,$C(7),"Lab test not found" G TEST
 S LRTST=+Y
 Q
SAMP ;Get Coll Sample & Specimen
 S:$D(LRFSAMP) LRSAMP=LRFSAMP S:$D(LRFSPEC) LRSPEC=LRFSPEC
 D:LRSAMP=""!(LRSPEC="") EN^LRXO7 I LRSAMP<1!(LRSPEC<1) W !,"Sample and specimen source incompletely defined, test skipped." S LREND=1 H 5 Q
 I $D(^LAB(60,LRTST,3,"B",+LRSAMP)) S N1=$O(^(+LRSAMP,0)) I N1'<1 S LRMAX=$P(^LAB(60,LRTST,3,N1,0),"^",2) I $O(^(1,0))'<1 W !!,"For "_$P(^LAB(60,LRTST,0),"^") W !,"WARD REMARKS: " D S1
 D SCRN^LRXO00
 Q
S1 S N2=0 F  S N2=$O(^LAB(60,LRTST,3,N1,1,N2)) Q:N2<1  W !,^(N2,0)
 Q
MAX ;Maximum order frequency
 S J=$P(LROST,"."),K=0
 F  S K=$O(^LRO(69,J,1,"AA",LRDFN,K)) Q:K<1  I $S($D(^LRO(69,J,1,K,1)):$P(^(1),U,4)'="U",1:1) S S=$S($D(^LRO(69,J,1,K,4,1,0)):+^(0),1:0) F I=0:0 S I=$O(^LRO(69,J,1,K,2,I)) Q:I<1  S X=+^(I,0) D TT
 ;I XQORNOD[";ORD(101," S LRTY=+LRTST D MAX^LRXO8
 I LRTST S LRTY=+LRTST D MAX^LRXO8
 Q
TT S:'$D(TT(X,S)) TT(X,S)=0 S TT(X,S)=TT(X,S)+1,TT(X,S,J\1)="",TT(X,S,-J\1)=""
 Q
DUP ;Check duplicate
 Q
SCHED ;Get Schedule
 D EN^LRXO2 Q
URG ;Get Urgency
 D URG^LRXO2 Q
DATE ;Get Collect time
 I $D(LRFDATE) S LROST=LRFDATE Q
 I $D(^XUTL("OR",$J,"LRFNDATE")),^("LRFNDATE")'=LRZX(1) D
 .I LRZX(1)["I" D EN^LRORDIM D  Q
 ..I '$D(LRCDT) Q
 ..S LROST=LRCDT K LRCDT
 ..D SET^LRXO5
 .D EN^LRXO5
 I $D(^XUTL("OR",$J,"LROLDZX")) S ^("LRFNDATE")=^("LROLDZX")
 I $D(^XUTL("OR",$J,"LROST")),'$D(LRFLG) S LROST=^("LROST")
 I $D(LRSOST),'$D(LRFLG) S LROST=LRSOST K LRSOST
 I '$D(LROST) D  Q:LREND
 .I LRZX(1)["I" D EN^LRORDIM D  Q
 ..I '$D(LRCDT) K LROST S LREND=1 Q
 ..S LROST=LRCDT K LRCDT
 ..D SET^LRXO5
 .D EN^LRXO5
 Q
COM ;Get Comments
 D RCOM^LRXO2 Q
END ;Clean-up & Exit
 K XQORNOD(0),LRFLG,LRONE,LRRCOL,LRSOST,LRMAXX,LRLABKY,LRLLOC,LRQ,LRUNQ
 K LRORGST,LRSPU,LRASKU,LRASK,LREND,DIC,DIE,DA,DR,LRSPEC,LRYR,LREXP,LRMAX1,LRMAX2,LRMAXX,LROSMAX,LROSON,LROSX,LRSAMP,LRSPEC,LRSCH,LRSX,LRTST,LRTSTNM,LRTY,LRURG
 D CLEAN^LRXO3
 Q
END1 K LRDFN,LRDPF,LRECUR,LREK,LRINPUT,LRMAX,LRORD,LROST,LRPREV,LROUTINE,LRPCE,LRPRAM,LRSTUB,LRZX,TT,Y,N1,N2,N3,SCRN
 Q
ONE ;Get any lab test
 Q:'$D(ORACTION)  Q:'$D(ORGY)  S DFN=+ORVP
 S LRDPF=+$P(@("^"_$P(ORVP,";",2)_"0)"),"^",2)_"^"_$P(ORVP,";",2) D END^LRDPA I LRDFN<1&(ORACTION'=7) W " LAB FILE NOT CONSISTENT WITH PATIENT FILE. CANNOT PLACE ORDER!" S XQORPOP=1 G END
 N LREND S LREND=0 D:$L($G(ORPK)) PATCK^LRXO0(ORPK) I LREND S XQORPOP=1 G END
 S LRONE=1
 I ORACTION=1 D EN^LROR6 Q  ;Edit
 I ORACTION=2 D EN^LROR7 Q  ;Renew
 I ORACTION=3 D EN^LROR8 Q  ;Flag
 I ORACTION=4 D EN1^LROR8 Q  ;Hold
 I ORACTION=6 D C^LROR3 Q  ;DC/Cancel
 I ORACTION=7 D P^LROR3 Q  ;Purge
 I ORACTION=8 D STAT^LROR1 Q  ;Detailed Display
 I ORGY=9 ;Release (Lab uses Transaction code field instead)
 I ORGY=10 D EN2^LROR8 Q  ;Verify
 I ORGY=0 F  D TEST Q:'LRTST  S XQORNOD=+Y,$P(XQORNOD(0),"^",4)=X D EN1^LRXO1
 D END1
 Q
