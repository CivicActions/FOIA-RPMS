 Q
AUTO ; RUNS THE SEARCH FOR THE PREVIOUS MONTH ONLY
 K %DT,X,Y
 Q:DT<($E(DT,1,5)_"15")
 S X=$S($E(DT,4,5)="01":($E(DT,1,3)-1),1:$E(DT,1,3))
 S X1=$S($E(DT,4,5)="01":"12",1:($E(DT,4,5)-1))
 S:X1<10 X1="0"_X1
 S X=X_X1_"15"
 K X1,Y D DAYS
 S LRRPS=$E(X1,1,5)_"01",LRRPE=$E(X1,1,5)_X
 S LRDT=$E(DT,1,5)_14
 S D0=0 F  S D0=$O(^LAB(69.5,D0)) Q:+D0'>0  D
 .Q:$P(^LAB(69.5,D0,0),U,2)="1"
 .Q:$P(^LAB(69.5,D0,0),U,7)=""
 .Q:'$D(^ORD(101,$P(^LAB(69.5,D0,0),U,7),0))
 .S LRRNDT=$P(^LAB(69.5,D0,0),U,4)
 .I LRRNDT="" S LREPI(D0)="" Q
 .Q:LRRNDT>LRDT
 .S LREPI(D0)=""
 S LRRTYPE=0
TASK ;LETS TASK THIS JOB
 K ZTSAVE
 S (ZTSAVE("LREPI("),ZTSAVE("LRRPS"),ZTSAVE("LRRPE"))=""
 S ZTSAVE("LRRTYPE")="" S:LRRTYPE=0 ZTDTH=DT
 S ZTIO="",ZTRTN="EN^LREPI",ZTDESC="Emerging Pathogen Search"
 D ^%ZTLOAD
 I '$D(ZTQUEUED)&(+$G(ZTSK)>0) W @IOF,!!,"The Task has been queued",!,"Task # ",$G(ZTSK) H 5
EXIT ;
 K D0,LRAUTO,LRBEG,LRDT,LREND,LRRNDT,LREPI,LRRPE,LRRPS,ZTSAVE
 K ZTRTN,ZTIO,ZTDESC,ZTDTH,ZTSK,X,Y,X1,%DT
 Q
MAN ;USED TO RERUN THE OPTION FOR ANY PRIOR MONTHS 
 S LRRTYPE=1
 W @IOF,?(IOM/2-15),"Emerging Pathogen rerun option"
 K DIR,DIRUT
 S DIR("A")="Enter the Month and Year "
 S DIR(0)="DO^:"_DT D ^DIR
 G:$D(DIRUT) EXIT
 K DIR,DIRUT
 S X=Y D DAYS
 S LRRPE=$E(Y,1,5)_X,LRRPS=$E(Y,1,5)_"01"
 K X,Y,X1
 S D0=0 F  S D0=$O(^LAB(69.5,D0)) Q:+D0'>0  D
 .Q:$P(^LAB(69.5,D0,0),U,2)="1"
 .Q:$P(^LAB(69.5,D0,0),U,7)=""
 .Q:'$D(^ORD(101,$P(^LAB(69.5,D0,0),U,7),0))
 .S LREPI(D0)=""
 G TASK
DAYS ;GET DAYS OF THE MONTH
 S X1=X,X=+$E(X,4,5),X=$S("^1^3^5^7^8^10^12^"[(U_X_U):31,X'=2:30,$E(X1,1,3)#4:28,1:29)
 Q
