ACHSUEI ;IHS/OIT/FCJ - VENDOR UEI REPORT
 ;;3.1;CONTRACT HEALTH MGMT SYSTEM;**29**;JUN 11, 2001;Build 86
 ;Print Vendor information from matching process completed during patch 29 install
 ;ACHS*3.1*29 New Routine
 ;
 ;
RPTTY ; Select type of Vendors to print.
 S ACHSRPT=$$DIR^XBDIR("S^1:Matched Vendor;2:Possible Matched Vendor;3:Unmatched Vendor","Print Vendor Status Report","1","","","^D HELP^ACHS(""H1"",""ACHSUEI"")",2)
 Q:$D(DUOUT)!$D(DTOUT)
 S ACHSRPTN=$S(ACHSRPT=1:"Matched",ACHSRPT=2:"Possible Matched",ACHSRPT=3:"Unmatched",1:3)
 S ACHSRPT=$S(ACHSRPT=1:"M",ACHSRPT=2:"P",ACHSRPT=3:"N",1:3)
 D DEV
 Q
 ;
EXIT ;
 K ^TMP("ACHSVUEI",$J)
 D EN^XBVK("ACHS")
 D ^ACHSVAR
 Q
 ;
CALC ;VENDOR REPORT
 ;S ACHSPG=0 D HDR
 S ACHSUIEN=0,ACHSTOT(ACHSRPT)=0
 F  S ACHSUIEN=$O(^ACHSVUEI(ACHSUIEN)) Q:ACHSUIEN'?1N.N  D
 .;SET PRINT DATA
 .S ACHSUREC=^ACHSVUEI(ACHSUIEN,0)
 .S ACHSSTA=$P(ACHSUREC,U,2)
 .Q:ACHSSTA'=ACHSRPT
 .S ACHSUEIN=$P(ACHSUREC,U,3),ACHSDUNS=$P(ACHSUREC,U,6),ACHSUEI=$P(ACHSUREC,U,4)
 .S ^TMP("ACHSVUEI",$J,ACHSUIEN)=$P(ACHSUREC,U)_U_ACHSUEIN_U_ACHSUEI_U_ACHSDUNS
 .I ACHSSTA="N" S ACHSTOT(ACHSSTA)=ACHSTOT(ACHSSTA)+1 Q
 .I $D(^ACHSVUEI(ACHSUIEN,1)) S ACHSVIEN=0 F  S ACHSVIEN=$O(^ACHSVUEI(ACHSUIEN,1,ACHSVIEN)) Q:ACHSVIEN'?1N.N  D
 ..S ACHSVDFN=$P(^ACHSVUEI(ACHSUIEN,1,ACHSVIEN,0),U),ACHSSTA=$P(^(0),U,2)
 ..Q:ACHSVDFN=""
 ..S ^TMP("ACHSVUEI",$J,ACHSUIEN,1,ACHSVIEN)=$E($P(^AUTTVNDR(ACHSVDFN,0),U),1,28)
 ..S ACHSTOT(ACHSSTA)=ACHSTOT(ACHSSTA)+1
 Q
 ;
PRINT ;PRINT REPORT
 S ACHSPG=0
 S (ACHSUIEN,ACHSUREC)=0
 D HDR
 F  S ACHSUIEN=$O(^TMP("ACHSVUEI",$J,ACHSUIEN)) Q:ACHSUIEN'?1N.N  D  G:$G(ACHSQUIT) EXIT
 .S ACHSUREC=^TMP("ACHSVUEI",$J,ACHSUIEN)
 .I ACHSRPT="N" D  Q
 ..W !,$E($P(ACHSUREC,U),1,46),?47,$P(ACHSUREC,U,2),?58,$P(ACHSUREC,U,3),?71,$P(ACHSUREC,U,4)
 ..D CHK
 .I $D(^TMP("ACHSVUEI",$J,ACHSUIEN,1)) S ACHSVDFN=0,ACHSVNM="" D
 ..F  S ACHSVDFN=$O(^TMP("ACHSVUEI",$J,ACHSUIEN,1,ACHSVDFN)) Q:ACHSVDFN'?1.N.N  D
 ...W !,^TMP("ACHSVUEI",$J,ACHSUIEN,1,ACHSVDFN)
 ...W:ACHSRPT="M" ?29,$E($P(ACHSUREC,U),1,28),?58,$P(ACHSUREC,U,3),?71,$P(ACHSUREC,U,4)
 ...W:ACHSRPT="P" ?29,$E($P(ACHSUREC,U),1,27),?57,$P(ACHSUREC,U,2),?68,$P(ACHSUREC,U,3)
 .D CHK
 D TOT
 I ACHSDEV'="B",IOST["C-" W ! D RTRN^ACHS
 Q
 ;
DEV ;
 K ACHSDEV
 W ! S DIR(0)="S^P:PRINT Output;B:BROWSE Output on Screen",DIR("A")="Do you wish to",DIR("B")="P" K DA D ^DIR K DIR
 S ACHSDEV=Y
 Q:$D(DIRUT)!$D(DTOUT)
 I $G(ACHSDEV)="B" D BROWSE Q
 S XBRP="PRINT^ACHSUEI",XBRC="CALC^ACHSUEI",XBRX="EXIT^ACHSUEI",XBNS="ACHS"
 D ^XBDBQUE
 Q
BROWSE ;
 S XBRP="VIEWR^XBLM(""PRINT^ACHSUEI"")"
 S XBRC="CALC^ACHSUEI",XBRX="EXIT^ACHSUEI",XBIOP=0,XBNS="ACHS" D ^XBDBQUE
 Q
 ;
CHK ;CHECK TO CONTINUE
 I ACHSDEV'="B",$Y>(IOSL-5),IOST["C-" W ! D RTRN^ACHS Q:$G(ACHSQUIT)  D HDR
 I ACHSDEV'="B",$Y>(IOSL-5),IOST'["C-" D HDR
 Q
HDR ;
 S ACHSPG=ACHSPG+1
 W:ACHSDEV'="B" @IOF,!!
 W $$C^XBFUNC("CHS VENDOR UEI REPORT",80)
 W:ACHSDEV'="B" ?65,"Page: ",ACHSPG
 W !,$$C^XBFUNC("For "_ACHSRPTN_" Vendors",80)
 W !,$$REPEAT^XLFSTR("=",80)
 I ACHSRPT="M" D
 .W !,"RPMS VENDOR",?29,"UFMS VENDOR",?60,"UEI",?71,"DUNS"
 .W !,$$REPEAT^XLFSTR("-",28),?29,$$REPEAT^XLFSTR("-",28),?58,$$REPEAT^XLFSTR("-",12),?71,$$REPEAT^XLFSTR("-",9)
 I ACHSRPT="P" D
 .W !,"RPMS VENDOR",?29,"UFMS VENDOR",?60,"EIN",?71,"UEI"
 .W !,$$REPEAT^XLFSTR("-",28),?29,$$REPEAT^XLFSTR("-",27),?57,$$REPEAT^XLFSTR("-",10),?68,$$REPEAT^XLFSTR("-",12)
 I ACHSRPT="N" D
 .W !,"UFMS VENDOR",?47,"EIN",?58,"UEI",?71,"DUNS"
 .W !,$$REPEAT^XLFSTR("-",46),?47,$$REPEAT^XLFSTR("-",10),?58,$$REPEAT^XLFSTR("-",12),?71,$$REPEAT^XLFSTR("-",9)
 Q
 ;
TOT ;
 W !,$$REPEAT^XLFSTR("=",80)
 W !,"Total Vendor UEI"
 W:ACHSRPT="M" !?5,"Total Matched: ",ACHSTOT("M")
 W:ACHSRPT="P" !?5,"Total Possible Matched: ",ACHSTOT("P")
 W:ACHSRPT="N" !?5,"Total Not Matched: ",ACHSTOT("N")
 Q
 ;
H1 ;EP - From HELP^ACHS() via ^DIR.
 ;;@;!
