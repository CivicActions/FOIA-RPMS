 I $E(X,119,119)=" "!($E(X,119,119)="") W ! G B4
 W ?14,"<",$E(X,119,134),">",!
 I $E(X,135,136)="  " W !! G B4
 W ?14,"<",$E(X,135,150),">",!!
 G B4
