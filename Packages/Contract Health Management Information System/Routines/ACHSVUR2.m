 ;       SPLT RTN ACHSVUR22
 ;
 ;ACHSQ-STOPPED DURING REPORT PRINTING Q IF NOT CREATING FILES
 ;ACHSQ1-STOPPED DURING REPORT PRINTING BUT CONTINUE TO FINISH CREATING FILES
 ;ACHSQP-BROWSE Q
 S ACHSIO=IO,ACHSQ1=0,ACHSQP=0
 K ^TMP($J),^TMP("ACHSVUR2",$J),^TMP($J,"ACHSVUR2"),^TMP("ACHSVUR2T",$J)
ST ;
 D BDT G EXT:$D(DUOUT)!$D(DTOUT)!(ACHSBDT<1)
 D DEV
 D:ACHSFIL="Y" FIL
 G END
 Q
 ;
END ; Kill vars, close device, quit.
 X:$D(IO("S")) ACHSPPC
 K DA,DFN,ZTSK
 K ^TMP($J,"ACHSVUR2"),^TMP("ACHSVUR2",$J),^TMP("ACHSVUR2T",$J)
 D ERPT^ACHS,EN^XBVK("ACHS"),^ACHSVAR:'$D(ZTQUEUED)
EXT ; Kill vars, close device, quit.
 K ACHSVIEN,ACHSVNAM,ACHSHRN,ACHSDOB,ACHSBDT,ACHSEDT,ACHSIO,ACHSQIO,ACHSRPT,DTOUT,DUOUT,ZTSK,ACHSERR
 K INS,INSF,XBPAFN,DAT,ACHDDOS
 D ^%ZISC
 Q
 ;
BDT ; Enter beginning date.
 S ACHSBDT=$$DATE^ACHS("B","Vendor Usage","ISSUE")
 Q:$D(DUOUT)!$D(DTOUT)!(ACHSBDT<1)
EDT ; Enter the ending date.
 S ACHSEDT=$$DATE^ACHS("E","Vendor Usage","ISSUE")
 G BDT:$D(DUOUT)!$D(DTOUT)!(ACHSEDT<1),EDT:$$EBB^ACHS(ACHSBDT,ACHSEDT)
DOCS ; Select type of docs to print.
 S ACHSRPT=$$DIR^XBDIR("S^1:ALL documents;2:OPEN documents only","Print which documents","1","","","^D HELP^ACHS(""H1"",""ACHSVUR2"")",2)
 G EDT:$D(DUOUT)!$D(DTOUT)
ALL ; Select ALL or specific vendor  ;ACHS*3.1*31 New section
 S ACHSALL=$$DIR^XBDIR("S^1:Specific Vendor;2:All Vendors","Include which Vendors","1","","","^D HELP^ACHS(""H3"",""ACHSVUR2"")",2)
 G DOCS:$D(DUOUT)!$D(DTOUT)
 G:ACHSALL=2 FILE
VEND ;  Select one vendor.
 S DIC(0)="AEQMZ",DIC="^AUTTVNDR(",DIC("A")="Enter Vendor:  "
 S:DIC(0)["L" DLAYGO=9999999.11
 D ^DIC
 K DIC,DLAYGO
 G DOCS:Y=-1
 S ACHSVIEN=+Y
 S ACHSVNAM=$P(^AUTTVNDR(ACHSVIEN,0),U)
FILE ;CREATE A FILE
 S %=$$DIR^XBDIR("Y","Create file(s)","N","","","^D HELP^ACHS(""H2"",""ACHSVUR2"")",2)
 G ALL:$D(DUOUT)!$D(DTOUT)
 S ACHSFIL=$S(%:"Y",1:"N")
 Q
DEV ; Select device for report.
 W !
 S %=$$PB^ACHS
 I $D(DTOUT)!$D(DUOUT) S ACHSQ1=1 Q
 I %="B" D VIEWR^XBLM("CALC^ACHSVUR2"),EN^XBVK("VALM") S ACHSQP=1 Q
 S %ZIS="PQ"
 D ^%ZIS,SLV^ACHSFU:$D(IO("S")) K %ZIS
 I POP W !,*7,"No device specified." D HOME^%ZIS S ACHSQ1=1
 I $D(IO("Q")) D QUE Q
 ;
CALC ;EP - TaskMan.
 D FC^ACHSUF
TRDT ; Loop thru transaction date x-ref.
 S ^TMP("ACHSVUR2T",$J,"TOTPD")=0,^TMP("ACHSVUR2T",$J,"TOTPDG")=0
 S ^TMP("ACHSVUR2T",$J,"TOTPO")=0,^TMP("ACHSVUR2T",$J,"TOTPOG")=0
 S ACHSTRDT=ACHSBDT-1,ACHSPD=0,ACHSPD("$")=0,ACHSPDG=0,ACHSPDG("$")=0
 F  S ACHSTRDT=$O(^ACHSF(DUZ(2),"TB",ACHSTRDT)) Q:(ACHSTRDT'?1N.N)!(+ACHSTRDT>ACHSEDT)  D
 .; Loop thru transaction type.
 .S ACHSTYPE="" F  S ACHSTYPE=$O(^ACHSF(DUZ(2),"TB",ACHSTRDT,ACHSTYPE)) Q:ACHSTYPE=""  D:ACHSTYPE="I"
 ..S (ACHSDOCN,ACHSFY,ACHSSTS,DFN,ACHSSCPT,ACHSSC,ACHSCLRK,ACHSDOC,ACHSBLNK,ACHSDOC0,ACHSDOC3,ACHSFDT,ACHSTDT)=""
 ..K INS,INSF
 ..S DA=0 F  S DA=$O(^ACHSF(DUZ(2),"TB",ACHSTRDT,ACHSTYPE,DA)) Q:DA'?1N.N  D
 ...S ACHSDOC0=^ACHSF(DUZ(2),"D",DA,0)
 ...S ACHSDOCN=$P(ACHSDOC0,U),ACHSFY=$E($P(ACHSDOC0,U,27),3,4),ACHSSTS=$P(ACHSDOC0,U,12),DFN=$P(ACHSDOC0,U,22),ACHSBLNK=+$P(ACHSDOC0,U,3)
 ...I ACHSALL=2 S ACHSVIEN=$P(ACHSDOC0,U,8),ACHSVNAM=$P(^AUTTVNDR(ACHSVIEN,0),U)
 ...I ACHSALL=1 Q:ACHSVIEN'=$P(ACHSDOC0,U,8)   ;SINGLE VENDOR
 ...Q:ACHSSTS=4                                ;CANCELLED
 ...Q:((ACHSRPT=2)&(ACHSSTS>2))                ;OPEN ONLY REPORT
 ...D PAY,PO
 K ACHSBLNK,ACHSDOCN,ACHSFY,ACHSSTS,ACHSTYPE
 D HDRV,PRINT
 Q
 ;
PAY ;Adjusted amount or payment amount, or, if that doesn't exist, the obligated amount
 S ACHSPDT=0
 I '$D(^TMP("ACHSVUR2T",$J,ACHSVIEN)) D
 .S ^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO")=0,^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")=0
 .S ^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPOG")=0,^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")=0
 I $D(^ACHSF(DUZ(2),"D",DA,"ZA")) S ACHS("$")=$G(^ACHSF(DUZ(2),"D",DA,"ZA")) S ACHSPDT=1 D PAYT Q
 I $D(^ACHSF(DUZ(2),"D",DA,"PA")) S ACHS("$")=$G(^ACHSF(DUZ(2),"D",DA,"PA")) S ACHSPDT=1 D PAYT Q
 S ACHS("$")=$P($G(^ACHSF(DUZ(2),"D",DA,0)),U,9) D PAYT
 Q
PAYT ;Vendor and Report Totals
 S ACHSPD=ACHSPD+1,ACHSPD("$")=ACHSPD("$")+ACHS("$")  ;GRAND TOTALS
 S:ACHSPDT ACHSPDG=ACHSPDG+1,ACHSPDG("$")=ACHSPDG("$")+ACHS("$")   ;GRAND TOTALS TOT IF PAID
 S ^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO")=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO")+1
 S ^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")+ACHS("$")
 S:ACHSPDT ^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPOG")=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPOG")+1
 S:ACHSPDT ^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")+ACHS("$")
 S ACHS("$")=$FN(+ACHS("$"),",",2) S:ACHSPDT ACHS("$")=ACHS("$")_"*"
 Q
 ;
PO ;Set TMP with PO data
 I DFN'="" S ACHSSSN=$P($G(^DPT(DFN,0)),"^",9),ACHSHRN=$S($D(^AUPNPAT(DFN,41,DUZ(2),0)):$P(^AUPNPAT(DFN,41,DUZ(2),0),"^",2),1:"")
 S ACHSSCPT=$P(ACHSDOC0,U,7)
 S ACHSSC=$P($G(^ACHS(3,DUZ(2),1,+ACHSSCPT,0)),"^",1)
 S ACHSCLRK=$P(ACHSDOC0,U,18),ACHSCLRK=$P($G(^VA(200,ACHSCLRK,0)),"^",2)
 S ACHSDOC=ACHSFY_"-"_ACHSFC_"-"_ACHSDOCN
 S ^TMP("ACHSVUR2",$J,ACHSVNAM,ACHSVIEN,ACHSDOC,DA)=$S(ACHSBLNK=0:$P(^DPT(DFN,0),U),ACHSBLNK=1:"* BLANKET",ACHSBLNK=2:"* SPECIAL TRANS",1:"")_U_ACHS("$")_U_$G(ACHSSSN)_U_$G(ACHSHRN)_U_$G(ACHSSC)_U_$G(ACHSCLRK)_U_DFN
 Q
 ;
PRINT ;Print
 D PRINT1 Q:ACHSQ
 D ENDPRNT
 Q
PRINT1 ;Format PO data
 S ACHSHDR="",ACHSQ=0
 S (ACHSDOC,ACHSDOB,ACHSVNAM,ACHSVIEN)="",(ACHSDOCT,ACHSREC)=0
 F  S ACHSVNAM=$O(^TMP("ACHSVUR2",$J,ACHSVNAM)) Q:ACHSVNAM=""  D  Q:(ACHSQ)&(ACHSFIL="N")
 .W:(ACHSALL=2)&('ACHSQ) "Vendor: ",ACHSVNAM
 .S ACHSVIEN="" F  S ACHSVIEN=$O(^TMP("ACHSVUR2",$J,ACHSVNAM,ACHSVIEN)) Q:ACHSVIEN=""  D  Q:(ACHSQ)&(ACHSFIL="N")
 ..F  S ACHSDOC=$O(^TMP("ACHSVUR2",$J,ACHSVNAM,ACHSVIEN,ACHSDOC)) Q:ACHSDOC=""  D  Q:(ACHSQ)&(ACHSFIL="N")
 ...S DA="",DA=$O(^TMP("ACHSVUR2",$J,ACHSVNAM,ACHSVIEN,ACHSDOC,DA)),ACHSNAME=$P(^(DA),U),ACHS("$")=$P(^(DA),U,2),ACHSSSN=$P(^(DA),U,3),ACHSHRN=$P(^(DA),U,4),ACHSSC=$P(^(DA),U,5),ACHSCLRK=$P(^(DA),U,6),DFN=$P(^(DA),U,7)
 ...Q:'$D(^ACHSF(DUZ(2),"D",DA,0))
 ...S ACHSTOS=$P(^ACHSF(DUZ(2),"D",DA,0),U,4),ACHSODT=$P(^(0),U,2)
 ...I +ACHSTOS>0 S ACHSTOS=$P($P($P($P(^DD(9002080.01,3,0),U,3),";",ACHSTOS),":",2)," ")
 ...S (Y,ACHSDOS,ACHDDOS)=""
 ...S ACHSDOC3=^ACHSF(DUZ(2),"D",DA,3)
 ...S (ACHSFDT,ACHDDOS)=$P(ACHSDOC3,U),ACHSTDT=$P(ACHSDOC3,U,2)            ;AUTH TO AND FROM DT AND ONLY ACTIVE INS
 ...S Y=ACHSODT,ACHSODT=$E(Y,4,5)_"/"_$E(Y,6,7)_"/"_(1700+$E(Y,1,3))
 ...S Y=ACHSFDT S:Y'="" ACHSFDT=$E(Y,4,5)_"/"_$E(Y,6,7)_"/"_(1700+$E(Y,1,3))
 ...S Y=ACHSTDT S:Y'="" ACHSTDT=$E(Y,4,5)_"/"_$E(Y,6,7)_"/"_(1700+$E(Y,1,3))
 ...S:ACHSTDT ACHSFDT=ACHSFDT_"-"_ACHSTDT
 ...S:+DFN Y=$P($G(^DPT(DFN,0)),U,3),ACHSDOB=$E(Y,4,5)_"/"_$E(Y,6,7)_"/"_(1700+$E(Y,1,3))  ;TEST FOR BLANKET
 ...S ACHSBLNK=$P(^ACHSF(DUZ(2),"D",DA,0),U,3)
 ...D PRINT2
 ..Q:ACHSQ  D PRINT4
 ..I 'ACHSQ1,$Y>ACHSBM D RTRN
 Q
 ;
PRINT2 ;DOC AND INSURANCE
 I 'ACHSQ1 D
 .W !,ACHSDOC,?13,ACHSODT,?26,$E(ACHSNAME,1,25),?52,ACHSHRN,?59,ACHSDOB,?70,$E(ACHSSSN,6,9),?76,ACHSTOS
 .W !?1,ACHSSC,?7,ACHSFDT,?30,ACHSCLRK
 .W ?50-$L(ACHS("$")),ACHS("$")
 I 'ACHSQ1,$Y>ACHSBM D RTRN Q:ACHSQ  I ACHSALL=2 W "Vendor:",ACHSVNAM
 ;
 ;INS  ;ACHS*3.1*28 NEW SECTION TO PRNT INS
 K INSF S INSF=0
 G:DFN'?1N.N PRINT3  ;CHECK FOR BLANKET
 D GET^ACHSRPIN G:'$D(INS) PRINT3
 I 'ACHSQ1 W !,?17,"ALTERNATE RESOURCE",?41,"POLICY NUMBER",?56,"ELIG ST",?67,"ELIG END",!
 I INS>0 D  G PRINT3:ACHSQ1 Q:$D(DUOUT)!$D(DTOUT)
 .F I=1:1:INS D  Q:$D(DUOUT)!$D(DTOUT)
 ..Q:(($P(INS(I),U)="MEDICARE")&($P(INS(I),U,4)="D"))
 ..I 'ACHSQ1 W ?17,$P(INS(I),U) W:($P(INS(I),U)="MEDICAID")!($P(INS(I),U)="MEDICARE") "  "_$P(INS(I),U,4) W ?41,$P(INS(I),U,2)
 ..I $P(INS(I),U,5)>0 S $P(INS(I),U,5)=$E($P(INS(I),U,5),1,2)_"/"_$E($P(INS(I),U,5),3,4)_"/"_$E($P(INS(I),U,5),5,6) W:'ACHSQ1 ?56,$P(INS(I),U,5)
 ..I $P(INS(I),U,6)>0 S $P(INS(I),U,6)=$E($P(INS(I),U,6),1,2)_"/"_$E($P(INS(I),U,6),3,4)_"/"_$E($P(INS(I),U,6),5,6) W:'ACHSQ1 ?67,$P(INS(I),U,6)
 ..W:('ACHSQ1)&(I'=INS) !
 ..S:($P(INS(I),U)="MEDICAID")!($P(INS(I),U)="MEDICARE") $P(INS(I),U)=$P(INS(I),U)_"  "_$P(INS(I),U,4)
 ..S INSF=INSF+1,INSF(INSF)=$P(INS(I),U)_"^"_$P(INS(I),U,2)_"^"_$P(INS(I),U,5)_"^"_$P(INS(I),U,6)
 ..I 'ACHSQ1,$Y>ACHSBM D RTRN Q:ACHSQ  I ACHSALL=2 W "Vendor:",ACHSVNAM W:I'=INS !
 E  W:'ACHSQ1 ?17,"No Alternate Resource"
PRINT3 ;
 D:ACHSFIL="Y" FILREC
 I 'ACHSQ1,$Y>ACHSBM D RTRN Q:ACHSQ  I ACHSALL=2 W "Vendor:",ACHSVNAM
 Q
 ;
PRINT4 ;PRINT TOTALS FOR EACH VENDOR
 W !?10,$$REPEAT^XLFSTR("-",55),!?10,"TOTALS"
 S X=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")
 S ACHSDOCT=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPOG")
 D PDF W ?25,"PAID: ",$J(ACHSDOCT,5)_"  DOLLARS: "_X
 S X=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")-^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")
 D PDF W !?18,"OUTSTANDING: "_$J(^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO")-ACHSDOCT,5)_"  DOLLARS: "_X
 I 'ACHSQ1,$Y>ACHSBM D RTRN Q:ACHSQ  I ACHSALL=2 W "Vendor:",ACHSVNAM
 S X=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")
 D PDF W !?20,"DOCUMENTS: "_$J(^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO"),5)_"  DOLLARS: "_X
 W !,$$REPEAT^XLFSTR("-",79),!
 Q
 ;
HDRV ;HEADER VARS
 S ACHST1=$$C^XBFUNC("VENDOR USAGE REPORT - "_$S(ACHSRPT=2:"OPEN DOCUMENTS ONLY",1:"OPEN AND PAID DOCUMENTS"))
 S ACHST2=$$C^XBFUNC("For the period "_$$FMTE^XLFDT(ACHSBDT)_" through "_$$FMTE^XLFDT(ACHSEDT)),X3=0
 S ACHSV=$S(ACHSALL=1:ACHSVNAM,1:"All Vendors"),ACHSV=$$C^XBFUNC("Vendor: "_ACHSV)
 D BRPT^ACHSFU
 X:$D(IO("S")) ACHSPPO
HDR ; Paginate.
 S ACHSPG=ACHSPG+1
 I 'ACHSQ1 D
 .W @IOF,!!?19,"***  CONTRACT HEALTH MANAGEMENT SYSTEM  ***",!,ACHSUSR,?71,"Page",$J(ACHSPG,3)
 .W !,ACHSLOC,!,ACHST1,!,ACHSV,!,ACHSTIME,!,ACHST2
 .W !!,"DOCUMENT #",?12,"PO ISSUE DATE",?26,"PATIENT NAME",?52,"HRN",?62,"DOB",?66,"LAST-4SSN",?76,"TYPE"
 .W !?2,"OC",?7,"AUTHORIZATION FROM-TO",?30,"STAFF",?40,"DOLLARS(*=PAID)"  ; ?17,"ALTERNATE RESOURCE",?41,"POLICY NUMBER",?56,"ELIG START",?67,"ELIG END"
 .W !,$$REPEAT^XLFSTR("=",79),!
 Q
ENDPRNT ;TOTALS FOR ALL DOCUMENTS
 ;REPORT
 I 'ACHSQ1 D
 .W !,$$REPEAT^XLFSTR("=",79),!
 .D PD W "TOTAL PAID",?21,"DOCUMENTS:",$J(ACHSPDG,5),?42,"DOLLARS:",X,!
 .D PD1 W "TOTAL OUTSTANDING",?21,"DOCUMENTS:",$J(ACHSPD-ACHSPDG,5),?42,"DOLLARS:",X,!,$$REPEAT^XLFSTR("-",79),!
 .D PD2 W "GRAND TOTALS",?21,"DOCUMENTS:",$J(ACHSPD,5),?42,"DOLLARS:",X
 .D RTRN^ACHS:'$D(IO("S"))
 .W @IOF
PD ;TOTAL PAID
 S X2="2$",X3=16,X=ACHSPDG("$") D COMMA^%DTC
 Q
PD1 ;TOTAL OUTSTANDING(OPEN)
 S X=ACHSPD("$")-ACHSPDG("$") D COMMA^%DTC
 Q
PD2 ;TOTAL PAID AND OPEN
 S X=ACHSPD("$") D COMMA^%DTC
 Q
 ;
FILREC ;
 S ACHSREC=ACHSREC+1
 S ^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)=ACHSDOC_"^"_ACHSODT_"^"_ACHSNAME_"^"_ACHSHRN_"^"_ACHSDOB_"^"_$E(ACHSSSN,6,9)_"^"_ACHSTOS_"^"_ACHSSC_"^"_ACHSFDT_"^"_ACHSCLRK_"^"_ACHS("$")
 I INSF<1 S ^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)=^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)_"^No Alternate Resource" Q
 S L=0 F  S L=$O(INSF(L)) Q:L'?1N.N  D
 .I L=1 S ^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)=^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)_"^"_INSF(L) Q
 .S ACHSREC=ACHSREC+1,^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)="^^^^^^^^^^^"_INSF(L)
 Q
FIL ;
 S ACHSVIEN=0
 F  S ACHSVIEN=$O(^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN)) Q:ACHSVIEN'?1N.N  D
 .S ACHSVNAM=$P(^AUTTVNDR(ACHSVIEN,0),U)
 .S ACHSREC=0 F  S ACHSREC=$O(^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)) Q:ACHSREC'?1N.N  D
 ..S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,ACHSREC+7)=^TMP("ACHSVUR2T",$J,"REC",ACHSVIEN,ACHSREC)
 .D FILHDR
 .D FILTOT
 .D FILSAV
 .K ^TMP($J,"ACHSVUR2","REC")
 W ! I $$DIR^XBDIR("E","Press RETURN to continue...")
 Q
FILHDR ;HEADER FOR FILE
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,1)=$$C^XBFUNC("***  CONTRACT HEALTH MANAGEMENT SYSTEM  ***")
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,2)=ACHSLOC
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,3)=ACHST1
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,4)=$$C^XBFUNC("Vendor:  "_ACHSVNAM)
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,5)=ACHSTIME
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,6)=ACHST2
 S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,7)="DOCUMENT #^PO ISSUE DATE^PATIENT NAME^HRN^DOB^LAST-4SSN^TYPE^OC^AUTHORIZATION FROM-TO^STAFF^DOLLARS *=PAID^ALTERNATE RESOURCE^POLICY NUMBER^ELIG START^ELIG END"
 Q
FILTOT ;TOTALS PER VENDOR
 S X=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")
 S ACHSDOCT=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPOG")
 D PDF S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,"TOT1")="TOTAL PAID DOCUMENTS: ^"_$J(ACHSDOCT,5)_"^DOLLARS: ^"_X
 S X=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")-^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPDG")
 D PDF S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,"TOT2")="TOTAL OUTSTANDING DOCUMENTS: ^"_$J(^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO")-ACHSDOCT,5)_"^DOLLARS: ^"_X
 S X=^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPD")
 D PDF S ^TMP($J,"ACHSVUR2","REC",ACHSVIEN,"TOT3")="GRAND TOTALS DOCUMENTS: ^"_$J(^TMP("ACHSVUR2T",$J,ACHSVIEN,"TOTPO"),5)_"^DOLLARS: ^"_X
 Q
PDF ;TOTAL PAID FOR FILE FORMAT
 S X2="2$",X3=16 D COMMA^%DTC
 Q
FILSAV ;SAVE FILE
 N XBFN,XBE,XBJ,XBUF,XBQ,XBMED,XBFLT,XBS1,XBIO,XBF,XBGL
 S X=$E(DT,4,7)_$E(DT,2,3)
 D NOW^%DTC S X=(%I(3)+1700)_$E(%,4,7)_"_"_$P(%,".",2)
 S:$L(X)'=15 X=X_0
 S X1=$$ASF^ACHS(DUZ(2)),XBE=$J
 S XBFN="CHS-"_$TR(ACHSVNAM," /\","")_"-"_X1_"."_X  ;ACHS*3.1*28 ADDED /\
 S XBMED="F",XBFLT=1
 S XBQ="N"
 S XBUF=$$PARM^ACHS(1,5)
 I XBUF="" S XBUF=$P(^AUTTSITE(1,1),U,2)
 S XBS1=""
 S XBIO=51,XBF=$J,XBGL="TMP("_$J_",""ACHSVUR2""," D ^XBGSAVE
 Q
