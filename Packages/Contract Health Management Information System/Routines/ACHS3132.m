P32MP ;UPDATE MEDICAL PRIORITY FILE
 Q:$$INSTALLD^ACHS31E1("ACHS*3.1*32")
 N MPIEN,MPRCD,MPRDES,IEN,ACHSFAC
 K ^XTMP("ACHSP32",$J,"MEDPR")
 D BMES^XPDUTL("Updating Medical Priority file . . .")
 S MPIEN=0,IEN=0
 F  S MPIEN=$O(^ACHSMPRI(MPIEN)) Q:MPIEN'?1N.N  D
 .S MPRCD=$E($P(^ACHSMPRI(MPIEN,0),U),1,5),MPRCD=$P(MPRCD," ")
 .S MPRDES=$P(^ACHSMPRI(MPIEN,0),U,2)
 .;ADD PRIORITIES AND INACTIVATE
 .S ^ACHSMPR(MPIEN,0)=MPRCD_U_DT_U_MPRDES,^ACHSMPR("B",MPRCD,MPIEN)="",$P(^ACHSMPR(0),U,3,4)=MPIEN_U_MPIEN
 .S IEN=IEN+1
 ;ADD NEW PRIORITIES
 F I=1:1 S MPRCD=$P($T(P32MPL+I),";",2) Q:MPRCD="END"  D
 .S IEN=IEN+1,MPRDES=$P($T(P32MPL+I),";",3)
 .S ^ACHSMPR(IEN,0)=MPRCD_U_U_MPRDES,^ACHSMPR("B",MPRCD,IEN)="",$P(^ACHSMPR(0),U,3,4)=IEN_U_IEN
 ;UPDATE CHS FILE
UPCHS ;
 D BMES^XPDUTL("Updating Medical Priority in CHS Facility file . . .")
 S IEN=0,ACHSFAC=0
 F  S ACHSFAC=$O(^ACHSF("B",ACHSFAC)) Q:ACHSFAC'?1N.N  D
 .S IEN=0 F  S IEN=$O(^ACHSF(ACHSFAC,"D",IEN)) Q:IEN'?1N.N  D
 ..S MPRCD=$P($G(^ACHSF(ACHSFAC,"D",IEN,3)),U,6)
 ..Q:MPRCD=""
 ..Q:'$D(^ACHSMPR("B",MPRCD))
 ..S MPIEN=0,MPIEN=$O(^ACHSMPR("B",MPRCD,MPIEN))
 ..S $P(^ACHSF(ACHSFAC,"D",IEN,3),U,6)=MPIEN
 Q
P32TX ;UPDATE TX STATUS FILE
 Q:$$INSTALLD^ACHS31E1("ACHS*3.1*32")
 N ACHSTX,ACHSFAC,IEN
 S ACHSFAC=0
 F  S ACHSFAC=$O(^ACHSTXST("B",ACHSFAC)) Q:ACHSFAC'?1N.N  D
 .Q:'$D(^ACHSTXST(ACHSFAC,1))
 .S IEN=0 F  S IEN=$O(^ACHSTXST(ACHSFAC,1,IEN)) Q:IEN'?1N.N  D
 ..I $P(^ACHSTXST(ACHSFAC,1,IEN,0),U,21)="S" S $P(^ACHSTXST(ACHSFAC,1,IEN,0),U,21)="T" Q
 ..Q:$P(^ACHSTXST(ACHSFAC,1,IEN,0),U,21)=""
 ..I $P(^ACHSTXST(ACHSFAC,1,IEN,0),U,21)'="N"  D
 ...S ACHSTX=$P(^ACHSTXST(ACHSFAC,1,IEN,0),U,20)
 ...K ^ACHSTXST("D",ACHSTX,ACHSFAC,IEN)
 ...S $P(^ACHSTXST(ACHSFAC,1,IEN,0),U,20)="R",^ACHSTXST("D","R",ACHSFAC,IEN)=""
