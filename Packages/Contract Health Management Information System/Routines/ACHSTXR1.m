ACHSTXR1 ; IHS/OIT/FCJ - EXPORT DOCUMENTS REPORT ;     [ 10/31/2003  12:12 PM ]
 ;;3.1;CONTRACT HEALTH MGMT SYSTEM;**29,30,32**;JUN 11, 2001;Build 39
 ;ACHS*3.1*29 7/1/2021 IHS.OIT.FCJ NEW ROUTINE TO PRINT DOCUMENTS IN EXPORT FILE
 ;ACHS*3.1*32 11.14.24 IHS.OIT.FCJ ADDED TEST FOR ALL TYPES OF EXPORTS;DISPLAY PO COUNT INSTEAD OF RECORD CT
 ;
ST ;DISPLAY EXPORTS
 W @IOF,!,"CHS EXPORT SUMMARY",!
 I '$D(^ACHSTXST(DUZ(2),1,0)) W !!,*7,"NO DATA ON FILE FOR THIS FACILITY, JOB CANCELLED" G EXIT
BDT ; Enter beginning date.
 S ACHSBDT=$$DIR^XBDIR("DO^::E","Select beginning export")
 G EXIT:$D(DUOUT)!$D(DTOUT)!(ACHSBDT<1)
EDT ; Enter the ending date.
 W ! S ACHSEDT=$$DIR^XBDIR("DO^::E","Select ending export")
 G EXIT:$D(DUOUT)!$D(DTOUT)!(ACHSEDT<1)
A1 ;DISPLAY EXPORT FILES
 S ACHSXIEN=0
 S ACHSBDT=(ACHSBDT-1)_".9999",ACHSEDT=ACHSEDT_".9999"
 F  S ACHSBDT=$O(^ACHSTXST("F",DUZ(2),ACHSBDT)) Q:(ACHSBDT="")!(ACHSBDT>ACHSEDT)  D  Q:$D(DUOUT)
 .S ACHSXIEN="" F  S ACHSXIEN=$O(^ACHSTXST("F",DUZ(2),ACHSBDT,ACHSXIEN)) Q:ACHSXIEN'?1N.N  D  Q:$D(DUOUT)
 ..S ^TMP("ACHSTXR11",$J,9999999-ACHSBDT)=ACHSXIEN_U_^ACHSTXST(DUZ(2),1,ACHSXIEN,0)
 I '$D(^TMP("ACHSTXR11",$J)) W !!,"There are not any files in this date range.",! S X=$$DIR^XBDIR("E","Press RETURN to continue...") G EXIT
 D HDR
 S ACHSTXDT=0,ACHSLN=0
 F  S ACHSTXDT=$O(^TMP("ACHSTXR11",$J,ACHSTXDT)) Q:(ACHSTXDT="")!($D(DUOUT))  D     ;ACHS*3.1*32 ADDED DUOUT TEST
 .S ACHSLN=ACHSLN+1
 .S X=^TMP("ACHSTXR11",$J,ACHSTXDT),ACHSXDT=$$FMTE^XLFDT($P(X,U,2)),ACHSXIEN=$P(X,U),ACHS(ACHSLN)=ACHSXIEN
 .;S ACHSXTYP=$S($P(X,U,22)="B":"Re-export UFMS/FI",$P(X,U,21)="F":"Re-export FI",$P(X,U,21)="U":"Re-export UFMS",$P(X,U,21)="S":"Re-export Stat",1:"Export-Standard")
 .S X4=$P(X,U,22)    ;ACHS*3.1*32
 .I X4="N" S ACHSXTYP=$S($P(X,U,21)="U":"UFMS/FI",$P(X,U,21)="S":"FI",$P(X,U,21)="T":"STATISTICAL",1:"")  ;ACHS*3.1*32
 .E  S ACHSXTYP=$S(X4="B":"Re-export UFMS/FI",X4="F":"Re-export FI",X4="U":"Re-export UFMS",X4="T":"Re-export Stat",1:"")  ;ACHS*3.1*32
 .;
 .S ACHSTXFD="" F F1=1:1:3 S ACHSTXF(F1)="",ACHSTXFD(F1)="",ACHSRCT(F1)=0    ;MULTIPLE FILES
 .I $D(^ACHSTXST(DUZ(2),1,ACHSXIEN,1)) D
 ..;RECORD COUNT FOR FILE ;ACHS*3.1*32 START OF PO COUNT CHANGE
 ..I $P(^ACHSTXST(DUZ(2),1,ACHSXIEN,1),U)'="" D  Q
 ...S ACHSTXF(1)=$P(^ACHSTXST(DUZ(2),1,ACHSXIEN,1),U)
 ...I $D(^ACHSTXST(DUZ(2),1,ACHSXIEN,2)) D
 ....S ACHSTXD=0 F  S ACHSTXD=$O(^ACHSTXST(DUZ(2),1,ACHSXIEN,2,ACHSTXD)) Q:ACHSTXD'?1N.N  D
 .....S ACHSDIEN=$P(^ACHSTXST(DUZ(2),1,ACHSXIEN,2,ACHSTXD,0),U) Q:'$D(^ACHSF(DUZ(2),"D",ACHSDIEN))
 .....S ACHSRCT(1)=ACHSRCT(1)+1
 ...E  S ACHSRCT(1)=$P(X,U,6)
 ..;F F1=1:1:3 S ACHSTXF(F1)=$P(^ACHSTXST(DUZ(2),1,ACHSXIEN,1),U,F1) S ACHSRCT(F1)=$S(F1=1:$P(X,U,6),F1=2:($P(X,U,17)/3),F1=3:$P(X,U,20),1:"")
 ..F F1=2,3 S ACHSTXF(F1)=$P(^ACHSTXST(DUZ(2),1,ACHSXIEN,1),U,F1) S ACHSRCT(F1)=$S(F1=2:($P(X,U,17)/3),F1=3:$P(X,U,20),1:"")
 ..;ACHS*3.1*32 END OF PO COUNT CHANGE
 .;
 .S ACHS(ACHSLN)=ACHSXIEN_U_ACHSXDT_U_ACHSXTYP_U_ACHSTXF(1)_U_ACHSTXF(2)_U_ACHSTXF(3)
 .F F1=1:1:3 I ACHSTXF(F1)'="" S ACHSTXFD=ACHSTXF(F1),ACHSTXR=ACHSRCT(F1),F2=F1+1 Q
 .W $J(ACHSLN,4),?5,ACHSXDT,?24,$E(ACHSTXFD,1,31),?55,$J(ACHSTXR,5),?63,ACHSXTYP,!
 .F F1=F2:1:3 W:ACHSTXF(F1)'="" ?24,$E(ACHSTXF(F1),1,31),?55,$J(ACHSRCT(F1),5),!
 .I '(ACHSLN#10) W:$$DIR^XBDIR("E","'^' TO STOP ") "" Q:$D(DUOUT)  D HDR ;G:$D(DUOUT) SEL D HDR
 D SEL
 G EXIT
 Q
 ;
SEL ;SELECT EXPORT
 S Y=$$DIR^XBDIR("N^1:"_ACHSLN,"Choose Item #","","","","",2)
 G:$D(DUOUT)!$D(DTOUT) EXIT
 S ACHSLN=+Y
 D DEV
 Q
EXIT ;
 K ^TMP("ACHSTXR1",$J),DFN,L1,Y,F,F1,F2,F3,C
 K ^TMP("ACHSTXR11",$J)
 D EN^XBVK("ACHS")
 D ^ACHSVAR
 Q
 ;
PROC ;FORMAT EXPORT DATA
 S ACHSXIEN=$P(ACHS(ACHSLN),U)
 F L1=4:1:6 D:$P(ACHS(ACHSLN),U,L1)'=""
 .S ACHSDIEN=0,ACHSTXD=0
 .I $D(^ACHSTXST(DUZ(2),1,ACHSXIEN,2)) F  S ACHSTXD=$O(^ACHSTXST(DUZ(2),1,ACHSXIEN,2,ACHSTXD)) Q:ACHSTXD'?1N.N  D
 ..S (ACHSDOC,ACHSORDN,ACHSDEST,ACHSTY,ACHSPAT,ACHSVND,ACHSOBL,ACHSDIEN,ACHSTR,ACHSTRT,ACHSOBL)=""
 ..S ACHSDIEN=$P(^ACHSTXST(DUZ(2),1,ACHSXIEN,2,ACHSTXD,0),U),ACHSTR=$P(^(0),U,2)
 ..Q:'$D(^ACHSF(DUZ(2),"D",ACHSDIEN))
 ..S ACHSDOC=^ACHSF(DUZ(2),"D",ACHSDIEN,0)
 ..S ACHSTRT=$P(^ACHSF(DUZ(2),"D",ACHSDIEN,"T",ACHSTR,0),U,2)
 ..;ACHS*3.1*30 FX FY IN NXT LINE
 ..S ACHSORDN=$E($P(ACHSDOC,U,27),4)_"-"_ACHSFC_"-"_$P(ACHSDOC,U),DFN=$P(ACHSDOC,U,22),ACHSVND=$P(^AUTTVNDR($P(ACHSDOC,U,8),0),U)
 ..S ACHSDEST=$P(^ACHSF(DUZ(2),"D",ACHSDIEN,0),U,17) S:$D(ACHSDEST) ACHSDEST=$S(ACHSDEST="I":"IHS",1:"FI")
 ..S ACHSTY=$P(ACHSDOC,U,4),ACHSTY=$S(ACHSTY=1:64,ACHSTY=2:57,ACHSTY=3:64,1:"??")
 ..S ACHSOBL=$P(ACHSDOC,U,9)
 ..I $P(ACHSDOC,U,3)<1 S ACHSPAT=$P(^DPT(DFN,0),U)
 ..E  S ACHSPAT=$S($P(ACHSDOC,U,3)=1:"* BLANKET",$P(ACHSDOC,U,3)=2:"* SPECIAL TRANS")
 ..D DATA
 Q
 ;
DATA ;SET PRINT DATA
 ;EXPORT TYPES:L1=4 BOTH IHS AND FI RECORDS;L1=5 ONLY FI RECORDS;L1=6 BOTH IHS AND FI RECORDS
 Q:(L1=5)&(ACHSDEST="IHS")
 S ^TMP("ACHSTXR1",$J,L1,ACHSDIEN,ACHSTR)=ACHSORDN_U_ACHSDEST_U_ACHSTY_"-"_ACHSTRT_U_ACHSPAT_U_ACHSVND_U_ACHSOBL
 Q
 ;
PRINT ;PRINT DATA
 S ACHSPG=0
 S (ACHSXTY,ACHSPO,ACHSDIEN,ACHSTR,ACHSTOT)=0
 F  S ACHSXTY=$O(^TMP("ACHSTXR1",$J,ACHSXTY)) Q:ACHSXTY'?1N.N  D  G:$G(ACHSQUIT) EXIT
 .D RPTHDR
 .S ACHSDIEN=0
 .F  S ACHSDIEN=$O(^TMP("ACHSTXR1",$J,ACHSXTY,ACHSDIEN)) Q:ACHSDIEN'?1N.N  D  Q:$G(ACHSQUIT)
 ..S ACHSTR=0
 ..F  S ACHSTR=$O(^TMP("ACHSTXR1",$J,ACHSXTY,ACHSDIEN,ACHSTR)) Q:ACHSTR'?1N.N  D  Q:$G(ACHSQUIT)
 ...S ACHSPRT=^TMP("ACHSTXR1",$J,ACHSXTY,ACHSDIEN,ACHSTR)
 ...W !,$P(ACHSPRT,U),?12,$P(ACHSPRT,U,2),?17,$P(ACHSPRT,U,3),?22,$E($P(ACHSPRT,U,4),1,24),?47,$E($P(ACHSPRT,U,5),1,23),?71,$J($P(ACHSPRT,U,6),8,2)
 ...S ACHSPO=ACHSPO+1,ACHSTOT=ACHSTOT+$P(ACHSPRT,U,6)
 ...I ACHSDEV'="B",$Y>(IOSL-5),IOST["C-" W ! D RTRN^ACHS Q:$G(ACHSQUIT)  D RPTHDR
 ...I ACHSDEV'="B",$Y>(IOSL-5),IOST'["C-" D RPTHDR
 .Q:$G(ACHSQUIT)
 .W !,?47,$$REPEAT^XLFSTR("=",32)
 .W !,"BATCH TOTAL:",?47,ACHSPO,"  POs",?70,$J(ACHSTOT,9,2)
 W !!,"E N D   O F   R E P O R T"
 Q
 ;
DEV ;
 K ACHSDEV
 W ! S DIR(0)="S^P:PRINT Output;B:BROWSE Output on Screen",DIR("A")="Do you wish to",DIR("B")="P" K DA D ^DIR K DIR
 S ACHSDEV=Y
 Q:$D(DIRUT)!$D(DTOUT)
 I $G(ACHSDEV)="B" D BROWSE Q
 S XBRP="PRINT^ACHSTXR1",XBRC="PROC^ACHSTXR1",XBRX="EXIT^ACHSTXR1",XBNS="ACHS"
 D ^XBDBQUE
 Q
BROWSE ;
 S XBRP="VIEWR^XBLM(""PRINT^ACHSTXR1"")"
 S XBRC="PROC^ACHSTXR1",XBRX="EXIT^ACHSTXR1",XBIOP=0,XBNS="ACHS" D ^XBDBQUE
 Q
 ;
HDR ;
 ;W !!,"ITM#",?5,"EXPORT DATE",?25,"FILE NAME",?53,"# RECORDS",?64,"EXPORT TYPE",!!  ;ACHS*3.1*32
 W !!,"ITM#",?5,"EXPORT DATE",?25,"FILE NAME",?54,"PO-COUNT",?64,"EXPORT TYPE",!!  ;ACHS*3.1*32
 Q
 ;
RPTHDR ;REPORT HEADER
 S ACHSPG=ACHSPG+1
 W:ACHSDEV'="B" @IOF,!
 W $$C^XBFUNC("CHS EXPORT SUMMARY",80)
 W:ACHSDEV'="B" ?65,"Page: ",ACHSPG
 W !!,"BATCH DATE:  ",$P(ACHS(ACHSLN),U,2)
 W !,"EXPORT:  ",$P(ACHS(ACHSLN),U,3)
 W !,"FILE NAME:  ",$P(ACHS(ACHSLN),U,ACHSXTY)
 W !,$$REPEAT^XLFSTR("=",80)
 W !,"P.O. NUMBER DEST TYPE PATIENT",?47,"VENDOR",?71,"EST COST"
 W !,"----------- ---- ---- ",$$REPEAT^XLFSTR("-",24)," ",?47,$$REPEAT^XLFSTR("-",23),?71,"--------"
 Q
 ;
