ACHSYRDS ;IHS/OIT/FCJ - RESET COUNT FOR DENIAL SIGNATURES;
 ;;3.1;CONTRACT HEALTH MGMT SYSTEM;**30**;JUNE 11,2001;Build 39
 ;ACHS*3.1*30 NEW ROUTINE
 ; Resets the denial signature count in the CHS DENIAL FACILITY
 ; 
 ;
ST ;
 ;CALCULATE NOT SIGNED
 S INDX=0,ACHSNSCT=0,ACHSSGCT=0
 F  S INDX=$O(^ACHSDEN("AP",DUZ(2),"QP",INDX)) Q:INDX'?1N.N  D
 .;IF CANCELLED OR REVERSED DO NOT COUNT
 .Q:($P(^ACHSDEN(DUZ(2),"D",INDX,0),U,8)="Y")!($P(^ACHSDEN(DUZ(2),"D",INDX,0),U,8)="R")
 .;CALCULATE SIGNED WAITING TO BE PRINTED AND NOT SIGNED
 .I $P(^ACHSDEN(DUZ(2),"D",INDX,0),U,11)'?1N.N S ACHSNSCT=ACHSNSCT+1 Q
 .E  S ACHSSGCT=ACHSSGCT+1
 D TOT
 W !!,"Counts have been updated: Unsigned - ",ACHSNSCT," Signed - ",ACHSSGCT,!
 I $$DIR^XBDIR("FO","   Press any key to exit")
 ;
EXT ;
 K INDX,ACHSSGCT,ACHSNSCT,DIRUT
 Q
 ;
TOT ;SET TOTALS
 S DIE="^ACHSDENR(",DA=DUZ(2)
 S DR=".14////"_ACHSNSCT_";.15////"_ACHSSGCT
 D ^DIE
 K DIC,DIE,DA,DR,D
 Q
 ;
