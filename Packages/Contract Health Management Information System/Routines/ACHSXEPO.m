ACHSXEPO ;IHS/OIT/FCJ - VIEW EPO FILE FROM THE FI
 ;;3.1;CONTRACT HEALTH MGMT SYSTEM;**29,30**;JUN 11, 2001;Build 39
 ;
 ;ACHS*3.1*29 7/1/2021 IHS.OIT.FCJ NEW ROUTINE TO VIEW EPO FILE
 ;This routine allow viewing the file recieved from the FI when a file is sent
 ;ACHS*3.1*30 6/0/2022 FIXED DISPLAY
 ;
FILES ;
 K DIR,X,Y
 S ACHSPTH=$S($$OS^ACHS=2:"c:\usr\spool\chsdata\epov",1:"/usr/spool/chsdata")
 ;RETURN A LIST OF FILES TO VIEW EPOV
 K ACHSLIST
 ;GET ALL FILES STARTING WITH EPO AND PUT IN ARRAY ACHSLIST
 I $$LIST^%ZISH(ACHSPTH,"epov*",.ACHSLIST)
 ;LIST OF FILES TO VIEW
 S ACHSCNT=0,ACHSNCNT=0
 F  S ACHSCNT=$O(ACHSLIST(ACHSCNT)) Q:'ACHSCNT  D  G:$G(ACHSQUIT) EXIT
 .S ACHSNCNT=ACHSNCNT+1,ACHSXDT="",L=""
 .D OPEN^%ZISH(ACHSPTH,ACHSLIST(ACHSCNT),"R") I POP S ACHSEMSG="M10" D ERROR^ACHSTCK1 S ACHSQUIT=1
 .;U IO F I=1:1 R ACHSEPOV:300 Q:$$STATUS^%ZISH  I ACHSEPOV["RUN" S L=$F(ACHSEPOV,"RUN DATE ") S ACHSXDT=$E(ACHSEPOV,L,L+7),ACHSLST(ACHSXDT,ACHSNCNT)=ACHSLIST(ACHSCNT)_U_$E(ACHSEPOV,13,20)
 .U IO F I=1:1 R ACHSEPOV:300 Q:$$STATUS^%ZISH  I ACHSEPOV["RUN" S L=$F(ACHSEPOV,"RUN DATE ") S ACHSXDT=$E(ACHSEPOV,L,L+7),ACHSLST(ACHSXDT,ACHSNCNT)=ACHSLIST(ACHSCNT)_U_ACHSXDT
 .D CLOSE^%ZISH
 .K ACHSLIST(ACHSCNT)
 U IO
 I ACHSNCNT<1 U IO(0) W *7,!!?5,"No EPO Files to view" G EXIT
SEL ;SELECT FILE
 D HD
 S ACHSXDT="",ACHSCNT=0
 F  S ACHSXDT=$O(ACHSLST(ACHSXDT)) Q:ACHSXDT=""  D  Q:$G(ACHSQUIT)
 .S ACHSI=0
 .F  S ACHSI=$O(ACHSLST(ACHSXDT,ACHSI)) Q:ACHSI'?1N.N  D  Q:$G(ACHSQUIT)
 ..S X=ACHSLST(ACHSXDT,ACHSI),ACHSCNT=ACHSCNT+1
 ..W !,$J(ACHSCNT,3),?7,$E($P(X,U),1,30),?35,$P(X,U,2)
 ..S ^TMP("ACHSEPOV",$J,ACHSCNT)=ACHSLST(ACHSXDT,ACHSI)
 ..I $Y>(IOSL-5) W ! D RTRN^ACHS Q:$G(ACHSQUIT)  D HD
 I ACHSCNT=0 W !!,"There are not any EPOV files to display.",! D RTRN^ACHS G EXIT
 S Y=$$DIR^XBDIR("N^1:"_ACHSCNT,"Choose Item #","","","","",2)
 G:$D(DUOUT)!$D(DTOUT) EXIT
 S ACHSI=+Y
 S ACHSFIL=$P(^TMP("ACHSEPOV",$J,ACHSI),U)
 D VIEW
 G EXIT
 Q
VIEW ;VIEW FILE
 D FILE^XBLM(ACHSPTH,ACHSFIL)
 Q
EXIT ;
 K ^TMP("ACHSEPOV",$J)
 D EN^XBVK("ACHS")
 S ACHSISAO=1 D ^ACHSVAR
 Q
HD ;HEADER
 W @IOF,!,"EPOV Files available:"
 W !!,"Item",?7,"File Name",?35,"Run Date"
 W !,"====",?7,"========================",?35,"========"
 Q
