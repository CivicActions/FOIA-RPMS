ACHSAD ; IHS/ADC/GTH - DISPLAY DOCUMENTS ; [ 09/17/97   9:12 AM ]
 ;;3.0;CONTRACT HEALTH MGMT SYSTEM;**2**;SEP 17, 1997
 ;;ACHS*3*2 FIX CAPTIONED PRINT PROBLEM
 ;
 S ACHSVIEW=""
 F  D ^ACHSUSC Q:$D(DUOUT)!$D(DTOUT)!'$D(ACHSDIEN)!$D(ACHSDVEW)
 KILL ACHSVIEW
 Q
 ;
DUMP ;EP - From Option.
 ; KILL DR
 KILL DR,D0,D1,D2,ACHSDIEN ; ACHS*3*2 IHS/ADC/GTH 12-30-97
 D ^ACHSUD
 G K:'$D(ACHSDIEN)
DEV ;
 S %=$$PB^ACHS
 I %="^"!$D(DUOUT)!$D(DTOUT) D K Q
 I %="B" D DIQ^XBLM("^ACHSF("_DUZ(2)_",""D"",",ACHSDIEN),HOME^%ZIS Q
 S %ZIS="OPQ"
 D ^%ZIS
 I POP S IOP=$I D ^%ZIS G K
 G:'$D(IO("Q")) START
 K IO("Q")
 I $D(IO("S"))!($E(IOST)'="P") W *7,!,"Please queue to system printers." D ^%ZISC G DEV
 S ZTRTN="START^ACHSAD",ZTDESC="DUMP OF DATA FROM DOCUMENT "_$$DOC^ACHS(0,14)_"-"_ACHSFC_"-"_$$DOC^ACHS(0,1)
 F ACHS="AC*","ACHS*" S ZTSAVE(ACHS)=""
 D ^%ZTLOAD
 G DEV:'$D(ZTSK)
 K ZTSK
 G K
 ;
START ;EP - TaskMan.
 S:$D(IO("S")) IOSL=66
 U IO
 S DIC="^ACHSF("_DUZ(2)_",""D"",",DA=ACHSDIEN
 D EN^DIQ
 I IO'=$G(ACHSIO) W @IOF
K ;
 K ACHSDIEN,D0,D1
 D ^%ZISC
 D ERPT^ACHS:$D(ZTQUEUED)
 D RTRN^ACHS
 Q
 ;
