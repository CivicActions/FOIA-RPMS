 . S SEG=$E(FDA,1,3)
 . S SIEN=$O(^BPDMRECA("B",SEG,0))
 . Q:'SIEN
 . S FIEN=$O(^BPDMRECA(SIEN,11,"B",FDA,0))
 . Q:'FIEN
 . S STII=$O(^BPDMRECA(SIEN,11,FIEN,12,"B",STI,0))
 . Q:'STII
 . I $P($G(^BPDMRECA(SIEN,11,FIEN,12,STII,0)),U,2)="N" D
 .. S $P(^BPDMRECA(SIEN,11,FIEN,12,STII,0),U,2)="RR"
