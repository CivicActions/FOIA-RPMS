 I Y<1 D IMES Q 0
 S DIC=DIC_+Y_",22,",X=$P(BPDMSTAL,"*",2)
 D ^DIC
 I Y<1 D IMES Q 0
 S DIC=DIC_+Y_",""PAH"",",X=$P(BPDMSTAL,"*",3)
 D ^DIC
 S BPDMY=Y
 D IMES
 Q $S(BPDMY<1:0,1:1)
IMES ;
 D MES^XPDUTL($$CJ^XLFSTR("Patch """_BPDMSTAL_""" is"_$S(Y<1:" *NOT*",1:"")_" installed.",IOM))
