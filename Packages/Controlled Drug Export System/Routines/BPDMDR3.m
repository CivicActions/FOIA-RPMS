 .S BPDMID=$P(^BPDMRDNF(BPDMX,11,BPDMY,0),U,1)
 .S BPDMREQ=$$REQ(BPDMSID,BPDMID,BPDMSTE)
 .Q:'BPDMREQ  ;$$REQ(BPDMSID,BPDMID,BPDMSTE)  ;not required so don't bother
 .S X=""
