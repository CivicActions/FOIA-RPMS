 I BPDMPROV="" S BPDMPROV=$P(BPDMREC,U,4)
 S BPDMORD=0 F  S BPDMORD=$O(^BPDMRDNF("AO",BPDMORD)) Q:BPDMORD'=+BPDMORD!(BPDME)  D
 .S BPDMX=$O(^BPDMRDNF("AO",BPDMORD,0))
 .Q:'$P(^BPDMRDNF(BPDMX,0),U,5)  ;DON'T DO PRE-CHECK
 .Q:$P(^BPDMRDNF(BPDMX,0),U,4)="N"  ;not used so skip it
 .S BPDMSID=$P(^BPDMRDNF(BPDMX,0),U,1)
 .;I $P(^PSDRUG(BPDMDRUG,0),U,3)[0!($P(^PSDRUG(BPDMDRUG,0),U,3)["M") S BPDMCDI=1
