 .S $P(^TMP($J,"ERROR",BPDMR),U,2)=BPDMMSG
 .S $P(^TMP($J,"ERROR",BPDMR),U,3)=BPDMRF
 .S $P(^TMP($J,"ERROR",BPDMR),U,4)=BPDMPFI
 .S $P(^TMP($J,"ERROR",BPDMR),U,5)=BPDMSTAT
 .S $P(^TMP($J,"ERROR",BPDMR),U,6)=BPDMFD
 .I $G(BPDME)=1 S BPDM("ERROR COUNT")=BPDM("ERROR COUNT")+1,$P(^TMP($J,"ERROR",BPDMR),U,4)=1,BPDMQFLG=1
 S $P(^TMP($J,"REVIEWED",BPDMR),U,1)=BPDMR
 S $P(^TMP($J,"REVIEWED",BPDMR),U,2)=BPDMMSG
 S $P(^TMP($J,"REVIEWED",BPDMR),U,3)=BPDMRF
 S $P(^TMP($J,"REVIEWED",BPDMR),U,4)=BPDMPFI
 S $P(^TMP($J,"REVIEWED",BPDMR),U,5)=BPDMSTAT
 S $P(^TMP($J,"REVIEWED",BPDMR),U,6)=BPDMFD
 I $G(BPDME)=1 S $P(^TMP($J,"REVIEWED",BPDMR),U,7)=1
