 S Y=$$OPEN^%ZISH(BPDMSDIR,BPDMFILE,"W")
 I Y I '$D(ZTQUEUED) S BPDMNOFI=1 W !!,"Failed to create export file" Q  ;20220202 Patch 1 73211
 I Y,$D(ZTQUEUED) S BPDMQFLG=1,BPDMQMSG="COULD NOT OPEN HOST FILE "_BPDMSDIR_" "_BPDMFILE Q
 U IO
 K BPDMRCD
 S DFN=0 F  S DFN=$O(^TMP($J,"EXPORT",DFN)) Q:DFN'=+DFN  D
 .S BPDMR=0 F  S BPDMR=$O(^TMP($J,"EXPORT",DFN,BPDMR)) Q:BPDMR'=+BPDMR  D
 ..S BPDMRF="" F  S BPDMRF=$O(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF)) Q:BPDMRF=""  D
 ...S BPDMPFI="" F  S BPDMPFI=$O(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,BPDMPFI)) Q:BPDMPFI=""  D
 ....;1995 segment
 ....S BPDMT=""
 ....S BPDMPART=$P(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,BPDMPFI),U,1)
 ....S BPDMSTAT=$P(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,BPDMPFI),U,2)
 ....S BPDMRDNF=^PSRX(BPDMR,0)
 ....S BPDMDRUG=$P(BPDMRDNF,U,6)
 ....S BPDMNDC=$P($G(^PSRX(BPDMR,2)),U,7)
 ....I BPDMNDC="" S BPDMNDC=$$VAL^XBDIQ1(50,BPDMDRUG,31)
 ....S BPDMNDC=$TR(BPDMNDC,"-","")
 ....S BPDMNDC=$$LZERO(BPDMNDC,11)
 ....S BPDMPROV=""
 ....I BPDMPART="01" S BPDMPROV=$P($G(^PSRX(BPDMR,"P",BPDMPFI,0)),U,17)
 ....I BPDMPROV="" S BPDMPROV=$P(BPDMRDNF,U,4)
 ....S BPDMFD=$$DATEFILL^BPDMUTL(BPDMR,$G(BPDMRF),$G(BPDMPFI),BPDMPART)
 ....I BPDMFD="",BPDMSTAT="02" S BPDMFD=BPDMOFD
 ....S BPDMORD="" F  S BPDMORD=$O(^BPDMRDNF("AO",BPDMORD)) Q:BPDMORD=""  D
 .....S BPDMY=$O(^BPDMRDNF("AO",BPDMORD,0))
 .....S BPDMC=0 F  S BPDMC=$O(^BPDMRDNF(BPDMY,11,"AP",BPDMC)) Q:BPDMC=""  D
 ......S BPDMX=0 F  S BPDMX=$O(^BPDMRDNF(BPDMY,11,"AP",BPDMC,BPDMX)) Q:BPDMX=""  D
 .......D REC
 ....W BPDMT,!
 ....Q
 ...Q
 D ^%ZISC
 Q
