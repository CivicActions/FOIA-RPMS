PROCESS ;EP
 ;FIRST SET ALL IN ^TMP by DFN and ien of prescription and ien of refill, if there are any errors at all then stop processing
 K ^TMP($J)
 S BPDM("COUNT")=0,BPDMQFLG=0
 S BPDMDTAX=$O(^ATXAX("B","BPDM DRUGS FOR PDM",0))
 W:'$D(ZTQUEUED) !,"Reviewing prescriptions...........  (1)"
 S BPDMSTAT="00"  ;THESE ARE ALL NEW
 S BPDMPART="02"
 S BPDMPFI=0  ;not partials
 S BPDMCNTR=0,BPDM("CONTROL DATE")=BPDM("RUN BEGIN")-1,BPDM("CONTROL DATE")=BPDM("CONTROL DATE")_".9999",BPDM("POSTING DATE")="      "
 F  S BPDM("CONTROL DATE")=$O(^PSRX("AL",BPDM("CONTROL DATE"))) Q:BPDM("CONTROL DATE")=""!($P(BPDM("CONTROL DATE"),".")>BPDM("RUN END"))  D PROCESS2
 ;now do partials
 S BPDMPART="01"  ;not partials
 S BPDMRF=0
 S BPDM("CONTROL DATE")=BPDM("RUN BEGIN")-1,BPDM("POSTING DATE")="      "
 F  S BPDM("CONTROL DATE")=$O(^PSRX("ADP",BPDM("CONTROL DATE"))) Q:BPDM("CONTROL DATE")=""!($P(BPDM("CONTROL DATE"),".")>BPDM("RUN END"))  D
 .S BPDMR="" F  S BPDMR=$O(^PSRX("ADP",BPDM("CONTROL DATE"),BPDMR)) Q:BPDMR=""  D
 ..S BPDMPFI="" F  S BPDMPFI=$O(^PSRX("ADP",BPDM("CONTROL DATE"),BPDMR,BPDMPFI)) Q:BPDMPFI=""  D
 ...Q:$P($G(^PSRX(BPDMR,"P",BPDMPFI,0)),U,16)]""
 ...D PROCESS3
 Q
PROCESS2 ;
 S BPDMR="" F  S BPDMR=$O(^PSRX("AL",BPDM("CONTROL DATE"),BPDMR)) Q:BPDMR=""  D
 .S BPDMRF="" F  S BPDMRF=$O(^PSRX("AL",BPDM("CONTROL DATE"),BPDMR,BPDMRF)) Q:BPDMRF=""  D PROCESS3
 Q
PROCESS3 ;EP
 I '$D(^PSRX(BPDMR,0)) Q
 K BPDME,BPDMMSG,BPDMNG S BPDM("ERROR COUNT")=0
 S BPDMMSG=""
 S BPDME=0
 S BPDM("COUNT")=BPDM("COUNT")+1
 X BPDMCNT
 ;first decide if the drug is one to be exported.
 S BPDMREC=^PSRX(BPDMR,0)
 ;I BPDMRTYP=3,'$G(BPDMREDO) S X=$P(BPDMREC,U,2) I X,'$D(BPDMPATR(X)) Q  ;don't want this patient
 S BPDMPNE=""
 S BPDMDIVI=$$VALI^XBDIQ1(52,BPDMR,20)
 ;ihs/cmi/maw 09142018 patch 4 CR10254
 I $G(BPDMRF) S BPDMDIVI=$P($G(^PSRX(BPDMR,1,BPDMRF,0)),U,9)  ;get refill division and only process if same as running site
 I BPDMDIVI="" S BPDMMSG="OUTPATIENT SITE IN PRESCRIPTION MISSING",BPDME=1 D RECORD Q
 Q:BPDMDIVI'=BPDMOSIT  ; not a prescription for this outpatient site being run for
 S BPDMFD=$$DATEFILL^BPDMUTL(BPDMR,$G(BPDMRF),$G(BPDMPFI),BPDMPART)
 ;I BPDMFD="",BPDMSTAT="02" S BPDMFD=BPDMOFD
 I BPDMFD="" S BPDMMSG="NO FILL DATE FOUND_DSP05",BPDME=1 D RECORD Q
 S BPDMDRUG=$P(BPDMREC,U,6)
 I BPDMDRUG="" S BPDMMSG="NO DRUG ON THIS PRESCRIPTION",BPDME=1 D RECORD Q
 I '$D(^PSDRUG(BPDMDRUG)) S BPDMMSG="NO DRUG ON THIS PRESCRIPTION",BPDME=1 D RECORD Q
 I $P(^PSDRUG(BPDMDRUG,0),U,3)="" S BPDMMSG="NOT A SCHEDULE 2,3,4 DRUG",BPDMNG=1 D RECORD Q
 ;I '$$COND^BPDMUTL(BPDMDRUG,BPDMDTAX) S BPDMMSG="NOT A DRUG TO BE EXPORTED",BPDMNG=1 D RECORD Q
 I '$$COND^BPDMUTL(BPDMDRUG,BPDMDTAX,BPDMSITE) S BPDMMSG="NOT A DRUG TO BE EXPORTED",BPDMNG=1 D RECORD Q
 I $P($G(^PSRX(BPDMR,"STA")),U)=1 S BPDMMSG="STATUS IS 1",BPDMNG=1 D RECORD Q
 I $P($G(^PSRX(BPDMR,"STA")),U)=3 S BPDMMSG="STATUS IS 3",BPDMNG=1 D RECORD Q
 I $P($G(^PSRX(BPDMR,"STA")),U)=4 S BPDMMSG="STATUS IS 4",BPDMNG=1 D RECORD Q
 I $P($G(^PSRX(BPDMR,"STA")),U)=5 S BPDMMSG="STATUS IS 5",BPDMNG=1 D RECORD Q
 I BPDMSTAT'="02",$P($G(^PSRX(BPDMR,"STA")),U)=15 S BPDMMSG="STATUS IS 15",BPDMNG=1 D RECORD Q
 I BPDMSTAT'="02",$P($G(^PSRX(BPDMR,"STA")),U)=13 S BPDMMSG="STATUS IS 13",BPDMNG=1 D RECORD Q
 I $P($G(^PSRX(BPDMR,"STA")),U)=16 S BPDMMSG="STATUS IS 16",BPDMNG=1 D RECORD Q
 I BPDMSTAT'="02",$$RTS^BPDMUTL(BPDMR,BPDMRF,$G(BPDMPFI)) S BPDMMSG="RETURNED TO STOCK BEFORE EXPORT",BPDMNG=1 D RECORD Q
 ;WHAT ELSE WOULD CAUSE THIS NOT TO GO??????
 ;CHECK DSP, PAT, PER for required data elements
 ;set variables needed for DSP
 S DFN=$P(BPDMREC,U,2)
 I DFN="" S BPDMMSG="NO PATIENT FOUND FOR PRESCRIPTION_PAT07",BPDME=1 D RECORD Q
 I '$D(^DPT(DFN,0)) S BPDMMSG="INVALID PATIENT FOR PRESCRIPTION",BPDME=1 D RECORD Q
 I $P($G(^DPT(DFN,0)),U)["DEMO,PATIENT" S BPDMMSG="DEMO PATIENT",BPDMNG=1 D RECORD Q
 S X=$O(^DIBT("B","RPMS DEMO PATIENT NAMES",0)) I X,$D(^DIBT(X,1,DFN)) S BPDMMSG="DEMO PATIENT",BPDMNG=1 D RECORD Q
 I $E(BPDMVER)=3 D ^BPDMDR2 Q
 I BPDMVER=1995 D ^BPDMDR3 Q
 I BPDMVER="4.2" D ^BPDMDR42 Q  ;go to version 4.2 driver
 I BPDMVER="4.2A" D ^BPDMDR4A Q  ;go to version 4.2a driver
 S BPDMX=$O(^BPDMREC("B","PAT",0))
 S BPDMY=0 F  S BPDMY=$O(^BPDMREC(BPDMX,11,BPDMY)) Q:BPDMY'=+BPDMY!(BPDME)  D
 .S BPDMID=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)
 .S BPDMREQ=$$REQ^BPDMUTL("PAT",BPDMID,BPDMSTE)
 .Q:'$$REQ^BPDMUTL("PAT",BPDMID,BPDMSTE)  ;not required so don't bother
 .S X=""
 .X ^BPDMREC(BPDMX,11,BPDMY,11)
 .I X="",BPDMREQ=1 S BPDMMSG=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,2)_"_"_$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)_" is missing and is required.",BPDME=1 D RECORD Q
 ;S BPDMSTAT=$S(BPDMRTYP=2:"00",1:"00")  ;LORI FIX THIS FOR REG EXPORT
 S BPDMDRUG=$P(^PSRX(BPDMR,0),U,6)
 S BPDMNDC=$P($G(^PSRX(BPDMR,2)),U,7)
 I BPDMNDC="" S BPDMNDC=$$VAL^XBDIQ1(50,BPDMDRUG,31)
 I BPDMNDC="" S BPDMMSG="NDC MISSING FOR THIS DRUG",BPDME=1 D RECORD Q
 S BPDMX=$O(^BPDMREC("B","DSP",0))
 S BPDMY=0 F  S BPDMY=$O(^BPDMREC(BPDMX,11,BPDMY)) Q:BPDMY'=+BPDMY!(BPDME)  D
 .S BPDMID=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)
 .S BPDMREQ=$$REQ^BPDMUTL("DSP",BPDMID,BPDMSTE)
 .Q:'$$REQ^BPDMUTL("DSP",BPDMID,BPDMSTE)  ;not required so don't bother
 .S X=""
 .X ^BPDMREC(BPDMX,11,BPDMY,11)
 .I $P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)="DSP01",$$VAL^XBDIQ1(9002315.01,BPDMSITE,.04)="MINNESOTA",$$VALI^XBDIQ1(9002315.01,BPDMSITE,.03)="4.0",BPDMSTAT="00" S X="" G NXT
 .I X="",BPDMREQ=1 S BPDMMSG=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,2)_"_"_$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)_" is missing and is required.",BPDME=1 D RECORD Q
NXT S BPDMPROV=""
 I BPDMPART="01" S BPDMPROV=$P($G(^PSRX(BPDMR,"P",BPDMPFI,0)),U,17)
 I BPDMPROV="" S BPDMPROV=$P(BPDMREC,U,4)
 ;check PRE data elements
 S BPDMX=$O(^BPDMREC("B","PRE",0))
 S BPDMY=0 F  S BPDMY=$O(^BPDMREC(BPDMX,11,BPDMY)) Q:BPDMY'=+BPDMY!(BPDME)  D
 .S BPDMID=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)
 .S BPDMREQ=$$REQ^BPDMUTL("PRE",BPDMID,BPDMSTE)
 .Q:'$$REQ^BPDMUTL("PRE",BPDMID,BPDMSTE)  ;not required so don't bother
 .S X=""
 .X ^BPDMREC(BPDMX,11,BPDMY,11)
 .I X="",BPDMREQ=1 S BPDMMSG=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,2)_"_"_$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)_" is missing and is required.",BPDME=1 D RECORD Q
 ;check CDI data elements if this is a compound drug
 I $P(^PSDRUG(BPDMDRUG,0),U,3)[0!($P(^PSDRUG(BPDMDRUG,0),U,3)["M") D
 .S BPDMX=$O(^BPDMREC("B","CDI",0))
 .S BPDMY=0 F  S BPDMY=$O(^BPDMREC(BPDMX,11,BPDMY)) Q:BPDMY'=+BPDMY!(BPDME)  D
 ..S BPDMID=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)
 ..S BPDMREQ=$$REQ^BPDMUTL("CDI",BPDMID,BPDMSTE)
 ..Q:'$$REQ^BPDMUTL("CDI",BPDMID,BPDMSTE)  ;not required so don't bother
 ..S X=""
 ..X ^BPDMREC(BPDMX,11,BPDMY,11)
 ..I X="",BPDMREQ=1 S BPDMMSG=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,2)_"_"_$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)_" is missing and is required.",BPDME=1 D RECORD Q
 S BPDMX=$O(^BPDMREC("B","AIR",0))
 S BPDMY=0 F  S BPDMY=$O(^BPDMREC(BPDMX,11,BPDMY)) Q:BPDMY'=+BPDMY!(BPDME)  D
 .S BPDMID=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)
 .S BPDMREQ=$$REQ^BPDMUTL("AIR",BPDMID,BPDMSTE)
 .Q:'$$REQ^BPDMUTL("AIR",BPDMID,BPDMSTE)  ;not required so don't bother
 .S X=""
 .X ^BPDMREC(BPDMX,11,BPDMY,11)
 .I X="",BPDMREQ=1 S BPDMMSG=$P(^BPDMREC(BPDMX,11,BPDMY,0),U,2)_"_"_$P(^BPDMREC(BPDMX,11,BPDMY,0),U,1)_" is missing and is required.",BPDME=1 D RECORD Q
N Q:BPDME
 S BPDMMSG="EXPORTED" D RECORD
 S ^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,$G(BPDMPFI))=BPDMPART_U_BPDMSTAT
 Q
