 I $E(BPDMVER)=3 D TAPE^BPDMDR2 Q
 I BPDMVER=1995 D TAPE^BPDMDR3 Q
 I BPDMVER="4.2" D TAPE^BPDMDR42 Q  ;go to version 4.2 driver maw 09172018 p4 account for ver 4.2A
 ;IHS/CMI/LAB - added for 4.2A
 I BPDMVER="4.2A" D TAPE^BPDMDR4A Q  ;goto ver4.2A
 K BPDMCNT S BPDMCNT=0,BPDM("REC COUNT")=0
 ;do header record TH
 S BPDMFILE=$$FN^BPDMUTL(BPDMSITE,BPDM("RUN LOG"),BPDMPTYP)
 S Y=$$OPEN^%ZISH(BPDMSDIR,BPDMFILE,"W")
 I Y I '$D(ZTQUEUED) W !!,"Failed to create export file" Q  ;20220202 Patch 1 73211
 I Y,$D(ZTQUEUED) S BPDMQFLG=1,BPDMQMSG="COULD NOT OPEN HOST FILE "_BPDMSDIR_" "_BPDMFILE Q
 U IO
 K BPDMRCD
 S BPDMTP1=0,BPDMTT2=0
 S BPDMTYPE="TH" D REC S BPDMTT2=BPDMTT2+1 ;header record
 S BPDMTYPE="IS" D REC S BPDMTT2=BPDMTT2+1 ;information source record
 S BPDMTYPE="PHA" D REC S BPDMTP1=BPDMTP1+1,BPDMTT2=BPDMTT2+1 ;pharmacy record
 S DFN=0 F  S DFN=$O(^TMP($J,"EXPORT",DFN)) Q:DFN'=+DFN  D
 .S BPDMTYPE="PAT" D REC S BPDMTP1=BPDMTP1+1,BPDMTT2=BPDMTT2+1  ;PATIENT DETAIL
 .S BPDMR=0 F  S BPDMR=$O(^TMP($J,"EXPORT",DFN,BPDMR)) Q:BPDMR'=+BPDMR  D
 ..S BPDMRF="" F  S BPDMRF=$O(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF)) Q:BPDMRF=""  D
 ...S BPDMPFI="" F  S BPDMPFI=$O(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,BPDMPFI)) Q:BPDMPFI=""  D
 ....;rx segment
 ....S BPDMPART=$P(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,BPDMPFI),U,1)
 ....S BPDMSTAT=$P(^TMP($J,"EXPORT",DFN,BPDMR,BPDMRF,BPDMPFI),U,2)
 ....S BPDMREC=^PSRX(BPDMR,0)
 ....S BPDMDRUG=$P(BPDMREC,U,6)
 ....S BPDMNDC=$P($G(^PSRX(BPDMR,2)),U,7)
 ....I BPDMNDC="" S BPDMNDC=$$VAL^XBDIQ1(50,BPDMDRUG,31)
 ....S BPDMNDC=$TR(BPDMNDC,"-","")
 ....S BPDMNDC=$$LZERO(BPDMNDC,11)
 ....S BPDMPROV=""
 ....I BPDMPART="01" S BPDMPROV=$P($G(^PSRX(BPDMR,"P",BPDMPFI,0)),U,17)
 ....I BPDMPROV="" S BPDMPROV=$P(BPDMREC,U,4)
 ....S BPDMFD=$$DATEFILL^BPDMUTL(BPDMR,$G(BPDMRF),$G(BPDMPFI),BPDMPART)
 ....I BPDMFD="",BPDMSTAT="02" S BPDMFD=BPDMOFD
 ....S BPDMTYPE="DSP" D REC S BPDMTP1=BPDMTP1+1,BPDMTT2=BPDMTT2+1
 ....S BPDMTYPE="PRE" D REC S BPDMTP1=BPDMTP1+1,BPDMTT2=BPDMTT2+1
 ....I $D(^BPDMSITE(BPDMSITE,12,BPDMDRUG,0)) D
 .....S BPDMTYPE="CDI" D REC S BPDMTP1=BPDMTP1+1,BPDMTT2=BPDMTT2+1
 ....S BPDMTYPE="AIR" D REC S BPDMTP1=BPDMTP1+1,BPDMTT2=BPDMTT2+1
 ....S BPDM("REC COUNT")=BPDM("REC COUNT")+1
 ....Q
 ...Q
 S BPDMTYPE="TP" D REC S BPDMTT2=BPDMTT2+1
 ;S BPDMTYPE="TT" D REC S BPDMTT2=BPDMTT2+1
