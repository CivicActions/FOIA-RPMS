REQ42B(T,I,S) ;EP  T segment ID, I is field ID, S is State ien
 NEW J,K,R
 S J=$O(^BPDMRECC("B",T,0))
 I 'J Q ""
 S K=$O(^BPDMRECC(J,11,"B",I,0))
 I 'K Q ""
 S R=$P($G(^BPDMRECC(J,11,K,12,S,0)),U,2)
 I R="N" Q 0
 I R="RWA" Q 2
 Q 1
 ;
PRE10(P,S) ;-- get the licensing state
 N IS,ISS
 S ISS=""
