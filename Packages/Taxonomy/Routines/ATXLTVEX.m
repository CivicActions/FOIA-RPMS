 .I ATXLFILE=80 S D=$$ICDDX^ICDEX(ATXLX,DT,,"I") S ATXL1=$P(D,U,2),ATXL2=$E($P(D,U,4),1,50),ATXL3=$P(D,U,20),ATXL3=$P(^ICDS(ATXL3,0),U,1),ATXC2="DESCRIPTION",ATXC1="ICD CODE",ATXC3="CODING SYSTEM"
