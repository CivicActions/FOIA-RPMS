 ..S ATXLF=".01"
 ..S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXC1=$P(^DD(ATXLFILE,.01,0),U,1)
 ..I ATXLFILE=60 S ATXC1="LAB TEST NAME"
 .I ATXCNT=1 D
 ..W $$Q(ATXC1)
 ..I ATXC2]"" W ",",$$Q(ATXC2)
