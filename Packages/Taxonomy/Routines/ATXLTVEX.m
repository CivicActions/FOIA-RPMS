 .I ATXLFILE=40.7 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$GET1^DIQ(ATXLFILE,ATXLX,1),ATXC1="CLINIC NAME",ATXC2="IHS CLINIC CODE"
 .I ATXLFILE=44 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$GET1^DIQ(ATXLFILE,ATXLX,2),ATXLC2=50
 .I ATXLFILE=4 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$GET1^DIQ(9999999.06,ATXLX,.12),ATXC1="LOCATION",ATXC2="ASUFAC CODE"
 .I ATXLFILE=9999999.06 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$GET1^DIQ(ATXLFILE,ATXLX,.12),ATXC1="LOCATION",ATXC2="ASUFAC CODE"
 .I ATXLFILE=9999999.18 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXC1="INSURER NAME"
 .I ATXLFILE=9999999.11 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXC1="VENDOR NAME"
 .I ATXLFILE=9999999.22 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXC1="SERVICE UNIT"
 .I ATXLFILE=9999999.03 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXC1="TRIBE NAME"
 .I ATXLFILE=9002010.03 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$GET1^DIQ(ATXLFILE,ATXLX,8801),ATXC1="ANATOMIC NAME",ATXC2="MNEMONIC"
 .I ATXLFILE=9999999.31 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$GET1^DIQ(ATXLFILE,ATXLX,.02),ATXC1="ADA CODE",ATXC2="DESCRIPTION"
 .I ATXLFILE=50.68 S ATXL1=ATXLX,ATXC1="VA PRODUCT ID",ATXLC2=50,Y=$O(^PSNDF(50.68,"C",ATXL1,0)) I Y S ATXL2=$$VAL^XBDIQ1(ATXLFILE,Y,.01),ATXC2="DRUG"
 .I ATXLFILE=50.605 S ATXL1=ATXLX,ATXC1="VA DRUG CLASS",Y=$O(^PS(50.605,"B",ATXL1,0)) I Y S ATXL2=$$VAL^XBDIQ1(ATXLFILE,Y,1),ATXC2="CODE"
 .I ATXLFILE=7 S ATXL1=$$GET1^DIQ(ATXLFILE,ATXLX,.01),ATXL2=$$VAL^XBDIQ1(7,ATXLX,9999999.01),ATXC1="PROVIDER DISCIPLINE",ATXC2="IHS CODE"
