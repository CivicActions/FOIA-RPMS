 W !,"County ",$P($G(^DIC(5,TN,1,D1,0)),"^")," of State ",$P($G(^DIC(5,TN,0)),"^")," is looked. It could not be corrected the VA County Code.",!,"Please using FileMan to correct VA County Code for this county."
 W !,"The corrected VA County Code of this county is: ",$P(CTN1,"^",3)
 Q
 ;
REINDEX(NT) ;Reindex 'C' x-ref after update counties
