 ;;8.0;KERNEL;**295**;Jul 10,1995
 ;This routine cleans up VA County Codes for States.
 Q
CRTP ;;Correct misspelling counties
 N I,CN,CN1,CN2,CN3,CN4
 F I=1:1 S CN=$T(SPELL+I) Q:(CN[";;EOF;;")  D
 .S CN=$P(CN,";;",2)
 .S CN1=$P(CN,"^"),CN2=$P(CN,"^",2),CN3=$P(CN,"^",3),CN4=$P(CN,"^",4)
 .Q:'$D(^DIC(5,CN1,0))
 .Q:($P($G(^DIC(5,CN1,0)),"^")='CN2)
 .Q:'$D(^DIC(5,CN1,1,0))
 .S CN=0 F  S CN=$O(^DIC(5,CN1,1,CN)) Q:CN'>0  D
 ..I $P($G(^DIC(5,CN1,1,CN,0)),"^")=CN3 D
 ...L +^DIC(5,CN1,1,CN,0):10 Q:'$T
 ...S $P(^DIC(5,CN1,1,CN,0),"^")=CN4
 ...L -^DIC(5,CN1,1,CN,0) Q
 ;Reindex to correct misspelling for B x-ref.
 N DA,DIK S DIK="^DIC(5," D IXALL2^DIK
 N DA,DIK S DIK="^DIC(5," D IXALL^DIK
 Q
