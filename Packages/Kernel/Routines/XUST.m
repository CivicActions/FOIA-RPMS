UPDATE(RTN,TAG,TN) ;Update Counties for State.
 N CTN,I
 S CTN=$T(@TAG^@RTN),CTN=$P(CTN,";;",2)
 Q:$P(CTN,"^")'=$P($G(^DIC(5,TN,0)),"^")
 Q:'$D(^DIC(5,TN,1,0))
 I $P($G(^DIC(5,TN,0)),"^",3)'=$P(CTN,"^",3) D
 .L +^DIC(5,TN,0) Q:'$T
 .S $P(^DIC(5,TN,0),"^",3)=$P(CTN,"^",3)
 .L -^DIC(5,TN,0)
 W !,"STATE:    ",$P($G(^DIC(5,TN,0)),"^"),!
 F I=1:1 S CTN=$T(@TAG+I^@RTN) Q:(CTN[";;EOF;;")  D UPDA
 D REINDEX(TN)
 Q
 ;
UPDA ;Do Update
 N CTN1,CTN2,D1
 S CTN1=$P(CTN,";;",2),CTN2=$P(CTN1,"^")
 S D1=0 F  S D1=$O(^DIC(5,TN,1,D1)) Q:D1'>0  D
 .I $P($G(^DIC(5,TN,1,D1,0)),"^")=CTN2 D
 ..Q:($P(^DIC(5,TN,1,D1,0),"^",3)=$P(CTN1,"^",3))
 ..L +^DIC(5,TN,1,D1,0):10 I '$T D MESS Q
 ..S $P(^DIC(5,TN,1,D1,0),"^",3)=$P(CTN1,"^",3)
 ..W "    COUNTY: ",CTN2,"       VA COUNTY CODE: ",$P(CTN1,"^",3),!
 ..L -^DIC(5,TN,1,D1,0)
 Q
