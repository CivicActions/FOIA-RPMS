 ; - RECORD = ASSOCIATED USER CLASS MEMBERSHIP FILE (#8930.3) RECORD IEN
 ; - USER   = NEW PERSON FILE (#200) IEN FOR ASSOCIATED USER
 ;
 N TYPE,ACTION
 S TYPE="TYPE~UCM"
 I +$L($T(FAUD^BUSAAPI)) S ACTION="U"  ;IF BUSA V1.0 PATCH 3 IS INSTALLED, SET ACTION TO "U" (CHANGE TO USER PRIVILEGE)
 E  S ACTION=""                        ;IF NOT, LEAVE ACTION NULL
 D FILER(EVENT,ACTION,CALL,DESC,TYPE,RECORD,USER)
 Q
