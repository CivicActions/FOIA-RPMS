UCM01(USER,CLASS,RECORD) ;RECORD EVENT CODE UCM01 - USER DESIGNATED AS MEMBER OF A USER CLASS
 ;INPUT PARAMETERS:
 ; - USER   = NEW PERSON FILE (#200) IEN FOR ASSOCIATED RECORD
 ; - CLASS  = USER CLASS FILE (#8930) IEN FOR ASSOCIATED RECORD
 ; - RECORD =  USER CLASS MEMBERSHIP FILE (#8930.3) IEN FOR ASSOCIATED RECORD
 ;
 N EVENT,DESC,CLASSNM,CLASSUSR,CLASSUNM,U,CALL
 S U="^"
 S EVENT="UCM01"
 S CALL="8930.3/.02 XREF AUCM02"
 S CLASSNM=$P(^USR(8930,CLASS,0),U,1)
 S CLASSUSR=$P(^USR(8930.3,RECORD,0),U,1)
 S CLASSUNM=$$NPNAME(CLASSUSR)
 S DESC="UCM - user "_CLASSUNM_" designated user class "_CLASSNM
 D UCM(EVENT,CALL,DESC,RECORD,USER)
 Q
