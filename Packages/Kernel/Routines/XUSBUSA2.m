NP(CODE,CALL,DESC,RECORD,USER) ;MAIN NEW PERSON EVENT BUSA FILER
 ;INPUT PARAMETERS:
 ; - CODE   = NEW PERSON EVENT CODE
 ; - CALL   = SOURCE INITIATING THE CALL TO THIS API SET
 ; - DESC   = BASE DESCRIPTION
 ; - RECORD = NEW PERSON FILE (#200) IEN FOR SOURCE-AFFECTED RECORD
 ; - USER   = NEW PERSON FILE (#200) IEN FOR USER INITIATING ACTION
 ;
 N TYPE,ACTION
 S TYPE="TYPE~NP"
 I +$L($T(FAUD^BUSAAPI)) S ACTION="U"  ;IF BUSA V1.0 PATCH 3 IS INSTALLED, SET ACTION TO "U" (CHANGE TO USER PRIVILEGE)
