FILER(EVENT,ACTION,CALL,DESC,TYPE,RECORD,USER) ;ASSEMBLE CALL PARAMETERS AND SEND TO KERNEL'S BUSA FILER
 N DO,DD  ;ADDED TO AVOID BUSA CAUSING PROBLEMS WITH NESTED FILE^DICN CALLS FURTHER DOWNSTREAM
 N CALLTYPE,CAT,FS,NAMESP,RESULT
 S CALLTYPE="A"  ;API call
 S CAT="S"  ;System event
 S FS="|"  ;Description's internal field separator
 S NAMESP="XU"
 S RESULT="RSLT~S"  ;Success assumed if FileMan executes the MUMPS cross-references
 ;Assemble full description:
 ;"|"-piece 1 = Base description
 ;"|"-piece 2 = Type
 ;"|"-piece 3 = Result
 ;"|"-piece 7 = Event Code
 S $P(DESC,FS,2)=TYPE
 S $P(DESC,FS,3)=RESULT
 S $P(DESC,FS,4)=NAMESP
 S $P(DESC,FS,7)=EVENT
 D LOG^XUSBUSA(TYPE,CAT,ACTION,CALL,DESC,"",USER)
 Q
