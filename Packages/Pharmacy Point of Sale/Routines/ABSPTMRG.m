ABSPTMRG  ;IHS/OIT/RAM - EXTENDED REJECTION REPORT BY REJECTION REASON
 ;;1.0;PHARMACY POINT OF SALE;**52**;JUN 01, 2001;Build 131
 ; IHS/OIT/RAM ; This is a new routine to set local variables for various routines
 ; fired from TaskMan.
 ;
 Q  ; DON'T CALL DIRECTLY, EACH ENTRY ROUTINE SHOULD BE THE NAME OF THE CALLING ROUTINE.
 ;
TMCANRPT ; THERE IS NO ROUTINE NAME, THIS IS A SORT / PRINT TEMPLATE.
 ;         IN THIS INSTANCE, IT'S THE * ABSP CAN PHARMACY REPORT * INTRODUCED WITH P52.
 ;         THE OPTION NAME THAT CALLS THIS FOR TaskMan IS * ABSP TM CAN PHARMACY RPT *
 ; *** NOTES ***
 ; DO NOT 'NEW' THE VARIABLES 'DIR' OR 'FILENAME' ; THESE VARIABLES MAY NEED TO BE SET
 ; THROUGH TASKMAN'S "VARIABLE NAME:" SETTINGS TO CUSTOMIZE WHERE THE FILES ARE SAVED.
 N I,I2,I3,J,J2,J3,K,K2,K3,OS,FNTEST,UNIQ
 N DEBUG,DSEP,FSEP,DAY,MONTH,LASTMONTH,YEAR,LASTYEAR,LASTDAY,FIRSTDAY,ZDAYS,%I,%H,X,%
 N FR,TO,FLDS,BY,DIC,IOP,%ZIS,FN,EXT
 S DAY=1,DSEP=".",OS=$E($SYSTEM.Version.GetOS(),1),K="",K2=0
 S DIR=$G(DIR),FILENAME=$G(FILENAME)
 S FSEP=$S(OS="U":"/",1:"\")
 S DEBUG=0 ; SET DEBUGGING FLAG TO 1 FOR EXTRA OUTPUT, 2 TO MANUALLY CHANGE DATE, 0 FOR PRODUCTION.
 D NOW^%DTC
 I DEBUG>1 S %I(1)=2,%I(3)=317 ; MANUALLY OVERRIDE DATE IF DEBUG SET HIGHER THAN 1.
 S MONTH=%I(1),(LASTYEAR,YEAR)=%I(3)+1700
 S LASTMONTH=MONTH-1
 S:LASTMONTH=0 LASTMONTH=12,LASTYEAR=LASTYEAR-1
 S LASTDAY=$$LASTDAY(LASTMONTH,LASTYEAR)
 S FIRSTDAY=LASTMONTH,$P(FIRSTDAY,DSEP,2)=DAY,$P(FIRSTDAY,DSEP,3)=LASTYEAR
 I DEBUG W FIRSTDAY_"^"_LASTDAY,!
 I '##class(%Library.File).Exists($G(DIR)) S DIR=$$GETKRNDR
 E  S DIR=##CLASS(%Library.File).NormalizeDirectory(DIR)
 I $G(DIR)="" W:DEBUG "No available output directory. Exiting.",! Q
 S FN=FILENAME
 I FN="" S FN="ABSPCANRPT_"_LASTYEAR_"_"_LASTMONTH,EXT="TXT"
 E  S I=$L(FN,"."),I=$S(I<2:2,1:I),EXT=$P(FN,".",I),FN=$P(FN,".",1,I-1) S:EXT="" EXT="TXT"
 F  Q:'##class(%Library.File).Exists(DIR_FN_K_"."_EXT)  D
 . S K=$S(K="":"_"_$J,1:"_"_($R(99998)+1))
 S FILENAME=FN_K_"."_EXT
 S FR=FIRSTDAY_",0"
 S TO=LASTDAY_",z"
 S FLDS="[ABSP CAN PHARMACY REPORT]"
 S BY="[ABSP CAN PHARMACY REPORT]"
 S DIC="^ABSPECX(""RPT"","
 S IOP="HFS;999;99999"
 ;
 I DEBUG D  ; Q
 . W "FN: ",FN,!
 . W "FILENAME: ",FILENAME,!
 . W "DIR: ",DIR,!
 . W "FR: ",FR,!
 . W "TO: ",TO,!
 . S J2=##class(%Library.File).%New(DIR_FILENAME) I J2 W "File Created!",! D J2.Open("WSN"),J2.Close()
 . ; W "FN: ",FN,!
 ;
 S %ZIS("HFSNAME")=DIR_FILENAME
 D EN1^DIP
 ;
 Q
 ;
GETKRNDR() ; /IHS/OIT/RAM ; GET THE KERNEL SYSTEM DIRECTORY -- RETURN THE PRIMARY
 ;         DIRECTORY IF IT EXISTS, IF NOT, RETURN THE SECONDARY DIRECTORY.
 ;         IF NEITHER EXIST, RETURN SOMETHING 'FALSY.' (read: 0)
 ;
 N DIR
 S DIR=##CLASS(%Library.File).NormalizeDirectory($$GET1^DIQ(8989.3,1,320))
 S:'##class(%Library.File).Exists(DIR) DIR=##CLASS(%Library.File).NormalizeDirectory($$GET1^DIQ(8989.3,1,320.2))
 I '##class(%Library.File).Exists(DIR) Q ""
 Q DIR
 ;
LASTDAY(MONTH,YEAR,SEP) ; /IHS/OIT/RAM ; EXTRINSIC ROUTINE TO GIVE THE LAST DAY OF THE MONTH.
 ;;1.0;PHARMACY POINT OF SALE;**52**;JUN 01, 2001;Build 45
 ; TAKES LEAP YEARS INTO ACCOUNT.
 ; RETURNS FULL DATE IN 'MM.DD.YY' IF SEP ISN'T DEFINED, OTHERWISE 'MM_SEP_DD_SEP_YY'
 N LEAP,ZDAYS,LASTDAY,FULLDATE
 S:'$D(SEP) SEP="."
 S ZDAYS="31^28^31^30^31^30^31^31^30^31^30^31"
 S LEAP=$S(YEAR#400=0:1,YEAR#100=0:0,YEAR#4=0:1,1:0)
 S LASTDAY=$P(ZDAYS,"^",MONTH) S:'+LASTDAY LASTDAY=31
 S:MONTH=2&LEAP LASTDAY=29
 S FULLDATE=MONTH,$P(FULLDATE,SEP,2)=LASTDAY,$P(FULLDATE,SEP,3)=YEAR
 Q FULLDATE
 ;
