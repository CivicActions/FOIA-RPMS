PSXCOPAY ;BIR/HTW,WPB-Routine to Do Copay ;[ 06/18/97  12:54 PM ]
 ;;2.0;CMOP;**3**;11 Apr 97
EN S XXX="" F  S XXX=$O(^PSRX("AR",XXX)) Q:XXX=""  S RXP="" F  S RXP=$O(^PSRX("AR",XXX,RXP)) Q:RXP=""  S PSXREF="" S PSXREF=$O(^PSRX("AR",XXX,RXP,"")) D START
 Q
START I '$D(PSOPAR) S PSOPAR=$G(^PS(59,$P(^PSRX(RXP,2),"^",9),1)),PSOSITE=$P(^PSRX(RXP,2),"^",9)
AC1 S PSIN=+$P($G(^PS(59.7,1,49.99)),"^",2)
 G:$D(^PSRX(RXP,0)) BC1
 Q
BC1 ;
 I $G(PSXREF)>0 S OUT=1 D REFILL K OUT G BC
BATCH I '$D(^PSRX(RXP,"TYPE")) S ^PSRX(RXP,"TYPE")=0
 S PSOCPN=$P(^PSRX(RXP,0),"^",2),TYPE=$P($G(^PSRX(RXP,"TYPE")),"^")
 ;I $P($G(^PSRX(RXP,2)),"^",13)]"" S PSXJOB=6,PSXREF="ORIGINAL" S:$G(PSXREF)<1 PSXREF="ORIGINAL" D ER6^PSXERR G REFILL
 I '$G(TYPE),'$P($G(^PSRX(RXP,2)),"^",13),+$P($G(^(2)),"^",2)'<PSIN D UPDATE
REFILL I '$G(TYPE) S XTYPE=1 D QTY
 Q:$G(OUT)>0  G BC
UPDATE I '$G(^PSRX(RXP,"TYPE")),+$G(^PSRX(RXP,"IB")) S PSOCPRX=$P(^PSRX(RXP,0),"^") I $D(^PSRX(RXP,"IB")) D CP^PSOCP
 Q
BC Q
 K %,DIC,IFN,J,DA,DR,DIE,X,X1,X2,Y,CX,PX,REC,DIR,YDT,REC,RDUZ,DIRUT
 K PSOCPN,PSOCPRX,QDRUG,QTY,TYPE,XTYPE,DUOUT,PSRH,XX,Y,PSIN,PSOPAR,OUT
 K PSODTCT,PSOPAR7,PSOSYS,YDT,RESK,PSXJOB,PSXER,Z,XXX,YY,PSXREF,RXP
 Q
BCI S RXP=0
RXP I $G(PSXVER) S RXP=$O(^PSRX("B",X,RXP)) I $P($G(^PSRX(+RXP,"STA")),"^",1)=13 G RXP ;GET RECORD NUMBER FROM SCRIPT NUMBER
 I '$G(PSXVER) S RXP=$O(^PSRX("B",X,RXP)) I $P($G(^PSRX(+RXP,0)),"^",15)=13 G RXP ;GET RECORD NUMBER FROM SCRIPT NUMBER
 Q
QTY S PSOCPN=$P(^PSRX(RXP,0),"^",2)
 D:$P($G(^PSRX(RXP,XTYPE,PSXREF,0)),"^")'<PSIN  I '$G(^PSRX(RXP,"TYPE")),+$G(^PSRX(RXP,"IB")),$G(IFN) S PSOCPRX=$P(^PSRX(RXP,0),"^") D CP^PSOCP
 .S (YY,IFN)=PSXREF
 Q
