CPTSM(P,BDATE,EDATE) ;EP - did pat have TOBACCO SCREENING cpt?
 NEW X
 S X=$$CPT^BDMDNDU(P,BDATE,EDATE,$O(^ATXAX("B","BGP TOBACCO SCREEN CPTS",0)),5)
 I X]"" Q $P(X,U,2)_U_$P(X,U,1)
 Q ""
