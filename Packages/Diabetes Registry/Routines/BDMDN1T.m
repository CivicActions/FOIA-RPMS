 .I $P(T,"-")="G9016" S %=T_U_$P(BDMG(X),U) Q
 .I $P(T,"-")="G9458" S %=T_U_$P(BDMG(X),U) Q
 .I $P(T,"-")="G0030" S %=T_U_$P(BDMG(X),U) Q
 .I $P(T,"-")]"",$$SNOMED^BDMUTL(2024,"PXRM BGP TOBACCO SCREENED",$P(T,"-")) S I=1 S %=T_U_$P(BDMG(X),U) Q
