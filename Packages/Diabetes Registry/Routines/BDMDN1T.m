 .Q:'$D(^AUPNVHF("AA",P,H))
 .S D="" F  S D=$O(^AUPNVHF("AA",P,H,D)) Q:D'=+D  D
 ..Q:(9999999-D)>ED  ;after time frame
 ..Q:(9999999-D)<BD  ;before time frame
 ..S C=C+1
 ..S @BDMRET@(D,C)=(9999999-$P(D,"."))_"^"_$P(^AUTTHF(H,0),U,1)_"^"_$P(^AUTTHF(H,0),U,2)
 .Q
