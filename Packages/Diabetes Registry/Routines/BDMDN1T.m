 I X="" Q "2  No"
 Q "1  Yes "_$$DATE^BDMS9B1($P(X,U,3))_" "_$P(X,U,2)
LASTENDS(P,BD,ED) ;EP - DON'T COUNT NON NICOTINE ONE
 I $G(P)="" Q ""
 I $G(BD)="" S BD=$$DOB^AUPNPAT(P)
