GETCGM(RETVAL,BDMSTR) ;-- get CGM records
 S X="MERR^BDMGU",@^%ZOSF("TRAP")
 N BDMI,BDMPAT,BDMIEN,BDMDATA,BDMDATA1,BDMDA,P,BDMIEN
 N BDMRDT,BDMSDT,BDMEDT,BDMDUR,BDMGMI,BDMTIR,BDMTL,BDMTH
 S P="|"
 S BDMI=0
 S BDMERR=""
 K ^BDMTMP($J)
 S RETVAL="^BDMTMP("_$J_")"
 S ^BDMTMP($J,BDMI)="T00010BDMIEN^T00030ReportDate^T00030StartDate^T00030EndDate^T00010Duration^T00010GMI^T00010TIR^T00010TargetLow^T00010TargetHigh"_$C(30)
 S BDMPAT=$P(BDMSTR,P)
 S BDMDA=0 F  S BDMDA=$O(^AUPNPRDI("AA",BDMPAT,BDMDA)) Q:'BDMDA  D
 . S BDMIEN=0 F  S BDMIEN=$O(^AUPNPRDI("AA",BDMPAT,BDMDA,BDMIEN)) Q:'BDMIEN  D
 .. Q:$$GET1^DIQ(9000090,BDMIEN,.01)'="CONTINUOUS GLUCOSE MONITORING"
 .. S BDMDATA=$G(^AUPNPRDI(BDMIEN,0))
 .. S BDMDATA1=$G(^AUPNPRDI(BDMIEN,11))
 .. Q:$P(BDMDATA,U,9)]""  ;don't display deleted
 .. S BDMRDT=$P(BDMDATA,U,4)
 .. S BDMSDT=$P(BDMDATA1,U)
 .. S BDMEDT=$P(BDMDATA1,U,2)
 .. S BDMDUR=$P(BDMDATA1,U,3)
 .. S BDMGMI=$P(BDMDATA1,U,4)
 .. S BDMTIR=$P(BDMDATA1,U,5)
 .. S BDMTL=$P(BDMDATA1,U,7)
 .. S BDMTH=$P(BDMDATA1,U,8)
 .. S BDMI=BDMI+1
 .. S ^BDMTMP($J,BDMI)=BDMIEN_U_BDMRDT_U_BDMSDT_U_BDMEDT_U_BDMDUR_U_BDMGMI_U_BDMTIR_U_BDMTL_U_BDMTH_$C(30)
 S ^BDMTMP($J,BDMI+1)=$C(31)
 Q
 ;
SAVECGM(RETVAL,BDMSTR) ;-- save CGM records
 ;S X="MERR^BDMGU",@^%ZOSF("TRAP") ; m error trap
 N BDMFDA,BDMIENS,BDMERR,P,MODE,CGMIEN,TYP,PAT,RDT,SDT,EDT,DUR,GMI,TIR,TL,TH
 K ^BDMTMP($J)
 S P="|"
 S BDMI=0
 S RETVAL="^BDMTMP("_$J_")"
 S ^BDMTMP($J,BDMI)="T00010BDMIENS"_$C(30)
 S MODE=$P(BDMSTR,P)
 S BDMIENS=$P(BDMSTR,P,2)
 S PAT=$P(BDMSTR,P,3)
 S RDT=$P(BDMSTR,P,4)
 S SDT=$P(BDMSTR,P,5)
 S EDT=$P(BDMSTR,P,6)
 S DUR=$P(BDMSTR,P,7)
 S GMI=$P(BDMSTR,P,8)
 S TIR=$P(BDMSTR,P,9)
 S TL=$P(BDMSTR,P,10)
 S TH=$P(BDMSTR,P,11)
 S TYP=$O(^AUTTPRDT("B","CONTINUOUS GLUCOSE MONITORING",0))
 S BDMIENS=""
 I MODE="A" D
 .S BDMIENS=$P(BDMSTR,P,2)
 .S PAT=$P(BDMSTR,P,3)
 .S RDT=$P(BDMSTR,P,4)
 .S SDT=$P(BDMSTR,P,5)
 .S EDT=$P(BDMSTR,P,6)
 .S DUR=$P(BDMSTR,P,7)
 .S GMI=$P(BDMSTR,P,8)
 .S TIR=$P(BDMSTR,P,9)
 .S TL=$P(BDMSTR,P,10)
 .S TH=$P(BDMSTR,P,11)
 .S BDMFDA(9000090,"+1,",.01)=TYP
 .S BDMFDA(9000090,"+1,",.02)=PAT
 .;S BDMFDA(9000090,"+1,",.03)=""  ;maybe visit at some time
 .S BDMFDA(9000090,"+1,",.04)=RDT
 .S BDMFDA(9000090,"+1,",.05)=$$NOW^XLFDT()
 .S BDMFDA(9000090,"+1,",.06)=DUZ
 .S BDMFDA(9000090,"+1,",.07)=$$NOW^XLFDT()
 .S BDMFDA(9000090,"+1,",.08)=DUZ
 .S BDMFDA(9000090,"+1,",1101)=SDT
 .S BDMFDA(9000090,"+1,",1102)=EDT
 .S BDMFDA(9000090,"+1,",1103)=DUR
 .S BDMFDA(9000090,"+1,",1104)=GMI
 .S BDMFDA(9000090,"+1,",1105)=TIR
 .S BDMFDA(9000090,"+1,",1106)="97506-0"
 .S BDMFDA(9000090,"+1,",1107)=TL
 .S BDMFDA(9000090,"+1,",1108)=TH
 .D UPDATE^DIE("","BDMFDA","BDMIENS","BDMERR(1)")
 .S BDMCGMI=$G(BDMIENS(1))
 .S BDMI=BDMI+1
 I MODE="E" D
 .S BDMIENS=$P(BDMSTR,P,2)
 .S RDT=$P(BDMSTR,P,4)
 .S SDT=$P(BDMSTR,P,5)
 .S EDT=$P(BDMSTR,P,6)
 .S DUR=$P(BDMSTR,P,7)
 .S GMI=$P(BDMSTR,P,8)
 .S TIR=$P(BDMSTR,P,9)
 .S TL=$P(BDMSTR,P,10)
 .S TH=$P(BDMSTR,P,11)
 .S BDMIENS=BDMIENS_","
 .S BDMFDA(9000090,BDMIENS,.04)=RDT
 .S BDMFDA(9000090,BDMIENS,.07)=$$NOW^XLFDT()
 .S BDMFDA(9000090,BDMIENS,.08)=DUZ
 .S BDMFDA(9000090,BDMIENS,1101)=SDT
 .S BDMFDA(9000090,BDMIENS,1102)=EDT
 .S BDMFDA(9000090,BDMIENS,1103)=DUR
 .S BDMFDA(9000090,BDMIENS,1104)=GMI
 .S BDMFDA(9000090,BDMIENS,1105)=TIR
 .S BDMFDA(9000090,BDMIENS,1107)=TL
 .S BDMFDA(9000090,BDMIENS,1108)=TH
 .D FILE^DIE("K","BDMFDA","BDMERR(1)")
 .S BDMCGMI=$G(BDMIENS(1))
 .S BDMI=BDMI+1
 I MODE="D" D
 .S BDMIENS=$P(BDMSTR,P,2)
 .S DELR=$P(BDMSTR,P,3)
 .S DELC=$P(BDMSTR,P,4)
 .S BDMIENS=BDMIENS_","
 .S BDMFDA(9000090,BDMIENS,.07)=$$NOW^XLFDT()
 .S BDMFDA(9000090,BDMIENS,.08)=DUZ
 .S BDMFDA(9000090,BDMIENS,.09)=$$NOW^XLFDT()
 .S BDMFDA(9000090,BDMIENS,.11)=DUZ
 .S BDMFDA(9000090,BDMIENS,.12)=DELR
 .S BDMFDA(9000090,BDMIENS,.13)=DELC
 .D FILE^DIE("K","BDMFDA","BDMERR(1)")
 .S BDMCGMI=$G(BDMIENS(1))
 .S BDMI=BDMI+1
 S ^BDMTMP($J,BDMI)=$G(BDMCGMI)_$C(30)
 S ^BDMTMP($J,BDMI+1)=$C(31)
 S BDMPAT=PAT
 I $G(BDMGCMI)="" S BDMGCMI=$G(CGMIEN)
 ;D BUSAE
 K BDMPAT,BDMGCMI
 Q
 ;
BUSAE ;-- log BUSA
 K BDMDETAL
 S BDMDETAL(1)=BDMPAT_"^"_BDMCGMI
 D LOG^BUSAAPI("O","O","A",$S($G(XQY0)]"":$P(XQY0,U),1:"BDMGCGM"),"UPDATED PATIENT CGM DATA","BDMDETAL")
 Q
