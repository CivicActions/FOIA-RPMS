 . S PCCTIME=$$GET1^DIQ(9000010,VIEN_",",.01,"I") Q:PCCTIME=""  ;PCC visit date/time
 . ;
 . ;Quit if times match
 . I EDTIME=PCCTIME Q
 . ;
 . ;Update ER ADMISSION entry
 . S AMERUPD(9009081,DFN_",",1)=PCCTIME
 . D FILE^DIE("","AMERUPD","ERR")
 . ;
 . ;Update BEDD if installed
 . I $T(VDTM^BEDDSYNC)]"" D VDTM^BEDDSYNC(VIEN,EDTIME,PCCTIME)
