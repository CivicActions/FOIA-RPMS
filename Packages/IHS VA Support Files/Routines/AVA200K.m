 ;;93.2;VA SUPPORT FILES;**27**;JUL 01, 1993;Build 8
 ;;
 N AVAQUIT  ;HIGH-LEVEL OPERATION TERMINATION FLAG
 D INIT
 D:'AVAQUIT MAIN
 D CLEAN
 Q
 ;
INIT ;PRIMARY MODULE INITIALIZATION
 N DIR,FILENUM
 I '$D(DT) D  ;
 . D NOW^%DTC
 . S DT=X
 D BACKUP Q:AVAQUIT  ;MAKE BACKUP COPY OF FILE 3/6/16/200 GLOBALS PRIOR TO REMOVAL
 Q
 ;
MAIN ;PRIMARY MODULE EXECUTION
 D XKILL  ;FLUSH FILE 3/6/16/200 SYNCHRONIZATION XREFS FIRST
 D FKILL  ;THEN KILL OFF FILES 3, 6, AND 16
 D INPMOD ;NECESSARY CLEANUP TO INPUT TRANSFORMS OF FILE 200 FIELDS
 Q
 ;
XKILL ;KILL OFF FILE 200 (NEW PERSON) XREFS USED TO SYNCHRONIZE ENTRY INFO IN FILES 3, 6 & 16
 N DELIM,XLINE,XREF,XREFLN,FILE,GROOT,FIELD,XREFIEN,DIK,DA
 D MSG("  ")
 D MSG("REMOVING SYNCRONIZATION CROSS-REFERENCES FROM FILE 200...")
 S DELIM=";"  ;IN-LINE FIELD DELIMITER
 F XLINE=2:1 S XREFLN=$T(XREFS+XLINE),XREF=$P(XREFLN,DELIM,3) Q:XREF=""  D  ;
 . S GROOT=$P(XREFLN,DELIM,4)  ;FILE CONTAINING XREF TO BE DELETED
 . S XREFIEN=$P(XREFLN,DELIM,5)  ;IEN OF  TO-BE-DELETED XREF IN THIS FIELD'S XREF LIST
 . S DA(1)=$P(XREFLN,DELIM,6)  ;ALSO # OF XREF'ED FIELD
 . S DA(2)=$P(XREFLN,DELIM,7)
 . D MSG("- FIELD #"_DA(1)_": "_XREF_" CROSS-REFERENCE (#"_XREFIEN_")...")
 . K DIK,DA
 . S DIK=GROOT,DA=XREFIEN,DA(1)=$P(XREFLN,DELIM,6),DA(2)=$P(XREFLN,DELIM,7)
 . ;W !!,"XKILL ^DIK REF: ^DD(",DA(2),",",DA(1),",1,",DA  ;DEBUG
 . D ^DIK  ;commented for safety's sake during development
 Q
 ;
FKILL ;NUKE FILES 3 (USER), 6 (PROVIDER), AND 16 (PERSON)
 N FILE
 K DIU  ;MAKE SURE DIU IS 'CLEAN' BEFORE PROCEEDING
 F FILE=3,6,16 D  ;
 . S DIU=FILE
 . S DIU(0)="DT"
 . D MSG("DELETING FILE "_DIU_"...")
 . ;W ! ZW DIU W !  ;DEBUG
 . D EN^DIU2  ;commented for safety's sake during development
 . K DIU  ;POST-EXECUTE CLEANUP
 Q
 ;
INPMOD ;FILE 200 FIELDS - INPUT TRANSFORM MODIFICATION(S)
 ;
 ;FILE 200, FIELD 9999999.09 - IHS ADC INDEX
 ; - CONVERT ^DIC(6 REFS IN INPUT TRANSFORM TO ^VA(200 REFS
 S ^DD(200,9999999.09,0)=$P(^DD(200,9999999.09,0),U,1,4)_"^K:X[""""""""!($A(X)=45) X I $D(X) K:$L(X)>6!($L(X)<4) X I $D(X),$D(^VA(200,""GIHS"",X)),'$D(^VA(200,""GIHS"",X,DA)) K X W:'$D(ZTQUEUED) ""  THAT AFFIL-DISC-CODE ALREADY USED! """
 ;
 Q
 ;
BACKUP ;PRE-OP SAFETY BACKUP OF FILES 3, 6, 16 AND 200
 ;
 ;NOTE: EARLIER VERSIONS OF ^AVA200K PROMPTED USER TO DELETE PRE-EXISTING BACKUPS IF DETECTED,
 ;      BUT USERS PERFORMING MULTIPLE SUCCESSIVE ^AVA200K RUNS RESULTED IN  FILE 3/6/16 DD AND 
 ;      DATA GLOBAL BACKUPS BEING DELETED BEYOND CAPACITY FOR RECOVERY.
 ;      BACKUP SUBROUTINE WAS CORRESPONDINGLY REWRITTEN TO DEFAULT TO RESTORATION (NOT DELETION)
 ;      OF EXISTING BACKUPS; USER HAS THE ABILITY TO MANUALLY REMOVE PRE-EXISTING BACKUPS BY 
 ;      A STRAIGHTFORWARD 'KILL OF THE ^AVA200K GLOBAL, BUT THIS SOFTWARE WILL NO LONGER DELETE
 ;      ANY SUCH PRE-EXISTING BACKUP FILES AS PART OF THE AUTOMATED PROCESS.
 ;
 ;OPERATIVE PROCEDURE IS NOW AS FOLLOWS:
 ; 1. SUBROUTINE CHECKS IF A BACKUP COPY OF THE RELEVANT GLOBALS EXISTS.
 ; 2. IF A BACKUP EXISTS FROM A PRIOR RUN, USER IS PROMPTED TO RESTORE IT.
 ; 3. IF USER ANSWERS 'YES' (RESTORE THE EXISTING BACKUP) THEN BACKUP IS RESTORED; THE OVERALL
 ;    ^AVA200K PROCESS CONTINUES.
 ; 4. IF USER ANSWERS 'NO' (NOT TO RESTORE THE EXISTING BACKUP) THEN A PROCESS TERMINATION FLAG
 ;    IS SET AND THE SUBROUTINE EXITED; THE OVERALL ^AVA200K PROCESS BEING ABORTED.
 ; 
 N FILE,FLAG,FIELD,SUB,NODE,SUBDD
 S AVAQUIT=0
 I '$D(DT) D  ;
 . D NOW^%DTC
 . S DT=X
 ;
 I $D(^AVA200K) D CHKBK ;CHECK IF PRE-EXISTING BACKUP COPY OF FILE GLOBALS EXISTS
 I 'AVAQUIT D  ;ABORT IF EARLIER BACKUP EXISTS AND USER OPTS TO NOT RESTORE, ELSE CONTINUE
 . D MSG("  ")
 . D MSG("MAKING PRE-EXECUTION BACKUP COPY OF FILES 3, 6, 16 AND 200...")
 . S ^AVA200K(0)=DT
 . ;
 . ;^DD BACKUPS
 . ;
 . F FILE=3,6,16,200  D  ;
 . . M ^AVA200K("DD",FILE)=^DD(FILE)
 . . D SUBBACK(FILE)
 . ;
 . ;^DIC BACKUPS
 . ;
 . F FILE=3,6,16,200  D  ;
 . . M ^AVA200K("DIC",FILE)=^DIC(FILE)
 . . D MSG("...FILE "_FILE_" BACKUP COMPLETED...")
 . D MSG("PRE-EXECUTION FILE BACKUP COMPLETE.")
 Q
 ;
SUBBACK(FILE,DEPTH) ;BACKUP ^DD NODES OF SPECIFIED FILE'S SUBFILES TO ^AVA2OOK
 ;                  (MAY BE CALLED RECURSIVELY TO ADDRESS SUBFILES OF SUBFILES)
 ;INPUT ARGUMENTS:
 ; - FILE    = NUMBER OF FILEMAN FILE BEING BACKED UP
 ; - DEPTH = TAB DEPTH FOR DEBUG OUTPUT (OPTIONAL, DEBUG ONLY)
 N FSCAN
 S FILE=$G(FILE),DEPTH=$G(DEPTH,1) Q:'FILE
 ;
 S FSCAN=0 F  S FSCAN=$O(^DD(FILE,"SB",FSCAN)) Q:'FSCAN  D  ;SCAN SB NODES
 . N FLD S FLD=$O(^DD(FILE,"SB",FSCAN,0)) Q:'FLD  ;FIELD IN PARENT FILE
 . Q:'$D(^DD(FILE,FLD))  ;CONFIRM EXISTS
 . Q:$G(^DD(FSCAN,0,"UP"))=FILE  ;CHECK UP NODE IN SUBFILE
 . S:$G(^DD(FSCAN,0,"UP"))'=FILE ^DD(FSCAN,0,"UP")=FILE  ;SET UP NODE IN SUBFILE
 ;
 ;W !,?(DEPTH-1*3),"CHECK: ",FILE  ;DEBUG
 S FSCAN=0
 F  S FSCAN=$O(^DD(FSCAN)) Q:'FSCAN  D  ;
 . Q:$G(^DD(FSCAN,0,"UP"))'=FILE
 . ;
 . I '$D(^DD(FILE,"SB",FSCAN)) D  ; IF SB NODE MISSING ON PARENT, FIND THE FIELD IN PARENT FILE AND SET
 . . N FLD S FLD=0 F  S FLD=$O(^DD(FILE,FLD)) Q:'FLD  S:+$P($G(^DD(FILE,FLD,0)),U,2)=FSCAN ^DD(FILE,"SB",FSCAN,FLD)=""
 . ;
 . ;W !,?(DEPTH*3),FILE,":",FSCAN,$S($D(^DD(FILE,"SB",FSCAN)):" GOOD",1:" *** SB MISSING ***")  ;DEBUG
 . M ^AVA200K("DD",FSCAN)=^DD(FSCAN)
 . D SUBBACK(FSCAN,DEPTH+2)
 Q
 ;
CHKBK ;IF PRE-EXISTING BACKUP COPY EXISTS, ASK IF IT SHOULD BE RESTORED
 N BKDATE
 Q:'$D(^AVA200K)  ;EXIT IF NO BACKUP FOUND
 S BKDATE=$G(^AVA200K(0))
 S BKDATE=$E(BKDATE,4,5)_"/"_$E(BKDATE,6,7)_"/"_($E(BKDATE,1,3)+1700)
 D MSG("  ")
 D MSG("BACKUP FOUND FROM A PREVIOUS RUN, DATED "_BKDATE_".")
 S DIR(0)="YA"
 S DIR("A")="RESTORE THIS BACKUP AND CONTINUE (Y/N)? "
 D ^DIR
 I +Y D  ;
 . D MSG("RESTORING PRE-EXISTING BACKUP OF FILES 3/6/16/200...")
 . D RESTORE
 E  D  ;
 . D MSG("PREVIOUS BACKUP NOT RESTORED - OPERATION ABORTED.")
 . S AVAQUIT=1
 Q
 ;
RESTORE ;RESTORE PRE-OP BACKUP OF FILES 3, 6, 16 AND 200
 N BKDATE,FILENUM
 I '$D(^AVA200K(0)) D  Q  ;CAN'T EXECUTE IF NO BACKUP FOUND
 . D MSG("NO BACKUP FOUND FOR RESTORATION; OPERATION ABORTED.")
 S BKDATE=$E(^AVA200K(0),4,5)_"/"_$E(^AVA200K(0),6,7)_"/"_($E(^AVA200K(0),1,3)+1700)
 D MSG("BACKUP FOUND, DATED "_BKDATE_".")
 D MSG("RESTORATION OF THIS BACKUP WILL *COMPLETELY* PURGE THE CURRENT NEW PERSON")
 D MSG("/ USER / PERSON / PROVIDER FILES, INCLUDING ANY EDITS MADE TO THESE FILES")
 D MSG("AFTER "_BKDATE_".")
 S DIR(0)="YA"
 S DIR("A")="ARE YOU **ABSOLUTELY CERTAIN** YOU WANT TO RESTORE THIS BACKUP (Y/N)? "
 D ^DIR
 I +Y D  ;
 . D MSG("PURGING EXISTING FILES AND RESTORING FROM BACKUP...")
 . ;
 . ;^DD RESTORES - FILES AND SUBFILES
 . ;
 . S FILENUM=""
 . F  S FILENUM=$O(^AVA200K("DD",FILENUM))  Q:FILENUM=""  D  ;
 . . K ^DD(FILENUM)
 . . M ^DD(FILENUM)=^AVA200K("DD",FILENUM)
 . ;
 . ;^DIC RESTORES - INCLUDES DATA FOR FILES 3/6/16
 . ;
 . S FILENUM=""
 . F  S FILENUM=$O(^AVA200K("DIC",FILENUM))  Q:FILENUM=""  D  ;
 . . K ^DIC(FILENUM)
 . . M ^DIC(FILENUM)=^AVA200K("DIC",FILENUM)
 . . D MSG("...FILE "_FILENUM_"...")
 . ;
 . D MSG("...BACKUP RESTORATION COMPLETE.")
 E  D  ;
 . D MSG("...BACKUP RESTORATION ABORTED.")
 . S AVAQUIT=1
 Q
