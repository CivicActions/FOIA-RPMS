 ; Create PID segment
PID(P) ;EP
 S HLQ=HL1("Q")
 N REC,SEX,RACEI,RACE,ZIP,CNTY,EDA,ETH,ETHI,NTYP,DOD,ADD,CITY,STI,STATE,ADD2,DOB,RACEE,ETHE,AUTH,NPI,CNTRY,RCNT
 S NTYP=$S($P($G(^DPT(P,0)),U)["UNK":"U",1:"S")  ;this needs to be determined
 S NPI=$G(^DIC(4,DUZ(2),"NPI"))
 S AUTH=$P($G(^DIC(4,DUZ(2),0)),U)
 S REC=$$HRN^AUPNPAT(P,DUZ(2))
 S SEX=$P($G(^DPT(P,0)),U,2)
 S DOB=$$FMTHL7^XLFDT($$GET1^DIQ(2,P,.03,"I"))
 I DOB=2000101 S DOB=""  ;2015 chit use this date if DOB is unknown because we require it on reg
 ;2015 chit start
 S RCNT=0
 S RDA=0 F  S RDA=$O(^DPT(P,.02,RDA)) Q:'RDA  D
 . S RCNT=RCNT+1
 . S RACEI=$P($G(^DPT(P,.02,RDA,0)),U)
 . S RACEE=$$GET1^DIQ(10,RACEI,.01)
 . S RACE=$$GET1^DIQ(10,RACEI,4)
 . D SET(.ARY,RACE,10,1,,RCNT)
 . D SET(.ARY,RACEE,10,2,,RCNT)
 . D SET(.ARY,"CDCREC",10,3,,RCNT)
 S ADD=$$GET1^DIQ(2,P,.111)
 S ADD2=$$GET1^DIQ(2,P,.112)
 S CITY=$$GET1^DIQ(2,P,.114)
 S STI=$$GET1^DIQ(2,P,.115,"I")
 S STATE=$S(STI]"":$P($G(^DIC(5,STI,0)),U,3),1:"")
 S ZIP=$$GET1^DIQ(2,P,.116)
 S CNTY=$$GET1^DIQ(2,P,.117)
 I CNTY="" D
 . N COR,CTY
 . S COR=$$GET1^DIQ(9000001,P,1117,"I")
 . S CTY=$S(COR:$$GET1^DIQ(9999999.05,COR,.02,"I"),1:"")
 . ;I CTY S CNTY=$$GET1^DIQ(9999999.23,CTY,.04)   mu2
 . I CTY S CNTY=$P($G(^AUTTCTY(CTY,0)),U,3)_$P($G(^AUTTCTY(CTY,0)),U,7)  ;2015 chit
 S CNTRY="USA"  ;2015 chit need to make dynamic
 S EDA=$O(^DPT(P,.06,"B",0))
 I EDA S ETHI=$P($G(^DPT(P,.06,EDA,0)),U)
 I $G(ETHI) S ETH=$$GET1^DIQ(10.2,ETHI,4),ETHE=$$GET1^DIQ(10.2,ETHI,.01)
 S DOD=$$GET1^DIQ(2,P,.351,"I")
 I DOD]"" S DOD=$$FMTHL7^XLFDT(DOD)
 D SET(.ARY,"PID",0)
 D SET(.ARY,1,1)
 D SET(.ARY,REC,3,1)
 D SET(.ARY,$E(AUTH,1,20),3,4,1)  ;2015 chit
 D SET(.ARY,NPI,3,4,2)  ;2015 chit
 D SET(.ARY,"NPI",3,4,3)  ;2015 chit
 D SET(.ARY,"MR",3,5)  ; Patient HRN
 D SET(.ARY,$S(NTYP]"":NTYP,1:"S"),5,7,,2)  ;this needs to be determined
 ;D SET(.ARY,DOB,7)
 D SET(.ARY,SEX,8)
 ;D SET(.ARY,RACE,10,1)
 ;D SET(.ARY,RACEE,10,2)
 ;I $G(RACE)]"" D SET(.ARY,"CDCREC",10,3)
 D SET(.ARY,ADD,11,1)
 D SET(.ARY,ADD2,11,2)
 D SET(.ARY,CITY,11,3)
 D SET(.ARY,STATE,11,4)
 D SET(.ARY,ZIP,11,5)
 D SET(.ARY,CNTRY,11,6)
 D SET(.ARY,CNTY,11,9)
 D SET(.ARY,$G(ETH),22,1)
 D SET(.ARY,$G(ETHE),22,2)
 I $G(ETH)]"" D SET(.ARY,"CDCREC",22,3)
 I EVN="A03" D
 . D SET(.ARY,DOD,29)
 . I DOD]"" D SET(.ARY,"Y",30)
 S X=$$ADDSEG^HLOAPI(.HLST,.ARY,.ERR)
 Q
