PATEXP ;-- ask the patient and visit date
 D GETPAT
 I APCDPAT="" W !!,"No PATIENT selected!" D EOJ Q
 D GETVISIT
 I APCDVSIT="" W !!,"No VISIT selected!" D EOJ Q
 F I="A04","A03","A08","A01" D MSG(I,APCDVSIT,APCDPAT)
