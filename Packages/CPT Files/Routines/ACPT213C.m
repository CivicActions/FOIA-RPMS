ACPT213C ;IHS/OIT/NKD - ACPT V2.13 CPT CATEGORY UPDATE 07/03/13 ;
 ;;2.13;CPT FILES;**2**;DEC 19, 2012;Build 1
 ;
 Q
UPDATE ; EP - PRIMARY UPDATE DRIVER - CATEGORY
 N ACPTT,ACPTA,ACPTC
 F ACPTT="CAT"  D
 . Q:'$D(^ACPT(ACPTT))
 . F ACPTA="C"  D
 . . Q:'$D(^ACPT(ACPTT,ACPTA))
 . . D BMES^XPDUTL($G(^ACPT(ACPTT,ACPTA,0))_" "_$S(ACPTA="C":"Adding/Modifying",1:"")_" Categories:")
 . . D MES^XPDUTL($J("",2)_"NAME"_$J("",50)_"NEW TYP SRC BEGIN END")
 . . D MES^XPDUTL($J("",2)_"===="_$J("",50)_"=== === === ===== =====")
 . . S ACPTC=0 F  S ACPTC=$O(^ACPT(ACPTT,ACPTA,ACPTC)) Q:'ACPTC  D
 . . . D CAT(ACPTA,$G(^ACPT(ACPTT,ACPTA,ACPTC)))
 Q
 ;
CAT(ACPTA,ACPTD) ; CATEGORY DRIVER
 N ACPTIEN,ACPTNAME,ACPTCAT,ACPTSRC,ACPTBEG,ACPTEND,ACPTPNM,ACPTMAJ,ACPTRES,ACPTCNT,ACPTCHG
 S ACPTNAME=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",1)))
 S ACPTCAT=$P(ACPTD,U,2)
 S ACPTSRC=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",3)))
 S ACPTBEG=$P(ACPTD,U,4)
 S ACPTEND=$P(ACPTD,U,5)
 S ACPTPNM=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",6)))
 S ACPTMAJ=$S(ACPTCAT="s":$$CATFIND(ACPTBEG,"M"),1:"") ; FIND MAJOR CATEGORY (IF SUB)
 S ACPTIEN=""
 ; CATEGORY SEARCH BY NAME THEN PREVIOUS NAME
 D FIND^DIC(81.1,"","@;.01","PX",$E(ACPTNAME,1,63),,"B",,,"ACPTRES")
 S ACPTCNT=$P($G(ACPTRES("DILIST",0)),U,1)
 S ACPTIEN=$S(ACPTCNT=1:$P($G(ACPTRES("DILIST",1,0)),U,1),ACPTCNT>1:$P($G(ACPTRES("DILIST",$P($G(ACPTRES("DILIST",0)),U,1),0)),U,1),1:"")
 I ACPTIEN']"" D
 . N ACPTRES,ACPTCNT
 . D FIND^DIC(81.1,"","@;.01","PX",$E(ACPTPNM,1,63),,"B",,,"ACPTRES")
 . S ACPTCNT=$P($G(ACPTRES("DILIST",0)),U,1)
 . S ACPTIEN=$S(ACPTCNT=1:$P($G(ACPTRES("DILIST",1,0)),U,1),ACPTCNT>1:$P($G(ACPTRES("DILIST",$P($G(ACPTRES("DILIST",0)),U,1),0)),U,1),1:"")
 S ACPTCHG=""
 D UPD811,DIS811
 Q
 ;
UPD811 ; PRIMARY UPDATER TO CPT CATEGORY FILE
 N FDA,NEWIEN,ACPTRES,ACPTCNT
 I ACPTIEN']"" D  ; If there isn't one, create it
 . K FDA,NEWIEN
 . S FDA(81.1,"+1,",.01)=ACPTNAME ; Name (.01)
 . D UPDATE^DIE(,"FDA","NEWIEN") ; Add the entry
 . S ACPTIEN=NEWIEN(1),ACPTCHG="N"
 Q:'ACPTIEN
 ;
 S FDA(81.1,ACPTIEN_",",.01)=ACPTNAME ; CATEGORY NAME (.01)
 S FDA(81.1,ACPTIEN_",",2)=ACPTCAT ; TYPE OF CATEGORY (2)
 S FDA(81.1,ACPTIEN_",",3)=$S(ACPTMAJ]"":ACPTMAJ,1:"@") ; MAJOR CATEGORY (3)
 S FDA(81.1,ACPTIEN_",",4)=ACPTBEG ; BEGIN CPT RANGE (4)
 S FDA(81.1,ACPTIEN_",",5)=ACPTEND ; END CPT RANGE (5)
 S FDA(81.1,ACPTIEN_",",6)=ACPTSRC ; SOURCE (6)
 D UPDATE^DIE(,"FDA")
 Q
 ;
DIS811 ; DISPLAY CPT CATEGORY ENTRY
 N ACPTDISP
 S:ACPTCAT="m" ACPTDISP=$E(ACPTNAME,1,50)_$J("",54-$L($E(ACPTNAME,1,50)))
 S:ACPTCAT="s" ACPTDISP="  "_$E(ACPTNAME,1,48)_$J("",52-$L($E(ACPTNAME,1,48)))
 S ACPTDISP=ACPTDISP_ACPTCHG_$J("",4-$L(ACPTCHG))_$$UP^XLFSTR(ACPTCAT)_$J("",4-$L(ACPTCAT))_ACPTSRC_$J("",4-$L(ACPTSRC))
 S ACPTDISP=ACPTDISP_ACPTBEG_$J("",6-$L(ACPTBEG))_ACPTEND
 D MES^XPDUTL($J("",2)_ACPTDISP)
 Q
 ;
CATFIND(ACPTC,ACPTCAT) ; EP - FIND CATEGORY FOR A GIVEN CPT/HCPCS CODE
 ; ACPTC=CPT/HCPCS CODE     ACPTCAT=S:SUB,M:MAJOR,BLANK:BOTH
 N ACPTN,ACPTRES,ACPTR,ACPTRI,ACPTIND,ACPTCNT
 S ACPTN=$$NUM^ICPTAPIU(ACPTC)
 S:'$D(ACPTCAT) ACPTCAT="S^M" ; IF NO CATEGORY SPECIFIED, SEARCH BOTH
 S (ACPTRES,ACPTR)=""
 F ACPTCNT=1:1:$L(ACPTCAT,U) Q:ACPTRES]""  D
 .S ACPTIND=$S($P(ACPTCAT,U,ACPTCNT)="S":"R",1:"M") ; CONVERT CATEGORY TYPE TO RANGE X-REF
 .S (ACPTRES,ACPTR)=""
 .; ADD .01 TO NUMERIC CODE AND SEARCH LAST 2 CATEGORY RANGES
 .S ACPTR=$O(^DIC(81.1,ACPTIND,ACPTN+.01),-1),ACPTRI=$S(ACPTR]"":$O(^DIC(81.1,ACPTIND,ACPTR,0)),1:"")
 .S:ACPTRI]"" ACPTRES=$S($$CATCHK(ACPTC,ACPTRI):ACPTRI,1:"")
 .Q:ACPTRES]""
 .S:ACPTR]"" ACPTR=$O(^DIC(81.1,ACPTIND,ACPTR),-1),ACPTRI=$S(ACPTR]"":$O(^DIC(81.1,ACPTIND,ACPTR,0)),1:"")
 .S:ACPTRI]"" ACPTRES=$S($$CATCHK(ACPTC,ACPTRI):ACPTRI,1:"")
 .Q:ACPTRES]""
 .S ACPTR=""
 .; SUBTRACT .01 FROM NUMERIC CODE AND SEARCH NEXT 2 CATEGORY RANGES
 .S ACPTR=$O(^DIC(81.1,ACPTIND,ACPTN-.01)),ACPTRI=$S(ACPTR]"":$O(^DIC(81.1,ACPTIND,ACPTR,0)),1:"")
 .S:ACPTRI]"" ACPTRES=$S($$CATCHK(ACPTC,ACPTRI):ACPTRI,1:"")
 .Q:ACPTRES]""
 .S:ACPTR]"" ACPTR=$O(^DIC(81.1,ACPTIND,ACPTR)),ACPTRI=$S(ACPTR]"":$O(^DIC(81.1,ACPTIND,ACPTR,0)),1:"")
 .S:ACPTRI]"" ACPTRES=$S($$CATCHK(ACPTC,ACPTRI):ACPTRI,1:"")
 Q ACPTRES
 ;
CATCHK(ACPTC,ACPTCI) ; CHECK IF CODE FALLS WITHIN CATEGORY RANGE
 ; ACPTC=CPT/HCPCS CODE    ACPTCI=CATEGORY IEN
 N ACPTN,ACPTB,ACPTE
 S ACPTN=$$NUM^ICPTAPIU(ACPTC) ; CONVERT CODE TO NUMERIC
 S ACPTB=$$NUM^ICPTAPIU($$GET1^DIQ(81.1,ACPTCI,4)) ; CONVERT BEGIN CODE TO NUMERIC
 S ACPTE=$$NUM^ICPTAPIU($$GET1^DIQ(81.1,ACPTCI,5)) ; CONVERT END CODE TO NUMERIC
 Q:(ACPTB=ACPTN)!(ACPTE=ACPTN) 1 ; RETURN 1 IF CODE = BEGIN OR END CODE
 Q ((ACPTB<ACPTN)&(ACPTN<ACPTE)) ; RETURN 1 IF BEGIN<CODE<END, 0 OTHERWISE
 ;
ASSIGN ; EP - ATTEMPT TO ASSIGN ALL CPT CODES TO A CATEGORY
 N ACPTI,ACPTR
 S (ACPTI,ACPTR("TOT"),ACPTR("UPD"),ACPTR("SUB"),ACPTR("MAJ"))=0
 D BMES^XPDUTL("ADDING/UPDATING ALL CPT CODES WITH CATEGORIES...")
 F  S ACPTI=$O(^ICPT(ACPTI)) Q:('ACPTI)  D
 . N ACPTC,ACPTCS,ACPTCP,ACPTCA,FDA
 . Q:'$D(^ICPT(ACPTI,0))
 . S ACPTC=$$GET1^DIQ(81,ACPTI,.01,"I")
 . Q:ACPTC']""
 . S ACPTCP=$$GET1^DIQ(81,ACPTI,3,"I"),ACPTCA=$$CATFIND(ACPTC) ; SET PREVIOUS AND AFTER CATEGORIES
 . ; SPECIAL CASE FOR THESE RE-SEQUENCED CODES
 . F ACPTCS=99224,99225,99226 S:ACPTC=ACPTCS ACPTCA=$O(^DIC(81.1,"B","HOSPITAL OBSERVATION SERVICES",0))
 . S FDA(81,ACPTI_",",3)=$S(ACPTCA]"":ACPTCA,1:"@")
 . D UPDATE^DIE(,"FDA")
 . ; CALCULATE RESULTS
 . S ACPTR("TOT")=ACPTR("TOT")+1
 . S:((ACPTCA]"")&(ACPTCP'=ACPTCA)) ACPTR("UPD")=ACPTR("UPD")+1
 . S:($$GET1^DIQ(81.1,ACPTCA,2,"I")="s") ACPTR("SUB")=ACPTR("SUB")+1
 . S:($$GET1^DIQ(81.1,ACPTCA,2,"I")="m") ACPTR("MAJ")=ACPTR("MAJ")+1
 . I ACPTR("TOT")#5000=0 W "."
 D MES^XPDUTL("TOTAL CODES SCANNED: "_ACPTR("TOT"))
 D MES^XPDUTL("            UPDATED: "_ACPTR("UPD")_$S(ACPTR("UPD")=0:" (ALL ARE CURRENT)",1:""))
 D MES^XPDUTL("ASSIGNMENT CATEGORY (MAJOR): "_ACPTR("MAJ"))
 D MES^XPDUTL("                      (SUB): "_ACPTR("SUB"))
 Q
CATCLN ; REMOVE ALL CPT RANGES FROM CATEGORIES
 ; NO "INACTIVATE" IN FILE WHICH MEANS INVALID CATEGORIES DISRUPT VALID RANGES
 N ACPTI
 D BMES^XPDUTL("Cleaning CPT Ranges from existing Categories.")
 S ACPTI=0 F  S ACPTI=$O(^DIC(81.1,ACPTI)) Q:'ACPTI  D
 . Q:'$D(^DIC(81.1,ACPTI,0))
 . N FDA
 . S FDA(81.1,ACPTI_",",4)="@"
 . S FDA(81.1,ACPTI_",",5)="@"
 . S FDA(81.1,ACPTI_",",9999999.03)="@"
 . D UPDATE^DIE(,"FDA")
 Q
CATRNDX ; COMPLETE RE-INDEX OF FILE 81.1
 N IND,DA,DIK
 D BMES^XPDUTL("Reindexing CPT Category file (81.1); this will take awhile.")
 K DA,DIK
 F IND="ACPT","B","C","M","R" K ^DIC(81.1,IND)
 S DIK="^DIC(81.1,"
 D IXALL^DIK
 Q
