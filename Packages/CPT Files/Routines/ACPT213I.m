ACPT213I ;IHS/OIT/NKD - ACPT V2.13 INCREMENTAL CPT UPDATE 12/19/12 ;
 ;;2.13;CPT FILES;**1,2**;DEC 19, 2012;Build 1
 ; 2/6/13 - ACPT*2.13*1 - ADDED SPECIAL CASE FOR 'RI' MODIFIER
 ; 07/03/13 - ACPT*2.13*2 - ADDED SPECIAL CASE FOR 'K0008' AND 'K0013' HCPCS
 ;                        - ADDED SPECIAL CASE FOR 'JE' MODIFIER
 ;
 Q
UPDATE ; EP - PRIMARY UPDATE DRIVER - INCREMENTAL
 N ACPTT,ACPTA,ACPTC
 F ACPTT="CPT","HCPCS"  D
 . Q:'$D(^ACPT(ACPTT))
 . F ACPTA="A","C","D","M"  D
 . . Q:'$D(^ACPT(ACPTT,ACPTA))
 . . D BMES^XPDUTL($G(^ACPT(ACPTT,ACPTA,0))_" "_ACPTT_" "_$S(ACPTA="A":"Adding",ACPTA="C":"Modifying",ACPTA="D":"Deleting",1:"Modifier")_" Codes:")
 . . D MES^XPDUTL($J("",2)_"CODE    SHORT DESCRIPTION"_$J("",16)_"NEW STATUS SHORT LONG")
 . . D MES^XPDUTL($J("",2)_"====    ================="_$J("",16)_"=== ====== ===== ====")
 . . S ACPTC=0 F  S ACPTC=$O(^ACPT(ACPTT,ACPTA,ACPTC)) Q:'ACPTC  D
 . . . D @(ACPTT)(ACPTA,$G(^ACPT(ACPTT,ACPTA,ACPTC)))
 Q
 ;
CPT(ACPTA,ACPTD) ; CPT DRIVER
 I ACPTA="M" D CPTM(ACPTD)  Q  ; CPT Modifier
 ; Input transforms
 N ACPTIEN,ACPTCODE,ACPTNAME,ACPTDESC,ACPTADT,ACPTEDT,ACPTTDT,ACPTCNT,ACPTRES,ACPTCHG
 S ACPTCODE=$P(ACPTD,"^",1)
 S ACPTNAME=$$UP^XLFSTR($TR($$CLEAN^ACPTUTL($P(ACPTD,"^",2)),";",","))
 S ACPTDESC=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",3)))
 S ACPTADT=$S($P(ACPTD,"^",4)]"":$P(ACPTD,"^",4)-17000000,1:"")
 S ACPTEDT=$S($P(ACPTD,"^",5)]"":$P(ACPTD,"^",5)-17000000,1:"")
 S ACPTTDT=$S($P(ACPTD,"^",6)]"":$P(ACPTD,"^",6)-17000000,1:"")
 S:((ACPTA="D")&(ACPTTDT']"")) ACPTTDT=ACPTEDT
 ; CPT search
 S ACPTIEN=""
 D FIND^DIC(81,,"@;.01;2;8I","OPX",ACPTCODE,,,"I ($P(^(0),U,4)'=1)",,"ACPTRES") ; Search actives
 S ACPTCNT=$P($G(ACPTRES("DILIST",0)),"^",1)
 S:ACPTCNT=0 ACPTIEN="" ; No matches found, create new
 S:ACPTCNT=1 ACPTIEN=$P($G(ACPTRES("DILIST",1,0)),"^",1) ; One match found, store IEN
 S:ACPTCNT>1 ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1) ; Multiple matches found, use the last
 I 'ACPTIEN D
 . K ACPTRES,ACPTCNT
 . D FIND^DIC(81,,"@;.01;2;8I","OPX",ACPTCODE,,,,,"ACPTRES") ; Include inactives
 . S ACPTCNT=$P($G(ACPTRES("DILIST",0)),"^",1)
 . S:ACPTCNT=0 ACPTIEN="" ; No matches found, create new
 . S:ACPTCNT=1 ACPTIEN=$P($G(ACPTRES("DILIST",1,0)),"^",1) ; One match found, store IEN
 . S:ACPTCNT>1 ACPTIEN=$P($G(ACPTRES("DILIST",1,0)),"^",1) ; Multiple matches found, use the first
 K ACPTRES
 ;
 I ((ACPTIEN']"")&(ACPTA="D")) D MES^XPDUTL($J("",2)_ACPTCODE_$J("",8-$L(ACPTCODE))_"Couldn't find code to inactivate") Q
 S ACPTCHG=""
 D UPD81^ACPT213U,DIS81^ACPT213U
 Q
 ;
HCPCS(ACPTA,ACPTD) ; HCPCS DRIVER
 ; Input transforms
 N ACPTIEN,ACPTCODE,ACPTNAME,ACPTDESC,ACPTADT,ACPTEDT,ACPTTDT,ACPTCNT,ACPTRES,ACPTCHG
 S ACPTCODE=$P(ACPTD,"^",1)
 S ACPTNAME=$$CLEAN^ACPTUTL($P(ACPTD,"^",2))
 S ACPTDESC=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",3)))
 S ACPTADT=$S($P(ACPTD,"^",4)]"":$P(ACPTD,"^",4)-17000000,1:"")
 S ACPTEDT=$S($P(ACPTD,"^",5)]"":$P(ACPTD,"^",5)-17000000,1:"")
 S ACPTTDT=$S($P(ACPTD,"^",6)]"":$P(ACPTD,"^",6)-17000000,1:"")
 I ACPTA="M" D HCPCSM(ACPTD) Q
 ; HCPCS search
 S ACPTIEN=""
 D FIND^DIC(81,"","@;.01;2;8I","OPX",ACPTCODE,"","","","","ACPTRES")
 S ACPTCNT=$P($G(ACPTRES("DILIST",0)),"^",1)
 S:ACPTCNT=0 ACPTIEN="" ; No matches found, create new
 S:ACPTCNT=1 ACPTIEN=$P($G(ACPTRES("DILIST",1,0)),"^",1) ; One match found, store IEN
 I ACPTCNT>1 D
 . F ACPTCNT=1:1:$P($G(ACPTRES("DILIST",0)),"^",1) D  Q:ACPTIEN]""
 . . ; If the Active Date (8) matches the HCPCS Add Date, store IEN
 . . I $P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",4)=ACPTADT S ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1) Q
 . ; If no entries match Active Date, use most recently added
 . S:ACPTIEN']"" ACPTIEN=$P($G(ACPTRES("DILIST",$P($G(ACPTRES("DILIST",0)),"^",1),0)),"^",1)
 K ACPTRES
 ;
 ; If the code was added the same year as the install, a previous match was found, and the active dates are different,
 ; then ignore and create new
 ; Activates when codes are re-used and should NOT be linked to their previous definition
 I ((ACPTIEN]"")&(ACPTA'="D")) D
 . Q:((ACPTCODE="K0008")!(ACPTCODE="K0013"))  ; IHS/OIT/NKD ACPT*2.13*2 RE-ACTIVATES
 . S:($E(ACPTADT,1,3)=$E(ACPTYR,1,3))&(ACPTADT'=$P($G(^ICPT(ACPTIEN,0)),"^",8)) ACPTIEN=""
 ;
 I ((ACPTIEN']"")&(ACPTA="D")) D MES^XPDUTL($J("",2)_ACPTCODE_$J("",8-$L(ACPTCODE))_"Couldn't find code to inactivate") Q
 S ACPTCHG=""
 D UPD81^ACPT213U,DIS81^ACPT213U
 Q
 ;
CPTM(ACPTD) ; CPT MODIFIER DRIVER
 ; Input transforms
 N ACPTIEN,ACPTCODE,ACPTNAME,ACPTDESC,ACPTPNM,ACPTINAC,ACPTADT,ACPTEDT,ACPTTDT,ACPTCNT,ACPTRES,ACPTCHG
 S ACPTCODE=$P(ACPTD,"^",1)
 S ACPTNAME=$$UP^XLFSTR($TR($$CLEAN^ACPTUTL($P(ACPTD,"^",2)),";",","))
 S ACPTDESC=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",3)))
 S ACPTPNM=$$UP^XLFSTR($$CLEAN^ACPTUTL($P(ACPTD,"^",4)))
 S ACPTINAC=$S($P(ACPTD,"^",5)]"":$P(ACPTD,"^",5),1:0)
 S ACPTADT=""
 S ACPTEDT=ACPTYR
 S ACPTTDT=$S(ACPTINAC:ACPTYR,1:"")
 S:ACPTNAME']"" ACPTNAME=$E(ACPTDESC,1,60)
 ; CPT Modifier search
 S ACPTIEN=""
 D FIND^DIC(81.3,,"@;.01;.02;.04I","OPX",ACPTCODE,,,,,"ACPTRES")
 S ACPTCNT=$P($G(ACPTRES("DILIST",0)),"^",1)
 S:ACPTCNT=0 ACPTIEN="" ; No matches found, create new
 S:ACPTCNT=1 ACPTIEN=$P($G(ACPTRES("DILIST",1,0)),"^",1) ; One match found, store IEN
 I ACPTCNT>1 D
 . F ACPTCNT=1:1:$P($G(ACPTRES("DILIST",0)),"^",1) D  Q:ACPTIEN]""
 . . ; If the source doesn't match, skip
 . . Q:$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",4)'="C"
 . . I $$UP^XLFSTR($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",3))=ACPTNAME S ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1) Q
 . . I $$UP^XLFSTR($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",3))=ACPTPNM S ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1) Q
 . Q:ACPTIEN]""
 . I ACPTIEN']"" F ACPTCNT=1:1:$P($G(ACPTRES("DILIST",0)),"^",1) D  Q:ACPTIEN]""
 . . I $$UP^XLFSTR($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",3))=ACPTNAME S ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1) Q
 . . I $$UP^XLFSTR($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",3))=ACPTPNM S ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1) Q
 K ACPTRES
 ;
 I ((ACPTIEN']"")&(ACPTINAC)) D MES^XPDUTL($J("",2)_ACPTCODE_$J("",8-$L(ACPTCODE))_"Couldn't find code to inactivate") Q
 S ACPTCHG=""
 D UPD813^ACPT213U,DIS813^ACPT213U
 Q
 ;
HCPCSM(ACPTD) ; HCPCS MODIFIER DRIVER
 ; Input transforms
 N ACPTIEN,ACPTCNT,ACPTRES,FDA,NEWIEN,ACPTLST,ACPTDFR,ACPTDTO,ACPTNEW
 S ACPTNAME=$$UP^XLFSTR(ACPTNAME)
 ; CPT Modifier search
 S ACPTIEN=""
 D FIND^DIC(81.3,"","@;.01;.02;8I","OPX",ACPTCODE,"","","","","ACPTRES")
 S ACPTCNT=$P($G(ACPTRES("DILIST",0)),"^",1)
 S:ACPTCNT=0 ACPTIEN="" ; No matches found, create new
 S:ACPTCNT=1 ACPTIEN=$P($G(ACPTRES("DILIST",1,0)),"^",1)
 ; IHT/OIT/NKD ACPT*2.13*1 - SPECIAL CASE FOR 'RI' MODIFIER
 ;S:(ACPTCNT=1)&(ACPTCODE="RI") ACPTIEN=$S($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",4)=ACPTADT:ACPTIEN,1:"")
 ; IHT/OIT/NKD ACPT*2.13*2 - SPECIAL CASE FOR 'JE' MODIFIER
 S:(ACPTCNT=1)&(ACPTCODE="JE") ACPTIEN=$S($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",4)=ACPTADT:ACPTIEN,1:"")
 I ACPTCNT>1 D
 . F ACPTCNT=1:1:$P($G(ACPTRES("DILIST",0)),"^",1) D  Q:ACPTIEN]""
 . . S:$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",4)=ACPTADT ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1)
 . Q:ACPTIEN]""
 . I ACPTIEN']"" F ACPTCNT=1:1:$P($G(ACPTRES("DILIST",0)),"^",1) D  Q:ACPTIEN]""
 . . S:$$UP^XLFSTR($P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",3))=ACPTNAME ACPTIEN=$P($G(ACPTRES("DILIST",ACPTCNT,0)),"^",1)
 K ACPTRES
 ;
 I ((ACPTIEN']"")&(ACPTTDT]"")) D MES^XPDUTL($J("",2)_ACPTCODE_$J("",8-$L(ACPTCODE))_"Couldn't find code to inactivate") Q
 S ACPTCHG=""
 D UPD813^ACPT213U,DIS813^ACPT213U
 Q
