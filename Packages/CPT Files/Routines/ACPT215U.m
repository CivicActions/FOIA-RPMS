 . S ACPTDISP=ACPTDISP_$S(ACPTCHG["C":"C",1:" ")_$J("",3)
 . S ACPTDN=$S(ACPTNAME]"":$E(ACPTNAME,1,30),1:$$GET1^DIQ(81,ACPTIEN,2,"I"))
 . ;D RSLT^ACPTUTL($J("",2)_ACPTCODE_$J("",8-$L(ACPTCODE))_ACPTDN_$J("",33-$L(ACPTDN))_ACPTDISP)  ;IHS/OIT/NKD ACPT*2.14*2 - MODIFIED DISPLAY
