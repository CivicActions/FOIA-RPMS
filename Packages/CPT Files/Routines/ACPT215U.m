 . S ACPTDISP=ACPTDISP_$S(ACPTCHG["A":"A",ACPTCHG["I":"I",1:" ")_$J("",6)
 . S ACPTDISP=ACPTDISP_$S(ACPTCHG["S":"S",1:" ")_$J("",5)
 . S ACPTDISP=ACPTDISP_$S(ACPTCHG["L":"L",1:" ")_$J("",4)
