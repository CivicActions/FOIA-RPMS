POST ;EP - POST INSTALL ACTIONS
 ; VERIFY TASKS ARE UNSCHEDULED AND PRODUCTION IS STOPPED
 I $$TASKSCH("AGMP ACK BCKGRND TSK") D BMES^XPDUTL("  AGMP ACK BCKGRND TSK is scheduled.  Install aborted.") Q
 I $$TASKSCH("AGMP MPI PURGE HLO MSGS") D BMES^XPDUTL("  AGMP MPI PURGE HLO MSGS is scheduled.  Install aborted.") Q
 I $$PRODRUN() D BMES^XPDUTL("  IRIS production is still running.  Install aborted.") Q
 ;
 ; CONVERT HLO "IN" QUEUE
 ; THE QUEUE WAS ^HLB("QUEUE","IN",DATE,"RPMS-MPI",MSGTYPE,MSGEVENT,HLBIEN)
 ; BUT SHOULD BE ^HLB("QUEUE","IN",DATE,"RPMS-MPI",HLBIEN)
 N Q,DIR,LINK,DT,TYPE,EVENT,HLBIEN
 D BMES^XPDUTL(" Converting HLO 'IN' queue")
 S Q="QUEUE",DIR="IN",LINK="RPMS-MPI"
 S (DT,TYPE,EVENT,HLBIEN)=""
 F  S DT=$O(^HLB(Q,DIR,DT)) Q:DT=""  D
 . F  S TYPE=$O(^HLB(Q,DIR,DT,LINK,TYPE)) Q:TYPE=""  D
 . . F  S EVENT=$O(^HLB(Q,DIR,DT,LINK,TYPE,EVENT)) Q:EVENT=""  D
 . . . F  S HLBIEN=$O(^HLB(Q,DIR,DT,LINK,TYPE,EVENT,HLBIEN)) Q:HLBIEN=""  D
 . . . . I $D(^HLB(HLBIEN)) S ^HLB(Q,DIR,DT,LINK,HLBIEN)=""
 . . . . K ^HLB(Q,DIR,DT,LINK,TYPE,EVENT,HLBIEN)
 ;
 ; CONVERT MISSING ICN LIST
 ; THE LIST CHANGES FROM ^XTMP("AGMPICN",DFNIEN,FACIEN) TO ^XTMP("AGMPICN",DFNIEN)
 N DFN,FAC,H
 ; LOOP THE THROUGH EXISTING LIST TO GET THE NEWEST $H VALUE AND USE THAT FOR THE NEW NODE
 S (DFN,FAC)=""
 F  S DFN=$O(^XTMP("AGMPICN",DFN)) Q:DFN=""  D
 . S H=""
 . F  S FAC=$O(^XTMP("AGMPICN",DFN,FAC)) Q:FAC=""  D
 . . I $G(^XTMP("AGMPICN",DFN,FAC))>H S H=$G(^XTMP("AGMPICN",DFN,FAC))
 . . K ^XTMP("AGMPICN",DFN,FAC)
 . I $G(^XTMP("AGMPICN",DFN))>H S H=$G(^XTMP("AGMPICN",DFN))  ; IN CASE WE'RE REINSTALLING
 . I H="" S H=+$H
 . S ^XTMP("AGMPICN",DFN)=H
 ;
 ; SET MAX DAILY UPDATES IF NOT ALREADY SET
 N DIE,DA,DR
 I '$$GET1^DIQ(9002021.01,"1,",.04) S DIE=9002021.01,DA=1,DR=".04///5000" D ^DIE
 ;
 ; LOAD CLASSES
 N TRNM,TRIEN,ERR,I
 D BMES^XPDUTL(" Importing AGMPI classes")
 S TRNM=$G(@XPDGREF@("SENT REC"))
 I TRNM="" D BMES^XPDUTL("  Error 'import 1' occurred during post-install.  Install aborted.") Q
 S TRIEN=$O(^AGMPCLS("B",TRNM,""))
 I $G(TRIEN)="" D BMES^XPDUTL("  Error 'import 2' occurred during post-install.  Install aborted.") Q
 D IMPORT^AGMPCLAS(TRIEN,.ERR)
 I $G(ERR) D  Q
 . D BMES^XPDUTL("  Error 'import 3' occurred duing post-install. Install aborted.")
 . F I=1:1 Q:'$D(ERR(I))  D BMES^XPDUTL("  "_ERR(I))
 Q
 ;
TASKSCH(TASK) ; CHECK IF A TASK IS SCHEDULED
 N ROOT,RUN,X
 S RUN=0,X=""
 D OPTSTAT^XUTMOPT(TASK,.ROOT)
 F  S X=$O(ROOT(X)) Q:X=""  I $P($G(ROOT(X)),"^",2)'="" S RUN=1 Q
 Q RUN
 ;
PRODRUN() ; CHECK IF PRODUCTION IS RUNNING
 N NS,MPINS
 N @("$NAMESPACE")
 S @("NS=$NAMESPACE")
 S MPINS="AGMPI"_NS
 ; Verify namespace exists
 I @("'##class(%SYS.Namespace).Exists(MPINS)") Q 0
 S @("$NAMESPACE=MPINS")
 Q @("##class(Ens.Director).IsProductionRunning()")
