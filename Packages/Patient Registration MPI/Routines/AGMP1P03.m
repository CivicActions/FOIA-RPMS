PRE ;EP - PRE INSTALL
 Q
 ;
POST ;EP - POST INSTALL ACTIONS
 N TRNM,TRIEN,ERR,DIK,DA,DIE,DR,ENTRY,MIEN,OIEN
 ;
 ; LOAD CLASSES
 D BMES^XPDUTL(" Importing AGMPI classes")
 S TRNM=$G(@XPDGREF@("SENT REC"))
 I TRNM="" D BMES^XPDUTL("  Error 'import 1' occurred during post-install.  Install aborted.") Q
 S TRIEN=$O(^AGMPCLS("B",TRNM,""))
 I $G(TRIEN)="" D BMES^XPDUTL("  Error 'import 2' occurred during post-install.  Install aborted.") Q
 D IMPORT^AGMPCLAS(TRIEN,.ERR)
 I $G(ERR) D  Q
 . D BMES^XPDUTL("  Error 'import 3' occurred duing post-install. Install aborted.")
 . F I=1:1 Q:'$D(ERR(I))  D BMES^XPDUTL("  "_ERR(I))
 ;
 ; ADD THE AGMP APP STAT OPTION TO AGMP MPI RPTS AND DEBUG MENU
 S MIEN=$$LKOPT^XPDMENU("AGMP MPI RPTS AND DEBUG MENU")
 I MIEN="" D BMES^XPDUTL("  Unable to find AGMP MPI RPTS AND DEBUG MENU option") I 1
 E  D
 . ; CHECK IF THE OPTION IS ALREADY ON THE MENU
 . S OIEN=$$LKOPT^XPDMENU("AGMP APP STATUS")
 . I '$D(^DIC(19,MIEN,10,"B",OIEN)) D
 . . ; ADD THE OPTION TO THE MENU
 . . D BMES^XPDUTL(" Updating reports menu")
 . . I $$ADD^XPDMENU("AGMP MPI RPTS AND DEBUG MENU","AGMP APP STATUS","STAT",8) Q
 . . D BMES^XPDUTL("  Option AGMP APP STATUS not added to AGMP MPI RPTS AND DEBUG MENU")
 ;
 ; REMOVE THE AGMP HLO SEND A40 OPTION FROM AGMPMENU
 S MIEN=$$LKOPT^XPDMENU("AGMPMENU")
 I MIEN="" D BMES^XPDUTL("  Unable to find AGMPMENU option") I 1
 E  D
 . ; VERIFY THE OPTION IS ON THE MENU
 . S OIEN=$$LKOPT^XPDMENU("AGMP HLO SEND A40")
 . I $D(^DIC(19,MIEN,10,"B",OIEN)) D
 . . ; REMOVE THE OPTION FROM THE MENU
 . . D BMES^XPDUTL(" Updating MPI menu")
 . . I $$DELETE^XPDMENU("AGMPMENU","AGMP HLO SEND A40") Q
 . . D BMES^XPDUTL("  Option AGMP HLO SEND A40 not removed from AGMPMENU")
 Q
