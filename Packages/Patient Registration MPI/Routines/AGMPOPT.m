 ;;1.0;AGMP;**2,4**;Apr 30, 2021;Build 55
 ;
 Q
 ;
OPTOUT ; Entry point
 L +^AGMPOPT:1 E  Q  ; DON'T RUN IF AN OPT-IN/OPT-OUT JOB IS ALREADY RUNNING
 N LINK,IENB,DATE,TYPE,EVENT,DR,DIE,DA,DIK,IEN,TASK,NS
 ;
 ; SET MAS PARAMETERS TO STOP ADDING ENTRIES TO PIVOT TABLE
 S DIE=43,DA=1,DR="391.7013////0" D ^DIE
 ;
 ; UNSCHEDULE TASKS
 F TASK="AGMP ACK BCKGRND TSK","AGMP MPI MISSING ICN TSK","AGMP A08 BCKGRND UPDATE TSK","AGMP MPI PURGE HLO MSGS","VAFH PIVOT PURGE" D
 . S IEN=$O(^DIC(19,"B",TASK))
 . I IEN S DIK="^DIC(19.2,",DA=0 F  S DA=$O(^DIC(19.2,"B",IEN,DA)) Q:'DA  D ^DIK
 ;
 ; SCHEDULE ONE LAST SET OF PURGE TASKS FOR SUNDAY AT LEAST 8 DAYS IN THE FUTURE
 W !!,"Scheduling one last set of purge tasks:"
 F TASK="AGMP MPI PURGE HLO MSGS","VAFH PIVOT PURGE" D
 . W !,TASK,": " D RESCH^XUTMOPT(TASK,$$SCHED("7D",$H+8_","_$P($H,",",2)))
 ;
 ; STOP ENSEMBLE PRODUCTION
 S @("NS=""AGMPI""_$NAMESPACE")
 I @("##class(%SYS.Namespace).Exists("""_NS_""")") D
 . N @("$NAMESPACE")  ; THIS WILL REVERT WHEN EXITING THE DOT STRUCTURE
 . S @("$NAMESPACE=NS")
 . ; STOP THE PRODUCTION
 . D @("##class(Ens.Director).StopProduction(30,1)")
 . ; REMOVE AUTO-START PRODUCTION
 . D @("##class(Ens.Director).SetAutoStart("""")")
 ;
 ; CLEAR HLO OUTBOUND QUEUE
 S (LINK,IENB)=""
 F  S LINK=$O(^HLB("QUEUE","OUT",LINK)) Q:LINK=""  D
 . F  S IENB=$O(^HLB("QUEUE","OUT",LINK,"MPI RPMS",IENB)) Q:IENB=""  D
 . . I $G(^HLB(IENB,0))'="" D MSGCMPLT^AGMPBGP(IENB) S $P(^HLB(IENB,0),U,16)=$$NOW^XLFDT  ; MARK COMPLETE
 . . K ^HLB("QUEUE","OUT",LINK,"MPI RPMS",IENB)  ; REMOVE FROM QUEUE
 . . I $G(^HLC("QUEUECOUNT","OUT",LINK,"MPI RPMS")) S ^HLC("QUEUECOUNT","OUT",LINK,"MPI RPMS")=^HLC("QUEUECOUNT","OUT",LINK,"MPI RPMS")-1  ; DECREMENT COUNTER
 ;
 ; CLEAR HLO INBOUND QUEUE
 S (FROM,IENB)=""
 F  S FROM=$O(^HLB("QUEUE","IN",FROM)) Q:FROM=""  D
 . F  S IENB=$O(^HLB("QUEUE","IN",FROM,"RPMS-MPI",IENB)) Q:IENB=""  D
 . . D MSGCMPLT^AGMPBGP(IENB)  ; MARK COMPLETE
 . . K ^HLB("QUEUE","IN",FROM,"RPMS-MPI",IENB)  ; REMOVE FROM QUEUE
 ;
 ; CLEAR PIVOT TABLE
 S IEN=0
 F  S IEN=$O(^VAT(391.71,"AXMIT",4,IEN)) Q:'IEN  D SETXMIT^AGMPPIVT(IEN)
 ;
 ; CLEAR ICNS FROM THE VA PATIENT FILE
 S DIE=2,DR="991.01///@;991.02///@",DA=0
 F  S DA=$O(^DPT(DA)) Q:'DA  I $D(^DPT(DA,"MPI")) D ^DIE
 ;
 ; CLEAR TREATING FACILITY LIST
 S DIK="^DGCN(391.91,",DA=0
 F  S DA=$O(^DGCN(391.91,DA)) Q:'DA  D ^DIK
 ;
 L -^AGMPOPT
 Q
 ;
OPTIN ; Entry point
 L +^AGMPOPT:1 E  Q  ; DON'T RUN IF AN OPT-IN/OPT-OUT JOB IS ALREADY RUNNING
 N DIE,DA,DR,NS
 ;
 ; SET MAS PARAMETERS TO START ADDING ENTRIES TO PIVOT TABLE
 S DIE=43,DA=1,DR="391.7013////1" D ^DIE
 ;
 ; SCHEDULE TASKS
 W !!,"Scheduling MPI tasks:"
 W !,"AGMP ACK BCKGRND TSK: " D RESCH^XUTMOPT("AGMP ACK BCKGRND TSK",$$SCHED("5M"),,"300S")
 W !,"AGMP MPI MISSING ICN TSK: " D RESCH^XUTMOPT("AGMP MPI MISSING ICN TSK",$$SCHED("1D"),,"1D")
 W !,"AGMP A08 BCKGRND UPDATE TSK: " D RESCH^XUTMOPT("AGMP A08 BCKGRND UPDATE TSK",$$SCHED("5M"),,"300S")
 W !,"AGMP MPI PURGE HLO MSGS: " D RESCH^XUTMOPT("AGMP MPI PURGE HLO MSGS",$$SCHED("7D"),,"7D")
 W !,"VAFH PIVOT PURGE: " D RESCH^XUTMOPT("VAFH PIVOT PURGE",$$SCHED("7D"),,"7D")
 ;
 ; START ENSEMBLE PRODUCTION
 S @("NS=""AGMPI""_$NAMESPACE")
 I @("##class(%SYS.Namespace).Exists("""_NS_""")") D
 . N @("$NAMESPACE")  ; THIS WILL REVERT WHEN EXITING THE DOT STRUCTURE
 . S @("$NAMESPACE=NS")
 . ; START THE PRODUCTION
 . D @("##class(Ens.Director).StartProduction(""AGMPI.PatientRegistration"")")
 . ; SET AUTO-START PRODUCTION
 . D @("##class(Ens.Director).SetAutoStart(""AGMPI.PatientRegistration"")")
 ;
 L -^AGMPOPT
 Q
 ;
 ; DETERMINE THE TIME TO SCHEDULE A TASK BASED ON THE RESCHEDULING FREQUENCY
 ; ADD 2.5 MINUTES, THEN ROUND UP TO:
 ;   5M - THE NEXT MULTIPLE OF 5 MINUTES
 ;   1D - THE NEXT TIME IT'S 11:00 PM
 ;   7D - THE NEXT SUNDAY AT 8:00 AM
 ;   ANY OTHER FREQUENCY - NO ADDITIONAL TIME ADDED
SCHED(FREQ,STARTH) ;
 N DATEH,TIMEH,DATES,TIMES,X
 I $G(FREQ)="" Q ""
 I $G(STARTH)="" S STARTH=$H
 S DATEH=$P(STARTH,","),TIMEH=$P(STARTH,",",2)
 S TIMES=TIMEH+150,DATES=DATEH  ; ADD 2.5 MIN
 I FREQ="5M" D
 . F  Q:'(TIMES#300)  S TIMES=TIMES+1  ; KEEP INCREMENTING UNTIL WE GET TO A MULTIPLE OF 5 MINUTES
 . I TIMES'<86400 S TIMES=TIMES-86400,DATES=DATES+1  ; IF IT PASSED INTO THE NEXT DAY
 I FREQ="1D" D
 . I TIMES>82800 S DATES=DATES+1  ; IF IT'S AFTER 11:00 PM, SCHEDULE FOR THE NEXT DAY
 . S TIMES=82800  ; SET FOR 11:00 PM
 I FREQ="7D" D
 . I TIMES>28800 S DATES=DATES+1  ; IF IT'S AFTER 8:00 AM, START WITH TOMORROW
 . F  S X=$$HTFM^XLFDT(DATES,1) D DW^%DTC Q:X="SUNDAY"  S DATES=DATES+1  ; SCHEDULE FOR THE NEXT SUNDAY
 . S TIMES=28800   ; SET FOR 8:00 AM
 Q $$HTFM^XLFDT(DATES_","_TIMES)
 ;
ENABLED(DISPMSG) ;
 I '$$GET1^DIQ(9002021.01,"1,",.02,"I") W:$G(DISPMSG) !!,"  *** This application has been disabled. ***" Q 0
 Q 1
