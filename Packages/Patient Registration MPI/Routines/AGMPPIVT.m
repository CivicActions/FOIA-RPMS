AGMPPIVT ; IHS/SD/TPF - MPI PIVOT TABLE BACKGROUND TASK; JAN 07, 2011
 ;;1.0;AGMP;**2,3,4**;Apr 30, 2021;Build 55
 ;
 ;RTN RUNS IN BACKGROUND AND TRAVERSES THE ADT/HL PIVOT FILE
 ;ANY RECORDS WITH FIELD 'REQUIRES TRANSMISSION' SET TO 'YES' 
A08UPDT ;EP - SEND A08 MESSAGES TO MPI
 I '$$ENABLED^AGMPOPT() Q  ; CHECK WHETHER MPI HAS BEEN DISABLED
 D UPDATE^AGMPCHK
 L +^AGMPPIVT:5 Q:'$T  ;DO NOT START IF ALREADY RUNNING
 N PIVOT,TYPE,DFN,HLMSG,TEMPDUZ2,MAX,SENT,H
 S TEMPDUZ2=DUZ(2)
 S TYPE=4  ;THIS IS NOT AN HL7 EVENT
 ;FOR LOOP TO DO OTHER TYPES IN FUTURE?
 ;4 = ONLY DO REGISTRATION UPDATES
 S ^XTMP("AGMPPIVT",0)=$$FMADD^XLFDT(DT,58)_U_DT_U_"AGMP A08 BCKGRND UPDATE TSK"
 S SENT=+$G(^XTMP("AGMPPIVT","SENT",+$H))
 S MAX=$$GET1^DIQ(9002021.01,"1,",.04) I +MAX=0 S MAX=5000
 S PIVOT=""
 F  S PIVOT=$O(^VAT(391.71,"AXMIT",4,PIVOT)) Q:'PIVOT!(SENT'<MAX)  D
 .S DFN=$P($G(^VAT(391.71,PIVOT,0)),U,3)  ;PATIENT
 .;I '$$XMITPAT^AGMPSMSG(DFN) Q  ;DEMO PATIENT CHECK
 .I $$DEMOPAT^AGMPSMSG(DFN) D SETXMIT(PIVOT) Q  ;DEMO PATIENT CHECK
 .S DUZ(2)=$P($G(^VAT(391.71,PIVOT,9999999)),U)
 .I DUZ(2)=""!(DUZ(2)=0) D  Q   ;IF INVALID, REMOVE IT AND PROCESS THE NEXT ONE
 ..D SETXMIT(PIVOT)
 ..;D NOTIF^AGMPHLO(DFN,"PIVOT="_PIVOT_" DUZ2="_$P($G(^VAT(391.71,PIVOT,9999999)),U))
 ..;THIS IS SOME TYPE OF TIMING ISSUE. MARK WILLIAMS IS INVESTIGATING
 ..;VAFCDD01 DOES NOT POPULATE DUZ2 IN ADT/HL7 PIVOT FILE
 .D SETXMIT(PIVOT)
 .;KICK OFF PROTOCOL
 .S X="AG UPDATE A PATIENT",DIC=101,INDA=DFN
 .D EN^XQOR
 .S SENT=SENT+1
 S DUZ(2)=TEMPDUZ2  ;RESTORE TASK TO ORIGINAL DUZ(2)
 S ^XTMP("AGMPPIVT","SENT",+$H)=SENT
 S H=+$H F  S H=$O(^XTMP("AGMPPIVT","SENT",H),-1) Q:'H  K ^XTMP("AGMPPIVT","SENT",H)  ;CLEAN UP OLD "SENT" COUNTS
 L -^AGMPPIVT  ;CLEAR RUNNING FLAG
 Q
 ;
SETXMIT(PIVOT) ;EP - RESET TRANSMIT FLAG
 K DIE,DIR,DIC,DA,DR
 S DIE="^VAT(391.71,"
 S DA=PIVOT
 S DR=".06///1;.08///0"  ;SET AS TRANSMITED AND NO TRANSMISSION REQUIRED
 D ^DIE
 Q
