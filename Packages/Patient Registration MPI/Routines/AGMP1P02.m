PRE ;EP - PRE INSTALL
 Q
 ;
POST ;EP - POST INSTALL ACTIONS
 N TRNM,TRIEN,ERR,DIK,DA,DIE,DR,ENTRY,MIEN,OIEN
 ;
 ;LOAD CLASSES
 ;D BMES^XPDUTL("  Importing AGMPI classes")
 ;S TRNM=$G(@XPDGREF@("SENT REC"))
 ;I TRNM="" D BMES^XPDUTL("  Error 'import 1' occurred during post-install.  Install aborted.") Q
 ;S TRIEN=$O(^AGMPCLS("B",TRNM,""))
 ;I $G(TRIEN)="" D BMES^XPDUTL("  Error 'import 2' occurred during post-install.  Install aborted.") Q
 ;D IMPORT^AGMPCLAS(TRIEN,.ERR)
 ;I $G(ERR) D  Q
 ;. D BMES^XPDUTL("  Error 'import 3' occurred duing post-install. Install aborted.")
 ;. F I=1:1 Q:'$D(ERR(I))  D BMES^XPDUTL("  "_ERR(I))
 ;
 ; DELETE THE TREATING FACILITY LIST (391.91)
 D BMES^XPDUTL("  Deleting the Treating Facility List")
 S DIK="^DGCN(391.91,",DA=0
 F  S DA=$O(^DGCN(391.91,DA)) Q:'DA  D ^DIK
 ;
 ; CONVERT THE AGMPZMGR SECURITY KEY TO AGMPZMENU
 D BMES^XPDUTL("  Updating security key")
 I $$LKUP^XPDKEY("AGMPZMENU")'="" D  I 1
 . I $$LKUP^XPDKEY("AGMPZMGR") D BMES^XPDUTL("  Unable to rename AGMPZMGR security key - AGMPZMENU already exists")
 E  D
 . I '$$RENAME^XPDKEY("AGMPZMGR","AGMPZMENU") D BMES^XPDUTL("  Error converting security key AGMPZMGR to AGMPZMENU") Q
 . D BMES^XPDUTL("  AGMPZMGR renamed AGMPZMENU")
 ;
 ; RENAME THE AGMP HLO MPI MANAGER OPTIONS MENU TO AGMPMENU
 D BMES^XPDUTL("  Updating menu")
 I $$LKOPT^XPDMENU("AGMPMENU")'="" D  I 1
 . I $$LKOPT^XPDMENU("AGMP HLO MPI MANAGER OPTIONS")'="" D BMES^XPDUTL("  Unable to rename AGMP HLO MPI MANAGER OPTIONS menu - AGMPMENU already exists")
 E  D RENAME^XPDMENU("AGMP HLO MPI MANAGER OPTIONS","AGMPMENU") D BMES^XPDUTL("  AGMP HLO MPI MANAGER OPTIONS renamed AGMPMENU")
 ;
 ; UPDATE ENTRY AND EXIT OPTIONS
 D
 . S DIE="^DIC(19,",DA=$$LKOPT^XPDMENU("AGMPMENU")
 . I DA="" D BMES^XPDUTL("  Error updating ENTRY and EXIT ACTION in AGMPMENU") Q
 . S ENTRY=$$GET1^DIQ(19,DA,20)
 . I ENTRY'="" X "S ENTRY=$REPLACE(ENTRY,""AGMP HLO MPI MANAGER OPTIONS"",""AGMPMENU"")"
 . E  S ENTRY="S AGMP(""RPT"")="""",X=""AGMPMENU"" D HDR^AGMPRPT2"
 . S DR="15///@;20///"_ENTRY
 . D ^DIE
 ;
 ; REMOVE DEPRECATED MENU OPTIONS
 D BMES^XPDUTL("  Removing deprecated menu options")
 D
 . S MIEN=$$LKOPT^XPDMENU("AGMPMENU"),OIEN=$$LKOPT^XPDMENU("AGMP HLO PROCESS MFN")
 . I MIEN="" D BMES^XPDUTL("  Could not find AGMPMENU") Q
 . I OIEN="" D BMES^XPDUTL("  Could not find AGMP HLO PROCESS MFN") Q
 . I '$D(^DIC(19,MIEN,10,"B",OIEN)) Q  ; ALREADY NOT ON MENU SO QUIT
 . I $$DELETE^XPDMENU("AGMPMENU","AGMP HLO PROCESS MFN") D BMES^XPDUTL("  Option AGMP HLO PROCESS MFN successfully removed") Q
 . D BMES^XPDUTL("  Option AGMP HLO PROCESS MFN not removed from AGMPMENU")
 D
 . S MIEN=$$LKOPT^XPDMENU("AGMP MPI RPTS AND DEBUG MENU"),OIEN=$$LKOPT^XPDMENU("AGMP MPI UNSUCCESSFUL MFE")
 . I MIEN="" D BMES^XPDUTL("  Could not find AGMP MPI RPTS AND DEBUG MENU") Q
 . I OIEN="" D BMES^XPDUTL("  Could not find AGMP MPI UNSUCCESSFUL MFE") Q
 . I '$D(^DIC(19,MIEN,10,"B",OIEN)) Q  ; ALREADY NOT ON MENU SO QUIT
 . I $$DELETE^XPDMENU("AGMP MPI RPTS AND DEBUG MENU","AGMP MPI UNSUCCESSFUL MFE") D BMES^XPDUTL("  Option AGMP MPI UNSUCCESSFUL MFE removed") Q
 . D BMES^XPDUTL("  Option AGMP MPI UNSUCCESSFUL MFE not removed from AGMP MPI RPTS AND DEBUG MENU")
 Q
