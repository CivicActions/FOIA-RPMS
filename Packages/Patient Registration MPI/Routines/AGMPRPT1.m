LISTOMSG(OMSGIEN,HLBIEN) ;EP - LIST ORIGINATING MESSAGE
 N DATA
 W !!,"ORIGINATING MESSAGE:"
 I $G(HLBIEN)'="" W !,$G(^HLB(HLBIEN,1)),$G(^HLB(HLBIEN,2))
 S REC=0
 F  S REC=$O(^HLA(OMSGIEN,1,REC)) Q:'REC  D
 .S DATA=$G(^HLA(OMSGIEN,1,REC,0))
 .Q:DATA=""
 .W !,DATA
 Q
