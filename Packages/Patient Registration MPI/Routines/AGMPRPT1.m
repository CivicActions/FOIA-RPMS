ASKEVN ;EP - ASK EVENT
 ; Reports containing SSNs have been disabled
 W !!,"THIS OPTION HAS BEEN DISABLED" Q
 N EVENT,MSGTYPE,ACKCODE,ONLYCNT
 K DIR
 S DIR(0)="SO^A28:ADD A PATIENT;A08:UPDATE A PATIENT"
 S DIR(0)=DIR(0)_";A01:ADMIT/CHECKIN A PATIENT;A03:DISCHARGE/CHECK OUT A PATIENT"
