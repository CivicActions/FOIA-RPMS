DOCOUNT(COUNT) ;EP - GET COUNT OF ERR MSGS
 S MSGIEN=0 F  S MSGIEN=$O(^HLA(MSGIEN)) Q:'MSGIEN  D
 .S ENTRYDT=$P($G(^HLA(MSGIEN,0)),U)
 .Q:ENTRYDT>TOERR!(ENTRYDT<FROMERR)
 .Q:$P($G(^HLA(MSGIEN,1,3,0)),U)'="ERR"
 .S COUNT=COUNT+1
 Q
