AGMPBGP  ;IHS/SD/TPF - MPI INCOMING MESSAGE PROCESSOR
 ;;1.0;AGMP;**3,4**;Apr 30, 2021;Build 55
 ;BGP = BACKGROUND PROCESSOR
 Q
TSK ;EP - TASKMAN ENTRY POINT
 I '$$ENABLED^AGMPOPT() Q  ; CHECK WHETHER MPI HAS BEEN DISABLED
 L +^AGMPBGP:5 Q:'$T  ;DO NOT START IF ALREADY RUNNING
 N MSGFROM,MSGIEN,MSGEVENT
 S MSGFROM=""  ;DATE MSG PLACED IN HLB
 F  S MSGFROM=$O(^HLB("QUEUE","IN",MSGFROM)) Q:MSGFROM=""  D
 .S MSGIEN=""
 .F  S MSGIEN=$O(^HLB("QUEUE","IN",MSGFROM,"RPMS-MPI",MSGIEN)) Q:MSGIEN=""  D
 ..;^HLB(4,2)=^20100429083020.78-0700^^ACK~A08^134^T^2.4^^^NE^NE
 ..;ACCEPT TYPE ACKNOWLEDGEMENT
 ..S ACCTYACK=$P($G(^HLB(MSGIEN,2)),U,10)
 ..I ACCTYACK="NE" D MSGCMPLT(MSGIEN) D KILL Q  ;NO NEED TO PROCESS
 ..I ACCTYACK="" D  Q
 ...S ERROR="NO ENTRY FOR THIS MSG IN THE 'IN QUEUE' KILLING X-REF"
 ...D MSGERR("","",MSGIEN,ERROR)
 ...D MSGCMPLT(MSGIEN),KILL
 ..S MSGEVENT=$P($P($G(^HLB(MSGIEN,2)),"^",4),"~",2)
 ..I MSGEVENT="A08" D PROC^AGMPACK(MSGIEN) D KILL Q
 ..I MSGEVENT="A28" D PROC^AGMPACK(MSGIEN) D KILL Q
 ..I MSGEVENT="Q02" D PROC^AGMPVQQ(MSGIEN) D KILL Q
 ..I MSGEVENT="M05" D PROC^AGMPMFN(MSGIEN) D KILL Q
 ..I MSGEVENT="A01" D PROC^AGMPADT(MSGIEN) D KILL Q
 ..I MSGEVENT="A03" D PROC^AGMPADT(MSGIEN) D KILL Q
 ..; REMAINING MESSAGE TYPES ENCOUNTERED NOT PROCESSED
 ..S ERROR="ENTRY FOR MESSAGE EVENT '"_MSGEVENT_"' NOT BEING PROCESSED."
 ..D MSGERR("","",MSGIEN,ERROR)
 ..D MSGCMPLT(MSGIEN)
 ..D KILL Q
 L -^AGMPBGP  ;CLEAR RUNNING FLAG
 Q
KILL ;EP - CLEAR QUEUE
 K ^HLB("QUEUE","IN",MSGFROM,"RPMS-MPI",MSGIEN)
 Q
 ;
MSGERR(LOCALID,ACKIEN,HLMSGIEN,ERROR) ;EP - PROCESS ERROR ALERT
 S AGERROR="MSG IEN: "_HLMSGIEN_" :: "_ERROR
 D NOTIF^AGMPHLO("",AGERROR)
 Q
 ;
MSGCMPLT(MSGID) ;EP - MARK MESSAGE SUCCESSFULLY COMPLETED. SET PURGE INFORMATION
 I MSGID="" Q
 N PDT,NODE,PDAYS
 S NODE=$G(^HLB(MSGID,0))
 I NODE="" Q
 ;IF A PURGE DATE ALREADY EXISTS, KILL THE OLD "AD" ENTRY TO MAINTAIN THE CROSS-REFERENCE'S INTEGRITY
 S PDT=$P(NODE,U,9)
 I PDT'="" K ^HLB("AD","OUT",PDT,MSGID)
 ;SET PURGE DATE/TIME BASED ON PARAMETER; DEFAULT TO 7
 S PDAYS=$$GET1^DIQ(9009061,DUZ(2)_",",2202)
 I PDAYS<1 S PDAYS=7
 S X="N+"_PDAYS_"D",%DT="T" D ^%DT S PDT=Y
 S $P(NODE,U,9)=PDT
 ;SET STATUS TO SUCCESS
 S $P(NODE,U,20)="SU"
 S ^HLB(MSGID,0)=NODE
 I PDT]"" S ^HLB("AD","OUT",PDT,MSGID)=""
 Q
