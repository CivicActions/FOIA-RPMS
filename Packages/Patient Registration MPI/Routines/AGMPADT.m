PROC(HLMSGIEN) ;EP - CALLED FROM AGMPBGP
 N DATA,HLMSTATE,MSGID,MSGSEG,ICNEUID,RETCODE,NEXTSEG,RESEND,AKMSGIEN,RFACACK,SFACORIG
 D PARSE(.DATA,HLMSGIEN,.HLMSTATE)
 ;NOW DO THE ORIGINATING MESSAGE
 S AKMSGIEN=HLMSGIEN  ;PRESERVE HLMSGIEN FOR THE ACK
 S HLMSGIEN=$P($G(DATA(1,3,1,1,1))," ",2)  ;MSG ID OF THE MSG THIS ACK IS RESPONDING TO
 I HLMSGIEN="" D  Q
 .S ERROR="NO ORIGINATING MSG ID FOUND FOR HLMSGIEN '"_AKMSGIEN_"'"
 .D NOTIF^AGMPHLO(AKMSGIEN,ERROR)
 .D MSGCMPLT^AGMPBGP(AKMSGIEN)
 S RFACACK=$G(HLMSTATE("HDR","RECEIVING FACILITY",1)),SFACORIG=$P($P($G(^HLB(HLMSGIEN,1)),U,4),"~")
 I RFACACK'=SFACORIG D  Q
 .S ERROR="FACILITY ID MISMATCH: "_RFACACK_" | "_SFACORIG
 .D NOTIF^AGMPHLO(AKMSGIEN,ERROR)
 .D MSGCMPLT^AGMPBGP(AKMSGIEN) ;SET ONLY THE ACK SUCCESSFUL
 D MSGCMPLT^AGMPBGP(AKMSGIEN)  ;SET THE ACK SUCCESSFUL
 D MSGCMPLT^AGMPBGP(HLMSGIEN)  ;SET THE ORIGINAL MESSAGE SUCCESSFUL
 Q
 ;
PARSE(DATA,MIEN,HLMSTATE) ;EP
 N SEG,CNT
 Q:'$$STARTMSG^HLOPRS(.HLMSTATE,MIEN)
 M DATA("HDR")=HLMSTATE("HDR")
 S CNT=0
 F  Q:'$$NEXTSEG^HLOPRS(.HLMSTATE,.SEG)  D
 .S CNT=CNT+1
 .M DATA(CNT)=SEG
 Q
 ;
 ;THIS CALL HANDLES CHECKINS (A01) AND CHECKOUTS (A03)
CHKINOUT(SDAMEVT,DFN,SDT,SUCCESS) ;EP - CREATE AND PLACE IN OUTGOING QUEUE
 ;CALLED BY PROTOCOL 'AGMP MPI CHECKIN CHECKOUT' WHICH SUBSCRIBES
 ;TO PROTOCOL 'BSDAM APPOINTMENT EVENTS' FOR CHECK INS AND CHECK OUTS
 ;PARAMETERS:
 ;   DFN
 ;   SDT = APPOINTMENT DATE
 ;   SDAMEVT = EVENT
 ;             1 = MAKE APPT
 ;             2 = CANCEL APPT
 ;             3 = NO-SHOW
 ;             4 = CHECK-IN
 ;             5 = CHECK-OUT
 ;
 I $G(SDAMEVT)'=4,$G(SDAMEVT)'=5 Q  ;ONLY CARE ABOUT CHECK INS AND CHECK OUTS
 ;
 I '$$ENABLED^AGMPOPT() Q  ; CHECK WHETHER MPI HAS BEEN DISABLED
 ;I '$$XMITPAT^AGMPSMSG($G(DFN)) Q  ;DEMO PATIENT CHECK
 I $$DEMOPAT^AGMPSMSG(DFN) Q  ;Demo Patient Check
 ;
 S SDT=$$CONDT^AGMPSMSG(SDT)  ;CONVERT DATE FOR MPI
 D CREATMSG^AGMPHLO(DFN,$S(SDAMEVT=4:"A01",1:"A03"),,.SUCCESS)
 I 'SUCCESS D
 .S AGERROR="MPI DFN="_DFN_" :: "_"ERROR WHEN CREATING "_$S(SDAMEVT=4:"A01",1:"A03")
 .D NOTIF^AGMPHLO(DFN,AGERROR)
 Q
 ;
 ;THIS CALL HANDLES ADMISSIONS (A01) AND DISCHARGES (A03)
ADMDIS(DFN,DGPMT,DGPMDA,SDT,SUCCESS) ;EP - CREATE AND PLACE IN OUTGOING QUEUE
 ;CALLED BY PROTOCOL 'AGMP MPI ADMIT DISCHARGE' WHICH SUBSCRIBES
 ;TO PROTOCOL 'BDGPM MOVEMENT EVENTS' FOR ADMISSIONS AND DISCHARGES
 ;PARAMETERS:
 ;   DFN
 ;   DGPMT  = TYPE OF MOVEMENT
 ;           1 = ADMISSION
 ;           2 = WARD TRANSFER
 ;           3 = DISCHARGE
 ;           4 = CHECK-IN LODGER
 ;           5 = CHECK-OUT LODGER
 ;           6 = SERVICE TRANSFER
 ;   DGPMDA = MOVEMENTS IEN
 ;   SDT = EVENT DATE (WHEN CALLING FROM THE MENU)
 ;
 I $G(DGPMT)'=1,$G(DGPMT)'=3 Q  ;HANDLE ADMIT AND DISCHARGE ONLY
 ;
 I '$$ENABLED^AGMPOPT() Q  ;CHECK WHETHER MPI HAS BEEN DISABLED
 ;I '$$XMITPAT^AGMPSMSG($G(DFN)) Q  ;DEMO PATIENT CHECK
 I $$DEMOPAT^AGMPSMSG(DFN) Q  ;Demo Patient Check
 ;
 ;GET DATE OF ADMISSION OR DISCHARGE. THIS IS PREPOPULATED WHEN CALLING FROM AGMPSMSG.
 I $G(SDT)="" S SDT=$$GET1^DIQ(405,+DGPMDA_",",.01,"I")
 S SDT=$$CONDT^AGMPSMSG(SDT)  ;CONVERT DATE FOR MPI
 ;
 D CREATMSG^AGMPHLO(DFN,$S(DGPMT=1:"A01",1:"A03"),,.SUCCESS)
 I 'SUCCESS D
 .S AGERROR="MPI DFN="_DFN_" :: "_"ERROR WHEN CREATING "_$S(DGPMT=1:"A01",1:"A03")
 .D NOTIF^AGMPHLO(DFN,AGERROR)
 Q
 ;
 ;THIS CALL HANDLES PATIENT REGISTRATION EVENTS (A28)
REG(DFN) ;EP - CREATE AND PLACE IN OUTGOING QUEUE
 ;CALLED BY PROTOCOL 'AGMP MPI REGISTRATION' WHICH SUBSCRIBES
 ;TO PROTOCOL 'AG REGISTER A PATIENT' FOR PATIENT REGISTRATION
 ;PARAMETER:
 ;   DFN
 ;
 I '$$ENABLED^AGMPOPT() Q  ;CHECK WHETHER MPI HAS BEEN DISABLED
 ;
 ;I '$$XMITPAT^AGMPSMSG($G(DFN)) Q  ;DEMO PATIENT CHECK
 I $$DEMOPAT^AGMPSMSG(DFN) Q  ;Demo patient check
 ;
 D CREATMSG^AGMPHLO(DFN,"A28",,.SUCCESS)
 I 'SUCCESS D
 .S AGERROR="MPI DFN="_DFN_" :: "_"ERROR WHEN CREATING A28"
 .D NOTIF^AGMPHLO(DFN,AGERROR)
 Q
 ;
 ;THIS CALL HANDLES PATIENT UPDATE EVENTS (A08)
UPDATE(DFN) ;EP - CREATE AND PLACE IN OUTGOING QUEUE
 ;CALLED BY PROTOCOL 'AGMP MPI UPGRADE' WHICH SUBSCRIBES
 ;TO PROTOCOL 'AG UPDATE A PATIENT' FOR PATIENT UPDATES
 ;PARAMETER:
 ;   DFN
 ;
 I '$$ENABLED^AGMPOPT() Q  ;CHECK WHETHER MPI HAS BEEN DISABLED
 ;Do not send A08 for demo patients
 ;I '$$XMITPAT^AGMPSMSG($G(DFN)) Q  ;DEMO PATIENT CHECK
 I $$DEMOPAT^AGMPSMSG($G(DFN)) Q  ;DEMO PATIENT CHECK
 ;
 D CREATMSG^AGMPHLO(DFN,"A08",,.SUCCESS)
 I 'SUCCESS D
 .S AGERROR="MPI DFN="_DFN_" :: "_"ERROR WHEN CREATING A08"
 .D NOTIF^AGMPHLO(DFN,AGERROR)
 Q
