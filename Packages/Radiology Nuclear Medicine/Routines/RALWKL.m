RALWKL ; IHS/ADC/PDW -Radiology Workload Reports by Functional Area 17:49 ;  [ 11/01/2001  10:23 AM ]
 ;;4.0;RADIOLOGY;**11**;NOV 20, 1997
 ;
 ;Instances of "^" have been replaced by U in this routine for patch 11
 ;IHS/HQW/SCR - 9/6/01 **11**
 ;
SUM ;
 S X="-----------------" W !!,RATITLE," Workload Report:",!,X,$E(X,1,$L(RATITLE)) K RAFL1
ASKSUM ;
 R !!,"Do you wish only the summary report? No// ",X:DTIME Q:'$T!(X[U)  S:X=""!("Nn"[$E(X)) X="N",RAFL1="" I '$D(RAFL1),"Yy"'[$E(X) W:X'["?" *7 W !!?3,"Enter 'YES' for a summary report, or 'NO' for a detailed report." G ASKSUM
 S ZTRTN="START^RALWKL" S:$D(RAFL) ZTSAVE("RAFL*")="" F RASV="BEGDATE","ENDDATE","RAFILE","RAPCE","RATITLE","RACRT" S ZTSAVE(RASV)=""
 D DATE^RAUTL G:POP Q^RALWKL1
 W !!,"NOTE: The statistics compiled for this report will be based only",!?6,"on exams that are in one of the following statuses:"
 F RASTAT=0:0 S RASTAT=$O(^RA(72,RASTAT)) Q:RASTAT'>0  I $D(^(RASTAT,0)),$D(^(.3)),$P(^(.3),U,RACRT)="y" W !?15,$P(^(0),U)
DEV ;
 W ! D ZIS^RAUTL G:POP Q^RALWKL1
START ;
 U IO K ^TMP($J,"RA") S RABEG=BEGDATE-.0001,RAEND=ENDDATE+.9999 D CRIT^RAUTL1
 F RADTE=RABEG:0:RAEND S RADTE=$O(^RADPT("AR",RADTE)) Q:RADTE'>0!(RADTE>RAEND)  F RADFN=0:0 S RADFN=$O(^RADPT("AR",RADTE,RADFN)) Q:RADFN'>0  D RADTI
 G ^RALWKL1
 ;
RADTI ;
 F RADTI=0:0 K RAOR,RABILAT,RAPORT S RADTI=$O(^RADPT("AR",RADTE,RADFN,RADTI)) Q:RADTI'>0  I $D(^RADPT(RADFN,"DT",RADTI,0)) S RAD0=^(0) D RACNI
 Q
RACNI ;
 F RACNI=0:0 S RACNI=$O(^RADPT(RADFN,"DT",RADTI,"P",RACNI)) Q:RACNI'>0  I $D(^(RACNI,0)) S RAP0=^(0) I $D(RACRT(+$P(RAP0,U,3))) D CHK
 Q
 ;
CHK ;
 S C=$P(RAP0,U,4),C=$S(C="I":1,C="O":2,C="R":3,1:4),RADIV=$S($D(^RA(79,+$P(RAD0,U,3),0)):+$P(RAD0,U,3),1:99)
 ;S RAFLD=$E($S($D(@(U_RAFILE_"+$P(RAP0,"U",RAPCE),0)")):$P(^(0),U),1:"OTHER"),1,30)
 ;
 ;Above line modified for VA patches IHS/HQW/SCR 9/6/01 **11**
 ;
 Q:'$P(RAP0,U,RAPCE)  S RAFLD=$E($S($D(@("^"_RAFILE_"+$P(RAP0,""^"",RAPCE),0)")):$P(^(0),U),1:"OTHER"),1,30)  ;IHS/HQW/SCR 9/6/01 **11**
 I RAFILE="SC(" Q:C=1  ;I RAFLD="OTHER",$P(RAP0,U,19)'="" Q
 I (RAFILE="DIC(42,"!(RAFILE="DIC(42.4,")!(RAFILE="DIC(49,")) Q:13'[C  ;I RAFLD="OTHER",$P(RAP0,U,8)'="" Q
 F I=0:0 S I=$O(^RADPT(RADFN,"DT",RADTI,"P",RACNI,"M",I)) Q:I'>0  I $D(^(I,0)) S RAQI=+^(0) S:RAQI=1 RABILAT="" S:RAQI=2 RAPORT="" S:RAQI=3 RAOR=""
 Q:'$D(^RAMIS(71,+$P(RAP0,U,2),0))  S RAPRI=^(0),RAPRC=$E($P(RAPRI,U),1,28) Q:'$D(^(2))  F I=0:0 S I=$O(^RAMIS(71,+$P(RAP0,U,2),2,I)) Q:I'>0  I $D(^(I,0)) S RAZ=^(0),RAMJ=$S($D(^RAMIS(71.1,+RAZ,0)):^(0),1:"") D PRC
 Q:'$D(RAMIS(1))
 I J=1 S RAMIS=RAMIS(1),RAWT=RAWT(1),RAMUL=RAMUL(1),RAWT=RAWT*RAMUL,RANUM=RAMUL
 I J>1 S RANUM=1,RAWT=0,RAMIS=RAMIS(1) F J=1:1 Q:'$D(RAMIS(J))  S I=RAWT(J),RAMUL=RAMUL(J),RAWT=RAWT+(RAMUL*I)
 D STORE K RAMIS,RAWT,RAMUL,RAZ,RAMJ,RAMULP,RAMULPFL,RABILAT,RAOR,RAPORT Q
 ;
STORE ;
 I $D(RAOR) S A=25 D AUX
 I $D(RAPORT) S A=26 D AUX
 I $D(RAMULP) S A="MULP" D AUX
 S:'($D(^TMP($J,"RA",RADIV))#2) ^(RADIV)="0^0^0^0^0" S X=^(RADIV),$P(X,U,C)=$P(X,U,C)+RANUM,$P(X,U,5)=$P(X,U,5)+RAWT,^(RADIV)=X
 S:'($D(^TMP($J,"RA",RADIV,"FLD",RAFLD))#2) ^(RAFLD)="0^0^0^0^0" S X=^(RAFLD),$P(X,U,C)=$P(X,U,C)+RANUM,$P(X,U,5)=$P(X,U,5)+RAWT,^(RAFLD)=X
 S:'$D(^TMP($J,"RA",RADIV,"FLD",RAFLD,"A",RAMIS,"P",RAPRC)) ^(RAPRC)="0^0^0^0^0" S X=^(RAPRC),$P(X,U,C)=$P(X,U,C)+RANUM,$P(X,U,5)=$P(X,U,5)+RAWT,^(RAPRC)=X
 Q
 ;
PRC ;
 I +RAZ=25 S RAOR="" Q
 I +RAZ=26 S RAPORT="" Q
 S:$P(RAZ,U,3)="Y" RABILAT="" F J=1:1 I '$D(RAMIS(J)) S RAMIS(J)=$S(RAMJ]"":+RAZ,1:99),RAWT(J)=+$P(RAMJ,U,2),RAMUL(J)=+$P(RAZ,U,2) S:$D(RABILAT) RAMUL(J)=RAMUL(J)*2 S:J>1 RAMULP="" Q
 K RABILAT
 Q
 ;
AUX ;
 S:'$D(^TMP($J,"RA",RADIV,"FLD",RAFLD,"A",A,"P",RAPRC)) ^(RAPRC)="0^0^0^0^0" S X=^(RAPRC),$P(X,U,C)=$P(X,U,C)+RANUM,$P(X,U,5)=$P(X,U,5)+RAWT,^(RAPRC)=X
 Q
WARD ;
 S RAFILE="DIC(42,",RACRT=5,RAPCE=6,RATITLE="Ward",RAFL="" G RALWKL
 ;
SERV ;
 S RAFILE="DIC(49,",RACRT=3,RAPCE=7,RATITLE="Service",RAFL="" G RALWKL
 ;
BEDSEC ;
 S RAFILE="DIC(42.4,",RACRT=2,RAPCE=19,RATITLE="PTF Bedsection",RAFL="" G RALWKL
 ;
CLINIC ;
 S RAFILE="SC(",RACRT=1,RAPCE=8,RATITLE="Clinic",RAFL="" G RALWKL
 ;
SHAR ;
 S RAFILE="DIC(34,",RACRT=4,RAPCE=9,RATITLE="Sharing/Contract",RAFL="" G RALWKL
