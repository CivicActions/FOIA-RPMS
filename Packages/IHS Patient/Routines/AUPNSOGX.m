S9001(DFN)  ;EP - BPRF PREFERRED NAME SET LOGIC
 N AUPNX,AUPNX1,AUPNX2
 S DFN=$G(DFN) Q:'DFN
 S AUPNX1=$P($P($G(^DPT(DFN,0)),U),","),AUPNX2=$P($G(^AUPNPAT(DFN,90)),U) Q:'$L(AUPNX1)  Q:'$L(AUPNX2)
 S AUPNX=AUPNX1_","_AUPNX2
 S ^DPT("B",$E(AUPNX,1,30),DFN,9001)=""
 Q
K9001(DFN,SRC)  ;EP - BPRF PREFERRED NAME KILL LOGIC
 N AUPNX,AUPNX1,AUPNX2
 S DFN=$G(DFN),SRC=$G(SRC,2) Q:'DFN
 S AUPNX1=$P($P($G(^DPT(DFN,0)),U),","),AUPNX2=$P($G(^AUPNPAT(DFN,90)),U)
 S AUPNX=$S(SRC=1:$P(X,","),1:AUPNX1)_","_$S(SRC=2:X,1:AUPNX2)
 K ^DPT("B",$E(AUPNX,1,30),DFN,9001)
 Q
9301(DFN,REC,ACT)  ;EP - ASO1 SEXUAL ORIENTATION #9000001.9301 SET/KILL LOGIC
 S DFN=$G(DFN),REC=$G(REC),ACT=$G(ACT) Q:'DFN  Q:'REC  Q:'$L(ACT)
 N AUPNX1,AUPNX2 S AUPNX1=$O(^AUPNPAT(DFN,93,"@"),-1) Q:'AUPNX1  ; MOST RECENT RECORD
 Q:AUPNX1'=REC  ; QUIT IF NOT MOST RECENT RECORD
 I ACT="S" D  ; IF CREATING OR EDITING A RECORD, KILL INDEXES FROM PREVIOUS RECORDS
 . S AUPNX2=0 F  S AUPNX2=$O(^AUPNPAT("ASO",AUPNX2)) Q:'AUPNX2  D  ; LOOP THROUGH EXISTING INDEXES
 . . Q:'$D(^AUPNPAT("ASO",AUPNX2,DFN))  ; IF NO ENTRY FOR PATIENT THEN QUIT
 . . K:'$D(^AUPNPAT(DFN,93,AUPNX1,1,"B",AUPNX2)) ^AUPNPAT("ASO",AUPNX2,DFN)  ; IF ENTRY DOES EXIST, AND SO DOESN'T EXIST IN MOST RECENT RECORD, DELETE
 I ACT="K" D  ; IF DELETING A RECORD, CREATE INDEXES FROM PREVIOUS RECORD
 . S AUPNX1=$O(^AUPNPAT(DFN,93,AUPNX1),-1) Q:'AUPNX1  ; NEXT TO MOST RECENT RECORD
 . S AUPNX2=0 F  S AUPNX2=$O(^AUPNPAT(DFN,93,AUPNX1,1,"B",AUPNX2)) Q:'AUPNX2  D  ; LOOP THROUGH ENTRIES
 . . S:'$D(^AUPNPAT("ASO",AUPNX2,DFN)) ^AUPNPAT("ASO",AUPNX2,DFN)=""  ; IF ENTRY DOESN'T EXIST, CREATE
 Q
93011(DFN,REC,IEN,ACT)  ;EP - ASO2 SEXUAL ORIENTATION #9000001.93011 SET/KILL LOGIC
 S DFN=$G(DFN),REC=$G(REC),IEN=$G(IEN),ACT=$G(ACT) Q:'DFN  Q:'REC  Q:'IEN  Q:'$L(ACT)
 N AUPNX1 S AUPNX1=$O(^AUPNPAT(DFN,93,"@"),-1) Q:'AUPNX1  ; MOST RECENT RECORD
 Q:AUPNX1'=REC  ; QUIT IF NOT MOST RECENT RECORD
 S:ACT="S" ^AUPNPAT("ASO",IEN,DFN)=""  ; CREATE INDEX
 K:ACT="K" ^AUPNPAT("ASO",IEN,DFN)     ; DELETE INDEX
 Q
