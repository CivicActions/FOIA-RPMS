POST ;EP
 ;add option to menu
 S X=$$ADD^XPDMENU("AMH M DATA ENTRY MENU","AMH INTAKE LIST","INTL",85)
 S X=$$ADD^XPDMENU("AMH MENU SCREENING REPORTS","AMH SDOH REPORTS","SOC",32)
 S X=$$ADD^XPDMENU("AMH MENU SCREENING REPORTS","AMHR DU SCREENING MENU","DRU",33)
 ;S X=$$ADD^XPDMENU("AMH MENU SCREENING REPORTS","AMHR AD SCREENING MENU","DRU",22)
 S X=$$ADD^XPDMENU("AMH MENU SCREENING REPORTS","AMHR TAPS SCREENING REPORT","TAPS",38)
 S X=$$ADD^XPDMENU("AMH MENU SCREENING REPORTS","AMHR TAPS POSITIVE SCREENING","TAPP",39)
 D INACTPC
 K DA,DR,DIE
 S DA=$O(^AMHPROB("B","F50.22",0))
 I DA S DR=".02///BULIMIA NERVOSA, MODERATE",DIE="^AMHPROB(" D ^DIE K DA,DIE,DR
 S DA=$O(^AMHPROB("B","F50.23",0))
 I DA S DR=".02///BULIMIA NERVOSA, SEVERE",DIE="^AMHPROB(" D ^DIE K DA,DIE,DR
 D ADDDSMV
 D BMXPO
 Q
 ;
ADDDSMV ;add all new dsm v codes
 D MES^XPDUTL($$CJ^XLFSTR("Adding codes.",IOM))
 S AMHX=0 F  S AMHX=$O(^AMHTPCAD(AMHX)) Q:AMHX'=+AMHX  D
DSM5 .;ADD DSM5 CODES
 .S AMHCODE=$P(^AMHTPCAD(AMHX,0),U,1)
 .Q:AMHCODE=""
 .S AMHNARR=$P(^AMHTPCAD(AMHX,0),U,2)
 .S AMHPC=$P(^AMHTPCAD(AMHX,0),U,3)
 .S AMHPCC=$O(^AMHPROBC("B",AMHPC,0))
 .I 'AMHPCC D MES^XPDUTL("PROBLEM CODE MISSING: "_AMHPC_" CODE "_AMHCODE_" NOT UPLOADED") Q
 .S AMHICD=$P(^AMHTPCAD(AMHX,0),U,17)
 .S AMHEHR=$P(^AMHTPCAD(AMHX,0),U,15)
 .S AMHNOBH=$P(^AMHTPCAD(AMHX,0),U,18)
 .S AMHCS=$P(^AMHTPCAD(AMHX,0),U,10)
 .;FIND EXISTING AND OVERLAY
 .K DIE,DA,DR
 .S DA=""
 .S AMHY=0 F  S AMHY=$O(^AMHPROB("B",AMHCODE,AMHY)) Q:AMHY'=+AMHY!(DA)  D
 ..Q:$$UP^XLFSTR($P(^AMHPROB(AMHY,0),U,2))'=$$UP^XLFSTR(AMHNARR)
 ..S DA=AMHY
 .I DA D EDITC Q
 .;ADD THEN EDIT
 .K DIC,DLAYGO,DIADD,DD,D0,DA
 .S DITC=1
 .S X=AMHCODE,DIC="^AMHPROB(",DLAYGO=9002012.2,DIADD=1,DIC(0)="EMQ"
 .S DIC("DR")=".02///"_AMHNARR_";.03////"_AMHPCC_";.17///"_AMHICD_";.1///"_AMHCS_";.15///"_AMHEHR_";.18///"_AMHNOBH_";.16////"_$P($G(^AMHTPCAD(AMHX,11)),U,1)
 .D FILE^DICN
 .I Y=-1 W !,"ERROR ON ",AMHX," ",AMHCODE," ",DIC("DR") K DA,DIC,DIADD,DLAYGO,DR,DD,D0,DO,DITC Q
 .K DA,DIC,DIADD,DLAYGO,DR,DD,D0,DO,DITC
 .D MES^XPDUTL("."_$P(^AMHPROB(+Y,0),U,1))
 .Q
 Q
EDITC ;
 S DIE="^AMHPROB(",DR=".02///"_AMHNARR_";.03////"_AMHPCC_";.17///"_AMHICD_";.1///"_AMHCS_";.15///"_AMHEHR_";.18///"_AMHNOBH_";.16////"_$P($G(^AMHTPCAD(AMHX,11)),U,1)
 D ^DIE
 I $D(Y) D MES^XPDUTL("DSM CODE FAILED UPDATING: "_AMHCODE) K DIE,DA,DR Q
 D MES^XPDUTL("."_$P(^AMHPROB(DA,0),U,1))
 K DIE,DA,DR
 Q
INACTPC ;
 D MES^XPDUTL($$CJ^XLFSTR("Inactivating codes.",IOM))
 S AMHX=0 F  S AMHX=$O(^AMHPCIN(AMHX)) Q:AMHX'=+AMHX  D
 .Q:$P(^AMHPCIN(AMHX,0),U,3)'=1
 .S AMHC=$P(^AMHPCIN(AMHX,0),U,1)
 .;loop through "B" on AMHPROB and flag all that are not already flagged
 .S AMHY=0 F  S AMHY=$O(^AMHPROB("B",AMHC,AMHY)) Q:AMHY'=+AMHY  D
 ..I '$D(^AMHPROB(AMHY,0)) Q
 ..Q:$P(^AMHPROB(AMHY,0),U,10)'=5  ;only inactivate DSM V codes
 ..Q:$P(^AMHPROB(AMHY,0),U,16)>3220930  ;don't inactvate newly added
 ..I $P(^AMHPCIN(AMHX,0),U,4)]"",$$UP^XLFSTR($P(^AMHPCIN(AMHX,0),U,4))'=$$UP^XLFSTR($P(^AMHPROB(AMHY,0),U,2)) Q  ;narratives must match
 ..S DA=AMHY,DIE="^AMHPROB(",DR=".13///1;.14////"_$P(^AMHPCIN(AMHX,0),U,5) D ^DIE K DA,DIE,DR
 ..D MES^XPDUTL("."_$P(^AMHPROB(AMHY,0),U,1)_" "_$P(^AMHPROB(AMHY,0),U,4))
 ..Q
 .Q
 Q
SETABU ;
 ;loop codes in ABUSE and set .19 field
 NEW AMHTEXT,AMHX,AMHC,AMHIEN
 S AMHTEXT="ABUSE" F AMHX=1:1 S AMHC=$P($T(@AMHTEXT+AMHX),";;",2) Q:AMHC=""  D
 .S AMHIEN=0 F  S AMHIEN=$O(^AMHPROB("B",AMHC,AMHIEN)) Q:AMHIEN'=+AMHIEN  D
 ..S DA=AMHIEN,DR=".19///1",DIE="^AMHPROB(" D ^DIE K DIE,DA,DR
 Q
 ;
BMXPO ;-- update the RPC file
 N AMHRPC
 S AMHRPC=$O(^DIC(19,"B","AMHGRPC",0))
 Q:'AMHRPC
 D CLEAN(AMHRPC)
 D GUIEP^BMXPO(.RETVAL,AMHRPC_"|AMH")
 Q
 ;
CLEAN(APP) ;-- clean out the RPC multiple first
 S DA(1)=APP
 S DIK="^DIC(19,"_DA(1)_","_"""RPC"""_","
 N AMHDA
 S AMHDA=0 F  S AMHDA=$O(^DIC(19,APP,"RPC",AMHDA)) Q:'AMHDA  D
 . S DA=AMHDA
 . D ^DIK
 K ^DIC(19,APP,"RPC","B")
 Q
