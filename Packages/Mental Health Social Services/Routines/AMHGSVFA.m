DSM5(RETVAL,AMHSTR) ;-- save POV called from method SaveDSM5 in clsVisitDataEntry
 S X="MERR^AMHGU",@^%ZOSF("TRAP") ; m error trap
 N AMHI,P,R,AMHDM,AMHREC,AMHA2,AMHP,AMHER
 S P="|",R="~"
 S RETVAL="^AMHTMP("_$J_")"
 S AMHI=0
 K ^AMHTMP($J)
 I $G(AMHSTR)="" D CATSTR^AMHGU(.AMHSTR,.AMHSTR)
 S AMHDM=$P(AMHSTR,P)
 S AMHREC=$P(AMHSTR,P,2)
 S AMHA2=$P(AMHSTR,P,3)
 S AMHP=$P(AMHSTR,P,4)
 N AMH2
 D ARRAY^AMHGU(.AMH2,AMHA2)
 D AXIS2^AMHGSVF(AMHDM,AMHREC,AMHP,.AMH2)  ;v4.0p4 this code should work the same for DSM V
 S @RETVAL@(AMHI)="T00030Result"_$C(30)
 S AMHI=AMHI+1
 S @RETVAL@(AMHI)=$S($G(AMHER)]"":AMHER,1:AMHREC)_$C(30)
 S @RETVAL@(AMHI+1)=$C(31)
 Q
 ;
SCREENN(RETVAL,AMHSTR) ;file screening from the wellness tab, called from the SaveScreening method of clsVisitDataEntry
 S X="MERR^AMHGU",@^%ZOSF("TRAP") ; m error trap
 N AMHI,P,R,AMHDM,AMHREC,AMHA,AMHAC,AMHD,AMHDC,AMHIP,AMHIPC,AMHPP,AMHP,AMHER,AMHAPRV,AMHDPRV,AMHIPRV,AMHSS,AMHSSC,AMHSSIP
 N AMHS,AMHSC,AMHSPRV
 S P="|",R="~"
 S RETVAL="^AMHTMP("_$J_")"
 S AMHI=0
 I $G(AMHSTR)="" D CATSTR^AMHGU(.AMHSTR,.AMHSTR)
 K ^AMHTMP($J)
 S AMHDM=$P(AMHSTR,P)
 S AMHREC=$P(AMHSTR,P,2)
 S AMHST=$P(AMHSTR,P,3)
 S AMHSR=$P(AMHSTR,P,4)
 S AMHSPI=$P(AMHSTR,P,5)
 S AMHSP=$P(AMHSTR,P,6)
 S AMHSC=$P(AMHSTR,P,7)
 D SCREENN^AMHGEVF(AMHDM,AMHREC,AMHST,AMHSR,AMHSPI,AMHSP,AMHSC)
 S @RETVAL@(AMHI)="T00030Result"_$C(30)
 S AMHI=AMHI+1
 S @RETVAL@(AMHI)=$S($G(AMHER)]"":AMHER,1:AMHREC)_$C(30)
 S @RETVAL@(AMHI+1)=$C(31)
 Q
