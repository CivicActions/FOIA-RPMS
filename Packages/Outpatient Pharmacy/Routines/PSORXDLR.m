PSORXDLR ;IHS/ASDS/ENM/POC - ROUTINE TO DELETE LAST REFILL IHS/OKCAO/POC [ 02/20/2001  3:32 PM ]
 ;;6.0;OUTPATIENT PHARMACY;**1,3**;09/03/97
 ;CALLED FROM PSORXDL
 ;N X ;SO IT WILL BE SET WHEN LEAVE
 ;DISPLAY REFILLS
 S APSDDISP=0 F  S APSDDISP=$O(^PSRX(DA,1,APSDDISP)) Q:APSDDISP'=+APSDDISP  S APSDDTE=$$FMTE^XLFDT(+^PSRX(DA,1,APSDDISP,0)) W !,APSDDTE
 S APSDLAST=APSDDTE ;LATEST DATE
ASK W !,"DO YOU WANT TO DELETE A REFILL"
 S %=2 D YN^DICN
 Q:%'=1  ;DID NOT ANSWER YES
 S APSDDIEN=DA,APSDDRUG=$P(^PSRX(APSDDIEN,0),"^",6)
 N DA ;SO IT WILL BE AS IT WAS BEFORE CALL
 ;APSDDIEN IS IEN OF RX
 ;SET RX STRINGS
 F I=0,2,3 S @("RX"_I)=@("^PSRX("_APSDDIEN_","_I_")")
 ;
DIC S DA(1)=APSDDIEN,DIC="^PSRX("_DA(1)_",1,",DIC(0)="AEQ"
 S DIC("B")=APSDLAST
 D ^DIC
 K DIC
 I Y=-1 G ASK
 Q:$D(DTOUT)!$D(DUOUT)
 S APSDDIE=+Y,APSDDATE=$P(Y,"^",2)
ASKA ;ASK IF FOR SURE
 ;NEXT TWO LINES WARNING ABOUT RETURN TO STOCK IHS/OKCAO/POC 5/21/98
 S APSDTURN=$P(^PSRX(APSDDIEN,1,APSDDIE,0),U,16)]""
 I APSDTURN W !,"WARNING-WARNING",!,"THIS REFILL HAS BEEN RETURNED TO STOCK. IF DELETED INVENTORY WILL NOT BE UPDATED AS INVENTORY WAS UPDATED WHEN REFILL WAS RETURNED TO STOCK!",!,"THE NUMBER OF REFILLS WILL BE DECREASED BY ONE AS THE RETURN TO STOCK OPTION ADDED ONE REFILL"
 W !,"ARE YOU SURE YOU WANT TO DELETE THE REFILL "_$$FMTE^XLFDT(APSDDATE)
 S %=2 D YN^DICN
 Q:%'=1
 Q:$D(DTOUT)!$D(DUOUT)
 ;LOCK THE ENTRY
 L +^PSRX(APSDDIEN):0 I '$T W *7,!!,"RX BEING ACCESSED BY OTHER USER" H 5 Q
 ;SAVE SOME VARIABLES
 S APSDSIEN=^PSRX(APSDDIEN,1,APSDDIE,0) ;KEEP REFILL INFO
 ;S:$P(%APSITE,"^",15)="Y" APSRX=$G(^PSRX(APSDDIEN,1,APSDDIE,999999911)),APSRM=APSDDIE I APSRX,APSRM S PS="EDIT" ;FOR X REF ON APCC REFILL LINK
 S:$P(%APSITE,"^",15)="Y" PS="EDIT" ;FOR X REF ON REFILL PCC LINK
 S APSDQTY=$P(APSDSIEN,"^",4) ;SAVE QTY
 S APSDRFDT=+APSDSIEN ;FIRST PIECE OF SUBNODE
 S APSDRFN=APSDDIE ;THE 1ST, 2ND, 3RD REFILL ETC
 ;S:APSDRFN>5 APSDRFN=APSDRFN+1 ;BECAUSE DD HAS 6 AS PARTIAL
 ;CHECK THIS OUT ***** ABOVE
 ;NEED TO MODIFY DD FOR ACTIVITY LOG OF 52 FOR ABOVE ****
 ;
 S DA=APSDDIE,DA(1)=APSDDIEN
 S DIK="^PSRX("_DA(1)_",1,"
 S PS="EDIT" ;FOR XREF APCC
 D ^DIK
 K DIK,DA
 ;FIX QTY IN DRUG FILE
 ;WONT ADD BACK TO INVENTORY IF RETURNED TO STOCK 4/21/98 IHS/OKCAO/POC
 I 'APSDTURN S:$D(^PSDRUG(APSDDRUG,660.1)) ^(660.1)=^(660.1)+APSDQTY
 I APSDTURN S $P(^(0),"^",9)=$P(^PSRX(APSDRTDA,0),"^",9)-1 ;DEC #OF REF
 ;CHECK THIS OUT ****** ABOVE
 ;NOW MAKE CALL TO BILLING IHS/OKCAO/POC 8/18/2000
 D  ; 
 .Q:APSDTURN  ;IT'S ALREADY BEEN RETURNED TO STOCK SO IT BEEN UNBILLED ALREADY
 .N APSQPST
 .S APSQPST=$$EN^APSQBRES(APSDDIEN,APSDDIE,"D")
 ;SET THE ACTIVITY LOG
 S K=1,D1=0 F Z=0:0 S Z=$O(^PSRX(APSDDIEN,"A",Z)) Q:'Z  S D1=Z,K=K+1
 S D1=D1+1 S:'$D(^PSRX(APSDDIEN,"A",0))#10 ^(0)="^52.3DA^^^" S ^(0)=$P(^(0),"^",1,2)_"^"_D1_"^"_K
 D NOW^%DTC
 S ^PSRX(APSDDIEN,"A",D1,0)=%_"^D^"_DUZ_"^"_APSDRFN_"^"_"REFILL DELETED FOR DATE "_$$FMTE^XLFDT(APSDDATE)
 ;DO THE NUMBER OF REFILLS GET RESET ?? *****
 ;EXPIRATION DATE AND STATUS
 S REPRINT="" S (PPL,J)=APSDDIEN,OEXDT=+$P(RX2,"^",6)
 D ^PSOEXDT S NEXDT=+$P(RX2,"^",6) I OEXDT'=NEXDT D
 .;S D=$P(^PSRX(DA,0),"^",2) K ^PS(55,D,"P","A",OEXDT,APSDDIEN)
 .S D=$P(^PSRX(APSDDIEN,0),"^",2) K ^PS(55,D,"P","A",OEXDT,APSDDIEN) ;IHS/OKCAO/POC 7/29/98 CHANGE DA TO APSDDIEN SINCE DA GETS KICKED BY FM CALL APSDDIEN IS THE SAME AS DA 
 .S ^PS(55,D,"P","A",NEXDT,APSDDIEN)="" K D,OEXDT,NEXDT
 .Q
 ;NEXT POSSIBLE REFILL AND LAST REFILL
 S IRXN=APSDDIEN
 ;F I="RX0","RX2","RX3","IRXN" S @("APSD("_I_")")=@I
 ;F I="RX0","RX2","RX3","IRXN" S @"APSD"@(I)=@I
 F I="RX0","RX2","RX3","IRXN" S APSD(I)=@I
 D NEXT^PSOUTIL(.APSD)
 K DIE,DR,DA
 S DIE="^PSRX(",DA=APSD("IRXN")
 S DR="101///"_$P(APSD("RX3"),"^")_";102///"_$P(APSD("RX3"),"^",2)
 D ^DIE
 K DIE,DA,DR,X,Y
 ;IS THIS SOME KIND OF COPAY XREF I'VE NEVER SEEN IT
 K ^PSRX("ACP",$P(^PSRX(APSDDIEN,0),"^",2),APSDRFDT,APSDDIE,APSDDIEN) ;APSDDIE=IEN OF SUBFILE APSDDIEN=IEN OF ROOT
 L -^PSRX(APSDDIEN)
 ;PCC LINK
 ;I $P(%APSITE,"^",15)="Y" D ^APSPCCD
 ;LET THE XREF TAKE CARE OF APCC
 ;CLEAN UP TIME
 K PS
 D EN^XBVK("APSD")
 Q
