PSORX ;IHS/DSD/JCM - MAIN RX DATA ENTRY DRIVER [ 01/16/2003  8:18 AM ]
 ;;6.0;OUTPATIENT PHARMACY;**1,2,3,4**;11/11/2002
 ;;6.0;OUTPATIENT PHARMACY;**73,120,133**;09/03/97
 ; Input variables: PSOFROM
 ; Values of PSOFROM: NEW for NEW Rxs
 ;                    REFILL for Refill Rxs
 ;                    EDIT for Editing Rxs
 ;                    DELETE for deleting Rxs
 ;                    CANCEL for cancelling Rxs
 ;                    RETURN for returning Rxs to Stock
 ;                    PARTIAL for doing partial prescriptions
 ;                    BATCH for batch Rx processing
 ;                    FAST FOR FAST OPTION IHS/OKCAO/POC 
 ; PSOFROM("PTLKUP")=1 If you want patient asked first and profile built
 ;
 ;
 ;---------------------------------------------------------
START ;   
 D INIT G:PSORX("QFLG") END
 D:$G(PSOFROM("PTLKUP"))]"" PT G:PSORX("QFLG") END
 D:$G(PSOFROM("PTLKUP"))]"" PROFILE
EN D @PSOFROM
 I $G(APSQFNEW) K APSQFNEW G EN  ;IHS/OKCAO/POC FAST OPTION
 I $G(APSQFAST) K APSQFAST G EN  ;IHS/OKCAO/POC FAST OPTION
 ;D:$G(PSORX("PSOL",1))]"" ^PSORXL K PSORX("PSOL")
 D:$G(PSORX("PSOL",1))]"" ^APSPNE4 K PSORX("PSOL")  ;IHS/DSD/ENM 9/14/94
 ;--- --- --- --- ---
 ;IHS/DSD/ENM 12/01/95 RX RELEASE FEATURE
 ;IHS/DSD/ENM 11/30/95 RELEASE DT SET IHS/OKCAO/POC 7/7/98 NOW RELEASE DONE FOR NEW AND REFILL AT LABEL
 ;I PSOFROM="NEW"!(PSOFROM="REFILL")!(PSOFROM="PARTIAL")&($D(^XUSEC("PSORPH",DUZ))) D  ;IHS/DSD/ENM 11/30/95 RELEASE DT SET
 ;I PSOFROM="NEW"!(PSOFROM="FAST")!(PSOFROM="REFILL")!(PSOFROM="PARTIAL")&($D(^XUSEC("PSORPH",DUZ))) D  ;IHS/DSD/ENM 11/30/95 RELEASE DT SET
 ;.Q:$G(APSPZRP)']""
 ;.F APSP1=1:1 Q:$P(APSPZRP,",",APSP1)=""  S RXP=$P(APSPZRP,",",APSP1) D  ;GET RELEASE INFO 
 ;..;S RXP=APSPZRP,PSRH=DUZ,PSIN=$P($G(^PS(59.7,1,49.99)),"^",2)
 ;..S PSRH=DUZ,PSIN=$P($G(^PS(59.7,1,49.99)),"^",2)
 ;..D ^APSPDISP
 ;--- --- --- --- ---
 I $G(PSOFROM("PTLKUP"))]"" D EOJ G START
END D EOJ
 K APSPX1("PROV"),APSEFDT  ;IHS/DSD/ENM 08/14/97
 Q
 ;---------------------------------------------------------
INIT ;
 S PSORX("QFLG")=0
 I $G(PSOFROM)']"" W !,"No option defined",! S PSORX("QFLG")=1 G INITX
 D:'$D(PSOPAR) ^PSOLSET I '$D(PSOPAR) S PSORX("QFLG")=1
 I $P($G(PSOPAR),"^",2),'$D(^XUSEC("PSORPH",DUZ)) S PSORX("VERIFY")=1
INITX Q
 ;
PT ;
 K DIC S PSORX("QFLG")=0
 S DIC=2,DIC(0)="QEAM" D ^DIC K DIC,DA
 I +Y'>0 S PSORX("QFLG")=1 G PTX
 S PSODFN=+Y,PSORX("NAME")=$P(Y,"^",2)
 S PSOQFLG=0 D ^PSOPTPST G:PSOQFLG PT  ; Post patient slection routine
 S DIC="^PS(55,",DLAYGO=55
 I '$D(^PS(55,PSODFN,0)) K DD,DO S DIC(0)="L",(DINUM,X)=PSODFN D FILE^DICN K DIC,DA,DR
 I $G(PSODFN),$P($G(^PS(55,PSODFN,0)),"^")="" S $P(^PS(55,PSODFN,0),"^")=PSODFN K DIK S DA=PSODFN,DIK="^PS(55,",DIK(1)=".01^B" D EN^DIK K DIK,DA  ;P133
 I $G(^PS(55,PSODFN,"PS"))']"" D
 .L +^PS(55,PSODFN):0 I '$T W *7,!!,"Patient Data is Being Edited by Another User!",! Q
 .;S DIE=55,DR=".02;1;3//OUTPATIENT;50",DA=PSODFN W !!,?5,">>PHARMACY PATIENT DATA<<",! D ^DIE L -^PS(55,PSODFN)  ;IHS/DSD/ENM 01/08/96
 .D:'$G(APSTAT) INP^PSOPTPST  ;IHS/OKCAO/POC 8/18/2000 FOR RTN EN1^APSQPMI
 .S DIE=55,DR=".02;1;3//^S X=APSTAT;50",DA=PSODFN W !!,?5,">>PHARMACY PATIENT DATA<<",! D ^DIE L -^PS(55,PSODFN)  ;IHS/DSD/ENM 01/08/96 WANT 3 TO BE STATUS FOUND IN APSTAT IHS/OKCAO/POC 8/18/2000
 ;S DIE=55,DR=".02:3;50",DA=PSODFN W !!,?5,">>PHARMACY PATIENT DATA<<",! D ^DIE L -^PS(55,PSODFN)
 ;IHS/DSD/ENM 01/26/98 CHECK FOR CORRECT PT STATUS - APSTAT SET IN
 ;PSOPTPST IF PT IS INPATIENT
 ;S PSOX=$S($G(APSTAT)]"":APSTAT,$G(^PS(55,PSODFN,"PS"))]"":^PS(55,PSODFN,"PS"),1:1) I PSOX]"" S PSORX("PATIENT STATUS")=$P($G(^PS(53,PSOX,0)),"^"),APST=PSOX
 ;S PSOX=$S($G(APSTAT)]"":APSTAT,1:1) I PSOX]"" S PSORX("PATIENT STATUS")=$P($G(^PS(53,PSOX,0)),"^"),APST=PSOX  ;IHS/OKCAO/POC 7/20/98 8/18/2000
 L +^PS(55,PSODFN):0 I '$T W *7,!!,"Patient Data is Being Edited by Another User!",! Q
 D:'$G(APSTAT) INP^PSOPTPST  ;IHS/OKCAO/POC 8/18/2000 FOR RTN EN1^APSQPMI
 S DIE=55,DR="3///^S X=APSTAT;50",DA=PSODFN D ^DIE L -^PS(55,PSODFN)  ;IHS/DSD/ENM 01/08/96 WANT 3 TO BE STATUS FOUND IN APSTAT IHS/OKCAO/POC 8/18/2000
 ;S PSOX=$G(^PS(55,PSODFN,"PS")) I PSOX]"" S PSORX("PATIENT STATUS")=$P($G(^PS(53,PSOX,0)),"^"),PSORX("PTST NODE")=$G(^(0))  ;UNCOMMENT THIS LINE AND COMMENT ABOVE LINE IHS/OKCAO/POC 8/18/2000
 S PSOX=$G(^PS(55,PSODFN,"PS")) I PSOX]"" S PSORX("PATIENT STATUS")=PSOX,PSORX("PTST NODE")=$G(^PS(53,PSOX,0))  ;UNCOMMENT THIS LINE AND COMMENT ABOVE LINE IHS/OKCAO/POC 8/18/2000 12/21/2001 JUST USE THE INTERNAL VALUE
 S:$G(PSORX("PATIENT STATUS"))']"" PSORX("PATIENT STATUS")=APSTAT  ;I GIVE UP HOW IS THIS NOT BEING SET SOMETIMES UNLESS PSOPAR IS SET AND THIS VARIABLE IS NOT WHEN THE USER IS JUMPING! IHS/OKCAO/POC 1/27/2002
 K DIE,DIC,DLAYGO,DR,DA,PSOX,APSTAT ;IHS/DSD/ENM 01/26/98
PTX ;
 K X,Y
 Q
 ;
PROFILE ;
 S (PSORX("REFILL"),PSORX("RENEW"))=0,PSOX=""
 D ^PSOBUILD
 ;I $D(PSOSD)'>1 W !,"This patient has no prescriptions" S:'$D(DFN) DFN=PSODFN D GMRA^PSODEM G PROFILEX
 I $D(PSOSD)'>1 W !,"This patient has no INHOUSE prescriptions.." S X="APSQSHOW" X ^%ZOSF("TEST") S:$T EN="SHOW" D:$T ^APSQSHOW S:'$D(DFN) DFN=PSODFN D GMRA^PSODEM G PROFILEX  ;IHS/DSD/ENM/POC 05/11/97 OUTSIDE RX CK
 S PSOX="" F  S PSOX=$O(PSOSD(PSOX)) Q:PSOX=""  S:$P(PSOSD(PSOX),"^",3)="" PSORX("RENEW")=1 S:$P(PSOSD(PSOX),"^",4)="" PSORX("REFILL")=1
 K PSOX
PROFILEX Q
 ;
NEW ;
 N PSOOPT S PSOOPT=3
 I $D(PSOSD)>1,$P($G(PSOPAR),"^",4) D ^PSORENW
 G:PSORX("QFLG") NEWX
 D ^PSONEW
 I $G(APSQFAST)=1 Q  ;G START ;G NEW ;TRY THIS IHS/OKCAO/POC
 G:PSORX("QFLG") NEWX
 I $G(PSORX("DO REFILL")),'PSORX("REFILL") W !,*7,"No prescriptions with refills allowed ",! G NEWX  ;IHS/DSD/ENM 02/14/97
 ;IHS/DSD/ENM 10/13/94 Next line chng to return to new rx module
 ;I $G(PSORX("DO REFILL")),$D(PSOSD)>1,PSORX("REFILL") W !,"Now entering Refill Option",! S PSOOPT=4 D ^PSOREF
 I $G(PSORX("DO REFILL")),$D(PSOSD)>1,PSORX("REFILL") W !,"Now entering Refill Option",! S PSOOPT=4 D ^PSOREF G APSX
 K APSPFLG  ;IHS/DSD/ENM 01/29/97
NEWX Q
 ;
FAST ;ADDED FOR FAST OPTION IHS/OKCAO/POC 
 N PSOOPT S PSOOPT=3  ;????
 S APSQFOPT=1  ;ADDED FOR PSODIR1 FOR CALCULATING DAYS SUPPLY DONT DO IT
 ;I $D(PSOSD)>1,$P($G(PSOPAR),"^",4) D ^PSORENW
 D ^PSODSPL
 I '$O(^APSQFA("A"),-1) W !,"NO QUICK CODES DEFINED" S PSORX("QFLG")=1  ;8/18/2000 IHS/OKCAO/POC 
 I '$O(^APSQFA("SD",PSOSITE,"")) W !,"NO QUICK CODES DEFINED" S PSORX("QFLG")=1  ;5/12/2001 IHS/OKCAO/POC
 G:PSORX("QFLG") FASTX
 D ^APSQFAS
 I $G(APSQFNEW)=1 S PSOFROM="NEW" Q  ;G START ;G NEW ;TRY THIS
 G:PSORX("QFLG") FASTX
 K APSQFOPT  ;FROM ABOVE
 I $G(PSORX("DO REFILL")),'PSORX("REFILL") W !,*7,"No prescriptions with refills allowed ",! G NEWX  ;IHS/DSD/ENM 02/14/97
 ;IHS/DSD/ENM 10/13/94 Next line chng to return to new rx module
 ;I $G(PSORX("DO REFILL")),$D(PSOSD)>1,PSORX("REFILL") W !,"Now entering Refill Option",! S PSOOPT=4 D ^PSOREF
 I $G(PSORX("DO REFILL")),$D(PSOSD)>1,PSORX("REFILL") W !,"Now entering Refill Option",! S PSOOPT=4 D ^PSOREF G APSXTOO
 K APSPFLG
FASTX Q
 ;
APSX ;IHS/DSD/ENM 10/13/94 Return to new Rx Module from Refill
 K PSORX("DO REFILL") S PSONEW("QFLG")=0,PSOOPT=3
 W !,*7,"<-<- Returning to ""NEW Rx"" Option <-<-",! G NEW
 Q
APSXTOO ;IHS/DSD/ENM 10/13/94 Return to FAST Rx Module from Refill
 K PSORX("DO REFILL") S PSONEW("QFLG")=0,PSOOPT=3
 W !,*7,"<-<- Returning to ""FAST Rx"" Option <-<-",! G FAST
 Q
 ;
REFILL ;
 I 'PSORX("REFILL") D  G REFILLX
 . W !,*7,"No prescriptions with refills allowed ",!
 . N PSOOPT S PSOOPT=4
 . D ^PSODSPL
 . K PSOQFLG Q
 ;
 N PSOOPT S PSOOPT=4
 I $D(PSOSD)>1 D ^PSOREF
REFILLX Q
 ;
BATCH ;
 D ^PSOBBC
 Q
EDIT ;
 G:$G(PSOSD)']"" EOJ  ;IHS/DSD/ENM 08/08/96
 D ^PSORXED
 Q
DELETE ;
 D ^PSORXDL
 Q
RETURN ;
 ;D ^PSORESK ;IHS/DSD/ENM 6/16/95
 D ^APSPRESK
 K PSIN   ;IHS/DSD/LWJ 9/3/99
 Q
PARTIAL ;
 D ^PSORXPAR
 Q
CANCEL ;
 D ^PSOCAN
 Q
EOJ ;
 K PSORX,PSOQFLG,PSOSD,PSODRUG,PSODFN,PSOOPT,PSOBILL,PSOCPAY,APSPZRP,RXP,APSPZDT  ;IHS/DSD/ENM 08/08/96 RXP + APSP ADDED
 Q
