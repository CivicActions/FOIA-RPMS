PSORXI ;IHS/DSD/JCM - LOGS PHARMACY INTERVENTIONS  [ 02/20/2001  3:35 PM ]
 ;;6.0;OUTPATIENT PHARMACY;**1,3**;09/03/97
 ;;6.0;OUTPATIENT PHARMACY;**52,131**;09/03/97
 ; This routine is used to create entries in the APSP
 ; INTERVENTION file.
 ;---------------------------------------------------------------
START ;   
 D INIT
 D DIC G:PSORXI("QFLG") END
 D EDIT
 S:'$D(PSONEW("PROVIDER")) PSONEW("PROVIDER")=$P(^APSPQA(32.4,PSORXI("DA"),0),"^",3)
END D EOJ
 Q
 ;---------------------------------------------------------------
INIT ;
 W !,"Now creating Pharmacy Intervention",!
 I $G(PSODRUG("IEN")) W "For  ",$P($G(^PSDRUG(PSODRUG("IEN"),0)),"^"),!
 K PSORXI
 S PSORXI("QFLG")=0
 Q
 ;
DIC ;
 K DIC,DR,DA,X,Y,DD,DO
 S DIC="^APSPQA(32.4,",DLAYGO=9009032.4,DIC(0)="L",X=DT
 S APSPCRI=$O(^APSPQA(32.3,"B","CRITICAL DRUG INTERACTION","")),APSPSIG=$O(^APSPQA(32.3,"B","SIGNIFICANT DRUG INTERACTION","")) ;IHS/DSD/ENM 12/22/97
 S DIC("DR")=".02////"_+PSODFN_";.04////"_DUZ_";.05////"_PSODRUG("IEN")_";.06///PHARMACY"
 S DIC("DR")=DIC("DR")_$S($G(PSORX("INTERVENE"))=1:";.07////"_APSPCRI,$G(PSORX("INTERVENE"))=2:";.07////"_APSPSIG,$G(PSORX("INTERVENE"))=3:";.07////6",$G(PSORX("INTERVENE"))=4:";.07////10",1:"")_";.14////0"_";.16////"_$S($G(PSOSITE)]"":PSOSITE,1:"") ;IHS/DSD ENM POC 05/08/98 IHS/OKCAO/POC 928/98 ADDED NON FORMULARY REQUEST
 D FILE^DICN K DIC,DR,DA
 ;IHS/DSD/ENM 01/06/98 OKCA/POC 11/17/97 MOD BEGIN
 S PSOZZZ=Y
 ;I Y>0 S PSORXI("DA")=+Y
 I PSOZZZ>0 S PSORXI("DA")=+PSOZZZ
 ;NEXT LINE FOR INTERACTING DRUG TO BE RECORDED
 I PSOZZZ>0 D:$G(PSORX("INTERVENE"))=1!($G(PSORX("INTERVENE"))=2)  ;IHS/DSD/ENM 01/06/98
 .S PSOZDATE=$P(Y,"^",2)
 .S ^APSPQA(32.4,+Y,13,0)="^^1^1^"_PSOZDATE_"^",^(1,0)="INTERACTING DRUG IS "_DRG
 .Q
 ;E  S PSORXI("QFLG")=1 G DICX
 I PSOZZZ<1 S PSORXI("QFLG")=1 G DICX
 ;IHS/DSD/ENM 01/06/98 END OF OKCA/POC MOD
 D DIE
DICX K X,Y
 Q
DIE ;
 K DIE,DIC,DR,DA
 S DIE="^APSPQA(32.4,",DA=PSORXI("DA"),DR=$S($G(PSORXI("EDIT"))]"":".03:1600",1:".03;.08")
 L +^APSPQA(32.4,PSORXI("DA")) D ^DIE K DIE,DIC,DR,X,Y,DA L -^APSPQA(32.4,PSORXI("DA"))
 W *7,!!,"See 'Pharmacy Intervention Menu' if you want to delete this",!,"intervention or for more options.",!
 Q
EDIT ;
 K DIR
 W !
 S DIR(0)="Y",DIR("A")="Would you like to edit this intervention "
 S DIR("B")="N"
 D ^DIR K DIR
 I $D(DIRUT)!'Y G EDITX
 S PSORXI("EDIT")=1
 D DIE
 G EDIT
EDITX K X,Y
 Q
 ;
EOJ ;
 K PSORXI
 Q
 ;
EN1(PSOX) ; Entry Point if have internal rx #
 N PSODFN,PSONEW,PSODRUG,PSOY
 I $G(^PSRX(+$G(PSOX),0))']"" W !,*7,"No prescription data" G EN1X
 S PSORXI("IRXN")=PSOX
 K PSOY S PSOY=^PSRX(PSORXI("IRXN"),0)
 S PSODFN=$P(PSOY,"^",2),PSONEW("PROVIDER")=$P(PSOY,"^",4)
 S PSODRUG("IEN")=$P(PSOY,"^",6)
 D START
EN1X Q
