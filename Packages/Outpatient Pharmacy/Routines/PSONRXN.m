PSONRXN ;IHS/DSD/JCM - GETS NEXT VALID RX NUMBER  [ 02/20/2001  3:41 PM ]
 ;;6.0;OUTPATIENT PHARMACY;**3**;09/03/97
 ;
 ; This routine asks for the next rx # if manually assigning rx#
 ; and gets next rx# if auto numbering.
 ;
 ;-------------------------------------------------------------------
 ;
MANUAL ; Entry Point to ask user for new rx #
 ;
 S PSONEW("DFLG")=0
 S DIR(0)="52,.01O"
 S DIR("A")="Select New Rx # for "_$S($G(PSORX("NAME"))]"":PSORX("NAME"),1:"")
 S:$G(PSONEW("RX #"))]"" DIR("B")=PSONEW("RX #")
 D DIR^PSODIR2 K DIR,DIC,DIE,DA
 I X="" S PSONEW("QFLG")=1 G MANUALX
 I "Pp"[Y K Y D ^PSODSPL G MANUAL
 I "Rr"[Y K Y S (PSONEW("QFLG"),PSORX("DO REFILL"))=1 G MANUALX
 I $G(PSODIR("DFLG"))=1 S (PSONEW("QFLG"),PSORX("QFLG"))=1 G MANUALX
 G:PSONEW("FIELD") MANUALX
 S PSOX=Y
 ;
CHECK ; Entry Point to check if valid new rx number
 S:'$D(PSOX) PSOX=$G(PSONEW("IRXN"))
 S PSONRXN("ERR FLG")=0
 S DIC="^PSRX(",DIC(0)="XZ",X=PSOX D ^DIC K DIC
 I Y'<0 D  G MANUALX
 . W *7,!,?10,"NOT A NEW PRESCRIPTION #",!,"RXN ",Y(0,0),!,"PATIENT :",$S($D(^DPT(+$P(Y(0),"^",2),0)):$P(^(0),"^"),1:"UNKNOWN"),!,"DRUG :",$S($D(^PSDRUG(+$P(Y(0),"^",6),0)):$P(^(0),"^"),1:"UNKNOWN")
 . S PSONRXN("ID")=$P(Y(0),"^",13)
 . I PSONRXN("ID") W !,"ISSUED: ",$E(PSONRXN("ID"),4,5),"-",$E(PSONRXN("ID"),6,7),"-",$E(PSONRXN("ID"),2,3)
 . K PSONRXN("ID"),Y
 . W:$G(PSODRUG("NAME")) !,"RX DELETED",!
 . S PSONRXN("ERR FLG")=1
 . Q
 S PSONEW("RX #")=PSOX
EN1 S PSOX1=$S($P(^PSRX(0),"^",3):$P(^PSRX(0),"^",3),1:1)
LOOP1 F  S PSOX1=PSOX1+1 Q:'$D(^PSRX(PSOX1))
 L +^PSRX(PSOX1):0 I $D(^PSRX(PSOX1))!'$T L -^PSRX(PSOX1) G LOOP1
 S PSONEW("IRXN")=PSOX1
MANUALX I $G(PSONRXN("ERR FLG"))=1 S PSONEW("DFLG")=1
 K PSONRXN,X,Y,DIRUT,DTOUT,DUOUT,DIC,DIE,DR,PSOX,PSODIR,PSOX1
 Q
 ;
AUTO ; Entry point for getting next rx # if autonumbering
 S PSONEW("QFLG")=0
 ;S PSONRXN("TYPE")=$S($G(^PS(59,+PSOSITE,2))']"":8,PSODRUG("DEA")["A":3,1:8)
 S PSONRXN("TYPE")=$S($G(^PS(59,+PSOSITE,2))']"":8,$G(^(2))=0:8,PSODRUG("DEA")["A":3,1:8) ;IHS/DSD/ENM 01/24/97
 L +^PS(59,+PSOSITE,PSONRXN("TYPE"))
 S PSOX1=^PS(59,+PSOSITE,PSONRXN("TYPE")),PSONRXN("LO")=$P(PSOX1,"^")
 S PSONRXN("HI")=$P(PSOX1,"^",2),PSOI=$P(PSOX1,"^",3),PSONEW("OLD LAST RX#",PSONRXN("TYPE"))=PSOI
 S:PSOI<PSONRXN("LO") PSOI=PSONRXN("LO")
LOOP2 ;F  S PSOI=PSOI+1 D:PSOI>PSONRXN("HI") FATAL Q:'$D(^PSRX(PSOI))!PSONEW("QFLG")
 ;TRY THIS SUBROUTINE TO REPLACE ABOVE LINE IHS/OKCAO/POC 11/18/99
 F  S PSOI=PSOI+1 D:PSOI>PSONRXN("HI") FATAL D  Q:('PSOHIT)!(PSONEW("QFLG"))
 .N PSOIEN,PSODIV
 .S PSOHIT=0
 .S PSOIEN=0 F  S PSOIEN=$O(^PSRX("B",PSOI,PSOIEN)) Q:PSOHIT!(PSOIEN'=+PSOIEN)  D
 ..S PSODIV=$P($G(^PSRX(PSOIEN,2)),"^",9)
 ..Q:'PSODIV  ;I DON'T KNOW WHY NOT A VALUE SO QUIT
 ..I +PSOSITE=PSODIV S PSOHIT=1 Q
 G:PSONEW("QFLG") AUTOX
 ;L +^PSRX(PSOI):0 I $D(^PSRX(PSOI))!'$T L -^PSRX(PSOI) G LOOP2
 ;TRY THIS SUBROUTINE TO REPLACE ABOVE LINE IHS/OKCAO/POC 11/18/99
 D  G:PSOHIT LOOP2
 .N PSOIEN,PSODIV
 .S PSOHIT=0
 .S PSOIEN=0 F  S PSOIEN=$O(^PSRX("B",PSOI,PSOIEN)) Q:PSOHIT!(PSOIEN'=+PSOIEN)  D
 ..S PSODIV=$P($G(^PSRX(PSOIEN,2)),"^",9)
 ..Q:'PSODIV  ;I DON'T KNOW WHY NOT A VALUE SO QUIT
 ..I +PSOSITE=PSODIV S PSOHIT=1 Q
 K DIC,DIE,DA
 S DIE=59,DA=PSOSITE
 S DR=$S(PSONRXN("TYPE")=8:"2003////"_PSOI,PSONRXN("TYPE")=3:"1002.1////"_PSOI,1:"2003////"_PSOI)
 S (PSONEW("RX #"),PSONEW("IRXN"))=PSOI
 D ^DIE K DIE,DIC,DR,DA
 L -^PS(59,+PSOSITE,PSONRXN("TYPE"))
AUTOX K PSOX1,PSONRXN,PSOI,X,Y
 Q
 ;
FATAL ;error in autonum queue if necessary and quit
 W !!,*7,"FATAL ERROR IN AUTONUMBERING SCHEME - NO NUMBERS LEFT",!!,*7
 S PSONEW("QFLG")=1
 Q
