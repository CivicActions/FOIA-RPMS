BMCFPRN4 ; IHS/PHXAO/TMJ - PRINT APPOINTMENT LETTER ;      [ 09/27/2006  2:01 PM ]
 ;;4.0;REFERRED CARE INFO SYSTEM;**16**;JAN 09, 2006;Build 168
 ;4.0*16 1.18.2024 IHS.OIT.FCJ NEW ROUTINE
 ;
PRINT ;
 ;print referral form
 S BMCR0=^BMCREF(BMCREF,0),BMCPG=0,BMCDFN=$P(BMCR0,U,3)
 D @("HEAD"_(2-($E(IOST,1,2)="C-")))
 S BMCQUIT=0
 W !!!!!!!!!
 W ?25,"IMPORTANT REFERRAL INFORMATION",!!!
 S Y=DT D DD^%DT W Y
 W !
DEMO ;Demographic Data
 ;Age - If under 19 Print "To Parents of"
 S BMCAGE=$$VAL^XBDIQ1(2,$P(BMCR0,U,3),.033)
 S X=$$VAL^XBDIQ1(90001,BMCREF,.03)
 S BMCPAT=$P(X,",",2)_" "_$P(X,",",1) S:$P(X,",",3)'="" BMCPAT=BMCPAT_" "_$P(X,",",3)
 W:BMCAGE<(BMCMNOR+1) !,"**To the Parents of "_BMCPAT,":",!
 S X=BMCPAT,C=0,T=0,N=1 D W Q:BMCQUIT
 S X=""
 S:$$VAL^XBDIQ1(9000001,$P(BMCR0,U,3),1602.2)]"" X=$$VAL^XBDIQ1(9000001,$P(BMCR0,U,3),1602.2) S C=0,T=0,N=1 D W Q:BMCQUIT
 S X=$$VAL^XBDIQ1(9000001,$P(BMCR0,U,3),1603.2)
 I X'="" S X=X_", "_$$VAL^XBDIQ1(9000001,$P(BMCR0,U,3),1604.2)_"   "_$$VAL^XBDIQ1(9000001,$P(BMCR0,U,3),1605.2),T=0,C=0,N=1 D W Q:BMCQUIT
 ;
REFTO ;
 I $$VALI^XBDIQ1(90001,BMCREF,.04)="I" D I Q:BMCQUIT
 E  D C Q:BMCQUIT
 ;
TXT1 ;Instruction text
 W !
 K BMCWP
 S BMCNODE=1,BMCFILE=90001.33,BMCDA=BMCFTYPE D WP^BMCFDRA
 ;
 S BMCY=0 F  S BMCY=$O(BMCWP(BMCY)) Q:BMCY'=+BMCY!(BMCQUIT)  D
 .I $Y>(IOSL-3) D HEAD Q:BMCQUIT
 .W !,BMCWP(BMCY)
 ;
TXT2 ;Instruction text
 W !
 K BMCWP
 S BMCCSPVR=$$VAL^XBDIQ1(90001.31,DUZ(2),.13)
 S BMCNODE=2,BMCFILE=90001.33,BMCDA=BMCFTYPE D WP^BMCFDRA
 ;
 S BMCY=0 F  S BMCY=$O(BMCWP(BMCY)) Q:BMCY'=+BMCY!(BMCQUIT)  D
 .I $Y>(IOSL-3) D HEAD Q:BMCQUIT
 .W !,BMCWP(BMCY)
 K BMCCSPVR
 ;
TXT3 ;Site specific text
 ;
 W !
 K BMCWP
 S BMCNODE=$S(BMCRIO="I":3601,BMCRIO="O":3501,1:"")
 Q:BMCNODE=""
 S BMCFILE=90001.31,BMCDA=DUZ(2) D WP^BMCFDRA
 ;
 S BMCY=0 F  S BMCY=$O(BMCWP(BMCY)) Q:BMCY'=+BMCY!(BMCQUIT)  D
 .I $Y>(IOSL-3) D HEAD Q:BMCQUIT
 .W !,BMCWP(BMCY)
 ;
TXTS ;Signature text
 W !!
 S X="Thank you, ___________________________",C=0,T=0,N=1 D W
 S X="PRC STAFF",C=0,T=11,N=1 D W Q:BMCQUIT
 W !
 Q
W ;
 Q:X=""
 NEW %
 S %=$L(X)
 I $Y>(IOSL-4) D HEAD Q:BMCQUIT
 I N F I=1:1:N W !
 I $G(C) W ?(IOM-$L(X)/2),X Q
 S %=$S($G(T):T,1:0) W ?%,X
 Q
W1 ;
 Q:X=""
 NEW %
 S %=$L(X)
 I N F I=1:1:N W !
 I $G(C) W ?(IOM-$L(X)/2),X Q
 S %=$S($G(T):T,1:0) W ?%,X
 Q
C ;CHS OR OTHER TYPE
 S BMCV=$P(BMCR0,U,7)
 Q:'BMCV
 S X="Provider:  "_$$VAL^XBDIQ1(90001,BMCREF,.07) S N=3,C=0,T=0 D W1
 D REFNO Q:BMCQUIT
 I $P(BMCR0,U,9) S X=$$VAL^XBDIQ1(90001,BMCREF,.09) S N=1,C=0,T=11 D W
 D APTDT Q:BMCQUIT
 S X=" Address:  "_$$VAL^XBDIQ1(9999999.11,BMCV,1301)_$S($$VAL^XBDIQ1(9999999.11,BMCV,1310)]"":",  "_$$VAL^XBDIQ1(9999999.11,BMCV,1310),1:"") S N=1,C=0,T=0 D W1
 D VSTNO Q:BMCQUIT
 S X=$$VAL^XBDIQ1(9999999.11,BMCV,1302)_",  "_$$VAL^XBDIQ1(9999999.11,BMCV,1303)_"   "_$$VAL^XBDIQ1(9999999.11,BMCV,1304),N=1,C=0,T=11 D W1
 S X=" Phone #:  "_$S($$VAL^XBDIQ1(9999999.11,BMCV,1109)]"":"("_$$VAL^XBDIQ1(9999999.11,BMCV,1109)_")",1:"") S N=1,C=0,T=0 D W Q:BMCQUIT      ;phone
 Q
I ; OTHER IHS FACILITY TYPE
 S BMCV=$P(BMCR0,U,8)
 Q:'BMCV
 S X="Provider:  "_$$VAL^XBDIQ1(90001,BMCREF,.08) S N=3,C=0,T=0 D W1
 D REFNO Q:BMCQUIT
 I $P(BMCR0,U,9) S X=$$VAL^XBDIQ1(90001,BMCREF,.09) S N=1,C=0,T=11 D W  ;SPECIFIC PROVIDER NAME
 D APTDT Q:BMCQUIT
 ;ADDRESS FR SITE PARM FILE
 I $$VAL^XBDIQ1(90001.31,BMCV,201)]"" D
 .S X=" Address: "_$$VAL^XBDIQ1(90001.31,BMCV,201) S N=1,C=0,T=0 D W1
 .D VSTNO Q:BMCQUIT
 .I $$VAL^XBDIQ1(90001.31,BMCV,202)]"" S X=$$VAL^XBDIQ1(90001.31,BMCV,202)_",  "_$$VAL^XBDIQ1(90001.31,BMCV,203)_"   "_$$VAL^XBDIQ1(90001.31,BMCV,204),N=1,C=0,T=11 D W1
 E  D
 .S X=" Address:  "_$$VAL^XBDIQ1(9999999.06,BMCV,.14) S N=1,C=0,T=0 D W1
 .D VSTNO Q:BMCQUIT
 .S X=$$VAL^XBDIQ1(9999999.06,BMCV,.15)_",  "_$$VAL^XBDIQ1(9999999.06,BMCV,.16)_"   "_$$VAL^XBDIQ1(9999999.06,BMCV,.17),N=1,C=0,T=11 D W1
 S X="Phone #:  "_$S($$VAL^XBDIQ1(9999999.06,BMCV,.13)]"":"("_$$VAL^XBDIQ1(9999999.06,BMCV,.13)_")",1:"") S N=1,C=0,T=0 D W Q:BMCQUIT
 Q
L ;
 S T=0,X=$TR($J(" ",IOM)," ","_") S N=1,C=0 D W Q:BMCQUIT
 Q
D ;
 S T=0,X=$TR($J(" ",IOM)," ","-") S N=1,C=0 D W Q:BMCQUIT
 Q
S ;
 S T=0,X=$TR($J(" ",IOM)," ","*") S N=1,C=0 D W Q:BMCQUIT
 Q
REFNO ;
 S X="Referral #: "_$$VAL^XBDIQ1(90001,BMCREF,.02)_$P($G(^BMCREF(BMCREF,1)),U)_$$CALLIN^BMCRLU(BMCREF),N=0,C=0,T=50 D W1
 Q
APTDT ;
 S X=$S($P(BMCR0,U,14)="I":"Admission Date",1:"Appointment Date")_": "_$$AVDOS^BMCRLU(BMCREF,"E"),C=0,T=50
 S N=$S($P(BMCR0,U,9)'="":0,1:1) D W1
 Q
VSTNO ;
 S X="Approved Visits: "_$S($P(BMCR0,U,14)="O":$$VAL^XBDIQ1(90001,BMCREF,1111),1:1),N=0,C=0,T=50 D W1
 Q
NOPRV ;
 S X="Provider:  " S N=3,C=0,T=0 D W1 Q:BMCQUIT
 D REFNO Q:BMCQUIT
 S X=" Address:  " S N=3,C=0,T=0 D W1 Q:BMCQUIT
 D APTDT Q:BMCQUIT
 W ! D VSTNO
 S X=" Phone #:  " S N=3,C=0,T=0 D W Q:BMCQUIT
 Q
HEAD ;
 NEW N,T,C,X,Y
 I $E(IOST)="C",IO=IO(0) W ! S DIR(0)="EO" D ^DIR K DIR I Y=0!(Y="^")!($D(DTOUT)) S BMCQUIT=1 Q
HEAD1 ;
 W:$D(IOF) @IOF
HEAD2 ;
 I 'BMCPG S BMCPG=BMCPG+1 Q
 S BMCPG=BMCPG+1 W:$D(IOF) @IOF W !,?(IOM-20),"Page ",BMCPG
 Q
