PRINT1 ;
 ;BMC*4.0*16 MULTIPLE CHANGES TO TEST FOR CSV TYPE FORMAT
 I BMCCSV,$Y>(IOSL-5) D HEAD Q:$D(BMCQUIT)
 W !,$S(BMCSTYPE="B":$$AVDOS^BMCRLU(BMCREF,"C"),1:$$FMTE^XLFDT($P(BMCRREC,U),"5D"))
