 W @$S(BMCCSV:"BMCSPRTR",1:"?49"),$S($P(BMCRREC,U,6):$$VAL^XBDIQ1(200,$P(BMCRREC,U,6),1),1:"--")
 W @$S(BMCCSV:"BMCSPRTR",1:"?54"),$E($$VAL^XBDIQ1(90001,BMCREF,.04),1,3)
 S BMCFAC=$$FACREF^BMCRLU(BMCREF)
 I BMCFAC="" S BMCFAC="????"
 W @$S(BMCCSV:"BMCSPRTR",1:"?59"),$E(BMCFAC,1,20)
 I $$VAL^XBDIQ1(90001,BMCREF,.09)]"" W !,@$S(BMCCSV:"BMCSPRTR",1:"?59"),$E($$VAL^XBDIQ1(90001,BMCREF,.09),1,20)
