BMCFDRAP ;IHS/OIT/FCJ - DRIVER TO PRINT APPOINTMENT LETTER ;
 ;;4.0;REFERRED CARE INFO SYSTEM;**16**;JAN 09, 2006;Build 168
 ;4.0*16 1.17.2024 IHS.OIT.FCJ NEW ROUTINE
 ;
START ;
 W:$D(IOF) @IOF
 W !,"**********  REFERRAL APPOINTMENT LETTER PRINT  **********",!!
 W "This will produce an Appointment Letter for CHS, Other IHS Facilities and Other type referrals.",!
 S BMCQUIT=0
GETTYPE ;Set Text Verbiage
 I '$D(^BMCTFORM("B","APPOINTMENT LETTER")) W !,?5,"THE APPOINTMENT LETTER IS NOT AVAILABLE" H 2 G XIT
 S BMCFTYPE=0,BMCFTYPE=$O(^BMCTFORM("B","APPOINTMENT LETTER",BMCFTYPE))
GETREF ;get referral entry
 W !! S BMCREF=""
 S DIC="^BMCREF(",DIC(0)="AEMQS",DIC("A")="Select Referral by Patient Name, date of referral or referral #: " D ^DIC K DA,DIC
 G:Y=-1 XIT
 S BMCREF=+Y
 I $$VALI^XBDIQ1(90001,BMCREF,.04)="N" W !?5,"This is an In-house referral, please select a CHS, IHS Facilities",!?5,"or Other type referral.",! G GETREF
 I $D(^BMCTFORM(BMCFTYPE,11)) X ^BMCTFORM(BMCFTYPE,11) G:BMCQUIT GETREF
 ;
ZIS ;
 W !! S XBRC="COMP^BMCFDRAP",XBRP="PRINT^BMCFDRAP",XBNS="BMC",XBRX="XIT^BMCFDRAP"
 D ^XBDBQUE
 Q
 ;
PRINT ;EP
 X:$D(^BMCTFORM(BMCFTYPE,12)) ^BMCTFORM(BMCFTYPE,12)
 Q
XIT ;
 K BMCCAP,BMCCHSR,BMCDA,BMCFILE,BMCFTYPE,BMCIOM,BMCKPDA,BMCNODE,BMCPG,BMCQUIT,BMCR0,BMCREF,BMCRNS,BMCV,BMCWP,BMCX,BMCY,BMCI,BMCDFN,BMCCHSAS
 K A,C,D,D0,D1,DA,DD,DDSFILE,DI,DIADD,DIC,DICR,DIE,DIK,DINUM,DIPGM,DIQ,DIR,DIWF,DIWL,DIWR,DLAYGO,DO,DQ,DR,DTOUT,F,G,I,J,N,P,T,X,Y,Z
 K BMCPROUT,BMCN,BMCNUM,BMCAGE,BMCFIRST,BMCLAST,BMCLTYP,BMCSIR
 Q
COMP ;
 Q
WP ;EP - Entry point to print wp fields pass node in BMCWP
 ;PASS FILE IN BMCFILE, ENTRY IN BMCREF
 NEW G,P,BMCX
 K BMCWP
 K ^UTILITY($J,"W")
 S BMCX=0,P=0
 S G=$S($G(G)]"":G,1:^DIC(BMCFILE,0,"GL")),G=G_BMCDA_","_BMCNODE_",BMCX)"
 S DIWR=$S($G(BMCIOM):BMCIOM,1:IOM),DIWL=0 F  S BMCX=$O(@G) Q:BMCX'=+BMCX  D
 .S Y=$P(G,")")_",0)"
 .S X="" I $G(BMCCAP)]"",BMCX=1 S X=BMCCAP
 .S X=X_@Y D ^DIWP
 .Q
WPS ;EP
 S Z=0 F  S Z=$O(^UTILITY($J,"W",DIWL,Z)) Q:Z'=+Z  S P=P+1,BMCWP(P)=^UTILITY($J,"W",DIWL,Z,0)
 K DIWL,DIWR,DIWF,Z
 K ^UTILITY($J,"W"),BMCNODE,BMCFILE,BMCDA,G,BMCCOL,BMCCAP
 Q
