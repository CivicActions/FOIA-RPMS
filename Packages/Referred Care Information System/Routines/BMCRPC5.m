CHKDUP(NRPAT,NRTYPE,NRPRV,NRVEN,NRIO,NRPRI) ;EP FR BMCADD;CHECKDUP PATIENT ;BMC*4.0*16
 ;CRITERIA:PATIENT-.03,REF TYPE-.04(BMCRTYPE),REF PROV-.06,VENDOR-.07,PRIORITY-.32,VISIT TYPE-.14(BMCRIO)
 N BMCTPRV,BMCTVEN,BMCTPRI,BMCRTYPE,BMCRIO,BMCDUP,BMCTDT,BMCDFN,BMCTIEN,BMCREFN
