EDITRD(RST,PATIEN,REFIEN,DXS,RDCODE,RDNAR) ;EP - Edit RCIS Diagnosis
 N RDIEN,DX,LOOP,K,SAVE,C,D,T,PS
 S RDIEN=$O(^BMCDX("AD",REFIEN,""))
 ;Delete all existing Dxs
 I +RDIEN>0 F  D  Q:(+RDIEN'>0)
 .S DIK="^BMCDX(",DA=RDIEN
 .D ^DIK
 .S RDIEN=$O(^BMCDX("AD",REFIEN,RDIEN))
 S LOOP=$P(DXS,"`~",1),SAVE=1
 I +LOOP<=0 D SAVERD(.RST,$G(RDCODE),$G(RDNAR),PATIEN,REFIEN,"","") Q "~`1"
 S DXS=$P(DXS,"`~",2)
 F K=1:1:LOOP  D
 .S DX=$P(DXS,"~",K),C=$P(DX,"^",2),D=$P(DX,"^",3),T=$P(DX,"^",4),PS=$P(DX,"^",5)
 .I $G(RDCODE)=C S SAVE=0 ;New Dx
 .D SAVERD(.RST,C,D,PATIEN,REFIEN,T,PS)
 I SAVE D SAVERD(.RST,RDCODE,RDNAR,PATIEN,REFIEN,"","")
 I $P($G(RST),"^")="~`0" Q RST
 Q "~`1"
 ;
