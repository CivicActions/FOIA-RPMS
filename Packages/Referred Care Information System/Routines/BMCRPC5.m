SAVERD(RST,CODE,DESC,PATIEN,REFIEN,TYPE,PRISEC) ;EP - Save RCIS Diagnosis
 ;Test Call: W $$SAVERD^BMCRPC5("C7A.094","Microaneurysm of right retinal artery1","18896","135286")
 N PNIEN,RDARRAY,OUT,ICDIEN
 I $G(DESC)="" S RST="~`0^Provider Narrative is missing!" Q RST
 S PNIEN=$$INSRTPN(DESC),CODE=$P($G(CODE),"|",1)
 I +PNIEN=0 S RST="~`0^Error in saving Provider Narrative!" Q RST
 I $G(CODE)="" S RST="~`0^ICD Code is missing" Q RST
 D LIST^DIC(80,"","@;.01","Q",1,$E(CODE,1,$L(CODE)-1),CODE,,,,"OUT")
 S ICDIEN=$G(OUT("DILIST",2,1))
 I +ICDIEN'>0 S RST="~`0^ICD DIAGNOSIS:"_CODE_" not found!" Q RST
