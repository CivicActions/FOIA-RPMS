 ;;2.0;REFERRED CARE INFO SYSTEM;**1**;JUN 05, 2002
 ;PATCH #1 Fixes process bug
START ;
 S (BMCBT,BMCBTH)=$H,BMCJOB=$J,BMCRCNT=0
 D PROCESS,END
 Q
 ;
PROCESS ;
 S BMCODAT=$O(^BMCCOM("B",BMCSD)) I BMCODAT="" S BMCET=$H Q
 S BMCODAT=BMCSD_".9999" F  S BMCODAT=$O(^BMCCOM("B",BMCODAT)) Q:BMCODAT=""!((BMCODAT\1)>BMCED)  D R1
 Q
 ;
R1 ;
 S BMCRDT="" F  S BMCRDT=$O(^BMCCOM("B",BMCODAT,BMCRDT)) Q:BMCRDT'=+BMCRDT  S BMCRREC=^BMCCOM(BMCRDT,0) D PROC
 Q
END ;
 S BMCET=$H
 Q
PROC ;
 Q:'$D(^BMCCOM(BMCRDT,0))
 S BMCRREC=^BMCCOM(BMCRDT,0),DFN=$P(BMCRREC,U,2),BMCDATE=$P(BMCRREC,U,1) ;IHS/PHXAO/TMJ Patch #1 10/23/02
 S BMCREF=$P(BMCRREC,U,3)
 S BMCREVP=$P(BMCRREC,U,4)
 ;
 Q:$P(^BMCREF(BMCREF,0),U,4)="N"
 Q:$P(^BMCREF(BMCREF,0),U,5)'=BMCFAC
 I BMCCREV'=0,(BMCCREV'=BMCREVP) Q
 ;S BMCDATE=$S($$AVDOS^BMCRLU(BMCRDT,"I")]"":$$AVDOS^BMCRLU(BMCRDT,"I"),1:"<none>") ;IHS/PHXAO/TMJ Patch #1 10/23/02
 S ^XTMP("BMCRR20",BMCJOB,BMCBTH,"DATA HITS",BMCDATE,$P(^DPT(DFN,0),U),BMCRDT)="",BMCRCNT=BMCRCNT+1
 Q
