BMCFPRNN ; IHS/OIT/FCJ - PRINT REFERRAL CASE NOTES FORMS ;   [ 09/27/2006  1:33 PM ]
 ;;4.0;REFERRED CARE INFO SYSTEM;**16**;JAN 09, 2006;Build 168
 ;IHS/OIT/FCJ NEW ROUTINE
 ;
PRT ;print referral form
 ;NEW REFTO,BMCCHSAS,BMCCMT,BMCDA,BMCDFN,BMCFILE,BMCFTYP,BMCNODE,BMCPCON,BMCQUIT,BMCR0,BMCV,BMCVIEN,BMCX,BMCY
 NEW DTOUT,I,X,BMCQ
 ;
 S BMCR0=^BMCREF(BMCREF,0),BMCDFN=$P(BMCR0,U,3)
 D HEAD1
 S BMCQUIT=0
 S X="CASE REVIEW COMMENTS" S N=1,C=1 D W
 S BMCFTYP=$S($E($P(^BMCTFORM(BMCFTYPE,0),U),1,4)="CALL":"CI",1:"S")
 S X=$$VAL^XBDIQ1(90001,BMCREF,.01),C=0,N=0,T=66 D W
 D S
DEMO ;Demographic Data
 Q:BMCQUIT
 S X="Patient Name:  "_$$VAL^XBDIQ1(90001,BMCREF,.03),C=0,T=0,N=1 D W Q:BMCQUIT
 S X="ID Number:  "_$$HRN^AUPNPAT($P(BMCR0,U,3),DUZ(2),2),N=0,T=50,C=0 D W Q:BMCQUIT
 S X="Referral: "_$$VAL^XBDIQ1(90001,BMCREF,.02)_$$CALLIN^BMCRLU(BMCREF)_" "_$$VAL^XBDIQ1(90001,BMCREF,101) S N=1,C=0,T=0 D W Q:BMCQUIT
 D L
CASECOM ;
 S BMCCMTS=999,BMCCIEN=0
 F  S BMCCIEN=$O(^BMCCOM("AD",BMCREF,BMCCIEN)) Q:BMCCIEN'?1N.N  D
 .Q:$P(^BMCCOM(BMCCIEN,0),U,5)'="C"
 .S BMCCMT(BMCCMTS)=BMCCIEN
 .S BMCCMTS=BMCCMTS-1
 I BMCCMTS<999 D
 .S BMCCMTS=0 F  S BMCCMTS=$O(BMCCMT(BMCCMTS)) Q:BMCCMTS'?1N.N  D
 ..S BMCCIEN=BMCCMT(BMCCMTS)
 ..S X="Case Review Comments:  "_$$VAL^XBDIQ1(90001.03,BMCCIEN,.01),C=0,T=0,N=2 D W
 ..I $Y>($S($G(PGL)]"":PGL,1:IOSL)-3) D HEAD Q:BMCQUIT
 ..S BMCNODE=1,BMCIOM=70,BMCFILE=90001.03,BMCDA=BMCCIEN,BMCNODE=1
 ..S Y=0 F  S Y=$O(^BMCCOM(BMCCIEN,1,Y)) Q:Y'=+Y  D
 ...I $Y>($S($G(PGL)]"":PGL,1:IOSL)-3) D HEAD Q:BMCQUIT
 ...W !?5,^BMCCOM(BMCCIEN,1,Y,0)
 I BMCCMTS=999 S X="No Case Review Comments",C=0,T=0,N=2 D W
 Q
 ;
W ;
 Q:X=""
 NEW %
 S %=$L(X)
 I $Y>($S($G(PGL)]"":PGL,1:IOSL)-4) D HEAD Q:BMCQUIT  ;GDIT/HS/BEE 10/19/17 - p12 CR#5450:Changed IOSL to PGL
 I N F I=1:1:N W !
 I $G(C) W ?(IOM-$L(X)/2),X Q
 S %=$S($G(T):T,1:0) W ?%,X
 Q
L ;
 S T=0,X=$TR($J(" ",IOM)," ","_"),N=1,C=0 D W Q:BMCQUIT
 Q
D ;
 S T=0,X=$TR($J(" ",IOM)," ","-"),N=1,C=0 D W Q:BMCQUIT
 Q
S ;
 S T=0,X=$TR($J(" ",IOM)," ","*"),N=1,C=0 D W Q:BMCQUIT
 Q
WP ;
 D WP^BMCFDR
 Q
 ;
HEAD ;
 Q:$G(PRTGUI)
 NEW N,T,C,X,Y
 I $E(IOST)="C",IO=IO(0) W ! S DIR(0)="EO" D ^DIR K DIR I Y=0!(Y="^")!($D(DTOUT)) S BMCQUIT=1 Q
 Q
HEAD1 ;
 W:$D(IOF) @IOF
HEAD2 ;
 I 'BMCPG S BMCPG=BMCPG+1 Q
 S BMCPG=BMCPG+1 W:$D(IOF) @IOF W !,?(IOM-20),"Page ",BMCPG
 Q
