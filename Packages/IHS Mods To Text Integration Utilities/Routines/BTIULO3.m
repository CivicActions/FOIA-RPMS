 . S RETURN(CNT)=RETURN(CNT)_$E(SITE,1,18)_$J("",19-$L($E(SITE,1,18)))_VISDT
 . S CNT=CNT+1,RETURN(CNT)=""
 . I SERIES]""!(MANU]"") S RETURN(CNT)=$E(SERIES,1,17)_$J("",18-$L($E(SERIES,1,17)))_MANU
 . S CNT=CNT+1,RETURN(CNT)=$S(NDC]"":$J("",18)_NDC,1:"")
