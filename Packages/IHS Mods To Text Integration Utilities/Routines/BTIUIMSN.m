EN(DFN,TIUDA,CONCT,DESCT,ICD) ;Send to problem list the vaccine code
 N VISIT,CONCT,DESCT,PROB,ARR,RET,VIMM,INP
 S RET=""
 S VISIT=$P($G(^TIU(8925,TIUDA,0)),U,3)
 S VIMM=$O(^AUPNVIMM("AD",VISIT,""))
 Q:'+VIMM
 S CONCT=$G(CONCT,713404003)
 S DESCT=$G(DESCT,3289271014)
 S ICD=$G(ICD,"Z23.")
 S PROB=$O(^AUPNPROB("APCT",DFN,CONCT,""))
 I PROB=""  D
 .S ARR(1)="P"_U_CONCT_U_DESCT_U_U_ICD_U_$G(DUZ(2))
 .D SET^BGOPROB(.RET,DFN,PROB,VISIT,.ARR)
 .I +RET S PROB=RET
 I PROB'="" D
 .S RET=""
 .S INP="0"_U_VISIT_U_PROB_U_DFN_U_U_DESCT_U_CONCT_U_ICD_U_U_DUZ
 .D SET^BGOVPOV(.RET,INP)
 Q
