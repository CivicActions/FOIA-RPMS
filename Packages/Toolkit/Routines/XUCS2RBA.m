PRINT ; Print Report
 S (XUCSAOG,XUCSI)=0 D S2^XUCSUTL3
 S XUCSDT=0 F  S XUCSDT=$O(^TMP($J,3,+XUCSDT)) Q:+XUCSDT=0!(XUCSEND)  D
 . S XUCSVG="" F  S XUCSVG=$O(^TMP($J,3,+XUCSDT,XUCSVG)) Q:XUCSVG=""!(XUCSEND)  D SITEH^XUCSUTL3 D
 .. S XUCSAP="" F  S XUCSAP=$O(^TMP($J,3,+XUCSDT,XUCSVG,XUCSAP)) Q:XUCSAP=""!(XUCSEND)  S XUCSTG=^(XUCSAP,"ZZZTGR") D HDR^XUCS2RA D  I 'XUCSEND D AVE^XUCS2RA,PAUSE^XUCSUTL
 ... S XUCSUCI="" F  S XUCSUCI=$O(^TMP($J,3,+XUCSDT,XUCSVG,XUCSAP,XUCSUCI)) Q:XUCSUCI=""!(XUCSUCI="ZZZTGR")!(XUCSEND)  D SHDR^XUCS2RA D  I 'XUCSEND D PAO
 .... S XUCSGBL="" F  S XUCSGBL=$O(^TMP($J,3,+XUCSDT,XUCSVG,XUCSAP,XUCSUCI,XUCSGBL)) Q:XUCSGBL=""!(XUCSEND)  S XUCSXG=^(XUCSGBL) D WL
 .... Q
 ... Q
 .. Q
 . Q
 Q
PAO ; Print All Other for UCI
 I XUCSI>0 DO
 . N X
 . W !,"<thresh" S XUCSX=((XUCSAOG/XUCSI)/$P(XUCSXG,"^")) D WLD
 . W !,?16,"# of Globals <thresh:" S X=$J(XUCSI,8,0) W ?38,X
 . W !,?16,"Tot. GREFs <thresh:" S X=$J(XUCSAOG,8,0) W ?38,X
 . I $E(IOST)="C" D PAUSE^XUCSUTL
 . Q
 S (XUCSAOG,XUCSI)=0
 Q
WL ; Print detail Line
 S XUCSX=$P(XUCSXG,"^",2)/$P(XUCSXG,"^") I XUCSX<XUCSTH S XUCSI=XUCSI+1,XUCSAOG=XUCSAOG+$P(XUCSXG,"^",2) Q
 W !,XUCSGBL
WLD ; Draw Picture
 S XUCSX1=XUCSX\XUCSS2G,XUCSX2=$S(XUCSX1<50:XUCSX1,1:49)
 W ?9,$J(XUCSX,6,1),$S(XUCSX1<28:" |",1:" +"),?17 F I1=1:1:XUCSX2 W "*"
 I $Y>(IOSL-4),$E(IOST)="C" D PAUSE^XUCSUTL
 I 'XUCSEND,$Y>(IOSL-4) D HDR^XUCS2RA,SHDR^XUCS2RA
 Q
