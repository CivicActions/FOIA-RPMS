EXIT ;
 K XUCMPARM,XUCMI,XUCMNODE,XUHFS,XUCMSTR,XUCMDISK,XUCM,^TMP($J)
 Q
FIL1 ;VAX HARDWARE DATA
 S U="^"
 S XUCMNODE="" F  S XUCMNODE=$O(XUCMNODE(XUCMNODE)) Q:XUCMNODE=""  D
 .S XUCMMEM=$P(XUCMNODE(XUCMNODE),U),XUCMHW=$P(XUCMNODE(XUCMNODE),U,2)
 .K DIC,DA,DR S (DIC,DLAYGO)=8986.3,DIC(0)="L",X=XUCMNODE D ^DIC S IEN=+Y
 .K DIC,DA,DR,DIE S DIE=8986.3,DA=IEN,DR="1///^S X=XUCMHW;2///^S X=XUCMMEM" D ^DIE K DIE,DA,DR
FIL2 ;DISK DRIVE HARDWARE DATA
 S XUCMDISK="" F  S XUCMDISK=$O(XUCMDISK(XUCMDISK)) Q:XUCMDISK=""  D
 .S XUCMTYP=$P(XUCMDISK(XUCMDISK),U),XUCMTBLK=$P(XUCMDISK(XUCMDISK),U,2),XUCMFRE=$P(XUCMDISK(XUCMDISK),U,3)
 .K IEN,DIC,DA,DR S (DIC,DLAYGO)=8986.35,DIC(0)="L",X=XUCMDISK D ^DIC S IEN=+Y
 .K DIC,DA,DR S DIE=8986.35,DA=IEN,DR="1///^S X=XUCMTYP;2///^S X=+XUCMTBLK;3///^S X=+XUCMFRE" D ^DIE K DIE,DA,DR
 .S X=+$P(^XUCM(8986.35,IEN,0),U,5) Q:'X
 .I (XUCMFRE/XUCMTBLK)*100'>X D
 ..S XQAMSG=" <DISK WARNING> "_$$HTE^XLFDT($H)_"/"_XUCMDISK_" / VMS Blocks FREE: "_XUCMFRE_"/"
 ..S XQA("G."_$P(XUCMPARM,U,5))=""
 ..D SETUP^XQALERT
 Q 
FIL3 ;FILE VOLUME SET DATA
 N A,B,C
 D FIL4
 S C=0 F  S C=$O(XUCM(C)) Q:C<1  S XUCMD=$G(XUCM(C)) D
 .D FIL5
 .S XUCMVOL=$P(XUCMD,U,2),XUCMFRE=$P(XUCMD,U,3),XUCMTOT=$P(XUCMD,U,4),XUCMMET="VBF" D
 ..S A=$E($P(XUCMVOL,"]",2),1,3),B=$G(A(A))
 ..S $P(B,U)=XUCMTOT+$P(B,U),$P(B,U,2)=XUCMFRE+$P(B,U,2),A(A)=B
 .S DA(1)=IEN,DA=IENDT,DIE="^XUCM(8986.6,DA(1),1,",DR="1///^S X=XUCMMET;9///^S X=XUCMVOL;10///^S X=XUCMTOT;11///^S X=XUCMFRE" D ^DIE K DIE,DA,DR
 S A="" F  S A=$O(A(A)) Q:A=""  D
 .S X=$P(XUCMPARM,U,11),XUCMTOT=$P(A(A),U),XUCMFRE=$P(A(A),U,2) I (XUCMFRE/XUCMTOT)*100'>X D
 ..S XQAMSG="<VOL SET WARNING> "_$$HTE^XLFDT($H)_" /"_A_"/ DSM Blocks FREE: "_XUCMFRE_"/"
 ..S XQA("G."_$P(XUCMPARM,U,5))=""
 ..D SETUP^XQALERT
 Q
FIL4 ;LOOKUP SITE ENTRY
 I $G(XUCMPARM)="" S XUCMPARM=$$PARM^XUCMVPU
 S XUCMSIT=$P(XUCMPARM,U,2),X="T-1",%DT="" D ^%DT S XUCMDT=Y
 S (DIC,DLAYGO)=8986.6,DIC(0)="L",X=XUCMSIT D ^DIC S IEN=+Y K DIC,DA
 I $G(^XUCM(8986.6,IEN,1,0))="" S ^(0)="^8986.61D^^"
 Q
FIL5 ;SETUP NEW DATE ENTRY
 I $G(XUCMPARM)="" S XUCMPARM=$$PARM^XUCMVPU
 S DLAYGO=8986,DA(1)=IEN,DIC="^XUCM(8986.6,DA(1),1,",DIC(0)="L",X=XUCMDT,DIADD=""
 D ^DIC S IENDT=+Y K DIC,DA,DR,DIADD
 Q
FIL6 ;DRIVE STATS FILE EDIT
 D FIL5 S DA(1)=IEN,DA=IENDT,DIE="^XUCM(8986.6,DA(1),1,"
 S DR="1///^S X=XUCMMET;4///^S X=XUCMDSK;5///^S X=XUCMTYP;6///^S X=XUCMMEAN;7///^S X=XUCMCNT"
 D ^DIE K DIE,DA,DR
 Q
FIL7 ;NODE SPECIFIC STATS EDIT
 D FIL5 S DA(1)=IEN,DA=IENDT,DIE="^XUCM(8986.6,DA(1),1,"
 S DR="1///^S X=XUCMMET;2///^S X=XUCMNODE;3///^S X=XUCMHW;6////^S X=XUCMAVE;7///^S X=XUCMC"
 D ^DIE K DIE,DA,DR
 Q
CHK(DATE,NODE,HW,MET,RES) ;Log event outside 3sd in ^TMP($J,"EVNT"
 N U,DIC,DA,METN,RULE,A,LO,HI
 S DIC=8986.4,DIC(0)="MNQZ",X=MET D ^DIC Q:Y<1  S METN=$P(Y(0),U) S RULE=$P(Y(0),U,4)
 K DIC,DA S DA(1)=+Y,DIC="^XUCM(8986.4,DA(1),1,",DIC(0)="MNQZ",X=HW D ^DIC
 S A=$G(Y(0)) Q:A=""  S SD3=3*($P(A,U,5)),HI=($P(A,U,4))+SD3,LO=($P(A,U,4))-SD3 S LO=$S(LO<0:0,1:LO)
 I RULE=""!(RULE=">") D
 .I $G(XUCMR(NODE,METN))="" S XUCMR(NODE,METN)="0.0"
 .I RES>HI S ^TMP($J,"EVNT",NODE,DATE,METN)=RES_U_LO_U_HI
 .I RES>XUCMR(NODE,METN) S $P(XUCMR(NODE,METN),U)=$J(RES,0,1)
 I RULE=""!(RULE="<") D
 .I RES<LO S ^TMP($J,"EVNT",NODE,DATE,METN)=RES_U_LO_U_HI
 .I $P($G(XUCMR(NODE,METN)),U,2)="" S $P(XUCMR(NODE,METN),U,2)="("_$J(RES,0,1)_")"
 .I RES<$P($G(XUCMR(NODE,METN)),"(",2) S $P(XUCMR(NODE,METN),U,2)="("_$J(RES,0,1)_")"
 Q
