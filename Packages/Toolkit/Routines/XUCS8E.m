E1 . S DA(2)=XUCSDA2,DA(1)=XUCSDA1
 . S DIC="^XUCS(8987.2,"_DA(2)_",1,"_DA(1)_",5,"
 . S DIC(0)="FLMXZ",DIC("P")=$P(^DD(8987.21,6,0),"^",2),X=$P(XUCSA(XUCSX),U) D ^DIC
 . I Y=-1 K DIC Q
 . S DIE=DIC K DIC S DA=+Y
 . S DR="1///^S X=$P(XUCSA(XUCSX),U,2);2///^S X=$P(XUCSA(XUCSX),U,3)"
 . D ^DIE K DIE,DR,DA,Y
 . Q
 K XUCSA,XUCSDA1,XUCSDA2,XUCSRDT,XUCSX
 D KILL^XUCSUTL
 Q
