 K XUCSA S XUCSA=""
 ; Get Raw Data & Build Local Table
 S XUCSSRN=0 F  S XUCSSRN=+$O(^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN)) Q:+XUCSSRN<1  D
EPL1 . S I2=^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN,"TIMES") F I1=1:1:I2 Q:I1=I2  D
EPL2 .. ; Get Beginning of Interval
 .. S XUCST1=^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN,"TIMES",I1)
 .. ; Get End of Interval
 .. S XUCST2=^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN,"TIMES",(I1+1))
 .. ; Get Number of Responses
 .. S XUCSR=^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN,"TTOT",I1)
 .. I '$D(XUCSA(I1)) S XUCSA(I1)=XUCST1_U_XUCST2_U_0
 .. S $P(XUCSA(I1),U,3)=$P(XUCSA(I1),U,3)+XUCSR
 .. K XUCSR,XUCST1,XICST2
 .. Q
EPL1C . ; Get Last Beginning Interval
 . S XUCST1=^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN,"TIMES",I1)
 . ; Get Last Number of Responses Recorded
 . S XUCSR=^%ZRTL("XUCS",XUCSVG,XUCSAP,+XUCSRN,+XUCSSRN,"TTOT",I1)
 . I '$D(XUCSA(I1)) S XUCSA(I1)=XUCST1_U_0_U_0
 . S $P(XUCSA(I1),U,3)=$P(XUCSA(I1),U,3)+XUCSR
 . K XUCSR,XUCST1
 .Q
 K XUCSR,XUCSSRN,XUCST1,XUCST2
EDIT ; Edit data into File 8987.2
 ; Update 8987.2.412 Multiple
 ; Loop thru XUCSA( array
 D EDIT^XUCSUTL ; Do common edit
