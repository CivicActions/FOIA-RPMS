 Q:'$$CHKF^XUCSUTL
 S XUCSEND=0 D A3^XUCSUTL3,A5^XUCSUTL3 G XIT:XUCSEND
 S %ZIS="MQ" D ^%ZIS I POP S POP=0 D HOME^%ZIS G XIT
 I $D(IO("Q")) D  G XIT
 . S ZTSAVE("XUCS*")="",ZTRTN="DEQUE^XUCS4R",ZTDESC="SYSTEM STATICAL REPORT",ZTIO=ION
 . S %DT="AEFRX",%DT("A")="Queue for what DATE/TIME? ",%DT("B")="NOW",%DT(0)="NOW" D ^%DT K %DT
 . I +Y'<0 S ZTDTH=Y D ^%ZTLOAD,HOME^%ZIS
 . K IO("Q")
 . Q
 U IO D:$E(IOST)="C" WAIT^DICD
DEQUE ;
 K ^TMP($J,4) D SORT,PRINT
XIT ; Common Xit Point
 I $E(IOST)'="C",'$D(ZTQUEUED) D ^%ZISC
 K ^TMP($J,4)
 K XUCSAP,XUCSBD,XUCSBDX,XUCSDT,XUCSED,XCSEND,XUCSET,XUCSHSW,XUCSITE,XUCSPG,XUCSRDT,XUCSRT,XUCSVG,XUCSX2,XUCSX3,XUCSITEH,XUCSDBUG
 Q
SORT ; Sort Report Data
 S XUCSBDX=(XUCSBD-1)_".999999" F  S XUCSBDX=$O(^XUCS(8987.2,"C",XUCSBDX)) Q:XUCSBDX=""!($P(XUCSBDX,".")>XUCSED)  D
SL1 . I $E($P(XUCSBDX,".",2),1,2)<12,XUCSRT="P" Q  ; PM Records Only
 . I $E($P(XUCSBDX,".",2),1,2)>11,XUCSRT="A" Q  ; AM Records Only
 . S XUCSX1=0 F  S XUCSX1=$O(^XUCS(8987.2,"C",XUCSBDX,XUCSX1)) Q:+XUCSX1<1  D
SL2 .. S XUCSX2=0 F  S XUCSX2=$O(^XUCS(8987.2,"C",XUCSBDX,XUCSX1,XUCSX2)) Q:+XUCSX2<1  D
SL3 ... S XUCSVG=^XUCS(8987.2,XUCSX1,0) I $D(XUCSFS),'$D(XUCSFS(XUCSVG)) Q
 ... S XUCSET=^XUCS(8987.2,XUCSX1,1,XUCSX2,0),XUCSRDT=$P(XUCSET,"^"),XUCSAJ=$P(XUCSET,"^",5),XUCSET=$P(XUCSET,"^",3)
 ... S XUCSX3=0,XUCSX3=$O(^XUCS(8987.2,XUCSX1,1,XUCSX2,3,XUCSX3)) Q:+XUCSX3<1  S XUCSX=^(+XUCSX3,0),^TMP($J,4,XUCSVG,XUCSBDX\1,$S($E($P(XUCSBDX,".",2),1,2)<12:"AM",1:"PM"))=XUCSET_"^"_$P(XUCSX,"^",2,99)_"~"_XUCSRDT_"^"_XUCSET_"^"_XUCSAJ
 ... Q
 .. Q
 . Q
 I $D(XUCSDBUG),'$D(ZTQUEUED) W !,$J D ^%GL K XUCSDBUG
 Q
PRINT ; Print the Report
 S XUCSVG="" F  S XUCSVG=$O(^TMP($J,4,XUCSVG)) Q:XUCSVG=""!(XUCSEND)  D SITEH^XUCSUTL3 D
PL1 . S XUCSDT=0 F  S XUCSDT=$O(^TMP($J,4,XUCSVG,XUCSDT)) Q:+XUCSDT<1!(XUCSEND)  D
PL2 .. S XUCSAP="" F  S XUCSAP=$O(^TMP($J,4,XUCSVG,XUCSDT,XUCSAP)) Q:XUCSAP=""!(XUCSEND)  S XUCSX=^(XUCSAP),XUCSX1=$P(XUCSX,"^") D HDR DO
 ... N X
WL1 ... S X=$P(XUCSX,"^",16),X=X/XUCSX1 W !,"MUMPS Commands",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",5),X=X/XUCSX1 W !,?6,"Global Set Commands",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",6),X=X/XUCSX1 W !,?6,"Global Kill Commands",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",7),X=X/XUCSX1 W !,?6,"Global Fetches",?32,$J(X,7,1)
WL5 ... S X=$P(XUCSX,"^",8),X=X/XUCSX1 W !,"TOTAL Global Accesses",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",9),X=X/XUCSX1 W !,"TOTAL Logical Read Operations",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",10),X=X/XUCSX1 W !,"TOTAL Logical Write Operations",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",2),X=X/XUCSX1 W !,?6,"Disk Reads",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",3),X=X/XUCSX1 W !,?6,"Disk Writes",?32,$J(X,7,1)
WL10 ... S X=$P(XUCSX,"^",4),X=X/XUCSX1 W !,"TOTAL Disk I/O",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",11),X=X/XUCSX1 W !,"TOTAL DDP Global Requests",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",12),X=X/XUCSX1 W !,"TOTAL RVG Requests",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",17),X=X/XUCSX1 W !,?6,"RVG Hits",?32,$J(X,7,1)
 ... I $P(XUCSX,"^",12)>0 S X=$P(XUCSX,"^",17),X=X/$P(XUCSX,"^",12),X=X*100 W !,?6,"RVG Cache eff.",?35,$J(X,3,0),"%"
 ... S X=$P(XUCSX,"^",13),X=X/XUCSX1 W !!,?6,"INPUT From Terminals",?32,$J(X,7,1)
 ... S X=$P(XUCSX,"^",14),X=X/XUCSX1 W !,?6,"OUTPUT To Terminals",?32,$J(X,7,1)
WL15 ... S X=$P(XUCSX,"^",15),X=X/XUCSX1 W !,"TOTAL Terminal I/O",?32,$J(X,7,1)
 ... K XUCSX,XUCSX1 D PAUSE^XUCSUTL
 ... Q
 .. Q
 . Q
 Q
HDR ; Header Sub-routine
 I $D(XUCSHSW),$E(IOST)="P",$Y>2,$Y<(IOSL-18) DO  Q
 . N X
 . W !
 . S X="",$P(X,"-",IOM)="" W !,X
 . D SITEH^XUCSUTL3,SHDR
 . Q
 W:$D(XUCSHSW) @IOF
 I '$D(XUCSHSW) S XUCSHSW=1,XUCSPG=0 D NOW^%DTC S Y=% D DD^%DT S XUCSRDT=$P(Y,"@")_"@"_$P($P(Y,":",1,2),"@",2) W:$E(IOST)="C" @IOF
 S XUCSPG=XUCSPG+1 W !,"System Statistics  ",XUCSRDT,?(IOM-10),"Page: ",XUCSPG
 N X
 S X="",$P(X,"-",IOM)="" W !,X
SHDR ; Sub-Header Sub-routine
 S XUCSXSV=XUCSX,XUCSX=$P(XUCSX,"~",2)
 S Y=$P(XUCSX,"^") D DD^%DT W !!,$P(Y,"@")," (",$P($P(Y,":",1,2),"@",2),"-" S X=$P(XUCSX,"^") D H^%DTC
 S %T=%T+$P(XUCSX,"^",2),%H=%H_","_%T D YMD^%DTC S Y=DT+% D DD^%DT W $P($P(Y,":",1,2),"@",2),") ",XUCSITEH,?(IOM-15),"Ave.# Jobs: ",$J($P(XUCSX,"^",3),2,0)
 W !,?32,"Events/Sec" S XUCSX=XUCSXSV K XUCSXSV
 Q
