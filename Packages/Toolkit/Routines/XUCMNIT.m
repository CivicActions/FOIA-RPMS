XUCMNIT ;SFISC/JC - Compile previous date's stats and mail ;8/1/97  11:29 [ 04/02/2003   8:47 AM ]
 ;;7.3;TOOLKIT;**1001**;APR 1, 2003
 ;;7.3;TOOLKIT;**17,25**;Aug 01, 1997
EXIT ;
 K ^TMP($J)
 I $D(ZTQUEUED) S ZTREQ="@"
 K X,XUCMDT,XUCMEDT,XUCMI,XUCMND,XUCMNODE,XUCMPARM,XUCMSDT,XUCMSIT,XUCMSTR,XUCMVOL,XUHFS,Y,ZTCPU,ZTDESC,ZTDTH,ZTIO,ZTRTN,ZTUCI
 K IEN,IENDT,XUCMCNT,XUCMD,XUCMFRE,XUCMHW,XUCMIO,XUCMMEAN,XUCMMEM,XUCMQ,XUCMSIT,XUCMTBLK,XUCMTOT,XUCMTYP
 K XUCM,M,XUCMAVE,XUCMC,XUCMDT,XUCMEDT,XUCMMET,XUCMND,XUCMNODE,XUCMT
 K C,I,J,LINE,PAD,XMSUB,XMY,XUCMBLK,XUCMDSK,XUCMDTX,XUCMIEN,XUCMPARM,XUCMVOL,XUHFN,Z
 K DA,DIK,XUCMFLG,XUCMTXT,XUCMYDT,DSK,DSK1,LINE,NUSE,VOL,VOL1,XUCMR
 Q
EN ;Pull in miscellaneous VAX data
 ;
 S U="^",XUCMPARM=$$PARM^XUCMVPU Q:'$P(XUCMPARM,U,10)
 I $G(^%ZOSF("OS"))'["DSM" Q
 D DB^%ZOSV2
 ;Clean up DSM symbols
 K %A,%BLCHECK,%BLFREE,%BLTYPE,%CLACC,%CLUST,%CMPLT,%CURRFLA,%DDPBASE
 K %DDPINH,%GARCUR,%GARNEXT,%GBLDEF,%GCPID,%GFIN,%GLCHARC,%GLCHARP
 K %GLOVECS,%GLPROC,%GLCROLC,%GLPROLP,%GLSBASE,%GLSTATS,%GPID
 K %JOBASE,%JOBSIZ,%JOBSLOT,%JOBTAB,%JRNFIL,%LOGINDI,%MAXPROC,%MAXVOLS
 K %RDACC,%RECPID,%ROUREF,%RTHIST,%RTHOVF,%SMSTART,%STATVAC,%TPPID
 K %TRNPTR,%UCIPTR,%UCISIZ,%VOL,%VOLERR,%VOLINH,%VOLTAB,%VTABSIZ
 K %WDPID,%WTACC
 D FIL3^XUCMNIT1,UCI^%ZOSV S XUCMVOL=$P(Y,",",2)
 N E
 ;STOP RTHIST AND MOVE YESTERDAY'S DATA TO %ZRTL
 D RTHSTOP^%ZOSV2
SUBMIT ;START NEW RTHIST SESSIONS
 S XUCMPARM=$$PARM^XUCMVPU
 S E=0 F  S E=$O(^XUCM(8986.3,E)) Q:E<1  S X1=$G(^XUCM(8986.3,E,0)),XUCMND=$P(X1,U) D
 .S XUCMMGR=$P(X1,U,6),XUCMQ=$P(X1,U,4),XUCMUSER=$P(X1,U,5)
 .Q:$G(XUCMMGR)=""
 .S XUCMDIR=$P(XUCMPARM,U,3) 
 .S FILE="XUCMRTH"_XUCMND_".COM"
 .S %SPAWN="SUBMIT/LOG/NOPRINT/QUE="_XUCMQ_" "_XUCMDIR_FILE
 .D ^%SPAWN
LCK ;give RTHSTOP a chance to run
 N C,Z,ZI,J,K,L,M,N,O,P,Q,R,T
 S U="^",XUCMPARM=$$PARM^XUCMVPU,(C,XUCMI)=0
 S XUCMN=0 F  S XUCMN=$O(^XUCM(8986.3,XUCMN)) Q:'XUCMN  S XUCMND=$P(^XUCM(8986.3,XUCMN,0),U) S C=0 D LCK1,LCK2
 G MISC
LCK1 ;Loop till processing has finished
 ;I $D(^%ZRTL("RTH",XUCMND)),$G(^%ZRTL("RTH",XUCMND)) Q
 ;S C=$G(C)+1 Q:C>60  H 60 G LCK1
 Q
LCK2 I $D(^%ZRTL("RTH",XUCMND)) S XUCMSIT=$P(XUCMPARM,U,2) D
 .S C=0 F  S C=$O(^%ZRTL("RTH",XUCMND,C)) Q:C<1  S Z=$G(^(C)) D
 ..S XUCMNODE=$P(Z,U),XUCMDT=$P(Z,U,3),T=$E($P(XUCMDT,"@",2),1,4),XUCMDT=$P(XUCMDT,"@")_"@"_T
 ..S ZI=$P(Z,U,5),J=$P(Z,U,6),K=$P(Z,U,7),L=$P(Z,U,8),M=$P(Z,U,9),N=$P(Z,U,10),O=$P(Z,U,11),P=$P(Z,U,12),Q=$P(Z,U,13),R=$P(Z,U,14)
 ..D RTH^XUCMFIL
LCK3 ;Collect response time data
 N RUN,SITIEN,NDIEN,STIME,RUNIEN,ET,CNT,ZCT,RUN,BIN
 S X=$P(XUCMPARM,U,2),DIC=8986.098,DIC(0)="LMZ" D ^DIC S SITIEN=+Y K DIC,DA
 S DA(1)=SITIEN,X=XUCMND,DIC="^XUCM(8986.098,DA(1),1,",DIC(0)="LMZ",DIC("P")=$P(^DD(8986.098,1,0),"^",2) D ^DIC S NDIEN=+Y
 I $G(SITIEN)'>0!($G(NDIEN)'>0) Q
 I $D(^%ZRTL("RTH",XUCMND,"RT")) S (GET,GCT,RUN)=0 F  S RUN=$O(^%ZRTL("RTH",XUCMND,"RT",RUN)) Q:RUN<1  D
 .S C=0,STIME=$G(^%ZRTL("RTH",XUCMND,"RT",RUN,C))
 .Q:STIME=""  S STIME=$$HTE^XLFDT(STIME,2)
 .K DIC,DA S DA(2)=SITIEN,DA(1)=NDIEN,DIC="^XUCM(8986.098,DA(2),1,DA(1),1,",DIC(0)="LMZ",DIC("P")=$P(^DD(8986.981,1,0),"^",2),X=STIME D ^DIC S RUNIEN=+Y
 .S (ET,CNT,ZCT)=0 F I=1:1:12 S BIN(I)=0
 .F I=1:1:34 S D=^%ZRTL("RTH",XUCMND,"RT",RUN,I) D
 ..S ET=ET+$P(D,";",2),E=$P(D,";",3),ZCT=ZCT+E
 ..F J=1:1:12 S Z=$P(E,",",J),CNT=CNT+Z,BIN(J)=BIN(J)+Z
 .I $$OS^%ZOSV2<6.4 S ET=ET+(.3*ZCT)+(.5*(CNT-ZCT))
 .E  S ET=ET/10
 .S RUNRT=$S(CNT:ET/CNT,1:0),BIN(7)=BIN(7)+BIN(8)+BIN(9)+BIN(10)+BIN(11)+BIN(12)
 .I RUNIEN>0 S DIE=DIC K DIC S DA=RUNIEN D
 ..S DR="1///^S X=+$J(ET,0,2);2///^S X=CNT;3///^S X=+$J(RUNRT,0,2);4///^S X=BIN(1);5///^S X=BIN(2);6///^S X=BIN(3);7///^S X=BIN(4);8///^S X=BIN(5);9///^S X=BIN(6);10///^S X=BIN(7)" D ^DIE
 ..K DIE,DR,DA,Y
 K ^%ZRTL("RTH",XUCMND)
 Q
MISC ;
 S XUCMI=0,XUCMDIR=$P(XUCMPARM,U,3) F  S XUCMI=$O(^XUCM(8986.3,XUCMI)) Q:XUCMI<1  S XUCMNODE=$P(^XUCM(8986.3,XUCMI,0),U) D
 .S Y=$$FTG^%ZISH(XUCMDIR,"MISC_"_XUCMNODE_".TMP",$NA(^TMP($J,XUCMNODE,0)),3)
 .;S %=$P(XUCMPARM,U,3)_"MISC_"_XUCMNODE_".TMP",XUHFN=XUCMNODE D ^XTCMFILN
 S XUCMNODE="",C=0
 F  S XUCMNODE=$O(^TMP($J,XUCMNODE)) Q:XUCMNODE=""  D
 .S C=0 F  S C=$O(^TMP($J,XUCMNODE,C)) Q:C<1  S XUCMSTR=$G(^(C)) D
 ..Q:XUCMSTR=""
 ..I XUCMSTR["Main Memory" S $P(XUCMNODE(XUCMNODE),U)=+$P(XUCMSTR,"(",2)
 ..I XUCMSTR["HARDWARE TYPE" S $P(XUCMNODE(XUCMNODE),U,2)=$P(XUCMSTR,"=",2)
 ..I XUCMSTR["device type" S XUCMDISK=$TR($P(XUCMSTR," ",2),","),XUCMDISK(XUCMDISK)="",XUCMDISK(XUCMDISK)=$TR($P(XUCMSTR,",",2)," device type ")
 ..I XUCMSTR["Total blocks" S $P(XUCMDISK(XUCMDISK),U,2)=$TR($E(XUCMSTR,20,36)," ")
 ..I XUCMSTR["Free blocks" S $P(XUCMDISK(XUCMDISK),U,3)=$TR($E(XUCMSTR,20,36)," ")
 D FIL1^XUCMNIT1
 D DSK
 D SYS^XUCMNIT2,EN2^XUCMPA,^XUCMNIT3,^XUCMNIT4
 D EXIT
 Q
DSK ;COMPUTE DKIO, DKQ
 ;FOR EACH DRIVE&SAMPLE TIME, ONLY THE SUM OF IO FROM ALL NODES
 ;AT THAT TIME IS SIGNIFICANT
 N H,I,J,K,L K XUCMDISK,XUCMDSK I $G(XUCMPARM)="" S XUCMPARM=$$PARM^XUCMVPU
 I $G(XUCMDT)="" S X="T-1",%DT="" D ^%DT S XUCMDT=Y
 S X="T-1@0800",%DT="XT" D ^%DT S XUCMSDT=Y
 S X="T-1@1630",%DT="XT" D ^%DT S XUCMEDT=Y
 N CNT
 S H=XUCMSDT F  S H=$O(^XUCM(8986.5,"C",H)) Q:H<1!(H>XUCMEDT)  D
 .;FIRST, LETS SUM, AVERAGE BY NODENAME
 .S C=0 F  S C=$O(^XUCM(8986.5,"C",H,C)) Q:C<1  S I=$G(^XUCM(8986.5,C,0)) Q:I=""  D
 ..S XUCMDSK=$P(I,U,4),XUCMND=$P(I,U,3),XUCMIO=$P(I,U,6),XUCMQ=$P(I,U,7)
 ..S J=$G(XUCMJ(XUCMDSK,XUCMND)),$P(J,U)=XUCMIO+$P(J,U),$P(J,U,2)=XUCMQ+$P(J,U,2),$P(J,U,3)=1+$P(J,U,3)
 ..S:$P(J,U,3) $P(J,U,4)=$P(J,U)/$P(J,U,3),$P(J,U,5)=$P(J,U,2)/$P(J,U,3)
 ..S XUCMJ(XUCMDSK,XUCMND)=J
DSK1 ;NOW SUM AVERAGES OF EACH NODE
 N D S XUCMDSK="" F  S XUCMDSK=$O(XUCMJ(XUCMDSK)) Q:XUCMDSK=""  D
 .S (XUCMIO,XUCMQ,XUCMCNT)=0,D="" F  S D=$O(XUCMJ(XUCMDSK,D)) Q:D=""  S J=$G(XUCMJ(XUCMDSK,D)),XUCMIO=XUCMIO+$P(J,U,4),XUCMQ=XUCMQ+$P(J,U,5),XUCMCNT=XUCMCNT+$P(J,U,3)
 .S XUCMTYP=$O(^XUCM(8986.35,"B",XUCMDSK,0)) I XUCMTYP S XUCMTYP=$P(^XUCM(8986.35,XUCMTYP,0),U,2),XUCMK(XUCMDSK)=XUCMTYP_U_XUCMIO_U_XUCMQ_U_XUCMCNT
 K XUCMJ
 D FIL4^XUCMNIT1
 S XUCMDSK="" F  S XUCMDSK=$O(XUCMK(XUCMDSK)) Q:XUCMDSK=""  S J=$G(XUCMK(XUCMDSK)) Q:J=""  D
 .S XUCMTYP=$P(J,U),XUCMCNT=$J($P(J,U,4),0),XUCMIO=+$J($P(J,U,2),0,2),XUCMQ=+$J($P(J,U,3),0,2)
 .S XUCMMEAN=XUCMIO,XUCMMET="DKIO" D FIL6^XUCMNIT1
 .S XUCMMEAN=XUCMQ,XUCMMET="DKQ" D FIL6^XUCMNIT1
 K XUCMTYP,XUCMMEAN,XUCMIO,XUCMMET,XUCMDSK,XUCMQ,XUCMV,XUCMCNT,XUCMJ,XUCMK
 Q
