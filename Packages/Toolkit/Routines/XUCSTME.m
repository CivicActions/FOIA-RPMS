 ;;7.3;TOOLKIT;**14,15**;Apr 25, 1995
 S XUCSEND=0 D  I XUCSEND K XUCSEND Q
 . N X
 . I +$$OSTYPE^%ZOSV>2 S XUCSEND=1 Q  ; Not PC or NT OS
 . I +$E($$VERSION^%ZOSV)'=4 S XUCSEND=1 Q  ; Not Version 4+ MSM
 . S X1=DT,X2=-1 D C^%DTC,H^%DTC ; Get Previous Day DOW
 . I %Y=0 S XUCSEND=1 Q  ; Skip Edits for Sunday (Funday)
 . I %Y=6 S XUCSEND=1 Q  ; Skip Edits for Saturday
 . D YMD^%DTC S DIC=40.5,DIC(0)="MXZ" D ^DIC K DIC I +Y>1 S XUCSEND=1 Q  ; It's a HOLIDAY
 . Q
PART1 ; Loop Thru ^%ZRTL("XUCS",,,"TRANSFER-STATUS" Nodes & Task Transfer
 S (XUCSVG,XUCSET,XUCSAP)="",XUCSHANG=1 F  S XUCSVG=$O(^%ZRTL("XUCS",XUCSVG)) Q:XUCSVG=""!(XUCSVG="ZZZ")  D
P1SL1 . ;
 . F I=1:1 S XUCSAP=$O(^%ZRTL("XUCS",XUCSVG,XUCSAP)) Q:XUCSAP=""  S XUCSX=^(XUCSAP,"TRANSFER-STATUS") D
P1SL2 .. ;
 .. I $P(XUCSX,"^")'="P1" K XUCSX Q
 .. S XUCSET(+I)=XUCSVG_"^"_XUCSAP,XUCSRN=$P(XUCSX,"^",3),XUCSSDT=$P(XUCSX,"^",2)
 .. S DIC="^XUCS(8987.1,1,1,",DIC(0)="FMXZ",X=XUCSVG D ^DIC K DIC Q:Y<0  S ZTUCI=$S($P(Y(0),"^",6)]"":$P(Y(0),"^",6),1:"MGR"),ZTCPU=XUCSVG
QUEIT .. ; Queue Out Transfer of RTHIST Data
 .. ; Check for Vol. Group OUT of SERVICE
 .. S XUCSVGX=0,XUCSVGX=$O(^%ZIS(14.7,"B",XUCSVG,+XUCSVGX))
 .. I +XUCSVGX<1 D ALERT^XUCSUTL S XQAMSG="XUCS- Vol. Group "_XUCSVG_", Does NOT Exist!" D SETUP^XQALERT K XQA,XQAMSG,XUCSVGX Q
 .. I +$P(^%ZIS(14.7,+XUCSVGX,0),"^",11)>0 D NOW^%DTC S Y=% D DD^%DT D ALERT^XUCSUTL S XQAMSG="XUCS- Vol. Group "_XUCSVG_", is OUT OF SERVICE, "_Y D SETUP^XQALERT K XQA,XQAMSG,XUCSVGX Q
 .. K XUCSVGX
 .. S ZTDESC="Transfer Node - "_XUCSVG_"/"_XUCSAP_" Raw RTHIST Data"
 .. S ZTRTN="PART4^%ZOSV2",ZTIO="",(ZTSAVE("XUCSRN"),ZTSAVE("XUCSVG"),ZTSAVE("XUCSSDT"),ZTSAVE("XUCSAP"))="",ZTDTH=$H D ^%ZTLOAD
 .. K XUCSX,XUCSRN,XUCSSDT,ZTUCI,ZTCPU,ZTDESC,ZTSAVE,ZTDTH
 .. H 60
 .. Q
 . Q
PART2 ; Check All "TRANSFER-STATUS" Nodes For Transfer Complete - $P#1="P2"
 S XUCSI=0,XUCSTOP=$P($H,",",2)+3600 F  S XUCSI=$O(XUCSET(+XUCSI)) Q:+XUCSI=0  D  H 15 I $P($H,",",2)>XUCSTOP S XUCSEND=1 G XIT ; waited for 1 Hr., something is wrong
 . S XUCSVG=$P(XUCSET(+XUCSI),"^"),XUCSAP=$P(XUCSET(+XUCSI),"^",2) I $P(^%ZRTL("XUCS",XUCSVG,XUCSAP,"TRANSFER-STATUS"),"^")="P2"!($P(^("TRANSFER-STATUS"),"^")="P3")!($P(^("TRANSFER-STATUS"),"^")="P4") K XUCSET(+XUCSI)
 . Q
 I $D(XUCSET)>1 G PART2 ; All Table Entries Haven't Been Deleted
PART3 ; Now Transfer Raw Data To File 8987.2
 S (XUCSVG,XUCSAP)="" F  S XUCSVG=$O(^%ZRTL("XUCS",XUCSVG)) Q:XUCSVG=""!(XUCSVG="ZZZ")  DO
P3SL1 . ;
 . F  S XUCSAP=$O(^%ZRTL("XUCS",XUCSVG,XUCSAP)) Q:XUCSAP=""  S XUCSX=^(XUCSAP,"TRANSFER-STATUS") DO
P3SL2 .. I $P(XUCSX,U)="P3" K ^%ZRTL("XUCS",XUCSVG,XUCSAP,"TRANSFER-STATUS"),^%ZRTL("XUCS","ZZZ","SYS-CONFIG",XUCSVG) Q
 .. I $P(XUCSX,U)="P4" D ALERT^XUCSUTL S XQAMSG="XUCS-"_$P(XUCSX,U,2) D SETUP^XQALERT K ^%ZRTL("XUCS",XUCSVG,XUCSAP,"TRANSFER-STATUS"),^%ZRTL("XUCS","ZZZ","SYS-CONFIG",XUCSVG) Q
 .. I $P(XUCSX,U)="P2" S XUCSSDT=$P(XUCSX,"^",2),XUCSRN=$P(XUCSX,"^",3) K XUCSX DO
P3SL3 ... ;
 ... ; Get RTHIST's ID Node & Correct $H of Node to PSA's $H
 ... S XUCSRID=^%ZRTL("XUCS",XUCSVG,XUCSAP,XUCSRN),X=XUCSSDT D H^%DTC S $P(XUCSRID,"^")=%H,$P(XUCSRID,"^",2)=%T
 ... S XUCSSIZ=^%ZRTL("XUCS",XUCSVG,XUCSAP,XUCSRN,1,"SIZE"),XUCSSIZ=$TR(XUCSSIZ,"^","~")
 ... K DIC S DIC="^XUCS(8987.1,1,1,",DIC(0)="FMXZ",X=XUCSVG D ^DIC K DIC Q:+Y<0
 ... S XUCSPAR=Y(0) ; Save File 8987.1, Field #3, zeroth node
 ... S XUCSVG=$P(XUCSPAR,U) ; Name of Vol. Group
 ... S XUCSPUCI=$P(Y(0),U,3) ; Name of Producton UCI
 ... S DA(1)=1,DIE="^XUCS(8987.1,"_DA(1)_",1,",DA=+Y,DR=$S(XUCSAP="AM":"6///^S X=XUCSSIZ",1:"7///^S X=XUCSSIZ") D ^DIE K DIE,DR,DA
 ... S XUCSALL=1
 ... D ^XUCS1E,^XUCS2E,^XUCS4E,^XUCS5E,^XUCS8E,^XUCSCDE
 ... K ^%ZRTL("XUCS",XUCSVG,XUCSAP,XUCSRN),^%ZRTL("XUCS",XUCSVG,XUCSAP,"TRANSFER-STATUS")
 ... S XUCSVGX=XUCSVG K XUCSALL,XUCSY0 D KILL^XUCSUTL S XUCSVG=XUCSVGX K XUCSVGX
 ... Q
 .. Q
 . Q
 I $D(^%ZRTL("XUCS","ZZZ","SYS-CONFIG")) D ^XUCS6E ; Update SYSTEM CONFIG PARMS
XIT ; Common eXIT point
 K XUCSAP,XUCSET,XUCSHANG,XUCSI,XUCSSDT,XUCSSIZ,XUCSTOP,XUCSVG
 I 'XUCSEND D
 . D DR^XUCSXDR ; create MPM Morning Report
 . D ^XUCSRV ; create server message to ISC-SF
 . Q
 K XUCSEND
 D DEQUE^XUCSPRG ; purge old entries here
 Q
