TMP ;GET ZSLOT INFO, THEN PARSE/PRE-SORT VMS MONITOR DATA IN ^TMP
 ;
 S U="^" N AST,DLK
 S XUCMND="" F  S XUCMND=$O(^TMP($J,XUCMND)) Q:XUCMND=""  D
 .S DLK=0,XUCMSLOT=+$G(^ZZSLOT(XUCMND,"ACTIVE"))
 .K XUCMMET,XUCMDT,XUCMDAT,XUCMDRV,XUCMAVE
 .S C=0 F  S C=$O(^TMP($J,XUCMND,C)) Q:C<1  D
 ..S XUCMSTR=$G(^TMP($J,XUCMND,C))
 ..I XUCMSTR["I/O Operation" S XUCMMET="DKIO"
 ..I XUCMSTR["I/O Request" S XUCMMET="DKQ"
 ..I XUCMSTR["on node" S XUCMDT=$$VMSDT^XUCMVPU(XUCMSTR)
 ..I XUCMSTR["$D"!(XUCMSTR?.E1"D".E1":".E) S XUCMDRV=$P(XUCMSTR,":",1)_":" S XUCMAVE=$TR($E(XUCMSTR,47,57)," ")
 ..I $D(XUCMMET),$D(XUCMND),$D(XUCMDT),$D(XUCMDRV),$D(XUCMAVE) D
 ...S:$G(^TMP($J,"XUCMDAT",XUCMDT,XUCMND,XUCMDRV))="" ^(XUCMDRV)="" S:XUCMMET="DKIO" $P(^(XUCMDRV),U,1)=XUCMAVE S:XUCMMET="DKQ" $P(^(XUCMDRV),U,2)=XUCMAVE
 ..I XUCMSTR["Current" S XUCMCUR=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"CUR",XUCMCUR)
 ..I XUCMSTR["Compute",XUCMSTR'["(Out" S XUCMCOM=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"COM",XUCMCOM)
 ..I XUCMSTR["Compute (Out" S XUCMCOMO=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"COMO",XUCMCOMO)
 ..I XUCMSTR["ENQs Forced" S XUCMENQF=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ENQF",XUCMENQF)
 ..;
 ..; process LOCK and AST data out of same area
 ..I XUCMSTR["DISTRIBUTED LOCK MANAGEMENT" K XUCMASTO F  S C=C+1,XUCMSTR=$G(^TMP($J,XUCMND,C)) Q:XUCMSTR["SUMMARIZING"  D
 ...I XUCMSTR["New ENQ Rate",XUCMSTR["(Local" S XUCMENQL=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ENQL",XUCMENQL)
 ...I XUCMSTR["(Incom" S XUCMENQI=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ENQI",XUCMENQI)
 ...I XUCMSTR["(Outgo" S XUCMENQO=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ENQO",XUCMENQO)
 ...I XUCMSTR["Block",XUCMSTR["(Local" S AST=1,XUCMASTL=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ASTL",XUCMASTL)
 ...I $G(AST),XUCMSTR["(Incom" S XUCMASTI=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ASTI",XUCMASTI)
 ...I $G(AST),XUCMSTR["(Outgo" S XUCMASTO=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ASTO",XUCMASTO) K AST
 ...;
 ..I XUCMSTR["Interrupt" S XUCMISTK=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"ISTK",XUCMISTK)
 ..I XUCMSTR["Kernel" S XUCMKRM=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"KRM",XUCMKRM)
 ..I XUCMSTR["User Mode" S XUCMUSR=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"USR",XUCMUSR)
 ..I XUCMSTR["Idle Time" S XUCMIDLE=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"IDLE",XUCMIDLE)
 ..I XUCMSTR["Process Count" S XUCMPRC=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"PRC",XUCMPRC)
 ..I XUCMSTR["Page Fault" S XUCMPGFR=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"PGFR",XUCMPGFR)
 ..I XUCMSTR["Page Read" S XUCMPGIO=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"PGIO",XUCMPGIO)
 ..I XUCMSTR["Free List" S XUCMFREL=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"FREL",XUCMFREL)
 ..I XUCMSTR["Modified List" S XUCMMODL=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"MODL",XUCMMODL)
 ..I XUCMSTR["Direct I/O" S XUCMDIO=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"DIO",XUCMDIO)
 ..I XUCMSTR["Buffered I/O" S XUCMBIO=+$TR($E(XUCMSTR,43,53)," ") D CHK^XUCMVPM(XUCMND,"BIO",XUCMBIO)
 ..I XUCMSTR["number of processes" S XUCMIPRC=+$TR($P(XUCMSTR,"=",3)," ") D CHK^XUCMVPM(XUCMND,"IPRC",XUCMIPRC) D SYS^XUCMFIL
 Q
 ;
STF ;FILE DISK DATA
 I $D(^TMP($J,"XUCMDAT")) D
 .S XUCMDT=0 F  S XUCMDT=$O(^TMP($J,"XUCMDAT",XUCMDT)) Q:XUCMDT<1  D
 ..S XUCMND1="" F  S XUCMND1=$O(^TMP($J,"XUCMDAT",XUCMDT,XUCMND1)) Q:XUCMND1=""  D
 ...S XUCMDRV="" F  S XUCMDRV=$O(^TMP($J,"XUCMDAT",XUCMDT,XUCMND1,XUCMDRV)) Q:XUCMDRV=""  D
 ....S XUCMIO=+$P(^TMP($J,"XUCMDAT",XUCMDT,XUCMND1,XUCMDRV),U,1),XUCMQUE=+$P(^(XUCMDRV),U,2) D DSK^XUCMFIL
 ....D CHK^XUCMVPM(XUCMDRV,"DKIO",XUCMIO),CHK^XUCMVPM(XUCMDRV,"DKQ",XUCMQUE)
 K ^TMP($J,"XUCMDAT")
 Q
