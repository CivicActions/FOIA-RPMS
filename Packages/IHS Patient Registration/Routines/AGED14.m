 N DA,DR,DIE,DUOUT,DTOUT,Y
 S DA=DFN
 S DIE="^AUPNPAT("
 S DR="9002Pronouns"
 D ^DIE
 Q:$D(DUOUT)!$D(DTOUT)!$D(Y)
 ;
 S DA=DFN
 S DIE="^AUPNPAT(",DIE("NO^")=""
 S DR="9003"_$S($$CHKPRN^AUPNSOGI(DFN,"OTH"):"R~Other Pronouns",1:"////@")
 D ^DIE
 ;
 Q
 ;
LSEX ;EP - EDIT LEGAL SEX
 N DA,DR,DIC,DIE,DUOUT,DTOUT,X,Y,RES,CNT
 S RES=$$HISTLSEX^AUPNSOGI(DFN,,,.RES)
 W !!,"EFFECTIVE DT",?15,"LEGAL SEX",?30,"LEGAL SEX SOURCE",?68,"DT ENTERED",!,$$REPEAT^XLFSTR("-",80)
 I '+RES W !,"No Legal Sex records to display."
 S CNT=0 F  S CNT=$O(RES(CNT)) Q:'CNT  D
 . W !,$$FMTE^XLFDT($P(RES(CNT),U),5),?15,$P(RES(CNT),U,2),?30,$E($P(RES(CNT),U,3),1,37),?68,$P(RES(CNT),U,4)
 W !,$$REPEAT^XLFSTR("-",80)
 ;
 S DIC="^AUPNPAT("_DFN_",92,"
 S DA(1)=DFN
 S DIC(0)="AELMQZ"
 S DIC("A")="Select Effective Date of Legal Sex: "
 S DIC("B")=$P(RES,U,2)
 S DIC("DR")=".01;1;2;3////"_$$DT^XLFDT
 D ^DIC
 K DIC
 Q:$D(DUOUT)!$D(DTOUT)!(+Y<1)
 Q:+$$HISTLSEX^AUPNSOGI(DFN)>+RES
 ;
 S DA=+Y,DA(1)=DFN
 S DIE="^AUPNPAT("_DFN_",92,"
 S DR=".01;1;2"
 D ^DIE
 ;
