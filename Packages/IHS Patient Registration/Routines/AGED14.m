SO ;EP - EDIT SEXUAL ORIENTATION
 N DA,DR,DIE,DUOUT,DTOUT,X,Y,OLD,NEW
 ;
 S OLD=$$GETSO^AUPNSOGI(DFN,"I")
 ; ALWAYS EDIT ENTRY FOR TODAY, CREATE/COPY VALUES FROM PREVIOUS IF NEEDED
 S NEW=$S($P(OLD,U)'=$$DT^XLFDT:$$SETSO^AUPNSOGI(DFN,$P(OLD,U,2,$L(OLD,U)-1),$P(OLD,U,$L(OLD,U))),1:OLD)
 ;
 S DA=$P(NEW,U),DA(1)=DFN
 S DIE="^AUPNPAT("_DA(1)_",93,"
 S DR="1Sexual Orientation(s)"
 D ^DIE
 ;
 ; IF USER EXITED OR TIMED-OUT, CHECK IF CHANGES WERE MADE THEN QUIT
 I $D(Y)!$D(DTOUT) D  Q
 . ; IF ENTRY WAS CREATED AND VALUES ARE THE SAME, DELETE NEW ENTRY
 . I $P(OLD,U)'=$P(NEW,U),$P(OLD,U,2,99)=$P($$GETSO^AUPNSOGI(DFN,"I"),U,2,99) S NEW=$$SETSO^AUPNSOGI(DFN,"@")
 ;
 S DA=$P(NEW,U),DA(1)=DFN
 S DIE="^AUPNPAT("_DA(1)_",93,",DIE("NO^")=""
 S DR="2"_$S($$CHKSO^AUPNSOGI(DFN,"OTH"):"R~Other Sexual Orientation",1:"////@")
 I $$SO^AUTSOGI($G(X),"C")="OTH"!('$$CHKSO^AUPNSOGI(DFN,"OTH")) D ^DIE
 ;
 D CHKSG^AGUTL2(DFN,"S")
 ;
 Q
 ;
GI ;EP -  EDIT GENDER IDENTITY
 N DA,DR,DIE,DUOUT,DTOUT,X,Y,OLD,NEW
 ;
 S OLD=$$GETGI^AUPNSOGI(DFN,"I")
 ; ALWAYS EDIT ENTRY FOR TODAY, CREATE/COPY VALUES FROM PREVIOUS IF NEEDED
 S NEW=$S($P(OLD,U)'=$$DT^XLFDT:$$SETGI^AUPNSOGI(DFN,$P(OLD,U,2,$L(OLD,U)-1),$P(OLD,U,$L(OLD,U))),1:OLD)
 ;
 S DA=$P(NEW,U),DA(1)=DFN
 S DIE="^AUPNPAT("_DA(1)_",94,"
 S DR="1Gender Identity(s)"
 D ^DIE
 ;
 ; IF USER EXITED OR TIMED-OUT, CHECK IF CHANGES WERE MADE THEN QUIT
 I $D(Y)!$D(DTOUT) D  Q
 . ; IF ENTRY WAS CREATED AND VALUES ARE THE SAME, DELETE NEW ENTRY
 . I $P(OLD,U)'=$P(NEW,U),$P(OLD,U,2,99)=$P($$GETGI^AUPNSOGI(DFN,"I"),U,2,99) S NEW=$$SETGI^AUPNSOGI(DFN,"@")
 ;
 S DA=$P(NEW,U),DA(1)=DFN
 S DIE="^AUPNPAT("_DA(1)_",94,",DIE("NO^")=""
 S DR="2"_$S($$CHKGI^AUPNSOGI(DFN,"OTH"):"R~Other Gender Identity",1:"////@")
 I $$GI^AUTSOGI($G(X),"C")="OTH"!('$$CHKGI^AUPNSOGI(DFN,"OTH")) D ^DIE
