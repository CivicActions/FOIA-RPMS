DRAW ;EP
 S CLLST=0
 S CALLS="PRN^AGED14,LSEX^AGED14,GI^AGED14,SO^AGED14"
 S AG("PG")=11
 S AG("N")=4
 S ROUTID=$P($T(+1)," ")  ;SET ROUTINE ID FOR PROGRAMMER VIEW
 S DA=DFN
 D ^AGED                          ;Main editor routine
 K ^UTILITY("DIQ1",$J)
 F AG=1:1:AG("N") D
 . N AGSCRN,LBL,LEN,CLM,DIC,DR,VLEN,VALUE,CNT
 . S AGSCRN=$P($T(@1+AG),";;",2,17)
 . S LBL=$P(AGSCRN,U)
 . S LEN=$P(AGSCRN,U,2)
 . S CLM=$P(AGSCRN,U,3)
 . S DIC=$P(AGSCRN,U,4)
 . S DR=$P(AGSCRN,U,5)
 . S VLEN=$P(AGSCRN,U,6)
 . ;
 . ;Prompt position
 . I CLM="" W !
 . E  W ?CLM
 . ;
 . ;Display Number
 . S CLLST=CLLST+1,CLLST(CLLST)=$P(CALLS,",",AG),AG("E")=CLLST
 . W CLLST,". "
 . ;
 . ;Display label
 . W LBL,$$REPEAT^XLFSTR(".",LEN-$L(LBL)),": "
 . ;
 . I AG=1 D  Q
 . . S VALUE=$$GETPRN^AUPNSOGI(DFN,$S($$CHKPRN^AUPNSOGI(DFN,"ASKU")!$$CHKPRN^AUPNSOGI(DFN,"UNK"):"E",1:"D")) W VALUE
 . I AG=2 D  Q
 . . S VALUE=$$GETLSEX^AUPNSOGI(DFN) I $L(VALUE) W $P(VALUE,U,2)," (",$P(VALUE,U,3),")"
 . I AG=3 D  Q
 . . N CNT
 . . S VALUE=$$GETGI^AUPNSOGI(DFN,"E") F CNT=2:1:$L(VALUE,U) W $P(VALUE,U,CNT) I CNT<$L(VALUE,U) W !,?32
 . I AG=4 D  Q
 . . N CNT
 . . S VALUE=$$GETSO^AUPNSOGI(DFN,"E") F CNT=2:1:$L(VALUE,U) W $P(VALUE,U,CNT) I CNT<$L(VALUE,U) W !,?32
 . ;
 . S VALUE=$$GET1^DIQ(DIC,DFN,DR)
 . S:VLEN>0 VALUE=$E(VALUE,1,VLEN)
 . W VALUE
 ;
 W !,AGLINE("EQ")
 K MYERRS,MYVARS
 D FETCHERR^AGEDERR(AG("PG"),.MYERRS)
 S MYVARS("DFN")=DFN,MYVARS("FINDCALL")="",MYVARS("SELECTION")=$G(AGSELECT),MYVARS("SITE")=DUZ(2)
 D EDITCHEK^AGEDERR(.MYERRS,.MYVARS,1)
 W !,AGLINE("-")
 D VERIF^AGUTILS
 Q
