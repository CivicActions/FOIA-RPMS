CHKRE(AGPATDFN,AGFLAG)  ;EP - DECLINED/UNKNOWN RACE/ETHNICITY CHECK
 N AGLIST,AGDEC,AGUNK,AGSTR,AGSCR,AGRES,AGCNT,AGEXIT,FDA
 S AGPATDFN=$G(AGPATDFN),AGFLAG=$G(AGFLAG) Q:'AGPATDFN  Q:AGFLAG']""  Q:"RE"'[AGFLAG
 S AGLIST=$S(AGFLAG="E":$$AGETHN^AGUTL(AGPATDFN),AGFLAG="R":$$AGRACE^AGUTL(AGPATDFN),1:"")
 Q:+AGLIST'>1  ; ONLY APPLIES FOR 2 OR MORE ENTRIES
 S AGDEC=$S(AGLIST["0000-0":1,1:0),AGUNK=$S(AGLIST["9999-4":1,1:0)
 Q:AGDEC+AGUNK'>0  ; ONLY APPLIES IF AT LEAST ONE IS DECLINED/UNKNOWN
 S AGSTR=$S(AGDEC:"DECLINED TO ANSWER",AGUNK:"UNKNOWN BY PATIENT",1:"")
 S AGEXIT=0 F  D  Q:AGEXIT
 . K DIR,DTOUT,DUOUT,DIRUT,DIROUT,X,Y
 . W !,"A) Keep "_AGSTR,!,"B) Remove "_AGSTR
 . S DIR("A")="Select one of the following: "
 . S DIR("?")="additional "_$S(AGFLAG="E":"Ethnicity",AGFLAG="R":"Race",1:"")_" entries must be removed."
 . S DIR("?",1)="When either DECLINED TO ANSWER or UNKNOWN BY PATIENT is selected,"
 . S DIR(0)="SA^A:Keep "_$P(AGSTR," ")_";B:Remove "_$P(AGSTR," ")
 . D ^DIR
 . Q:$D(DTOUT)!$D(DUOUT)!$D(DIRUT)!$D(DIROUT)
 . S AGEXIT=1
 ; SET UP SCREENING LOGIC
 S AGSCR=$S(AGDEC:"""0000-0""",AGUNK:"""9999-4""",1:"")
 ; REMOVE BOTH DECLINED/UNKNOWN ON OPTION B IF THERE ARE OTHER ENTRIES
 S:$G(Y)="B" AGSCR=$S(+AGLIST-AGDEC-AGUNK>0:"""0000-0^9999-4""",1:AGSCR)
 ; OPTION A SCREENS DECLINED/UNKNOWN, OPTION B SCREENS OTHER ENTRIES
 S AGSCR="I "_AGSCR_$S($G(Y)="A":"'",1:"")_"[$$GET1^DIQ("_$S(AGFLAG="E":"10.2",AGFLAG="R":"10",1:"")_",Y,3)"
 ; RETRIEVE ENTRIES AND DELETE ENTRIES NOT SCREENED
 D LIST^DIC($S(AGFLAG="E":"2.06",AGFLAG="R":"2.02",1:""),","_DFN_",","@;.01I","P",,,,,AGSCR,,"AGRES")
 S AGCNT=0 F  S AGCNT=$O(AGRES("DILIST",AGCNT)) Q:'AGCNT  D
 . S FDA($S(AGFLAG="E":"2.06",AGFLAG="R":"2.02",1:""),$P(AGRES("DILIST",AGCNT,0),U,1)_","_DFN_",",.01)="@"
 D UPDATE^DIE(,"FDA",)
 Q
CHKSG(AGPATDFN,AGFLAG)  ;EP - DECLINED/UNKNOWN SEXUAL ORIENTATION/GENDER IDENTITY CHECK
 N AGLIST,AGDEC,AGUNK,AGSTR,AGSCR,AGRES,AGCNT,AGEXIT,AGINT,AGRES,AGTMP,AGANS
 S AGPATDFN=$G(AGPATDFN),AGFLAG=$G(AGFLAG) Q:'AGPATDFN  Q:AGFLAG']""  Q:"SG"'[AGFLAG
 S AGLIST=$S(AGFLAG="S":$$GETSO^AUPNSOGI(AGPATDFN,"C"),AGFLAG="G":$$GETGI^AUPNSOGI(AGPATDFN,"C"),1:"")
 Q:$L(AGLIST,U)'>2  ; ONLY APPLIES FOR 2 OR MORE ENTRIES
 S AGDEC=$S(AGLIST["ASKU":1,1:0),AGUNK=$S(AGLIST["UNK":1,1:0)
 Q:AGDEC+AGUNK'>0  ; ONLY APPLIES IF AT LEAST ONE IS DECLINED/UNKNOWN
 S AGSTR=$S(AGDEC:"DECLINE TO ANSWER",AGUNK:"DO NOT KNOW",1:"")
 S AGEXIT=0 F  D  Q:AGEXIT
 . N DIR,DTOUT,DUOUT,DIRUT,DIROUT,X,Y
 . W !,"A) Keep "_AGSTR,!,"B) Remove "_AGSTR
 . S DIR("A")="Select one of the following: "
 . S DIR("?")="additional "_$S(AGFLAG="S":"Sexual Orientation",AGFLAG="G":"Gender Identity",1:"")_" entries must be removed."
 . S DIR("?",1)="When either DECLINE TO ANSWER or DO NOT KNOW is selected,"
 . S DIR(0)="SA^A:Keep "_$P(AGSTR," ")_";B:Remove "_$P(AGSTR," ")
 . D ^DIR
 . Q:$D(DTOUT)!$D(DUOUT)!$D(DIRUT)!$D(DIROUT)
 . S AGEXIT=1,AGANS=$G(Y)
 ;
 ; SET UP SCREENING LOGIC
 S AGSCR=$S(AGDEC:"ASKU",AGUNK:"UNK",1:"")
 ; REMOVE BOTH DECLINED/UNKNOWN ON OPTION B IF THERE ARE OTHER ENTRIES
 S:AGANS="B" AGSCR=$S($L(AGLIST,U)-AGDEC-AGUNK-1>0:"ASKU^UNK",1:AGSCR)
 ; 
 S AGINT=$S(AGFLAG="S":$$GETSO^AUPNSOGI(AGPATDFN,"I"),AGFLAG="G":$$GETGI^AUPNSOGI(AGPATDFN,"I"),1:"")
 F AGCNT=2:1:$L(AGLIST,U) S AGTMP=$P(AGLIST,U,AGCNT) Q:'$L(AGTMP)  D
 . I AGSCR[AGTMP,AGANS="A" S AGRES=$G(AGRES)_$S($L($G(AGRES)):U,1:"")_$S(AGFLAG="S":$$SO^AUTSOGI(AGTMP,"I"),AGFLAG="G":$$GI^AUTSOGI(AGTMP,"I"),1:"")
 . I AGSCR'[AGTMP,AGANS="B" S AGRES=$G(AGRES)_$S($L($G(AGRES)):U,1:"")_$S(AGFLAG="S":$$SO^AUTSOGI(AGTMP,"I"),AGFLAG="G":$$GI^AUTSOGI(AGTMP,"I"),1:"")
 ;
 S:AGFLAG="S" AGTMP=$$SETSO^AUPNSOGI(AGPATDFN,AGRES,$S(AGANS="B":$P(AGINT,U,$L(AGINT,U)),1:""),$P(AGINT,U))
 S:AGFLAG="G" AGTMP=$$SETGI^AUPNSOGI(AGPATDFN,AGRES,$S(AGANS="B":$P(AGINT,U,$L(AGINT,U)),1:""),$P(AGINT,U))
 Q
EDBAN(DFN)  ;EP - DISPLAY BANNER FOR AGEDIT PAGES
 N AGELSTS S DFN=$G(DFN) Q:'DFN
 W !,$$GETPREF^AUPNSOGI(DFN,"E")
 I $D(AGCHRT) W ?55,"HRN:",AGCHRT
 S AGELSTS=$P($G(^AUPNPAT(DFN,11)),U,12)
 W ?66,$S(AGELSTS="C":"CHS & DIRECT",AGELSTS="I":"INELIGIBLE",AGELSTS="D":"DIRECT ONLY",AGELSTS="P":"PEND. VERIF",1:"NONE")
 Q
EDREAD ;EP - PROMPTS THE USER FOR A RESPONSE WITH FUNCTIONALITY SPECIFIC TO THE AGEDIT PAGES
 ; DEFAULTS TO A STANDARD HELP TEXT IF THE CALLING PROCESS DID NOT SPECIFY DIR
 K DFOUT,DTOUT,DUOUT,DQOUT,DLOUT,AG("ED"),AG("ERR"),DIROUT
 I '$D(DIR("?")) D
 . S DIR("?")="Enter your choice now."
 . S DIR("?",1)="You may enter the item number of the field you wish to edit,"
 . S DIR("?",2)="OR you can enter 'P#' where P stands for 'page' and '#' stands for"
 . S DIR("?",3)="the page you wish to jump to, OR enter '^' to go back one page"
 . S DIR("?",4)="OR, enter '^^' to exit the edit screens, OR RETURN to go to the next screen."
 S DIR(0)="FO"
 D ^DIR
 ; COMMON FILTERING/PROCESSING OF USER RESPONSE
 Q:$D(DTOUT)
 S:Y="/.,"!(Y="^^") DFOUT=""
 S:Y="" DLOUT=""
 S:Y="^" (DUOUT,Y)=""
 S:Y?1"?".E!(Y["^") (DQOUT,Y)=""
 S X=Y,Y=$$UP^XLFSTR(X)
 Q:Y="P"
 ; PROCESS A PAGE JUMP
 I $E(Y,1)="P"&($P($G(^AUPNPAT(DFN,11)),U,12)'="") D
 . S AG("ED")=$$CHKPG(Y)
 . I '$L(AG("ED")) W *7,!!,Y," not found." K AG("ED") S AG("ERR")=""
 ;
 I $E(Y,1)="P"&($P($G(^AUPNPAT(DFN,11)),U,12)="") D
 . W *7,!!,"Eligibility Status must be entered." H 2
 Q
