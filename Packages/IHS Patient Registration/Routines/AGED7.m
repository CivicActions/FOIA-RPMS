 ;  BEGIN REMOVAL OF OLD CODE IHS/SD/EFG  AG*7*2  #5
 ;  S:Y="" DLOUT=""
 ;  G END:$D(DTOUT)!$D(DLOUT)!$D(DIROUT)!$D(DUOUT)!(Y["N"),L6:Y="A",VAR:$D(AG("ERR")) G:$D(AG("ED"))&'$D(AGXTERN) @("^AGED"_AG("ED"))
