 ;  END REMOVAL OF OLD CODE IHS/SD/EFG  AG*7*2  #5
L4 ;
L4DATA ;
 S AGELP("HRN")=AGCHRT,AGELP("TYPE")="PI",AGELP("PDFN")=DFN
