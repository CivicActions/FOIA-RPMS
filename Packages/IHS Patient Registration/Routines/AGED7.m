 I $D(AGSEENLY) S AGINSPTR=$P($P(AGINSREC,U,11),",",3) G L4DATA
 I '$D(AGSEENLY)&(AGADDINS="E") S AGINSPTR=$P($P(AGINSREC,U,11),",",3) G L3
