BRNREP ; IHS/OIT/GAB - ROI DISCLOSURES PRINT REPORTS BY DATE RANGE AND FACILITY;
 ;;2.0;RELEASE OF INFO SYSTEM;*5*;APR 10, 2003;Build 20
 ;IHS/OIT/GAB 06/2023
 ;The option sets BRNOP=1 (Request Method) or BRNOP=2 (Record Dissemination) report type
 ;
ASK ;Ask For Date Range
 ;
 ;
BD ;get beginning date
 W !! S DIR(0)="D^:"_DT_":EP",DIR("A")="Enter beginning ROI Initiated Date" D ^DIR K DIR S:$D(DUOUT) DIRUT=1
 I $D(DIRUT) G END
 S BRNBD=Y
ED ;get ending date
 W ! S DIR(0)="D^"_BRNBD_":"_DT_":EP",DIR("A")="Enter ending ROI Initiation Date"  D ^DIR K DIR S:$D(DUOUT) DIRUT=1
 I $D(DIRUT) G BD
 S BRNED=Y
 S X1=BRNBD,X2=-1 D C^%DTC S BRNSD=X
 W !
 ;
 NEW BRNFAC,BRNFACN D ASKFAC^BRNU(.BRNFAC) I BRNFAC="" D ED Q
 I BRNFAC>0 S BRNFACN=$$GET1^DIQ(90264.2,BRNFAC,.01)
 I BRNOP=1 D PRINTM
 I BRNOP=2 D PRINTD
 D END
 Q
 ;
PRINTM ;PRINT DISCLOSURE TOTALS BY REQUEST METHOD (field .21), DATE & FACILITY 
 S FLDS="[BRN RPT REQUEST METHOD]",BY="FACILITY,'(#.01),INTERNAL(#.08)=""C"",+.21",DIC="^BRNREC(",L=0
 I BRNFAC=0 S FR="@,"_BRNBD,TO="ZZZ,"_BRNED
 E  S FR=BRNFACN_","_BRNBD,TO=BRNFACN_","_BRNED
 ;
 K DHIT,DIOEND,DIOBEG
 D EN1^DIP
 Q
PRINTD ;PRINT DISCLOSURES TOTALS BY DISSEMINATION TYPE, DATE & FACILITY
 S FLDS="[BRN RPT RECORD DISSEM]",BY="FACILITY,'(#.01),INTERNAL(#.08)=""C"",+23,.03",DIC="^BRNREC(",L=0
 I BRNFAC=0 S FR="@,"_BRNBD,TO="ZZZ,"_BRNED
 E  S FR=BRNFACN_","_BRNBD,TO=BRNFACN_","_BRNED
 ;
 K DHIT,DIOEND,DIOBEG
 D EN1^DIP
 Q
END ;
 K BRNBD,BRNED,BRNSD,X,DD0,B,BRNOP Q
