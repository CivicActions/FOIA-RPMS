BRNPTN ; IHS/PHXAO/TMJ - ROI CUMULATIVE PATIENT HX FOR CHART ; 
 ;;2.0;RELEASE OF INFO SYSTEM;**4,5,6**;APR 10, 2003;Build 11
 ;;IHS/OIT/GAB PATCH 4 - FIX UNDEFINED ERROR; April 2017
 ;;IHS/GDIT/AEF PATCH 5 - PRINT PATIENT WITH NO DISCLOSURES; May 2023
SERVICE ;PICK PATIENT NAME ENTRY
 W !!!
 S DIC=2 S DIC("A")="Enter a Patient Name: " S DIC(0)="AEMIQO" D ^DIC
 G END:Y<1 S BRNPTN=+Y
 I BRNPTN="" Q
 ;D CKDIS           ;IHS/OIT/GAB PATCH 4 CHECK FOR ANY DISCLOSURES
 ;G RETURN:NODIS=1  ;IHS/OIT/GAB PATCH 4 END SINCE NO DISCLOSURES
 I '$D(^BRNREC("AA",BRNPTN)) D NODIS(BRNPTN) Q   ;IHS/GDIT/AEF PATCH 5 NO DISCLOSURES
PRINT ;PRINT CUMULATIVE PATIENT RECORD OF ALL DISCLOSURES
 W !
 S FLDS="[BRN GS ACCOUNTING RECORD]",BY="@INTERNAL(#.03)",DIC="^BRNREC(",L=0
 S FR=BRNPTN,TO=BRNPTN
 K DHIT,DIOEND,DIOBEG
 D EN1^DIP
 G RETURN
 Q
CKDIS ; IHS/OIT/GAB PATCH 4 - FIX UNDEF ERROR WHEN THERE ARE NO DISCLOSURES
 S NODIS=""
 I '$D(^BRNREC("AA",BRNPTN)) W !!,?20,"******** NO EXISTING DISCLOSURES FOR THIS PATIENT ***" S NODIS=1
 Q
NODIS(BRNPTN) ;IHS/GDIT/AEF PATCH 5 PRINT PATIENT WITH NO DISCLOSURES
 ;----- PRINT WHEN THERE ARE NO DISCLOSURES
 ;
 N BY,DIC,FLDS,FR,TO
 S DIC="^AUPNPAT("
 S FLDS="[BRN GS ACCOUNTING - NONE]"
 S BY="@INTERNAL(#.01)"
 S (FR,TO)=BRNPTN
 K DHIT,DIOEND,DIOBEG
 D EN1^DIP
 D RETURN
 Q
RETURN ;   /IHS/GAB ADDED PATCH #4
 ; Press return to continue
 W !
 S DIR(0)="E"
 S DIR("A")="Press return to continue..."
 D ^DIR
 K DIR
END ;
 K BRNPTN,X,DD0,B Q
