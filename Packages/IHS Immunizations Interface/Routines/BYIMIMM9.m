LE ;EP;PROCESS SEQUENTIAL EXPORT
 S BYIMDUZ=$$DUZ^BYIMIMM()
 I $G(BYIMLEDS),$G(BYIMLEDT),BYIMLEDS<BYIMLEDT S BYIMXREF="B"
 E  S BYIMXREF="APCIS"
 I '$G(BYIMLEDT)!'$G(BYIMLEDS) D LED^BYIMIMM7
 S:'$G(BYIMLEDE) BYIMLEDE=9999999
 ;FIND NEW/EDITED IMMUNIZATIONS
 S BYIMLEDE=BYIMLEDE+.99
 S ZD19=$P(BYIMLEDS,".")-.01
 F  S ZD19=$O(^AUPNVSIT(BYIMXREF,ZD19)) Q:'ZD19!(ZD19>BYIMLEDE)  D
 .S ZVA=9999999999
 .F  S ZVA=$O(^AUPNVSIT(BYIMXREF,ZD19,ZVA),-1) Q:'ZVA  D
 ..S X=$G(^AUPNVSIT(ZVA,0))
 ..S DFN=$P(X,U,5)
 ..Q:'DFN
 ..Q:$D(^BYIMTMP($J,"BYIM EXP",DFN))
 ..Q:'$O(^AUPNVIMM("AD",ZVA,0))
 ..Q:$G(^DPT(DFN,.35))!'$O(^AUPNPAT(DFN,41,0))!'$G(^AUPNPAT(DFN,0))
 ..;PATCH 3 CR-12431 Filter out Demo patients per standard screen
 ..Q:$$DEMO^BYIMIMM2(DFN)
 ..;PATCH 3 CR-12431 END
 ..S:'$G(BYIMALL) BYIMALL=1
 ..S:'$G(BYIMADM) BYIMADM=1
 ..S:'$G(DDATE) DDATE=""
 ..I YEARS=19 Q:$P(X,U,3)<DATE19
 ..I YEARS=65 Q:$P(X,U,3)<DATE19&($P(X,U,3)>DATE65)
 ..;INCLUDE DATE/TIME TO ENSURE RXA'S ARENT'S MISSED
 ..I BYIMALL=1!(BYIMADM=1) Q:'$$NEW^BYIMIMM6(DFN,BYIMALL,BYIMADM,BYIMLEDT,BYIMLEDS,BYIMLEDE,BYIMHX)
 ..S ^BYIMTMP($J,"BYIM EXP",DFN,ZVA)=""
 ..W:'$D(ZTQUEUED) "."
 ;V3.0 PATCH 7 - FID-97512 MISSING OR OUT-OF ORDER SEGMENTS
 ;FIND NEW/EDITED REFUSALS
 S ZD19=$P(BYIMLEDS,".")-.01
 F  S ZD19=$O(^AUPNPREF("ADLM",ZD19)) Q:'ZD19!(ZD19>BYIMLEDE)  D
 .S ZVA=0
 .F  S ZVA=$O(^AUPNPREF("ADLM",ZD19,ZVA)) Q:'ZVA  D
 ..Q:+$G(^AUPNPREF(ZVA,0))'=3
 ..S X=$G(^AUPNPREF(ZVA,0))
 ..S DFN=$P(X,U,2)
 ..Q:'DFN
 ..Q:$D(^BYIMTMP($J,"BYIM EXP",DFN))
 ..;PATCH 3 CR-12431 Filter out Demo patients per standard screen
 ..Q:$$DEMO^BYIMIMM2(DFN)
 ..Q:$G(^DPT(DFN,.35))!'$O(^AUPNPAT(DFN,41,0))!'$G(^AUPNPAT(DFN,0))
 ..;PATCH 3 CR-12431 END
 ..S ^BYIMTMP($J,"BYIM EXP",DFN,+$O(^AUPNVSIT("AC",DFN,9999999999),-1))=""
 ;FIND NEW CONTRAINDICATIONS
 S ZVA=+$G(^BYIMPARA(BYIMDUZ,12))
 S:'ZVA ZVA=$O(^BIPC(9999999999),-1)
 F  S ZVA=$O(^BIPC(ZVA)) Q:'ZVA  D
 .S X=$G(^BIPC(ZVA,0))
 .S Y=$P(X,U,3)
 .Q:"^11^16^17^18^"[(U_Y_U)
 .S DFN=+X
 .Q:'DFN
 .;PATCH 3 CR-12431 Filter out Demo patients per standard screen
 .Q:$$DEMO^BYIMIMM2(DFN)
 .;PATCH 3 CR-12431 END
 .Q:$D(^BYIMTMP($J,"BYIM EXP",DFN))
 .Q:$G(^DPT(DFN,.35))!'$O(^AUPNPAT(DFN,41,0))!'$G(^AUPNPAT(DFN,0))
 .S ^BYIMTMP($J,"BYIM EXP",DFN,+$O(^AUPNVSIT("AC",DFN,9999999999),-1))=""
 S $P(^BYIMPARA(BYIMDUZ,12),U)=$O(^BIPC(9999999999),-1)
 ;V3.0 PATCH 7 - FID-97512 MISSING OR OUT-OF ORDER SEGMENTS
 ;FIND NEW/EDITED DELETES 
 S ZVA=$P(^BIVIMMD(0),U,3)-100
 F  S ZVA=$O(^BIVIMMD(ZVA)) Q:'ZVA  D:'$D(^BYIMEXP("DEL"_BYIMDUZ,ZVA))
 .Q:$P($G(^BIVIMMD(ZVA,0)),U,19)'="d"
 .S X=$G(^BIVIMMD(ZVA,0))
 .S ZVV=$P(X,U,3)
 .S DFN=$P(X,U,2)
 .Q:'DFN
 .Q:$D(^BYIMTMP($J,"BYIM EXP",DFN))
 .;PATCH 3 CR-12431 Filter out Demo patients per standard screen
 .Q:$$DEMO^BYIMIMM2(DFN)
 .Q:$G(^DPT(DFN,.35))!'$O(^AUPNPAT(DFN,41,0))!'$G(^AUPNPAT(DFN,0))
 .;PATCH 3 CR-12431 END
 .S ^BYIMTMP($J,"BYIM EXP",DFN,ZVV)=""
 I '$O(^BYIMTMP($J,"BYIM EXP",0)) D  Q
 .D NOW^%DTC
 .S BYIMFILE="NoImmsToExp_"_$P($G(^AUTTLOC(BYIMDUZ,0)),U,10)_"_"_(DT+17000000)_"_"_$P(%,".",2)_".dat"
 .S:'$G(BYIMTEST) $P(^BYIMPARA(BYIMDUZ,"LAST EXPORT",DT),U)=%,$P(^(DT),U,2)=%
 .S DFNCNT=0
 .S IMMCNT=0
 .D PATH^BYIMIMM6
 .D LOG^BYIMIMM4(BYIMFILE,"E",DFNCNT,IMMCNT,,,,OPATH)
 .S:'$G(BYIMTEST) $P(^BYIMPARA(BYIMDUZ,"LAST EXPORT",DT),U,2)="NoImmsToExp"
 .Q:$D(ZTQUEUED)
 .W !!,"No Immunizations found that need to be exported."
 .D PAUSE^BYIMIMM6
 ;V3.0 PATCH 7 - FID-97512 END
 D DEX^BYIMIMM
 S Y=2,DUZ(Y)=BYIMDUZ(2)
 Q
