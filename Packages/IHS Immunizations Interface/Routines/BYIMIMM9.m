EXST ;EP;SETUP EXTERIOR STATE EXPORT LIST
 ;V3.0 PATCH 6 - FID-83363 EXPORT FOR SELECTED STATES
 S BYIMEXST=$G(BYIMEXST)
 I $G(BYIMEXST)="",$G(^BYIMTMP("EXST"))]"" S BYIMEXST=^("EXST") K ^BYIMTMP("EXST")
 I BYIMEXST="" D
 .S BYIMEXST=","
 .S X=0
 .F  S X=$O(^BYIMPARA(BYIMDUZ,3,X)) Q:'X  S X0=$G(^(X,0)) D
 ..I $G(^BYIMTMP("EXST"))'="COVID" S BYIMEXST=BYIMEXST_X_","
 ..I $G(^BYIMTMP("EXST"))="COVID",$P(X0,U)["COVID" S BYIMEXST=BYIMEXST_X_","
 .S BYIMEXST=BYIMEXST_BYIMDUZ_","
 .K ^BYIMTMP("EXST")
 Q:$D(ZTQUEUED)
 I $G(BYIMEXST)]"" D
 .S END=$L(BYIMEXST,",")-1
 .F J=1:1:END D
 ..S Y=+$P(BYIMEXST,",",J)
 ..S:Y'=BYIMDUZ ST=+$P($G(^BYIMPARA(BYIMDUZ,3,Y,0)),U,14)
 ..S:Y=BYIMDUZ ST=+$P($G(^BYIMPARA(BYIMDUZ,0)),U,14)
 ..S OPATH=$G(OPATH(ST))
 ..W !?10,$P($G(^DIC(5,ST,0)),U),?25,OPATH
 Q
