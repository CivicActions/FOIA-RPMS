NLE ;EP;PROCESS FIRST EXPORT
 ;PATCH 3 CR-12383 SPECIFY IMMUNIZATIONS TO SEND
 N BYIMALL
 S BYIMALL=1
 S BYIMADM=1
 S BYIMLEDT=DT-100000
 S BYIMLEDS=BYIMLEDT
 S BYIMLEDE=9999999
 S BYIMHX=0
 F  S D19=$O(^DPT("ADOB",D19)) Q:'D19  D
 .S DFN=0
 .F  S DFN=$O(^DPT("ADOB",D19,DFN)) Q:'DFN  D:$O(^AUPNVIMM("AC",DFN,0))
 ..Q:$G(^DPT(DFN,.35))!'$G(^AUPNPAT(DFN,0))
 ..Q:$O(^BYIMTMP($J,"BYIM EXP",DFN,0))
 ..;PATCH 3 CR-12431 Filter out Demo patients per standard screen
 ..Q:$$DEMO^BYIMIMM2(DFN)
 ..;PATCH 3 CR-12431 END
 ..S X=+$O(^AUPNVIMM("AC",DFN,9999999999),-1)
 ..S X=$G(^AUPNVIMM(X,0))
 ..Q:X=""
 ..S VDT=$P($P($G(^AUPNVIMM(X,12)),U),".")
 ..S X=+$P(X,U,3)
 ..Q:'$G(^AUPNVSIT(X,0))
 ..S:'VDT VDT=$P($P($G(^AUPNVSIT(X,0)),U),".")
 ..Q:+VDT<BYIMLEDT
 ..S ^BYIMTMP($J,"BYIM EXP",DFN,X)=""
 ..W:'$D(ZTQUEUED) "."
 Q:'$O(^BYIMTMP($J,"BYIM EXP",0))
 D DEX^BYIMIMM
 S Y=2,DUZ(Y)=BYIMDUZ(2)
 Q
