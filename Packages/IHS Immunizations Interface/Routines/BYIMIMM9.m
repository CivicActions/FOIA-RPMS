PID25(X,ST) ;EP;CHECK BIRTH ORDER CONTROL FOR MSH-25   
 ;V3.0 PATCH 6 - FID 96332 CHECK BIRTH ORDER CONTROL FOR MSH-25   
 ;If BOC = 0 LEAVE MSH-25 AS IS
 ;If BOC = 1 and Multiple Birth flag = N set MSH-25 = NULL
 ;X  = MSH SEGMENT
 ;ST = STATE FOR EXPORT FILE
 N BOC,MB
 S BOC=0,MB=""
 S:'$G(ST) ST=BYIMDUZ
 S:ST=BYIMDUZ BOC=+$P($G(^BYIMPARA(BYIMDUZ,10)),U,6)
 S:ST'=BYIMDUZ BOC=+$P($G(^BYIMPARA(BYIMDUZ,3,ST,10)),U,6)
 S MB=$P($P(X,"|",$S(X["PID|":25,1:11)),U)
 I BOC,MB="N",ST S $P(X,"|",$S(X["PID|":26,1:12))=""
 Q X
 ;V3.0 PATCH 6 - FID 96332 END
