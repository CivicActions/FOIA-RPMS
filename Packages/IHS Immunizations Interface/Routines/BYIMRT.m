 S Y=$$DEL^%ZISH(DPATH,FILE)
 S X1=$G(^BYIMRT(RSPDA,1,0))
 S X2=$G(^BYIMRT(RSPDA,1,1,0))
 S X3=$G(^BYIMRT(RSPDA,1,2,0))
 S X4=$G(^BYIMRT(RSPDA,1,3,0))
