RTIMP(IPATH,FILE,STATE,OPATH) ;EP;IMPORT RESPONSE MESSAGES
 ;IPATH = INBOUND PATH FOR STATE
 ;FILE  = NAME OF FILE TO BE PROCESSED
 ;STATE = STATE WITH WHICH SITE IS EXCHANGING
 ;
