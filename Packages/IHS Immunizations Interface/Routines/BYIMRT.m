 ;DELETE IMPORTED FILE
 S Y=$$DEL^%ZISH(IPATH,FILE)
 ;DELETE 'SENT' COPY
 S SLASH=$S(IPATH["/":"/",1:"\")
 S LNG=$L(OPATH,SLASH)-2
 S PATH=$P(OPATH,SLASH,1,LNG)
 S DPATH=PATH_"sent"_SLASH
 ;S Y=$$DEL^%ZISH(DPATH,FILE)
 ;DELETE 'BACKUP' COPY
