 S INDA("BYIMADM")=$G(BYIMADM)
 S INDA("BYIMLEDT")=$G(BYIMLEDT)
 S INDA("BYIMLEDS")=$G(BYIMLEDS)
 S INDA("BYIMLEDE")=$G(BYIMLEDE)
 S INDA("BYIMTEST")=$G(BYIMTEST)
