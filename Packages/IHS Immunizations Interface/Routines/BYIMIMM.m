V04(BYIMVST,BYIMALL,BYIMADM,BYIMLEDT,BYIMLEDS,BYIMLEDE,BYIMHX) ;CREATE MESSAGE
 ;this is the unsolicited Imm record
 ;
 ;BYIMVST  = IEN OF VISIT
 ;BYIMALL  = 1 EXPORT NEW or EDITED ONLY
 ;           2 EXPORT ALL IMMS FOR PATIENT
 ;BYIMADM  = 1 EXPORT ONLY ADMINISTERED IMMUNIZATIONS
 ;           2 EXPORT ADMINISTERED AND HISTORIC
 ;BYIMLEDT = LAST EXPORT DATE
 ;BYIMLEDS = SELECTED EXPORT START DATE
 ;BYIMLEDE = SELECTED EXPORT END DATE
 ;BYIMHX   = 0 EXCLUDE HISTORIC IF BYIMALL=1
 ;           1 INCLUDE HISTORIC IF BYIMALL=1
 ;             & START DATE LESS THAN LAST EXPORT DATE
 ;
