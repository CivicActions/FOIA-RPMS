ADJTIME(DFN,TIME) ;Adjust seconds if inpatient and capture time is same as admission time sans seconds
 N VAIN,ADTM,PADTM,PTIME
 D INP^VADPT
 S ADTM=$P($G(VAIN(7)),U)
 Q:'ADTM TIME
 S PADTM=$E(ADTM,1,12)
 S PTIME=$E(TIME,1,12)
 I PADTM=PTIME,(TIME<ADTM) D
 .S TIME=PTIME_$S($L($E(ADTM,13,14)):$E(ADTM,13,14),1:"00")
 Q TIME
