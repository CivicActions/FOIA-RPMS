GETTYPES(DATA) ;Return list of data input types
 ;ID^TYPE^LABEL
 N LP,N0,NAME,TMP,SET,EXC
 D GETPARLI^CIAVMRPC(.EXC,"BEHOGMY EXCLUDE IV TYPES",,,DUZ)
 K DATA,CNT
 S CNT=0
 S LP=0 F  S LP=$O(^GMRD(126.56,LP)) Q:'LP  S N0=^(LP,0) D
 .S NAME=$P(N0,U)
 .S CNT=CNT+1,DATA(CNT)=LP_U_"I"_U_NAME_U_$S(NAME="PO":"ORAL",1:NAME)
 S LP=0 F  S LP=$O(^GMRD(126.58,LP)) Q:'LP  S N0=^(LP,0) D
 .S NAME=$P(N0,U)
 .S CNT=CNT+1,DATA(CNT)=LP_U_"O"_U_NAME_U_NAME
 ;IV set of codes
 D FIELD^DID(126.03,3,"","POINTER","TMP")
 S SET=$G(TMP("POINTER"))
 I $L(SET) D
 .S LP=1 F  S VAL=$P(SET,";",LP) Q:'$L(VAL)  D  S LP=LP+1
 ..Q:$$EXCLUDE($P(VAL,":"))  ;value in BEHOGMY EXCLUDE IV TYPES xpar
 ..S CNT=CNT+1,DATA(CNT)=$P(VAL,":")_U_"IV"_U_$P(VAL,":",2)_U_$S($P(VAL,":")="B":"BLOOD PRODUCTS",1:$P(VAL,":",2))
 Q
EXCLUDE(V) ;Check for exclusion
 N LP,ND,RES
 S RES=0
 S LP=0 F  S LP=$O(@EXC@(LP)) Q:'LP!RES  S ND=$G(@EXC@(LP)) D
 .S RES=$P(ND,U,2)=V
 Q RES
