 .S PROVIEN=$P(@DOCS@("DILIST",INDEX,0),U),NAME=$P(@DOCS@("DILIST",INDEX,0),U,2)
 .N ACCOUNT S ACCOUNT=$$ACTIVE^XUSER($G(PROVIEN)),DEA=$P(@DOCS@("DILIST",INDEX,0),U,3)
 .D VRFYPHSH^BEHOEP3(.R,$G(PROVIEN))
 .I $G(R)=1 S PISTATUS="MATCH",MISMATCH=0
 .E  S PISTATUS="MISMATCH",MISMATCH=1 ;Display Mismatch First
 .I 'DISINC,(+ACCOUNT<1),($P(ACCOUNT,U,2)'="") Q
 .S ACCOUNT("TEXT")=$S($P(ACCOUNT,U,2)'="":$P(ACCOUNT,U,2),ACCOUNT=0:"CANNOT SIGN ON",1:"UNKNOWN")
 .;INCLUDE USERS WHO CANNOT SIGN-ON IN ACTIVE LISTING
 .I ACCOUNT=0 S ACCOUNT=1
 .S @DATA@(+ACCOUNT,MISMATCH,NAME)=$$LJ^XLFSTR($G(DEA),11,"  ")_$$LJ^XLFSTR($G(PISTATUS),15,"  ")_ACCOUNT("TEXT")
 F ACCOUNT=1:-1:0  D  Q:$G(STOP)
 .Q:'$D(@DATA@(ACCOUNT))
 .F MISMATCH=1:-1:0  D  Q:$G(STOP)
 ..Q:'$D(@DATA@(ACCOUNT,MISMATCH))
 ..I ACCOUNT=0 D
 ...I ($Y+CBUFFER+4)>IOSL!($Y=0) D  Q:$G(STOP)
 ....S STOP=$$HEADER^BEHOEP3(RPTNAME,.PGNUM,.COL,.COVERCOL)
 ...D DISUSERD
 ...W $$CJ^XLFSTR($$REPEAT^XLFSTR("-",34),$S(+$G(IOM)>0:IOM,1:80)," "),!
 ..S NAME="" F  S NAME=$O(@DATA@(ACCOUNT,MISMATCH,NAME)) Q:NAME=""!($G(STOP))  D
 ...I ($Y+CBUFFER)>IOSL!($Y=0) D  Q:$G(STOP)
 ....S STOP=$$HEADER^BEHOEP3(RPTNAME,.PGNUM,.COL,.COVERCOL)
 ...W $$LJ^XLFSTR(NAME,35," ")_@DATA@(ACCOUNT,MISMATCH,NAME),!
 ...S OUTPUT=1
 I '$G(STOP) D
 .I '$G(OUTPUT) D  Q:$G(STOP)
 ..S STOP=$$HEADER^BEHOEP3(RPTNAME,.PGNUM,.COL,.COVERCOL)
 ..Q:STOP
 ..D NODATA
 .I ($Y+2)>IOSL S STOP=$$HEADER^BEHOEP3(RPTNAME,.PGNUM,.COL,.COVERCOL)
 .Q:$G(STOP)
 .D RPTEND
 K @DOCS,@DATA
 S:$D(ZTQUEUED) ZTREQ="@"
