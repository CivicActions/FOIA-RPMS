PPIQ ;EP - TASKMAN ENTRY POINT
 ;CBUFFER IS HANDLED BY DEVICE^ORUTL OR TASKMAN
 N DOCS,DATA,SCR,ERROR,INDEX,PGNUM,COL,STOP,ACCOUNT,OUTPUT,PISTATUS,PROVIEN,TMP,MISMATCH,NAME,RPTNAME,COVERCOL,R
 ;RETRIEVE ALL USERS WITH A EPCS PROFILE HASH in FILE #200
 S DOCS=$NA(^TMP($J,"EPCSPPI")),DATA=$NA(^TMP($J,"EPCSPPIDATA")),RPTNAME="PROVIDER PROFILE INTEGRITY REPORT"
 K @DOCS,@DATA
 S I=$$SENSTIV1^BEHOEP3(.COL)
 S I=I+1,COL(I)="NAME                               DEA#       HASH STATUS    ACCOUNT STATUS"
 S I=$$SENSTIV2^BEHOEP3(.COVERCOL)
 S I=I+1,COVERCOL(I)="NAME                               DEA#       HASH STATUS    ACCOUNT STATUS"
 S SCR="I 1"
 S SCR=SCR_" & (($P($G("_"^"_"(""IHSEPCS1"")),""^"",1))'="""""_")"
 S INDEX=$O(PROVARRY(0))
 I +INDEX>0 S TMP=" & ("
 I +INDEX>0 F  D  Q:(+INDEX'>0)
 .S TMP=$G(TMP)_"(($P($G("_"^"_"(0)),""^"",1))="_""""_$G(PROVARRY(INDEX))_""""_")"
 .I $O(PROVARRY(INDEX))>0 S TMP=$G(TMP)_" ! "
 .S INDEX=$O(PROVARRY(INDEX))
 S:$G(TMP)'="" TMP=$G(TMP)_")",SCR=SCR_$G(TMP)
 D LIST^DIC(200,,"@;.01;53.2","PQ",,,,,SCR,,DOCS,"ERROR")
 I $D(ERROR) D  Q
 .D FMERROR^ORUTL(.ERROR)
 .S:$D(ZTQUEUED) ZTREQ="@"
 .K @DOCS
