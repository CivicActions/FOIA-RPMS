 ;;1.1;BEH COMPONENTS;**073001**;March 20,2007
 ;Utility RPCs to support implantable devices
 Q
PROB(DATA,DFN) ;EP  Allow user to search for problem to attach
 N IEN,ITM,ICD,CNT,STAT,NARR,IMP,SNODATA
 K DATA
 S IEN=0,CNT=0
 S STAT="" F  S STAT=$O(^AUPNPROB("ACTIVE",DFN,STAT)) Q:STAT=""  D
 .Q:STAT="I"!(STAT="D")                     ;Make it exclusionary
 .S IEN="" F  S IEN=+$O(^AUPNPROB("ACTIVE",DFN,STAT,IEN)) Q:'IEN  D
 ..S ITM=$$GET1^DIQ(9000011,IEN,.05),ICD=+$G(^AUPNPROB(IEN,0))
 ..S CONCID=$$GET1^DIQ(9000011,IEN,80001)
 ..Q:CONCID=""
 ..S SNODATA=$$CONC^BSTSAPI(CONCID_"^^^1")
 ..S ICD=$P($P(SNODATA,U,5),";",1)
 ..D ADDIND(ITM,ICD,IEN,CONCID)
 K DATA(0)
 Q
ADDIND(ITM,ICD,IEN,CONCID) ;EP-
 N CODE,ONSET
 S CODE=$P($$ICDDX^ICDEX(ICD,$$NOW^XLFDT),U,2)
 I '$L(ITM) D
 .S ITM=$P($$ICDDX^ICDEX(ICD,$$NOW^XLFDT),U,4)
 .E  S ITM=$$GET1^DIQ(80,ICD,3)
 S:'$L($P(ITM,"| ",2)) ITM=$P(ITM,"| ")
 S CNT=CNT+1
 S ONSET=$$GET1^DIQ(9000011,IEN,.13)
 S DATA(CNT)=IEN_U_ITM_U_CODE_U_CONCID_U_ONSET
 Q
