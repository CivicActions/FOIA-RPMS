 I AMQQSQPS=2,Y'="S" Q
SET1 S @AMQQSQPG@(+X)=""
 Q
 ;
PASS2 N AMQP S AMQQSQPN=AMQQSQP1-.001
 F  S AMQQSQPN=$O(AMQV("QQ",AMQQSQPN)) Q:'AMQQSQPN  Q:AMQQSQPN>AMQQSQP2  S AMQQSQPT=AMQV("QQ",AMQQSQPN,1) D TEST
 Q
