TM2RUN   ;
 N JOB,LOC,DBGCNT,START,END,X
 K ^BARTMP("BARTMCALC"),^BARTMP("BARTMTOT")
 S (JOB,LOC,DBGCNT)=0
 F  S JOB=$O(^BARTMP("BARTIME",JOB)) Q:'JOB  D
 .F X="BILLKUP","CLM2","BARFNDGT20","BARFNDLT20","RMVPM","PLB","CLMFLG","HIPAACLM","HIPAAREA" D
 ..S (DBGCNT)=0
 ..F  S DBGCNT=$O(^BARTMP("BARTIME",JOB,X,DBGCNT)) Q:'DBGCNT  D
 ...S (START,END)=0
 ...I $G(^BARTMP("BARTIME",JOB,X,DBGCNT,1))'>0 Q
 ...I $G(^BARTMP("BARTIME",JOB,X,DBGCNT,2))'>0 Q
 ...S START=$P($P($G(^BARTMP("BARTIME",JOB,X,DBGCNT,1)),U,1),",",2)
 ...S END=$P($P($G(^BARTMP("BARTIME",JOB,X,DBGCNT,2)),U,1),",",2)
 ...S ^BARTMP("BARTMCALC",JOB,X,DBGCNT)=END-START_U_JOB_U_X_U
 ...S $P(^BARTMP("BARTMTOT",JOB,X),U,1)=($P($G(^BARTMP("BARTMTOT",JOB,X)),U,1))+END-START
 ...S $P(^BARTMP("BARTMTOT",JOB,X),U,2)=($P($G(^BARTMP("BARTMTOT",JOB,X)),U,2)+1)
 ...S $P(^BARTMP("BARTMTOT",JOB,X),U,3)=($P($G(^BARTMP("BARTMTOT",JOB,X)),U,1))/($P($G(^BARTMP("BARTMTOT",JOB,X)),U,2))
 ...W !,JOB,"  ",X,"  ELAPSED: ",END-START,"   COUNT: "_$P($G(^BARTMP("BARTMTOT",JOB,X)),U,2),"    AVG: "_$P($G(^BARTMP("BARTMTOT",JOB,X)),U,3)
 Q
TM2KILL  ;
 N JOB
 S JOB=0
 F  S JOB=$O(^BARTMP("BARTIME",JOB)) Q:+JOB=0  D
 .K ^BARTMP("BARTIME",JOB)
 .K ^BARTMP("BARTMTOT",JOB)
 .K ^BARTMP("BARTMCALC",JOB)
 Q
