 ...S ^BAR(90052.06,DA(2),DA(1),12,CT,0)=X_"^^^"_BAROIEN_"^^"_$P($T(@CT),";",2)
 ...S ^BAR(90052.06,DA(2),DA(1),12,"B",X,CT)=""
