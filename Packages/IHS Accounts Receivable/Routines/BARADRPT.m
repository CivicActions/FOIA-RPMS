BARADRPT ; IHS/SD/SDR - A123 FY22 AR-01 FIELD AUDIT REPORT ; 04/09/2013
 ;;1.8;IHS ACCOUNTS RECEIVABLE;**33**;OCT 26, 2005;Build 133
 ;;IHS/SD/SDR 1.8*33 ADO85707 New Report for FY22 Audit
 Q
 ;****************************************************
 ;
EN ; EP
 K BARY,BAR
 S BARDONE=0
 D:'$D(BARUSR) INIT^BARUTL      ; Set up basic A/R Variables
 S BARP("RTN")="BARADRPT"       ; Routine used to gather data
 S BAR("PRIVACY")=1             ; Privacy act applies (used BARRHD)
 S BAR("LOC")="VISIT"           ; should always be VISIT
 D DATES                        ; Ask transaction date range
 I +BARSTART<1 D XIT Q          ; Dates answered wrong
 D SETHDR                       ; Build header array
 S BARQ("RC")="COMPUTE^BARADRPT"  ; Build tmp global with data
 S BARQ("RP")="PRINT^BARADRPT"    ; Print reports from tmp global
 S BARQ("NS")="BAR"             ; Namespace for variables
 S BARQ("RX")="POUT^BARRUTL"    ; Clean-up routine
 D ^BARDBQUE                    ; Double queuing
 D PAZ^BARRUTL                  ; Press return to continue
 Q
 ;****************************************************
DATES ;
 S BARY("DT")="A"  ;always approval date
 ; Ask Date Range
 W !!," ============ Entry of Approval Date Range =============",!
 S BARSTART=$$DATE^BARDUTL(1)
 I BARSTART<1 Q
 S BAREND=$$DATE^BARDUTL(2)
 I BAREND<1 W ! G DATES
 I BAREND<BARSTART D  G DATES
 .W *7
 .W !!,"The END date must not be before the START date.",!
 S BARY("DT",1)=BARSTART
 S BARY("DT",2)=BAREND
 Q
 ; ************************************************
 ;
SETHDR ;
 ; Build header array
 S BAR("OPT")="ADR"
 S BAR("LVL")=0
 S BAR("HD",0)="A123 FY22 AR-01 Audit Report"
 D DT^BARRHD
 S BAR("LVL")=$G(BAR("LVL"))+1
 S BAR("HD",BAR("LVL"))=""
 S BAR("TXT")="ALL"
 I $D(BARY("LOC")) S BAR("TXT")=$P(^DIC(4,BARY("LOC"),0),U)
 S BAR("TXT")=BAR("TXT")_" Visit location(s) regardless of Billing Location"
 S BAR("CONJ")="at "
 D CHK^BARRHD
 S BAR("CONJ")=""
 S BAR("TXT")="Generated By "_$P($G(^VA(200,DUZ,0)),U)
 D CHK^BARRHD
 Q
 ; *********************************************************************
 ;
COMPUTE ; EP
 S BAR("SUBR")="BAR-ADR"
 K ^TMP($J,"BAR-ADR")
 S BARCNT=1
 S BARDUZ2=DUZ(2)
 S DUZ(2)=0
 F  S DUZ(2)=$O(^BARBL(DUZ(2))) Q:'DUZ(2)  I $$IHS^BARUFUT(DUZ(2)) D LOOP^BARRUTL ;loop thru locations (federal only)
 S DUZ(2)=BARDUZ2
 Q
 ; *********************************************************************
 ;
DATA ; EP
 S BAR0=$G(^BARBL(DUZ(2),BAR,0))
 S D0=$P(BAR0,U,3)
 S BARITYP=$$VALI^BARVPM(8)  ;GET INTERNAL CODE INSTEAD OF 'STANDS FOR'
 I "^P^N^"'[("^"_BARITYP_"^") Q  ;only Private and Non-Ben on report
 ;
 S BARADT=$P(BAR0,U,18)  ;approval date
 S BARTFLG=0
 D TRANS
 I BARTFLG=0 Q  ;there isn't a 120-day gap between any transactions (BILL NEW, pymts and adjs) - skip it
 ;
 S:(BARITYP'="N") BARACCT=$$GET1^DIQ(90050.02,$P(BAR0,U,3),".01","E")
 S:(BARITYP="N") BARACCT="NON-BENEFICIARY PATIENT"
 S BARAPPDT=$$CDT^BARDUTL($P(BAR0,U,18))  ;approval date
 S BARBILLN=$P(BAR0,U)
 S BARDUZ2=$P(BAR0,U,22)
 S BARIEN=$P(BAR0,U,17)
 S BARUINV=$$TRANSMIT^ABMUEAPI(BARDUZ2,BARIEN)  ;UFMS Invoice#
 I +BARUINV=0 S BARUINV="NO INV"_BARCNT,BARCNT=BARCNT+1
 S BARBAMT=$J($FN($P(BAR0,U,13),",",2),13)
 S BARCBAMT=$J($FN($P(BAR0,U,15),",",2),13)
 S D0=$P(BAR0,U,3)
 S BARITYP=$$VALI^BARVPM(8)  ;GET INTERNAL CODE INSTEAD OF 'STANDS FOR'
 S BARITIEN=$O(^AUTTINTY("C",BARITYP,0))
 S BARITYP=$$GET1^DIQ(9999999.181,BARITIEN,".01","E")
 S BARVLOC=$$GET1^DIQ(90050.01,BAR,108,"E")
 S BARDOSB=$$GET1^DIQ(90050.01,BAR,102,"E")
 S BARDOSE=$$GET1^DIQ(90050.01,BAR,103,"E")
 S BARREC=BARVLOC_U_BARUINV_U_BARDOSB_U_BARDOSE_U_BARAPPDT_U_BARBAMT_U_BARACCT_U_BARITYP
 ;S BARREC=BARREC_U_BARBILLN
 S ^TMP($J,"BAR-ADR",BARUINV)=BARREC
 Q
 ; *********************************************************************
XIT ;
 D ^BARVKL0
 Q
PRINT ;
 S BAR("PG")=1
 D WHD^BARRHD  ;Report header
 W !,"VISIT LOCATION^UFMS INVOICE NUMBER^DATE OF SERVICE BEGIN^DATE OF SERVICE END^DATE APPROVED^AMOUNT BILLED^ACTIVE INSURER^INSURER TYPE"
 I '((IO["\")!(IO["/")) W "~"
 S BARB=""
 I '$D(^TMP($J,"BAR-ADR")) W !!,"<<No Data to Print>>"
 F  S BARB=$O(^TMP($J,"BAR-ADR",BARB)) Q:BARB=""  D
 .W !,$G(^TMP($J,"BAR-ADR",BARB))
 .I '((IO["\")!(IO["/")) W "~"
 W !!,"End of Report"
 Q
TRANS ;
 ;loop thru trans looking for 120-day gap after approval, checking only pymts/adjs
 K BARTLST
 S BART=0,BARC=0
 F  S BART=$O(^BARTR(DUZ(2),"AC",BAR,BART)) Q:'BART  D
 .S BARTTYP=$P($G(^BARTR(DUZ(2),BART,1)),U)
 .I "^40^43^49^"'[("^"_BARTTYP_"^") Q  ;pymts/adjs/bill new only
 .S BARTLST($P($G(^BARTR(DUZ(2),BART,0)),U,12),BART)=BARTTYP
 .S BARC=BARC+1
 I +$P($G(^BARBL(DUZ(2),BAR,0)),U,15)'=0 S BARTLST(DT,DT)=""  ;add a final date of today if there's still a balance; this is for old bills that still have an open balance
 ;
 S BARD1=""
 F  S BARD1=$O(BARTLST(BARD1)) Q:'BARD1  D
 .S BART=0
 .F  S BART=$O(BARTLST(BARD1,BART)) Q:'BART  D
 ..S BARD2=$O(BARTLST(BARD1))
 ..I ((BARD2="")&(BARC'=1)) Q
 ..S X2=BARD1
 ..S X1=BARD2
 ..I ((BARC=1)&(X1="")) S X1=DT  ;default to today for 120 check if only one transaction on bill
 ..D ^%DTC
 ..I X>119 S BARTFLG=1
 Q
