 .;W ?5,$E($P($G(^BARCOL(DUZ(2),BARCBIEN,1,BARITEM,0)),U,11),1,20)  ;item check#
 .W ?5,$$PRTFMT^BARCHKU1($$VAL^XBDIQ1(90051.1101,"BARCBIEN,BARITEM",11),20)  ;ITEM CHECK # BAR*1.8*30 CR10550
