 I '$G(BARESIG) D SIG^XUSESIG Q:X1=""  ;elec signature test
 S ^BARTMP("UFMSPST",DUZ(2),DUZ,UFMSESID)=+$G(^BARTMP("UFMSPST",DUZ(2),DUZ,UFMSESID))+1 ;bar*1.8*37 IHS/SD/SDR ADO60825
