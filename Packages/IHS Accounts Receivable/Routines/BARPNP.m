 ;;1.8;IHS ACCOUNTS RECEIVABLE;**21,31,37**;OCT 26, 2005;Build 210
 ;IHS/SD/FCJ 1.8*31 10.29.2020 CR#6156 MODIFIED TO SORT/DISPLAY BY CLINIC OR VISIT
