 D RAYGO^BARPST
 ;IHS/SD/TPF BAR*1.8*21 8/3/2011 HEAT20490
 I $$NOTOPEN^BARUFUT(.DUZ,$G(UFMSESID)) Q  ;IS SESSION STILL OPEN
 W !!
 S BARSRT1=1    ;BAR*1.8*31 10.29.2020 IHS.OIT.FCJ CR#6156
 F  D  Q:BARPASS=0
 .S BARPASS=$$EN^BARPST1()
 .Q:BARPASS=0!($G(UFMSESID)="")
 .Q:$D(DUOUT)    ;BAR*1.8*31 10.29.2020 IHS.OIT.FCJ CR#6156
 .S BARCNT=$$EN^BARPNP2(BARPASS)
 .I +BARCNT=0 W *7,!!,"No bills in this date range!" W !! Q
 .D EN^BARPNP3
 .K BARSRT,BARSRTA D KILL^AUPNPAT   ;BAR*1.8*31 10.29.2020 IHS.OIT.FCJ CR#6156
 S ^BARTMP("UFMSPST",DUZ(2),DUZ,UFMSESID)=+$G(^BARTMP("UFMSPST",DUZ(2),DUZ,UFMSESID))-1 ;bar*1.8*37 IHS/SD/SDR ADO60825
 ;
EXIT ;
