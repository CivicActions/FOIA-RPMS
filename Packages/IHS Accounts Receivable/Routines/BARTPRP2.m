 ;;1.8;IHS ACCOUNTS RECEIVABLE;**39**;OCT 26, 2005;Build 231
 ;IHS/SD/SDR 1.8*39 ADO160244 INC0393324 New Group Payment Report, originally for Oklahoma Area
 ;
PRINT ;
 I BARY("RTYP")=2 D PRINT2 Q  ;delimited output
 S BAR("PG")=1
 D HDR
 S BAR("TXT")="",BAR("L")="",BAR("I")="",BAR("T")="",BAR("V")="",BAR("G")=""
 S BARBILLS=0,BARPAIDS=0
 S BARBILLT=0,BARPAIDT=0
 S BARBILLV=0,BARPAIDV=0
 F  S BAR("TXT")=$O(^TMP("BAR-TP",$J,BAR("TXT"))) Q:$G(BAR("TXT"))=""  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 .I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  D VIS2 W " (cont)"
 .I BAR("L")=""!(BAR("L")'=$P(BAR("TXT"),U)) D LOC S (BAR("SORT"),BAR("S"))=""
 .I BAR("SORT")=""!(BAR("SORT")'=$P(BAR("TXT"),U,2)) D SORT S (BAR("SORT"),BAR("S"))=""
 .I BAR("S")'="",BAR("S")'=$P(BAR("TXT"),U,3) S (BAR("I"))="" D VSUB,VIS S BAR("I")=""
 .I BAR("S")="" D VIS
 .S BAR("L")=$P(BAR("TXT"),U)
 .S BAR("SORT")=$P(BAR("TXT"),U,2)  ;Ins/Itype/Grp/Tribe/Employer
 .S BAR("S")=$P(BAR("TXT"),U,3)
 .W !,$E($$GET1^DIQ(2,$P(BAR("TXT"),U,4),".01","E"),1,26)
 .W ?28,$P(BAR("TXT"),U,6)
 .W ?37,$$SDT^BARDUTL($P(BAR("TXT"),U,5))
 .W ?48,$J($FN($P(BAR("TXT"),U,7),",",2),12)
 .W ?62,$J($FN(+$P(BAR("TXT"),U,8),",",2),12)
 .S BARBILLS=+$G(BARBILLS)+$P(BAR("TXT"),U,7)
 .S BARBILLV=+$G(BARBILLV)+$P(BAR("TXT"),U,7)
 .S BARBILLT=+$G(BARBILLT)+$P(BAR("TXT"),U,7)
 .S BARPAIDS=+$G(BARPAIDS)+$P(BAR("TXT"),U,8)
 .S BARPAIDV=+$G(BARPAIDV)+$P(BAR("TXT"),U,8)
 .S BARPAIDT=+$G(BARPAIDT)+$P(BAR("TXT"),U,8)
 D VSUB
 W !!?48,"============",?62,"============"
 W !?30,"Report Totals",?48,$J($FN(BARBILLT,",",2),12),?62,$J($FN(BARPAIDT,",",2),12)
 K ^TMP("BAR-TP",$J)
 Q
PRINT2 ;delimited printing
 D HDR
 S BAR("TXT")=""
 F  S BAR("TXT")=$O(^TMP("BAR-TP",$J,BAR("TXT"))) Q:$G(BAR("TXT"))=""  D
 .S BARTXT=BAR("TXT")
 .S $P(BARTXT,U,3)=$P($G(^DPT($P(BARTXT,U,3),0)),U)
 .S $P(BARTXT,U,6)="("_$P(BARTXT,U,6)_") "_$P($G(^AUTNINS($P(BARTXT,U,6),0)),U)
 .S $P(BARTXT,U,12)=$$SDT^BARDUTL($P(BARTXT,U,12))
 .S $P(BARTXT,U,13)=$J($FN($P(BARTXT,U,13),",",2),12)
 .W !,BARTXT
 Q
 ;
HD ;
 D PAZ^BARRUTL Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 S BAR("PG")=+$G(BAR("PG"))+1
HDR ;
 D EN^BARVDF("IOF")
 W !
 S BAR("HD",0)="GROUP PAYMENT REPORT"
 I $D(BARY("GRP")) S BAR("HD",0)="Group Name/Number Payment Report"
 D NOW^%DTC
 W BAR("HD",0),?$S($D(BAR(132)):103,1:48) S Y=% X ^DD("DD") W Y_$S($G(BARY("RTYP"))=1:"   Page "_BAR("PG"),1:"")
 W !,"for "_$S(BARY("DT")="V":"Visit",BARY("DT")="A":"Approval",1:"Transaction")_" Dates from ",$$SDT^BARDUTL(BARY("DT",1))," to ",$$SDT^BARDUTL(BARY("DT",2))
 W !,"Billing Location: ",$P($G(^AUTTLOC(DUZ(2),0)),U,2)
 I BARY("RTYP")=1 D
 .W !
 .F BAR=1:1:80 W "="
 .W !,"PATIENT",?28,"BILL#",?37,"DOS",?48,"AMOUNT BILLED",?62,"AMOUNT PAID",!
 .F BAR=1:1:80 W "="
 .W !
 I BARY("RTYP")=2 D
 .W !,"VISIT LOCATION^BILL NUMBER^PATIENT^VISIT TYPE^CLINIC^INSURER^INSURER TYPE^TRIBE^GROUP NAME^GROUP NUMBER^"
 .W "EMPLOYER^SERVICE DATE^AMOUNT BILLED^BILL STATUS^TRANSACTION DATE^TRANSACTION TYPE^TRANSACTION AMOUNT^BATCH NAME^"
 .W "ITEM NUMBER^TDN^ADJUSTMENT CATEGORY^ADJUSTMENT TYPE"
 Q
SORT ;
 I $D(BARY("INS"))>0 D INS
 I $D(BARY("ITYP"))>0 D INS
 I $D(BARY("GRP"))>0 D GRP
 I $D(BARY("TRIBE"))>0 D TRIBE
 I $D(BARY("EMP"))>0 D EMP
 Q
LOC ;
 W !,"Location: ",$P(BAR("TXT"),U)
 Q
GRP ;
 W !?2,"Group Name/Number: ",$P(BAR("TXT"),U,2)
 Q
VIS ;
 W !
 I BARY("SORT")="C" W ?4,"Clinic: ",$P(^DIC(40.7,$P(BAR("TXT"),U,3),0),U)
 I BARY("SORT")="V" W ?4,"Visit Type: ",$P(^ABMDVTYP($P(BAR("TXT"),U,3),0),U)
 Q
VIS2 ;
 I BARY("SORT")="C" W ?2,"Clinic: ",$P(^DIC(40.7,$P(BAR("TXT"),U,3),0),U)
 I BARY("SORT")="V" W ?2,"Visit Type: ",$P(^ABMDVTYP($P(BAR("TXT"),U,3),0),U)
 Q
VSUB ;
 W !?48,"============",?62,"============"
 W !?30
 I BARY("SORT")="C" W ?5,"Clinic"
 I BARY("SORT")="V" W ?5,"Visit Type"
 W " Totals",?48,$J($FN(BARBILLV,",",2),12),?62,$J($FN(BARPAIDV,",",2),12),!
 S BARBILLV=0,BARPAIDV=0
 Q
INS ;
 I $D(BARY("INS")) W !?2,"Insurer: "
 I $D(BARY("ITYP")) W !?2,"Insurer Type: "
 W $P(BAR("TXT"),U,2)
 Q
TRIBE ;
 W !?2,"Tribe: ",$P(BAR("TXT"),U,2)
 Q
EMP ;
 W !?2,"Employer: ",$P(BAR("TXT"),U,2)
 Q
BTRNSDTL ;
 S BARBNUM=$P($G(^ABMDBILL(DUZ(2),BAR,0)),U)
 S BARPHRN=$P($G(^AUPNPAT(BAR("P"),41,BAR("L"),0)),U,2)
 S BARH=DUZ(2)
 S DUZ(2)=BARPAR
 S BARBSUF=$P($G(^ABMDPARM(BAR("L"),1,2)),U,4)
 I (($P($G(^ABMDPARM(BAR("L"),1,2)),U,4)'="")&($G(BARBSUF)'="")) S BARBNUM=$G(BARBNUM)_"-"_BARBSUF
 I (($P($G(^ABMDPARM(BAR("L"),1,3)),U,3)=1)&($G(BARPHRN)'="")) S BARBNUM=$G(BARBNUM)_"-"_BARPHRN
 S BARAIEN=$O(^BARBL(DUZ(2),"B",BARBNUM,0))
 I +BARAIEN=0  S BARP("HIT")=0 S DUZ(2)=BARH Q  ;no A/R bill
 S BARTRIEN=0,BARTCNT=1
 K BARTREC,BARCPYMT
 F  S BARTRIEN=$O(^BARTR(DUZ(2),"AC",BARAIEN,BARTRIEN)) Q:BARTRIEN=""  D
 .S BARARREC=$G(^BARTR(DUZ(2),BARTRIEN,0))
 .I $G(BAROPDT)="" S BAROPDT=$P($P(BARARREC,U),".")
 .Q:(+$P(BARARREC,U,2)=0&(+$P(BARARREC,U,3)=0))
 .S BARTTYP=$P($G(^BARTR(DUZ(2),BARTRIEN,1)),U,1)
 .S BARADJC=$P($G(^BARTR(DUZ(2),BARTRIEN,1)),U,2)
 .S BARCAT=""
 .Q:BARTTYP=49  ;skip bill news
 .I BARTTYP=40 S BARCAT="PAYMENT",BARCPYMT=1
 .;I ($D(BARY("POST"))&((BARADJC'=20)&(BARTTYP'=40))) S BARP("HIT")=0,DUZ(2)=BARH Q  ;take out payment credits
 .I ($D(BARY("POST"))&(BARTTYP'=40)) S BARP("HIT")=0,DUZ(2)=BARH Q  ;only payments
 .S (BARAC,BARTTYP,BARBTCH,BARITEM,BARTDN)=""
 .I "^3^4^13^14^15^16^19^20^"[("^"_BARADJC_"^") D
 ..S BARCAT="ADJUSTMENT"
 ..S BARAC="("_BARADJC_") "_$$GET1^DIQ(90050.03,BARTRIEN,102,"E")
 ..S BARTTYP="("_$P($G(^BARTR(DUZ(2),BARTRIEN,1)),U,3)_") "_$$GET1^DIQ(90050.03,BARTRIEN,103,"E")
 .S BARTDT=$$CDT^BARDUTL($P($G(^BARTR(DUZ(2),BARTRIEN,0)),U,18))
 .S BARTAMT=$$GET1^DIQ(90050.03,BARTRIEN,3.5,"E")
 .S BARBTCH=$$GET1^DIQ(90050.03,BARTRIEN,14,"E")
 .S BARITEM=$$GET1^DIQ(90050.03,BARTRIEN,15,"E")
 .I +$P($G(^BARTR(DUZ(2),BARTRIEN,0)),U,14)'=0 S BARTDN=$P($G(^BARCOL(DUZ(2),$P($G(^BARTR(DUZ(2),BARTRIEN,0)),U,14),0)),U,28)
 .S BARTREC(BARTCNT)=BARTDT_U_BARCAT_U_BARTAMT_U_BARBTCH_U_BARITEM_U_BARTDN_U_BARAC_U_BARTTYP
 .S BARTCNT=BARTCNT+1
 I (('$D(BARTREC))&($D(BARY("POST")))) S BARP("HIT")=0  ;no data and it is payments only
 I $D(BARTREC) S BARP("HIT")=1
 ;I ((+$G(BARCPYMT)=0)&($P($G(^ABMDBILL(DUZ(2),BAR,0)),U,4)="X")) S BARP("HIT")=0  ;if there's no payment and the bill is cancelled skip it
 I BARP("HIT")=0 K BARTREC  ;don't write if there's no payment or payment credit
 S DUZ(2)=BARH
 Q
BTRNSONE(BARDUZ2) ;
 S BARH3=DUZ(2)
 S DUZ(2)=BARDUZ2
 S BARARREC=$G(^BARTR(DUZ(2),BARTRIEN,0))
 I $G(BAROPDT)="" S BAROPDT=$P($P(BARARREC,U),".")
 Q:(+$P(BARARREC,U,2)=0&(+$P(BARARREC,U,3)=0))
 S BARTTYP=$P($G(^BARTR(DUZ(2),BARTRIEN,1)),U,1)
 S BARADJC=$P($G(^BARTR(DUZ(2),BARTRIEN,1)),U,2)
 S BARCAT=""
 I BARTTYP=49 S DUZ(2)=BARH3 Q  ;skip bill news
 I BARTTYP=40 S BARCAT="PAYMENT"
 S BARHIT=0
 ;I BARADJC=20 S BARHIT=1  ;skip payment credits
 I BARTTYP=40 S BARHIT=1
 I ($D(BARY("POST"))&(BARHIT'=1)) S BARP("HIT")=0,DUZ(2)=BARH3 Q  ;if posting only, we want pymts and pymt credits only
 S (BARAC,BARTTYP,BARBTCH,BARITEM,BARTDN)=""
 I "^3^4^13^14^15^16^19^20^"[("^"_BARADJC_"^") D
 .S BARCAT="ADJUSTMENT"
 .S BARAC="("_BARADJC_") "_$$GET1^DIQ(90050.03,BARTRIEN,102,"E")
 .S BARTTYP="("_$P($G(^BARTR(DUZ(2),BARTRIEN,1)),U,3)_") "_$$GET1^DIQ(90050.03,BARTRIEN,103,"E")
 S BARTDT=$$CDT^BARDUTL($P($G(^BARTR(DUZ(2),BARTRIEN,0)),U,18))
 S BARTAMT=$$GET1^DIQ(90050.03,BARTRIEN,3.5,"E")
 S BARBTCH=$$GET1^DIQ(90050.03,BARTRIEN,14,"E")
 S BARITEM=$$GET1^DIQ(90050.03,BARTRIEN,15,"E")
 I +$P($G(^BARTR(DUZ(2),BARTRIEN,0)),U,14)'=0 S BARTDN=$P($G(^BARCOL(DUZ(2),$P($G(^BARTR(DUZ(2),BARTRIEN,0)),U,14),0)),U,28)
 S BARTREC(1)=BARTDT_U_BARCAT_U_BARTAMT_U_BARBTCH_U_BARITEM_U_BARTDN_U_BARAC_U_BARTTYP
 S DUZ(2)=BARH3
 Q
GRPCHK ;
 S BARITMP=0
 F  S BARITMP=$O(^ABMDBILL(DUZ(2),BAR,13,"B",BAR("I"),BARITMP)) Q:'BARITMP  D
 .;private
 .I $P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U,8)'="" D  ;get policy holder group
 ..I $$GET1^DIQ(9999999.181,$$GET1^DIQ(9999999.18,$P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U),".211","I"),1,"I")="W" D WRKC Q
 ..S BARGF=0
 ..I ($P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U,8)=$P($G(^ABMDBILL(DUZ(2),BAR,0)),U,26)) S BARGF=1  ;not right PI entry
 ..I ($P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U)=BAR("I")) S BARGF=1
 ..Q:'BARGF
 ..S BARPH=$P($G(^AUPNPRVT(BAR("P"),11,$P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U,8),0)),U,8)
 ..Q:'BARPH
 ..S BARG=$P($G(^AUPN3PPH(BARPH,0)),U,6)
 ..I (+$G(BARG)'=0) I $D(BARY("GRP",BARG)) S BARNG=1
 .;medicaid
 .I $P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U,6)'="" D
 ..S BARMCDI=$P($G(^ABMDBILL(DUZ(2),BAR,13,BARITMP,0)),U,6)
 ..S BARG=$P($G(^AUPNMCD(BARMCDI,0)),U,17)
 ..I (+$G(BARG)'=0) I $D(BARY("GRP",BARG)) S BARNG=1
 Q
WRKC ;
 I $G(^AUPNWC(BAR("P"),0))'="" D  ;entry in 9000042-Workman's Comp
 .S BARWCIEN=0
 .F  S BARWCIEN=$O(^AUPNWC(BAR("P"),11,BARWCIEN)) Q:+BARWCIEN=0  D  Q:$D(BARLW)
 ..S BARWEFDT=$P($G(^AUPNWC(BAR("P"),11,BARWCIEN,0)),U,12)
 ..S BARWEXDT=$P($G(^AUPNWC(BAR("P"),11,BARWCIEN,0)),U,13)
 ..I BARWEFDT>($P(BAR("D"),".",1)) Q
 ..I BARWEXDT'="",BARWEXDT<$P(BAR("D"),".",1) Q
 ..S BARG=$P($G(^AUPNWC(BAR("P"),11,BARWCIEN,0)),U,11)
 ..I (+$G(BARG)'=0) I $D(BARY("GRP",BARG)) S BARNG=1
 Q
