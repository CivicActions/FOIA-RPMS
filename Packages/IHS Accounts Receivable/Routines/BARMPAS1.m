BARMPAS1 ; IHS/SD/LSL - Patient Account Statement ;
 ;;1.8;IHS ACCOUNTS RECEIVABLE;**2,4,5,19,20,23,24,35,36**;OCT 26, 2005;Build 199
 ;
 ;IHS/SD/SDR 1.8*36 See routine for comments up to and thru bar*1.8*36
 ;*********
 Q
ACCOUNT ;
 ;Find Bills where A/R Account is marked for Patient Account Statement
 ;D INIT^BARUTL  ;this keeps it from printing to a slave printer
 S (BARBL,BARTOT,BARVISL,BARDOSV)=0
 D BARPSAT^BARUTL0  ;IHS/SD/POT BAR*1.8*23 HEAT63286 added call to populate array BARPSAT
 S BARPATNM=$$PATNAME^BARMPAS(BARACDA)  ;IHS/SD/POT BAR*1.8*23
 I BARSRTBY=0 S BARPATNM="X" ;IHS/SD/POT BAR*1.8*23 NOT SORTED BY NAMES - CREATE DUMMY NAME
 ;loop thru CURRENT BILL AMOUNT xref 
 F  S BARBL=$O(^BARBL(DUZ(2),"ABAL",BARACDA,BARBL)) Q:'+BARBL  D
 .S BARBAL=$$GET1^DIQ(90050.01,BARBL,15)  ;CURRENT BILL AMOUNT
 .I $$GET1^DIQ(90050.01,BARBL,17.2)="CANCELLED" Q  ;bar*1.8*36 IHS/SD/SDR ADO74825
 .;Q:BARBAL'>0  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I +$G(BARPRO)=0 Q:BARBAL'>0  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I ((+$G(BARPRO)=0)&($P($G(^BARBL(DUZ(2),BARBL,0)),U,18)>BARDTE)) Q  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I +$G(BARPRO)=1 D DTCK Q:BARF=1  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .S BARVISL=$$GET1^DIQ(90050.01,BARBL,108)  ;VisitLoc
 .I BARVISL="" S BARVISL=BARPSAT(DUZ(2),.01)  ;if not listed, default to DUZ(2)
 .S BARDOSV=$P(^BARBL(DUZ(2),BARBL,1),U,2)  ;DOS - VA format
 .I BARVISL="" S BARVISL="??"
 .I BARDOSV="" S BARDOSV="??"
 .S BARTOT=BARTOT+BARBAL
 .S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),1,BARPATNM,BARACDA,"OB",BARBL)=""  ;NODE 1 FOR 'OB'
 .S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),1,BARPATNM,BARACDA,BARVISL,BARDOSV,"OB",BARBL)=""  ;NODE 1 FOR 'OB'
 .S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),0,BARPATNM,BARACDA,BARVISL,BARDOSV,BARBL,"A")=""
 ;
 S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),1,BARPATNM,BARACDA,"OB")=BARTOT
 ;It currently kills all previous entries if the Balance = 0
 I BARTOT=0 K ^XTMP("BARPAS"_BARRUNDT,DUZ(2),0,BARPATNM,BARACDA)
 D GETBILLS^BARMPAS1  ;split routine bar*1.8*36 IHS/SD/SDR ADO74824
 Q
GETBILLS ;
 ;Find bills by patient balance
 S BARPAT=$$GET1^DIQ(90050.02,BARACDA,1.001)
 S BARBL=0 F  S BARBL=$O(^BARBL(DUZ(2),"APBAL",BARPAT,BARBL)) Q:'+BARBL  D
 .I $$GET1^DIQ(90050.01,BARBL,17.2)="CANCELLED" Q  ;bar*1.8*36 IHS/SD/SDR ADO74825
 .I ((+$G(BARPRO)=0)&($P($G(^BARBL(DUZ(2),BARBL,0)),U,18)>BARDTE)) Q  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I +$G(BARPRO)=1 D DTCK Q:BARF=1  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .;Add sorts: VisitLocation and DOS
 .S BARVISL=$$GET1^DIQ(90050.01,BARBL,108)  ;VisitLoc
 .I BARVISL="" S BARVISL=BARPSAT(DUZ(2),.01)  ;if not listed, default to DUZ(2)
 .S BARDOSV=$P(^BARBL(DUZ(2),BARBL,1),U,2)  ;DOS
 .S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),0,BARPATNM,BARACDA,BARVISL,BARDOSV,BARBL,"P")=""
 .Q
 ;--------------------------
 ;Find bills by patient
 S BARBL=0 F  S BARBL=$O(^BARBL(DUZ(2),"C",BARPAT,BARBL)) Q:'+BARBL  D
 .S BARBAL=$$GET1^DIQ(90050.01,BARBL,15)
 .I $$GET1^DIQ(90050.01,BARBL,17.2)="CANCELLED" Q  ;bar*1.8*36 IHS/SD/SDR ADO74825
 .;Q:BARBAL'>0  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I +$G(BARPRO)=0 Q:BARBAL'>0  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I ((+$G(BARPRO)=0)&($P($G(^BARBL(DUZ(2),BARBL,0)),U,18)>BARDTE)) Q  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I +$G(BARPRO)=1 D DTCK Q:BARF=1  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .;IHS/SD/PKD 1.8*19 9/10/10
 .;Add sorts:  VisitLocation and DOS
 .S BARVISL=$$GET1^DIQ(90050.01,BARBL,108)  ;VisitLoc
 .I BARVISL="" S BARVISL=BARPSAT(DUZ(2),.01)  ;if not listed, default to DUZ(2)
 .S BARDOSV=$P(^BARBL(DUZ(2),BARBL,1),U,2)  ;DOS
 .;STANDARD SORTING (NO PATNAME)
 .I BARVISL="" S BARVISL="??"
 .I BARDOSV="" S BARDOSV="??"
 .S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),0,BARPATNM,BARACDA,BARVISL,BARDOSV,BARBL,"P")=""
 .D ABAL(BARBL)
 ;----------------------------
 ;Find bills by transaction
 ;start old bar*1.8*36 IHS/SD/SDR ADO74824
 ;S BARTR=BARDTB
 ;F  S BARTR=$O(^BARTR(DUZ(2),"AE",BARACDA,BARTR)) Q:BARTR\1>BARDTE  Q:'+BARTR  D
 ;.S BARBL=$$GET1^DIQ(90050.03,BARTR,4,"I")
 ;.Q:'+BARBL  ;No bill on transaction
 ;.I $$GET1^DIQ(90050.01,BARBL,17.2)="CANCELLED" Q
 ;.I ((+$G(BARPRO)=0)&($P($G(^BARTR(DUZ(2),BARTR,0)),U,18)>BARDTE)) Q
 ;.I +$G(BARPRO)=1 D DTCK2 Q:BARF=1
 ;.;IHS/SD/PKD 1.8*19 9/10/10
 ;.;Add sorts:  VisitLocation and DOS
 ;.S BARVISL=$$GET1^DIQ(90050.01,BARBL,108)  ;VisitLoc
 ;.I BARVISL="" S BARVISL=BARPSAT(DUZ(2),.01)  ;if not listed, default to DUZ(2)
 ;.S BARDOSV=$P(^BARBL(DUZ(2),BARBL,1),U,2)  ;DOS
 ;.STANDARD SORTING (NO PATNAME)
 ;.I BARVISL="" S BARVISL="??"
 ;;.I BARDOSV="" S BARDOSV="??"
 ;.S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),0,BARPATNM,BARACDA,BARVISL,BARDOSV,BARBL,"T")=""
 ;.D ABAL(BARBL)
 ;end old start new bar*1.8*36 IHS/SD/SDR ADO74824
TRNS ;
 S BARD=BARDTB
 F  S BARD=$O(^BARTR(DUZ(2),"AE",BARACDA,BARD)) Q:BARD\1>BARDTE  Q:'+BARD  D
 .S BARTR=0
 .F  S BARTR=$O(^BARTR(DUZ(2),"AE",BARACDA,BARD,BARTR)) Q:'BARTR  D
 ..S BARBL=$$GET1^DIQ(90050.03,BARTR,4,"I")
 ..Q:'+BARBL  ;No bill on transaction
 ..I $$GET1^DIQ(90050.01,BARBL,17.2)="CANCELLED" Q
 ..I ((+$G(BARPRO)=0)&($P($G(^BARTR(DUZ(2),BARTR,0)),U,18)>BARDTE)) Q
 ..I +$G(BARPRO)=1 D DTCK2 Q:BARF=1
 ..;Add sorts:  VisitLocation and DOS
 ..S BARVISL=$$GET1^DIQ(90050.01,BARBL,108)  ;VisitLoc
 ..I BARVISL="" S BARVISL=BARPSAT(DUZ(2),.01)  ;if not listed, default to DUZ(2)
 ..S BARDOSV=$P(^BARBL(DUZ(2),BARBL,1),U,2)  ;DOS
 ..;STANDARD SORTING (NO PATNAME)
 ..I BARVISL="" S BARVISL="??"
 ..I BARDOSV="" S BARDOSV="??"
 ..S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),0,BARPATNM,BARACDA,BARVISL,BARDOSV,BARBL,"T")=""
 ..D ABAL(BARBL)
 ;end new bar*1.8*36 IHS/SD/SDR ADO74824
 Q
 ;start new bar*1.8*36 IHS/SD/SDR ADO74824
DTCK ;if PRO option make sure date is within range
 S BARF=0
 I $P($G(^BARBL(DUZ(2),BARBL,0)),U,18)<BARDTB S BARF=1
 I $P($G(^BARBL(DUZ(2),BARBL,0)),U,18)>BARDTE S BARF=1
 Q
DTCK2 ;
 S BARF=0
 I $P($G(^BARTR(DUZ(2),BARTR,0)),U,18)<BARDTB S BARF=1
 I $P($G(^BARTR(DUZ(2),BARTR,0)),U,18)>BARDTE S BARF=1
 Q
 ;
ABAL(BARBL) ;P.OTT COLLECT BILLS WITH NONZERO BALANCE
 N BARBAL
 S BARBAL=$$GET1^DIQ(90050.01,BARBL,15)  ;CURRENT BILL AMOUNT
 ;Q:BARBAL'>0  ;bar*1.8*36 IHS/SD/SDR ADO74824
 ;start new bar*1.8*36 IHS/SD/SDR ADO74824
 S BARBFLG=1
 I +$G(BARTR)'=0 D
 .;I (BARTR<BARDTB)!(BARTR>BARDTE) S BARBFLG=0 Q  ;outside range  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I ($P($G(^BARTR(DUZ(2),BARTR,0)),U,18)<BARDTB)!($P($G(^BARTR(DUZ(2),BARTR,0)),U,18)>BARDTE) S BARBFLG=0 Q  ;outside range  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .I (BARBAL+($$GET1^DIQ(90050.03,"3.5","I"))>0) S BARBFLG=1 Q
 I +$G(BARTR)=0 I BARBAL'>0 S BARBFLG=0
 I BARBFLG=0 Q
 ;end new bar*1.8*36 IHS/SD/SRD ADO74824
 I '$D(^BARBL(DUZ(2),"ABAL",BARACDA,BARBL)) Q  ;IHS/SD/POT bar*1.8*24 HEAT#100207
 S ^XTMP("BARPAS"_BARRUNDT,DUZ(2),1,BARACDA,"OB",BARBL)=""
 Q
PURGE ;
 NEW BARTMP,BARTMP0,BARARR
 ;start old bar*1.8*36 IHS/SD/SDR ADO74824
 ;S BARCNT=0,(BARTMP,BARTMP0)="BARPAS" F  S BARTMP=$O(^XTMP(BARTMP)) Q:BARTMP=""  Q:BARTMP'[BARTMP0  S BARCNT=BARCNT+1,BARARR(BARCNT)=BARTMP
 ;I 'BARCNT W !!,"NO ENTRIES TO PURGE",!! QUIT
 ;end old start new bar*1.8*36 IHS/SD/SDR ADO74824
 S BARCNT=0,(BARTMP,BARTMP0)="BARPAS"
 F  S BARTMP=$O(^XTMP(BARTMP)) Q:BARTMP=""  Q:BARTMP'[BARTMP0  D
 .I '$D(^XTMP(BARTMP,DUZ(2))) Q
 .S BARCNT=BARCNT+1,BARARR(BARCNT)=BARTMP
 I 'BARCNT W !!,"NO ENTRIES TO PURGE ",$$FACNAME(DUZ(2)),!! QUIT
 ;end new bar*1.8*36 IHS/SD/SDR ADO74824
 D LISTRUNS^BARMPAS
 I BARCNT=1 W !!,"CANNOT PURGE THE ONLY PAS RUN ON FILE.",!! QUIT
 I BARCNT>2 W !,"Entries 1-",BARCNT-1," can be purged."
 I BARCNT=2 W !,"Entry 1 can be purged."
 S DIR("A")="OK to purge?"
 S DIR("B")="NO"
 S DIR(0)="Y"
 D ^DIR
 K DIR
 I Y'=1 Q
 F I=1:1:BARCNT-1 D
 .W !,"PURGING ",BARARR(I)
 .K ^XTMP(BARARR(I))
 .Q
 W !,"LAST ENTRY ",BARARR(BARCNT)," NOT PURGED."
 D PAZ^BARRUTL  ;Press return to continue
 Q
FACNAME(X) ;
 Q $P($G(^DIC(4,X,0)),"^",0)
STMTP ;
 F  S BARBNUM=$O(BARBILLS(VISLOC,BARVDT,BARBNUM)) Q:BARBNUM'>0  D  Q:$G(BARF1)
 .N BARBILLD,BARITOT,BARPTOT,BARATOT,BARPRSP,BARPTAC,BARPRV,BARNON,BARCXL
 .S (BARBILLD,BARITOT,BARPTOT,BARATOT,BARPRSP,BARPTAC,BARINSOW,BARCXL)=0
 .D BLDA^BARMPAS2
 .S BARPBNUM=BARBNUM_" "
 .S BARPBNUM=$O(^BARBL(DUZ(2),"B",BARPBNUM))
 .Q:$D(BARBILL("X",BARPBNUM))  ;Trx Error code is now BARBIL("XTR",... pkd
 .;If bill cancelled (3PB) and no payments or ADJ have been made, quit
 .I BARITOT=0&(BARPTOT=0)&(BARATOT=0)&(BARCXL) Q
 .I BARITOT=0&(BARPTOT=0)&(BARATOT=0)&(BARBILLD=0) Q  ;No amts period/shouldn't get this far
 .I BARBILLD=0  D  ;If all bills for bill CXL'd, get Amt from "A" bill
 ..N BLIEN,BLA
 ..S BLA=$O(^BARBL(DUZ(2),"B",+BAR(.01)_"A"))
 ..S BLIEN=$O(^BARBL(DUZ(2),"B",BLA,""))
 ..S BARBILLD=$P(^BARBL(DUZ(2),BLIEN,0),U,13)
 .D PG^BARMPAS3(10)  ;IHS/SD/PKD 1.8*21 3/24/11 Stmt Page Length
 .Q:$G(BARF1)
 .I $G(BARPRV)="" S BARPRV="***** "
 .W !!,"SERVICE DT: ",$$SHDT^BARDUTL(BAR(102,"I"))
 .;W ?30,"BILL #: ",BARBNUM  ;bar*1.8*36 ADO74824
 .W ?23,"BILL#: ",BARBNUM  ;bar*1.8*36 ADO74824
 .;W ?50,"PROVIDER: ",$E(BARPRV,1,20)  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .W ?42,"PRV: ",$E(BARPRV,1,20)  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .W ?68,"CLN: ",$E($$GET1^DIQ(90050.01,BARBL,112,"E"),1,7)  ;clinic  ;bar*1.8*36 IHS/SD/SDR ADO74824
 .W !?6,$J($FN(BARBILLD,"p",2),9)
 .W ?18,$J($FN(BARITOT,"p",2),9),?30,$J($FN(BARPTOT,"p",2),9),?41,$J($FN(BARATOT,"p",2),9)
 .W ?56,$J($FN(BARINSOW,"p",2),9)
 .I (BARPTAC=1!(BARNON=1)!('BARCXL))&(BARPRSP) D
 ..W ?69,$J($FN(BARPRSP,"p",2),9) S BARTOTL("PR")=BARTOTL("PR")+BARPRSP
 .E  W ?74,"**"
 .;May want to exclude some of these amts from totals if bill was cancelled
 .S BARTOTL("B")=BARTOTL("B")+BARBILLD,BARTOTL("I")=BARTOTL("I")+BARITOT
 .S BARTOTL("P")=BARTOTL("P")+BARPTOT,BARTOTL("A")=BARTOTL("A")+BARATOT
 .S BARTOTL("IO")=BARTOTL("IO")+BARINSOW
 Q
