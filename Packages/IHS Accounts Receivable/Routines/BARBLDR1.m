BARBLDR1 ; IHS/SD/SDR - LOCKED BATCHES WITH BALANCE REPORT - DETAIL
 ;;1.8;IHS ACCOUNTS RECEIVABLE;**35**;OCT 26, 2005;Build 187
 ;IHS/SD/SDR 1.8*35 ADO77760 New routine
 ;
PRINT ;
 I BARY("RTYP")=3 D PRINT2 Q  ;detail delimited
 S BARGITOT=0
 S BARGBALT=0
 S BAR("PG")=0
 D HDB
 S BARCLPT=0
 F  S BARCLPT=$O(^XTMP("BARBLDR",$J,"D",BARCLPT)) Q:'BARCLPT  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 .S BARCBCNT=0,BARIBCHT=0,BARIBALT=0
 .I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  W !," (cont)"
 .W !!?9,"COLLECTION ID: "_$P($G(^BAR(90051.02,DUZ(2),BARCLPT,0)),U)
 .F  S BARCBDFN=$O(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN)) Q:'BARCBDFN  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 ..I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  W !?9,"COLLECTION ID: "_$P($G(^BAR(90051.02,DUZ(2),BARCLPT,0)),U)_" (cont)"
 ..S BARCBCNT=BARCBCNT+1
 ..S BARREC=$G(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN))
 ..S BARITMT=$P(BARREC,U,2)  ;item total
 ..S BARITMB=$P(BARREC,U,3)  ;item balance
 ..S BARIBCHT=BARIBCHT+BARITMT
 ..S BARIBALT=BARIBALT+BARITMB
 ..S BARCOL=$P($G(^BARCOL(DUZ(2),BARCBDFN,0)),U)
 ..S BARCOL=$P(BARCOL,"-",($L(BARCOL,"-")-1),($L(BARCOL,"-")))  ;use the last two pieces of batch name delimited with '-'
 ..W !,BARCOL   ;batch name
 ..W ?20,$P($G(^BARCOL(DUZ(2),BARCBDFN,0)),U,28)  ;tdn/ipac(20)
 ..;W ?50,$J($P(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN),U),4)      ;item
 ..W ?43,$$GET1^DIQ(90051.01,BARCBDFN,31,"I")  ;batch lockdown date
 ..W ?56,$J($FN($P(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN),U,2),",",2),10)  ;batch total item total
 ..W ?69,$J($FN($P(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN),U,3),",",2),10)  ;batch total item balance
 ..S BARITM=0
 ..F  S BARITM=$O(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM)) Q:'BARITM  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 ...I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  W !?9,"COLLECTION ID: "_$P($G(^BAR(90051.02,DUZ(2),BARCLPT,0)),U)_" (cont)"
 ...W !?3,"ITEM "_BARITM_$S($P(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM),U,3)="*":"*",1:"")  ;item number
 ...S IENS=BARITM_","_BARCBDFN_","
 ...W ?12,$$GET1^DIQ(90051.1101,IENS,7,"E")  ;Item A/R Account
 ...W ?56,$J($FN($P(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM),U),",",2),10)  ;item total
 ...W ?69,$J($FN($P(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM),U,2),",",2),10)  ;item balance
 .Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 .D SUBTOT
 Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 D GRANDTOT
 W !!,"An '*' after the item number means at least part of it is in Unallocated Cash"
 W !!,"<END OF REPORT>"
 D PAZ^BARRUTL
 K ^XTMP("BARBLDR",$J)
 Q
HD D PAZ^BARRUTL Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
HDB S BAR("PG")=BAR("PG")+1 D WHD^BARBLDR2
HDR ;
 I BARY("RTYP")=3 W !,"COLLECTION ID^LOCATION^ALLOWANCE CATEGORY^COLLECTION BATCH NAME^TDN/IPAC^LOCK DATE^ITEM COUNT^TOTAL BATCHED^BALANCE^ITEM NUMBER^PAYER^CHECK NUMBER^ITEM AMOUNT^ITEM BALANCE" Q
 W !,"COLLECTION BATCH NAME",?25,"TDN/IPAC",?46,"LOCK",?60,"TOTAL"
 W !,?4,"ITEM",?12,"A/R ACCOUNT",?46,"DATE",?59,"BATCHED",?71,"BALANCE"
 W !
 F BARI=1:1:80 W "="
 Q
SUBTOT ;
 W !?56,"----------",?69,"----------"
 W !?15,BARCBCNT_" BATCHES",?56,$J($FN(BARIBCHT,",",2),10),?69,$J($FN(BARIBALT,",",2),10)
 S BARGITOT=BARGITOT+BARIBCHT
 S BARGBALT=BARGBALT+BARIBALT
 Q
GRANDTOT ;
 W !?56,"==========",?69,"=========="
 W !?15,"TOTAL",?56,$J($FN(BARGITOT,",",2),10),?69,$J($FN(BARGBALT,",",2),10)
 Q
PRINT2 ;
 S BAR("PG")=0
 D HDB
 S BARCLPT=0
 F  S BARCLPT=$O(^XTMP("BARBLDR",$J,"D",BARCLPT)) Q:'BARCLPT  D
 .S BARCBCNT=0,BARIBCHT=0,BARIBALT=0
 .F  S BARCBDFN=$O(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN)) Q:'BARCBDFN  D
 ..S BARCBCNT=BARCBCNT+1
 ..S BARREC=$G(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN))
 ..S BARITMT=$P(BARREC,U,2)  ;item total
 ..S BARITMB=$P(BARREC,U,3)  ;item balance
 ..S BARIBCHT=BARIBCHT+BARITMT
 ..S BARIBALT=BARIBALT+BARITMB
 ..S BARBREC=$P($G(^BAR(90051.02,DUZ(2),BARCLPT,0)),U)  ;collection point
 ..S BARBREC=BARBREC_U_$P($G(^DIC(4,$P($G(^BARCOL(DUZ(2),BARCBDFN,0)),U,8),0)),U)  ;site location name
 ..S BARBREC=BARBREC_U_$$GET1^DIQ(90051.02,BARCLPT,7,"E")  ;allowance category
 ..S BARBREC=BARBREC_U_$P($G(^BARCOL(DUZ(2),BARCBDFN,0)),U)       ;batch name
 ..S BARBREC=BARBREC_U_$P($G(^BARCOL(DUZ(2),BARCBDFN,0)),U,28)  ;tdn/ipac
 ..S BARBREC=BARBREC_U_$$GET1^DIQ(90051.01,BARCBDFN,31,"I")  ;batch lockdown date
 ..S BARBREC=BARBREC_U_$P(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN),U)  ;item count
 ..S BARBREC=BARBREC_U_$P(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN),U,2)  ;batch total item total
 ..S BARBREC=BARBREC_U_$P(^XTMP("BARBLDR",$J,"S",BARCLPT,BARCBDFN),U,3)  ;batch total item balance
 ..S BARITM=0
 ..S BARITMC=0
 ..F  S BARITM=$O(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM)) Q:'BARITM  D
 ...S BARITMC=BARITMC+1
 ...I ((BARDET=0)&(BARITMC'=1)) S $P(BARBREC,U,7)="",$P(BARBREC,U,8)="",$P(BARBREC,U,9)=""  ;this is from the prompt of if they want the batch balance once or on every item
 ...W !,BARBREC_U_BARITM_$S($P(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM),U,3)="*":"*",1:"")  ;item number
 ...W U_$$GET1^DIQ(90050.02,$P($G(^BARCOL(DUZ(2),BARCBDFN,1,BARITM,0)),U,7),.01,"E")  ;payer
 ...W U_$P($G(^BARCOL(DUZ(2),BARCBDFN,1,BARITM,8)),U)  ;check number
 ...W U_$P(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM),U)  ;item total
 ...W U_$P(^XTMP("BARBLDR",$J,"D",BARCLPT,BARCBDFN,BARITM),U,2)  ;item balance
 W !!,"An '*' after the item number means at least part of it is in Unallocated Cash"
 W !!,"<END OF REPORT>"
 K ^XTMP("BARBLDR",$J)
 Q
