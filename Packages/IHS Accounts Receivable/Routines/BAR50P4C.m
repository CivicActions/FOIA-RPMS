 S (REASDA,BARMMTST)=0
 F  S REASDA=$O(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,4,REASDA)) Q:'REASDA  D
 .I '$$MMNUOR^BAR50P4C(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,4,REASDA,0)) S BARMMTST=1
 Q BARMMTST
