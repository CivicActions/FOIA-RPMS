CHKBACD(IMPDA,CLMDA,CHKMM) ;PRIVATE Returns TRUE if there are reasons not to post other than MM
 ;PASS CHKMM BY REFERENCE
 ;IHS/DIT/CPC BAR*1.8*29 CR 6982 & 10777 Skip MANUAL MATCH reason
 N REASDA,REAIEN,REASCODE,REASON
 S REASDA=0,CHKMM=$G(CHKMM)
 F  S REASDA=$O(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,4,REASDA)) Q:'REASDA  D
 .S REAIEN=^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,4,REASDA,0)
 .S REASON=$$GET1^DIQ(90056.21,REAIEN,.01,"E")
 .I (REASON="BA")!(REASON="CD") S CHKMM(REASON)=""
 Q
 ;
LOADERR(IMPDA,CLMDA,ERRORS)  ;Private
 N REASDA,REAIEN,REASCODE,REASON
 S REASDA=0
 F  S REASDA=$O(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,4,REASDA)) Q:'REASDA  D
 .S REAIEN=^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,4,REASDA,0)
 .S REASON=$$GET1^DIQ(90056.21,REAIEN,.01,"E")
 .S ERRORS(REASON)=""
 Q
