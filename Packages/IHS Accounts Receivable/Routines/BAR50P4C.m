CLM2()  ;PRIVATE 
 S NEWSTAT=$G(NEWSTAT) ;init value 12/12/13
 ;I BARCNT>1,($$GET1^DIQ(90056.0205,BAREIENS,1.01)=""),('$D(BARRVW)) D   ;IHS/DIT/CPC - BAR*1.8*28
 I BARFND=30,($$GET1^DIQ(90056.0205,BAREIENS,1.01)="") D
 .S QCHSN=0
 .W !!,"The following ERA item has multiple items that it may match to.",!,"Please match as appropriate.",!
 .F  D  Q:($G(BARSEL)'="B"&($G(BARSEL)'="H"))!$G(QFLG)
 ..D HDR^BAR50P04
 ..D RABILL^BAR50P4B
 ..D ARBILL^BAR50P4B
 ..D CHOOSE^BAR50P4B
 ..I ($G(BARSEL)="Q") S (QFLG,QCHSN)=1 Q  ;
 ..I (+$G(BARANS)'=0)&(($G(BARSEL)'="B")&($G(BARSEL)'="H")) D
 ...K DIR
 ...S DIR(0)="Y"
 ...S DIR("A")="Are you sure?"
 ...S DIR("B")="N"
 ...D ^DIR
 ...I +Y<1 S BARANS=0 S BARSEL="B"
 .I $G(QFLG) D  Q
 ..S BARSEL="N"
 ..S NEWSTAT="C",QCHSN=1
 ..S BARFND=0,BARMMTCH=0,BARCNTR=-1,BARTST=0
 ..K BARTEMP
 .I ($G(BARSEL)="")!($G(BARSEL)="N")!('+BARANS) D  Q
 ..W !!,"BILL WILL NOT BE MATCHED AND WILL BE SET TO 'NOT MATCHED'."
 ..W:'$G(QFLG) "  CONTINUING.."
 ..S BARSEL="N"
 ..S BARMSG=$C(9)_"Selected Not to Match..." D INS^BAR50DET(BARMSG,0)
 ..S NEWSTAT="C",ERRORS("UOR")="",CLMTYP="UOR",QCHSN=1
 ..S BARFND=0,BARMMTCH=0,BARCNTR=-1,BARTST=0
 ..K BARTEMP
 .I BARSEL="M" D
 ..D DELREAS^BAR50P4B(IMPDA,CLMDA,1)
 ..K BARTEMP,DIE,DA,DR
 ..S NEWSTAT="M",QCHSN=0,BARFND=20
 ..S BARTEMP(BARBILL,BARANS1)="",BARMMTCH=0,ERRORS("MM")="",BARFND30="MM"   ;CHECK BARMMTCH
 ..S (BARTMP2(BARANS1),BARBIEN,BIENTEST,BARBLDA)=BARANS1
 ..K BARANS1,BARANS
 ..S DIE=$$DIC^XBDIQ1(90056.0205)
 ..S DA=CLMDA
 ..S DA(1)=IMPDA
 ..S DR="1.01////^S X=BARBIEN"
 ..S MATCH="M"
 ..S DR=DR_";.02////^S X=MATCH"
 ..D ^DIE
 ..K DIE,DA,DR
 ..S BARMSG=$C(9)_"Matched to A/R Bill: "_$P($G(^BARBL(DUZ(2),BARBIEN,0)),U) D INS^BAR50DET(BARMSG,0)
 ;Match amounts  ;IHS/SD/CPC - CR6982 BAR*1.8*29
 ;I BARFND>0,BARBIEN']"" S BARBIEN=$O(BARTEMP(BARBILL,BARBIEN))
 Q:$G(QFLG) 0
 I ($D(ERRORS("UOR")))!($D(ERRORS("CT"))) D ADDREAS^BAR50P4B(IMPDA,CLMDA,.ERRORS) Q 0
 S ERABILL=$$GET1^DIQ(90056.0205,BAREIENS,.05)  ;BILLED AMOUNT FROM ERA
 S BARBILD=$$GET1^DIQ(90050.01,BARBIEN,13,"I")  ;BILLED AMOUNT FROM A/R BILL
 I +ERABILL<0 S ERABILL=-ERABILL  ;Negative ERA billed amounts are considered equivalent to positive Bar Bill amounts.
 I +ERABILL'=+BARBILD S CLMTYP="BA",ERRORS("BA")=""  ;IHS/DIT/CPC - BAR*1.8*29
 ;END CR6982
 ;IHS/SD/CPC - BAR*1.8*29 CR 10407
 ;Match DOS
 ;S (BARRADT,X)=$$GET1^DIQ(90056.0205,BAREIENS,.08) ;BAR 1.8*23
 ;I X]"" D ^%DT I Y'=-1 S BARRADT=Y  ;DOS begin
 S BARRADT=$$GET1^DIQ(90056.0205,BAREIENS,.08,"I")  ;EDI E-DOS BEGIN
 S BARRADTE=$$GET1^DIQ(90056.0205,BAREIENS,.09,"I") ;EDI E-DOS END
 S BARBBDT=$$GET1^DIQ(90050.01,BARBIEN,102,"I")   ;A/R BILL DOS BEGIN
 S BARBDTE=$$GET1^DIQ(90050.01,BARBIEN,103,"I")  ;A/R BILL DOS END
 S BARBDTB=$$GET1^DIQ(90050.01,BARBIEN,7,"I")    ;A/R BILLED DATE
 S BARBBDT=$$Y2KD2^BARDUTL(BARBBDT)  ;CONVERT TO YYYYMMDD
 S BARBDTE=$$Y2KD2^BARDUTL(BARBDTE)
 S BARBDTB=$$Y2KD2^BARDUTL(BARBDTB)
 ;I BARRADT'=BARBDT S CLMTYP="CD",ERRORS("CD")=""  ;DOS DOESN'T MATCH RPMS ; BAR*1.8*5 SRS-80
 ;I (BARRADT=BARBBDT)!(BARBDTE=BARRADTE)!(BARBDTB=BARRADT) K ERRORS("CD")
 I (BARRADT=BARBBDT)!(BARBDTE=BARRADTE) K ERRORS("CD")  ;DON'T MATCH BILLED DATE
 E  S CLMTYP="CD",ERRORS("CD")=""
 ;END CR 10407
 ;See if 3P cancelled
 ;REMOVE IF PARAMETER SET.
 S BAR3PIEN=$$FIND3PB^BARUTL(DUZ(2),BARBIEN)
 I BAR3PIEN]"" D   ;
 .I $$IHSCANCB^BARUFUT(DUZ(2)) Q  ;allow cancelled bills 2/4/2014 BAR 1.8*24
 .S BARBSTAT=$P($G(^ABMDBILL($P(BAR3PIEN,","),$P(BAR3PIEN,",",2),0)),U,4)
 .S:BARBSTAT="X" CLMTYP="CC",ERRORS("CC")=""  ;bar*1.8*29 IHS/SD/CPC CR10777
 ;I $$GET1^DIQ(90056.0205,BAREIENS,.02,"I")="M" Q 1  ;IHS/DIT/CPC - BAR*1.8*28    ****DEBUG
 ;Q:$D(ERRORS("CT"))!($D(ERRORS("CF")))!($D(ERRORS("UOR"))) 0   ;IHS/DIT/CPC - BAR*1.8*28
 ;D TRANSCK^BAREDP4A  ;MOVE TO BELOW ;IHS/DIT/CPC - BAR*1.8*29 INTERNAL TESTING
 ;I $G(NEWSTAT)="M"!($$GET1^DIQ(90056.0205,BAREIENS,.02,"I")="M") Q 1
 ;Q:$D(ERRORS) 0
 Q:$G(QFLG) 0
 Q:$D(ERRORS("BA"))&(+$G(BARMMTCH)'=1) 0
 Q:$D(ERRORS("CD"))&(+$G(BARMMTCH)'=1) 0
 ;Bill matches RPMS-log AR Bill IEN in Image
 S DIE=$$DIC^XBDIQ1(90056.0205)
 S DA=CLMDA
 S DA(1)=IMPDA
 S DR="1.01////^S X=BARBIEN"
 S MATCH="M"
 S DR=DR_";.02////^S X=MATCH"
 D ^DIE
 D TRANSCK^BAREDP4A
 I (BILMATCH=1)!(BARMMTCH=1) D
 .S NEWSTAT="M",BARCNT=1
 .S BARMSG=$J("",5)_"ERA BILL "_$$GET1^DIQ(90056.0205,BAREIENS,.01)
 .;I '$D(BARREPRC)!($G(BARREPRC)=CLMDA) W !,BARMSG          ;BAR*1.8*31
 .I '$D(BARREPRC)!($G(BARREPRC)=CLMDA),$G(BARDSP) W !,BARMSG  ;BAR*1.8*31;OIT.IHS.FCJ CR#6984 REQ 2
 .I $G(BARDBG) D INS^BAR50DET(BARMSG,0)
 .S BARMSG=" MATCHED TO A/R BILL "_$P($G(^BARBL(DUZ(2),BARBIEN,0)),U)
 .;I '$D(BARREPRC)!($G(BARREPRC)=CLMDA) W BARMSG   ;BAR*1.8*31;
 .I '$D(BARREPRC)!($G(BARREPRC)=CLMDA),$G(BARDSP)=1 W BARMSG  ;BAR*1.8*31;OIT.IHS.FCJ CR#6984 REQ 2
 .I $G(BARDBG) D INS^BAR50DET(BARMSG,1)
 Q 1
 ;
CLM(CLMDA) ;EP ;MATCH/SET/FLAG E-CLM TO A/R BILL
 S X=$$VAL^XBDIQ1(90056.0205,"IMPDA,CLMDA",.01)
 K DIC,DA,DR
 S DIC=90050.01
 S DIC(0)="M"
 D ^DIC
 I Y'>0 Q 0
 S BARBLDA=+Y
 S DIE=$$DIC^XBDIQ1(90056.0205)
 S DA=CLMDA
 S DA(1)=IMPDA
 S DR="1.01////^S X=BARBLDA"
 D ^DIE
 Q 1
 ;
REA(CLMDA) ;EP ;LOOP MATCH/SET/FLAG RSN CODES OF E-CLM
 K ADJ
 S REAFLG=1
 S ADJDA=0
 F  S ADJDA=$O(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,30,ADJDA)) Q:ADJDA'>0  D
 .S ACAT=$$VALI^XBDIQ1(90056.0208,"IMPDA,CLMDA,ADJDA",.04)
 .S AREA=$$VALI^XBDIQ1(90056.0208,"IMPDA,CLMDA,ADJDA",.05)
 .S REA=$$VAL^XBDIQ1(90056.0208,"IMPDA,CLMDA,ADJDA",.03)
 .I '$L(REA) S REAFLG=0 Q
 .;lookup rsn in rsn table
 .K DIC,DA,DR
 .S DIC=$$DIC^XBDIQ1(90056.0107)
 .S DA(1)=TRDA
 .S X=$P(REA," ")
 .S DIC(0)="X"
 .D ^DIC
 .I Y'>0 D  Q
 ..;S BARMSG=" NO REASON "_X W !,BARMSG  ;BAR*1.8*31
 ..S BARMSG=" NO REASON "_X W:$G(BARDSP) !,BARMSG   ;BAR*1.8*31;OIT.IHS.FCJ CR#6984 REQ 2
 ..I $G(BARDBG) D INS^BAR50DET(BARMSG,0)
 ..S REAFLG=0
 .S READA=+Y
 .;line below to ignore inpt w/non-cov'd days
 .I +CLMFLG>0,$P($G(^BARBL(DUZ(2),BARBLDA,1)),U,14)=111,$P(Y,U,2)=96 Q
 .Q:$P(Y,U,2)=93  ;Q if rsn is 93 w/o attempting to match
 .;pull A/R cat & rsn
 .S ACAT=$$VALI^XBDIQ1(90056.0107,"TRDA,READA",2.01)
 .S AREA=$$VALI^XBDIQ1(90056.0107,"TRDA,READA",2.02)
 .I ACAT,AREA D SETREA^BAR50P4B I 1
 .E  S REAFLG=0
 Q REAFLG
 ;
CLMNOUT(BARFROM,BARBIEN,IMPDA,CLMDA,BARMMTYP) ;Checks for matching Date, Bill Amount DOES NOT OUTPUT TO XTMP
 NEW ERABILL,ERADOS,ERADOSE,BARDOS,BARDOSE,BARDOSBD,BARAMT,ERATYPE
 S ERABILL=$P($G(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,0)),U,5)  ;E-BILLED
 S ERADOS=$P($G(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,0)),U,8)  ;E-DOS BEGIN
 S ERADOSE=$P($G(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,0)),U,9)  ;E-DOS END ;IHS/SD/CPC 11/11/19 CR 10407
 S BARDOS=$$GET1^DIQ(90050.01,BARBIEN,102,"I")  ;DOS BEGIN
 S BARDOSE=$$GET1^DIQ(90050.01,BARBIEN,103,"I")  ;DOS END ;IHS/SD/CPC 11/11/19 CR 10407
 ;S BARDOSBD=$$GET1^DIQ(90050.01,BARBIEN,7,"I")   ;BILL DATE 
 S ERATYPE=$P($G(^BAREDI("I",DUZ(2),IMPDA,30,CLMDA,0)),U,11)  ;E-CLM STATUS CD
 S BARDOS=$$Y2KD2^BARDUTL(BARDOS)  ;
 S BARDOSE=$$Y2KD2^BARDUTL(BARDOSE) ;IHS/SD/CPC 11/11/19 CR 10407
 S BARAMT=$$GET1^DIQ(90050.01,BARBIEN,13,"I")  ;AMT BILLED
 S BARMMTYP=0 ;MATCH TYPE
 I ERABILL<0 S ERABILL=-ERABILL  ;bar*1.8*26 IHS/SD/SDR HEAT166023
 ;DATE & BILL MATCH
 I (+ERABILL=+BARAMT),(ERADOS=BARDOS) Q 1 ;P.OTT 12/2/2013
 ;I ERADOSE]"",(BARDOSE]""),(+ERABILL=+BARAMT),(ERADOSE=BARDOSE) Q 1 ;IHS/SD/CPC 11/11/19 CR 10407
 ;I (BARDOSBD]""),(+ERABILL=+BARAMT),(ERADOS=BARDOSBD) Q 1 ;IHS/SD/CPC 11/11/19 CR 10407
 ;END DATE & BILL MATCH
 I BARFND=10 Q 1 ;BAR*1.8*29  ADD MANUAL MATCH TEST
 ; ---- NOT MATCHED - OTHER REASON FALL THRU
 I BARFROM=1 QUIT 0
 Q 0
 ;
SVMATCH(BARSTAT,CLMDA,IMPDA)  ;UPDATES ^BAREDI WITH MATCHING STATUS
 N DA,DR,DIE,DIC,DIR
 S DIE=$$DIC^XBDIQ1(90056.0205)
 S DR=""  ;REQ4
 S IENS=CLMDA_","_IMPDA_","  ;REQ4
 I $$GET1^DIQ(90056.0205,IENS,71)="" S DR=".02///^S X=BARSTAT"  ;'STATUS' FLD CLM UNMATCHED ;REQ4
 S DA(1)=IMPDA
 S DA=CLMDA
 D ^DIE
 K DA,DR,DIE,DIC,DIR
 Q
