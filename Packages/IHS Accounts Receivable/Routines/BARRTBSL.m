ASKDEV ;EP - ASK DEVICE
 ;start new bar*1.8*39 IHS/SD/SDR ADO106475
 ;Select Report Type Inclusion Parameter
 I BARSORT=2 D  G:$D(DUOUT)!$D(DTOUT)!$D(DIRUT)!$D(DIROUT) ASKTYPE  ;only do delimited if they selected sort option 2, by TDN/IPAC
 .K DIR,BARY("RTYP")
