 W !
 D ^%ZIS
 G:POP ASKTYPE
 I $D(IO("Q")) D QUE G ASKTYPE
 U IO
 D:(DATETYPE=1) SORT
 D:(DATETYPE=2) TRANSORT
 I '$D(^XTMP("BARRTBSL",$J)) D  G ASKTYPE
 .;start new bar*1.8*39 IHS/SD/SDR ADO106475
 .S PAGE=0
 .D NOW^%DTC S Y=% X ^DD("DD") S NOW=Y
 .S Y=BARFROM X ^DD("DD") S EXFROM=Y
 .S Y=BARTO X ^DD("DD") S EXTO=Y
