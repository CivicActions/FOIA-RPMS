 .;end new bar*1.8*39 IHS/SD/SDR ADO106475
 .W !!,"THERE IS NO DATA TO PRINT"
 .D ^%ZISC  ;bar*1.8*39 IHS/SD/SDR ADO106475
 .U 0  ;bar*1.8*39 IHS/SD/SDR ADO106475
 .K DIR
 .S DIR(0)="E"
 .D ^DIR
 ;
 D:(BARSORT=1)!(BARSORT=2) PRINT
 D:(BARSORT>2) PRINTNI^BARRTBS1
 D ^%ZISC
 D HOME^%ZIS
 G ASKTYPE
 Q
