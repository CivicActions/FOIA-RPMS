 ..S DIR("A")="Enter Path"
 ..S DIR("B")=$P($G(^BAR(90052.06,DUZ(2),DUZ(2),0)),"^",17)
 ..D ^DIR K DIR
 ..Q:$D(DIRUT)!$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 ..S BARPATH=Y
