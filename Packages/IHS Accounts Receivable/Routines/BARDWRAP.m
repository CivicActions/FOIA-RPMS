BARDWRAP ; IHS/SD/SDR - UTILITY FOR WRAP-AROUND PRINTING ;
 ;;1.8;IHS ACCOUNTS RECEIVABLE;**35**;OCT 26, 2005;Build 187
 ;IHS/SD/SDR 1.8*35 ADO77760 New routine
 ;
PRTTXT ; EP - UTIL FOR WRAP-AROUND
 S BARU("TAB")=$S($D(BARU("TAB")):BARU("TAB"),1:0),BARU("LNG")=BARU("RM")-BARU("LM")
 I $D(BARU("2TXT")) S BARU("2TAB")=$S($D(BARU("2TAB")):BARU("2TAB"),1:0),BARU("2LNG")=BARU("2RM")-BARU("2LM")
 F BARU("Q")=1:1 Q:(BARU("TXT")=""!("    "[BARU("TXT")))&'$D(BARU("2TXT"))  D PRTTXT2
 ;
XIT K BARU
 Q
 ;
PRTTXT2 K BARU("FLG") I $L(BARU("TXT"))<BARU("LNG") S BARU("F")=BARU("TXT"),BARU("TXT")="" G PRTTXT3
 S BARU("FLG")="" F BARU("C")=BARU("LNG"):-1:1 S BARU("L")=$E(BARU("TXT"),BARU("C")) Q:BARU("L")=" "!(BARU("L")="-")!(BARU("L")="\")!(BARU("L")=",")!(BARU("L")="/")
 S BARU("F")=$E(BARU("TXT"),1,BARU("C")-1),BARU("TXT")=$E(BARU("TXT"),BARU("C")+1,255)
 K:"    "[BARU("TXT") BARU("FLG")
 ;
PRTTXT3 I $D(BARU("2TXT")) D 2
 W ?BARU("LM"),BARU("F") I $D(BARU("2TXT")) W ?BARU("2LM"),BARU("2F")
 I BARU("Q")=1 F BARU("I")=1:1 Q:'$D(BARU(BARU("I")))  W @$P(BARU(BARU("I")),U),$P(BARU(BARU("I")),U,2)
 W:$D(BARU("FLG")) ! S BARU("LM")=BARU("LM")-BARU("TAB"),BARU("LNG")=BARU("LNG")+BARU("TAB"),BARU("TAB")=0
 I $D(BARU("2TXT")) S BARU("2LM")=BARU("2LM")-BARU("2TAB"),BARU("2LNG")=BARU("2LNG")+BARU("2TAB"),BARU("2TAB")=0
 Q
 ;
2 I BARU("2TXT")=""!("    "[BARU("2TXT")) K BARU("2TXT") Q
 I $L(BARU("2TXT"))<BARU("2LNG") S BARU("2F")=BARU("2TXT"),BARU("2TXT")="" Q
 S BARU("FLG")="" F BARU("2C")=BARU("2LNG"):-1:1 S BARU("2L")=$E(BARU("2TXT"),BARU("2C")) Q:BARU("2L")=" "!(BARU("2L")="-")
 S BARU("2F")=$E(BARU("2TXT"),1,BARU("2C")-1),BARU("2TXT")=$E(BARU("2TXT"),BARU("2C")+1,255)
 Q
LNG ;EP FOR WRAP-AROUND FOR FIXED LENGTH
 S BARU("TAB")=$S($D(BARU("TAB")):BARU("TAB"),1:0)
 F BARU("Q")=1:1:(BARU+1) Q:(BARU("TXT")=""!("    "[BARU("TXT")))  D LNG2
 Q
 ;
LNG2 K BARU("FLG") I $L(BARU("TXT"))<BARU("LNG")!(BARU("Q")=BARU) S BARU("F")=BARU("TXT"),BARU("TXT")="" G LNG3
 S BARU("FLG")="" F BARU("C")=BARU("LNG")+1:-1:1 S BARU("L")=$E(BARU("TXT"),BARU("C")) Q:BARU("L")=" "!(BARU("L")="-")!(BARU("L")="\")!(BARU("L")=",")!(BARU("L")="/")
 S BARU("F")=$E(BARU("TXT"),1,BARU("C")-1)
 I BARU("F")]"" S BARU("TXT")=$E(BARU("TXT"),BARU("C")+1,255)
 E  S BARU("F")=$E(BARU("TXT"),1,BARU("LNG")),BARU("TXT")=$S($P(BARU("TXT")," ",2,99)]"":$P(BARU("TXT")," ",2,99),1:$E(BARU("TXT"),BARU("LNG")+1,256))
 K:"    "[BARU("TXT") BARU("FLG")
 ;
LNG3 S BARU(BARU("Q"))=BARU("F")
 S BARU("LNG")=BARU("LNG")+BARU("TAB"),BARU("TAB")=0
 Q
