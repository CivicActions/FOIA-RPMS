SETCOLL  ;EP
 ;Create COLLECTION STATUS multiple for A/R Bill
 N DR,DA,DIC,J,I
 S DA(1)=BARBLDA
 S DIC="^BARBL(DUZ(2),"_DA(1)_",9,"
 S DIC(0)="LX"
 S DIC("P")=$P(^DD(90050.01,901,0),U,2)
