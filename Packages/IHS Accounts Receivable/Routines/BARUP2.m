 S BARSAT=$G(@BAR3PUP@("VSLC"))
 S BARSCODE=$G(@BAR3PUP@(74,BARMIEN,"STAT"))
 S BARUSER=$G(@BAR3PUP@(74,BARMIEN,"USR"))
 S BARDATE=$G(@BAR3PUP@(74,BARMIEN,"DT"))
