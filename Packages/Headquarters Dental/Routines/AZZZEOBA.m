 S ACHSDOC=^ACHSF(DUZ(2),"D",ACHSDIEN,0),ACHSTRAN=^ACHSF(DUZ(2),"D",ACHSDIEN,"T",ACHSTIEN,0),ACHSLOC=$P(^AUTTLOC($P(^ACHSF(DUZ(2),0),U),0),U,10)
 KILL ^TMP("ACHSEOB",$J)
A ;
 F %=1:1:15 S ACHSEOBR("A",%)=""
 S ACHSEOBR("A",1)=$E(ACHSLOC,1,2),ACHSEOBR("A",2)=$E(ACHSLOC,3,4),ACHSEOBR("A",3)=$E(ACHSLOC,5,6),ACHSEOBR("A",4)="0"_$P(ACHSDOC,U,14),ACHSEOBR("A",5)=$E($P(ACHSTRAN,U,17),8,17),ACHSEOBR("A",6)="A",ACHSEOBR("A",8)=$P(ACHSTRAN,U,14)
 S ACHSEOBR("A",9)=$P(ACHSTRAN,U,18),ACHSEOBR("A",10)=$P(ACHSTRAN,U,19),ACHSEOBR("A",11)=$P(ACHSTRAN,U,13),ACHSEOBR("A",11)=$E(ACHSEOBR("A",11),2,7)
 S ACHSEOBR("A",12)=$P(ACHSDOC,U,14)_"-"_ACHSFC_"-"_$P(ACHSDOC,U),ACHSEOBR("A",13)=$E($P(ACHSTRAN,U,17),1,7),ACHSEOBR("A",14)=ACHSEOBR("A",1)_ACHSEOBR("A",2)_ACHSEOBR("A",3),ACHSEOBR("A",15)=$P(ACHSDOC,U,4)
 S ACHSEOBR("A",15)=$S(ACHSEOBR("A",15)=1:43,ACHSEOBR("A",15)=2:57,ACHSEOBR("A",15)=3:64)
B ;
 F %=8:1:14 S ACHSEOBR("B",%)=""
 S %=$P(ACHSDOC,U,22)
 I % S ACHSEOBR("B",8)=$P(^DPT($P(ACHSDOC,U,22),0),U)
 E  S ACHSEOBR("B",8)=$S($P(ACHSDOC,U,3)=1:"* BLANKET",$P(ACHSDOC,U,3)=2:"* SPECIAL TRANS",1:"")
 S ACHSEOBR("B",9)=$P(ACHSDOC,U,21),ACHSEOBR("B",10)=17000000+$P(ACHSDOC,U,2)
 S ACHSEOBR("B",11)=$P(ACHSTRAN,U,9)
 S %=$$DOC^ACHS(8,1)
 I % S %="00"_%,ACHSEOBR("B",12)=$E(%,$L(%)-2,$L(%))
 S %=$$DOC^ACHS(8,4)
 I % S ACHSEOBR("B",13)=$P($G(^DIC(42.2,%,9999999)),U)
 S ACHSEOBR("B",14)=$P(^ACHS(3,DUZ(2),1,$P(ACHSDOC,U,7),0),U)
 ;
C ;
 F %=8:1:16 S ACHSEOBR("C",%)=""
 S ACHSEOBR("C",8)=$P(^ACHS(2,$P(ACHSDOC,U,6),0),U)
 S ACHSEOBR("C",9)=$P(^ACHSOCC($P(ACHSDOC,U,10),0),U)
 S ACHSEOBR("C",10)=$P(ACHSTRAN,U,20),ACHSEOBR("C",11)=$P(ACHSDOC,U,3)
 S ACHSEOBR("C",11)=$S(ACHSEOBR("C",11)=1:"Y",ACHSEOBR("C",11)=0:"N",1:"")
 S ACHSEOBR("C",12)="OM"
 I $P(ACHSDOC,U,5) S ACHSEOBR("C",12)=$P(^AUTTVNDR($P(ACHSDOC,U,8),"CN",$P(ACHSDOC,U,5),0),U)
 ;S ACHSEOBR("C",13)=$P(ACHSTRAN,U,15),ACHSEOBR("C",14)=$E($P(^ACHSF(DUZ(2),"D",ACHSDIEN,3),U,1),2,7),ACHSEOBR("C",15)=$E($P(^(3),U,2),2,7) ;ACHS*3*10 'C' RECORD CHANGES
 ;S ACHSEOBR("C",13)=$P(ACHSTRAN,U,15),ACHSEOBR("C",14)=17000000+$P(^ACHSF(DUZ(2),"D",ACHSDIEN),U,1),ACHSEOBR("C",15)=17000000+$P(^(3),U,2) ;ACHS*3*11 PRINTING PROBLEM;ACHS*3*10 'C' RECORD CHANGES
 S ACHSEOBR("C",13)=$P(ACHSTRAN,U,15),ACHSEOBR("C",14)=17000000+$P(^ACHSF(DUZ(2),"D",ACHSDIEN),U,1),ACHSEOBR("C",15)=17000000+$P(^(3),U,2) ;ACHS*3*11 PRINTING PROBLEM;ACHS*3*10 'C' RECORD CHANGES
 I $D(^AUTTVNDR($P(ACHSDOC,U,8),11)) S ACHSEOBR("C",16)=$P(^AUTTVNDR($P(ACHSDOC,U,8),11),U,1) I $P(^AUTTVNDR($P(ACHSDOC,U,8),11),U,2)'="" S ACHSEOBR("C",16)=ACHSEOBR("C",16)_"-"_$P(^AUTTVNDR($P(ACHSDOC,U,8),11),U,2)
 ;
D ;
 F %=8:1:11 S ACHSEOBR("D",%)=""
 I $D(^AUTTVNDR($P(ACHSDOC,U,8))) S ACHSEOBR("D",8)=$P(^AUTTVNDR($P(ACHSDOC,U,8),0),U,1)
 S ACHSEOBR("D",11)=$P(ACHSTRAN,U,8),ACHSEOBR("D",9)=0,ACHSEOBR("D",10)=0
 I ACHSEOBR("D",11)["." S %=ACHSEOBR("D",11),%=$P(%,".")_$E($P(%,".",2)_"00",1,2),ACHSEOBR("D",11)=%
 ;
I ; Interest info.
 S DA(2)=DUZ(2),DA(1)=ACHSDIEN,DA=ACHSTIEN
 F ACHS=22:1:28 S ACHSEOBR("I",ACHS-14)=$$VAL^XBDIQ1(9002080.02,.DA,ACHS)
 S ACHSEOBR("I",9)=$P(ACHSEOBR("I",9),".")_$P(ACHSEOBR("I",9),".",2)
 ;
E ;
 F %=8:1:16 S ACHSEOBR("E",%)=""
 S ACHSEOBR("E",8)=$P(ACHSTRAN,U,4),ACHSEOBR("E",9)=$P(ACHSTRAN,U,21)
 I ACHSEOBR("E",8)["." S %=ACHSEOBR("E",8),%=$P(%,".")_$E($P(%,".",2)_"00",1,2),ACHSEOBR("E",8)=%
 E  S ACHSEOBR("E",8)=ACHSEOBR("E",8)_"00"
 S ACHSICD="",ACHSCNTR=11
E1 ;
 S ACHSICD=$O(^ACHSF(DUZ(2),"D",ACHSDIEN,9,ACHSICD))
 G E1:ACHSICD=0
 G:ACHSICD="" ^AZZZEOBF
 G:ACHSICD'?1N.N F^AZZZEOBF
 I $P(^ACHSF(DUZ(2),"D",ACHSDIEN,9,ACHSICD,0),U,2)=ACHSTIEN,$P(^(0),U) S ACHSCNTR=ACHSCNTR+1,ACHSEOBR("E",ACHSCNTR)=$P(^(0),U),ACHSEOBR("E",ACHSCNTR)=$P(^ICD9(ACHSEOBR("E",ACHSCNTR),0),U)
 G E1
 ;
