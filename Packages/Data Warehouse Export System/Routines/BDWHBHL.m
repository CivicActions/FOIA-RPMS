 ;;1.0;IHS DATA WAREHOUSE;**6,9,10,11**;JAN 24, 2006;Build 14
 ;
MAIN(RXARRY) ;EP - this is the main routine driver
 S INQUE=1
 D LOOP(.RXARRAY)
 Q
 ;
LOOP(ARRY) ;-- look the RXARRY and get the info
 N ADA,AIEN,FLDT,RXCNT
 S RXCNT=0
 S AIEN=0 F  S AIEN=$O(ARRY(AIEN)) Q:'AIEN  D
 . S RXCNT=RXCNT+1
 . S AOEN="" F  S AOEN=$O(ARRY(AIEN,AOEN)) Q:$G(AOEN)=""  D
 .. S FLDT=$G(ARRY(AIEN,AOEN))
 .. S BDWRTSF=$P($G(ARRY(AIEN,AOEN)),U,3)
 .. D RXD(RXCNT,AIEN,FLDT),ZMD(RXCNT,AIEN,AOEN),ZMP(RXCNT,AIEN,AOEN,FLDT)
 .. K BDWRTSF
 Q
 ;
ZMP(CNT,R,RF,FLDT) ;-- create zmp fields
 N P,Z
 I '$G(RF) S P=$P($G(^PSRX(R,0)),U,4)
 I $G(RF) S P=$P($G(^PSRX(R,1,RF,0)),U,17)
 S INDA("ZMP",1)=""
 S INA("BDW1ZMP1",CNT,1)=CNT
 S INA("BDW1ZMP2",CNT,1)=$$NPI^BDWHUTL(P)
 S INA("BDW1ZMP3",CNT,1)=$$DETOXMID^BDWHUTL(P)
 S INA("BDW1ZMP4",CNT,1)=$$DEA^BDWHUTL(P)
 S INA("BDW1ZMP5",CNT,1)=$$VANUM^BDWHUTL(P)
 S INA("BDW1ZMP6",CNT,1)=$$AFFIL^BDWHUTL(P)
 S INA("BDW1ZMP7",CNT,1)=$$PC^BDWHUTL(P)
 S Z=$$PCC^BDWHUTL(P,FLDT)
 S INA("BDW1ZMP8",CNT,1)=$P(Z,U,1)
 S INA("BDW1ZMP9",CNT,1)=$P(Z,U,2)
 S INA("BDW1ZMP10",CNT,1)=$P(Z,U,3)
 S INA("BDW1ZMP11",CNT,1)=$P(Z,U,4)
 S INA("BDW1ZMP12",CNT,1)=$$HLNAME^BDWUTIL2(200,.01,P)
 Q
 ;
ZMD(CNT,R,RF) ;-- create zmd fields
 NEW H,C,%,O,V
 S INDA("ZMD",1)=""
 S INA("BDW1ZMD1",CNT,1)=CNT
 S INA("BDW1ZMD2",CNT,1)=$$GET1^DIQ(50,$P($G(^PSRX(R,0)),U,6),.01)
 S INA("BDW1ZMD5",CNT,1)=$S($G(RF):$P($G(^PSRX(R,1,RF,0)),U,4),1:$P($G(^PSRX(R,0)),U,7))
 S INA("BDW1ZMD3",CNT,1)=$$GET1^DIQ(50,$P($G(^PSRX(R,0)),U,6),31)
 S INA("BDW1ZMD4",CNT,1)=$$GET1^DIQ(50,$P($G(^PSRX(R,0)),U,6),2)
 S INA("BDW1ZMD6",CNT,1)=$S($G(RF):"Y",1:"N")
 S INA("BDW1ZMD7",CNT,1)=$$DAYS^BDWHUTL(R)
 S INA("BDW1ZMD8",CNT,1)=$$DEAVA^BDWHUTL(R)
 S INA("BDW1ZMD9",CNT,1)=$$RXDETOXN^BDWHUTL(R)
 S INA("BDW1ZMD11",CNT,1)=$$RXNORM^BDWHUTL(R)   ;PATCH 9 ADDED
 S INA("BDW1ZMD14",CNT,1)=$$ASFPF^BDWHUTL(R)  ;PATCH 9 ADDED, THIS IS ASUFAC OF THE OUTPATIENT SITE INSTITUTION 
 S INA("BDW1ZMD15",CNT,1)=""    ;OUTSIDE MED
 S INA("BDW1ZMD16",CNT,1)=$$DS^BDWHUTL(R)  ;drug schedule patch 9 added
 S C="",%=$$VAL^XBDIQ1(52,R,9999999.22)
 I %]"" S C=$P($$ICDDX^ICDEX(%),U,20)
 S V=%_"^^"_$S(C=30:"I10",C=1:"I9",1:"")
 S INA("BDW1ZMD17",CNT,1)=V
 S INA("BDW1ZMD18",CNT,1)=$TR($$VAL^XBDIQ1(52,R,9999999.21),"|","")
 S O=$P($G(^PSRX(R,"OR1")),U,2)  ;order number
 S H=""
 I O S H=$$VAL^XBDIQ1(100,O,6),I=+$$VALI^XBDIQ1(100,O,6)
 S INA("BDW1ZMD19",CNT,1)=H    ;HOSPITAL LOCATION FROM THE ORDER FILE
 S %=""
 I O,$$VALI^XBDIQ1(100,O,6)["SC(" S %=$$VALI^XBDIQ1(44,I,8) I % S %=$$VAL^XBDIQ1(40.7,%,1)
 S INA("BDW1ZMD21",CNT,1)=%
 ;maw p10 next 2 lines added
 S INA("BDW1ZMD24",CNT,1)="N"
 I $G(BDWRTSF) S INA("BDW1ZMD24",CNT,1)="Y"
 Q
 ;
RXD(CNT,R,FDT) ;-- create rxd fields
 S INDA("RXD",CNT)=""
 S INA("BDW1RXD1",CNT)=CNT
 S INA("BDW1RXD3",CNT)=$$FMTHL7^XLFDT(FDT)
 S INA("BDW1RXD7",CNT)=$$GET1^DIQ(52,R,.01)
 Q
 ;
HDR ;-- generate the header record
 S INQUE=1
 NEW BDWDEST S BDWDEST=$O(^INRHD("B","HL IHS DW1HOPE IE",0)) K ^INLHDEST(BDWDEST) ;IHS/CMI/LAB - kill at beginning of each batch
 ;cmi/anch/maw 3/7/2007 added the following 3 lines for common header vars
 S INA("BDW1ZHS4")="HL7"
 I $G(INA("FILE")) S INA("BDW1ZHS5")="EXO^"_$$GET1^DIQ(INA("FILE"),INDA,.23)  ;cmi/anch/maw 3/7/2007 for desc of option ran
 S INA("BDW1ZHS6")="2.5.1"  ;change this when version changes
 S INA("BDW1BHS7")=$$DATE^INHUT($$GET1^DIQ(90213.1,INDA,.01,"I"),1)
 S INA("BDW1BHS9")=INDA
 S INA("BDW1BHS11")=$P($G(^AUTTLOC($P(^AUTTSITE(1,0),U),0)),U,10)_INDA
 S INA("BDW1ZHS1")=$S($$GET1^DIQ(90213.1,INDA,1102,"I"):$$DATE^INHUT($$GET1^DIQ(90213.1,INDA,1102,"I")),1:$$DATE^INHUT($$GET1^DIQ(90213.1,INDA,.02,"I")))
 S INA("BDW1ZHS2")=$$DATE^INHUT($$GET1^DIQ(90213.1,INDA,.03,"I"))
 S INA("BDW1ZHS3")=INDA
 Q
 ;
TRL ;-- generate the trailer record
 S INQUE=1
 S INA("BDW1ZTS1")=+$$GET1^DIQ(90213.1,INDA,1104)+$$GET1^DIQ(90213.1,INDA,.08)
 S INA("BDW1ZTS2")=$$GET1^DIQ(90213.1,INDA,.06)
 S INA("BDW1ZTS3")=0
 S INA("BDW1ZTS4")=0
 S INA("BDW1ZTS5")=$$GET1^DIQ(90213.1,INDA,1105)
 S INA("BDW1BTS1")=$G(INA("BDW1ZTS1"))
 S INA("BDW1BTS2")=$P($G(^DIC(4,$P(^AUTTSITE(1,0),U),0)),U)
 S INA("BDW1BTS3")=1
 D HFS  ;task to host file
 ;
QCNT(DEST) ;-- let's loop through and get a quick count of records
 N BDWDA,BDWCNT
 S BDWCNT=0
 S BDWDA=0 F  S BDWDA=$O(^BDWHTMP(DEST,BDWDA)) Q:'BDWDA  D
 . Q:'$O(^INTHU("AT",BDWDA,0))
 . ;cmi/anch/maw 12/7/2007 filter out A40's?
 . S BDWCNT=BDWCNT+1
 Q +$G(BDWCNT)
 ;
SETZTS6(PFL,PIEN,QCNT) ;-- set ZTS-6 with actual counts
 N TRL
 S TRL=$P($G(^BDWHLOG(PIEN,0)),U,12)
 Q:'TRL
 S TRL=$O(^INTHU("AT",TRL,0))
 Q:'$G(TRL)
 N LDA
 S LDA=0 F  S LDA=$O(^INTHU(TRL,3,LDA)) Q:'LDA  D
 . I $E($G(^INTHU(TRL,3,LDA,0)),1,3)="ZTS" D
 .. S $P(^INTHU(TRL,3,LDA,0),"|",7)=QCNT_"|CR"
 Q
 ;
GETDIR ;get export directory
 S BDWHDIR=$S($P($G(^AUTTSITE(1,1)),U,2)]"":$P(^AUTTSITE(1,1),U,2),1:$G(^XTV(8989.3,1,"DEV")))
 I $G(BDWHDIR)="" S BDWHDIR="/usr/spool/uucppublic/"
 Q
HFSA(DEST,BHLHDIR,BHLHFNM) ;EP - export from this destination
 S Y=$$OPEN^%ZISH(BHLHDIR,BHLHFNM,"W")
 Q:Y
 S BHLX=0 F  S BHLX=$O(^BDWHTMP(DEST,BHLX)) Q:'BHLX  D
 . S BHLU=$O(^INTHU("AT",BHLX,0))
 . Q:'BHLU  ;cmi/maw 6/28/2004 added for null node
 . ;D LPINTHU(BHLU)
 . D LPINTHU^BDWBHL(BHLU)  ;20250529 p11 for long PID
 D ^%ZISC
 ;
 I $P($G(^AUTTSITE(1,0)),U,21)=1 S BDWSLASH="/" I 1
 E  S BDWSLASH="\"
 S BDWNOSLA=1 I $E(BDWHDIR,$L(BDWHDIR))="/"!($E(BDWHDIR,$L(BDWHDIR))="\") S BDWNOSLA=0
 S BDWPAFN=BHLHDIR_$S(BDWNOSLA:BDWSLASH,1:"")_BHLHFNM
 ;now loop through and delete them
 S BHLX=0 F  S BHLX=$O(^BDWHTMP(DEST,BHLX)) Q:'BHLX  D
 .S BHLU=$O(^INTHU("AT",BHLX,0))
 .Q:'BHLU  ;cmi/maw 6/28/2004 added for null node
 .S DA=BHLU,DIE="^INTHU(",DR=".03////C" D ^DIE K DIE,DA,DR
 .Q
 K ^BDWHTMP(DEST)
 D UPLOG(BHLHFNM,BDWPIEN)
 D AUTOSEND^BDWHBHL1
FTP ;
 ;PUT FTP TO DW MACHINE HERE
BULL ;now send mailman message to user who queued the job
 D BULL^BDWHBHL1
 Q
 ;
UPLOG(FNM,PIEN) ;-- update the log with the filename
 S DIE="^BDWHLOG(",DA=PIEN,DR="1101///"_FNM
 D ^DIE
 Q
 ;
LPINTHU(BHLUIEN)       ;EP - loop through UIF and set to file
 S BHLUDA=0 F  S BHLUDA=$O(^INTHU(BHLUIEN,3,BHLUDA)) Q:'BHLUDA  D
 . U IO W $P($G(^INTHU(BHLUIEN,3,BHLUDA,0)),"|CR|"),!
 Q
 ;
DELAY(IDA,IA) ;-- determine the delay based on records
 N BDWRCNT
 S BDWRCNT=$$GET1^DIQ(90213.1,IDA,1105)
 I BDWRCNT>10000 Q 30
 I BDWRCNT>40000 Q 60
 I BDWRCNT>60000 Q 90
 Q 15
 ;
HFS ;-- task the file to the host system
 NEW BDWUSER,BDWDESC,F,BDWPFL,BDWPIEN
 S BDWUSER=$$VALI^XBDIQ1(INA("FILE"),INDA,8801)
 S BDWPFL=$G(INA("FILE"))  ;maw added 4/7/2005  for ZTS-6
 S BDWPIEN=$G(INDA)  ;maw added 4/7/2005 for ZTS-6
 S BDWDESC="HOPE Export for: "_$$VAL^XBDIQ1(90213.1,INDA,.01)_" to "_$$VAL^XBDIQ1(90213.1,INDA,.02)
 S ZTRTN="HFS1^BDWHBHL"
 S ZTIO="",ZTDTH=$$FMADD^XLFDT($$NOW^XLFDT,,,$$DELAY(INDA,INA("FILE"))),ZTDESC="DW HOPE HFS CREATION" S ZTSAVE("BDW*")=""
 D ^%ZTLOAD
 Q
HFS1 ;EP - called from taskman
 N BDWDEST,BDWHDIR,BDWHFNM
 S BDWDEST=$O(^INRHD("B","HL IHS DW1HOPE IE",0))
 Q:'BDWDEST
 K ^INLHDEST(BDWDEST) ;kill off inlhdest
 N BDWQCNT
 S BDWQCNT=$$QCNT(BDWDEST)  ;4/7/05 maw quick count of records to put in ZTS-6
 D SETZTS6(BDWPFL,BDWPIEN,BDWQCNT)
 D GETDIR
 Q:BDWHDIR=""
 N BDWASU
 S BDWASU=$P($G(^AUTTLOC($P($G(^AUTTSITE(1,0)),U),0)),U,10)
 D NOW^%DTC
 N BDWDTS
 S BDWDTS=$TR(%,".")
 S BDWHFNM=BDWASU_BDWDTS_".BDWH"
 D HFSA(BDWDEST,BDWHDIR,BDWHFNM)
 S ZTREQ="@"
 Q
 ;
MAINOM(OMARRAY) ;EP - this is the main routine driver
 S INQUE=1
 K INA
 D LOOPOM(.OMARRAY)
 Q
 ;
LOOPOM(ARRY) ;-- look the RXARRY and get the info
 N ADA,PIEN,FLDT,RXCNT,MIEN
 S RXCNT=0
 S PIEN=0 F  S PIEN=$O(ARRY(PIEN)) Q:'PIEN  D
 . S MIEN="" F  S MIEN=$O(ARRY(PIEN,MIEN)) Q:$G(MIEN)=""  D
 .. S RXCNT=RXCNT+1
 .. D OMRXD(RXCNT,PIEN,MIEN),OMZMD(RXCNT,PIEN,MIEN)
 Q
 ;
 ;
OMZMD(CNT,PIEN,MIEN) ;-- create zmd fields
 NEW H,C,%,O,V,ZNODE,D
 S INDA("ZMD",1)=""
 S INA("BDW1ZMD1",CNT,1)=CNT
 S ZNODE=$G(^PS(55,PIEN,"NVA",MIEN,0))
 S D=$P(ZNODE,U,2)
 I D="" S D=$$OMOID^BDWHRDR(PIEN,MIEN)
 S INA("BDW1ZMD2",CNT,1)=$$GET1^DIQ(50,D,.01)
 S INA("BDW1ZMD3",CNT,1)=$$GET1^DIQ(50,D,31)
 S INA("BDW1ZMD4",CNT,1)=$$GET1^DIQ(50,D,2)
 S INA("BDW1ZMD5",CNT,1)=""  ;QUANTITY
 S INA("BDW1ZMD6",CNT,1)=""
 S INA("BDW1ZMD7",CNT,1)=""
 S INA("BDW1ZMD8",CNT,1)=""
 S INA("BDW1ZMD9",CNT,1)=""
 S INA("BDW1ZMD10",CNT,1)=$$FMTHL7^XLFDT($P(ZNODE,U,7))
 S INA("BDW1ZMD11",CNT,1)=$$VAL^XBDIQ1(50,D,9999999.27)
 S INA("BDW1ZMD14",CNT,1)=""
 S INA("BDW1ZMD15",CNT,1)="YES"    ;OUTSIDE MED
 S INA("BDW1ZMD16",CNT,1)=$$VAL^XBDIQ1(50,D,3)  ;drug schedule patch 9 added
 S INA("BDW1ZMD17",CNT,1)=""
 S INA("BDW1ZMD18",CNT,1)=""
 s %=""
 S H=$P(ZNODE,U,12) I H S %=$P(^SC(H,0),U,1)
 S INA("BDW1ZMD19",CNT,1)=%    ;HOSPITAL LOCATION FROM THE ORDER FILE
 S %=""
 I H S %=$$VALI^XBDIQ1(44,H,8) I % S %=$$VAL^XBDIQ1(40.7,%,1)
 S INA("BDW1ZMD21",CNT,1)=%
 S INA("BDW1ZMD22",CNT,1)=$$FMTHL7^XLFDT($P(ZNODE,U,10))
 S %=$P(ZNODE,U,6)
 I % S INA("BDW1ZMD23",CNT,1)=$$EXTSET^XBFUNC(55.05,5,%)
 Q
 ;
OMRXD(CNT,PIEN,MIEN) ;-- create rxd fields
 NEW ZNODE
 S ZNODE=$G(^PS(55,PIEN,"NVA",MIEN,0))
 S INDA("RXD",CNT)=""
 S INA("BDW1RXD1",CNT)=CNT
 S INA("BDW1RXD3",CNT)=$$FMTHL7^XLFDT($P(ZNODE,U,9))
 Q
