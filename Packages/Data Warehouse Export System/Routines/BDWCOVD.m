BDWCOVD ; IHS/CMI/LAB - MAIN DRIVER DW EXPORT ;
 ;;1.0;IHS DATA WAREHOUSE;**7,11**;JAN 24, 2006;Build 14
 ;
START ;EP - called from option
 K BDWDDR
 I $D(ZTQUEUED) S BDWO("SCHEDULED")=""
 S BDWO("RUN")="NEW" ;      Let BDWDRI know this is a new run.
 D ^BDWCOVD1 ;           Do initialization
 ;I $D(BDWO("QUEUE")) D EOJ W !!,"Okay, request queued!!",!! Q
 I BDW("QFLG")=99 D EOJ W !!,"Bye",!! Q
 I BDW("QFLG")=4 W:'$D(ZTQUEUED) !,"Contact your site manager.  ^BDWCTMP still exists." D  D EOJ Q
 .Q:$D(ZTQUEUED)
 .S DIR(0)="EO",DIR("A")="Press ENTER to continue" K DA D ^DIR K DIR
 I BDW("QFLG") S DIR(0)="EO",DIR("A")="Press ENTER to continue" K DA D ^DIR K DIR D EOJ D ABORT Q
DRIVER ;EP called from TSKMN+2
 S BDW("BT")=$H
 D NOW^%DTC S BDW("RUN START")=% K %,%H,%I
 S DIE="^BDWCVLOG(",DA=BDWLOG,DR=".1///R"_";.01////"_BDW("RUN START")_";.14///"_$S($$PROD^XUPROD():"P",1:"T") D ^DIE K DA,DIE,DR
 I BDW("QFLG") D ABORT Q
 D PROCESS ;            Generate trasactions
 I BDW("QFLG") D ABORT Q
 D LOG ;                Update Log
 I BDW("QFLG") D ABORT Q
 D RUNTIME
 S BDWMSGT=""
 S BDWMSGT=$$TRAIL^BDWCBHL(BDWLOG,.BDWCOUNT)
 S ^BDWCTMP(BDWIEDST,BDWMSGT)=""
 S DA=BDWLOG,DIE="^BDWCVLOG(",DR=".11////"_$G(BDWMSGH)_";.12////"_$G(BDWMSGT)_";.13////"_BDWRUN_";.1////C" D ^DIE
 S DA=BDWLOG,DIK="^BDWCVLOG(" D IX^DIK K DA,DIK
 I BDW("QFLG") D ABORT Q
 I '$D(ZTQUEUED) S DIR(0)="EO",DIR("A")="DONE -- Press ENTER to Continue" K DA D ^DIR K DIR
 D EOJ
 Q
 ;
PROCESS ;EP
 ;loop through begin date through end date and generate daily and cumulative numbers
 ;message for each date
PROCN ;
 S BDWMSGH=$$HEAD^BDWCBHL(BDWLOG)
 S ^BDWCTMP(BDWIEDST,BDWMSGH)=""
 W:'$D(ZTQUEUED) !!,"Generating transactions.  .."
 S BDWDATE=$$FMADD^XLFDT(BDW("RUN BEGIN"),-1)
 F  S BDWDATE=$$FMADD^XLFDT(BDWDATE,1) Q:BDWDATE>BDW("RUN END")  D PROCESS2
 Q
PROCESS2 ;
 W:'$D(ZTQUEUED) ".."
 K BDWDAILY
 D LABTESTS^BDWCOV1(BDWDATE,,"BDWDAILY")
 K BDWCUML
 D LABTESTS^BDWCOV1(BDWDATE,1,"BDWCUML")
 D AUTHBEDS^BDWCOVAD
 D CHKBED^BDWCOVAD(BDWDATE)
 D DISC^BDWCOVAD(BDWDATE)
 D SET^BDWCOVAD
 ;D SETLORI ;LORI FIX
 D SETHL7^BDWCBHL(BDWLOG,BDWDATE,BDWZHB,BDWZIC,BDWZCP,BDWZPD,.BDWDAILY,.BDWCUML)
 ;S BDWMSGI=$$Z19^BDWCBHL(BDWLOG,.INDA,.INA)
 ;S ^BDWCTMP(BDWIEDST,BDWMSGI)=""
LOGUPD ;UPDATE LOG FOR THIS DATE/DATA
 S BDWDM=$O(^BDWCVLOG(BDWLOG,11,"B",BDWDATE,0))  ;;1100 MULTIPLE FOR THIS DATE
 I 'BDWDM D GETDM
 ;1100 MULTIPLE FOR THIS ASUFAC
 ;HOSPITAL DATA FIELDS FIRST ALWAYS EVEN IF NOT A HOSPITAL
 ;CREATE ASUFAC MULTIPLE
 S BDWLOC=$P(^AUTTSITE(1,0),U,1),BDWAS=$P(^AUTTLOC(BDWLOC,0),U,10) I BDWAS="" S BDWAS="999999"
 S BDWASM=$O(^BDWCVLOG(BDWLOG,11,"B",BDWDATE,11,BDWAS,0))
 S X=BDWAS
 I 'BDWASM D GETASM
 D ^XBFMK
 S DA(2)=BDWLOG
 S DA(1)=BDWDM
 S DA=BDWASM
 S DIE="^BDWCVLOG("_BDWLOG_",11,"_BDWDM_",11,"
 S:'$D(^BDWCVLOG(BDWLOG,11,BDWDM,11,0)) ^BDWCVLOG(BDWLOG,11,BDWDM,11,0)="^90213.21111A"
 S DR=".02////"_BDWLOC_";.13///"_$P(BDWZHB,U)_";.14///"_$P(BDWZHB,U,2)_";.15///"_$P(BDWZHB,U,3)_";.16///"_$P(BDWZIC,U)_";.17///"_$P(BDWZIC,U,2)_";.18///"_$P(BDWZIC,U,3)_";.19///"_$P(BDWZCP,U)
 S DR=DR_";.2///"_$P(BDWZCP,U,2)_";.21///"_$P(BDWZPD,U)_";.22///"_$P(BDWZPD,U,2)_";.23///"_$P(BDWZPD,U,3)
 D ^DIE
 D ^XBFMK
 ;IF MAIN FACILITY HAS NO TESTS, SET ZEROES
 I '$D(BDWCUML(BDWAS)) F X=1:1:8 S $P(BDWCUML(BDWAS),U,X)=0
 I '$D(BDWDAILY(BDWAS)) F X=1:1:8 S $P(BDWDAILY(BDWAS),U,X)=0
 S BDWAS="" F  S BDWAS=$O(BDWCUML(BDWAS)) Q:BDWAS=""  D
 .S BDWLOC=$O(^AUTTLOC("C",BDWAS,0))
 .S J=$G(BDWDAILY(BDWAS)) K BDWDAILY(BDWAS)
 .S K=$G(BDWCUML(BDWAS))
 .S BDWASM=$O(^BDWCVLOG(BDWLOG,11,BDWDM,11,"B",BDWAS,0))
 .I 'BDWASM D GETASM  ;IEN IN MULTIPLE
 .K DIC,DA,DD,DO,D0
 .S DA(2)=BDWLOG
 .S DA(1)=BDWDM
 .S DA=BDWASM
 .S DIE="^BDWCVLOG("_BDWLOG_",11,"_BDWDM_",11,"
 .S:'$D(^BDWCVLOG(BDWLOG,11,BDWDM,11,0)) ^BDWCVLOG(BDWLOG,11,BDWDM,11,0)="^90213.21111A"
 .S DR=".02////"_BDWLOC_";.03///"_+$P(J,U,1)_";.04///"_+$P(J,U,2)_";.05///"_+$P(J,U,3)_";.06///"_+$P(J,U,5)_";.07///"_+$P(J,U,4)_";.08///"_+$P(K,U,1)_";.09///"_+$P(K,U,2)_";.1///"_+$P(K,U,3)_";.11///"_+$P(K,U,5)_";.12///"_+$P(K,U,4)
 .S DR=DR_";.24///"_+$P(J,U,6)_";.25///"_+$P(J,U,7)_";.26///"_+$P(J,U,8)_";.27///"_+$P(K,U,6)_";.28///"_+$P(K,U,7)_";.29///"_+$P(K,U,8)
 .D ^DIE
 .D ^XBFMK
 ;ANY DAILY'S LEFT?
 S BDWAS="" F  S BDWAS=$O(BDWDAILY(BDWAS)) Q:BDWAS=""  D
 .W BDWDATE,BOMB
 .S BDWLOC=$O(^AUTTLOC("C",BDWAS,0))
 .S J=$G(BDWDAILY(BDWAS))
 .S K=$G(BDWCUML(BDWAS))
 .S BDWASM=$O(^BDWCVLOG(BDWLOG,11,BDWDM,11,"B",BDWAS,0))
 .I 'BDWASM D GETASM  ;IEN IN MULTIPLE
 .K DIC,DA,DD,DO,D0
 .S DA(2)=BDWLOG
 .S DA(1)=BDWDM
 .S DA=BDWASM
 .S DIE="^BDWCVLOG("_BDWLOG_",11,"_BDWDM_",11,"
 .S:'$D(^BDWCVLOG(BDWLOG,11,BDWDM,11,0)) ^BDWCVLOG(BDWLOG,11,BDWDM,11,0)="^90213.21111A"
 .S DR=".02////"_BDWLOC_";.03///"_+$P(J,U,1)_";.04///"_+$P(J,U,2)_";.05///"_+$P(J,U,3)_";.06///"_+$P(J,U,5)_";.07///"_+$P(J,U,4)_";.08///"_+$P(K,U,1)_";.09///"_+$P(K,U,2)_";.1///"_+$P(K,U,3)_";.11///"_+$P(K,U,5)_";.12///"_+$P(K,U,4)
 .S DR=DR_";.24///"_+$P(J,U,6)_";.25///"_+$P(J,U,7)_";.26///"_+$P(J,U,8)_";.27///"_+$P(K,U,6)_";.28///"_+$P(K,U,7)_";.29///"_+$P(K,U,8)
 .D ^DIE
 .D ^XBFMK
 Q
GETASM ;
 K DIC,DA,DD,DO,D0
 S DA(2)=BDWLOG
 S DA(1)=BDWDM
 S DIC="^BDWCVLOG("_BDWLOG_",11,"_BDWDM_",11,"
 S DIC(0)="L"
 S:'$D(^BDWCVLOG(BDWLOG,11,BDWDM,11,0)) ^BDWCVLOG(BDWLOG,11,BDWDM,11,0)="^90213.21111A"
 S X=BDWAS
 D FILE^DICN
 S BDWASM=+Y
 Q
GETDM ;
 S DA(1)=BDWLOG
 S DIC="^BDWCVLOG("_BDWLOG_",11,"
 S DIC(0)="L" K DD,DO
 S:'$D(^BDWCVLOG(BDWLOG,11,0)) ^BDWCVLOG(BDWLOG,11,0)="^90213.211DA"
 S X=BDWDATE ;$$FMTE^XLFDT(BDWDATE)
 D FILE^DICN
 S BDWDM=+Y
 Q
 ;
 ;
 ;
ABORT ; ABNORMAL TERMINATION
 D EOJ
 Q
 ;
LOG ; UPDATE LOG
 W:'$D(ZTQUEUED) !,"Updating log entry."
 D NOW^%DTC S BDW("RUN STOP")=%
 S DA=BDWLOG,DIE="^BDWCVLOG("
 S DR=".04////"_BDW("RUN STOP") D ^DIE I $D(Y) S BDW("QFLG")=26 Q
 K DIE,DA,DR
 ;S DA=BDWLOG,DIE="^BDWCVLOG(",DR=".13////"_BDWRUN D ^DIE I $D(Y) S BDW("QFLG")=26 Q
 K DR,DIE,DA,DIV,DIU
 Q
 ;
C(X,X2,X3) ;
 I X="" Q ""
 D COMMA^%DTC
 Q X
EOJ ; EOJ
 K ^TMP($J)
 K DIADD,DLAYGO
 D ^XBFMK
 D EN^XBVK("BDW")
 I $D(ZTQUEUED) S ZTREQ="@"
 Q
RUNTIME ;EP - SHOW RUN 
 S B=BDW("BT")
 S E=$HOROLOG
 S T=(86400*($P(E,",")-$P(B,",")))+($P(E,",",2)-$P(B,",",2)),H=$P(T/3600,".")
 S:H="" H=0
 S T=T-(H*3600),M=$P(T/60,".")
 S:M="" M=0
 S T=T-(M*60),S=T
 W:'$D(ZTQUEUED) !!,"RUN TIME (H.M.S): ",H,".",M,".",S
 S BDWRUN=H_"."_M_"."_S
 K B,E,H,M,S,T
 Q
QUEUE ;EP - called from option that can be scheduled to run automatically
 K BDWDDR
 I $D(ZTQUEUED) S BDWO("SCHEDULED")=""
 S BDWO("RUN")="NEW" ;      Let BDWDRI know this is a new run.
 D ^BDWCOVD1 ;           Do initialization
 I BDW("QFLG") D ABORT Q
 D DRIVER
 Q
