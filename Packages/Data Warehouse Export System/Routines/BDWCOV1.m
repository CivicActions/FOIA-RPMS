LABTESTS(TDATE,CUML,BDWARRY) ;
 ;Input:  TDATE - DATE TO LOOK FOR DATA
 ;        CUML - pass a 1 if you want cumulative and I will go from 03012020 or whatever date to T-1
 ;output:  BDWARRY(DATE,ASUFAC)=# TESTS^# POSITIVE^# NEGATIVE^# IN PROCESS^# INDETERMINATE^abbott done^abbott resulted^abbott pos
 NEW BDWLABT,BDATE,EDATE,BDWD,X,P,BDWV,BDWASF,BDWCOL,BDWVLI,BDWABB,BDWRES,BDWRESD,BDWPST,BDWPSTA
 K @BDWARRY
 S BDWPST=$P($G(^AUTTSITE(1,0)),U)  ;added if no tests at parent site
 S BDWPSTA=$P($G(^AUTTLOC(BDWPST,0)),U,10)  ;added if no tests at parent site
 S @BDWARRY@(BDWPSTA)=""  ;added in case no tests at parent site
 S BDWDA=$O(^ATXLAB("B","BDW COVID-19 DIAGNOSTIC TESTS",0))
 I 'BDWDA D CREATETX^BDWCOVT S BDWDA=$O(^ATXLAB("B","BDW COVID-19 DIAGNOSTIC TESTS",0))
 I '$O(^ATXLAB(BDWDA,21,0)) S BDWMSG="Lab Taxonomy BDW COVID-19 DIAGNOSTIC TESTS has no entries" D ALERT Q   ;NOT SURE ALERT IS NECESSARY LORI
 I $G(CUML) S BDATE=3200301,EDATE=TDATE
 I '$G(CUML) S (BDATE,EDATE)=TDATE
 ;S BDWD=$S($G(CUML):"CUMULATIVE",1:TDATE)
 ;S BDWD=TDATE
 S BDWLABT=0 F  S BDWLABT=$O(^ATXLAB(BDWDA,21,"B",BDWLABT)) Q:BDWLABT'=+BDWLABT  D LOOPLAB
 Q
LOOPLAB ;
 NEW BDWVLI,BDWV,BDWAFG,BDWCOL,BDWRESDT,BDWRES,BDWABB
 S BDWVLI=0 F  S BDWVLI=$O(^AUPNVLAB("B",BDWLABT,BDWVLI)) Q:BDWVLI'=+BDWVLI  D
 .S BDWABB=""
 .Q:'$D(^AUPNVLAB(BDWVLI,0))  ;BAD XREF
 .I $P(^AUPNVLAB(BDWVLI,0),U,6)="" Q  ;MUST HAVE AN ACCESSION NUMBER
 .;SKIP DEMO PATIENTS
 .S P=$$VALI^XBDIQ1(9000010.09,BDWVLI,.02)
 .I 'P Q
 .I $$DEMO^BDWHUTL(P,"E") Q
 .S BDWV=$P(^AUPNVLAB(BDWVLI,0),U,3)
 .S BDWASF=$$LOCENC^APCLV(BDWV,"C")
 .I BDWASF="" S BDWASF=999999   ;NOT SURE WHAT TO PUT HERE
 .I $$VAL^XBDIQ1(9000010.09,BDWVLI,.01)="_COVID-19(Abbott ID NOW)" S BDWABB=1  ;it's an abbott test
 .S BDWCOL=$P($$VALI^XBDIQ1(9000010.09,BDWVLI,1201),".",1)  ;COLLECTION DATE/TIME
 .I BDWCOL="" Q  ;MUST BE DONE BY LAB
 .S BDWRES=$$VALI^XBDIQ1(9000010.09,BDWVLI,.04)
 .S BDWRESD=$P($$VALI^XBDIQ1(9000010.09,BDWVLI,1212),".",1)
 .I '$G(CUML) D  Q     ;DAILY COUNTS
 ..I BDWCOL=TDATE D   ;SET TEST DONE COUNTER
 ...I '$D(@BDWARRY@(BDWASF)) S @BDWARRY@(BDWASF)=""
 ...S $P(@BDWARRY@(BDWASF),U,1)=$P(@BDWARRY@(BDWASF),U,1)+1 I BDWABB S $P(@BDWARRY@(BDWASF),U,6)=$P(@BDWARRY@(BDWASF),U,6)+1 ;TESTS 
 ...I BDWRESD=""!(BDWRESD>TDATE) S $P(@BDWARRY@(BDWASF),U,4)=$P(@BDWARRY@(BDWASF),U,4)+1 Q  ;not resulted on this day so put in progress
 ..I BDWRESD=TDATE D   ;RESULTED ON THIS DATE SO SET RESULT COUNTERS
 ...I '$D(@BDWARRY@(BDWASF)) S @BDWARRY@(BDWASF)=""
 ...I BDWABB S $P(@BDWARRY@(BDWASF),U,7)=$P(@BDWARRY@(BDWASF),U,7)+1  ;ANY RESULT ABBOTT
 ...;CHECK TAXONOMY FIRST
 ...I $$POS(BDWDA,BDWLABT,BDWRES) S $P(@BDWARRY@(BDWASF),U,2)=$P(@BDWARRY@(BDWASF),U,2)+1 S:BDWABB $P(@BDWARRY@(BDWASF),U,8)=$P(@BDWARRY@(BDWASF),U,8)+1 Q  ;POSITIVE
 ...I $$NEG(BDWDA,BDWLABT,BDWRES) S $P(@BDWARRY@(BDWASF),U,3)=$P(@BDWARRY@(BDWASF),U,3)+1 Q  ;NEGATIVE
 ...S $P(@BDWARRY@(BDWASF),U,5)=$P(@BDWARRY@(BDWASF),U,5)+1 Q  ;INDETERMINATE
 .;
 .I $G(CUML) D  Q    ;cumulative counts
 ..I BDWCOL'<BDATE,BDWCOL'>EDATE D
 ...I '$D(@BDWARRY@(BDWASF)) S @BDWARRY@(BDWASF)=""
 ...;SET TOTAL COLLECTED TO DATE
 ...S $P(@BDWARRY@(BDWASF),U,1)=$P(@BDWARRY@(BDWASF),U,1)+1 I BDWABB S $P(@BDWARRY@(BDWASF),U,6)=$P(@BDWARRY@(BDWASF),U,6)+1 ;TESTS
 ...;RESULT COUNTS
 ...I '$D(@BDWARRY@(BDWASF)) S @BDWARRY@(BDWASF)=""
 ...I BDWRESD>EDATE S $P(@BDWARRY@(BDWASF),U,4)=$P(@BDWARRY@(BDWASF),U,4)+1 Q
 ...I BDWRESD="" S $P(@BDWARRY@(BDWASF),U,4)=$P(@BDWARRY@(BDWASF),U,4)+1 Q
 ...;ATTEMPT TO FIGURE OUT IF POSITIVE OR NEGATIVE, WHAT IF I CAN'T TELL?
 ...I BDWABB S $P(@BDWARRY@(BDWASF),U,7)=$P(@BDWARRY@(BDWASF),U,7)+1  ;ANY RESULT ABBOTT
 ...;CHECK TAXONOMY FIRST
 ...I $$POS(BDWDA,BDWLABT,BDWRES) S $P(@BDWARRY@(BDWASF),U,2)=$P(@BDWARRY@(BDWASF),U,2)+1 S:BDWABB $P(@BDWARRY@(BDWASF),U,8)=$P(@BDWARRY@(BDWASF),U,8)+1 Q  ;POSITIVE
 ...I $$NEG(BDWDA,BDWLABT,BDWRES) S $P(@BDWARRY@(BDWASF),U,3)=$P(@BDWARRY@(BDWASF),U,3)+1 Q  ;NEGATIVE
 ...S $P(@BDWARRY@(BDWASF),U,5)=$P(@BDWARRY@(BDWASF),U,5)+1 Q  ;INDETERMINATE
 Q
 ;
POS(T,LT,R) ;IS RESULT POSITIVE?
 I '$G(T) Q ""
 I '$G(LT) Q ""
 I $G(R)="" Q ""
 S R=$$UP^XLFSTR(R)
 NEW X,Y,Z,G,S,B
 S Z=$O(^ATXLAB(T,21,"B",LT,0))
 S G=0
 S X="" F  S X=$O(^ATXLAB(T,21,Z,12,"B",X)) Q:X=""!(G)  D
 .S B=$$UP^XLFSTR(X)
 .I B=R S G=1 Q
 Q G
NEG(T,LT,R) ;IS RESULT NEGATIVE?
 I '$G(T) Q ""
 I '$G(LT) Q ""
 I $G(R)="" Q ""
 S R=$$UP^XLFSTR(R)
 NEW X,Y,Z,G,S,B
 S Z=$O(^ATXLAB(T,21,"B",LT,0))
 S G=0
 S X="" F  S X=$O(^ATXLAB(T,21,Z,13,"B",X)) Q:X=""!(G)  D
 .S B=$$UP^XLFSTR(X)
 .I B=R S G=1
 Q G
IND(T,LT,R) ;IS RESULT INDETERMINATE?
 I '$G(T) Q ""
 I '$G(LT) Q ""
 I $G(R)="" Q ""
 S R=$$UP^XLFSTR(R)
 NEW X,Y,Z,G,S,B
 S Z=$O(^ATXLAB(T,21,"B",LT,0))
 S G=0
 S X="" F  S X=$O(^ATXLAB(T,21,Z,14,"B",X)) Q:X=""!(G)  D
 .S B=$$UP^XLFSTR(X)
 .I B=R S G=1
 Q G
ALERT ;
 Q
COVPOS(P,EDATE,POSONLY) ;EP
 I '$G(P) Q ""
 ;find all covid 19 tests for patient P, put in array by inverse date
 ;if any are positive pass back a 1 for yes
 ;if no positives, take last test, if result is null or indeterminate pass back 1 for yes
 ;otherwise pass back ""
 NEW BDWLABT,BDWTIEN,BDWTESTS,ID,BDWX,R,POS,RV,D
 S BDWTIEN=$O(^ATXLAB("B","BDW COVID-19 DIAGNOSTIC TESTS",0))
 I 'BDWTIEN Q ""  ;no taxonomy 
 S BDWLABT="",POS=""
 F  S BDWLABT=$O(^ATXLAB(BDWTIEN,21,"B",BDWLABT)) Q:BDWLABT=""!(POS)  D
 .S ID=0 F  S ID=$O(^AUPNVLAB("AA",P,BDWLABT,ID)) Q:ID=""!(POS)  D
 ..S BDWX=0 F  S BDWX=$O(^AUPNVLAB("AA",P,BDWLABT,ID,BDWX)) Q:BDWX=""!(POS)  D
 ...Q:$$VALI^XBDIQ1(9000010.09,BDWX,.06)=""  ;MUST HAVE ACCESSION #
 ...S D=$P($$VALI^XBDIQ1(9000010.09,BDWX,1201),".")  ;collection date
 ...I D>EDATE Q   ;test done after end date
 ...S R=$$VAL^XBDIQ1(9000010.09,BDWX,.04)
 ...I R]"",$$POS(BDWTIEN,$$VALI^XBDIQ1(9000010.09,BDWX,.01),R) S POS=1 Q  ;EVER HAD A POSITIVE SO STOP PROCESSING
 ...S BDWTESTS(ID,9999999999-BDWX)=R
 ...Q
 ..Q
 .Q
 I POS Q 1
 ;THERE WAS NO POSITIVE TEST SO CHECK LAST ONE
 I $G(POSONLY) Q ""  ;ONLY WANT POSITIVE SO DON'T LOOK FURTHER
 S D=$O(BDWTESTS(0)) I D="" Q ""
 S BDWX=$O(BDWTESTS(D,0)) I BDWX="" Q ""
 S R=BDWTESTS(D,BDWX)
 I R="" Q 1  ;LAST ONE IN PROGRESS
 I $$IND(BDWTIEN,$$VALI^XBDIQ1(9000010.09,BDWX,.01),R) Q 1  ;LAST ONE INDETERMINATE
 Q ""
 ;
TEST ;
 S BDWDATE=3200420
 K BDWDAILY,LORI
 D LABTESTS^BDWCOV1(BDWDATE,,"BDWDAILY")
 ;W ! ZW BDWDAILY
 ;W ! ZW LORI
 K BDWCUML,LORI
 ;W !!!,"CUMULATIVE",!
 D LABTESTS^BDWCOV1(BDWDATE,1,"BDWCUML")
 ;W ! ZW BDWCUML
 ;W ! ZW LORI
 Q
