AL(RETVAL,BDWP) ;EP
 ;returns
 ;RETVAL(CNT)=UNIQUE ID^ALLERY TYPE^ORIGINATION DATE/TIME^REACTANT^OBSERVED/HISTORICAL^VERIFIED^VERIFICATION DATE/TIME^ENTERED IN ERROR^DATE/TIME ENTRED IN ERROR
