PRB(RETVAL,BDWP) ;EP
 N PRB,DX,DXS,DXI,DXD,DXT,DTA,DLM,PN,FAC,POVN,SC,ST,DTDL,DTO,CNT
 K RETVAL
 I '$D(^AUPNPROB("AC",BDWP)) Q
 S CNT=0
 N PDA,DATA
 S PDA=0 F  S PDA=$O(^AUPNPROB("AC",BDWP,PDA)) Q:'PDA  D
 . S CNT=CNT+1
 . S DATA=$G(^AUPNPROB(PDA,0))
 . S DXI=$P(DATA,U)
 . I $D(^ICDS(0)) S DXS=$$ICDDX^ICDEX(DXI)
 . I '$D(^ICDS(0)) S DXS=$$ICDDX^ICDCODE(DXI)
 . S DX=$TR($P(DXS,U,2),"|","")
 . ;S DXD=$TR($P(DXS,U,4),"|","")  ;IHS/CMI/LAB COMMENTED OUT
 . S DXD=""   ;IHS/CMI/LAB REMOVE DX NARRATIVE
 . S DXT=$P(DXS,U,20)
 . S DXT=$S(DXT=30:"I10",1:"I9")
 . S DTA=$$FMTHL7^XLFDT($E($P(DATA,U,8),1,7))
 . S DLM=$$FMTHL7^XLFDT($E($P(DATA,U,3),1,7))
 . S PN=$P(DATA,U,7)
 . S FAC=$$GET1^DIQ(9999999.06,$P(DATA,U,6),.12)
 . S POVN=$$GET1^DIQ(9000011,PDA,.05)
 . S POVN=$TR(POVN,"|","")
 . S SC=$P(DATA,U,12)
 . S ST=$$GET1^DIQ(9000011,PDA,.12)
 . S DTDL=$$FMTHL7^XLFDT($E($P($G(^AUPNPROB(PDA,2)),U,2),1,7))
 . S DTO=$$FMTHL7^XLFDT($E($P(DATA,U,13),1,7))
 . S RETVAL(CNT)=DX_U_DXD_U_DXT_U_DTA_U_DLM_U_PN_U_FAC_U_POVN_U_SC_U_ST_U_DTDL_U_DTO
 . ;P8
 . S $P(RETVAL(CNT),U,13)=$$VALI^XBDIQ1(9000011,PDA,80001)  ;SNOMED CONCEPT ID
 . S $P(RETVAL(CNT),U,14)=$TR($$VALI^XBDIQ1(9000011,PDA,.22),"|","*")  ;LATERALITY
 . NEW I,V,Y
 . ;SEVERITY
 . S I=0,V="" F  S I=$O(^AUPNPROB(PDA,13,I)) Q:I'=+I  D
 .. S Y=$P($G(^AUPNPROB(PDA,13,I,0)),U,1)
 .. I V]"" S V=V_"~"
 .. S V=V_Y
 . S $P(RETVAL(CNT),U,15)=V
 . ;CLINICAL COURSE
 . S I=0,V="" F  S I=$O(^AUPNPROB(PDA,18,I)) Q:I'=+I  D
 .. S Y=$P($G(^AUPNPROB(PDA,18,I,0)),U,1)
 .. I V]"" S V=V_"~"
 .. S V=V_Y
 . S $P(RETVAL(CNT),U,16)=V
 Q
