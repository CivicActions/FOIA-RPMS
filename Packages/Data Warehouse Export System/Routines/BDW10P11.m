FBHLMSH(MIEN,MCODE) ;
 NEW A,B,C
 S A=0 F  S A=$O(^INTHL7M(MIEN,1,A)) Q:A'=+A  D
 .S B=0 F  S B=$O(^INTHL7M(MIEN,1,A,5,B)) Q:B'=+B  D
 ..S C=^INTHL7M(MIEN,1,A,5,B,0)
 ..I C["BHLMSH" S ^INTHL7M(MIEN,1,A,5,B,0)=MCODE
 S A=0 F  S A=$O(^INTHL7M(MIEN,6,A)) Q:A'=+A  D
 .S C=^INTHL7M(MIEN,6,A,0)
 .I C["BHLMSH" S ^INTHL7M(MIEN,6,A,0)=MCODE
