 ;;1.0;IHS DATA WAREHOUSE;**6,8,9,10,11**;JAN 24, 2006;Build 14
 ;
START ;EP - called from option
 K BDWDDR
 I $D(ZTQUEUED) S BDWO("SCHEDULED")=""
 S BDWO("RUN")="NEW" ;      Let BDWDRI know this is a new run.
 D ^BDWHRDRI
 I $D(BDWO("QUEUE")) D EOJ W !!,"Okay, request queued!!",!! Q
 I BDW("QFLG")=99 D EOJ W !!,"Bye",!! Q
 I BDW("QFLG")=4 W:'$D(ZTQUEUED) !,"Contact your site manager.  ^BDWHTMP still exists." D  D EOJ Q
 .S DIR(0)="EO",DIR("A")="Press ENTER to continue" K DA D ^DIR K DIR
 I BDW("QFLG") D ABORT Q
DRIVER ;EP called from TSKMN+2
 S BDW("BT")=$H
 D NOW^%DTC S BDW("RUN START")=%,BDW("MAIN TX DATE")=$P(%,".") K %,%H,%I
 S BDWT=$$PROD^XUPROD()
 S DIE="^BDWHLOG(",DA=BDWLOG,DR=".15///R"_";.1////"_BDW("RUN START")_";1108///"_$S(BDWT:"P",1:"T") D ^DIE K DA,DIE,DR
 I BDW("QFLG") D ABORT Q
 S BDWCNT=$S('$D(ZTQUEUED):"X BDWCNT1  X BDWCNT2",1:"S BDWCNTR=BDWCNTR+1"),BDWCNT1="F BDWCNTL=1:1:$L(BDWCNTR)+1 W @BDWBS",BDWCNT2="S BDWCNTR=BDWCNTR+1 W BDWCNTR,"")"""
 D PROCESS ;            Generate trasactions
 I BDW("QFLG") D ABORT Q
 D LOG ;                Update Log
 I BDW("QFLG") D ABORT Q
 S BDWMSGT=""
 S BDWMSGT=$$DW1HTRLR^BDWHEVNT(BDWLOG)
 S ^BDWHTMP(BDWIEDST,BDWMSGT)=""
 D RUNTIME
 S DA=BDWLOG,DIE="^BDWHLOG(",DR=".13////"_BDWRUN_";.12////"_BDWMSGT_";.15////C" D ^DIE
 S DA=BDWLOG,DIK="^BDWHLOG(" D IX^DIK K DA,DIK
 I BDW("QFLG") D ABORT Q
 D:'$D(ZTQUEUED) CHKLOG
 I '$D(ZTQUEUED) S DIR(0)="EO",DIR("A")="DONE -- Press ENTER to Continue" K DA D ^DIR K DIR
 D EOJ
 Q
 ;
PROCESS ;EP
 K ^TMP($J,"RXBYVISIT")
 S BDWMSGH=$$DW1HHDR^BDWHEVNT(BDWLOG)
 S ^BDWHTMP(BDWIEDST,BDWMSGH)=""
 D ^XBFMK S DA=BDWLOG,DIE="^BDWHLOG(",DR=".11////"_BDWMSGH D ^DIE,^XBFMK
 ;DOES ANY DIVISION NEED TO BE BACKLOADED
 S BDWCNTR=0,BDWTRXC=0,BDWSKIP=0,BDWTOTHL=0,BDW("OM COUNT")=0,BDW("OM BL COUNT")=0,BDW("FPROC")=0,BDWTC=0,BDW("COUNT")=0,BDW("BL COUNT")=0,BDWLC=0
 I $G(BDWDDR)=1 G PROCN
 S X=0 K Y,BDWNBLI F  S X=$O(BDWHOPE(X)) Q:X'=+X  I BDWHOPE(X)="" S BDWNBLI(X)=""
 I $O(BDWNBLI(0)) D
 .S BDWIAMBL=1,BDW("BL COUNT")=0,BDW("OM BL COUNT")=0
 .W:'$D(ZTQUEUED) !!,"Generating Backload Prescription transactions.  Counting fills and refills.  (1)"
 .M BDWHOLDH=BDWHOPE K BDWHOPE M BDWHOPE=BDWNBLI
 .S BDWFILLD=$$FMADD^XLFDT(BDWBLBD,-1)  ;START DATE FOR BACKLOAD
 .F  S BDWFILLD=$O(^PSRX("AD",BDWFILLD)) Q:BDWFILLD'=+BDWFILLD!(BDWFILLD>BDWBLED)  D PROCESS2 Q:BDW("QFLG")
OMBL .;
 .S BDWDOCD=BDWBLBD,BDW("OM BL COUNT")=0
 .F  S BDWDOCD=$O(^PS(55,"ADCDT",BDWDOCD)) Q:BDWDOCD'=+BDWDOCD!($P(BDWDOCD,".")>BDWBLED)  D
 ..S BDWOMP=0 F  S BDWOMP=$O(^PS(55,"ADCDT",BDWDOCD,BDWOMP)) Q:BDWOMP'=+BDWOMP  D
 ...S BDWOMI=0 F  S BDWOMI=$O(^PS(55,"ADCDT",BDWDOCD,BDWOMP,BDWOMI)) Q:BDWOMI'=+BDWOMI  D
 ....S N=$G(^PS(55,BDWOMP,"NVA",BDWOMI,0))
 ....S D=$P(N,U,2)
 ....I D="" S D=$$OMOID(BDWOMP,BDWOMI)
 ....I D="" Q  ;NO DRUG SO DON'T SEND
 ....Q:$$DEMO^APCLUTL(BDWOMP,"E")  ;demo patient so skip
 ....I BDWDOCD<3190101 Q:'$$DRUGOP(D)  ;ONLY OPIOIDS IN BACKLOAD PRIOR TO 2019
 ....;get visit ien from v med if there is one
 ....S V=""
 ....S M=$P($G(^PS(55,BDWOMP,"NVA",BDWOMI,999999911)),U,1)
 ....I M S V=$$VALI^XBDIQ1(9000010.14,M,.03)
 ....I 'V S V="NOVISIT"
 ....;do we need to check division somehow?
 ....S ^TMP($J,"OMBYVISIT",V,BDWOMP,BDWOMI)=BDWDOCD_U_1
 .S X=0 F  S X=$O(BDWHOPE(X)) Q:X'=+X  S $P(^BDWSITE(1,21,X,0),U,3)=BDW("RUN DATE")
 .K BDWHOPE M BDWHOPE=BDWHOLDH
 .K BDWIAMBL
PROCN ;
 S BDWCNTR=0
 W:'$D(ZTQUEUED) !!,"Generating Prescription transactions.  Counting fills and refills.  (1)"
 S BDWFILLD=BDW("RUN BEGIN")-1,BDW("FPROC")=0
 F  S BDWFILLD=$O(^PSRX("AD",BDWFILLD)) Q:BDWFILLD'=+BDWFILLD!(BDWFILLD>BDW("RUN END"))  D PROCESS2 Q:BDW("QFLG")
OMPROC ;
 S BDW("OM COUNT")=0
 S BDWDOCD=BDW("RUN BEGIN")
 F  S BDWDOCD=$O(^PS(55,"ADCDT",BDWDOCD)) Q:BDWDOCD'=+BDWDOCD!($P(BDWDOCD,".")>BDW("RUN END"))  D
 .S BDWOMP=0 F  S BDWOMP=$O(^PS(55,"ADCDT",BDWDOCD,BDWOMP)) Q:BDWOMP'=+BDWOMP  D
 ..S BDWOMI=0 F  S BDWOMI=$O(^PS(55,"ADCDT",BDWDOCD,BDWOMP,BDWOMI)) Q:BDWOMI'=+BDWOMI  D
 ...S N=$G(^PS(55,BDWOMP,"NVA",BDWOMI,0))
 ...S D=$P(N,U,2)  ;NO DISPENSE DRUG SO DON'T SEND
 ...I D="" S D=$$OMOID(BDWOMP,BDWOMI)
 ...I D="" Q  ;no ndc found
 ...Q:$$DEMO^APCLUTL(BDWOMP,"E")  ;demo patient so skip
 ...I $G(BDWDDR),BDWDOCD<3190101 Q:'$$DRUGOP(D)  ;ONLY OPIOIDS IN BACKLOAD PRIOR TO 2019
 ...;
 ...S V=""
 ...S M=$P($G(^PS(55,BDWOMP,"NVA",BDWOMI,999999911)),U,1)
 ...I M S V=$$VALI^XBDIQ1(9000010.14,M,.03)
 ...I 'V S V="NOVISIT"
 ...;
 ...S ^TMP($J,"OMBYVISIT",V,BDWOMP,BDWOMI)=BDWDOCD
 ;CHECK AND SET IF VISIT IEN CHANGED FROM PREVIOUS EXPORTS
 D VIEN^BDWHRDRA
 I $P(^BDWHLOG(BDWLOG,0),U,7)="R" D RELEASED^BDWHRDRA  ;get released meds that never exported if in a regular export
 D RTS^BDWHRDRA
 ;NOW LOOP ^TMP BY VISIT AND CALL HL7
 S BDWVSIT="",BDWTBLC=0,BDWTOTHL=0
 F  S BDWVSIT=$O(^TMP($J,"RXBYVISIT",BDWVSIT)) Q:BDWVSIT=""  D GENHL7
 S BDWTRXC=BDWTRXC-BDWSKIP
 K ^TMP($J,"RXBYVISIT")
 ;NOW GENERATE OM HL7 MESSAGES AND UPDATE THE LOG
 D OMHL7^BDWHRDRA
 Q
PROCESS2 ;
 S BDWRXIEN="" F  S BDWRXIEN=$O(^PSRX("AD",BDWFILLD,BDWRXIEN)) Q:BDWRXIEN=""  D PROCESS3 Q:BDW("QFLG")
 Q
PROCESS3 ;
 S BDWRFIEN=""  ;REFILL IEN
 F  S BDWRFIEN=$O(^PSRX("AD",BDWFILLD,BDWRXIEN,BDWRFIEN)) Q:BDWRFIEN=""  D PROCESS4
 Q
PROCESS4 ;EP
 I '$G(BDWHIARS) S BDW("FPROC")=$G(BDW("FPROC"))+1
 X BDWCNT
 S BDWVSIT="",BDWVMSG="",BDWMSGI="",BDWTXGEN=""
 D SETTMP
 I '$G(BDWIAMBL),BDWVMSG]"" D
 .S:'$D(^BDWHLOG(BDWLOG,21,0)) ^BDWHLOG(BDWLOG,21,0)="^90213.12101PA^^"
 .S BDWTRXC=BDWTRXC+1,BDWSKIP=BDWSKIP+1
 .S ^BDWHLOG(BDWLOG,21,BDWTRXC,0)=BDWRXIEN_U_$P(^PSRX(BDWRXIEN,0),U,1)_U_BDWRFIEN_U_BDWFILLD_U_BDWOSITE_U_$G(BDWMSGI)_U_BDWTXGEN_U_BDWVMSG_U_$S($G(BDWVSIT):BDWVSIT,1:"")
 .;S ^BDWHLOG(BDWLOG,21,"B",BDWRXIEN,BDWTRXC)=1
 .;I BDWTXGEN S ^BDWHLOG("AEXP",BDWRXIEN,BDWRFIEN,BDWLOG,BDWTRXC)=""
 .S $P(^BDWHLOG(BDWLOG,21,0),U,3)=BDWRXIEN,$P(^(0),U,4)=$P(^(0),U,4)+1
 K DIE,DR,DIC
 S BDWVMSG="",BDWMSGI="",BDWTXGEN=""
 Q
GENHL7 ;
 ;
 K BDWT,BDWV,BDWE,BHLVIEN
 S BDWTXGEN=0
 I BDWVSIT="NOVISIT" D  Q
 .S BDWRXIEN=0 F  S BDWRXIEN=$O(^TMP($J,"RXBYVISIT",BDWVSIT,BDWRXIEN)) Q:BDWRXIEN=""  D
 ..S BDWRFIEN="" F  S BDWRFIEN=$O(^TMP($J,"RXBYVISIT",BDWVSIT,BDWRXIEN,BDWRFIEN)) Q:BDWRFIEN=""  D
 ...S BDWV=""
 ...K BDWRXS
 ...S BDWRXS(BDWRXIEN,BDWRFIEN)=^TMP($J,"RXBYVISIT",BDWVSIT,BDWRXIEN,BDWRFIEN),BDWDFN=$$VALI^XBDIQ1(52,BDWRXIEN,2) D GENHL71
 K BDWRXS
 S BDWRXIEN=0,BDWV=BDWVSIT F  S BDWRXIEN=$O(^TMP($J,"RXBYVISIT",BDWVSIT,BDWRXIEN)) Q:BDWRXIEN=""  D
 .S BDWDFN=$$VALI^XBDIQ1(52,BDWRXIEN,2)
 .S BDWRFIEN="" F  S BDWRFIEN=$O(^TMP($J,"RXBYVISIT",BDWVSIT,BDWRXIEN,BDWRFIEN)) Q:BDWRFIEN=""  D
 ..S BDWRXS(BDWRXIEN,BDWRFIEN)=^TMP($J,"RXBYVISIT",BDWVSIT,BDWRXIEN,BDWRFIEN)
 D GENHL71
 Q
GENHL71 ;
 ;ADD VISIT IEN TO GET LOCATION OF ENCOUNTER FOR PV1
 S BHLVIEN="" I BDWVSIT'="NOVISIT" S BHLVIEN=BDWVSIT
 K BDWIAMBL
 S BDWMSGI=$$O13^BDWHEVNT(BDWV,.BDWRXS,BDWDFN)
 I BDWMSGI]"" S BDWTXGEN=1,^BDWHTMP(BDWIEDST,BDWMSGI)=""
 ;WAS THIS A BACKLOAD MESSAGE?
 S R=0,BDWTC=0 F  S R=$O(BDWRXS(R)) Q:R=""  S RF="" F  S RF=$O(BDWRXS(R,RF)) Q:RF=""  S BDWIAMBL=$P(BDWRXS(R,RF),U,2),BDWTC=BDWTC+1
 I BDWTXGEN,'$G(BDWIAMBL) S BDW("COUNT")=BDW("COUNT")+BDWTC,BDWTOTHL=BDWTOTHL+BDWTC
 I BDWTXGEN,$G(BDWIAMBL) S BDW("BL COUNT")=BDW("BL COUNT")+BDWTC,BDWTOTHL=BDWTOTHL+BDWTC
 I 'BDWTXGEN,$G(BDWIAMBL) Q  ;don't log baCkload unless exported
SET I $G(BDWIAMBL) D  Q
 .S:'$D(^BDWHLOG(BDWLOG,41,0)) ^BDWHLOG(BDWLOG,41,0)="^90213.141PA^^"
 .S R=0 F  S R=$O(BDWRXS(R)) Q:R=""  S RF="" F  S RF=$O(BDWRXS(R,RF)) Q:RF=""  D
 ..S BDWTBLC=BDWTBLC+1
 ..I RF=0 S BDWOSITE=$$VALI^XBDIQ1(52,R,20)
 ..I RF S BDWOSITE=$P(^PSRX(R,1,RF,0),U,9)
 ..S ^BDWHLOG(BDWLOG,41,BDWTBLC,0)=R_U_$P(^PSRX(R,0),U,1)_U_RF_U_$P(BDWRXS(R,RF),U,1)_U_BDWOSITE_U_$G(BDWMSGI)_"^^^"_BDWVSIT ;_U_BDWTXGEN_U_BDWVMSG
 ..S ^BDWHLOG(BDWLOG,41,"B",R,BDWTBLC)=1
 ..S $P(^BDWHLOG(BDWLOG,41,0),U,3)=R,$P(^(0),U,4)=$P(^(0),U,4)+1
SET1 S:'$D(^BDWHLOG(BDWLOG,21,0)) ^BDWHLOG(BDWLOG,21,0)="^90213.12101PA^^"
 S R=0 F  S R=$O(BDWRXS(R)) Q:R=""  S RF="" F  S RF=$O(BDWRXS(R,RF)) Q:RF=""  D
 .S BDWTRXC=BDWTRXC+1
 .I RF=0 S BDWOSITE=$$VALI^XBDIQ1(52,R,20)
 .I RF S BDWOSITE=$P(^PSRX(R,1,RF,0),U,9)
 .S ^BDWHLOG(BDWLOG,21,BDWTRXC,0)=R_U_$P(^PSRX(R,0),U,1)_U_RF_U_$P(BDWRXS(R,RF),U,1)_U_BDWOSITE_U_$G(BDWMSGI)_U_BDWTXGEN_U_BDWVMSG_U_BDWVSIT
 .S ^BDWHLOG(BDWLOG,21,"B",R,BDWTRXC)=1
 .S $P(^BDWHLOG(BDWLOG,21,0),U,3)=R,$P(^(0),U,4)=$P(^(0),U,4)+1
 K DIE,DR,DIC
 Q
SETTMP ;
 ;CHECK OUTPATIENT SITE FOR OPT OUT
 S BDWOSITE=""
 I BDWRFIEN=0 S BDWOSITE=$$VALI^XBDIQ1(52,BDWRXIEN,20)
 I BDWRFIEN,'$D(^PSRX(BDWRXIEN,1,BDWRFIEN,0)) S BDWVMSG="REFILL NODE DELETED - SKIPPED" Q
 I BDWRFIEN S BDWOSITE=$P(^PSRX(BDWRXIEN,1,BDWRFIEN,0),U,9)
 I 'BDWOSITE S BDWVMSG="NO OUTPATIENT SITE TO CHECK - SKIPPED" Q
 I '$D(^BDWSITE(1,21,BDWOSITE,0)) S BDWVMSG="OUTPATIENT SITE NOT IN SITE PARAMETER FILE - SKIPPED" Q
 I '$D(BDWHOPE(BDWOSITE)) Q  ;S BDWVMSG="OUTPATIENT SITE DIVISION OPTED OUT - SKIPPED" Q
 ;
 I $G(BDWIAMBL),'$D(BDWNBLI(BDWOSITE)) Q  ;in backload and doesn't need backloading
 S S=$$VALI^XBDIQ1(52,BDWRXIEN,100)  ;status
 I S=13 S BDWVMSG="STATUS IS DELETED - SKIPPED" Q
 ;is there a release date?  if not don't send it, otherwise send it
 S R=$$RD(BDWRXIEN,BDWRFIEN)
 I 'R Q  ;NO release date so don't send
 ;I S=5 S BDWVMSG="STATUS IS SUSPENDED - SKIPPED" Q
 ;SKIP RETURNED TO STOCK FOR FILL OR REFILL
 ;I $$RTS(BDWRXIEN,BDWRFIEN) S BDWVMSG="RETURNED TO STOCK - SKIPPED" Q  ;20240222 p10 maw orig
 ;S ^MAW($J,BDWRXIEN,BDWRFIEN,+$G(BDWHIARS))=BDWRXIEN_"//"_BDWRFIEN_"//"_BDWFILLD_"//"_$G(BDWHIARS)
 I '$G(BDWHIARS),$$RTSCHK^BDWHUTL(BDWRXIEN,BDWRFIEN,"",BDWFILLD) S BDWVMSG="RETURNED TO STOCK - SKIPPED" Q
 ;DEMO PATIENT SKIP
 S P=$$VALI^XBDIQ1(52,BDWRXIEN,2)
 I $$DEMO^BDWHUTL(P,"E") S BDWVMSG="DEMO PATIENT SKIPPED" Q
 ;
 I $G(BDWIAMBL),BDWFILLD<3190101 Q:'$$OPIOID(BDWRXIEN)  ;ONLY OPIOIDS IN BACKLOAD PRIOR TO 2019
 I $G(BDWDDR),BDWFILLD<3190101 Q:'$$OPIOID(BDWRXIEN)  ;IN DATE RANGE ONLY OPIODS PRIOR TO 010119
 ;SET ^TMP BY VISIT
 ;get v med then visit ien
 S M=$S(BDWRFIEN:$P($G(^PSRX(BDWRXIEN,1,BDWRFIEN,999999911)),U,1),1:$P($G(^PSRX(BDWRXIEN,999999911)),U,1))
 I 'M S V="NOVISIT" G SETTMP1
 S V=$P($G(^AUPNVMED(M,0)),U,3)
 I 'V S V="NOVISIT"
SETTMP1 ;
 S ^TMP($J,"RXBYVISIT",V,BDWRXIEN,BDWRFIEN)=BDWFILLD_U_$G(BDWIAMBL)_U_$G(BDWRTSF)
 Q
OPIOID(R) ;
 NEW D,C,N,T
 S D=$$VALI^XBDIQ1(52,R,6)   ;DRUG IEN FROM FILE 50
 S N=$$NDC^BDWHUTL(R)
 S C=$$VADC^BDWHUTL(R)
 I C="AD100" Q 1
 I C="CN101" Q 1
 I C="CN102" Q 1
 I C="CN302" Q 1
 S T=$O(^ATXAX("B","BGP PQA BENZODIAZ OP NDC",0))
 I N]"",T,$D(^ATXAX(T,21,"B",N)) Q 1
 S T=$O(^ATXAX("B","BGP PQA OPIOID NDC",0))
 I N]"",T,$D(^ATXAX(T,21,"B",N)) Q 1
 S T=$O(^ATXAX("B","BGP PQA BENZODIAZ OP MEDS",0))
 I D]"",T,$D(^ATXAX(T,21,"B",D)) Q 1
 S T=$O(^ATXAX("B","BGP PQA OPIOID MEDS",0))
 I D]"",T,$D(^ATXAX(T,21,"B",D)) Q 1
 ;CHECK CLASS AGAINST LIST
 ;CHECK NDC AGAINST TAXONOMIES
 ;CHECK D AGAINST BPDM TAXONOMY
 Q ""
RTS(P,R,F) ;EP
 NEW T
 S T=""
 I $G(R) D  Q T  ;refill
 .I $P($G(^PSRX(P,1,R,0)),U,16)]"" S T=1
 I $G(F) D  Q T
 .I $P($G(^PSRX(P,"P",F,0)),U,16)]"" S T=1
 I $P($G(^PSRX(P,2)),U,15)]"" Q 1
 Q 0
 ;
CHKLOG ; CHECK LOG FILE
 S BDW("X")=0 F BDW("I")=BDWLOG:-1:1 Q:'$D(^BDWHLOG(BDW("I")))  I $O(^BDWHLOG(BDW("I"),21,0)) S BDW("X")=BDW("X")+1
 I BDW("X")>12 W !,"-->There are more than 12 generations of ENCOUNTERs stored in the LOG file.",!,"-->Time to do a purge."
 Q
 ;
ABORT ; ABNORMAL TERMINATION
 I $D(BDWLOG) S BDW("QFLG1")=$O(^BDWERRC("B",BDW("QFLG"),"")),DA=BDWLOG,DIE="^BDWHLOG(",DR=".15///F;.16////"_BDW("QFLG1")
 I $D(ZTQUEUED) D ERRBULL^BDWRDRI2,EOJ Q
 S DIR(0)="EO",DIR("A")="Press ENTER to continue" K DA D ^DIR K DIR
 D EOJ
 Q
 ;
LOG ;EP
 W:'$D(ZTQUEUED) !!,(BDWTOTHL+2)," HL7 Messages were generated."
 W:'$D(ZTQUEUED) !,"Updating log entry."
 D NOW^%DTC S BDW("RUN STOP")=%
 S DA=BDWLOG,DIE="^BDWHLOG("
 S DR=".04////"_BDW("RUN STOP")_";.05////"_BDW("FPROC")_";.06////"_BDWSKIP_";.08///"_BDW("COUNT")_";1104////"_$G(BDWTBLC)_";1105///"_($G(BDWTOTHL)+2)_";1107///"_BDW("OM BL COUNT")_";1106///"_BDW("OM COUNT") D ^DIE I $D(Y) S BDW("QFLG")=26 Q
 K DIE,DA,DR
 D AUDIT(BDWLOG)
 ;S DA=BDWLOG,DIE="^BDWHLOG(",DR=".13////"_BDWRUN D ^DIE I $D(Y) S BDW("QFLG")=26 Q
 K DR,DIE,DA,DIV,DIU
 S X=0 F  S X=$O(BDWNBLI(X)) Q:X'=+X  S $P(^BDWSITE(1,21,X,0),U,3)=DT
 Q
AUDIT(LOG) ;-- lets put the audit in place
 N FDA,FERR,FIENS
 S FIENS="+2,"_LOG_","
 S FDA(90213.131,FIENS,.01)=$$NOW^XLFDT()
 S FDA(90213.131,FIENS,.02)=$G(DUZ)
 S FDA(90213.131,FIENS,.03)=$G(XQY)
 S FDA(90213.131,FIENS,.04)=$S('BDW("QFLG"):"S",1:"F")
 D UPDATE^DIE("","FDA","FIENS","FERR(1)")
 Q
 ;
C(X,X2,X3) ;
 I X="" Q ""
 D COMMA^%DTC
 Q X
S ;
 S BDWLC=BDWLC+1
 S ^BDWHLOG(BDWLOG,99,BDWLC,0)=X
 K X
 Q
EOJ ; EOJ
 K ^TMP($J)
 K DIADD,DLAYGO
 D ^XBFMK
 D EN^XBVK("BDW")
 I $D(ZTQUEUED) S ZTREQ="@"
 Q
RUNTIME ;EP - SHOW RUN 
 S B=BDW("BT")
 S E=$HOROLOG
 S T=(86400*($P(E,",")-$P(B,",")))+($P(E,",",2)-$P(B,",",2)),H=$P(T/3600,".")
 S:H="" H=0
 S T=T-(H*3600),M=$P(T/60,".")
 S:M="" M=0
 S T=T-(M*60),S=T
 W:'$D(ZTQUEUED) !!,"RUN TIME (H.M.S): ",H,".",M,".",S
 S BDWRUN=H_"."_M_"."_S
 K B,E,H,M,S,T
 Q
QUEUE ;EP
 K BDWDDR
 I $D(ZTQUEUED) S BDWO("SCHEDULED")=""
 S BDWO("RUN")="NEW" ;      Let BDWDRI know this is a new run.
 D ^BDWHRDRI ;           Do initialization
 I BDW("QFLG") D ABORT Q
 D DRIVER
 Q
DRUGOP(D) ;
 S N=$$VAL^XBDIQ1(50,D,31)
 S C=$$VAL^XBDIQ1(50,D,2)  ;VA DRUG CLASS
 I C="AD100" Q 1
 I C="CN101" Q 1
 I C="CN102" Q 1
 I C="CN302" Q 1
 S T=$O(^ATXAX("B","BGP PQA BENZODIAZ OP NDC",0))
 I N]"",T,$D(^ATXAX(T,21,"B",N)) Q 1
 S T=$O(^ATXAX("B","BGP PQA OPIOID NDC",0))
 I N]"",T,$D(^ATXAX(T,21,"B",N)) Q 1
 S T=$O(^ATXAX("B","BGP PQA BENZODIAZ OP MEDS",0))
 I D]"",T,$D(^ATXAX(T,21,"B",D)) Q 1
 S T=$O(^ATXAX("B","BGP PQA OPIOID MEDS",0))
 I D]"",T,$D(^ATXAX(T,21,"B",D)) Q 1
 ;CHECK CLASS AGAINST LIST
 ;CHECK NDC AGAINST TAXONOMIES
 ;CHECK D AGAINST BPDM TAXONOMY
 Q ""
OMOID(BDWOMP,BDWOMI) ;EP - get drug from NVA
 NEW OIP,DIEN,D,N,G
 S G=""
 S OIP=$P($G(^PS(55,BDWOMP,"NVA",BDWOMI,0)),U,1)
 I 'OIP Q ""
 S DIEN=0 F  S DIEN=$O(^PSDRUG("ASP",OIP,DIEN)) Q:DIEN'=+DIEN!(G)  D
 .S N=$$VAL^XBDIQ1(50,DIEN,31)
 .I N]"" S G=DIEN
 Q G
 ;
RD(P,R) ;return release date
  I '$G(R) Q $P($G(^PSRX(P,2)),U,13)
  Q $P($G(^PSRX(P,1,R,0)),U,18)
TESTRD ;
 S FD=3150101 F  S FD=$O(^PSRX("AD",FD)) Q:FD=""  D
 .S RX=0 F  S RX=$O(^PSRX("AD",FD,RX)) Q:RX'=+RX  D
 ..S RF="" F  S RF=$O(^PSRX("AD",FD,RX,RF)) Q:RF'=+RF  D
 ...S X=$$RD(RX,RF)
 ...I X="" W !,FD," ",RX," ",RF
 Q
