CHANGEP(PARAMETER,FILE) ; EP - Modify Pointer Parameter
 NEW (PARAMETER,FILE,DILOCKTM,DISYS,DT,DTIME,DUZ,ENTITY,IO,IOBS,IOF,IOM,ION,IOS,IOSL,IOST,IOT,IOXY,PARAMETER,U,XPARSYS,XQXFLG)
 ;
 D SETBLRVS
 ;
 S HEADER(1)="RPMS Laboratory"
 S HEADER(2)=PARAMETER_" Parameter"
 S HEADER(3)=$$CJ^XLFSTR("Modify Value",IOM)
 ;
 D HEADERDT^BLRGMENU
 ;
 S RESULTI=$$GET^XPAR("ALL",PARAMETER)
 S RESULTE=$$GET1^DIQ(FILE,RESULTI,.01)
 ;
 D ^XBFMK
 S DIR(0)="PO^"_FILE_":MZ"
 S DIR("A")=PARAMETER_" Value"
 S:$L(RESULTE) DIR("B")=RESULTE
 D ^DIR
 ;
 I +$G(DIRUT)!($G(Y)="") D  Q
 . W !!,?4,"Invalid/No Entry/Quit. Routine Ends."
 . D PRESSKEY^BLRGMENU(9)
 ;
 S ANSWERI=+Y
 S ANSWERE=$P(Y,U,2)
 ;
 I ANSWERI=RESULTI D  Q
 . W !!,?4,"Input same as previous entry.  Routine Ends."
 . D PRESSKEY^BLRGMENU(9)
 ;
 D EN^XPAR("SYS",PARAMETER,,ANSWERE,.ERRS)
 ;
 I +$G(ERRS)<1 D
 . S RESULT=$$GET^XPAR("SYS",PARAMETER,1,"E")
 . W !!,?4,"Parameter ",PARAMETER," has been changed.",!!
 . W ?9,"Parameter is currently ",RESULT
 . D PRESSKEY^BLRGMENU(4)
 ;
 D:+$G(ERRS)>0 RPTERR^BLREMERA(.ERRS,PARAMETER)
 ;
 Q
