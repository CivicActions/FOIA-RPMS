 ;
 D SETBLRVS("SHOWDESC")
 S HEADER(1)=$S(DICN=3.8:"Mail Group Description",DICN=8989.51:"Parameter Description")
 S HEADER(2)=WOT
 S HEADER(3)=""
 D HEADERDT^BLRGMENU
 D ^XBFMK
 S DA=+$$FIND1^DIC(DICN,,"O",WOT)
 S DIC=$$GET1^DIQ(1,DICN,"GL")
 S DR=$S(DICN=3.8:2,DICN=8989.51:20)
 D EN^DIQ
 D PRESSKEY^BLRGMENU(4)
