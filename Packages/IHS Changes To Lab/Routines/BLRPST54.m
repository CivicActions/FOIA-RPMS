EEP ; Ersatz EP
 W !!,$C(7),$C(7),$C(7)
 W ?9,$$SHOUTMSG^BLRGMENU("Must use Line Labels to access subroutines",60)
 W !!,$C(7),$C(7),$C(7)
 Q
 ;
 ;
POST ; EP - POST INSTALL
 NEW BLRVERN,CP,CPSTR,PATCHNUM,TAB,TODAY,WOTCNT
 ; 
 D SETEVARS
 ;
 S TODAY=$$DT^XLFDT
 S WOTCNT=$$WOTCNT(BLRVERN)
 S TAB=$J("",5)
 ;
 D BMES^XPDUTL("Post Install of LR*5.2*1054 Begins.")
 ;
 D MAKEMNV2^BLRMNUV2      ; Create and populate BLRMeNU Version 2
 ;
 D ADDVAROP     ; Add Various Options to Various Menus
 ;
 D REMVAROP     ; Remove Various Options from Various Menus
 ;
 D ADDSRLCK     ; Make sure LRSUPER REPORTS has LRSUPER Lock
 ;
 D UPDOPTR      ; Ensure BLR LAB TESTS REF RANGES option uses BLRLTRR2 routine
 ;
 D FORCENO      ; Set ALL incoming XPARS to NO
 ;
 D POST^BLRDESIG     ; Deprecate Lab E-SIG
 ;
 D QUEUPDT^BLRVFOLU  ; Update V LAB and V MICRO files Ordering Locations, if possible.
 D QUEUPDT^BLRVFUID  ; Update V LAB and V MICRO files UIDs, if possible.
 ;
 D MES^XPDUTL("Post Install of LR*5.2*1054 Ends.")
 D MES^XPDUTL("")
 Q
 ;
 ;
ADDVAROP ; EP - Add Various Options to Various Menus
 NEW OKAY,TAB,TAB2,TAB3,WOTOPT
 ;
 S TAB=$J("",5),TAB2=$J("",10),TAB3=$J("",15)
 ;
 D ADDLOINC     ; Update IHS LOINC/UCUM Menu and LRMENU
 ;
 D ADDSUPER     ; Add new options to LRSUPER REPORTS Menu
 ;
 D ADDBLROP     ; Add new options to BLRMENU
 Q
 ;
ADDLOINC ; Update IHS LOINC/UCUM Menu and LRMENU
 Q:$$LKOPT^XPDMENU("IHS LOINC/UCUM MENU")<1   ; Skip if it doesn't exist
 ;
 D BMES^XPDUTL(TAB_"Adding Options to IHS LOINC/UCUM MENU.")
 S WOTOPT="IHS LOINC/UCUM MENU"
 D ADDIT(WOTOPT,"BLRLOINC","LOI",11)
 D ADDIT(WOTOPT,"BLRNOLER","NLO",12)
 D MES^XPDUTL(TAB_"Completed Adding Options to IHS LOINC/UCUM MENU.")
 ;
 ; Add to LRMENU Menu
 D BMES^XPDUTL(TAB_"Adding IHS LOINC/UCUM MENU Option to LRMENU.")
 D ADDIT("LRMENU","IHS LOINC/UCUM MENU","ILUM")
 D MES^XPDUTL(TAB_"Completed Adding Option to LRMENU.")
 Q
 ;
 ;
ADDIT(WOTOPT,OPTADD,SYNONYM,ITEM) ; EP 
 NEW Q
 ;
 S Q=$C(34)
 ;
 S OKAY=$$ADD^XPDMENU(WOTOPT,OPTADD,SYNONYM,$G(ITEM))
 I +OKAY D MES^XPDUTL(TAB2_Q_OPTADD_Q_" Option Added.")  Q
 ;
 ; At this point, there was an error
 D MES^XPDUTL(TAB2_Q_OPTADD_Q_" Option **NOT** Added.")
 D MES^XPDUTL(TAB3_"Error:"_$P(OKAY,U,2))
 Q
 ;
 ;
ADDSUPER ; EP - Add options to LRSUPER REPORTS Menu
 D BMES^XPDUTL(TAB_"Adding PURGE OLD ORDERS & ACCESSION REPORTS Option to LRSUPER REPORTS Menu.")
 D ADDIT("LRSUPER REPORTS","BLRPURGU","PURR")
 D MES^XPDUTL(TAB_"Completed Adding Option to LRSUPER REPORTS Menu.")
 Q
 ;
 ;
ADDBLROP ; Add NEW options to BLRMENU
 NEW OKAY,TAB,TAB2,TAB3,WOTOPT
 ;
 S WOTOPT="BLRMENU",TAB=$J("",5),TAB2=$J("",10),TAB3=$J("",15)
 ;
 D BMES^XPDUTL(TAB_"Adding Options to BLRUMENU.")
 D ADDIT(WOTOPT,"BLR LAB CANCELLED REPORTS","CANR")
 D ADDIT(WOTOPT,"BLR INACTIVATE TEST","INAC")
 D ADDIT(WOTOPT,"BLR F60 INACT DATE REPORT","F60I")
 D ADDIT(WOTOPT,"BLR EDIT LABORATORY TEST FILE","E60")
 D MES^XPDUTL(TAB_"Completed Adding Options to BLRUMENU.")
 D MES^XPDUTL("")
 Q
 ;
 ;
ADDSRLCK     ; EP - Make sure LRSUPER REPORTS has LRSUPER Lock
 NEW FDA,ERRS,IEN
 ;
 S IEN=$$LKOPT^XPDMENU("LRSUPER REPORTS")
 Q:$$GET1^DIQ(19,IEN,"LOCK")="LRSUPER"   ; Skip if already there
 ;
 D ^XBFMK
 S FDA(19,IEN_",","LOCK")="LRSUPER"
 ;
 D UPDATE^DIE("","FDA",,"ERRS")
 ;
 I $D(ERRS)<1 D  Q
 . D BMES^XPDUTL(TAB_"Added LRSUPER key to LRSUPER REPORTS menu.")
 . D MES^XPDUTL("")
 ;
 ; At this point, known error.  Report it.
 D BMES^XPDUTL(TAB_"Did NOT Add LRSUPER key to LRSUPER REPORTS menu.")
 D MES^XPDUTL(TAB_TAB_"Error:"_$G(ERRS("DIERR",1,"TEXT",1)))
 D MES^XPDUTL("")
 Q
 ;
 ;
UPDOPTR ; EP - Ensure BLR LAB TESTS REF RANGES option uses BLRLTRR2 routine
 NEW ERRS,F19IEN,FDA
 ;
 S F19IEN=$$FIND1^DIC(19,,"O","BLR LAB TESTS REF RANGES")
 Q:F19IEN<1
 ;
 S FDA(19,F19IEN_",","ROUTINE")="EP^BLRLTRR2"
 D UPDATE^DIE("S","FDA",,"ERRS")
 I $D(ERRS)<1 D  Q
 . D BMES^XPDUTL(TAB_"Updated BLR LAB TESTS REF RANGES option.")
 . D MES^XPDUTL("")
 ;
 ; At this point, known error.  Report it.
 D BMES^XPDUTL(TAB_"Did NOT update BLR LAB TESTS REF RANGES option.")
 D MES^XPDUTL(TAB_TAB_"Error:"_$G(ERRS("DIERR",1,"TEXT",1)))
 D MES^XPDUTL("")
 Q
 ;
 ;
FORCENO ; EP - Force any new parameters to be NO
 NEW (DEBUG,DILOCKTM,DISYS,DT,DTIME,DUZ,IO,IOBS,IOF,IOM,ION,IOS,IOSL,IOST,IOT,IOXY,U,XPARSYS,XQXFLG)
 ;
 D SETEVARS
 Q:'$$GETPARS(CPSTR,.PARSLIST)
 ;
 I +$G(DEBUG) D  Q      ; If DEBUG, just print messages & quit
 . S TAB=$J("",10)
 . D BLANK,OKAY^BLRKIDSU("DEBUG: FORCENO^"_BLRVERN,4)
 . S PARAMETER=""
 . F  S PARAMETER=$O(PARSLIST(PARAMETER))  Q:PARAMETER=""  D
 .. D MES^XPDUTL(TAB_"DEBUG will *NOT* modify "_PARAMETER_" parameter.")
 ;
 S PARAMETER=""
 F  S PARAMETER=$O(PARSLIST(PARAMETER))  Q:PARAMETER=""  D
 . K ERRS
 . D EN^XPAR("SYS",PARAMETER,,"NO",.ERRS)
 . I +$G(ERRS) D
 .. D BLANK,MES^XPDUTL(PARAMETER_" parameter not modified."),BLANK
 .. D STORERRS(BLRVERN,ERRS,PARAMETER)
 Q
 ;
GETPARS(CPSTR,PARSLIST) ; EP - Function to get Parameters from BUILD file
 NEW (CPSTR,PARSLIST,DILOCKTM,DISYS,DT,DTIME,DUZ,IO,IOBS,IOF,IOM,ION,IOS,IOSL,IOST,IOT,IOXY,U,XPARSYS,XQXFLG)
 ;
 K PARSLIST
 ;
 S BUILDIEN=$$FIND1^DIC(9.6,,,CPSTR)
 Q:BUILDIEN<1 0
 ;
 S PARAMTRS="",CNT=0
 F  S PARAMTRS=$O(^XPD(9.6,BUILDIEN,"KRN",8989.51,"NM","B",PARAMTRS))  Q:PARAMTRS=""  D
 . S PARSLIST(PARAMTRS)="",CNT=CNT+1
 ;
 Q CNT
 ;
 ;
STORERRS(BLRVERN,ERROR,PARAMETER) ; EP - Store Error(s)
 NEW (BLRVERN,ERROR,PARAMETER,DILOCKTM,DISYS,DT,DTIME,DUZ,IO,IOBS,IOF,IOM,ION,IOS,IOSL,IOST,IOT,IOXY,PARAMETER,U,XPARSYS,XQXFLG)
 ;
 S:$D(^XTMP(BLRVERN))<1 XTMP(BLRVERN)="^"_$$DT^XLFDT_"^Install Error(s)"
 S $P(^XTMP(BLRVERN),"^")=$$HTFM^XLFDT(+$H+90)
 ;
 S NOWH=$H
 S ERRTYPE="PARAMETER '"_PARAMETER_"' UPDATE ERROR"
 S ^XTMP(BLRVERN,ERRTYPE,NOWH)=$$HTE^XLFDT(NOWH,"5Z")
 M ^XTMP(BLRVERN,ERRTYPE,NOWH,"01","DUZ")=DUZ
 S ^XTMP(BLRVERN,ERRTYPE,NOWH,"02","ERROR")=ERROR
 ;
 Q
 ;
 ;
REMVAROP ; EP - Remove Various Options from Various Menus
 NEW LINE,LINETAG,OPTCNT,REMOPT,REMOVED,TAB,TAB2,TAB3,WOTOPT
 ;
 S TAB=$J("",5),TAB2=$J("",10),TAB3=$J("",15)
 ;
 D BMES^XPDUTL(TAB_"Begin Removal of OBSOLETE options from Various Menus.")
 D MES^XPDUTL("")
 ;
 S OPTCNT=0
 S LINETAG="OBSOLETE"
 F LINE=1:1  S LINESTR=$T(@LINETAG+LINE)  Q:LINESTR=""!(LINESTR["$$END")  D
 . S WOTOPT=$P(LINESTR,";",3)
 . S REMOPT=$P(LINESTR,";",4)
 . D GETRID(WOTOPT,REMOPT,.OPTCNT)
 ;
 D BMES^XPDUTL(TAB_"End Removal of OBSOLETE options from Various Menus.")
 Q
 ;
GETRID(WOTOPT,REMOPT,OPTCNT) ; EP - Actual option manipulation
 Q:$$LKOPT^XPDMENU(WOTOPT)<1    ; Skip if WOTOPT does not exist
 Q:$$ITEMLKUP(WOTOPT,REMOPT)<1  ; Skip if REMOPT not an item under WOTOPT
 ;
 S REMOVED=$$DELETE^XPDMENU(WOTOPT,REMOPT)
 D MES^XPDUTL(TAB2_REMOPT_$S(REMOVED:"",1:" not")_" removed from "_WOTOPT_".")
 S OPTCNT=OPTCNT+1
 Q
 ;
ITEMLKUP(WOTOPT,LKUPOPT)  ; EP - Determine if LKUPOPT is an item under WOTOPT
 NEW WOTIEN,LKUPIEN
 ;
 S WOTIEN=$$LKOPT^XPDMENU(WOTOPT)  Q:WOTIEN<1 0
 S LKUPIEN=$$LKOPT^XPDMENU(LKUPOPT) Q:LKUPIEN<1 0
 ;
 Q $S($D(^DIC(19,WOTIEN,10,"B",LKUPIEN)):1,1:0)
 ;
