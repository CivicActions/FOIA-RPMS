 ;;5.2;IHS LABORATORY;**1054**;NOV 01, 1997;Build 20
 ;
 ; IHS/MSC/MKK - LR*5.2*1054 - 19-May-2022 - Item 95356 - Send UID to V Files
 ;
SETLRUID ; EP - Setup UID to go over to PCC
 NEW LRAA,LRAD,LRAN,LRAS,LRDFN,LRIDT,LRSS,LRUID
 ;
 S LRAS=$G(APCDALVR("APCDTACC"))
 Q:$L(LRAS)<1      ; Skip if no Accession
 ;
 S X=$$GETACCCP^BLRUTIL3(LRAS,.LRAA,.LRAD,.LRAN)
 Q:X<1   ; Skip if function not successful
 ;
 S LRSS=$P($G(^LRO(68,+LRAA,0)),U,2)
 Q:LRSS=""    ; Skip if no LR SUSCRIPT
 ;
 S LRDFN=+$G(^LRO(68,LRAA,1,LRAD,1,LRAN,0))
 S LRIDT=+$P($G(^LRO(68,LRAA,1,LRAD,1,LRAN,3)),U,5)
 S LRUID=$P($G(^LR(LRDFN,LRSS,LRIDT,"ORU")),U)
 ;
 S:$L(LRUID) APCDALVR("APCDTUID")=LRUID
 Q
 ;
RESETCDT  ; Complete Date Setup.  Moved from BLRLINK3
 NEW BLRLRAA,BLRLRAD,BLRLRAN,BLRLRDFN,BLRLRIDT,ERRS,F60DATAN,F60DATAD,FDA,LRCOMPDT,LRUID
 ;
 Q:+$G(BLRCOMDT)<1  ; Skip if no complete date
 ;
 S LRCOMPDT=0
 ;
 I +$G(LRDFN),+$G(LRIDT) D
 . S LRCOMPDT=$P($G(^LR(LRDFN,BLRSS,LRIDT,0)),U,3)  ; DATE REPORT COMPLETED.  Both "CH" & "MI"
 . I BLRSS="CH",$$ISPANEL^BLRPOC(BLRTLAB)<1  D   ; If Chemistry & Atomic test, get test result date
 .. S F60DATAN=$G(^LAB(60,BLRTLAB,.2))      ; DATANAME
 .. S F60DATAD=$P($G(^LR(LRDFN,"CH",LRIDT,+F60DATAN)),U,6)  ;  Check Result Date at Test Level
 .. S:F60DATAD LRCOMPDT=F60DATAD
 ;
 I LRCOMPDT<1,'+$G(LRDFN),'+$G(LRIDT),+$G(BLRAA),+$G(BLRAD),+$G(BLRAN) D
 . S BLRLRDFN=+$G(^LRO(68,BLRAA,1,BLRAD,1,BLRAN,0))
 . S BLRLRIDT=$P($G(^LRO(68,BLRAA,1,BLRAD,1,BLRAN,3)),U,5)
 . S LRCOMPDT=$P($G(^LR(BLRLRDFN,BLRSS,BLRLRIDT,0)),U,3)  ; DATE REPORT COMPLETED.  Both "CH" & "MI"
 . I BLRSS="CH",$$ISPANEL^BLRPOC(BLRTLAB)<1  D
 .. S F60DATAN=$G(^LAB(60,BLRTLAB,.2))      ; DATANAME
 .. S F60DATAD=$P($G(^LR(BLRLRDFN,"CH",BLRLRIDT,+F60DATAN)),U,6)  ; If Chemistry, check Result Date at Test Level
 .. S:F60DATAD LRCOMPDT=F60DATAD
 ;
 I LRCOMPDT D
 . I BLRSS="MI" S APCDALVR("APCDTCMD")=LRCOMPDT
 . E  S APCDALVR("APCDTRDT")=LRCOMPDT
 ;
 I LRCOMPDT<1,BLRCOMDT>DT D
 . I BLRSS="MI" S APCDALVR("APCDTCMD")=BLRCOMDT
 . E  S APCDALVR("APCDTRDT")=BLRCOMDT
 ;
 ; If Lab Data File Complete Date greater, change 9009022 Complete Date to match.
 I LRCOMPDT,LRCOMPDT>BLRCOMDT D
 . D ^XBFMK
 . S FDA(9009022,BLRLOGDA_",","COMPLETE DATE")=LRCOMPDT
 . D UPDATE^DIE("S","FDA",,"ERRS")
 ;
 ; Make sure LAB DATA's MICROBIOLOGY node has complete date in DATE REPORT COMPLETED field
 Q:BLRSS'="MI"!(BLRCOMDT<1)  ; Skip if not MICRO accession OR there is no Complete Date
 ;
 S BLRLRDFN=$G(LRDFN),BLRLRIDT=$G(LRIDT)
 I $L($G(BLRACC)),+BLRLRDFN<1!(+BLRLRIDT<1) D
 . Q:$$GETACCCP^BLRUTIL3(BLRACC,.BLRLRAA,.BLRLRAD,.BLRLRAN)<1
 . S BLRLRDFN=$P($G(^LRO(68,+BLRLRAA,1,+BLRLRAD,1,+BLRLRAN,0)),U),BLRLRIDT=$P($G(^(3)),U,5)
 ;
 Q:BLRLRDFN<1!(BLRLRIDT<1)   ; Skip if variables not set
 ;
 Q:$P($G(^LR(BLRLRDFN,"MI",BLRLRIDT,0)),U,3)=BLRCOMDT  ; Skip if Complete Date already set
 ;
 I +$G(LRCOMPDT)<1,BLRCOMDT<1 Q   ; Skip if date variables not set
 ;
 D ^XBFMK
 K FDA,ERRS
 S FDA(63.05,BLRLRIDT_","_BLRLRDFN_",",.03)=$S(+$G(LRCOMPDT)>BLRCOMDT:LRCOMPDT,1:BLRCOMDT)
 D UPDATE^DIE("S","FDA",,"ERRS")
 Q
 ;
