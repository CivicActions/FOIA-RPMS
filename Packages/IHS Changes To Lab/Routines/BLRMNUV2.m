ADDIT(WOTOPT,ADDOPT,SYNONYM,ORDER,OKCNT) ; EP - Add Options
 S:$L($G(ORDER)) ORDER=ORDER+1
 S X=$$ADD^XPDMENU(WOTOPT,ADDOPT,SYNONYM,$G(ORDER))
 S OKCNT=OKCNT+X
 Q:X
