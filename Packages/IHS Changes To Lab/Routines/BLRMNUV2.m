PURGMENU(OPTNAME) ; EP - Purge (i.e., clear out all ITEMS) Option Menu
 NEW (OPTNAME,DILOCKTM,DISYS,DT,DTIME,DUZ,IO,IOBS,IOF,IOM,ION,IOS,IOSL,IOST,IOT,IOXY,U,XPARSYS,XQXFLG)
 ;
 S OPTIEN=$$LKOPT^XPDMENU(OPTNAME)
 S (CNT,CNTDEL,ITEM)=0
 F  S ITEM=$O(^DIC(19,OPTIEN,10,ITEM))  Q:ITEM<1  D
 . S SUBMENU=$$GET1^DIQ(19.01,ITEM_","_OPTIEN,"ITEM")
 . S CNT=CNT+1
 . I $$DELETE^XPDMENU(OPTNAME,SUBMENU) S CNTDEL=CNTDEL+1
 ;
 Q $S(CNT=CNTDEL:1,1:0)
 ;
