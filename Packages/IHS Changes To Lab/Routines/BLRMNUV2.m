REDOMENU ; EP - Modified BLRMENU V2 Menus
 NEW (DILOCKTM,DISYS,DT,DTIME,DUZ,IO,IOBS,IOF,IOM,ION,IOS,IOSL,IOST,IOT,IOXY,U,XPARSYS,XQXFLG)
 ;
 D:$G(IOST)="" HOME^%ZIS
 ;
 S ONGO="YES"
 F  Q:ONGO="NO"  D
 . K HEADER
 . S HEADER(1)="Modify Option File"
 . D HEADERDT^BLRGMENU
 . ;
 . S DIR(0)="SO^1:;2:;3:;4:;5:;6:;6:;7:;Exit"
 . S DIR("L",1)="Select a Menu:"
 . S DIR("L",2)=""
 . S DIR("L",3)="     1) BLRMENU V2"
 . S DIR("L",4)="     2) BLR USEFUL OPTIONS"
 . S DIR("L",5)="     3) BLRMENU V2 REPORTS"
 . S DIR("L",6)="     4) BLR PARAMETERS"
 . S DIR("L",7)="     5) BLR ERRORS"
 . S DIR("L",8)="     6) BLRMENU V2 LOINC/UCUM"
 . S DIR("L",9)="     7) EXIT"
 . S DIR("L")=""
 . D ^DIR
 . I +Y<1!(Y=12) S ONGO="NO"  Q
 . ;
 . W !!
 . ;
 . ; Could not use $S due to line length being too long.
 . S OPTNAME="EXIT"
 . S:Y=1 OPTNAME="BLRMENU V2"
 . S:Y=2 OPTNAME="BLR USEFUL OPTIONS"
 . S:Y=3 OPTNAME="BLRMENU V2 REPORTS"
 . S:Y=4 OPTNAME="BLR PARAMETERS"
 . S:Y=5 OPTNAME="BLR ERRORS"
 . S:Y=6 OPTNAME="BLRMENU V2 LOINC/UCUM"
 . S:Y=7 OPTNAME="EXIT"
 . ;
 . I OPTNAME["EXIT" S ONGO="NO"  Q
 . ;
 . S OPTIEN=$$FIND1^DIC(19,,"O",OPTNAME)
 . ;
 . I $$GET1^DIQ(19,OPTIEN,"TYPE","I")'="M" D  Q
 .. W !!,"OPTION ",OPTNAME," Not a MENU type."
 .. D PRESSKEY^BLRGMENU(4)
 . ;
 . S HEADER(2)="OPTION: "_OPTNAME
 . D HEADERDT^BLRGMENU
 . ;
 . D ^XBFMK
 . S DIR(0)="SO^1:List;2:Delete;3:Exit"
 . S DIR("L",1)="Select the Action:"
 . S DIR("L",2)=""
 . S DIR("L",3)="     1) List"
 . S DIR("L",4)="     2) Clear"
 . S DIR("L",5)="     3) Recreate"
 . S DIR("L",6)="     4) Exit"
 . S DIR("L")=""
 . D ^DIR
 . I +Y<1!(Y=4) Q
 . ;
 . S ACTION=Y
 . S WHATDO=$S(ACTION=1:"LIST",ACTION=2:"CLEAR",ACTION=3:"RECREATE",1:"<NOT SURE>")
 . S HEADER(3)=$$CJ^XLFSTR(WHATDO,IOM)
 . S HEADER(4)=" "
 . S $E(HEADER(5),7)="NAME"
 . S $E(HEADER(5),38)="MENU TEXT"
 . D HEADERDT^BLRGMENU
 . ;
 . I ACTION=3 D RECREATE  Q
 . ;
 . S ITEM=0
 . F  S ITEM=$O(^DIC(19,OPTIEN,10,ITEM))  Q:ITEM<1  D
 .. S SUBMENU=$$GET1^DIQ(19.01,ITEM_","_OPTIEN,"ITEM")
 .. S F19IEN=$$FIND1^DIC(19,,"O",SUBMENU)
 .. S SUBMTEXT=$$GET1^DIQ(19,F19IEN,"MENU TEXT")
 .. W $J(ITEM,4),?6,$E(SUBMENU,1,30),?37,$E(SUBMTEXT,1,$S(ACTION=1:43,1:30))
 .. I ACTION=1 W !  Q
 .. ;
 .. W ?69,$S($$DELETE^XPDMENU(OPTNAME,SUBMENU):"REMOVED",1:"NOT REMOVED"),!
 . D PRESSKEY^BLRGMENU(9)
 ;
 W !!
 Q
