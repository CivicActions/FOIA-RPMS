ROLLUP ; EP 
 NEW F61P2PTR,ORG,ORGSNO,ROLLSTR,ROLLUP,VFILENUM,VMIEN
 ;
 S VFILENUM=$G(BLRVFILE)
 Q:VFILENUM'=9000010.25      ; Skip if not V MICRO file
 ;
 S VMIEN=$G(BLRVIEN)
 Q:VMIEN<1                   ; Skip if no V MICRO IEN.
 ;
 S ORG=$$GET1^DIQ(9000010.25,VMIEN,"ORGANISM")
 S F61P2PTR=$$GET1^DIQ(9000010.25,VMIEN,"ORGANISM","I")
 S ORGSNO=$$GET1^DIQ(61.2,F61P2PTR,"SNOMED")
 S ROLLUP=$$GET1^DIQ(90476,ORGSNO,"F6 MAPPING","I")
 Q:ROLLUP<1   ; Skip if no Rollup
 ;
 S ROLLSTR="SNOMED "_ORGSNO_" ROLLUP = "_ROLLUP
 I $L($G(APCDALVR("APCDTLC1")))<1 S APCDALVR("APCDTLC1")=ROLLSTR  Q
 I $L($G(APCDALVR("APCDTLC2")))<1 S APCDALVR("APCDTLC2")=ROLLSTR  Q
 I $L($G(APCDALVR("APCDTLC3")))<1 S APCDALVR("APCDTLC3")=ROLLSTR
 Q
