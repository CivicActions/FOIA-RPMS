ECODES ;
 K DIC,X
 F ABMI=1:1 S ABMLN=$P($T(ECODETXT+ABMI),";;",2) Q:ABMLN="END"  D
 .S ABMCODE=$P(ABMLN,U)
 .I $D(^ABMDCODE("AC",$P(ABMLN,U,2),ABMCODE)) D  Q
 ..S DA=$O(^ABMDCODE("AC",$P(ABMLN,U,2),ABMCODE,0))
 ..S $P(^ABMDCODE(DA,0),U,2)=$P(ABMLN,U,2),$P(^(0),U,3)=$P(ABMLN,U,3)
 ..I "^84^86^"[("^"_ABMCODE_"^") S $P(^ABMDCODE(DA,0),U,4)=1  ;inactivates these two codes
 ..E  S $P(^ABMDCODE(DA,0),U,4)=0  ;activates code
 ..D BMES^XPDUTL("Updated code "_ABMCODE_" "_$P(ABMLN,U,2)_" "_$P(ABMLN,U,3)_$S("^84^86^"[("^"_ABMCODE_"^"):" (inactive)",1:""))
 .S ABMDESC=$P(ABMLN,U,3)
 .S DIC="^ABMDCODE("
 .S DIC(0)="ML"
 .S X=ABMCODE
 .S DIC("DR")=".02///"_$P(ABMLN,U,2)
 .S DIC("DR")=DIC("DR")_";.03///"_ABMDESC
 .I "^84^86^"[("^"_ABMCODE_"^") S DIC("DR")=DIC("DR")_";.04///"_1  ;inactive codes
 .K DD,DO
 .D FILE^DICN
 .I Y>0 D BMES^XPDUTL("Added code "_ABMCODE_" "_$P(ABMLN,U,2)_" "_$P(ABMLN,U,3)_$S("^84^86^"[("^"_ABMCODE_"^"):" (inactive)",1:""))
 Q
