ABMBLRX1 ; IHS/SD/SDR - 3PB Pharmacy POS Bill Cleanup Report  
 ;;2.6;IHS Third Party Billing System;**36,37**;NOV 12, 2009;Build 739
 ;
 ;IHS/SD/SDR 2.6*36 ADO74860 New routine; this is the BRIEF report for the BLRX option
 ;IHS/SD/SDR 2.6*37 ADO81491 Updated to display PPN preferred name if parameter is on
 ;**************************
 K DIC,DIR,ABMY
 S U="^"
 S ABMY("RFOR")="1",ABMY("RFOR","NM")="Brief"
 S ABMY("DT")="A"
 S X1=DT
 S X2=-30
 D C^%DTC
 S ABMY("DT",1)=X
 S ABMY("DT",2)=DT
 D PARENT  ;make sure ABM("PAR") is set for report
SEL D LOOP Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 S ABM("HD",0)="PHARMACY POS 3P BILL CLEANUP REPORT"
 S ABM("HD",1)="GENERATED BY: "_$P($G(^VA(200,DUZ,0)),U)
 S ABM("HD",2)="   for "_$S(ABMY("DT")="A":"Approval",1:"Visit")_" Dates "_$$SDT^ABMDUTL(ABMY("DT",1))_" to "_$$SDT^ABMDUTL(ABMY("DT",2))
 I $D(ABMY("TYP")) S ABM("HD",3)="   for "_ABMY("TYP","NM")
 I $D(ABMY("INS")) S ABM("HD",3)="   for "_$P(^AUTNINS(ABMY("INS"),0),U)
 I $D(ABMY("PAT")) S ABM("HD",3)="   for "_$P(^DPT(ABMY("PAT"),0),U)
 S ABM("LVL")=0
 S ABM("PG")=0
 ;
 S ABMQ("RC")="COMPUTE^ABMBLRXC"
 S ABMQ("RX")="POUT^ABMDRUTL"
 S ABMQ("NS")="ABM"
 S ABMQ("RP")="PRINT^ABMBLRX"_ABMY("RFOR")
 D ^ABMDRDBQ
 K ^TMP("ABM-BLRX",$J)
 Q
LOOP ;
 ; Display current exclusion parameters
 G XIT:$D(DUOUT)!$D(DTOUT)!$D(DIROUT)
 W !!?3,"EXCLUSION PARAMETERS Currently in Effect for RESTRICTING the EXPORT to:",!?3,"======================================================================="
 W !?3,"- Visit Location.....: "
 I $D(ABMY("LOC")) D
 .S ABMI=0
 .F  S ABMI=$O(ABMY("LOC",ABMI)) Q:+ABMI=0  W !?12,$P(^DIC(4,ABMI,0),U)
 E  W "ALL"
 I $D(ABMY("TYP")) W !?3,"- Billing Entity.....: ",ABMY("TYP","NM")
 I $D(ABMY("INS")) W !?3,"- Billing Entity.....: ",$P(^AUTNINS(ABMY("INS"),0),U)
 ;I $D(ABMY("PAT")) W !?3,"- Billing Entity.....: ",$P(^DPT(ABMY("PAT"),0),U)  ;abm*2.6*37 IHS/SD/SDR ADO81491
 I $D(ABMY("PAT")) W !?3,"- Billing Entity.....: ",$P(^DPT(ABMY("PAT"),0),U) I $$GETPREF^AUPNSOGI(ABMY("PAT"),"I",1)'="" W " - ",$$GETPREF^AUPNSOGI(ABMY("PAT"),"I",1)_"*"  ;abm*2.6*37 IHS/SD/SDR ADO81491
 I $D(ABMY("DT")) D
 .W !?3,"- "
 .I ABMY("DT")="A" W "Approval Dates from: "
 .I ABMY("DT")="V" W "Visit Dates from...: "
 .W $$SDT^ABMDUTL(ABMY("DT",1))_" to: "_$$SDT^ABMDUTL(ABMY("DT",2))
 I $G(ABMY("RFOR")) W !?3,"- Report Type........: ",ABMY("RFOR","NM")_" Listing"
 ;
PARM ;
 ; Choose additional exclusion parameters
 K DIR
 S DIR(0)="SO^1:VISIT LOCATION;2:BILLING ENTITY;3:DATE RANGE;4:REPORT TYPE"
 S DIR("A")="Select ONE or MORE of the above EXCLUSION PARAMETERS"
 S DIR("?")="The report can be restricted to one or more of the listed parameters. A parameter can be removed by reselecting it and making a null entry."
 D ^DIR
 K DIR
 G XIT:$D(DIRUT)!$D(DIROUT)
 I Y=1!(Y=2)!(Y=3)!(Y=4) D @($S(Y=4:"RFOR",Y=1:"LOC",Y=2:"TYP",1:"DT")) G LOOP
 Q
INS ;
 W !!?5,"You can RESTRICT the REPORT to either a SPECIFIC INSURER or",!?5,"else a TYPE of INSURER (i.e. PRIVATE INSURANCE, MEDICAID...).",!
 S DIR(0)="Y"
 S DIR("A")="Restrict Report to a SPECIFIC INSURER (Y/N)"
 S DIR("B")="N"
 D ^DIR
 G XIT:$D(DIRUT)
 D @($S(Y=1:"INS2",1:"TYP")_"^ABMBLRX1")
 I '$D(DTOUT)!'$D(DUOUT)!'$D(DIROUT) G LOOP
 Q
INS2 ;EP
 K ABMY("TYP"),ABMY("INS")
 W !
 S DIC="^AUTNINS("
 S DIC(0)="QEAM"
 D ^DIC
 Q:+Y<0
 S ABMY("INS")=+Y
 Q
PAT ;
 K ABMY("TYP"),ABMY("PAT")
 W !
 S DIC="^AUPNPAT("
 S DIC(0)="QEAM"
 D ^DIC
 K AUPNLK("ALL")
 Q:+Y<0
 S ABMY("PAT")=+Y
 Q
 ;
RFOR ;EP
 K DIR
 S DIR(0)="SO^1:BRIEF LISTING (80 Width);2:STATISTICAL SUMMARY ONLY"
 S DIR("A")="Select TYPE of Output"
 D ^DIR
 K DIR
 Q:$D(DIRUT)
 S ABMY("RFOR")=Y
 S ABMY("RFOR","NM")=Y(0)
 Q
PARENT ;EP
 S ABMSAT=DUZ(2)
 S ABM("PAR")=0  ;Parent
 ; check site active at DOS to ensure bill added to correct site
 S DA=0
 F  S DA=$O(^BAR(90052.06,DA)) Q:DA'>0  D  Q:ABM("PAR")
 .Q:'$D(^BAR(90052.06,DA,DA))  ; Pos Parent UNDEF Site Parameter
 .Q:'$D(^BAR(90052.05,DA,ABMSAT))  ; Satellite UNDEF Parent/Satellit
 .Q:+$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,5)  ; Par/Sat not usable
 .; Q if sat NOT active at DOS
 .I DT<$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,6) Q
 .; Q if sat became NOT active before DOS
 .I $P($G(^BAR(90052.05,DA,ABMSAT,0)),U,7),(DT>$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,7)) Q
 .S ABM("PAR")=$S(ABMSAT:$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,3),1:"")
 I +ABM("PAR")=0 S ABM("PAR")=DUZ(2)
 Q
LOC ;EP
 W ! K DIC,ABMY("LOC")
 S DIC="^BAR(90052.05,"_ABM("PAR")_","
 S DIC(0)="AEMQ"
 S DIC("A")="Select LOCATION: ALL// "
 F  D  Q:+Y<0
 .I $D(ABMY("LOC")) S DIC("A")="Select Another Location: "
 .D ^DIC
 .Q:+Y<0
 .S ABMY("LOC",+Y)=""
 I '$D(ABMY("LOC")) D
 .I $D(DUOUT) K ABMY("SORT") Q
 .W "ALL"
 K DIC
 Q
DT ;EP
 S DIR(0)="SO^1:Approval Date;2:Visit Date"
 S DIR("A")="Select TYPE of DATE Desired"
 D ^DIR
 Q:$D(DIROUT)!$D(DIRUT)
 ;
 K DIR
 Q:$D(DIRUT)
 S ABMY("DT")=$S(Y=1:"A",1:"V")
 S Y=$S(Y=1:"APPROVAL",1:"VISIT")_" DATE"
 W !!," ============ Entry of ",Y," Range =============",!
 S DIR("A")="Enter STARTING "_Y_" for the Report"
 S DIR(0)="DO^::EP"
 D ^DIR
 G DT:$D(DIRUT)
 S ABMY("DT",1)=Y
 W !
 S DIR("A")="Enter ENDING DATE for the Report"
 D ^DIR
 K DIR
 G DT:$D(DIRUT)
 S ABMY("DT",2)=Y
 I ABMY("DT",1)>ABMY("DT",2) W !!,*7,"INPUT ERROR: Start Date is Greater than than the End Date, TRY AGAIN!",!! G DT
 Q
TYP ;EP
 K DIR,ABMY("TYP"),ABMY("INS"),ABMY("PAT")
 S DIR(0)="SO^1:MEDICARE;2:MEDICAID;3:PRIVATE INSURANCE;4:NON-BENEFICIARY PATIENTS;5:BENEFICIARY PATIENTS;6:SPECIFIC INSURER;7:SPECIFIC PATIENT;8:WORKMEN'S COMP;9:PRIVATE + WORKMEN'S COMP;10:CHIP;11:VETERANS MEDICAL BENEFIT"
 S DIR(0)=DIR(0)_";12:3P LIABILITY;13:FPL 133 PERCENT;14:GUARANTOR;15:MEDICARE HMO;16:STATE EXCHANGE PLAN;17:TRIBAL SELF INSURED"
 S DIR("A")="Select TYPE of BILLING ENTITY to Display"
 D ^DIR
 K DIR
 Q:$D(DIRUT)!$D(DIROUT)
 I Y=1 S ABMY("TYP")="^R^MMC^MC^MD^"
 I Y=2 S ABMY("TYP")="^D^"
 I Y=3 S ABMY("TYP")="^P^H^F^C^M^"
 I Y=4 S ABMY("TYP")="^N^"
 I Y=5 S ABMY("TYP")="^I^"
 I Y=8 S ABMY("TYP")="^W^"
 I Y=9 S ABMY("TYP")="^P^H^F^M^W^"
 I Y=10 S ABMY("TYP")="^K^"
 I Y=11 S ABMY("TYP")="^V^"
 I Y=12 S ABMY("TYP")="^T^"
 I Y=13 S ABMY("TYP")="^FPL^"
 I Y=14 S ABMY("TYP")="^G^"
 I Y=15 S ABMY("TYP")="^MH^"
 I Y=16 S ABMY("TYP")="^SEP^"
 I Y=17 S ABMY("TYP")="^TSI^"
 S:$G(ABMY("TYP"))="" ABMY("TYP")=Y
 S ABMY("TYP","NM")=Y(0)
 ;
 I Y'=6,Y'=7 Q  ;Only want specific insurer or patient
 D CK ; Check for date range
 Q:$D(DIRUT)!$D(DIROUT)
 G INS:ABMY("TYP","NM")["INS",PAT:ABMY("TYP","NM")["PAT" ;Y has changed
 Q
 ;
CK I $D(ABMY("DT",2)) Q  ;Already has dates set
 D DT ; Go get date range
 I '$D(ABMY("DT",2)) S DIROUT=1 ; Set quit if date not set
 Q
XIT ;
 K ABMY("I"),ABMY("X"),DIR
 Q
PRINT ;EP
 S ABM("HD",0)="Brief Listing of Pharmacy POS BILLS W/O Medication"
 S ABM("HD",1)="GENERATED BY: "_$P($G(^VA(200,DUZ,0)),U)
 S ABM("HD",2)="   for "_$S(ABMY("DT")="A":"Approval",1:"Visit")_" Dates "_$$SDT^ABMDUTL(ABMY("DT",1))_" to "_$$SDT^ABMDUTL(ABMY("DT",2))
 I $D(ABMY("TYP")) S ABM("HD",3)="   for "_ABMY("TYP","NM")
 I $D(ABMY("INS")) S ABM("HD",3)="   for "_$P(^AUTNINS(ABMY("INS"),0),U)
 I $D(ABMY("PAT")) S ABM("HD",3)="   for "_$P(^DPT(ABMY("PAT"),0),U)
 ;
 D HDB
 S ABMLOC=""
 S ABMCNT=0
 F  S ABMLOC=$O(^TMP("ABM-BLRX",$J,"D",ABMLOC)) Q:$G(ABMLOC)=""  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 .I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  W !," (cont)"
 .I ABMCNT'=0 W !
 .S ABMCNT=ABMCNT+1
 .W !?3,"Visit Location: "_ABMLOC
 .S ABMINS=""
 .F  S ABMINS=$O(^TMP("ABM-BLRX",$J,"D",ABMLOC,ABMINS)) Q:$G(ABMINS)=""  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 ..S ABMBILL=""
 ..F  S ABMBILL=$O(^TMP("ABM-BLRX",$J,"D",ABMLOC,ABMINS,ABMBILL)) Q:$G(ABMBILL)=""  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 ...S ABMREC=$G(^TMP("ABM-BLRX",$J,"D",ABMLOC,ABMINS,ABMBILL))
 ...S ABMHRN=$P(ABMREC,U)
 ...S ABMVDT=$P(ABMREC,U,2)
 ...S ABMOTHI=$P(ABMREC,U,3)
 ...S ABMBAMT=$P(ABMREC,U,4)
 ...W !,$E(ABMINS,1,18)  ;insurer
 ...W ?20,ABMBILL  ;bill#
 ...W ?30,ABMHRN  ;HRN
 ...W ?37,ABMVDT  ;visit date
 ...W ?49,ABMOTHI  ;other bill identifier
 ...W ?70,ABMBAMT  ;bill amount
 ..W !,?47,"----------",?63,"-----------------"
 ..W !?3,$E(ABMINS,1,23)_" subtotal: ",?47,$J($P(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC,ABMINS),U),"10R"),?63,$J($FN($P(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC,ABMINS),U,2),",",2),17),!
 .W !,?47,"----------",?63,"-----------------"
 .W !?3,$E(ABMLOC,1,23)_" subtotal: ",?47,$J($P(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC),U),"10R"),?63,$J($FN($P(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC),U,2),",",2),17)
 W !,?47,"==========",?63,"================="
 W !,"Total: ",?47,$J(+$P($G(^TMP("ABM-BLRX",$J,"TOT")),U),"10R"),?63,$J($FN(+$P($G(^TMP("ABM-BLRX",$J,"TOT")),U,2),",",2),17),!
 W !!,"<END OF REPORT>"
 Q
HD D PAZ^ABMDRUTL Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
HDB ;
 S ABM("PG")=ABM("PG")+1
 D WHD
 W !?22,"Bill",?39,"Visit",?49,"Other Bill",?70,"Billed"
 W !?6,"Insurer",?21,"Number",?32,"HRN",?39,"Date",?49,"Identifier",?70,"Amount"
 W !,ABM("-LINE")
 Q
WHD ;EP for writing Report Header
 W $$EN^ABMVDF("IOF"),!
 I $D(ABM("PRIVACY")) W ?($S($D(ABM(132)):34,1:8)),"WARNING: Confidential Patient Information, Privacy Act Applies",!
 K ABM("=LINE") S $P(ABM("=LINE"),"=",$S($D(ABM(132)):132,1:80))="" W ABM("=LINE"),!
 K ABM("-LINE") S $P(ABM("-LINE"),"-",$S($D(ABM(132)):132,1:80))=""
 D NOW^%DTC
 W ABM("HD",0),?$S($D(ABM(132)):103,1:48) S Y=% X ^DD("DD") W Y,"   Page ",ABM("PG")
 W:$G(ABM("HD",1))]"" !,ABM("HD",1)
 W:$G(ABM("HD",2))]"" !,ABM("HD",2)
 W:$G(ABM("HD",3))]"" !,ABM("HD",3)
 ;
 W !,"Parent Location: ",$P($G(^AUTTLOC(ABM("PAR"),0)),U,2)
 W !,"For Visit Locations: "
 S ABMI=0
 S ABMU("TXT")=""
 F  S ABMI=$O(ABMY("LOC",ABMI)) Q:'ABMI  D
 .I ABMU("TXT")="" S ABMU("TXT")=$P($G(^AUTTLOC(ABMI,0)),U,2)
 .E  S ABMU("TXT")=ABMU("TXT")_", "_$P($G(^AUTTLOC(ABMI,0)),U,2)
 S ABMU("LM")=3,ABMU("RM")=78
 D ^ABMDWRAP
 W !,ABM("=LINE") K ABM("=LINE")
 Q
