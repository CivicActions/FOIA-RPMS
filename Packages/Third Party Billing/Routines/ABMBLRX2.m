ABMBLRX2 ; IHS/SD/SDR - 3PB Pharmacy POS Bill Cleanup Report  
 ;;2.6;IHS Third Party Billing System;**36**;NOV 12, 2009;Build 698
 ;
 ;IHS/SD/SDR 2.6*36 ADO74860 New routine; this is the parameters and summary report for the BLRX option
 ;**************************
 ;
PRINT ;EP
 S ABM("HD",0)="Summary of Pharmacy POS BILLS W/O Medication"
 S ABM("HD",1)="GENERATED BY: "_$P($G(^VA(200,DUZ,0)),U)
 S ABM("HD",2)="   for "_$S(ABMY("DT")="A":"Approval",1:"Visit")_" Dates "_$$SDT^ABMDUTL(ABMY("DT",1))_" to "_$$SDT^ABMDUTL(ABMY("DT",2))
 D HDB
 S ABMCNT=0
 S ABMLOC=""
 F  S ABMLOC=$O(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC)) Q:$G(ABMLOC)=""  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 .I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  W !," (cont)"
 .I ABMCNT'=0 W !
 .S ABMCNT=ABMCNT+1
 .W !,"Visit Location: "_ABMLOC  ;location name
 .S ABMINS=""
 .F  S ABMINS=$O(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC,ABMINS)) Q:$G(ABMINS)=""  D  Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 ..I $Y>(IOSL-5) D HD Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)  W !," (cont)"
 ..W !?3,$E(ABMINS,1,32)  ;insurer name
 ..W ?37,$J($P($G(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC,ABMINS)),U),"10R")  ;number bills
 ..W ?62,$J($FN($P($G(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC,ABMINS)),U,2),",",2),17)  ;bill amount
 .W !,?37,"----------",?62,"------------------"
 .W !,$E(ABMLOC,1,26)_" subtotal: ",?37,$J($P(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC),U),"10R"),?62,$J($FN($P(^TMP("ABM-BLRX",$J,"SUBT",ABMLOC),U,2),",",2),17)
 W !,?37,"==========",?62,"================="
 W !,"Total: ",?37,$J(+$P($G(^TMP("ABM-BLRX",$J,"TOT")),U),"10R"),?62,$J($FN(+$P($G(^TMP("ABM-BLRX",$J,"TOT")),U,2),",",2),17),!
 W !!,"<END OF REPORT>"
 Q
HD D PAZ^ABMDRUTL Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
HDB ;
 S ABM("PG")=ABM("PG")+1
 D WHD
 W !?67,"Billed"
 W !?5,"Insurer",?38,"# Bills",?67,"Amount"
 W !,ABM("-LINE")
 Q
WHD ;EP for writing Report Header
 W $$EN^ABMVDF("IOF"),!
 I $D(ABM("PRIVACY")) W ?($S($D(ABM(132)):34,1:8)),"WARNING: Confidential Patient Information, Privacy Act Applies",!
 K ABM("=LINE") S $P(ABM("=LINE"),"=",$S($D(ABM(132)):132,1:80))="" W ABM("=LINE"),!
 K ABM("-LINE") S $P(ABM("-LINE"),"-",$S($D(ABM(132)):132,1:80))=""
 D NOW^%DTC
 W ABM("HD",0),?$S($D(ABM(132)):103,1:48) S Y=% X ^DD("DD") W Y,"   Page ",ABM("PG")
 W:$G(ABM("HD",1))]"" !,ABM("HD",1)
 W:$G(ABM("HD",2))]"" !,ABM("HD",2)
 W:$G(ABM("HD",3))]"" !,ABM("HD",3)
 ;
 W !,"Parent Location: ",$P($G(^AUTTLOC(ABM("PAR"),0)),U,2)
 W !,"For Visit Locations: "
 S ABMI=0
 S ABMU("TXT")=""
 F  S ABMI=$O(ABMY("LOC",ABMI)) Q:'ABMI  D
 .I ABMU("TXT")="" S ABMU("TXT")=$P($G(^AUTTLOC(ABMI,0)),U,2)
 .E  S ABMU("TXT")=ABMU("TXT")_", "_$P($G(^AUTTLOC(ABMI,0)),U,2)
 S ABMU("LM")=3,ABMU("RM")=78
 D ^ABMDWRAP
 W !,ABM("=LINE") K ABM("=LINE")
 Q
