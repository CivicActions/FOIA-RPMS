ABMSTAT ; IHS/SD/TPF - SELECT CLAIM PEND STATUS ;    
 ;;2.6;IHS 3P BILLING SYSTEM;**31,34**;NOV 12, 2009;Build 645
 ;
 ;IHS/SD/SDR 2.6*31 CR11834 Updated to capture PENDING DATE/TIME
 ;IHS/SD/SDR 2.6*34 ADO60696 Updated so you can hit return at the pending status prompt but it doesn't pend the claim either
 ;
EN(CLAIMNUM) ;EP - ALLOW USER ENTRY OF CLAIM STATUS AND PENDING STATUS
 I $G(CLAIMNUM)="" W !,"CLAIM NUMBER NOT DEFINED!" H 3 Q
 I '$D(^ABMDCLM(DUZ(2),CLAIMNUM,0))#2 W !,"CLAIM NUMBER NOT FOUND ON FILE!" H 3 Q
 D EDITSTAT(CLAIMNUM)
 Q:$G(X)=""!($G(X)[U)
 D STUFPEND(CLAIMNUM)
 D STUFFDUZ(CLAIMNUM,.DUZ)
 Q
STUFPEND(CLAIMNUM) ;EP - STUFF PEND STATUS INTO STATUS FIELD
 K DIE,DR,DIC,DIR,DA
 S DA=CLAIMNUM
 S DR=".04////^S X=""P"""
 S DIE="^ABMDCLM("_DUZ(2)_","
 D ^DIE
 K DIE,DR,DIC,DIR,DA
 Q
EDITSTAT(CLAIMNUM) ;EP - EDIT PEND STATUS
 K DIE,DR,DIC,DIR,DA
 S DA=CLAIMNUM
 ;S DR=".18R"  ;abm*2.6*34 IHS/SD/SDR ADO60696
 S DR=".18"_$S($P($G(^ABMDCLM(DUZ(2),CLAIMNUM,0)),U,18)'="":"R",1:"")  ;abm*2.6*34 IHS/SD/SDR ADO60696
 S DIE("NO^")="OUTOK"  ;abm*2.6*34 IHS/SD/SDR ADO60696
 S DIE="^ABMDCLM("_DUZ(2)_","
 D ^DIE
 K DIE,DR,DIC,DIR,DA
 Q
STUFFDUZ(CLAIMNUM,DUZ) ;EP - STUFF USER INTO 'PENDING STATUS UPDATER'
 K DIE,DR,DIC,DIR,DA
 S DA=CLAIMNUM
 S DR=".19R////^S X=DUZ"
 S DR=DR_";.026R///NOW"  ;abm*2.6*31 IHS/SD/SDR CR11834
 S DIE="^ABMDCLM("_DUZ(2)_","
 D ^DIE
 K DIE,DR,DIC,DIR,DA
 Q
