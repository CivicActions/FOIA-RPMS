ABMCGRPT ; IHS/SD/SDR - Claim Generator Report
 ;;2.6;IHS Third Party Billing;**35,40**;NOV 12, 2009;Build 785
 ;IHS/SD/SDR 2.6*35 ADO60700 New report
 ;IHS/SD/SDR 2.6*40 ADO85530 Added logic for if Loc. Of Encounter is missing from Visit, still report it
 K ABM,ABMY
 S ABMY("RTYP")=1
 S ABM("RTYP","NM")="CG RPT"
 ;
 ;prompt defaults
 S ABMY("RFOR")=1
 S ABMY("DT")="C"  ;default to CG run date
 ;
 S ABMIIEN=$O(^XPD(9.7,"B","ABM*2.6*35",0))
 I +$G(ABMIIEN)'=0 S ABMY("DT",1)=$P($G(^XPD(9.7,ABMIIEN,1)),U,3)
 I +$G(ABMY("DT",1))=0 S ABMY("DT",1)=3210801
 ;
 S ABMY("DT",2)=DT
 ;
 D PARENT  ;make sure ABMPAR is set for report
 ;
SEL D LOOP Q:$D(DTOUT)!$D(DUOUT)!$D(DIROUT)
 S ABM("HD",0)="CLAIM GENERATOR PRODUCTIVITY REPORT"
 S ABM("HD",1)="GENERATED BY: "_$P($G(^VA(200,DUZ,0)),U)
 S ABM("HD",2)="   for "_$S(ABMY("DT")="C":"Claim Generator Run",1:"Visit")_" Dates "_$$SDT^ABMDUTL(ABMY("DT",1))_" to "_$$SDT^ABMDUTL(ABMY("DT",2))
 S ABM("LVL")=0
 S ABM("PG")=0
 ;
 S ABMQ("RC")="COMPUTE^ABMCGRPT"
 S ABMQ("RX")="POUT^ABMDRUTL"
 S ABMQ("NS")="ABM"
 S ABMQ("RP")="PRINT^ABMCGRP"_ABMY("RFOR")
 I ABMY("RFOR")=1 D ^ABMDRDBQ Q  ;printer
 ;if it gets here it's the delimited option
 D COMPUTE
 S ABM("RTN")="PRINT^ABMCGRP"_ABMY("RFOR")
 D @(ABM("RTN"))
 K ^TMP("ABM-CGRPT",$J)
 ;
 Q
 ;
LOOP ;
 ; Display current exclusion parameters
 S ABMY("X")="W $$SDT^ABMDUTL(X)"
 G XIT:$D(DUOUT)!$D(DTOUT)!$D(DIROUT)
 W !!?3,"EXCLUSION PARAMETERS Currently in Effect for RESTRICTING the EXPORT to:"
 W !?3,"======================================================================="
 W !?3,"- Visit Location.....: "
 I $D(ABMY("LOC")) D
 .S ABMI=0
 .F  S ABMI=$O(ABMY("LOC",ABMI)) Q:+ABMI=0  W !?12,$P(^DIC(4,ABMI,0),U)
 E  W "ALL"
 I $D(ABMY("DT")) D
 .W !?3,"- "_$S(ABMY("DT")="C":"Claim Generator Run",1:"Visit")_" Date from: "
 .S X=ABMY("DT",1) X ABMY("X") W "  to: " S X=ABMY("DT",2) X ABMY("X")
 W !?3,"- Report Type...: "
 I $D(ABMY("RFOR")) D
 .W $S(ABMY("RFOR")=2:"Delimited Summary",ABMY("RFOR")=3:"Delimited Detailed",1:"Printer")
 I (+$G(ABMY("RFOR"))>1) W !,?7,"Write file to ",ABMY("RPATH")_ABMY("RFN")
 ;
PARM ;
 ; Choose additional exclusion parameters
 K DIR
 S DIR(0)="SO^1:LOCATION;2:DATE RANGE;3:REPORT TYPE"
 S DIR("A")="Select ONE or MORE of the above EXCLUSION PARAMETERS"
 S DIR("?")="The report can be restricted to one or more of the listed parameters. A parameter can be removed by reselecting it and making a null entry."
 D ^DIR
 K DIR
 G XIT:$D(DIRUT)!$D(DIROUT)
 I Y=1!(Y=2)!(Y=3) D @($S(Y=3:"RFOR",Y=1:"LOC",1:"DT")_"^ABMCGRPT") G LOOP
 Q
 ;
COMPUTE ;
 S ABM("SUBR")="ABM-CGRPT"
 K ^TMP("ABM-CGRPT",$J)
 I '$D(ABMY("LOC")) D
 .;S ABMA=0 F  S ABMA=$O(^BAR(90052.05,ABMPAR,ABMA)) Q:'ABMA  S ABMY("LOC",ABMA)=""  ;put our locs in array if they accepted ALL  ;abm*2.6*40 IHS/SD/SDR ADO85530
 .S ABMA="" F  S ABMA=$O(^BAR(90052.05,ABMPAR,ABMA)) Q:(ABMA="")  S ABMY("LOC",+ABMA)=""  ;put our locs in array if they accepted ALL  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ;
CGDATE ;
 ;if CG run date
 I $G(ABMY("DT"))="C" D
 .S ABMSDT=ABMY("DT",1)-.00001,ABMEDT=ABMY("DT",2)+.999999
 .F  S ABMSDT=$O(^ABMCGAUD("B",ABMSDT)) Q:'ABMSDT!(ABMSDT>ABMEDT)  D  ;loop thru our date range
 ..S ABMCGI=0
 ..F  S ABMCGI=$O(^ABMCGAUD("B",ABMSDT,ABMCGI)) Q:'ABMCGI  D  ;loop thru CG run entries for our date range
 ...S ABMOPT=$P($G(^ABMCGAUD(ABMCGI,0)),U,2)  ;what option did it
 ...;next line makes sure there is a line for each visit location even if there aren't any visits for that patient during the CG1P option
 ...I ABMOPT="CG1P" D CG1P
 ...D BKMG
 ...;S ABMVLOC=0  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ...S ABMVLOC=""  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ...;F  S ABMVLOC=$O(ABMY("LOC",ABMVLOC)) Q:'ABMVLOC  D  ;loop thru selected locations  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ...F  S ABMVLOC=$O(ABMY("LOC",ABMVLOC)) Q:(ABMVLOC="")  D  ;loop thru selected locations  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ....;
 ....I '$D(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT)) S ^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","VSTS")=0  ;cg run total - initialize
 ....I '$D(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC)) S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC),U)=0  ;visit location total - visits initialize
 ....;
 ....S ABMVDFN=0
 ....F  S ABMVDFN=$O(^ABMCGV("AC",ABMCGI,ABMVLOC,ABMVDFN)) Q:'ABMVDFN  D  ;loop thru visits for CG run
 .....S ABMVIEN=$O(^ABMCGV("B",ABMVDFN,0))
 .....S ABMCLMS=$P($G(^ABMCGV(ABMVIEN,1,ABMCGI,0)),U,3)  ;actual claim numbers for detail
 .....;
 .....I +ABMCLMS=0 S ABMCCNT=0
 .....E  S ABMCCNT=$L(ABMCLMS,"/")  ;count of claims
 .....I ABMY("RFOR")=3 D DTAIL Q  ;put together and store detail record; can skip the counts
 .....;
 .....D COUNTS
 ;
VDATE ;
 ;if visit date
 I $G(ABMY("DT"))="V" D
 .S ABMSDT=ABMY("DT",1)-.00001,ABMEDT=ABMY("DT",2)+.999999
 .F  S ABMSDT=$O(^AUPNVSIT("B",ABMSDT)) Q:'ABMSDT!(ABMSDT>ABMEDT)  D
 ..S ABMVDFN=0
 ..F  S ABMVDFN=$O(^AUPNVSIT("B",ABMSDT,ABMVDFN)) Q:'ABMVDFN  D
 ...S ABMVIEN=ABMVDFN
 ...;S ABMVLOC=$P($G(^AUPNVSIT(ABMVDFN,0)),U,6)  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ...S ABMVLOC=+$P($G(^AUPNVSIT(ABMVDFN,0)),U,6)  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ...I '$D(ABMY("LOC",ABMVLOC)) Q  ;not a selected visit location
 ...S ABMCGI=0
 ...F  S ABMCGI=$O(^ABMCGV(ABMVDFN,1,ABMCGI)) Q:'ABMCGI  D
 ....S ABMOPT=$P($G(^ABMCGAUD(ABMCGI,0)),U,2)  ;what option did it
 ....D BKMG
 ....S ABMCLMS=$P($G(^ABMCGV(ABMVDFN,1,ABMCGI,0)),U,3)  ;actual claim numbers for detail
 ....I +ABMCLMS=0 S ABMCCNT=0
 ....E  S ABMCCNT=$L(ABMCLMS,"/")  ;count of claims
 ....I ABMY("RFOR")=3 D DTAIL Q  ;put together and store detail record; can skip the counts
 ....;
 ....D COUNTS
 Q
COUNTS ;
 ;daily totals by visit location,option
 S ^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","VSTS")=+$G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","VSTS"))+1  ;cg run total
 S ^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","CLMS")=+$G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","CLMS"))+ABMCCNT  ;cg run total
 I $P($G(^ABMCGV(ABMVIEN,1,ABMCGI,1)),U)="Y" S ^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","RCVSTS")=+$G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","RCVSTS"))+1  ;recheck visits total
 I $P($G(^ABMCGV(ABMVIEN,1,ABMCGI,1)),U)'="Y" D RSNS I RSNS>0 D
 .S ^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","RCVSTS")=+$G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","RCVSTS"))+1  ;recheck visits total
 ;
 ;subtotals by visit location
 S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC),U)=+$P($G(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC)),U)+1  ;visit location total - visits
 S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC),U,2)=+$P($G(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC)),U,2)+ABMCCNT  ;visit location total - claims
 I ((+$G(RSNS)>0)!($P($G(^ABMCGV(ABMVIEN,1,ABMCGI,1)),U)="Y")) S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC),U,3)=+$P($G(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC)),U,3)+1  ;visit location total - rechecked visits
 ;
 ;grand totals
 S $P(^TMP("ABM-CGRPT",$J,"GTOT"),U)=+$P($G(^TMP("ABM-CGRPT",$J,"GTOT")),U)+1  ;grand total - visits
 S $P(^TMP("ABM-CGRPT",$J,"GTOT"),U,2)=+$P($G(^TMP("ABM-CGRPT",$J,"GTOT")),U,2)+ABMCCNT  ;grand total - claims
 I ((+$G(RSNS)>0)!($P($G(^ABMCGV(ABMVIEN,1,ABMCGI,1)),U)="Y")) S $P(^TMP("ABM-CGRPT",$J,"GTOT"),U,3)=+$P($G(^TMP("ABM-CGRPT",$J,"GTOT")),U,3)+1  ;grand total - rechecked visits
 Q
RSNS ;EP - Check for reasons not to post
 S RSNS=0
 S ABMI=ABMCGI
 F  S ABMI=$O(^ABMCGV(ABMVIEN,1,ABMI),-1) Q:'ABMI  D  Q:RSNS=1
 .I +$P($G(^ABMCGV(ABMVIEN,1,ABMI,0)),U,2)'=0 S RSNS=1
 Q
CG1P ;
 ;to get here ABMOPT must equal "CG1P"
 ;S ABMI=0  ;abm*2.6*40 IHS/SD/SDR ADO85530
 S ABMI=""  ;abm*2.6*40 IHS/SD/SDR ADO85530
 ;F  S ABMI=$O(ABMY("LOC",ABMI)) Q:'ABMI  D  ;abm*2.6*40 IHS/SD/SDR ADO85530
 F  S ABMI=$O(ABMY("LOC",ABMI)) Q:(ABMI="")  D  ;abm*2.6*40 IHS/SD/SDR ADO85530
 .I $G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMI,ABMOPT,"TOT","VSTS"))="" S ^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMI,ABMOPT,"TOT","VSTS")=0
 .I $P($G(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMI)),U)="" S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMI),U)=0
 Q
BKMG ;
 N ABMVLOC
 S ABMBKMG="NO"  ;default it to NO for report
 I '$D(^ABMCGAUD(ABMCGI,1,0)) Q  ;there aren't any BKMGs to check
 S ABMI=0
 F  S ABMI=$O(^ABMCGAUD(ABMCGI,1,ABMI)) Q:'ABMI  D
 .S ABMVLOC=$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U,2)
 .I '$D(ABMY("LOC",ABMVLOC)) Q  ;not the visit loc we are doing
 .S ABMBKMG="YES"_$S($P($G(^VA(200,$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U,3),0)),U,2)'="":"-"_$P($G(^VA(200,$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U,3),0)),U,2),1:"")
 .S ^TMP("ABM-CGRPT",$J,"S-BKMG",ABMCGI,ABMVLOC,ABMOPT)=ABMBKMG
 .S ^TMP("ABM-CGRPT",$J,"B",$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U),$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U,2),$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U,3),$P($G(^ABMCGAUD(ABMCGI,1,ABMI,0)),U,4))=""
 .I $P($G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","VSTS")),U)="" S $P(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","VSTS"),U)=0
 .I $P($G(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","CLMS")),U)="" S $P(^TMP("ABM-CGRPT",$J,"S",ABMCGI,ABMVLOC,ABMOPT,"TOT","CLMS"),U)=0
 .I $P($G(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC)),U)="" S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC),U)=0
 .I $P($G(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC)),U,2)="" S $P(^TMP("ABM-CGRPT",$J,"VLOCTOT",ABMVLOC),U,2)=0
 Q
DTAIL ;
 S ABMREC=$P($G(^DPT($P($G(^AUPNVSIT(ABMVDFN,0)),U,5),0)),U)  ;patient
 S ABMREC=ABMREC_U_$P($G(^AUPNPAT($P($G(^AUPNVSIT(ABMVDFN,0)),U,5),41,ABMVLOC,0)),U,2)  ;HRN
 S ABMREC=ABMREC_U_$S($P($G(^ABMCGV(ABMVIEN,1,ABMCGI,1)),U)="Y":"Y",1:"N")  ;BKMGd visit
 D RSNS
 S ABMREC=ABMREC_U_$S(+$G(RSNS)>0:"Y",1:"N")  ;Rechecked Visit
 ;
 S ABMREC=ABMREC_U_$$GET1^DIQ(9000010,ABMVIEN,".22","E")  ;hospital location
 S ABMREC=ABMREC_U_$$GET1^DIQ(9000010,ABMVIEN,".08","E")  ;clinic
 S ABMREC=ABMREC_U_$$GET1^DIQ(9000010,ABMVIEN,".07","E")  ;serv cat
 ;
 S ABMREC=ABMREC_U
 I +$P($G(^ABMCGV(ABMVIEN,1,ABMCGI,0)),U,2)'=0 S ABMREC=ABMREC_$P($G(^ABMCGV(ABMVIEN,1,ABMCGI,0)),U,2)_"-"_$P(^ABMDCS($P($G(^ABMCGV(ABMVIEN,1,ABMCGI,0)),U,2),0),U)  ;reason no claim
 ;
 S ABMREC=ABMREC_U_$S(+ABMCLMS'=0:ABMCLMS,1:"NO CLAIM")
 S ABMREC=ABMREC_U
 I +$P($G(^ABMCGV(ABMVIEN,1,ABMCGI,0)),U,4)'=0 S ABMREC=ABMREC_$P($G(^AUTNINS(+$P($G(^ABMCGV(ABMVIEN,1,ABMCGI,0)),U,4),0)),U)  ;active insurer
 D PROV  ;primary provider
 S ABMREC=ABMREC_U_ABMPRV
 D DXS  ;diagnosis codes (list with '/' delimiter)
 S ABMREC=ABMREC_U_ABMDX
 D EHRCAUD  ;Review/Chart Audit Status
 S ABMREC=ABMREC_U_ABMEHR
 S ^TMP("ABM-CGRPT",$J,"D",ABMCGI,ABMVLOC,ABMOPT,ABMVDFN)=ABMREC  ;detail up to VDFN; sorted for report
 Q
PROV ;
 S ABMPRV=""
 S ABMPRIEN=0
 F  S ABMPRIEN=$O(^AUPNVPRV("AD",ABMVIEN,ABMPRIEN)) Q:'ABMPRIEN  D
 .Q:$P($G(^AUPNVPRV(ABMPRIEN,0)),U,4)'="P"
 .I +$P($G(^AUPNVPRV(ABMPRIEN,0)),U)'=0 S ABMPRV=$P($G(^VA(200,$P($G(^AUPNVPRV(ABMPRIEN,0)),U),0)),U)
 Q
DXS ;
 S ABMDX=""
 S ABMDIEN=0
 F  S ABMDIEN=$O(^AUPNVPOV("AD",ABMVDFN,ABMDIEN)) Q:'ABMDIEN  D
 .I ABMDX'="" I $P($G(^AUPNVPOV(ABMDIEN,0)),U)'="" S ABMDX=ABMDX_"/"_$P($G(^ICD9($P($G(^AUPNVPOV(ABMDIEN,0)),U),0)),U)
 .I ABMDX="" I $P($G(^AUPNVPOV(ABMDIEN,0)),U)'="" S ABMDX=$P($G(^ICD9($P($G(^AUPNVPOV(ABMDIEN,0)),U),0)),U)
 Q
EHRCAUD ;
 S ABMEHR=""
 S ABMEIEN=999999999999
 S ABMCGDT=$P($G(^ABMCGAUD(ABMCGI,0)),U)  ;claim generator run date/time
 F  S ABMEIEN=$O(^AUPNVCA("AD",ABMVDFN,ABMEIEN),-1)  Q:'ABMEIEN  D  Q:($G(ABMEHR)'="")  ;reverse $O to find the most recent one first
 .I $P($G(^AUPNVCA(ABMEIEN,0)),U)>ABMCGDT Q
 .S ABMEHR=$$BDT^ABMDUTL($P($G(^AUPNVCA(ABMEIEN,0)),U))_U_$P($G(^AUPNVCA(ABMEIEN,0)),U,4)
 Q
 ;
LOC ;EP
 W ! K DIC,ABMY("LOC")
 S DIC="^BAR(90052.05,"_ABMPAR_","
 S DIC(0)="AEMQ"
 S DIC("A")="Select LOCATION: ALL// "
 F  D  Q:+Y<0
 .I $D(ABMY("LOC")) S DIC("A")="Select Another Location: "
 .D ^DIC
 .Q:+Y<0
 .S ABMY("LOC",+Y)=""
 I '$D(ABMY("LOC")) D
 .I $D(DUOUT) K ABMY("SORT") Q
 .W "ALL"
 K DIC
 Q
PARENT ;EP
 S ABMSAT=DUZ(2)
 S ABMPAR=0  ;Parent
 ; check site active at DOS to ensure bill added to correct site
 S DA=0
 F  S DA=$O(^BAR(90052.06,DA)) Q:DA'>0  D  Q:ABMPAR
 .Q:'$D(^BAR(90052.06,DA,DA))  ; Pos Parent UNDEF Site Parameter
 .Q:'$D(^BAR(90052.05,DA,ABMSAT))  ; Satellite UNDEF Parent/Satellit
 .Q:+$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,5)  ; Par/Sat not usable
 .; Q if sat NOT active at DOS
 .I DT<$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,6) Q
 .; Q if sat became NOT active before DOS
 .I $P($G(^BAR(90052.05,DA,ABMSAT,0)),U,7),(DT>$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,7)) Q
 .S ABMPAR=$S(ABMSAT:$P($G(^BAR(90052.05,DA,ABMSAT,0)),U,3),1:"")
 I +ABMPAR=0 S ABMPAR=DUZ(2)
 Q
DT ;EP
DTYP2 ;
 S DIR(0)="SO^1:Claim Generator Run Date;2:Visit Date"
 S DIR("A")="Select TYPE of DATE Desired"
 D ^DIR
 Q:$D(DIROUT)!$D(DIRUT)
 ;
 K DIR
 ;K ABMY("DT")
 Q:$D(DIRUT)
 S ABMY("DT")=$S(Y=1:"C",1:"V")
 S Y=$S(Y=1:"CLAIM GENERATOR RUN",1:"VISIT")_" DATE"
 W !!," ============ Entry of ",Y," Range =============",!
 S DIR("A")="Enter STARTING "_Y_" for the Report"
 S DIR(0)="DO^::EP"
 D ^DIR
 G DT:$D(DIRUT)
 S ABMY("DT",1)=Y
 W !
 S DIR("A")="Enter ENDING DATE for the Report"
 D ^DIR
 K DIR
 G DT:$D(DIRUT)
 S ABMY("DT",2)=Y
 I ABMY("DT",1)>ABMY("DT",2) W !!,*7,"INPUT ERROR: Start Date is Greater than than the End Date, TRY AGAIN!",!! G DT
 Q
RFOR ;EP
 K DIR
 S DIR(0)="SO^1:Printer;2:Delimited Summary;3:Delimited Detailed"
 S DIR("A")="Select TYPE of Output"
 D ^DIR
 K DIR
 Q:$D(DIRUT)
 S ABMY("RFOR")=Y
 S ABMY("RFOR","NM")=Y(0)
 I ABMY("RFOR")=1 Q  ;stop here if printer
 ;ask path and filename
 K DIR
 S DIR(0)="F"
 S DIR("A")="Path"
 D ^DIR
 K DIR
 Q:$D(DIRUT)
 S ABMY("RPATH")=Y
 K DIR
 S DIR(0)="F"
 S DIR("A")="Filename"
 D ^DIR
 K DIR
 Q:$D(DIRUT)
 S ABMY("RFN")=Y
 Q
XIT ;
 K ABMY("I"),ABMY("X"),DIR
 Q
