 ;;2.5;IHS 3P BILLING SYSTEM;**12,13,16**;MAY 24, 2007
 ; New routine - v2.5 p12
 ;
 ; IHS/SD/SDR - v2.5 p13 - IM25962
 ;   Fix for missing TIN check; was failing on Bens
