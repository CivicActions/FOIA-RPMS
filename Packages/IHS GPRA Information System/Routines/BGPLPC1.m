 S X="BGP IPC",DA=976
 F  S X=$O(^ATXAX("B",X)) Q:X=""!(X]"BGP IPCZZZZZZ")  D
 .Q:$D(^BGPTAXL("B",X))  ;already in there
 .S DA=DA+1
 .S ^BGPTAXL(DA,0)=X_"^D^^0"
