TOBHF(P,BDATE,EDATE) ;EP - return DATE^SMOKER CAT^DATE^SMOKELESS CAT^DATE^ETS CATEGORY
 NEW SM,SL,EX,EN,BGPTOB
 S BGPTOB=""
 S SM=$$LASTHF^BGPLD7(P,"C017",BDATE,EDATE) K O,D,H
 S SL=$$LASTHF^BGPLD7(P,"C016",BDATE,EDATE) K O,D,H
 S EX=$$LASTHF^BGPLD7(P,"C015",BDATE,EDATE) K O,D,H
 S EN=$$LASTHF^BGPLD7(P,"C019",BDATE,EDATE) K O,D,H
 I SM]""!(SL]"")!(EX]"")!(EN]"") D TOBHFS Q BGPTOB
 S BGPTOB=$$LASTHF^BGPLD7(P,"C004",BDATE,EDATE) K O,D,H
 I $P(BGPTOB,U,1)["SMOKELESS" S $P(BGPTOB,U,4)=BGPTOB
 I $P(BGPTOB,U,1)["ENVIRON"!($P(BGPTOB,U,1)["IN HOME") S $P(BGPTOB,U,7)=BGPTOB
 Q BGPTOB
TOBHFS ;
 I SM]"" S BGPTOB=SM
 I SL]"" S $P(BGPTOB,U,5)=SL
 I EX]"" S $P(BGPTOB,U,9)=EX
 I EN]"" S $P(BGPTOB,U,13)=EN
 Q
