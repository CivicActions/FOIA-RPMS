 S X=$$EN^APSQBRES(RXIEN,RFIEN,ADDDEL)
 Q
EN(RXIEN,RFIEN,ADDDEL) ;EP
 ;CALLED FROM RTNS PSORXED AND PSORXDL AND PSORXED AND PSORXDLR AND PSORL AND APSPRESK
 ;RXIEN=RX IEN RFIEN= REFILL IEN ADDDEL= ADDING OR DELETING
 ;S QUIT=0 D TEST Q:QUIT ""
 S QUIT=0 D TEST("ABSPOSRX") Q:QUIT ""  ;IHS/OKCAO/POC 1/22/2001 ****
 I '+$G(RXIEN) S PSOREST="ERROR" G FIN ;I DON'T KNOW
 ;NEW PSORES,PSOREST,DA,DR,DIE,X,Y
 NEW PSORES,PSOREST,DA,DR,DIE,X,Y,D0,DO,DD ;IHS/OKCAO/POC 12/20/2000
 K:$G(RFIEN)="" RFIEN
 I ADDDEL="A" D
 .S:$G(RFIEN)']"" PSORES=$$CLAIM^ABSPOSRX(RXIEN)
 .S:$G(RFIEN)]"" PSORES=$$CLAIM^ABSPOSRX(RXIEN,$G(RFIEN))
 .;S ^PATRICK($H,"A","RXIEN",$G(RXIEN,0),"RFIEN",$G(RFIEN,0))=""
 .S PSORES=$G(PSORES)
 .S PSOREST=$S(PSORES=1:"WILL PROCESS",PSORES=2:"WILL PROCESS BUT HAS BEEN SUBMITTED PREVIOUSLY",PSORES=3:"PREVIOUS PAPER CLAIM, REVERSE AND RESUBMIT",PSORES=4:"PREVIOUS ELECTRONIC CLAIM, REVERSE AND RESUBMIT",PSORES=-1:"ALREADY CLAIM IN PROGRESS",PSORES<0:"FAILURE",1:"UNKNOWN CLAIM STATUS")
 .I +$G(RFIEN) S DA=RFIEN,DA(1)=RXIEN,DIE="^PSRX("_DA(1)_",1,",DR="9999999.08///^S X=PSOREST" D ^DIE
 .I '+$G(RFIEN) S DA=RXIEN,DR="9999999.08///^S X=PSOREST",DIE="^PSRX(" D ^DIE
 I ADDDEL="D" D
 .S:$G(RFIEN)']"" PSORES=$$UNCLAIM^ABSPOSRX(RXIEN)
 .S:$G(RFIEN)]"" PSORES=$$UNCLAIM^ABSPOSRX(RXIEN,$G(RFIEN))
 .;S ^PATRICK($H,"D","RXIEN",$G(RXIEN,0),"RFIEN",$G(RFIEN,0))=""
 .S PSORES=$G(PSORES)
 .S PSOREST=$S(PSORES=1:"WILL RESUBMIT FOR REVERSAL",PSORES=-1:"ALREADY TRANSACTION IN PROGRESS",PSORES=-2:"NO RECORD OF PREVIOUS CLAIM TO REVERSE",PSORES=-3:"NOT REVERSIBLE, MUST BE PAPER OR E PAYABLE",1:"UNKNOWN REVERSAL STATUS")
 .;I +$G(RFIEN) S DA=RFIEN,DA(1)=RXIEN,DIE="^PSRX("_DA(1)_",1,",DR="9999999.08///^S X=PSOREST" D ^DIE
 .I '+$G(RFIEN) S DA=RXIEN,DR="9999999.08///^S X=PSOREST",DIE="^PSRX(" D ^DIE
FIN Q PSOREST
 ;
STATUS(RXIEN,RFIEN) ;EP
 ;S QUIT=0 D TEST Q:QUIT ""
 S QUIT=0 D TEST("ABSPOSRX") Q:QUIT ""  ;IHS/OKCAO/POC 1/22/2001 ****
 I '$G(RXIEN) Q ""
 I $G(RFIEN)]"" Q $$STATUS^ABSPOSRX(RXIEN,RFIEN) ;10/13/2000
 Q $$STATUS^ABSPOSRX(RXIEN)
 ;Q "TEST "_RXIEN_" "_$G(RFIEN)
 ;
OVERRIDE(APSQIEN,APSQDIEN)         ;EP
 ;IHS/SD/lwj 6/23/03 (patch 4) added APSQDIEN to parameter list
 ;APSQIEN = pointer to ^ABSP(9002313.511)
 ;APSQDIEN = pointer to ^ABSP(9002313.473)
 ;
 ;******   NOTE - CALLING ROUTINE SHOULD 'NEW' APSQDUR !!!!!
 ;******   (or kill it) - DON'T LEAVE THIS FIELD
 ;******   It is only done this way because of limitations
 ;******   experienced in PSORXED (can't handle multiple
 ;******   values coming back)
 ;
 ;W !,"I GOT HERE"
 ;S QUIT=0 D TEST1 Q:QUIT ""
 S QUIT=0 D TEST("ABSPOSO") Q:QUIT ""  ;IHS/OKCAO/POC 1/22/2001 ****
 N (RFD,APSQIEN,DUZ,IO,IOBS,IOF,IOM,ION,IOS,IOSL,ILST,IOT,IOXY,U,DT,DTIME,APSQCOM,APSQCOMR,APSQDIEN,APSQDUR)
 S APSQDUR=""   ;pre set to null
 ;IHS/SD/lwj 6/23/03 (patch 4) altered the way to call for the
 ; insurance menu - old code remarked out
 ;
 ;I $G(APSQIEN)]"" S APSQNIEN=$$OVERRIDE^ABSPOSO(APSQIEN)
 ;I $G(APSQIEN)']"" S APSQNIEN=$$OVERRIDE^ABSPOSO
 ;
 S APSQNIEN=$$NEWOVER^ABSPOSO($G(APSQIEN),$G(APSQDIEN))  ;IHS/SD/lwj 6/23/03 new
 S APSQDUR=$P(APSQNIEN,U,2)   ;this is the DUR pntr
 S APSQNIEN=$P(APSQNIEN,U)    ;just the ins pntr passd bck
 ;
 ;IHS/SD/lwj 6/23/03 (patch 4) end of changes
 ;
 I $D(RFD) D  ;IF RFD DEFINE WE ARE EDITING A PRESCRIPTION
 .S:'RFD APSQCOM="INSURER INFO ADDED/EDITED," ;FOR GUARANTEE CAPTURE OF THIS INFO, PRINTING, AND CALLING BILLING PROGRAM.
 .S:RFD APSQCOMR="REFILL INSURER INFO ADDED/EDITED," ;FOR GURANTEE CAPTURE OF THIS INFO, PRINTING, AND CALLING BILLING PROGRAM.
 Q APSQNIEN
TEST(X) ;EP
 ;S APSQNNBB=1 ;UNCOMMENT TO STOP PROCESSING POS
 I $G(APSQNNBB) S QUIT=1 ;TURNS OFF BILLING
 ;I '+$P(%APSITE3,U,7) S QUIT=1 ;IHS/OKCAO/POC 5/2/2001
 I '+$P($G(%APSITE3),U,7) S QUIT=1 ;IHS/ASD/POC/ENM 05/30/01
 ;S X="ABSPOSRX" X ^%ZOSF("TEST") I '$T S QUIT=1 ;Q ""
 X ^%ZOSF("TEST") I '$T S QUIT=1 ;Q ""
 Q
