APSPCTR ; IHS/DSD/ENM - CONTROLLED DRUG LIST BY DIV 03-22-93 ;  [ 02/13/2003  11:25 AM ]
 ;;6.0;IHS PHARMACY MODIFICATIONS;**1,2,4**;09/03/97
EN ;EP
 K ^TMP("APSP",$J)
 W @IOF,!!,"Pharmacy Controlled Drug List by Division",!,*7,?10,"132 Character Format!",!
 ;S %DT("A")="Beginning Date: ",%DT="AEP" D ^%DT S APSPBD=Y_.0001 Q:Y<0
 S %DT("A")="Beginning Date: ",%DT="AEP" D ^%DT S APSPBD=Y-1 Q:Y<0  ;IHS/DSD/ENM 07/28/97
 ;S %DT("A")="Ending Date: ",%DT("B")="TODAY",%DT="AEP" D ^%DT S APSPED=Y_".2359" Q:Y<0
 S %DT("A")="Ending Date: ",%DT("B")="TODAY",%DT="AEP" D ^%DT S APSPED=Y Q:Y<0  ;IHS/DSD/ENM 07/28/97
 ;SELECT DIVISION
 S DIR(0)="Y",DIR("A")="Would you like all divisions",DIR("B")="YES",DIR("?")="Enter 'Yes' or 'No'" D ^DIR K DIR Q:$D(DTOUT)
 I X="YES" S APSPANS="A" G DDD
 S DIR(0)="PO^59:EMZ",DIR("A")="Select Division",DIR("?")="Enter the Division Name or Number "
 D ^DIR G:$D(DTOUT)!$D(DUOUT) ZAP K DIR
 S APSPANS=+Y
DDD ;Select by date/drug
 S DIR(0)="S^1:DATE;2:DRUG;",DIR("A")="By Date or Drug" D ^DIR K DIR
 G:$D(DUOUT)!$D(DIRUT) ZAP S APSPDTDR=Y
 S U="^" D MENU
 I APSPOP=""!(APSPOP["^") G ZAP
 S APSPGO=$P($T(MEN+APSPOP),"^",2,3)
DEV S %ZIS="QM",%ZIS("A")="Select Printer: "
 D ^%ZIS K %ZIS
 I POP G ZAP
 I $D(IO("Q")),IO=0 W !,"QUEUEING TO YOUR SCREEN IS NOT ALLOW! " K IO("Q") G DEV
 I IO=IO(0)!('$D(IO("Q"))) G LOOP
 S ZTRTN="LOOP^APSPCTR"
 S ZTDESC="Control Drug List"
 F X="ZTDESC","U","APSPBD","APSPED","APSPGO","APSPANS","APSPDTDR" S ZTSAVE(X)=""
 D ^%ZTLOAD
 G ZAP
MENU F APSPZZ=1:1 D MENU1 Q:APSPDES=""
 S APSPITM=APSPZZ-1
MENU2 ;R !!,"Enter the Option Number: ",APSPOP:DTIME ;IHS/DSD/ENM 05/24/96
 S DIR("A")="Enter the Option Number",DIR(0)="S^1:C-2'S;2:C-3'S to C5'S;3:All;" D ^DIR S APSPOP=X ;IHS/DSD/ENM 05/24/96
 I '$T!(APSPOP["^")!(APSPOP="") Q
 I (APSPOP<1)!(APSPOP>APSPITM)!(APSPOP="?") W !!,"Select a '1' for C-2's or '2' for C-3's to C5's...or",!,"Enter ""3"" for 'All'"
 ;I  W !!,?10,"Press Return to Continue...",APSPOP:DTIME G MENU2 ;IHS/DSD/ENM 5/24/96 READ STMT REMOVED
 Q
MENU1 S APSPDES=$P($T(MEN+APSPZZ),";;",2)
 I APSPDES'="" W !,APSPZZ,$P(APSPDES,"^",1)
 Q
LOOP S (APSPN,APSPX)=""
 I +APSPANS,APSPDTDR=2 S APSPD=APSPANS-1 G LOOP1
 I APSPANS="A" S APSPD=0 G LOOP1
 G LOOP2
 Q
LOOP1 F APSPD=APSPD:0 S APSPD=$O(^PSRX("AD2",APSPD)) Q:'APSPD  F APSPX=APSPBD:0 S APSPX=$O(^PSRX("AD2",APSPD,APSPX)) Q:'APSPX!(APSPX>APSPED)  F  S APSPN=$O(^PSRX("AD2",APSPD,APSPX,APSPN)) Q:'APSPN  D
 .Q:$P($G(^PSRX(APSPN,0)),U,15)=13  ;IHS/DSD/ENM/POC 05/11/98 DELETED
 .S APSPRX=$P($G(^PSRX(APSPN,0)),U,6) Q:APSPRX=""
 .S APSPSH=$P($G(^PSDRUG(APSPRX,0)),U,3) D @APSPGO
 D ^APSPCTR1,ZAP
 Q
LOOP2 ;
 F APSPX=APSPBD:0 S APSPX=$O(^PSRX("AD2",APSPANS,APSPX)) Q:'APSPX!(APSPX>APSPED)  F  S APSPN=$O(^PSRX("AD2",APSPANS,APSPX,APSPN)) Q:'APSPN  D
 .Q:$P($G(^PSRX(APSPN,0)),U,15)=13  ;IHS/DSD/ENM/POC 05/11/98 DELETED
 .S APSPRX=$P($G(^PSRX(APSPN,0)),U,6) Q:APSPRX=""
 .S APSPSH=$P($G(^PSDRUG(APSPRX,0)),U,3),APSPD=APSPANS D @APSPGO
 D ^APSPCTR1,ZAP
 Q
C2 ;Search for c-subs C2.
 S APSPMSG="Special Handling Code ""2"" Drugs"
 I "2"[+APSPSH D APSPSET
 Q
C3 ;Search for c-subs (C3 to C5)
 S APSPMSG="Special Handling Code(s) ""3"" to ""5"" Drugs"
 I "345"[+APSPSH D APSPSET
 Q
C4 ;Search for 'All' c-sub drugs
 S APSPMSG="Special Handling Code(s) ""2"" to ""5"" Drugs"
 I "2345"[+APSPSH D APSPSET
 Q
APSPSET ;
 ;RX DATA
 S APSPRN=$O(^PSRX("AD2",APSPD,APSPX,APSPN,-1)) ;REFILL NUMBER
 ;IHS/DSD/ENM/POC 05/21/98 NEXT 2 LINES ADDED
 I 'APSPRN Q:$P(^PSRX(APSPN,2),U,15)]""  ;ORIGINAL RX RTN TO STOCK
 I APSPRN Q:$P($G(^PSRX(APSPN,1,+APSPRN,0)),U,16)]""  ;REFILL RTN TO STOCK USE THIS LINE WITH $G AS XREF 'AD2' MAY HAVE A 'P' IF PARTIAL IHS/ITSC/POC/ENM 10/10/01
 ;I APSPRN Q:$P(^PSRX(APSPN,1,APSPRN,0),U,16)]""  ;REFILL RTN TO STOCK
 S APSPRXN=$P(^PSRX(APSPN,0),"^",1) ;RX NUMBER ON FILE
 I APSPRN>0&(APSPRN'["P") S APSPRXN=APSPRXN_" RF# "_APSPRN
 I APSPRN>0&(APSPRN["P") S APSPRXN=APSPRXN_" Partial# "_+APSPRN
 S APSPAT=$P(^PSRX(APSPN,0),"^",2) ;PATIENT NBR FOR PERSON FILE
 S APSPATN=$P(^DPT(APSPAT,0),"^",1) ;PATIENT NAME
 S APSPCHN="UNKNOWN" I '$D(DUZ(2)) S DUZ(2)=$O(^DIC(59,0)) I DUZ(2)>0,$D(^(DUZ(2),0)) S DUZ(2)=$P(^(0),"^",6)
 I DUZ(2)>0,$D(^AUPNPAT(APSPAT,41,DUZ(2),0)) S APSPCHN=$P(^(0),"^",2)
 I APSPRN=0 S APSPQTY=+$P(^PSRX(APSPN,0),"^",7) ;QTY
 I APSPRN>0&(APSPRN'["P") S APSPQTY=+$P(^PSRX(APSPN,1,+APSPRN,0),U,4)
 I APSPRN>0&(APSPRN["P") S APSPQTY=+$P(^PSRX(APSPN,"P",+APSPRN,0),U,4)
 I APSPRN=0 S APSPCLER=$P(^PSRX(APSPN,0),U,16) ;CLERK CODE
 I APSPRN>0&(APSPRN'["P") S APSPCLER=$P(^PSRX(APSPN,1,APSPRN,0),U,7)
 I APSPRN>0&(APSPRN["P") S APSPCLER=$P(^PSRX(APSPN,"P",+APSPRN,0),U,7)
 S APSPC9=$P($G(^VA(200,APSPCLER,0)),U,2) ;IHS/DSD/ENM 01/09/96
 S APSPDIV=APSPD
 S APSPDRUG=$P(^PSDRUG(APSPRX,0),"^",1) ;DRUG NAME
 S APSPMD=$P(^VA(200,$P(^PSRX(APSPN,0),"^",4),0),"^",1) ;MD NAME
 I APSPMD?1.A1",".E S APSPMD=$E(APSPMD,1,$F(APSPMD,","))_"."
 ;SET ARRAY
 I APSPDTDR=1 D S1 Q
 I +APSPANS,APSPDTDR=2 Q:APSPDIV'=APSPANS
SS ;
 S ^TMP("APSP",$J,APSPDIV,APSPRX,+APSPSH,APSPX,APSPN)=APSPATN_U_APSPCHN_U_APSPX_U_APSPDRUG_U_APSPQTY_U_APSPRXN_U_APSPMD_U_APSPC9 ;IHS/DSD/ENM 01/09/96
 Q
S1 ;SET ARRAY IN DIV/DATE ORD
 S ^TMP("APSP",$J,APSPDIV,APSPX,+APSPSH,APSPRX,APSPN)=APSPATN_U_APSPCHN_U_APSPX_U_APSPDRUG_U_APSPQTY_U_APSPRXN_U_APSPMD_U_APSPC9 ;IHS/DSD/ENM 01/09/95
 Q
MEN ;MENU TX AND LINE TAG
 ;;..........Controlled Drug List (C-2 Only)^C2^APSPCTR
 ;;..........Controlled Drug List (C-3 to C-5)^C3^APSPCTR
 ;;..........Controlled Drug List (All)^C4^APSPCTR
 Q
ZAP ;Kill variables
 K %DT("B"),%DT("A"),APSP("PAGE"),APSPAT,APSPATN,APSPBD,APSPCHN,APSPD,APSPDES,APSPDR,APSPBD,APSPED,APSPX,APSPN,APSPRX,APSPSH,DIC,APSPANS,APSPDTDR
 K APSPDRUG,APSPED,APSPGO,APSPITM,APSPMD,APSPMSG,APSPOP,APSPQTY,APSPRN,APSPRXN,APSPZZ,APSPBD,^TMP("APSP",$J),APSPCLER,APSPDIV,APSPDV
 K APSP(2),APSP("3-5"),APSPGT,APSPT(2),APSPT(35),APSPC9
 Q
