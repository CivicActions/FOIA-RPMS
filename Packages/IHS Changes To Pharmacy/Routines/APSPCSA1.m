APSPCSA1 ;IHS/MSC/MGH - CONTROLLED SUBSTANCE AUDIT LOG ;18-Apr-2018 08:51;DU
 ;;7.0;IHS PHARMACY MODIFICATIONS;**1023**;Sept 23,2004;Build 121
HASH(IEN,SIEN,TXT) ;Audit log hash Return: String
 ;Hash Method
 N HASH,STR,RTN,EXEC
 D READATA(.STR,IEN,SIEN,TXT)
 S EXEC="S HASH=##class(%SYSTEM.Encryption).SHAHash(256,STR)" X EXEC
 S EXEC="S RTN=$System.Encryption.Base64Encode(HASH)" X EXEC
 Q $G(RTN)
READATA(STR,IEN,SIEN,TXT) ;Read in the fields of the APSP CS AUDIT LOG
 ;INPUT: IEN of entrry
 ;SIEN: IEN of status entry
 ;
 N FNUM,DATA,MSG,PT,PRV,DTE,NAT,AIEN,STAT,SDTE,DRUG,REA,SCH,DEA,UP,INST
 S STR=""
 S FNUM=9009036
 D GETS^DIQ(FNUM,IEN,".01;.03;.04;.09","I","DATA","MSG")
 S PT=$G(DATA(9009036,IEN_",",.01,"I"))
 S PRV=$G(DATA(9009036,IEN_",",.03,"I"))
 S DTE=$G(DATA(9009036,IEN_",",.04,"I"))
 S INST=$G(DATA(9009036,IEN_",",.09,"I"))
 S STR=$G(PT)_"^"_$G(PRV)_"^"_$G(DTE)_"^"_$G(NAT)
 K DATA,MSG
 S FNUM=9009036.01
 S AIEN=SIEN_","_IEN_","
 D GETS^DIQ(FNUM,AIEN,"**","I","DATA","MSG")
 S STAT=$G(DATA(9009036.01,AIEN,.01,"I"))
 S SDTE=$G(DATA(9009036.01,AIEN,.02,"I"))
 S DRUG=$G(DATA(9009036.01,AIEN,.03,"I"))
 S REA=$G(DATA(9009036.01,AIEN,.04,"I"))
 S SCH=$G(DATA(9009036.01,AIEN,.05,"I"))
 S DEA=$G(DATA(9009036.01,AIEN,.06,"I"))
 S UP=$G(DATA(9009036.01,AIEN,.07,"I"))
 S STR=STR_"^"_$G(STAT)_"^"_$G(SDTE)_"^"_$G(DRUG)_"^"_$G(REA)_"^"_$G(SCH)_"^"_$G(DEA)_"^"_$G(UP)
 S STR=STR_"^"_TXT
 Q STR
LOGBUSA(ORDER,RX,LIEN,SIEN,FLG) ;Send data to BUSA for logging
 N STS,DET,DESC,PT,ACTION,CAT,DEA,ST,DRUG,AIEN,DEATEXT,FNUM,DATA,MSG
 N RSLT,TYPE,OPTION,CALL,NAMSP,PRE,AUD,MSG1,DIEN,SEVEN,EVENT,DEATXT,DEACODE
 S ORDER=$G(ORDER),RX=$G(RX),FLG=$G(FLG)
 S PT=0
 I +ORDER S PT=+$$GET1^DIQ(100,+ORDER,.02,"I")
 I +RX&(+ORDER=0) D
 .S PT=$$GET1^DIQ(52,RX,2,"I")
 I +PT D
 .S CAT="P"
 .S DET(1)=PT
 E  S CAT="S"
 S ACTION="A"
 S OPTION="IHS CS AUDIT LOG",CALL="APSPCSA"
 ;Get the description fields
 S FNUM=9009036.01
 S AIEN=SIEN_","_LIEN_","
 D GETS^DIQ(FNUM,AIEN,".01;.02;.03;.04;.05;.06;.07","EI","DATA","MSG")
 S DEA=DATA(9009036.01,AIEN,.06,"I")
 S DEACODE=DATA(9009036.01,AIEN,.06,"E")
 S ST=DATA(9009036.01,AIEN,.01,"E")
 S DRUG=DATA(9009036.01,AIEN,.03,"E")
 S DIEN=DEA_","
 D GETS^DIQ(9009036.2,DIEN,".01;.02;.03;.04;.05;.06","I","AUD","MSG2")
 S DEATXT=AUD(9009036.2,DIEN,.02,"I")
 S RSLT="RSLT~"_AUD(9009036.2,DIEN,.05,"I")
 S TYPE="TYPE~"_AUD(9009036.2,DIEN,.04,"I")
 S EVENT="EP~"_AUD(9009036.2,DIEN,.03,"I")
 S NAMSP="NAMSP~"_$S(EVENT="E":"OR",1:"PS")
 S SEVEN=AUD(9009036.2,DIEN,.06,"I")
 S PRE="OR("
 I +ORDER S PRE="OR("_ORDER
 E  I +RX S PRE="PS("_RX
 S DESC=PRE_";Action: "_DEATXT_" "_DRUG_"|"_TYPE_"|"_RSLT_"|"_NAMSP_"|"_OPTION_"|"_EVENT_"|"_SEVEN
 I (DEACODE="CR")!(DEACODE="DE")!(DEACODE="DC")!(DEACODE="DS")!(DEACODE="SC")!(DEACODE="ED") D
 .S PRV=$P($G(^APSPCSA(LIEN,0)),U,3)
 .S DESC=DESC_"|"_PRV
 I DEACODE="DI" D
 .S PRV=$P($G(^PSRX(RX,2)),U,3)
 .S DESC=DESC_"|"_PRV
 ;I +FLG S DESC=DESC_"|Electronic"
 S STS=$$LOG^XUSBUSA("A",CAT,ACTION,CALL,DESC,.DET,"")
 Q
