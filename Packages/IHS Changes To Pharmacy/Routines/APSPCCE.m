APSPCCE ; IHS/DSD/ENM - CREATE PCC EDIT RX LINKAGE ;  [ 05/08/2003  10:09 AM ]
 ;;6.0;IHS PHARMACY MODIFICATIONS;**1,3,4**;09/03/97
 ;THE ROUTINE NOW ADDS NEW ENTRY FOR ORIG RX AND LAST REFILL THEN DELETES THE OLD ENTRY.  THIS IS SO CAN CHANGE THE PATIENT STATUS OR SERVICE CAT IN VISIT FILE IHS/OKCAO/POC 8/18/2000
 ; NOTE: CALLED FROM PSRXEDIT,PSORENW1
 ;IHS/OHPRD/JCM 6/16/89 ADDED \1 TO EDITRX+7,EDITREF+5
 ;IHS/OHPRD/JCM 6/16/89 CHANGED 999999911 TO 9999999 IN SETALVRC+5&6
 ;IHS/OHPRD/JCM 6/18/90 SETALVRC+1&2 Changed logic to always set
 ; PSDFN irregardless of whether it exists or not.
 ;
EN ; NORMAL ENTRY POINT FROM PHARMACY ROUTINES
 ;I APSRCT,'$D(^PSRX(APSRX,1,APSRCT,999999911)) D SETALVR2 G ^APSPCCR
 ;I 'APSRCT,'$D(^PSRX(APSRX,999999911)) D SETALVR1 G ^APSPCCN
 ;IHS/DSD/ENM/POC 02/3/98 2 LINES ABOVE COM/OUT NX 10 LINES ADDED NEW/REF/PCC EDIT VAR
 ;START OF CHANGES IHS/OKCAO/POC 8/18/2000
 ;S APSPSVRX=APSRX ;SAVE VAR
 ;S APSPSVRF=APSRCT ;"    "
 S (APSPNEW,APSPKILL,APSPREF)=0
 I $P(%APSITE,U,15)="Y" D  ; 
 .D:'APSRCT ORI D:APSRCT REF
 K APSPGTRX,APSPGTRF
 Q
 ;
ORI D  ;
 . S APSDVRXI=$P($G(^PSRX(PSORXED("IRXN"),999999911)),U,1) I APSDVRXI,$D(^AUPNVMED(APSDVRXI,0))#10 D  ;
 .. ;S APSRX=PSORXED("IRXN")
 .. ;S APSRM=APSDVRXI
 .. ;S APSPPDFN=$P(PSORXED("RX0"),U,2)
 .. S APSPFDTE=$P(^AUPNVMED(APSDVRXI,0),U,3) S:APSPFDTE APSPFDTE=+$G(^AUPNVSIT(APSPFDTE,0)) ;VISIT
 .. ;D ^APSPCCD ;DELETE IT
 .. S APSPGTRX=1
 .. S DIE="^PSRX(",DR="9999999.11///@",DA=PSORXED("IRXN") D ^DIE K DIE ;GET RID OF ENTRY IN 9999999.11
 . S APSRX=PSORXED("IRXN")
 . S APSPNEW=1 D SETALVR1,VR1,^APSPCCN ;IHS/DSD/ENM 05/15/98 ADD IT BACK
 . ;S APSDVRXI=$P($G(^PSRX(PSORXED("IRXN"),999999911)),U,1) I APSDVRXI,$D(^AUPNVMED(APSDVRXI,0))#10 D  
 . I $G(APSPGTRX) D  ;
 .. ;I WANT TO DELETE THE ENTRY LAST
 .. S APSRX=PSORXED("IRXN")
 .. S APSRM=APSDVRXI
 .. S APSPPDFN=$P(PSORXED("RX0"),U,2)
 .. ;S APSPFDTE=$P(^AUPNVMED(APSDVRXI,0),U,3) S:APSPFDTE APSPFDTE=+$G(^AUPNVSIT(APSPFDTE,0)) ;VISIT
 .. D ^APSPCCD ;DELETE IT
 ..Q
 .Q
 Q
 ;
REF D  ;
 . I APSRCT S APSRX=PSORXED("IRXN"),APSPVREF=$P($G(^PSRX(APSRX,1,APSRCT,999999911)),U,1) I APSPVREF,$D(^AUPNVMED(APSPVREF,0))#10 D  ;
 .. ;S APSRM=APSPVREF
 .. ;S ASPPDFN=$P(PSORXED("RX0"),U,2)
 .. S APSPRDTE=$P(^AUPNVMED(APSPVREF,0),U,3) S:APSPRDTE APSPRDTE=+$G(^AUPNVSIT(APSPRDTE,0))
 .. ;D ^APSPCCD ;DELETE IT
 .. S APSPGTRF=1
 .. S DA=APSPVREF,DA(1)=PSORXED("IRXN"),DIE="^PSRX("_DA(1)_",1,"_DA_",",DR="9999999.11///@" D ^DIE K DIE ;GET RID OF ENTRY IN 9999999.11 OF REFILL APSRCTENTRY
 . I APSRCT S APSRX=PSORXED("IRXN"),APSPREF=1 D SETALVR2,VR2,^APSPCCR ;IHS/DSD/ENM 05/15/98
 . ;I APSRCT S APSRX=PSORXED("IRXN"),APSPVREF=$P($G(^PSRX(APSRX,1,APSRCT,999999911)),U,1) I APSPVREF,$D(^AUPNVMED(APSPVREF,0))#10 D   
 . I $G(APSPGTRF) D
 .. ;I WANT TO DELETE THE REFILL LAST
 .. S APSRM=APSPVREF
 .. S ASPPDFN=$P(PSORXED("RX0"),U,2)
 .. ;S APSPRDTE=$P(^AUPNVMED(APSPVREF,0),U,3) S:APSPRDTE APSPRDTE=+$G(^AUPNVSIT(APSPRDTE,0))
 .. D ^APSPCCD ;DELETE IT
 .. Q
 . Q
 Q
 ;END OF CHANGES  IHS/OKCAO/POC 8/18/2000
 ;
 I $D(^PSRX(APSRX,0))#10 S APSDVRXI=$G(^PSRX(APSRX,999999911),"NONE") I '$D(^AUPNVMED(APSDVRXI,0))#10 S APSPNEW=1 D SETALVR1,VR1,^APSPCCN ;IHS/DSD/ENM 05/15/98
 S APSRX=APSPSVRX ;IN CASE GETS KILLED
 S APSRCT=APSPSVRF ;"   "   "     "
 ;ABOVE SAYS GOT RX BUT NO ENTRY IN V MED FILE
 I $D(^PSRX(APSRX,1,APSRCT,0))#10 S APSDVREF=$G(^PSRX(APSRX,999999911),"NONE") I '$D(^AUPNVMED(APSDVREF,0))#10 S APSPREF=1 D SETALVR2,VR2,^APSPCCR ;IHS/DSD/ENM 05/15/98
 ;ABOVE SAYS GOT REFILL BUT NO ENTRY IN V MED FILE
 S APSRX=APSPSVRX,APSRCT=APSPSVRF ;IN CASE GETS KILLED
 ;END OF MODES
 ;
 ; TASKMAN SETUP
 I APSRCT S APSRCTL=+^PSRX(APSRX,1,APSRCT,999999911),APSRCT0=^PSRX(APSRX,1,APSRCT,0)
 I 'APSRCT S APSRCTL="",APSRCT0=""
 S APSL=$S($D(^PSRX(APSRX,999999911))#2:+^(999999911),1:0)
 S APSRX0=^PSRX(APSRX,0)
 K ZTSAVE F %="APSRX","APSRX0","APSRCT","APSRCT0","APSL","APSRCTL","APCDALVR(" S ZTSAVE(%)=""
 F %="APSPKILL","APSPNEW","APSPREF" S ZTSAVE(%)="" ;IHS/DSD/ENM/POC 02/3/98
 S ZTRTN="ZTSK^APSPCCE",ZTDESC="EDIT PRESCRIPTION LINK TO PCC FROM PHARMACY",ZTIO="",ZTDTH=DT D ^%ZTLOAD K ZTSK
 K APSRX,APSRX0,APSRCT,APSRCT0,APSL,APSRCTL,ZTRTN,ZTDESC,ZTIO,ZTDTH
 Q
 ;
SETALVR1 ; SET UP APCDALVR FOR ORIGINAL RX
 ;S APSED=$P(^PSRX(APSRX,0),U,13) G SETALVRC
 S APSED=$S($G(APSPFDTE):APSPFDTE,1:$P(^PSRX(APSRX,2),U,2)_".12") G SETALVRC ;IF GOT A V MED VISIT DATE USE IT, IF NOT USE FILL DATE DEFAULTED TO 1200 IHS/OKCAO/POC 8/18/2000
SETALVR2 ; SET UP APCDALVR FOR REFILL
 ;S APSED=$P(^PSRX(APSRX,1,APSRCT,0),U,1) G SETALVRC 
 S APSED=$S($G(APSPRDTE):APSPRDTE,1:$P(^PSRX(APSRX,1,APSRCT,0),U,1)_".12") G SETALVRC ;IF GOT A V MED VISIT DATE USE IT, IF NOT USE REFILL DATE DEFAULTED TO 1200IHS/OKCAO/POC 8/18/2000
SETALVRC ;
 S APSDFN=$S($D(PSDFN):1,1:0) ;IHS/OHPRD/JCM 6/18/90
 S PSDFN=$P(^PSRX(APSRX,0),U,2) ;IHS/OHPRD/JCM 6/18/90
 D ^APSPCCV
 S APCDALVR("APCDPAT")=PSDFN
 ;COMMENT OUT NEXT 4 LINES IHS/OKCAO/POC 8/18/2000
 ;S APSEDI=9999999-$P(APSED,".",1)-1_".99999" ;IHS/OHPRD/JCM 6/16/89
 ;S APSEDI=$O(^AUPNVSIT("AA",PSDFN,APSEDI)) S APSEDI=9999999-$P(APSEDI,".",1)_"."_$P(APSEDI,".",2) ;IHS/OHPRD/JCM 5/25/89
 ;I $P(APSEDI,".",1)=$P(APSED,".",1) S APSED=APSEDI
 ;S:$P(APSED,".",2)="" APSED=+APSED_".12"
 S APCDALVR("APCDDATE")=APSED
 S:$P(^PSRX(APSRX,0),U,3)'=1 APCDALVR("APCDCAT")="I" ;IHS/OKCAO/POC 8/18/2000 IN CASE IN HOSPITAL VISIT
 K:'APSDFN PSDFN K APSDFN,APSED,APSEDI
 Q
 ;
ZTSK ; TASKMAN ENTRY POINT (IN BACKGROUND)
 D ^APSPCCLQ ; HANG IN QUEUE
EDIT ;D EDITRX:APSL,EDITREF:APSRCT ;IHS/DSD/ENM/POC 02/05/98
 ;IHS/DSD/ENM/POC 02/05/98 NX TWO LINES ADDED
 I APSL,'APSPNEW D EDITRX ;RX WITH NO CALL TO APSPCCN
 I APSRCT,'APSPREF D EDITREF ;REFILL W NO CALL TO APSPCCR AND NOT DELETED
 I '$D(Y) D K^APSPCCLQ S:$D(ZTQUEUED) ZTREQ="@"
ZTSKX K %,APSRX,APSRX0,APSRCT,APSRCT0,APSL,APSRCTL
 Q
 ;
EDITRX K DIE,DR S DIE="^AUPNVMED(",DA=APSL
 S %=APSRX0
 S DR=".01///`"_$P(%,U,6)
 ;S APCDALVR("APCDTRX")="`"_$P(%,U,6) ; NEED TO CHECK IF VISIT CHANGED
 S APSX=$P(%,U,10),APSS=$S($L(APSX)<33:APSX,1:$E(APSX,1,31)_"~")
 S DR=DR_";.05///"_APSS
 K APSX,APSS
 ;S DR=DR_";.06///"_(+$P(%,U,7)\1) ;IHS/OHPRD/JCM 6/16/89
 S DR=DR_";.06///"_$P(%,U,7) ;IHS/OKCAO/POC/ENM 5/7/2003
 S DR=DR_";.07///"_+$P(%,U,8)
 ;IHS/DSD/ENM/POC 03/02/98 NEXT 3 LINES CK FOR PROV @ 200 OR 16
 S APSPPROV=$P(%,U,4)
 S APSPPROV=$S($P(^DD(9000010.14,1202,0),"^",3)="DIC(6,":$P(^VA(200,APSPPROV,0),"^",16),1:APSPPROV)
 S DR=DR_";1202////"_APSPPROV
 ;S APC("PRV")=$P(%,U,4) ;IHS/DSD/ENM 01/02/98
 ;S APSPDOC1=$P($G(^VA(200,APC("PRV"),0)),U,16),APCDALVR("P")=$S($P($G(^AUTTSITE(1,0)),U,22):APC("PRV"),1:APSPDOC1) ;IHS/DSD/ENM 01/02/98
 ;S DR=DR_";.09////"_APCDALVR("P") ;IHS/OHPRD/JCM 01/02/98
 ;S DR=DR_";.09////"_$P(%,U,4) ;IHS/OHPRD/JCM 10/10/89
 D CHKDC
 I APSDC S DR=DR_";.08////"_APSDCDT
 K APSDC,APSDCDT
 S:$D(^APSPCCTM) (^APSPCCTM,APSPCCTM)=^APSPCCTM+1,^APSPCCTM(APSPCCTM,1)=$H_"^D"
DIE1 D ^DIE K DIE,DR
 I $D(APSPCCTM) S ^APSPCCTM(APSPCCTM,2)=$H K APSPCCTM
 D:$D(Y) RXERR
 Q
 ;
CHKDC ; CHECK FOR DISCONTINUED RX
 S APSDC=0
 S APSI=0 F APSQ=0:0 S APSI=$O(^PSRX(APSRX,"A",1,APSI)) Q:APSI=""  S APSCOM=$P(^(APSI),U,5) I (APSCOM["CANCELLED")!(APSCOM["DISCONTINUED")!(APSCOM?.E1P1"DC"1P.E) S APSDC=1,APSDCDT=+^(APSI) Q
 K APSI,APSQ,APSCOM
 Q
 ;
EDITREF K DIE,DR S DIE="^AUPNVMED(",DA=APSRCTL
 S %=APSRX0
 S DR=".01///`"_$P(%,U,6)
 ;S APCDALVR("APCDTRX")="`"_$P(%,U,6) ; NEED TO CHECK IF VISIT CHANGED
 S X=$P(%,U,10),DR=DR_";.05///"_$S($L(X)<33:X,1:$E(X,1,31)_"~")
 ;S APSQTY=+$P(%,U,7)\1 ;IHS/OHPRD/JCM 6/16/89
 S APSQTY=+$P(%,U,7)  ;IHS/OKCAO/POC/ENM 5/7/2003
 S APSDAY=$P(%,U,8)
 ;
 S %=APSRCT0
 S DR=DR_";.07///"_((APSDAY*($P(%,U,4)/APSQTY))+.5\1)
 ;S DR=DR_";.06///"_(+$P(%,U,4)\1) ;IHS/OHPRD/JCM 6/16/89
 S DR=DR_";.06///"_$P(%,U,4)  ;IHS/OKCAO/POC/ENM 5/7/2003
 ;IHS/DSD/ENM/POC 03/02/98 NEXT 3 LINES GET PROV FOR REFILLS
 S APSPDOC=$P(^PSRX(APSRX,1,APSRCT,0),"^",17)
 ;S APSPDOC=$S($P(^DD(9000010.14,1202,0),"^",3)="DIC(6,":$P(^VA(200,APSPDOC,0),"^",16),1:X)
 S APSPDOC=$S($P(^DD(9000010.14,1202,0),"^",3)="DIC(6,":$P(^VA(200,APSPDOC,0),"^",16),1:APSPDOC)
 S DR=DR_";1202////"_APSPDOC
 K APSDAY,APSQTY
 ;
 S:$D(^APSPCCTM) (^APSPCCTM,APSPCCTM)=^APSPCCTM+1,^APSPCCTM(APSPCCTM,1)=$H_"^D"
DIE2 D ^DIE K DIE,DR
 I $D(APSPCCTM) S ^APSPCCTM(APSPCCTM,2)=$H K APSPCCTM
 D:$D(Y) REFERR
 Q
 ;
RXERR S APSBR="" G LBULL
REFERR S APSBR=" (refill #"_APSRCT_")" G LBULL
 ;
LBULL ; SEND BULLETIN - LINK FAILURE
 K XMB
 S XMB(1)=+^PSRX(APSRX,0)_APSBR
 S APSPAT=$P(^PSRX(APSRX,0),U,2)
 S XMB(2)=$P(^DPT(APSPAT,0),U,1)_" (DFN "_APSPAT_")"
 S XMB(3)="edited"
 S Y=DT X ^DD("DD")
 S XMB(4)=Y
 S XMB(5)="Edit"
 S XMB="APSP ED FAIL"
 S APSDUZ=DUZ,DUZ=.5 D ^XMB S DUZ=APSDUZ K XMB,APSDUZ,APSBR,APSPAT
 S Y=-1
 Q
VR1 ;IHS/DSD/ENM/POC 03/02/98 NEW SUB ROUTINE
 S APSERDOC=$P(^PSRX(APSRX,0),U,4)
 S APSERDOC=$S($P(^DD(9000010.14,1202,0),"^",3)="DIC(6,":$P(^VA(200,APSERDOC,0),U,16),1:APSERDOC)
 S APCDALVR("APCDTPRV")=APSERDOC
 Q
VR2 ;IHS/DSD/ENM/POC 03/02/98 NEW SUB ROUTINE
 S APSEFDOC=$P(^PSRX(APSRX,1,APSRCT,0),U,17)
 S APSEFDOC=$S($P(^DD(9000010.14,1202,0),"^",3)="DIC(6,":$P(^VA(200,APSEFDOC,0),U,16),1:APSEFDOC)
 S APCDALVR("APCDTPRV")=APSEFDOC
 Q
