APSPDR3 ;IHS/OHPRD/JCM - PHARMACY DRUG RECALL; [ 02/20/2001  1:45 PM ]
 ;;6.0;IHS PHARMACY MODIFICATIONS;**1,3**;09/03/97
 ;THIS ROUTINE BUILDS THE PHARMACY DRUG RECALL LIST
 ;IT RUNS AND THEN CALLS PSOZDR1 TO PRINT THE ACTUAL LIST
 ;**NOTE** MOD MADE TO EXCLUDE  'RETURN TO STOCK DRUGS' 02/08/96
 ;OUTPUT VARIABLES: AD1,AD2,AD3,BD,ED,BDN,CITY,DATE,DRUNAME,HPHONE,
 ;IRXN,PAT,PATN,PSZCHN,PSZDRUG,QTY,RXN,STATE,STATEN,WPHONE,ZIP,DN
 ;^TMP("PSOZDR",$J,PAT,DATE,IRXN),^TMP("PSODR",$J,
 ;I,X,Y
 ;
 ;EXTERNAL CALLS: ^%DT,^DIR,^APSPDR4,^%ZTLOAD,^DIC,^%ZIS
INIT ;
 ;K ^TMP("PSOZDR",$J),^TMP("PSODR",$J)
 K ^TMP("PSOZDR"),^TMP("PSODR") ;IHS/DSD/ENM 05/29/97
 W @IOF
 W !,"Pharmacy Drug Recall List",!!
 S %DT("A")="PLEASE ENTER BEGINNING DATE: "
 S %DT="AE"
 D ^%DT
 I Y=-1 G EXIT
 S BD=Y_.0001
 S %DT("A")="PLEASE ENTER ENDING DATE: "
 D ^%DT
 I Y=-1 G:X="" INIT G EXIT
 S ED=Y_.2359
LKUP ;
 K DIC S DIC="^PSDRUG("
 S DIC(0)="QEMA"
 S DIC("A")="SELECT THE DRUG NAME: "
 D ^DIC
 I Y=-1 G:X="" INIT G:X="^" EXIT G LKUP
 S ^TMP("PSODR",$J,+Y)=""
QUEST ;
 S DIR(0)="YO",DIR("A")="Want to Select Another Drug"
 S DIR("B")="NO"
 S DIR("?")="Enter a 'Y' or 'YES' to include more drugs in your search."
 D ^DIR K DIR
 G:Y=1 LKUP
 G:$D(DIRUT) EXIT
QUE ;
 W !
 S %ZIS="QM"
 D ^%ZIS
 I POP G EXIT
 I $D(IO("Q")),IO=IO(0) W !!,"Sorry, you can't queue to your screen or a slave device.",! K IO("Q") G QUE
 I IO=IO(0)!('$D(IO("Q"))) G DATE
 S ZTRTN="DATE^APSPDR3",ZTIO=ION,ZTSAVE("BD")="",ZTSAVE("ED")=""
 S ZTSAVE("^TMP(""PSODR"",$J,")="",ZTDESC="PHARMACY DRUG RECALL LISTING"
 D ^%ZTLOAD
 G EXIT
DATE ;
 ;IHS/DSD/ENM 02/08/96 LOOP ON "ZAL"
 S (IRXN,APSPNOD,APSPNOD1)=""
 F DATE=BD:0 S DATE=$O(^PSRX("ZAL",DATE)) Q:DATE=""!(DATE>ED)  D
 .F  S IRXN=$O(^PSRX("ZAL",DATE,IRXN)) Q:'IRXN  D
 ..F  S APSPNOD=$O(^PSRX("ZAL",DATE,IRXN,APSPNOD)) Q:APSPNOD=""  D
 ...F  S APSPNOD1=$O(^PSRX("ZAL",DATE,IRXN,APSPNOD,APSPNOD1)) Q:APSPNOD1=""  D CHECK
 D ^APSPDR4
 K:$D(ZTSK) ZTSK ;IHS/DSD/ENM 01/14/97
EXIT ;
 K PATN,STATEN,^TMP("PSOZDR",$J),BDN,QTY,RXN,IRXN,DRUNAME
 K PSOZDN,PSZCHN,PSZDRUG,DATE,AD1,AD2,AD3,CITY,STATE,ZIP,HPHONE,WPHONE
 K PAT,BD,ED,ZTRTN,ZTIO,ZTSAVE("ED"),ZTSAVE("BD"),DN,IO("C"),I,X,Y
 K ZTSAVE("^TMP(""PSODR"",$J,"),ZTDESC,IO("Q"),%ZIS,POP,ZTIO,ZTSK
 K DIC,DFOUT,DLOUT,DTOUT,DUOUT,%DT,DIRUT,DIR,APSPNOD,APSPNOD1
 Q
CHECK ;
 Q:'$D(^PSRX(IRXN,0))
 S DRUNAME=$P($G(^PSRX(IRXN,0)),"^",6) Q:$G(DRUNAME)']""  ;DRUG NUMBER FOR THE DRUG FILE
 I '$D(^PSDRUG(DRUNAME,0)) Q
 I $D(^TMP("PSODR",$J,DRUNAME)) D RXN
 Q
RXN ;
 I $P($G(^PSRX(IRXN,0)),"^",15)=13 Q  ;IHS/DSD/ENM 02/09/96 CK FOR CANCELLED
 I APSPNOD1="N"&($P($G(^PSRX(IRXN,2)),"^",15)]"") Q  ;IHS/DSD/ENM 02/08/96 CK RETURN TO STOCK FOR NEW RX
 ;IHS/DSD/ENM 05/18/98 NEXT 2 LINES COPIED MODIFIED
 ;I APSPNOD1="R"&($P($G(^PSRX(IRXN,1,APSPNOD,0)),"^",18)]"") Q  ;IHS/DSD/ENM 02/08/96 CK RTN TO STOCK FOR REFILL RX
 ;I APSPNOD1="P"&($P($G(^PSRX(IRXN,"P",APSPNOD,0)),"^",19)]"") Q  ;IHS/DSD/ENM 02/08/96 CK RTN TO STOCK FOR PARTIAL
 I APSPNOD1="R"&($P($G(^PSRX(IRXN,1,APSPNOD,0)),"^",16)]"") Q  ;IHS/DSD/ENM 02/08/96 CK RTN TO STOCK FOR REFILL RX
 I APSPNOD1="P"&($P($G(^PSRX(IRXN,"P",APSPNOD,0)),"^",16)]"") Q  ;IHS/DSD/ENM 02/08/96 CK RTN TO STOCK FOR PARTIAL
 S RXN=$P(^PSRX(IRXN,0),"^",1) ;PRESCRIPTION NUMBER ON FILE
 S PAT=$P(^PSRX(IRXN,0),"^",2) ;PATIENT NUMBER FOR THE PERSON FILE
 S PATN=$P(^DPT(PAT,0),"^",1) ;PATIENTS ACTUAL NAME
 ;PATIENTS ADDRESS
 S AD1=$S($D(^DPT(PAT,.11)):$P(^DPT(PAT,.11),"^",1),1:"ADDRESS UNKNOWN")
 S AD2=$S(AD1'["UNKNOWN":$P(^DPT(PAT,.11),"^",2),1:"")
 S AD3=$S(AD1'["UNKNOWN":$P(^DPT(PAT,.11),"^",3),1:"")
 S CITY=$S(AD1'["UNKNOWN":$P(^DPT(PAT,.11),"^",4),1:"")
 S STATEN=$S(AD1'["UNKNOWN":$P(^DPT(PAT,.11),"^",5),1:"")
 S STATE=$S(AD1'["UNKNOWN":$P(^DIC(5,STATEN,0),"^",2),1:"")
 S ZIP=$S(AD1'["UNKNOWN":$P(^DPT(PAT,.11),"^",6),1:"")
 ;PHONE NUMBER
 S HPHONE="UNKNOWN",WPHONE="UNKNOWN"
 I $D(^DPT(PAT,.13)) S:$P(^DPT(PAT,.13),"^",1)'="" HPHONE=$P(^DPT(PAT,.13),"^",1)
 I $D(^DPT(PAT,.13)) S:$P(^DPT(PAT,.13),"^",2)'="" WPHONE=$P(^DPT(PAT,.13),"^",2)
 S BDN=$P(^DPT(PAT,0),"^",3) ;BIRTH DATE
 S BDN=$E(BDN,4,5)_"-"_$E(BDN,6,7)_"-"_$E(BDN,2,3)
 ;
 ;TO GET PATIENTS CHART NUMBER
 S PSZCHN="UNKNOWN" I '$D(DUZ(2)) S DUZ(2)=$O(^DIC(59,0)) I DUZ(2)>0,$D(^(DUZ(2),0)) S DUZ(2)=$P(^(0),"^",6)
 I DUZ(2)>0,$D(^AUPNPAT(PAT,41,DUZ(2),0)) S PSZCHN=$P(^(0),"^",2)
 ;
 S QTY=+$P(^PSRX(IRXN,0),"^",7) ;QTY OF MEDICATION ISSUED
 S PSZDRUG=$P(^PSDRUG(DRUNAME,0),"^",1) ;DRUG NAME
 ;SET ARRAY
 S ^TMP("PSOZDR",$J,PAT,DATE,IRXN)=PATN_"^"_PSZCHN_"^"_BDN_"^"_DATE_"^"_PSZDRUG_"^"_QTY_"^"_RXN_"^"_AD1_"^"_AD2_"^"_AD3_"^"_CITY_"^"_STATE_"^"_ZIP_"^"_HPHONE_"^"_WPHONE
 Q
