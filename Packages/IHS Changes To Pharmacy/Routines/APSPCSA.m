ATTEMPT(PROV,ITEM,FLAG,RE) ;EP  Store an attempt to order a narcotic that is rejected
 N FNUM,ERR,DIERR,IENS,TXT,DFN,LIEN,SIEN,DEA
 S FNUM=9009036
 Q:'+ITEM
 Q:'+PROV
 S LIEN=0
 S DFN=$$GETVAR^CIANBUTL("PATIENT.ID.MRN",,"CONTEXT.PATIENT")
 Q:'+DFN
 S TXT=$S($P(RE,U,1)=1:"No current DEA",$P(RE,U,1)=2:"Unauthorized for schedule "_$P(RE,U,2),1:"DEA failure")
 K FDA,ERR,DIERR,IENS
 S FDA(FNUM,"+1,",.01)=DFN
 S FDA(FNUM,"+1,",.02)="@"
 S FDA(FNUM,"+1,",.03)=PROV
 S FDA(FNUM,"+1,",.04)=$$NOW^XLFDT
 S FDA(FNUM,"+1,",.09)=DUZ(2)
 D UPDATE^DIE("","FDA","IENS","ERR")
 I $D(IENS(1)) S LIEN=IENS(1)
 Q:'+LIEN
 K FDA,ERR,IENS
 S AIEN="+1,"_LIEN_","
 S DEA=$O(^APSPAUDF("B","AT",""))
 S FDA(9009036.01,AIEN,.01)="AT"
 S FDA(9009036.01,AIEN,.02)=$$NOW^XLFDT
 S FDA(9009036.01,AIEN,2.1)=ITEM
 S FDA(9009036.01,AIEN,.05)=FLAG
 S FDA(9009036.01,AIEN,.06)=DEA
 S FDA(9009036.01,AIEN,.07)=DUZ
 S FDA(9009036.01,AIEN,.04)=TXT
 D UPDATE^DIE("","FDA","IENS","ERR")
 D LOGBUSA^APSPCSA1("","",LIEN,IENS(1))
 K FDA,IENS,ERR
 Q
