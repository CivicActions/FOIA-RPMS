CHKLOG(LST,IEN,FLAG) ;EP Check the audit file for tampering
 ;IEN of entry in log is input
 ;Return is LST=0 for all good or LST=number of bad entries
 ;LST(n)=IEN of sub entry changed and ^ status when changed
 N SUB,CNT,RECHK,OLD
 S CNT=0,FLAG=$G(FLAG)
 S SUB=0 F  S SUB=$O(^APSPCSA(IEN,1,SUB)) Q:'+SUB  D
 .S OLD=$P($G(^APSPCSA(IEN,1,SUB,2)),U,1)
 .I OLD'="" D
 ..S RECHK=$$CHECKSUM(IEN,SUB)
 ..I OLD'=RECHK S CNT=CNT+1
 ..I FLAG=1&(CNT>0) D
 ...S LST(CNT)=SUB_"^"_$P($G(^APSPCSA(IEN,1,SUB,0)),U,6)
 S LST(0)=CNT
 Q
