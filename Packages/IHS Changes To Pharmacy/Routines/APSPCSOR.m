 ..I +ORRX D
 ...S BACKDOOR=$P($G(^PSRX(ORRX,"PKI")),U,2)
 ...I BACKDOOR=1 S IND="Yes"
 ..I ORRX="" S DA=$P($G(^OR(100,ORIFN,3)),"^",3) I +DA S DIC=100.01,DR=.01,DIQ="ORS" D EN^DIQ1 S ORRX=ORS(100.01,DA,.01)
 ..S ORRX=ORRX,ORPNM=$P(DATA5,"^"),ORDRUG=$E($P(DATA1,"^",2),1,22),ORDEA=$E($P(DATA1,"^",4))
 ..S PRVNME=$P(DATA3,U,3),ORDTE=$P(DATA1,U,1),PRV=$P(DATA3,U,4),ORPT=$P(DATA5,U,2),DRG=$P(DATA1,U,3)
 ..S DRCL=+$P($G(^PSDRUG(DRG,0)),U,3)
 ..Q:DRCL=2&(APSPDCLS=3)
 ..Q:DRCL'=2&(APSPDCLS=1)
 ..S STR=ORIFN_U_ORDTE_U_PRV_U_ORPT_U_DRG_U_ORDRUG_U_DRCL_U_PRVNME_U_ORPNM_U_IND
 ..S CNT=CNT+1
 ..S ^TMP("CS","DRG",$J,PRVNME,ORDRUG,CNT)=STR
 ..S ^TMP("CS","DTE",$J,PRVNME,ORDTE,CNT)=STR
 ..S ^TMP("CS","PT",$J,PRVNME,ORPNM,CNT)=STR
