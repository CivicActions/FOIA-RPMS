 ..S ROMAN=$S(CLS=2:"II",CLS=3:"III",CLS=4:"IV",CLS=5:"V",1:"")
 ..S LCNT=LCNT+1,PCNT=PCNT+1,^TMP("STOUT",$J,LCNT)="S4"_U_"CLASS "_ROMAN_": "_$G(STATS("CLASS",PRV,CLS))
