APSPDRX ; IHS/DSD/ENM - DAILY RX LOG 5.13.94 ;  [ 08/23/1999  12:37 PM ]
 ;;6.0;IHS PHARMACY MODIFICATIONS;**2**;09/03/97
EP ;ENTRY POINT
INIT ;
 D:'$D(PSOPAR) ^PSOLSET I '$D(PSOPAR) W !,"No Site Param's Defined!..quitting." Q  ;IHS/DSD/ENM 01/28/96
 S APSPDIV=$S($D(^PS(59,PSOSITE,0)):$P(^(0),U,6),1:"") ;SITE NBR
 W @IOF
 W "Pharmacy Daily Rx Report",!!
 S %DT("A")="Enter the Start Date: "
 S %DT="AE"
 D ^%DT
 I Y=-1 G EXIT
 S APSPFD=Y_.0001
 S %DT("A")="Ending Date: "
 S %DT("B")="TODAY",%DT="AEP"
 D ^%DT
 I Y=-1 G EXIT
 S APSPED=Y_.2359 Q:Y<0
 ;SELECT DIVISION
 S DIR(0)="Y",DIR("A")="Would you like all divisions",DIR("B")="YES",DIR("?")="Enter 'Yes' or 'No'" D ^DIR K DIR Q:$D(DTOUT)
 I X="YES" S APSPANS="A" G ZZ
 S DIR(0)="PO^59:EMZ",DIR("A")="Select Division",DIR("?")="Enter the Division Name or Number "
 D ^DIR G:$D(DTOUT)!$D(DUOUT) EXIT K DIR
 I Y=-1 G EXIT ;IHS/DSD/ENM 01/29/96
 S APSPANS=+Y
ZZ ;ZIS CALL IHS/DSD/ENM 03/30/99 NEXT 8 LINES MODIFIED
 K %ZIS,IOP,ZTSK,POP S %ZIS="QM",%ZIS("A")="Select Printer: "
 D ^%ZIS K %ZIS I POP G EXIT
 I $D(IO("Q")),IO=0 W !,"QUEUEING TO YOUR SCREEN IS NOT ALLOWED! " K IO("Q") G ZZ
 I IO=IO(0)!('$D(IO("Q"))) G FST
 S ZTDESC="DAILY RX REPORT",ZTRTN="FST^APSPDRX"
 F G="ZTDESC","APSPDIV","PSOSITE","APSPFD","APSPED","APSPANS" S ZTSAVE(G)=""
 D ^%ZTLOAD W:$D(ZTSK) !,"REPORT QUEUED TO PRINT !",! K ZTSK G EXIT
 ;K PSOION I $D(IO("Q")) S ZTDESC="DAILY RX REPORT",ZTRTN="FST^APSPDRX" F G="ZTDESC","APSPDIV","PSOSITE","APSPFD","APSPED","APSPANS" S:$D(@G) ZTSAVE(G)="" D ^%ZTLOAD W:$D(ZTSK) !,"REPORT QUEUED TO PRINT !",! K ZTSK G EXIT
FST U IO K ^TMP($J,"APSPX") S (APSPG,APSPDT,APSPRN,APSPLN,APSPTY)=0,APSPOUT=""
 F APSPDT=APSPFD:0 S APSPDT=$O(^PSRX("ZAL",APSPDT)) Q:'APSPDT!(APSPDT>APSPED)  D PRT
 D PRNT W !!,"End of Report"
 G EXIT
PRT ;
 F  S APSPRN=$O(^PSRX("ZAL",APSPDT,+APSPRN)) Q:'APSPRN  D PR1
 Q
PR1 F  S APSPLN=$O(^PSRX("ZAL",APSPDT,APSPRN,+APSPLN)) Q:'APSPLN  D PR2
 Q
PR2 F  S APSPTY=$O(^PSRX("ZAL",APSPDT,APSPRN,APSPLN,APSPTY)) Q:APSPTY=""  D DSET
 Q
DSET ;
 S APSPRX=$P($G(^PSRX(APSPRN,0)),U),APSPDFN=$P($G(^(0)),U,2),APSPDG=$P($G(^(0)),U,6)
 ;S APSPN=$P($G(^DPT(APSPDFN,0)),U),APSPDRG=$P($G(^PSDRUG(APSPDG,0)),U),APSPCN=$P($G(^AUPNPAT(APSPDFN,41,APSP("DIV"),0)),U,2)
 S APSPN=$P($G(^DPT(APSPDFN,0)),U),APSPDRG=$P($G(^PSDRUG(APSPDG,0)),U)
 I APSPTY="N" D NRX Q
 I APSPTY="R" D RRX Q
 I APSPTY="P" D PRX Q
 Q
NRX ;GRAB NEW RX DATA
 S APSPD=$P($G(^PSRX(APSPRN,0)),U,4),APSPQ=$P($G(^(0)),U,7),APSPP=$P($G(^VA(200,APSPD,0)),U),APSP("DIV")=$P($G(^PSRX(APSPRN,2)),U,9)
 S APSP("D")=$P($G(^PS(59,APSP("DIV"),0)),U,6)
 S APSPCN=$P($G(^AUPNPAT(APSPDFN,41,APSP("D"),0)),U,2)
 I APSPANS="A" S ^TMP($J,"APSPX",APSP("DIV"),APSPDT,APSPRX)=APSPRX_U_APSPN_U_APSPDRG_U_APSPP_U_APSPQ_U_APSPCN_U_APSPTY Q
 I +APSPANS=APSP("DIV") S ^TMP($J,"APSPX",APSP("DIV"),APSPDT,APSPRX)=APSPRX_U_APSPN_U_APSPDRG_U_APSPP_U_APSPQ_U_APSPCN_U_APSPTY
 Q
RRX ;GRAB REFILL RX DATA
 S APSPD=$P($G(^PSRX(APSPRN,1,APSPLN,0)),U,17),APSPQ=$P($G(^(0)),U,4),APSPP=$P($G(^VA(200,APSPD,0)),U),APSP("DIV")=$P($G(^PSRX(APSPRN,1,APSPLN,0)),U,9)
 S APSP("D")=$P($G(^PS(59,APSP("DIV"),0)),U,6)
 S APSPCN=$P($G(^AUPNPAT(APSPDFN,41,APSP("D"),0)),U,2)
 I APSPANS="A" S ^TMP($J,"APSPX",APSP("DIV"),APSPDT,APSPRX)=APSPRX_U_APSPN_U_APSPDRG_U_APSPP_U_APSPQ_U_APSPCN_U_APSPTY Q
 I +APSPANS=APSP("DIV") S ^TMP($J,"APSPX",APSP("DIV"),APSPDT,APSPRX)=APSPRX_U_APSPN_U_APSPDRG_U_APSPP_U_APSPQ_U_APSPCN_U_APSPTY
 Q
PRX ;GRAB PARTIAL RX DATA
 S APSPD=$P($G(^PSRX(APSPRN,"P",APSPLN,0)),U,17),APSPQ=$P($G(^(0)),U,4),APSPP=$P($G(^VA(200,APSPD,0)),U),APSP("DIV")=$P($G(^PSRX(APSPRN,"P",APSPLN,0)),U,9)
 S APSP("D")=$P($G(^PS(59,APSP("DIV"),0)),U,6)
 S APSPCN=$P($G(^AUPNPAT(APSPDFN,41,APSP("D"),0)),U,2)
 I APSPANS="A" S ^TMP($J,"APSPX",APSP("DIV"),APSPDT,APSPRX)=APSPRX_U_APSPN_U_APSPDRG_U_APSPP_U_APSPQ_U_APSPCN_U_APSPTY Q
 I +APSPANS=APSP("DIV") S ^TMP($J,"APSPX",APSP("DIV"),APSPDT,APSPRX)=APSPRX_U_APSPN_U_APSPDRG_U_APSPP_U_APSPQ_U_APSPCN_U_APSPTY
 Q
PRNT S (APSPDP,APSPZX)="" D HDR,DSPL
 Q
DSPL ;GET DATA FROM TMP GBL
 S APSP("DIV")="" F  S APSP("DIV")=$O(^TMP($J,"APSPX",APSP("DIV"))) Q:'APSP("DIV")  D  ;GET DIVISION 
 .S APSP("DV")=$P($G(^PS(59,APSP("DIV"),0)),U)
 .F  S APSPDP=$O(^TMP($J,"APSPX",APSP("DIV"),APSPDP)) Q:'APSPDP!($G(APSPOUT))  F  S APSPZX=$O(^TMP($J,"APSPX",APSP("DIV"),APSPDP,APSPZX)) Q:'APSPZX!($G(APSPOUT))  D DSPS Q:$G(APSPOUT)
 Q
DSPS S APSP=^TMP($J,"APSPX",APSP("DIV"),APSPDP,APSPZX),APSPN=$P(APSP,U,2),APSPDRG=$P(APSP,U,3),APSPP=$P(APSP,U,4),APSPQ=$P(APSP,U,5),APSPCN=$P(APSP,U,6)
 S APSPTY=$P(APSP,U,7),APSPTYP=$S(APSPTY="N":"NEW RX",APSPTY="R":"REFILL",APSPTY="P":"PARTIAL",1:"")
 D:$Y+4>IOSL HDR Q:$G(APSPOUT)
 S Y=$P(APSPDP,".")_"."_$E($P(APSPDP,".",2),1,4) X ^DD("DD") S APSPDT=Y
 W !,"Rx #: "_APSPZX,?12,"Name: "_APSPN,?37,"Chart #: "_APSPCN,?53,"D/Time: "_APSPDT,!,"DRUG: "_APSPDRG,?37,"Qty: "_APSPQ,?47,"Provider: "_APSPP,!,"Division: "_APSP("DV"),?37,APSPTYP,!
 Q
HDR I APSPG,$E(IOST)="C" K DIR S DIR(0)="FO",DIR("A")="Press Return to Continue or ""^"" to Exit" D ^DIR I X["^" S APSPOUT=1 Q
 S APSPG=APSPG+1 D NOW^%DTC W @IOF,?38,"(",APSPG,")",!,"DAILY PRESCRIPTION ACTIVITY REPORT" S Y=$P(%,".",1)_"."_$E($P(%,".",2),1,4) X ^DD("DD") W ?51,"Date: ",?59,Y,! F I=1:1:80 W "."
 S Y=APSPFD X ^DD("DD") S APSPFDZ=Y,Y=APSPED X ^DD("DD") S APSPEDZ=Y
 W !,?10,"For Rx's dispensed from "_APSPFDZ_" to "_APSPEDZ,!!
 Q
EXIT ;
 W ! D ^%ZISC K %DT("A"),%DT("B"),APSPFD,APSPED,APSPRN,APSP,APSPCN,APSPD,APSPDFN,APSPDG,APSPDIV,APSPDP,APSPDRG,APSPDT,APSPED,APSPTYP,ZZ
 K DIR,APSPG,APSPOUT,APSPEDZ,APSPFD,APSPFDZ,APSPLN,APSPN,APSPP,APSPQ,APSPRN,APSPRX,APSPTY,APSPZX
 Q
