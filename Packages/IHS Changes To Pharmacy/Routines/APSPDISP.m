APSPDISP ; IHS/DSD/ENM - BHAM ISC/SAB/IHS/DSD/ENM - AUTO RX RELEASE FUNCTION ;  [ 01/27/2003  11:14 AM ]
 ;;6.0;IHS PHARMACY MODIFICATIONS;**3,4**;09/03/97
 ;ORIGINAL ROUTINE IS PSODISP
AC N DA,DR,DIE,X,X1,X2,Y,CX,PX,REC,DIR,YDT,REC,RDUZ,DIRUT,PSOCPN,PSOCPRX,YY,QDRUG,QTY,TYPE,XTYPE,DUOUT
AC1 W:'$G(APSPNOPR) ! S PSIN=+$P($G(^PS(59.7,1,49.99)),"^",2) ;IHS/OKCAO/POC 7/7/98 CONDITIONAL WRITE
 ;S DIC("S")="I $D(^XUSEC(""PSORPH"",+Y))",DIC("A")="Enter PHARMACIST: ",DIC="^VA(200,",DIC(0)="QEAM" D ^DIC G:"^"[X EXIT K DIC G:$D(DTOUT)!($D(DUOUT))!($D(DIRUT))!(Y=-1) EXIT S PSRH=+Y
BC K ISUF,DIR,LBL,LBLP ;S DIR("A")="Enter/Wand PRESCRIPTION number",DIR("?")="^D HELP^PSODISP",DIR(0)="FO" D ^DIR
 ;I $D(DIRUT)!($D(DTOUT))!($D(DUOUT)) K DIRUT,DTOUT,DUOUT G AC1
 ;I X'["-" D BCI W:'$G(RXP) !,"INVALID PRESCRIPTION NUMBER" G:'$G(RXP) BC G BC1 
 ;I X["-",$P(X,"-")'=$P(^DD("SITE",1),"^") W !?7,*7,*7,"   INVALID STATION NUMBER !!",*7,*7,! G BC 
 ;I X["-" S RXP=$P(X,"-",2) I '$D(^PSRX(+$G(RXP),0))!($G(RXP)']"") W !?7,*7,*7,*7,"   NON-EXISTENT PRESCRIPTION" G BC 
 G:$D(^PSRX(RXP,0)) BC1
 Q
 ;W !?7,*7,*7,*7,"   IMPROPER BARCODE FORMAT" G BC
BC1 ;
 I +$P($G(^PSRX(+RXP,0)),"^",15)=13!+$P($G(^PSRX(+RXP,0)),"^",2)=0 W:'$G(APSPNOPR) !?7,*7,*7,"    PRESCRIPTION IS A DELETED PERSCRIPTION NUMBER" Q  ;IHS/OKCAO/POC 7/7/98
 I +$P($G(^PSRX(+RXP,0)),"^",15),$S($P(^(0),"^",15)=2:0,$P(^(0),"^",15)=5:0,$P(^(0),"^",15)=11:0,$P(^(0),"^",15)=12:0,1:1) D STAT,EXIT Q
 I $P(^PSRX(RXP,2),"^",13) S Y=$P(^PSRX(RXP,2),"^",13) X ^DD("DD") S OUT=1 D  D EXIT Q
 .W:'$G(APSPNOPR) !?7,*7,*7,"ORIGINAL PRESCRIPTION WAS LAST RELEASED ON "_Y,!?7,"CHECKING FOR UNRELEASED REFILLS/PARTIALS " D REF ;IHS/OKCAO/POC 7/7/98
BATCH ;
 I $P(^PSRX(RXP,2),"^",15) S RESK=$P(^(2),"^",15)  W:'$G(APSPNOPR) !,"Original Fill returned to stock on "_$E(RESK,4,5)_"/"_$E(RESK,6,7)_"/"_$E(RESK,2,3),! G REF ;IHS/OKCAO/POC 7/7/98
 S PSOCPN=$P(^PSRX(RXP,0),"^",2),QTY=$P($G(^PSRX(RXP,0)),"^",7),QDRUG=$P(^PSRX(RXP,0),"^",6)
 ;ORIGINAL
 I '$P($G(^PSRX(RXP,2)),"^",13),+$P($G(^(2)),"^",2)'<PSIN S RXFD=$P(^(2),"^",2) D  D:$G(LBLP) UPDATE Q:+$G(OUT)  I $G(ISUF) D UPDATE G REF
 .S SUPN=$O(^PS(52.5,"B",RXP,0)) I SUPN,$D(^PS(52.5,"C",RXFD,SUPN)),$G(^PS(52.5,SUPN,"P"))'=1 S ISUF=1 Q
 .F LBL=0:0 S LBL=$O(^PSRX(RXP,"L",LBL)) Q:'LBL  I '+$P(^PSRX(RXP,"L",LBL,0),"^",2) S LBLP=1
 .Q:'$G(LBLP)  S:$D(^PSDRUG(QDRUG,660.1)) ^PSDRUG(QDRUG,660.1)=^PSDRUG(QDRUG,660.1)-QTY
 .;D NOW^%DTC S DIE="^PSRX(",DA=RXP,DR="31///"_%_";23///"_PSRH
 .D NOW^%DTC S DIE="^PSRX(",DA=RXP,DR="31///"_%_";23///`"_PSRH ;IHS/OKCAO/POC 2/25/00 SINCE THIS IS AN INTERNAL NUMBER BETTER PUT A TICK IN
 .L +^PSRX(DA):20 D ^DIE K DIE,DR L -^PSRX(DA) K DA,LBL
REF ;release refills and partials
 K LBLP F XTYPE=1,"P" D QTY
 S:'XTYPE $P(^PSRX(RXP,"TYPE"),"^")=0
 ;Q:+$G(OUT)
 I +$G(OUT) D EXIT Q
UPDATE I $G(ISUF) W:'$G(APSPNOPR) *7,!?7,"ORIGINAL FILL ON SUSPENSE !",!,*7 Q  ;IHS/OKCAO/POC 7/7/98
 ;I +$G(^PSRX(RXP,"IB")) S PSOCPRX=$P(^PSRX(RXP,0),"^") D CP^PSOCP
 W:'$G(APSPNOPR) !?7,"PRESCRIPTION NUMBER "_$P(^PSRX(RXP,0),"^")_" RELEASED" ;IHS/OKCAO/POC 7/7/98
 S OUT=1
 Q
EXIT K OUT,RX2,RXFD,RESK,ISUF,SUPN,%,DIC,IFN,J,DA,DR,DIE,X,X1,X2,Y,RXP,CX,PX,REC,DIR,YDT,REC,RDUZ,DIRUT,PSOCPN,PSOCPRX,YY,QDRUG,QTY,TYPE,XTYPE,DUOUT,PSRH,XX,Y,PSIN
 Q
HELP ;W !!,"Wand the barcode number of the prescription or manually key in",!,"the number below the barcode or the prescription number.",!,"The barcode number should be of the format - 'NNN-NNNNNNN'"
 Q
BCI S RXP=0
RXP S RXP=$O(^PSRX("B",X,RXP)) I $P($G(^PSRX(+RXP,0)),"^",15)=13 G RXP ;GET RECORD NUMBER FROM SCRIPT NUMBER
 Q
QTY S PSOCPN=$P(^PSRX(RXP,0),"^",2),QDRUG=$P(^PSRX(RXP,0),"^",6) K LBLP
 F YY=0:0 S YY=$O(^PSRX(RXP,XTYPE,YY)) Q:'YY  D:$P($G(^PSRX(RXP,XTYPE,YY,0)),"^")'<PSIN  K ISUF,LBLP S OUT=1
 .S RXFD=$E($P(^PSRX(RXP,XTYPE,YY,0),"^"),1,7),SUPN=$O(^PS(52.5,"B",RXP,0)) I SUPN,$D(^PS(52.5,"C",RXFD,SUPN)),$G(^PS(52.5,SUPN,"P"))'=1 S ISUF=1 Q
 .I $P(^PSRX(RXP,XTYPE,YY,0),"^",$S($G(XTYPE):18,1:19))]""!($P(^(0),"^",16)) K IFN Q
 .F LBL=0:0 S LBL=$O(^PSRX(RXP,"L",LBL)) Q:'LBL  I $P(^PSRX(RXP,"L",LBL,0),"^",2)=$S('XTYPE:(99-YY),1:YY) S LBLP=1
 .Q:'$G(LBLP)  S IFN=YY S:$G(^PSDRUG(QDRUG,660.1))]"" QTY=$P(^PSRX(RXP,XTYPE,YY,0),"^",4),^PSDRUG(QDRUG,660.1)=^PSDRUG(QDRUG,660.1)-QTY
 .;IHS/DSD/ENM 10/30/96 NEXT LINE COPIED/MOD FORCE DIE SET OF REL DT
 .;D NOW^%DTC S $P(^PSRX(RXP,XTYPE,YY,0),"^",$S($G(XTYPE):18,1:19))=%,^PSRX($S($G(XTYPE):"AL",1:"AM"),%,RXP,YY)="",$P(^PSRX(RXP,XTYPE,YY,0),"^",5)=PSRH
 .D NOW^%DTC I XTYPE=1 S DIE="^PSRX("_RXP_","_XTYPE_",",DA(1)=RXP,DA=YY,DR=17_"///"_%,^PSRX($S($G(XTYPE):"AL",1:"AM"),%,RXP,YY)="",$P(^PSRX(RXP,XTYPE,YY,0),"^",5)=PSRH ;IHS/DSD/ENM 11/01/96
 .I XTYPE="P" S DA(1)=RXP,DIE="^PSRX("_DA(1)_",""P"",",DA=YY,DR=8_"///"_%,^PSRX($S($G(XTYPE):"AL",1:"AM"),%,RXP,YY)="",$P(^PSRX(RXP,XTYPE,YY,0),"^",5)=PSRH ;IHS/DSD/ENM 11/01/96
 .L +^PSRX(RXP):20 D ^DIE K DIE,DR L -^PSRX(RXP) K DA ;IHS/DSD/ENM 10/30/96
 .K:XTYPE ^PSRX("ACP",$P($G(^PSRX(RXP,0)),"^",2),$P($G(^PSRX(RXP,1,YY,0)),"^"),YY,RXP)
 .I XTYPE,+$G(^PSRX(RXP,"IB")),$G(IFN),'$G(ISUF) S PSOCPRX=$P(^PSRX(RXP,0),"^") D CP^PSOCP
 ;W:$G(IFN) !?7,"PRESCRIPTION NUMBER "_$P(^PSRX(RXP,0),"^")_$S('$G(XTYPE):" Partial Fill",1:" REFILL(s)")_" RELEASED" 
 I $G(IFN),'$G(APSPNOPR) W !?7,"PRESCRIPTION NUMBER "_$P(^PSRX(RXP,0),"^")_$S('$G(XTYPE):" Partial Fill",1:" REFILL(s)")_" RELEASED" ;IHS/OKCAO/POC 7/7/98
 ;W:'$G(IFN) !?7,"NO "_$S($G(XTYPE):"REFILL(s)",1:"PARTIAL(s)")_" TO BE RELEASED"
 I '$G(IFN),'$G(APSPNOPR) W !?7,"NO "_$S($G(XTYPE):"REFILL(s)",1:"PARTIAL(s)")_" TO BE RELEASED" ;IHS/OKCAO/POC 7/7/98
 K IFN
 Q
STAT S RX0=^PSRX(RXP,0),RX2=^PSRX(RXP,2),J=RXP D ^PSOFUNC
 W:'$G(APSPNOPR) !,*7,*7,"    Prescription has a status of "_ST_" and is not eligible for release",!,"    Please check with a Pharmacist" ;IHS/OKCAO/POC 7/7/98
 K RX0,ST Q
