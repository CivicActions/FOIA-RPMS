NWORDRPC(RET,REQIEN,DFN,PROV,LOC) ;-
 N VAL,FN,FDA
 S VAL=$$NEWORD(REQIEN,DFN,PROV,+$G(LOC))
 I +VAL>0 D
 .S FN=9009033.91
 .S FDA(FN,REQIEN_",",.03)=1
 .S FDA(FN,REQIEN_",",.02)=+VAL
 .D FILE^DIE("K","FDA")
 S RET=+VAL>0
 Q
