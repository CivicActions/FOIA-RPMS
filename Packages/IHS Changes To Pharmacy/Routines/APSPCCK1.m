APSPCCK1 ; IHS/DSD/ENM - CREATE PCC NEW RX LINKAGE ;  [ 01/13/2003  10:53 AM ]
 ;;6.0;IHS PHARMACY MODIFICATIONS;**4**;11/17/2002
 ; Only called from APSPCCK to create linkage in real time not
 ; background.
 ;
START S APSPCCK1=^PSRX(APSPCCK("IRXN"),0),(APSPCCK1("VMDFN"),APSPCCK1("VDFN"))=""
VISIT I '$D(APCDALVR("APCDVSIT")) D GVISIT G:'$D(APCDALVR("APCDVSIT")) EXIT S APSPCCK1("VDFN")=APCDALVR("APCDVSIT")
VMED K APCDALVR("APCDADFN") D GVMED G:'$D(APCDALVR("APCDADFN")) EXIT S APSPCCK1("VMDFN")=APCDALVR("APCDADFN")
RX ;
 I 'APSPCCK("RFN"),$D(^PSRX(APSPCCK("IRXN"))) NEW DIE,DR,DA S DIE="^PSRX(",DR="9999999.11////"_APCDALVR("APCDADFN"),DA=APSPCCK("IRXN") D ^DIE K DA,DIE,DR ;IHS/OHPRD/JCM 6/11/90
 I APSPCCK("RFN"),$D(^PSRX(APSPCCK("IRXN"),1,APSPCCK("RFN"))) N DR,DA,DIE S DIE="^PSRX(APSPCCK(""IRXN""),1,",DA(1)=APSPCCK("IRXN"),DA=APSPCCK("RFN"),DR="9999999.11////"_APCDALVR("APCDADFN") D ^DIE K DIE,DA,DR ;IHS/OHPRD/JCM 6/11/90
EXIT S ^TMP("APSPCCK",APSPCCK("IRXN"),APSPCCK("VMDFN"),APSPCCK("VDFN"))=APSPCCK1("VMDFN")_U_APSPCCK1("VDFN")
 K APSPCCK1,X,Y
 Q
 ;
GVISIT ;
 ;W !,"Creating a visit to which prescriptions will link .. "
 S APCDALVR("APCDAUTO")="",APCDALVR("APCDANE")=""
 S AUPNTALK=""
 D ^APCDALV
 K APCDALVR("APCDAUTO"),APCDALVR("APCDANE"),AUPNTALK
 G:$D(APCDALVR("APCDAFLG")) @("V"_APCDALVR("APCDAFLG"))
 Q
 ;
GVMED ;
 S APCDALVR("APCDTRX")="`"_$P(APSPCCK1,U,6)
 ;S X=$P(APSPCCK1,U,10),APCDALVR("APCDTSIG")=$S($L(X)<33:X,1:$E(X,1,31)_"~")
 S X=$P(APSPCCK1,U,10),APCDALVR("APCDTSIG")=$S($L(X)>145:$E(X,1,144)_"~",1:X)  ;IHS/OKCAO/POC 11/17/2002 DONT TRUNCATE IF LESS THAN 145 THE SIZE OF SIG IN V MED 
 S APCDALVR("APCDTQTY")=+$P(APSPCCK1,U,7)\1
 S APCDALVR("APCDTDAY")=$P(APSPCCK1,U,8)
 S APCDALVR("APCDTDIS")=""
 ;
 S APCDALVR("APCDATMP")="[APCDALVR 9000010.14 (ADD)]"
 K APCDALVR("APCDAFLG")
 D ^APCDALVR
 G:$D(APCDALVR("APCDAFLG")) @APCDALVR("APCDAFLG")
 Q
 ;
V2 S APSPCCK1("ERROR")="inability to create visit",APSPCCK1("BN")="V" G LBULL
V3 S APSPCCK1("ERROR")="invalid visit parameters (date, location, etc.)",APSPCCK1("BN")="V" G LBULL
 ;
1 S APSPCCK1("ERROR")="incorrect template specification",APSPCCK1("BN")="VMED" G LBULL
2 S APSPCCK1("ERROR")="invalid values being passed to V MED",APSPCCK1("BN")="VMED" G LBULL
 ;
LBULL ; SEND BULLETIN - LINK FAILURE
 K XMB
 S XMB(1)=+APSPCCK1
 S XMB(2)=$P(^DPT(APSPCCK("PSDFN"),0),U,1)_" (DFN "_APSPCCK("PSDFN")_")"
 S XMB(3)=$S(APSPCCK("RFN")=0:"established",1:"refilled")
 S Y=DT X ^DD("DD")
 S XMB(4)=Y
 S XMB(5)=APSPCCK1("ERROR")
 S XMB="APSP LINK FAIL "_APSPCCK1("BN")
 S APSPCCK("DUZ")=DUZ,DUZ=.5 D ^XMB S DUZ=APSPCCK("DUZ") K XMB,APSPCCK("DUZ"),APSPCCK1("ERROR"),APSPCCK1("BN")
 Q
