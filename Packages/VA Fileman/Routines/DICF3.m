CHKONE(DIFILE,DIEN,DIFLAGS,DIROOT,DIVALUE,DINDEX,DICOUNT,DISCREEN,DIDENT,DILIST) 
 ; CHKALL--check one index for possible matches
 ; proc, DIFILE, DIFLAGS, & DIROOT by value
 N DIXFORM S DIXFORM=0
 N DIBEFORE S DIBEFORE=DICOUNT
 N DICOMMA,DIEXTRNL,DITRY F  D  Q:DIXFORM=""
 . S DIXFORM=$O(@DILIST("LVA")@("V",DIXFORM)) I DIXFORM="" Q
 . S DIVALUE=@DILIST("LVA")@("V",DIXFORM)
 . S DISCREEN=$G(@DILIST("LVA")@("S",DIXFORM))
 . I DISCREEN="" S DISCREEN=@DILIST("LVA")@("S")
 . S DICOMMA=$D(@DILIST("LVA")@("V",DIXFORM,"c"))&(DIFLAGS'["C")
C1 . S DITRY=1
 . D
 . . I 'DICOMMA D EXACT(DIFILE,.DIEN,.DIFLAGS,.DIROOT,.DIVALUE,.DINDEX,.DICOUNT,.DISCREEN,.DIDENT,.DILIST)
 . . I DIFLAGS["q" S DIXFORM="" Q
 . . I DIFLAGS["X" Q
 . . I DINDEX(0,"TYPE")["P" Q
 . . I DINDEX(0,"TYPE")["D",DIVALUE?.NP,+DIVALUE=DIVALUE D  Q:'DIEXTRNL
C2 . . . S DIEXTRNL=$G(@DILIST("LVA")@("V",DIXFORM,1))
 . . D PARTIAL(DIFILE,.DIEN,.DIFLAGS,.DIROOT,.DIVALUE,.DINDEX,.DICOUNT,.DISCREEN,.DIDENT,.DILIST)
 . . I DIFLAGS["q" S DIXFORM=""
 ; S DIVALUE=@DILIST("SAVE")@("V")
 Q
 
PARTIAL(DIFILE,DIEN,DIFLAGS,DIROOT,DIPART,DINDEX,DICOUNT,DISCREEN,DIDENT,DILIST) 
 ; CHKONE--return the list of partial matches to DIVALUE in DINDEX
 ; proc, DIVALUE, DICOUNT, DISCREEN, DIDENT by reference
 N DIOUT S DIOUT=0
 N DIVALUE S DIVALUE=DIPART
 I DICOMMA N DIPREC,DIPOSTC,DIPPREC,DIPPOSTC,DISKIP D  ; ltd comma piece
 . S DIPPREC=$P(DIVALUE,",") ; partial pre-comma value
 . S DIPPOSTC=$P(DIVALUE,",",2) ; partial post-comma value
 . S DIVALUE=DIPPREC
 N DIMORE S DIMORE=$S(DIPART["E":0,1:+DIPART=DIPART)
 I DIMORE D MORE Q:DIOUT
 ;
P1 F  D  Q:DIOUT
 . S DIVALUE=$O(@DIROOT@(DINDEX,DIVALUE))
 . I DICOMMA S DISKIP=0 D  Q:DIOUT  Q:DISKIP  ; limited comma piecing
 . . I '$$PREFIX(DIVALUE,DIPPREC) S DIOUT=1 Q
 . . I $P(DIVALUE,",")=DIPPREC S DIVALUE=DIPPREC_",~" S DISKIP=1 Q
 . . S DIPREC=$P(DIVALUE,",")
 . . S DIPOSTC=$P(DIVALUE,",",2)
 . . I $$PREFIX(DIPOSTC,DIPPOSTC) Q  ; we have a comma piece match
P2 . . I $L(DIVALUE)=30,$L(DIVALUE,",")<3,$$PREFIX(DIPPOSTC,DIPOSTC) D  Q
 . . . S DIFLAGS=DIFLAGS_"c"
 . . . S DIVALUE("c")=DIPPOSTC
 . . D SKIP(.DISKIP,.DIVALUE,DIPREC,DIPOSTC,DIPPREC,DIPPOSTC,DIROOT,DINDEX)
 . ;
P3 . E  D  I DIOUT Q:'DIMORE  D MORE D  Q:DIOUT
 . . I '$$PREFIX(DIVALUE,DIPART) S DIOUT=1
 . D EXACT(DIFILE,.DIEN,.DIFLAGS,.DIROOT,.DIVALUE,.DINDEX,.DICOUNT,.DISCREEN,.DIDENT,.DILIST)
 . I DICOMMA,DIFLAGS["c" D
 . . S DIFLAGS=$TR(DIFLAGS,"c")
 . . K DIVALUE("c")
 . I DIFLAGS["q" S DIOUT=1 Q
 Q
 
PREFIX(DISTRING,DIPREFIX) 
 Q $E(DISTRING,1,$L(DIPREFIX))=DIPREFIX
 
SKIP(DISKIP,DIVALUE,DIPREC,DIPOSTC,DIPPREC,DIPPOSTC,DIROOT,DINDEX) 
 ; skip forward within index based on limited comma piecing
 ; pass DISKIP and DIVALUE by reference
 I DIPPOSTC]DIPOSTC D  ; go to matching first names
 . S DIVALUE=DIPREC_","_DIPPOSTC
 . I '$D(@DIROOT@(DINDEX,DIVALUE)) S DISKIP=1
 . ; note that if the name we skip to is defined, we process it
 E  S DIVALUE=DIPREC_",~",DISKIP=1
 Q
 
MORE 
 ; PARTIAL--continue numeric partial down into string numerics
 S DIMORE=0,DIOUT=0
 S DIVALUE=DIPART_" "
 S DIVALUE=$O(@DIROOT@(DINDEX,DIVALUE),-1)
 S DIOUT=$E($O(@DIROOT@(DINDEX,DIVALUE)),1,$L(DIPART))'=DIPART
 Q
 
EXACT(DIFILE,DIEN,DIFLAGS,DIROOT,DIVALUE,DINDEX,DICOUNT,DISCREEN,DIDENT,DILIST) 
 ; CHKONE/PARTIAL--consider selecting value DIVALUE
 ; proc, DIEN, DIVALUE, DICOUNT, DISCREEN, DIDENT by reference
 N DIENTRY S DIENTRY="" F  D  I DIENTRY="" Q
 . S DIENTRY=$O(@DIROOT@(DINDEX,DIVALUE,DIENTRY)) Q:DIENTRY=""
 . S DIEN=DIENTRY_DIEN
 . D ENTRY(DIFILE,DIEN,.DIFLAGS,.DIROOT,.DIVALUE,.DINDEX,.DICOUNT,.DISCREEN,.DIDENT,.DILIST)
 . S $P(DIEN,",")=""
 . I DIFLAGS["q" S DIENTRY="" Q
 Q
 
ENTRY(DIFILE,DIEN,DIFLAGS,DIROOT,DIVALUE,DINDEX,DICOUNT,DISCREEN,DIDENT,DILIST) 
 ; SPECIAL/EXACT--consider selecting entry # DIENTRY
 ; proc, DIEN, DIVALUE, DICOUNT, DISCREEN, DIDENT by reference
 N DIENTRY S DIENTRY=$P(DIEN,",")
 N DINODE S DINODE=$G(@DIROOT@(DIENTRY,0))
 I '$$VMINUS9^DIEFU(DIFILE,DIEN) Q
 N DIKEY S DIKEY=$P(DINODE,"^") Q:DIKEY=""
 N DIFIELD,DIOUT S DIOUT=0
COMMA 
 I DIFLAGS["c" D  Q:DIOUT  ; limited comma piecing (test long IX values)
 . S @("DIFIELD="_DINDEX(0,"GET"))
 . N DIPOSTC S DIPOSTC=$P(DIFIELD,",",2)
 . I '$$PREFIX(DIPOSTC,DIVALUE("c")) S DIOUT=1
SCREEN 
 N DISCR F DISCR="DISCREEN","DISCREEN(""F"")" I @DISCR'="" D  Q:DIOUT
 . I $D(DINDEX(0,"GET")),'$D(DIFIELD) S @("DIFIELD="_DINDEX(0,"GET"))
 . N %
 . N D S D=DINDEX
 . N DIC S DIC=DIROOT("O")
 . S DIC(0)=$TR(DIFLAGS,"fglpqtuv")
 . N X S X=DIVALUE
 . N Y M Y=DIEN S Y=DIENTRY
 . N Y1 S Y1=$G(@DIROOT@(DIENTRY,0)),Y1=DIEN
 . I 1 X @DISCR ;***** NAKED *****
 . E  S DIOUT=1
 . I $G(DIERR) D
 . . S DIOUT=1,DIFLAGS=DIFLAGS_"q"
 . . N DICONTXT
 . . S DICONTXT=$S(DISCR["F":"Whole File Screen",1:"Screen Parameter")
 . . D ERR^DICF6(120,DIFILE,DIEN,"",DICONTXT)
 Q:DIOUT
ACCEPT 
 I $D(@DILIST@("B",DIKEY,DIENTRY)) Q
 I 'DICOUNT("LOOK") D  Q:DIFLAGS["q"
 . S DICOUNT=DICOUNT+1
 . I DIFLAGS'["f" D 
ANCHOR . .
 . . N DINODE S DINODE(0)=""
 . . I DIFLAGS'["S" D
 . . . I DIFLAGS'["P" S @DILIST@(1,DICOUNT)=DIKEY
 . . . E  S DINODE(0)=DIKEY
 . . I DINDEX'="#" D
 . . . I DIFLAGS'["P" S @DILIST@(2,DICOUNT)=DIENTRY
 . . . E  S DINODE(0)=DINODE(0)_$E(U,DIFLAGS'["S")_DIENTRY
IDS . .
 . . D IDS^DICU2(DIFILE,.DIEN,.DIFLAGS,DIVALUE,.DIROOT,.DINDEX,.DICOUNT,.DIDENT,.DILIST,.DINODE)
 . . I $G(DIERR) S DIOUT=1,DIFLAGS=DIFLAGS_"q"
 . . I DIFLAGS["P" M @DILIST@(DICOUNT)=DINODE
FORCED .
 . E  D
 . . I DIFLAGS'["v" S @DILIST@(DICOUNT)=DIENTRY
 . . E  S @DILIST@(DICOUNT)=DIENTRY_DIROOT("V")
 . S @DILIST@("B",DIKEY,DIENTRY)=""
LOOKING 
 E  S DICOUNT("MORE")=1
 I DICOUNT("MAX")="" Q
 I DICOUNT=DICOUNT("MAX") S DICOUNT("LOOK")=1
 I DICOUNT("MORE") S DIFLAGS=DIFLAGS_"q",DIOUT=1
 Q
