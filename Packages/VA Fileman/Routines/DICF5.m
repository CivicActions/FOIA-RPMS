PREPP(DIFILE,DIFLAGS,DINDEX,DITYPE,DISKIP,DILIST) 
 ; PREPIX^DICF2--transform value for indexed pointer field
 ; proc, DINDEX passed by ref
 N DIF,DINODE,DIPFILE,DIPLIST,DIPVAL,DISAVE
 S DIF=$TR(DIFLAGS,$TR(DIFLAGS,"Xg"))
 S DIPVAL=@DILIST("LVA")@("V")
 S DILIST("SAVE")=DILIST("LVA")
 S DILIST("LVA")="^TMP(""DILVA"",$J,"_DILIST(0)_")"
 S DIPLIST=$NA(@DILIST("LVA")@("V"))
90 S DIPLIST(0)=DILIST(0)+1
 S DIPLIST("LVA")=DILIST("SAVE")
 S DISAVE=@DILIST("SAVE")@("S")
 I DISAVE'="" S @DILIST("SAVE")@("S")=""
 F DINODE="1","2","0,5","0,6" D
 . S @("DISAVE("_DINODE_")=$G(@DILIST(""SAVE"")@(""S"","_DINODE_"))")
 . I @("DISAVE("_DINODE_")'=""""") D
 . . S @("@DILIST(""SAVE"")@(""S"","_DINODE_")=""""")
91 N DIXFILE S DIXFILE=DINDEX(0,"FILE")
 N DIXFIELD S DIXFIELD=DINDEX(0,"FIELD")
 I DITYPE="P" D
 . N DIPFILE M DIPFILE("CHAIN")=DIFILE("CHAIN")
 . S DIPFILE=+$P($P(DINDEX(0,"DEF"),U,2),"P",2)
 . D FIND^DICF(.DIPFILE,"","","Mp"_DIF,"","","","","",.DIPLIST)
 . I $G(DIERR) S DIFLAGS=DIFLAGS_"q",DISKIP=1 Q
 . S DISKIP=DIPLIST("C")=0
 I DISKIP Q
92 I DITYPE="VP" D
 . S DIPLIST("C")=0
 . N DIVFILE,DIOUT S DIVFILE=0,DIOUT=0 F  D  Q:DIOUT
 . . S DIVFILE=$O(^DD(DIXFILE,DIXFIELD,"V","B",DIVFILE))
 . . I DIVFILE="" S DIOUT=1 Q
 . . N DIPFILE M DIPFILE("CHAIN")=DIFILE("CHAIN")
 . . S DIPFILE=DIVFILE
 . . D FIND^DICF(.DIPFILE,"","","Mpv"_DIF,"","","","","",.DIPLIST)
 . . I $G(DIERR) S DIFLAGS=DIFLAGS_"q",DIOUT=1,DISKIP=1 Q
 . . S DISKIP=DIPLIST("C")=0&DISKIP
 . Q:$G(DIERR)
93 . I DIPVAL["." D
 . . D PIECES(.DIFILE,DIXFILE,DIXFIELD,DIF,DIPVAL,.DISKIP,.DIPLIST)
 Q:$G(DIERR)
 I DISAVE'="" S @DILIST("SAVE")@("S")=DISAVE
 I 'DISKIP S @DILIST("LVA")@("S")=@DILIST("SAVE")@("S")
 F DINODE="1","2","0,5","0,6" D
 . I @("DISAVE("_DINODE_")'=""""") D
 . . S @("@DILIST(""SAVE"")@(""S"","_DINODE_")=DISAVE("_DINODE_")")
 Q
 
PIECES(DIFILE,DIXFILE,DIXFIELD,DIFLAGS,DIPVAL,DISKIP,DIPLIST) 
 ; proc, add file.value lookups to index's LVA
 N DILOWER S DILOWER="abcdefghijklmnopqrstuvwxyz"
 N DIUPPER S DIUPPER="ABCDEFGHIJKLMNOPQRSTUVWXYZ"
 N DIFILEN S DIFILEN=$TR($P(DIPVAL,"."),DILOWER,DIUPPER)
 N DIFILES D VPFILES^DIEV1(DIXFILE,DIXFIELD,DIFILEN,.DIFILES)
 K DIPLIST("LVA")
 N DIVALN S DIVALN=$P(DIPVAL,".",2,9999)
 N DIVFILE S DIVFILE=""
 F  S DIVFILE=$O(DIFILES(DIVFILE)) Q:DIVFILE=""  D  Q:$G(DIERR)
 . N DIPFILE M DIPFILE("CHAIN")=DIFILE("CHAIN")
 . S DIPFILE=DIVFILE
 . D FIND^DICF(.DIPFILE,"","","Mlpv"_DIFLAGS,DIVALN,"","","","",.DIPLIST)
 I $G(DIERR) S DIFLAGS=DIFLAGS_"q",DISKIP=1 Q
 S DISKIP=DIPLIST("C")=0&DISKIP
 Q
