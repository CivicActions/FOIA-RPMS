MEDS(DFN,NGET,BDT,EDT,NFOUND,TEST,DATE,VALUE,TEXT) ; data contains medications
 N REQTERM S REQTERM=0
 I $L($G(TEST)) S REQTERM=TEST S:'REQTERM REQTERM=$O(^PXRMD(811.5,"B",REQTERM,""))
 I REQTERM K REQCODE D CDARRCR1(REQTERM,50.6),CDARRCR1(REQTERM,50.605),CDARRCR1(REQTERM,50)
 K TEST S TEST(1)=0,NFOUND=0 Q:'$D(^PS(55,DFN,"P","A"))
 N STDT,FNDT,PIEN,INDIC,DOSEUN,DOSE,DRUG K VALUE,TEXT,DATE
 S STDT=$S($G(BDT):BDT-.01,1:""),FNDT=$S($G(EDT):EDT,1:9999999)
 F  S STDT=$O(^PS(55,DFN,"P","A",STDT)) Q:'STDT  D
 .S PIEN="" F  S PIEN=$O(^PS(55,DFN,"P","A",STDT,PIEN)) Q:'PIEN  D:$D(^PSRX(PIEN,0))
 ..N FILLDT S FILLDT=$P($G(^PSRX(PIEN,2)),U,2) I FILLDT<$G(BDT)!(FILLDT>FNDT) Q
 ..S DRUG=$P(^PSRX(PIEN,0),U,6) Q:'DRUG
 ..N NDDRUG S NDDRUG=$P($G(^PSDRUG(DRUG,"ND")),U)
 ..N DRUGCL S DRUGCL=$P($G(^PSDRUG(DRUG,"ND")),U,6)
 ..I REQTERM,'$D(REQCODE(NDDRUG_";PS(50.6,")),'$D(REQCODE(DRUG_";PSDRUG(")),'$D(REQCODE(DRUGCL_";PS(50.605,")) Q
 ..N PSIEN S PSIEN=$O(^PSRX(PIEN,6,0)) Q:'PSIEN
 ..S DOSE=$$GET1^DIQ(52.0113,PSIEN_","_PIEN,.01),DOSEUN=$$GET1^DIQ(52.0113,PSIEN_","_PIEN,2)
 ..I DOSEUN="",DOSE'=+DOSE S DOSEUN=$P(DOSE," ",$L(DOSE," "))
 ..S NFOUND=NFOUND+1,TEST(NFOUND)=1
 ..S (VALUE(NFOUND,"DATE"),DATE(NFOUND))=$$FMTE^XLFDT(FILLDT,"5D")    ;Entry Date
 ..S VALUE(NFOUND,"DRUG")=$$GET1^DIQ(50,DRUG,.01)
 ..S VALUE(NFOUND,"DOSAGE")=DOSE
 ..S VALUE(NFOUND,"DOSE UNIT")=DOSEUN
 ..S VALUE(NFOUND,"INDICATION")=$$GET1^DIQ(52,PIEN,9999999.22)
 Q
CDARRCRT(REQTEST) ; Array of Diag codes creation
 N I,J,K K REQCODE Q:'REQTEST
 S I=0 F  S I=$O(^PXD(811.2,REQTEST,20,I)) Q:'I  D
 .S J=0 F  S J=$O(^PXD(811.2,REQTEST,20,I,1,J)) Q:'J  D
 ..S K=0 F  S K=$O(^PXD(811.2,REQTEST,20,I,1,J,1,K)) Q:'K  S REQCODE(^PXD(811.2,REQTEST,20,I,1,J,1,K,0))=""
 Q
CDARRCR1(REQTERM,FLNM) ; Array of Med codes creation
 N I Q:'REQTERM  N FL S FL=$G(^DIC(FLNM,0,"GL")),FL=$E(FL,2,$L(FL))
 S I=0 F  S I=$O(^PXRMD(811.5,REQTERM,20,I)) Q:'I  D
 .N CODE S CODE=$P(^PXRMD(811.5,REQTERM,20,I,0),U) Q:$P(CODE,";",2)'=FL
 .S REQCODE(CODE)=""
 Q
 
