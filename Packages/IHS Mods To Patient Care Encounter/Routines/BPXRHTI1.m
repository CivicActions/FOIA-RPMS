 .S NFOUND=NFOUND+1,DATE(NFOUND)=DATE(1),TEXT(NFOUND)=TEXT(1),TEST(NFOUND)=1
 .M VALUE(NFOUND)=VALUE(1)
 .S VALUE(NFOUND,"RACE")=RACE(I)
