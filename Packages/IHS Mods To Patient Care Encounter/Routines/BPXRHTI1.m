 I SEXDT S SEXIN=$O(^AUPNPAT(DFN,93,SEXDT,1,"A"),-1) I SEXIN D
 .S SEXORIEN=$$GET1^DIQ(9000001.93011,SEXIN_","_SEXDT_","_DFN,.01)
