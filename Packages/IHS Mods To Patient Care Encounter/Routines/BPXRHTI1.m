DIAGDT(DFN,NGET,BDT,EDT,NFOUND,TEST,DATE,VALUE,TEXT) ; data contains Problems Dates
 ;This computed finding will check the Active Patient Problems file to find
 ;Diagnosis and Resolution Dates
 N STAT,PRIEN,DFL,REQTEST,REQCODE S REQTEST=0
 I $L($G(TEST)) S REQTEST=TEST S:'REQTEST REQTEST=$O(^PXD(811.2,"B",REQTEST,""))
 I REQTEST D CDARRCRT(REQTEST)
 K TEST S TEST(1)=0,DFL=9000011
 S DATE=$$NOW^PXRMDATE,NFOUND=0 K VALUE,TEXT
 S STAT="" F  S STAT=$O(^AUPNPROB("ACTIVE",DFN,STAT)) Q:STAT=""  D:STAT'="D"
 .S PRIEN="" F  S PRIEN=$O(^AUPNPROB("ACTIVE",DFN,STAT,PRIEN)) Q:'PRIEN  D
 ..N ARRAY,KEY,DIAGDT,RESOLVDT,DIAG S KEY=PRIEN_","
 ..D GETS^DIQ(DFL,PRIEN,".01;.24;1.07;.08","I","ARRAY")
 ..I '$G(ARRAY(DFL,KEY,.24,"I")),'$G(ARRAY(DFL,KEY,1.07,"I")) Q
 ..S DIAG=$$GET1^DIQ(DFL,PRIEN,.01) I REQTEST Q:'$D(REQCODE(DIAG))
 ..S DIAGDT=$$FMTE^XLFDT(ARRAY(DFL,KEY,.24,"I"),"5D")
 ..S RESOLVDT=$$FMTE^XLFDT(ARRAY(DFL,KEY,1.07,"I"),"5D")
 ..S NFOUND=NFOUND+1,TEST(NFOUND)=1
 ..S (DATE(NFOUND),DTDG)=$$FMTE^XLFDT(ARRAY(DFL,KEY,.08,"I"),"5D") ;Entry Date
 ..S VALUE(NFOUND,"DATE")=DTDG
 ..S VALUE(NFOUND,"PROBLEM")=DIAG                                 ;Problem
 ..S VALUE(NFOUND,"DIAGDT")=DIAGDT                                ;Date of Diagnosis
 ..S VALUE(NFOUND,"RESOLVDT")=RESOLVDT                            ;Date of Resolution
 ..S TEXT(NFOUND)="THE PROBLEM "_DIAG_" GOT DIAGNOSED ON "_$S(DIAGDT:DIAGDT,1:DTDG)_" AND RESOLVED ON "_RESOLVDT
 Q
LABSTAT1(DFN,NGET,BDT,EDT,NFOUND,TEST,DATE,VALUE,TEXT) ; data contains Lab Results
 ;This computed finding will check the OR file to find
 N ORIEN,STAT,ORDT,TO,DFNKEY,LRPCK,REQTEST S REQTEST=0
 I $L($G(TEST)) S REQTEST=TEST I 'REQTEST S REQTEST=$O(^ORD(101.43,"B",REQTEST,""))
 K TEST S TEST(1)=0
 S DATE=$$NOW^PXRMDATE,NFOUND=0 K VALUE,TEXT
 S DFNKEY=DFN_";DPT(",EDT=$G(EDT,DATE)
 S ORDT=9999999-EDT,BDT=9999999-$G(BDT)
 F  S ORDT=$O(^OR(100,"AR",DFNKEY,ORDT)) Q:ORDT>BDT!'ORDT  D
 .S ORIEN="" F  S ORIEN=$O(^OR(100,"AR",DFNKEY,ORDT,ORIEN)) Q:'ORIEN  D
 ..Q:$$GET1^DIQ(100,ORIEN,12)'="LAB SERVICE"
 ..N ORIN,FOUND S ORIN=0
 ..F  S ORIN=$O(^OR(100,ORIEN,.1,ORIN)) Q:'ORIN  D
 ...S FOUND=$S(REQTEST:0,1:1)
 ...I REQTEST,^OR(100,ORIEN,.1,ORIN,0)=REQTEST S FOUND=1
 ...Q:'FOUND
 ...S NFOUND=NFOUND+1,TEST(NFOUND)=1
 ...S (DATE(NFOUND),VALUE(NFOUND,"DATE"))=$$FMTE^XLFDT(ORDT\1,"D") ;Test Date
 ...S VALUE(NFOUND,"TEST")=$$GET1^DIQ(100.001,ORIN_","_ORIEN,.01) ;Test
 ...S VALUE(NFOUND,"STATUS")=$$GET1^DIQ(100,ORIEN,5)              ;Status
 ...S TEXT(NFOUND)="THE TEST "_VALUE(NFOUND,"TEST")_" HAS A STATUS "_VALUE(NFOUND,"STATUS")
 Q
LABSTAT(DFN,NGET,BDT,EDT,NFOUND,TEST,DATE,VALUE,TEXT) ; data contains Lab Results
 ;This computed finding will check the V LAB file to find
 N LIEN,REQTERM,REQCODE S REQTERM=0
 I $L($G(TEST)) S REQTERM=TEST I 'REQTERM S REQTERM=$O(^PXRMD(811.5,"B",REQTERM,""))
 I REQTERM K REQCODE D CDARRCR1(REQTERM,60)
 K TEST S TEST(1)=0
 S DATE=$$NOW^PXRMDATE,NFOUND=0 K VALUE,TEXT
 S LIEN="" F  S LIEN=$O(^AUPNVLAB("AC",DFN,LIEN)) Q:'LIEN  D
 .I REQTERM Q:'$D(REQCODE($P(^AUPNVLAB(LIEN,0),U)_";LAB(60,"))
 .N NDATE S NDATE=$P(^AUPNVLAB(LIEN,12),U)                        ;Collection Date & Time
 .Q:NDATE<BDT  I EDT,NDATE>EDT Q                                  ;Out of Range
 .S NFOUND=NFOUND+1,TEST(NFOUND)=1
 .S DATE(NFOUND)=$$FMTE^XLFDT($P(^AUPNVLAB(LIEN,12),U),4)         ;Collection Date & Time
 .S VALUE(NFOUND,"DATE")=DATE(NFOUND)                             ;Collection Date & Time
 .S VALUE(NFOUND,"TEST")=$$GET1^DIQ(9000010.09,LIEN,.01)          ;Test
 .S VALUE(NFOUND,"STATUS")=$$GET1^DIQ(9000010.09,LIEN,1109)       ;Status
 .S TEXT(NFOUND)="THE TEST "_VALUE(NFOUND,"TEST")_" HAS A STATUS "_VALUE(NFOUND,"STATUS")
 Q
VITALOX(DFN,NGET,BDT,EDT,NFOUND,TEST,DATE,VALUE,TEXT) ; data contains Vitals O2 Qualifiers
 K TEST N TYP S TYP=$O(^AUTTMSR("B","O2","")),TEST(1)=0 Q:'TYP
 N DT,MSRIEN,MSRIN
 S DATE=$$NOW^PXRMDATE,NFOUND=0 K VALUE,TEXT
 S DT="" F  S DT=$O(^AUPNVMSR("AA",DFN,TYP,DT)) Q:'DT  D:(9999999-DT)'<$G(BDT)  Q:NFOUND
 .I $G(EDT) Q:(9999999-DT)>EDT
 .S MSRIEN="" F  S MSRIEN=$O(^AUPNVMSR("AA",DFN,TYP,DT,MSRIEN),-1) Q:'MSRIEN  D  Q:NFOUND
 ..S MSRIN="A" F  S MSRIN=$O(^AUPNVMSR(MSRIEN,5,MSRIN),-1) Q:'MSRIN  D  Q:NFOUND
 ...S NFOUND=NFOUND+1,TEST(NFOUND)=1
 ...S DATE(NFOUND)=$$FMTE^XLFDT(9999999-DT,"5D")                         ;Entry Date
 ...S (DATE(NFOUND,"LAST DONE"),VALUE(NFOUND,"DATE"))=DATE(NFOUND)
 ...S VALUE(NFOUND,"QUAL")=$$GET1^DIQ(9000010.015,MSRIN_","_MSRIEN,.01)  ;Qualifier
 ...S VALUE(NFOUND,"RESULT")=$$GET1^DIQ(9000010.01,MSRIEN,.04)           ;Value
 ...S VALUE(NFOUND,"SUPPLEMENTAL O2")=$$SUPL($$GET1^DIQ(9000010.01,MSRIEN,1.4))  ;Supplemental O2 
 S:NFOUND TEXT(NFOUND)="Vitals O2 data of "_DATE(NFOUND)
 Q
