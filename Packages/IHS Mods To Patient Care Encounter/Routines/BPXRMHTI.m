OCAINACT(DFN,NGET,BDT,EDT,NFOUND,TEST,DATE,VALUE,TEXT) ; IHS/OCA/JTC - 2024.08.08 - fix for pxrm 1014
 ;This computed finding will check the Patient Implanted Device file to find
 ;devices and return the Inactive dates.
 ; return true only if ALL devices in the category are inactive
 ;D APPERROR^ZTER("PXRM DEBUG 3")
 S BEHOPAR=TEST
 I BEHOPAR="" S BEHOPAR="Contraceptive/GYN" ; reminder computed parameter - default to contra/gyn, cant use $Get because test="" if blank
 N BEHODEIN,FND K TEST S TEST(1)=0
 S DATE=$$NOW^PXRMDATE
 N PXRMA,PXRMAA
 K VALUE,TEXT,PXRMA,PXRMAA
 S BEHODEIN="" F  S BEHODEIN=$O(^AUPNPDEV("AC",DFN,BEHODEIN),-1) Q:'BEHODEIN  D
 .S BEHOCAT=$$GET1^DIQ(9000091,BEHODEIN,.01) Q:BEHOCAT'=BEHOPAR
 .I $$GET1^DIQ(9000091,BEHODEIN,"CURRENT STATUS")="ACTIVE" S PXRMAA(BEHODEIN)="" Q
 .; make array by last updated for the non-actives:
 .N PXRMDT S PXRMDT=$$GET1^DIQ(9000091,BEHODEIN,"DATE LAST MODIFIED","I")
 .I 'PXRMDT S PXRMDT=$$GET1^DIQ(9000091,BEHODEIN,"DATE IMPLANTED","I")
 .Q:'PXRMDT
 .S PXRMA(PXRMDT,BEHODEIN)=""
 ; loop array and populate values with the NEWEST date as nfound=1
 S NFOUND=0
 S PXRMDT="" F  S PXRMDT=$O(PXRMA(PXRMDT),-1) Q:'PXRMDT  D
 .S BEHODEIN="" F  S BEHODEIN=$O(PXRMA(PXRMDT,BEHODEIN)) Q:'BEHODEIN  D
 ..S NFOUND=NFOUND+1 S TEST(NFOUND)=1
 ..S DATE(NFOUND)=PXRMDT ; last modified date
 ..S TEXT(NFOUND)="THE DEVICE WAS LAST MODIFIED "_$$ENTRY^CIAUDT(DATE(NFOUND),1)
 ..S TEXT(NFOUND)=TEXT(NFOUND)_". Current Status: "_$$GET1^DIQ(9000091,BEHODEIN,"CURRENT STATUS","I")
 ..N BEHODES S BEHODES=$$GET1^DIQ(9000091,BEHODEIN,"DESCRIPTION") I BEHODES'="" S TEXT(NFOUND)=TEXT(NFOUND)_" ("_BEHODES_")"
 ..S VALUE(NFOUND,"IEN")=BEHODEIN
 ..S VALUE(NFOUND,"CAT")=BEHOPAR
 ..S VALUE(NFOUND,"EFF UNTIL")=$$GET1^DIQ(9000091,BEHODEIN,"EFFECTIVE UNTIL","I")
 ..S VALUE(NFOUND,"IMPL DT")=$$GET1^DIQ(9000091,BEHODEIN,"DATE IMPLANTED","I")
 ..S VALUE(NFOUND,"STATUS")=$$GET1^DIQ(9000091,BEHODEIN,"CURRENT STATUS","I")
 ..N LASTDT,LAST,STATUS,EIN S LASTDT=$O(^AUPNPDEV(BEHODEIN,3,"B",""),-1) Q:'LASTDT
 ..S LAST=$O(^AUPNPDEV(BEHODEIN,3,"B",LASTDT,""),-1) Q:'LAST
 ..S EIN=LAST_","_BEHODEIN,STATUS=$$GET1^DIQ(9000091.3,EIN,.02,"I")
 ..S VALUE(NFOUND,"REASON")=$$UP^XLFSTR($$GET1^DIQ(9000091.3,EIN,.04))
 ..S VALUE(NFOUND,"CHANGED ON")=$$GET1^DIQ(9000091.3,EIN,.01,"I")
 ..S VALUE(NFOUND,"CHANGED BY")=$$UP^XLFSTR($$GET1^DIQ(9000091.3,EIN,.03))
 I $D(PXRMAA) S NFOUND=0 F  S NFOUND=$O(TEST(NFOUND)) Q:'NFOUND  S TEST(NFOUND)=0
 I $G(DATE(1))'="" S DATE=DATE(1)
 Q
