 ;;8.5;IMMUNIZATION;**12,26,29,30**;OCT 24,2011;Build 125
 ;;* MICHAEL REMILLARD, DDS * CIMARRON MEDICAL INFORMATICS, FOR IHS *
 ;;  GATHER DATA FOR ADULT IMMUNIZATION REPORT.
 ;;  PATCH 1:  Commented out for ref to ICPT for Code Set versioning. LASTFLU+25
 ;;  PATCH 2: Filter for Active Clinical, using new standard $$ACTCLIN^BIUTL6 call.
 ;;           GETSTATS+60
 ;;  PATCH 3: Set HPV upper limit for males to 21 years of age. GETSTATS+119
 ;;  PATCH 12: Include CVX 133 in Pneumo stats.  PNEU+15
 ;;            Add new Composite Measures.  GETSTATS+32
 ;
 ;
 ;----------
GETSTATS(BIQDT,BICC,BIHCF,BIBEN,BICPTI,BIUP,BITOTS) ;EP
 ;---> Produce array for ADULT Immunization Report.
 ;---> Parameters:
 ;     1 - BIQDT  (req) Quarter Ending Date.
 ;     2 - BICC   (req) Current Community array.
 ;     3 - BIHCF  (req) Health Care Facility array.
 ;     4 - BIBEN  (req) Beneficiary Type array.
 ;     5 - BICPTI (opt) 1=Include CPT Coded Visits, 0=Ignore CPT (default).
 ;     6 - BIUP   (req) User Population/Group (Registered, Imm, User, Active).
 ;     7 - BITOTS (ret) array of values for report
 ;                      
 ;
 ;**********
 ;
 N BIADOB,BIADOBE
 N BIAGE,BIVAL,BI19P,BI60P,BI65P,BI1926,BI1959,BITDAPEV,BITD10YR,BITDBOTH,BIREFUS
 N BIHEPB1,BIHEPB2,BIHEPBC,BIHPVD,BIHPVF1,BIHPVF2,BIHPVFC,BISHIN1,BISHINC,BIPCV13,BIPPSV23,BIPCV20,BIPCV15,BIALLAPP,BI66APN,BI65APN
 N BI19P,BI60P,BI65P,BI1926,BI1959,BI50P,BI1964,BI1949,BI5059,BI6065,BI66P
 K BITOTS
 ;
 S:('$D(BICPTI)) BICPTI=0
 S:('$G(BIQDT)) BIQDT=$G(DT)
 ;
 ;initialize array variables
 F X="PTS19+","PTS60","PTS65+","PTS19-26","PTS19-59","PTS50+","PTS19-64","PTS19-49","PTS50-59","PTS60-65","PTS66+","19+TDAPEVER","19+TDAP&TD10YR","19-59HEPB1","19-59HEPB2","19-59HEPBC","REFUSALS","ALLAPP" S BITOTS(X)=0
 F X="19-26HPV1","19-26HPV2","19-26HPVC","50+SHINGRIX1","50+SHINGRIXC","19-64PCV13PPSV23","19-64PCV20","19-64PCV15PPSV23","65+PCV13PPSV23","65+PCV20","65+PCV15PPSV23","65+TDAP/TD10YR" S BITOTS(X)=0
 F X="19-49TDAPEVER","19-49TDAP/TD10YR","19-49TDAP&TD10YR","19-49HEPBC","19-49ALL","50-59TDAPEVER","50-59TDAP/TD10YR","50-59TDAP&TD10YR","50-59HEPBC","50-59SHINC","50-59ALL" S BITOTS(X)=0
 F X="60-65TDAPEVER","60-65TDAP/TD10YR","60-65TDAP&TD10YR","60-65SHINC","60-65ALL","66+TDAPEVER","66+TDAP/TD10YR","66+TDAP&TD10YR","66+SHINC","66+PCV13PPSV23","66+PCV20","66+PCV15PPSV23","66+ANYPNEU","66+MET1","66+MET2","66+MET3" S BITOTS(X)=0
 ;---> Loop through Patient global looking for visits and immunizations.
 ;---> DOB must be at least 19 years before Quarter Ending Date.
 S BIADOB=0,BIADOBE=BIQDT-190000
 F  S BIADOB=$O(^DPT("ADOB",BIADOB)) Q:(BIADOB>BIADOBE)  D
 .S BIDFN=0
 .F  S BIDFN=$O(^DPT("ADOB",BIADOB,BIDFN)) Q:'BIDFN  D
 ..;
 ..;---> Filter for standard Patient Population parameter.
 ..Q:'$$PPFILTR^BIREP(BIDFN,.BIHCF,BIQDT,BIUP)
 ..;
 ..;---> Get Age in Years for Stats.
 ..;V8.5 PATCH 29 - FID-
 ..S BIAGE=+$$AGE^BIUTL1(BIDFN,1,BIQDT)
 ..;---> Quit if under age 19 on the Quarter Ending Date.
 ..Q:BIAGE<19
 ..;
 ..;---> Quit if Beneficiary Type doesn't match.
 ..Q:$$BENT^BIDUR1(BIDFN,.BIBEN)
 ..;
 ..;---> Quit if Current Community doesn't match.
 ..Q:$$CURCOM^BIEXPRT2(BIDFN,.BICC)
 ..;
 ..;---> Set patient as Not Due, BIVAL=2
 ..;---> If patient is due (change below), set BIVAL=1.
 ..S BIVAL=2
 ..;
 ..;
 ..D GETVAL^BIREPL4
 ..;
TOT ..;---> Set Age totals. Denominators
 ..S BITOTS("PTS19+")=$G(BITOTS("PTS19+"))+1    ;line 1
 ..I BI60P S BITOTS("PTS60+")=$G(BITOTS("PTS60+"))+1
 ..S:BI65P BITOTS("PTS65+")=$G(BITOTS("PTS65+"))+1
 ..I BI1926 S BITOTS("PTS19-26")=$G(BITOTS("PTS19-26"))+1
 ..I BI1959 S BITOTS("PTS19-59")=$G(BITOTS("PTS19-59"))+1
 ..I BI50P S BITOTS("PTS50+")=$G(BITOTS("PTS50+"))+1
 ..I BI1964 S BITOTS("PTS19-64")=$G(BITOTS("PTS19-64"))+1
 ..I BI1949=1 S BITOTS("PTS19-49")=$G(BITOTS("PTS19-49"))+1
 ..I BI5059=1 S BITOTS("PTS50-59")=$G(BITOTS("PTS50-59"))+1
 ..I BI6065=1 S BITOTS("PTS60-65")=$G(BITOTS("PTS60-65"))+1
 ..I BI66P=1 S BITOTS("PTS66+")=$G(BITOTS("PTS66+"))+1
 ..;**********
 ..;TD lines
 ..I BITDAPEV S BITOTS("19+TDAPEVER")=$G(BITOTS("19+TDAPEVER"))+1  ;line 2
 ..I BITDBOTH S BITOTS("19+TDAP&TD10YR")=$G(BITOTS("19+TDAP&TD10YR"))+1
 ..;HEPB lines
 ..I BIHEPB1,BI1959 S BITOTS("19-59HEPB1")=$G(BITOTS("19-59HEPB1"))+1
 ..I BIHEPB2,BI1959 S BITOTS("19-59HEPB2")=$G(BITOTS("19-59HEPB2"))+1
 ..I BIHEPBC,BI1959 S BITOTS("19-59HEPBC")=$G(BITOTS("19-59HEPBC"))+1
 ..;HPV lines
 ..I BIHPVF1,BI1926 S BITOTS("19-26HPV1")=$G(BITOTS("19-26HPV1"))+1
 ..I BIHPVF2,BI1926 S BITOTS("19-26HPV2")=$G(BITOTS("19-26HPV2"))+1
 ..I BIHPVFC,BI1926 S BITOTS("19-26HPVC")=$G(BITOTS("19-26HPVC"))+1
 ..;shingrix lines
 ..I BI50P,BISHIN1 S BITOTS("50+SHINGRIX1")=$G(BITOTS("50+SHINGRIX1"))+1
 ..I BI50P,BISHINC S BITOTS("50+SHINGRIXC")=$G(BITOTS("50+SHINGRIXC"))+1
 ..;PNEUMO lines
 ..I BI1964,BIPCV20 S BITOTS("19-64PCV20")=$G(BITOTS("19-64PCV20"))+1 G N65
 ..I BI1964,BIPPSV23,BIPCV15 S BITOTS("19-64PCV15PPSV23")=$G(BITOTS("19-64PCV15PPSV23"))+1 G N65
 ..I BI1964,BIPCV13,BIPPSV23 S BITOTS("19-64PCV13PPSV23")=$G(BITOTS("19-64PCV13PPSV23"))+1 G N65
N65 ..;65 lines
 ..I BI65P,BIPCV20 S BITOTS("65+PCV20")=$G(BITOTS("65+PCV20"))+1,BI65APN=1 G TD65
 ..I BI65P,BIPPSV23,BIPCV15 S BITOTS("65+PCV15PPSV23")=$G(BITOTS("65+PCV15PPSV23"))+1,BI65APN=1 G TD65
 ..I BI65P,BIPCV13,BIPPSV23 S BITOTS("65+PCV13PPSV23")=$G(BITOTS("65+PCV13PPSV23"))+1,BI65APN=1 G TD65
TD65 ..;TD
 ..I BI65P,BITD10YR S BITOTS("65+TDAP/TD10YR")=$G(BITOTS("65+TDAP/TD10YR"))+1
 ..;refusal totals
 ..I BIREFUS S BITOTS("REFUSALS")=$G(BITOTS("REFUSALS"))+1
 ..;composite totals
 ..I BI1949,BITDAPEV S BITOTS("19-49TDAPEVER")=$G(BITOTS("19-49TDAPEVER"))+1
 ..I BI1949,BITD10YR S BITOTS("19-49TDAP/TD10YR")=$G(BITOTS("19-49TDAP/TD10YR"))+1
 ..I BI1949,BITDBOTH S BITOTS("19-49TDAP&TD10YR")=$G(BITOTS("19-49TDAP&TD10YR"))+1
 ..I BI1949,BIHEPBC S BITOTS("19-49HEPBC")=$G(BITOTS("19-49HEPBC"))+1
 ..I BI1949,BITDAPEV,BITDBOTH,BIHEPBC S BITOTS("19-49ALL")=$G(BITOTS("19-49ALL"))+1,BIALLAPP=1
 ..;50-59 composite totals
 ..I BI5059,BITDAPEV S BITOTS("50-59TDAPEVER")=$G(BITOTS("50-59TDAPEVER"))+1
 ..I BI5059,BITD10YR S BITOTS("50-59TDAP/TD10YR")=$G(BITOTS("50-59TDAP/TD10YR"))+1
 ..I BI5059,BITDBOTH S BITOTS("50-59TDAP&TD10YR")=$G(BITOTS("50-59TDAP&TD10YR"))+1
 ..I BI5059,BIHEPBC S BITOTS("50-59HEPBC")=$G(BITOTS("50-59HEPBC"))+1
 ..I BI5059,BITDAPEV,BITDBOTH,BIHEPBC,BISHINC S BITOTS("50-59ALL")=$G(BITOTS("50-59ALL"))+1,BIALLAPP=1
 ..I BI5059,BISHINC S BITOTS("50-59SHINC")=$G(BITOTS("50-59SHINC"))+1
 ..;
 ..I BI6065,BITDAPEV S BITOTS("60-65TDAPEVER")=$G(BITOTS("60-65TDAPEVER"))+1
 ..I BI6065,BITD10YR S BITOTS("60-65TDAP/TD10YR")=$G(BITOTS("60-65TDAP/TD10YR"))+1
 ..I BI6065,BITDBOTH S BITOTS("60-65TDAP&TD10YR")=$G(BITOTS("60-65TDAP&TD10YR"))+1
 ..I BI6065,BITDAPEV,BITDBOTH,BISHINC S BITOTS("60-65ALL")=$G(BITOTS("60-65ALL"))+1,BIALLAPP=1
 ..I BI6065,BISHINC S BITOTS("60-65SHINC")=$G(BITOTS("60-65SHINC"))+1
 ..;
 ..;66+ COMPOSITE TOTALS
 ..I BI66P,BITDAPEV S BITOTS("66+TDAPEVER")=$G(BITOTS("66+TDAPEVER"))+1
 ..I BI66P,BITD10YR S BITOTS("66+TDAP/TD10YR")=$G(BITOTS("66+TDAP/TD10YR"))+1
 ..I BI66P,BITDBOTH S BITOTS("66+TDAP&TD10YR")=$G(BITOTS("66+TDAP&TD10YR"))+1
 ..I BI66P,BISHINC S BITOTS("66+SHINC")=$G(BITOTS("66+SHINC"))+1
 ..;pneumo
 ..I BI66P,BIPCV20 S BITOTS("66+PCV20")=$G(BITOTS("66+PCV20"))+1,BI66APN=1 G PN
 ..I BI66P,BIPPSV23,BIPCV15 S BITOTS("66+PCV15PPSV23")=$G(BITOTS("66+PCV15PPSV23"))+1,BI66APN=1 G PN
 ..I BI66P,BIPCV13,BIPPSV23 S BITOTS("66+PCV13PPSV23")=$G(BITOTS("66+PCV13PPSV23"))+1,BI66APN=1 G PN
PN ..;
 ..I BI66P,BI66APN S BITOTS("66+ANYPNEU")=$G(BITOTS("66+ANYPNEU"))+1
 ..I BI66P,BITDAPEV,BITDBOTH,BISHINC,BIPCV20 S BITOTS("66+MET2")=$G(BITOTS("66+MET2"))+1,BIALLAPP=1 G M
 ..I BI66P,BITDAPEV,BITDBOTH,BISHINC,BIPCV15,BIPPSV23 S BITOTS("66+MET3")=$G(BITOTS("66+MET3"))+1,BIALLAPP=1 G M
 ..I BI66P,BITDAPEV,BITDBOTH,BISHINC,BIPCV13,BIPPSV23 S BITOTS("66+MET1")=$G(BITOTS("66+MET1"))+1,BIALLAPP=1 G M
M ..;
 ..I BIALLAPP S BITOTS("ALLAPP")=$G(BITOTS("ALLAPP"))+1
 ..;calculate met age range composite measure
 ..;---> Will Set ^TMP("BIDUL",$J,CURCOM,1,HRCN,BIDFN)=$G(BIVAL)
 ..;ONLY CHECK TD/TDAP IN 10 YEARS AND IF 65+ CHECK PNEUMO FOR CURRENT/NOT CURRENT LIST OER SURYAM EMAIL 01/05/23
 ..S BIVAL=1
 ..I BIAGE<64,BITD10YR S BIVAL=2
 ..I BIAGE>64,BITD10YR,BI65APN S BIVAL=2
S ..D STORE^BIDUR1(BIDFN,DT,9,,$G(BIVAL))
 Q
