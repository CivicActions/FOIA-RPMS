TIMETBL(START,END) ; Creation of multiple stay timetable (Admission/ER) based on the period
 N ADMDT,TRNDT,TRNNM,DISDT,FIND,LOC,FIND,TRNTP,MOVE,LOCNM,ADMLOC K BHSADM,BHSADM1
 S FIND=0,TRNDT=END+.000001 F  S TRNDT=$O(^DGPM("ADFN"_DFN,TRNDT),-1) Q:'TRNDT  D  Q:FIND
 .S TRNNM=$O(^DGPM("ADFN"_DFN,TRNDT,"")),TRNTP=$$GET1^DIQ(405,TRNNM,.04,"I"),TRNTP=$$GET1^DIQ(405.1,TRNTP,.02)
 .I TRNTP="DISCHARGE" S:TRNDT<START FIND=1 Q:FIND  S MOVE(TRNDT,"Z")=0
 .I TRNTP="ADMISSION"!(TRNTP="TRANSFER") S MOVE(TRNDT,TRNTP)=TRNNM I TRNTP="ADMISSION" S:TRNDT<START FIND=1
 S TRNDT="" F  S TRNDT=$O(MOVE(TRNDT)) Q:'TRNDT  D:$O(MOVE(TRNDT,""))'="Z"
 .S TRNTP=$O(MOVE(TRNDT,"")),TRNNM=MOVE(TRNDT,TRNTP),LOC=$$GET1^DIQ(405,TRNNM,.06,"I")
 .S LOCNM=$$GET1^DIQ(42,LOC,44,"I"),LOC=$$GET1^DIQ(44,LOCNM,.01)
 .I TRNTP="TRANSFER" S $P(BHSADM(ADMLOC),U,5)=$P(BHSADM(ADMLOC),U,5)_LOC_";" Q
 .S DISDT=TRNDT F  S DISDT=$O(MOVE(DISDT)) Q:'DISDT  Q:$O(MOVE(DISDT,""))="Z"  
 .S:DISDT DISDT=DISDT-.000001 S:'DISDT DISDT=END
 .S NEWBAL=$$FLBAL(TRNDT,DISDT,LOC,TRNDT) I '$D(BHSADM(LOC)) S BHSADM(LOC)=NEWBAL_U_U_TRNDT_U_DISDT
 .E  F I=1:1:4 S $P(BHSADM(LOC),U,I)=$P(BHSADM(LOC),U,I)+$P(NEWBAL,U,I)
 .S ADMLOC=LOC,BHSADM1(TRNDT)=DISDT_U_LOCNM_U_LOC
 Q
