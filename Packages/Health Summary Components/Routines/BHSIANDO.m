FLBAL(FROM,TO,LOC,ADMDT) ; Defines an Admission Fluid Balance and Balance for 24, 12 and 8 hours
 N BHSDAT,SDATE,VAIP,NUMHRS,ADMBAL,ADM
 S BHSDAT=9999999-TO-0.000001,ADM=0
 I ADMDT S ADMDT=9999999-ADMDT,ADM=1,ADMBAL=$$ADMBAL(9999999-BHSDAT)
 F NUMHRS=24,12,8 S ADMDT=9999999-$$FMADD^XLFDT(NOW,,-NUMHRS),@("BAL"_NUMHRS)=$$ADMBAL(9999999-BHSDAT)
 Q BAL24_U_BAL12_U_BAL8_U_$G(ADMBAL)
 ;
ADMBAL(SDATE) ; Calculate Fluid Balance
 N ADMBAL S ADMBAL=0
 S LP=SDATE F  S LP=$O(^GMR(126,DFN,"IO",LP)) Q:LP>ADMDT!'LP  D
 .S LP1="" F  S LP1=$O(^GMR(126,DFN,"IO",LP,LP1)) Q:LP1=""  D
 ..S GIEN="" F  S GIEN=$O(^GMR(126,DFN,"IO",LP,LP1,GIEN)) Q:'GIEN  D
 ...I LP1="IN"!(LP1="OUT") D
 ....S IOFILE=$S(LP1="IN":126.01,1:126.02)
 ....S IENS=GIEN_","_DFN_","
 ....Q:$$GET1^DIQ(IOFILE,IENS,9999999.01,"I")      ; entered in error
 ....S OF=(LP1="OUT")
 ....I 'ADM Q:$$GET1^DIQ(IOFILE,IENS,7-OF)'=LOC    ; entered for another location
 ....S AMT=$$GET1^DIQ(IOFILE,IENS,4-OF)
 ....S ADMBAL=ADMBAL+$S(LP1="IN":AMT,1:-AMT)
 ...I LP1="IV" D 
 ....S IVIEN="" F  S IVIEN=$O(^GMR(126,DFN,"IO",LP,LP1,GIEN,IVIEN)) Q:'IVIEN  D
 .....S IENS2=IVIEN_","_GIEN_","_DFN_","
 .....Q:$$GET1^DIQ(126.313,IENS2,9999999.11,"I")   ; entered in error
 .....I 'ADM Q:$$GET1^DIQ(126.313,IENS2,4)'=LOC    ; entered for another location
 .....S AMT=$$GET1^DIQ(126.313,IENS2,2)
 .....S ADMBAL=ADMBAL+AMT
 Q ADMBAL
