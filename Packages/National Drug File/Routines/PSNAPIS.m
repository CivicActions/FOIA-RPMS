PSA(NDC,LIST) ; ENTRY FOR DRUG ACCOUNTABILITY
 N J,P50,PN,PN1,PTR,Y
 ;CALL IS S COUNT=$$PSA^PSNAPIS(NDC,.ARRAY NAME)
 ;INPUT NDC
 ;OUTPUT LIST(IEN FILE 50)=DRUG NAME
 ;OUTPUT COUNT=NUMBER OF ENTRIES
 ;
 S Y="^PSNDF(""NDC"",""""_NDC_"""")",J=0
 F  S Y=$Q(@Y) Q:$QS(Y,2)'=NDC  D
 .S PTR=$P(^PSNDF($QS(Y,3),2,$QS(Y,4),3,$QS(Y,5),4,$QS(Y,6),5,$QS(Y,7),6,$QS(Y,8),7,$QS(Y,9),0),"^",4) Q:'PTR
 .S PN=$P(^PSNDF($QS(Y,3),5,PTR,0),"^"),PN1=$E(PN,1,30) Q:PN=""
 .S P50=0 F  S P50=$O(^PSDRUG("VAPN",PN1,P50)) Q:'P50  I $P(^PSDRUG(P50,"ND"),"^",2)=PN S LIST(P50)=$P(^PSDRUG(P50,0),"^"),J=J+1
 Q J
 ;
PSJING(DA,K,LIST) ;ENTRY FOR INPATIENT TO GET INGREDIENTS
 N J,I,L,D,IN,INP,CT
 ;OUTPUT LIST(INGRED IEN)=INGRED IEN^ING NAME^STRENGTH-UNITS
 ;OUTPUT X=NUMBER OF INGREDIENTS
 ;
 I 'DA!('K)!('$D(^PSNDF(+DA,5,+K,0))) Q 0
 S CT=0 F J=1,3,4 S L=$G(^PSNDF(+DA,5,+K,J)) I L]"" F I=1:1:$L(L,",") D
 .S D=$P(L,",",I),IN=$P(^PSNDF(+DA,1,$P(D,"/"),0),"^"),INP=+$O(^PS(50.416,"B",IN,0)) I '$D(LIST(INP)) S LIST(INP)=INP_"^"_IN_"^"_$P(D,"/",2)_"^"_$P(D,"/",3,30),CT=CT+1
 Q CT
 ;
PSJDF(DA,K) ;ENTRY FOR INPATIENT TO GET DOSE FORM
 ;OUTPUT X=DOSE FORM IEN^DOSE FORM NAME
 N DF,X
 I DA=0!(K=0)!('$D(^PSNDF(+DA,5,+K,0))) Q 0
 S X=+$P($G(^PSNDF(+DA,5,+K,0)),"^",2),X=+$G(^PSNDF(+DA,2,X,0)),DF=$P($G(^PS(50.606,X,0)),"^")
 Q X_"^"_DF
 ;
PSJST(DA,K) ;ENTRY FOR INPATIENT TO GET STRENGTH
 ;OUTPUT X=STRENGTH ENTRY^STRENGTH
 N ST,X,Y
 I DA=0!(K=0)!('$D(^PSNDF(+DA,5,+K,0))) Q 0
 S Y=+$P(^PSNDF(+DA,5,+K,0),"^",2),X=+$P(^(0),"^",3),ST=$P($G(^PSNDF(+DA,2,Y,3,X,0)),"^")
 Q X_"^"_ST
 ;
 ;
CLASS(CL) ;ENTRY FOR OE/RR TO GET CLASS $D
 I CL']"" Q 0
 Q $D(^PS(50.605,"C",CL))>0
 ;
DRUG(DR) ;ENTRY FOR OE/RR TO GET DRUG $D
 I DR']"" Q 0
 Q $D(^PSNDF("B",DR))!$D(^PSNDF("T",DR))
 ;
PROD0(P1,P2) ;GET 0 NODE OF THE PRODUCT MULTIPLE
 I 'P1!'P2 Q ""
 Q $G(^PSNDF(P1,5,P2,0))
 ;
PROD2(P1,P2) ;GET 2 NODE OF PRODUCT MULTIPLE
 I 'P1!'P2 Q ""
 Q $G(^PSNDF(P1,5,P2,2))
 ;
DCLASS(DA,K) ;GET CLASS FROM PRODUCT
 I 'DA!('K)!('$D(^PSNDF(+DA,5,+K,0))) Q 0
 S X=^PSNDF(+DA,5,+K,0),X=+$P(X,"^",2),X=$P($G(^PSNDF(+DA,2,X,0)),"^",3),Y=$P($G(^PS(50.605,X,0)),"^",2)
 Q X_"^"_Y
 ;
DCLCODE(DA,K) ;RETURN DRUG CLSSIFICATION CODE
 I 'DA!('K)!('$D(^PSNDF(+DA,5,+K,0))) Q 0
 S X=^PSNDF(+DA,5,+K,0),X=+$P(X,"^",2),X=+$P($G(^PSNDF(+DA,2,X,0)),"^",3),X=$P($G(^PS(50.605,X,0)),"^")
 Q X
 ;
VAGN(DA) ;GET VA GENERIC NAME
 I 'DA!('$D(^PSNDF(+DA,0))) Q 0
 Q $P(^PSNDF(+DA,0),"^")
 ;
DFSU(DA,K) ;RETURN DOSE FORM, STRENGTH, AND UNITS FOR PDM AND CPRS
 N U1,UN,X,Y,Z
 I 'DA!('K)!('$D(^PSNDF(+DA,5,+K,0))) Q 0
 S X=^PSNDF(+DA,5,+K,0),Y=+$P(X,"^",2),Z=+$P(X,"^",3),X=+$P(X,"^",4)
 S U1=+$P($G(^PSNDF(+DA,2,Y,3,Z,4,X,0)),"^",1),UN=$P($G(^PS(50.607,U1,0)),"^")
 Q $$PSJDF(DA,K)_"^"_$$PSJST(DA,K)_"^"_U1_"^"_UN
 ;
VAP(DA,LIST) ;GIVEN A GENERIC RETURN ARRAY LIST(IEN)=IEN^PRODUCT^DF PTR^DOSE FORM
 ;S X=$$VAP^PSNAPIS(DA,.LIST)
 I 'DA!'$D(^PSNDF(+DA)) Q 0
 N J,K,X,DF,DFNA,CL,CLN
 S K=0 F J=0:1 S K=$O(^PSNDF(+DA,5,+K)) Q:'K  S X=^(K,0) D
 . S DF=$P(X,"^",2),DF=$G(^PSNDF(+DA,2,+DF,0)),CL=+$P(DF,"^",3),CLN=$P($G(^PS(50.605,CL,0)),"^"),DFNA=$P($G(^PS(50.606,+DF,0)),"^"),LIST(K)=K_"^"_$P(X,"^")_"^"_DF_"^"_DFNA_"^"_CL_"^"_CLN
 Q J
 ;
PSPT(DA,K,LIST) ;GIVEN DA & K RETURN LIST(IEN1^IEN2)=IEN1^PSIZE^IEN2^PTYPE
 ;S X=$$PSPT^PSNAPIS(DA,K,.LIST)
 N CT,ND,PS,PT,S1,S2,S3,S4,S5
 I 'DA!'K!'$D(^PSNDF(+DA,5,+K)) Q 0
 S X=^PSNDF(+DA,5,+K,0),S1=$P(X,"^",2),S2=$P(X,"^",3),S3=$P(X,"^",4)
 S (S4,CT)=0 F  S S4=$O(^PSNDF(+DA,2,S1,3,S2,4,S3,5,S4)),S5=0 Q:'S4  S PS=$G(^(S4,0)) F  S S5=$O(^PSNDF(+DA,2,S1,3,S2,4,S3,5,S4,6,S5)),S6=0 Q:'S5  S PT=$G(^(S5,0)) D
 .F  S S6=$O(^PSNDF(+DA,2,S1,3,S2,4,S3,5,S4,6,S5,7,S6)) Q:'S6  S ND=$G(^(S6,0)) D
 ..I $P(ND,"^",4)=K S LIST(PS_"^"_PT)=PS_"^"_$P($G(^PS(50.609,PS,0)),"^")_"^"_PT_"^"_$P($G(^PS(50.608,PT,0)),"^"),CT=CT+1
 Q CT
 ;
DSS(DA,K,DATE) ;RETURNS DSS NUMBER *(DA PADDED TO 4 _ K PADDED TO 3)
 ;DATE IS NOT USED FOR THIS ONE - ALL FEEDER KEYS ARE OLD STYLE
 Q $$RJ^XLFSTR(DA,4,0)_$$RJ^XLFSTR(K,3,0)
 ;
CPRS(DA,K) ;CALL FOR CPRS
 ;RETURNS X=DOSE FORM^CLASS^STRENGTH^UNITS
 I '$D(^PSNDF(+DA,5,+K,0)) Q 0
 N X,DF,ST,UN,CL
 S X=^PSNDF(+DA,5,+K,0),DF=+$P(X,"^",2),ST=+$P(X,"^",3),UN=+$P(X,"^",4) D
 .S UN=+$G(^PSNDF(+DA,2,DF,3,ST,4,UN,0)),UN=$P($G(^PS(50.607,UN,0)),"^"),ST=+$G(^PSNDF(+DA,2,DF,3,ST,0)),DF=$G(^PSNDF(+DA,2,DF,0)),CL=$P($G(^PS(50.605,+$P(DF,"^",3),0)),"^"),DF=$P($G(^PS(50.606,+DF,0)),"^")
 Q DF_"^"_CL_"^"_ST_"^"_UN
 ;
CIRN(NDC,LIST) ;RETURN ARRAY FOR CIRN
 N ARG,ARG1,J
 K LIST S LIST="000000000000"  F J=1:1:7 S LIST(J)=""
 I NDC["-" S NDC=$$RJ^XLFSTR($P(NDC,"-"),6,0)_$$RJ^XLFSTR($P(NDC,"-",2),4,0)_$$RJ^XLFSTR($P(NDC,"-",3),2,0)
 S ARG=$Q(^PSNDF("NDC",NDC)) Q:$QS(ARG,2)'=NDC
 S LIST=NDC,LIST(0)=$QS(ARG,3),LIST(1)=^PSNDF($QS(ARG,3),0)
 S ARG1="^PSNDF("_$QS(ARG,3)_","
 F J=2:1:7 S ARG1=ARG1_J_","_$QS(ARG,J+2)_",",LIST(J)=$G(@(ARG1_"0)")) I LIST(J)="NO DATA" S LIST(J)=""
 Q
 ;
B() ;RETURNS THE GLOBAL ROOT OF THE "B" CROSS REFERENCE IN NDF
 Q "^PSNDF(""B"")"
 ;
T() ;RETURNS THE GLOBAL ROOT OF THE "T" CROSS REFERENCE IN NDF
 Q "^PSNDF(""T"")"
 ;
TTOG(TRADE,LIST) ;RETURNS THE IENS FOR A GIVEN TRADE NAME
 ;LIST(IEN)=IEN_"^"_GENERIC NAME
 I TRADE="" Q 0
 N K,CT S (K,CT)=0 F  S K=$O(^PSNDF("T",TRADE,K)) Q:'K  S LIST(K)=K_"^"_$P(^PSNDF(K,0),"^"),CT=CT+1
 Q CT
 ;
CLIST(DA,LIST) ;RETURNS LIST(IEN)=IEN OF CLASS^CLASS CODE
 ;FOR ALL CLASSES IN PSNDF(DA)
 I 'DA Q 0
 I '$D(^PSNDF(+DA)) Q 0
 N K,CT,IEN S (K,CT)=0 F  S K=$O(^PSNDF(DA,2,K)) Q:'K  S IEN=$P(^(K,0),"^",3) I '$D(LIST(IEN)) S LIST(IEN)=IEN_"^"_$P(^PS(50.605,IEN,0),"^"),CT=CT+1
 Q CT
 ;
TGTOG(NAME) ;GIVEN A NAME FIND IT IN EITHER T OR B CROSS REFERENCE AND RETURN IEN
 N X
 I NAME="" Q 0
 S X=$O(^PSNDF("B",NAME,0)) I X Q X
 S X=$O(^PSNDF("T",NAME,0)) I X Q X
 Q 0
 ;
TGTOG2(NAME,LIST) ;PARTIAL LOOKUP ON T OR B
 I NAME="" Q 0
 N NAM,X,CT
 S CT=0
 S NAM=$E(NAME,1,$L(NAME)-1)_$C($A($E(NAME,$L(NAME)))-1)_"z" F  S NAM=$O(^PSNDF("B",NAM)),X=0 Q:NAM'[NAME  F  S X=$O(^PSNDF("B",NAM,X)) Q:'X  I '$D(LIST(X)) S LIST(X)=X_"^"_NAM,CT=CT+1
 S NAM=$E(NAME,1,$L(NAME)-1)_$C($A($E(NAME,$L(NAME)))-1)_"z" F  S NAM=$O(^PSNDF("T",NAM)),X=0 Q:NAM'[NAME  F  S X=$O(^PSNDF("T",NAM,X)) Q:'X  I '$D(LIST(X)) S LIST(X)=X_"^"_$P(^PSNDF(X,0),"^"),CT=CT+1
 Q CT
 ;
CIRN2(P1,P3,LIST) ;RETURNS LIST(J)=NDC ASSOCIATED WITH PRODUCT
 I 'P3 Q 0
 I '$D(^PSNDF(+P1,5,+P3,0)) Q 0
 N J,S2,S3,S4,S5,S6,S7,X
 S X=^PSNDF(P1,5,P3,0),S2=$P(X,"^",2),S3=$P(X,"^",3),S4=$P(X,"^",4),(J,S5,S6,S7)=0
 F  S S5=$O(^PSNDF(P1,2,S2,3,S3,4,S4,5,S5)),S6=0 Q:'S5  F  S S6=$O(^PSNDF(P1,2,S2,3,S3,4,S4,5,S5,6,S6)),S7=0  Q:'S6  F  S S7=$O(^PSNDF(P1,2,S2,3,S3,4,S4,5,S5,6,S6,7,S7)) Q:'S7  S X=^(S7,0) D
 .I $P(X,"^",4)=P3 S J=J+1,LIST(J)=$P(X,"^")
 Q J
CLASS2(IEN) ;RETURNS PIECE 1 AND 2 OF ^PS(50.605,IEN,0)
 Q $P($G(^PS(50.605,+IEN,0)),"^",1,2)
