SER(TAX,SCOUN,INC,RESULT) ;EP - Find a series
 ; INC = 1 include refusals, otherwise do not include
 NEW TREF,TIEN,RDTM
 S INC=$G(INC,0)
 S TREF=$NA(^TMP($J,"BQITAX")) K @TREF
 D BLD^BQITUTL(TAX,TREF)
 S GREF=$$ROOT^DILFD(FREF,"",1) I FREF=9000010.11 S RREF=9999999.14
 S IEN="",QFL=0,CT=0,RESULT=CT
 F  S IEN=$O(@GREF@("AC",BQDFN,IEN),-1) Q:'IEN  D
 . S ITIEN=$$GET1^DIQ(FREF,IEN,.01,"I") I ITIEN="" Q
 . I $G(TIEN)'="",ITIEN'=TIEN Q
 . I $G(TIEN)="",'$D(@TREF@(ITIEN)) Q
 . S RES=$$GET1^DIQ(FREF,IEN,.04,"I")
 . S VISIT=$$GET1^DIQ(FREF,IEN,.03,"I") I VISIT="" Q
 . I $P($G(^AUPNVSIT(VISIT,0)),"^",11)=1 Q
 . S VSDTM=$P($G(^AUPNVSIT(VISIT,0)),"^",1)\1 I VSDTM=0 Q
 . S RDTM=$$GET1^DIQ(FREF,IEN,1201,"I")\1
 . I RDTM'=0 S VSDTM=RDTM
 . S CT=CT+1,RESULT=CT
 . S RESULT(CT)=1_U_VSDTM_U_ITIEN_U_VISIT_U_IEN_U_RES
 ; check for refusal
 I INC D
 . I $O(^AUPNPREF("AA",BQDFN,RREF,""))'="" D
 .. I $D(TREF) S TIEN="" F  S TIEN=$O(@TREF@(TIEN)) Q:TIEN=""  D
 ... S BDT="",STOP=0
 ... F  S BDT=$O(^AUPNPREF("AA",BQDFN,RREF,TIEN,BDT)) Q:BDT=""  D
 .... S IEN="" F  S IEN=$O(^AUPNPREF("AA",BQDFN,RREF,TIEN,BDT,IEN)) Q:IEN=""  D
 ..... S DTM=$P(^AUPNPREF(IEN,0),U,3)
 ..... I $P(RESULT,U,2)'="",DTM<$P(RESULT,U,2) Q
 ..... S CT=CT+1,RESULT=CT
 ..... S RESULT(CT)=1_U_DTM_U_TIEN_U_U_U_U_"refusal"
 ..... S CT=CT-1,RESULT=CT
 I RESULT=0 D  Q
 . I $G(RESULT(1))["refusal" D  Q
 .. I $G(SCOUN)="" S RESULT=$$FMT(1) Q
 .. S RESULT=""
 . S RESULT=""
 NEW NRES,NCT,TIEN,STNM
 S NRES=""
 I $G(IENS)'="" S BQDTFRM=$$GET1^DIQ(90506.51,IENS,.08,"I")
 I $G(BQDTFRM)="" S BQDTFRM="S"
 S NCT=""
 F  S NCT=$O(RESULT(NCT),-1) Q:NCT=""  D
 . S TIEN=$P(RESULT(NCT),"^",3),STNM=$P(^AUTTIMM(TIEN,0),"^",2)
 . S MAX=$$MAX^BQIPTIMM(TIEN)
 . I $G(SCOUN)'="" D  Q
 .. I CT'<MAX S NRES="Complete" Q
 .. E  S NRES="Incomplete"
 . S NRES=NRES_$S(BQDTFRM="S":$$FMTE^BQIUL1($P(RESULT(NCT),U,2)),1:$$FMTMDY^BQIUL1($P(RESULT(NCT),U,2)))_" ("_STNM_")"
 . I $P(RESULT(NCT),U,7)="refusal" S NRES=NRES_"(refusal)"
 . S NRES=NRES_$C(13)_$C(10)
 K RESULT,BQDTFRM S RESULT=NRES
 Q
 ;
FMT(ICT) ;EP - format result
 S NRES=""
 S TIEN=$P(RESULT(ICT),"^",3),STNM=$P(^AUTTIMM(TIEN,0),"^",2)
 S NRES=NRES_$S(BQDTFRM="S":$$FMTE^BQIUL1($P(RESULT(ICT),U,2)),1:$$FMTMDY^BQIUL1($P(RESULT(ICT),U,2)))_" ("_STNM_")"
 I $P(RESULT(ICT),U,7)="refusal" S NRES=NRES_"(refusal)"
 Q NRES
