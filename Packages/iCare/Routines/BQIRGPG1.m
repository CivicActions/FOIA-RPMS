PRVIS(PDFN,TYP,NUM) ;EP - Prenatal visits
 ; "BQIPGVST" = Prenatal Visits
 ; "BQIPGRD" = Prenatal Radiology Visits
 ; "BQIPGLD" = Labor & Delivery Visits
 NEW RES,NM,HLC,VISIT,VCT,VSDTM
 S RES=""
 I $G(NUM)="" D
 . S NM=$G(^XTMP(TYP,"NUM")),VCT=0
 . S HLC="" F  S HLC=$O(^XTMP(TYP,PDFN,"VS",HLC)) Q:HLC=""  D
 .. S VISIT="" F  S VISIT=$O(^XTMP(TYP,PDFN,"VS",HLC,VISIT)) Q:VISIT=""  D
 ... S VCT=VCT+1 I VCT>NM Q
 ... S VSDTM=^XTMP(TYP,PDFN,"VS",HLC,VISIT)
 ... S RES=RES_$P(^SC(HLC,0),"^",1)_" ("_$$FMTMDY^BQIUL1(VSDTM)_")"_$C(10)_$C(13)
