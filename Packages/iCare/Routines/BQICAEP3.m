BASE ;EP Baseline
 ; baseline is from Jan 1, 2016
 NEW FDT,TDT,TMFRAME
 S ^XTMP("BQIOPIOID",0)=$$DATE^BQIUL1("T+12M")_"^"_DT_"^Opioid Baseline"
 D BEG
 S FDT=3160101,TDT=DT
 D OPO(FDT,TDT)
 D FIN
 Q
 ;
OPO(FDT,TDT) ; EP - Opioid surveillance
 S TMFRAME="T-"_$$FMDIFF^XLFDT(DT,FDT)
 D OPDX(TMFRAME)
 F SUBS="PXRM BQI OPIOID PSBL EXPOSURE","PXRM BQI OPIOID EXPOSURE" D OPDS(TMFRAME)
 ;
 ; GO through all found records and generate HL7 message
 K PIEN,VISIT
 S DFN="" F  S DFN=$O(BQDXS(DFN)) Q:DFN=""  D
 . S VDATE="" F  S VDATE=$O(BQDXS(DFN,VDATE)) Q:VDATE=""  D
 .. S PIEN=$O(BQDXS(DFN,VDATE,"PROB","")) I PIEN'="" D
 ... ;I $D(^XTMP("BQIOPIOID",DFN,VDATE,"PROB",PIEN)) Q
 ... ;S ^XTMP("BQIOPIOID",DFN,VDATE,"PROB",PIEN)=DT
 ... D PROB
 .. S PIEN=$O(BQDXS(DFN,VDATE,"POV","")) I PIEN'="" D
 ... ;I $D(^XTMP("BQIOPIOID",DFN,VDATE,"POV",PIEN)) Q
 ... ;S ^XTMP("BQIOPIOID",DFN,VDATE,"POV",PIEN)=DT
 ... D POV
 . ;
 ;
 S DFN="" F  S DFN=$O(BQSNO(DFN)) Q:DFN=""  D
 . S VDATE="" F  S VDATE=$O(BQSNO(DFN,VDATE)) Q:VDATE=""  D
 .. S PIEN=$O(BQSNO(DFN,VDATE,"PROB","")) I PIEN'="" D
 ... ;I $D(^XTMP("BQIOPIOID",DFN,VDATE,"PROB",PIEN)) Q
 ... ;S ^XTMP("BQIOPIOID",DFN,VDATE,"PROB",PIEN)=DT
 ... D PROB
 .. S PIEN=$O(BQSNO(DFN,VDATE,"POV","")) I PIEN'="" D
 ... ;I $D(^XTMP("BQIOPIOID",DFN,VDATE,"POV",PIEN)) Q
 ... ;S ^XTMP("BQIOPIOID",DFN,VDATE,"POV",PIEN)=DT
 ... D POV
 ;
 ;D FIN
 Q
 ;
PROB ; Get data from problem file
 S POS=0,CONF=0 D SCHK
 I CONF S ALERT="Behavorial Health",DIAG="Confirmed Opioid Exposure",GRP="M"
 I POS S DIAG="Possible Opioid Exposure"
 S ALERT="Behavorial Health",GRP="M"
 S DXN=$$GET1^DIQ(9000011,PIEN_",",.01,"E"),VFILE=9000011
 S SNO=$$GET1^DIQ(9000011,PIEN_",",80001,"E"),VISIT=PIEN
 S COMM=$$GET1^DIQ(9000001,DFN_",",1117,"I")
 I COMM="" S COMM=$$COMM()
 D NFILE(COMM,DIAG,DXN,VDATE,VISIT,DFN,VFILE)
 ;D EXP
 K PIEN,VISIT,COMM
 Q
 ;
POV ; Get data from POV file
 S VISIT=$P($G(^AUPNVPOV(PIEN,0)),"^",3) I VISIT="" Q
 S COMM=$$GET1^DIQ(9000001,DFN_",",1117,"I")
 I COMM="" S COMM=$$COMM()
 S POS=0,CONF=0 D SCHK
 I CONF S ALERT="Behavorial Health",DIAG="Confirmed Opioid Exposure",GRP="M"
 I POS S DIAG="Possible Opioid Exposure"
 S ALERT="Behavorial Health",GRP="M"
 S DXN=$$GET1^DIQ(9000010.07,PIEN_",",.01,"E")
 S SNO=$$GET1^DIQ(9000010.07,PIEN_",",1101,"E")
 S VFILE=9000010.07
 D NFILE(COMM,DIAG,DXN,VDATE,VISIT,DFN,VFILE)
 ;D EXP
 K PIEN,VISIT,COMM
 Q
 ;
NFILE(COMM,DIAG,DXN,DATE,VISIT,DFN,VFILE) ;
 ; Input
 ;   COMM  - Community
 ;   DIAG  - Diagnosis Category
 ;   DXN   - Diagnosis Code
 ;   DATE  - Event Date
 ;   VISIT - Visit to make it unique
 ;   PT    - DFN
 ; Assumed that the Alert Type is Behavorial Health
 NEW DIC,DA,AIEN,CIEN,DIEN,RIEN,NFLG,USR
 ;  Set the community
 S DIC="^BQI(90507.6,",X="`"_COMM,DIC(0)="LMZ"
 D ^DIC
 S CIEN=+Y
 I CIEN=-1 S (X,DINUM)=COMM K DO,DD D FILE^DICN S CIEN=+Y
 ;  Set the Alert Type
 S DA(1)=CIEN,X="Behavorial Health",DIC="^BQI(90507.6,"_DA(1)_",1,",DIC(0)="LMN"
 I $G(^BQI(90507.6,DA(1),1,0))="" S ^BQI(90507.6,DA(1),1,0)="^90507.61A^^"
 D ^DIC
 S AIEN=+Y
 ;  Set the DX Category
 S DA(2)=CIEN,DA(1)=AIEN,X=DIAG,DIC(0)="LMN"
 S DIC="^BQI(90507.6,"_DA(2)_",1,"_DA(1)_",1,"
 I $G(^BQI(90507.6,DA(2),1,DA(1),1,0))="" S ^BQI(90507.6,DA(2),1,DA(1),1,0)="^90507.611A^^"
 D ^DIC
 S DIEN=+Y
 ;  Set the Dx Code
 K X
 ;S DA(3)=CIEN,DA(2)=AIEN,DA(1)=DIEN,X(1)=DXC,X(2)=DATE,X(3)=VISIT,DIC(0)="LN",D="C"
 S DA(3)=CIEN,DA(2)=AIEN,DA(1)=DIEN,X(1)=DXN,X(2)=DATE,X(3)=VISIT,DIC(0)="LN",D="D"
 S DIC="^BQI(90507.6,"_DA(3)_",1,"_DA(2)_",1,"_DA(1)_",1,"
 I $G(^BQI(90507.6,DA(3),1,DA(2),1,DA(1),1,0))="" S ^BQI(90507.6,DA(3),1,DA(2),1,DA(1),1,0)="^90507.6111A^^"
 D IX^DIC
 I Y=-1 D
 . K X,D
 . S X(1)=DXN,X(2)=DATE,X(3)=VISIT,D="C"
 . D IX^DIC
 S (RIEN,DA)=+Y,NFLG=+$P(Y,U,3)
 S IENS=$$IENS^DILF(.DA)
 S BQIUPD(90507.6111,IENS,.02)=DATE
 S BQIUPD(90507.6111,IENS,.03)=PIEN
 S BQIUPD(90507.6111,IENS,.04)=DFN
 S BQIUPD(90507.6111,IENS,.05)=VFILE
 S BQIUPD(90507.6111,IENS,.06)=VISIT
 I $D(BQIUPD) D FILE^DIE("","BQIUPD","ERROR")
 Q
 ;
SCHK ;
 I $G(BQSNO(DFN,VDATE,"PROB",PIEN))="PXRM BQI OPIOID EXPOSURE" S CONF=1 K BQSNO(DFN,VDATE,"PROB",PIEN) Q
 I $G(BQSNO(DFN,VDATE,"PROB",PIEN))="PXRM BQI OPIOID PSBL EXPOSURE" S POS=1 K BQSNO(DFN,VDATE,"PROB",PIEN) Q
 S CONF=1
 Q
 ;
OPDX(TMFRAME) ;
 S TMFRAME=$G(TMFRAME,"")
 I TMFRAME'="" S BGDT=$$DATE^BQIUL1(TMFRAME),ENDT=DT
 I TMFRAME="" S BGDT=$O(^AUPNVSIT("B",""))\1,ENDT=DT
 S TREF=$NA(^TMP("BQITAX",$J)) K @TREF
 D BLD^BQITUTL("BQI OPIOID HCUP DXS",TREF)
 S TIEN="" F  S TIEN=$O(@TREF@(TIEN)) Q:TIEN=""  D
 . S PIEN="" F  S PIEN=$O(^AUPNPROB("B",TIEN,PIEN)) Q:PIEN=""  D
 .. S PRDT=$$PROB^BQIUL1(PIEN)
 .. I PRDT'="",PRDT<BGDT Q
 .. S BQDFN=$P(^AUPNPROB(PIEN,0),"^",2) S:PRDT="" PRDT="~"
 .. S BQDXS(BQDFN,PRDT,"PROB",PIEN)=TIEN
 . S IEN=""
 . F  S IEN=$O(^AUPNVPOV("B",TIEN,IEN)) Q:IEN=""  D
 .. ;  if a bad record (no zero node), quit
 .. I $G(^AUPNVPOV(IEN,0))="" Q
 .. S VISIT=$P(^AUPNVPOV(IEN,0),"^",3),VSDTM=$P($G(^AUPNVSIT(VISIT,0)),"^",1)\1 I VSDTM=0 Q
 .. I TMFRAME'="",VSDTM<BGDT Q
 .. S BQDFN=$P(^AUPNVPOV(IEN,0),"^",2)
 .. S BQDXS(BQDFN,VSDTM,"POV",IEN)=TIEN
 Q
 ;
OPDS(TMFRAME) ;
 K RESULT
 S TMFRAME=$G(TMFRAME,"")
 I TMFRAME'="" S BGDT=$$DATE^BQIUL1(TMFRAME),ENDT=DT
 I TMFRAME="" S BGDT=$O(^AUPNVSIT("B",""))\1,ENDT=DT
 S TREF=$NA(^TMP("BQISNOM",$J)) K @TREF
 S BQIOK=$$SUBLST^BSTSAPI(TREF,SUBS_"^36^1")
 I 'BQIOK Q
 S SNIEN=""
 F  S SNIEN=$O(@TREF@(SNIEN)) Q:SNIEN=""  D
 . S SCID=$P(@TREF@(SNIEN),U,1)
 . S PIEN="" F  S PIEN=$O(^AUPNPROB("ASCT",SCID,PIEN)) Q:PIEN=""  D
 .. S PRDT=$$PROB^BQIUL1(PIEN)
 .. I PRDT'="",PRDT<BGDT Q
 .. S BQDFN=$P(^AUPNPROB(PIEN,0),"^",2) S:PRDT="" PRDT="~"
 .. S BQSNO(BQDFN,PRDT,"PROB",PIEN)=SUBS
 . ;
 . S IEN=""
 . F  S IEN=$O(^AUPNVPOV("ASCI",SCID,IEN)) Q:IEN=""  D
 .. ;  if a bad record (no zero node), quit
 .. I $G(^AUPNVPOV(IEN,0))="" Q
 .. I $P($G(^AUPNVPOV(IEN,11)),U,1)'=SCID Q
 .. S VISIT=$P(^AUPNVPOV(IEN,0),"^",3),VSDTM=$P($G(^AUPNVSIT(VISIT,0)),"^",1)\1 I VSDTM=0 Q
 .. I TMFRAME'="",VSDTM<BGDT Q
 .. S BQDFN=$P(^AUPNVPOV(IEN,0),"^",2)
 .. S BQSNO(BQDFN,VSDTM,"POV",IEN)=SUBS
 Q
 ;
BEG ;EP Begin Export
 S ASUN=$P(^AUTTSITE(1,0),U),ASUFAC=$P($G(^AUTTLOC(ASUN,0)),U,10),ASUNM=$P(^DIC(4,ASUN,0),U)
 S CT=0,N=0,DELIM=","
 ;K ^BQIDATA($J)
 ; Get export format type 'D' is delimited and 'H' or blank is HL7
 S FRM=$P($G(^BQI(90508,1,0)),U,2)
 ;S IN=$S(FRM="D":1,1:0)
 S IN=^BQIDATA($J)
 I FRM="D",$G(^BQIDATA($J))=0 S HDR=$$JDATE(DT)_DELIM_CT_DELIM_ASUNM,^BQIDATA($J,IN)=HDR
 Q
 ;
EXP ;EP Export
 S DELIM=",",FRM=$P($G(^BQI(90508,1,0)),U,2)
 S LOC=$S($G(VISIT)'="":$P($G(^AUPNVSIT(VISIT,0)),U,6),$G(PIEN)'="":$P($G(^AUPNPROB(PIEN,0)),"^",6),1:DUZ(2))
 S DIAG=$$STRIP^XLFSTR(DIAG,",")
 ; Unique Identifier
 S RECORD=$$UID(DFN)
 ; HRN
 S $P(RECORD,DELIM,2)=$S($$HRN^AUPNPAT(DFN,LOC)]"":$$HRN^AUPNPAT(DFN,LOC),1:$$HRN^AUPNPAT(DFN,DUZ(2)))
 ; Gender
 S $P(RECORD,DELIM,3)=$P(^DPT(DFN,0),U,2)
 ; DOB
 S $P(RECORD,DELIM,4)=$S(FRM="D":$$JDATE($P($G(^DPT(DFN,0)),U,3)),1:$$FMTHL7^XLFDT($P($G(^DPT(DFN,0)),U,3)))
 ; Age
 S $P(RECORD,DELIM,5)=$P($$AGE^BQIAGE(DFN,"",1)," ",1)
 ; Age Units
 S $P(RECORD,DELIM,6)=$P($$AGE^BQIAGE(DFN,"",1)," ",2)
 ; Patient Street Address
 S $P(RECORD,DELIM,7)=$$GET1^DIQ(2,DFN_",",.111,"E")
 ; Patient Address City
 S $P(RECORD,DELIM,8)=$$GET1^DIQ(2,DFN_",",.114,"E")
 ; Patient Address State
 NEW ST
 S ST=$$GET1^DIQ(2,DFN_",",.115,"I")
 S $P(RECORD,DELIM,9)=$$PTR^BQIUL2(2,.115,ST,1)
 ; Patient Address Zip
 S $P(RECORD,DELIM,10)=$S($$GET1^DIQ(2,DFN_",",.1112,"E")'="":$$GET1^DIQ(2,DFN_",",.1112,"E"),1:$$GET1^DIQ(2,DFN_",",.116,"E"))
 ; Patient County
 S $P(RECORD,DELIM,11)=$$COUN^BQIULPT(DFN)
 ; Current community of residence
 S $P(RECORD,DELIM,12)=$$COMMRES^AUPNPAT(DFN,"C")
 ; Race
 NEW RACE,RCN
 S RACE=$$RCE^BQIPTDMG(DFN,.01),RCN=$P(RACE,$C(28),1)
 I RCN'="" S $P(RECORD,DELIM,13)=$P(^DIC(10,RCN,0),U,3)
 ; Ethnicity
 NEW ETHN,ETN
 S ETHN=$$ETHN^BQIPTDMG(DFN,.01),ETN=$P(ETHN,$C(28),1)
 I ETN'="" S $P(RECORD,DELIM,14)=$P(^DIC(10.2,ETN,0),U,2)
 ; ASUFAC of encounter location
 S $P(RECORD,DELIM,15)=$S(LOC'="":$P($G(^AUTTLOC(LOC,0)),U,10),1:"")
 ; Visit Date
 S $P(RECORD,DELIM,16)=$S(FRM="D":$$JDATE(VDATE),1:$$FMTHL7^XLFDT(VDATE))
 ; Visit ID
 S $P(RECORD,DELIM,29)="P"
 I $G(VISIT)'="" S $P(RECORD,DELIM,17)=$S($P($G(^AUPNVSIT(VISIT,11)),U,14)]"":$P($G(^AUPNVSIT(VISIT,11)),U,14),1:$$UIDV^AUPNVSIT(VISIT)),$P(RECORD,DELIM,29)="V"
 ; Dxn ICD code
 S $P(RECORD,DELIM,18)=DXN
 ; SNOMED code
 S $P(RECORD,DELIM,34)=SNO
 ; CDC diagnosis narrative
 S $P(RECORD,DELIM,19)=DIAG
 ; Type of alert
 S $P(RECORD,DELIM,20)=ALERT
 ; Group
 S $P(RECORD,DELIM,21)=GRP
 ; Visit last modified
 S DTLMD=$S(VFILE=9000011:$P($G(^AUPNPROB(PIEN,0)),"^",3),VFILE'[9000010:$P($G(^AMHREC(VISIT,11)),U,14),1:$P($G(^AUPNVSIT(VISIT,0)),U,13))
 S $P(RECORD,DELIM,22)=$S(FRM="D":$$JDATE(DTLMD),1:$$FMTHL7^XLFDT(DTLMD))
 ; Highest Temperature for OBX
 S $P(RECORD,DELIM,23)=""
 ; Vitals for OBX
 S $P(RECORD,DELIM,24)=""
 ;
 S IN=$O(^BQIDATA($J,""),-1)+1
 S ^BQIDATA($J,IN)=RECORD,LIN=IN
 S ^BQIDATA($J)=IN
 Q
 ;
FIN ; EP - Finish file
 ; If HL7
 I $G(FRM)'="D" D ^BQICAHLO
 D WRITE
 Q
 ;
UID(BQIDFN) ;EP - Given DFN return unique patient record id.
 I $G(BQIDFN)="" Q ""
 I $G(^AUPNPAT(BQIDFN,0))="" Q ""
 I $G(^DPT(BQIDFN,0))="" Q ""
 Q $$GET1^DIQ(9999999.06,$P(^AUTTSITE(1,0),U),.32)_$E("0000000000",1,10-$L(BQIDFN))_BQIDFN
 ;
JDATE(DATE) ;EP - Format the date
 I $G(DATE)="" Q ""
 NEW A
 S A=$$FMTE^XLFDT(DATE)
 Q $E(DATE,6,7)_$$UP^XLFSTR($P(A," ",1))_(1700+$E(DATE,1,3))
 ;
DATE(D) ;
 Q (1700+$E(D,1,3))_$E(D,4,5)_$E(D,6,7)
 ;
 ;send file
WRITE ; use XBGSAVE to save the temp global (BQIDATA) to a file that is exported
 S FLNM="BQIOPO"
 NEW XBGL,XBQ,XBQTO,XBNAR,XBMED,XBFLT,XBUF,XBFN
 S XBMED="F",XBQ="N",XBFLT=1,XBF=$J,XBE=$J
 S XBGL=$S($G(FRM)="D":"BQIDATA",1:"BQIHL7")
 S XBNAR="OPIOID SURVEILLANCE EXPORT"
 S ASUFAC=$P($G(^AUTTLOC($P(^AUTTSITE(1,0),U),0)),U,10)  ;asufac for file name
 S XBFN=FLNM_"_"_ASUFAC_"_"_$$DATE(DT)_".txt"
 S XBS1="CANE SURVEILLANCE SEND"
 S XBUF=$P($G(^AUTTSITE(1,1)),"^",2)
 I XBUF="" S XBUF=$P($G(^XTV(8989.3,1,"DEV")),"^",1)
 ;
 D ^XBGSAVE
 ;
 K ^BQIDATA($J),^BQIHL7($J),@TREF
 K ASUFAC,BGDT,BQDFN,BQDXS,BQSNO,BQIOK,BQQN,DFN,HLSTATE,I,IEN,ENDT,FLNM
 K N,NMID,PIEN,PRDT,SCID,SNIEN,SUBS,TIEN,TMFRAME,TREF,VDATE,VSDTM,XBFLG
 K XBPAFN,XBS1,ZISHC,ZISHDA1
 K ALERT,CONF,DELIM,DIAG,DXN,FRM,GRP,IN,LIN,LOC,POP,POS,RECORD,SNO,VFILE
 Q
