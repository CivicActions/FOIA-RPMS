OCON(DATA,TYPE) ;Other Consult cross-references
 NEW GLOB,FIND,II,SVC,DESC
 I $G(TYPE)="CDURG" S GLOB=$NA(^XTMP("BQICNURG")),FIND=$NA(^ORD(101))
 I $G(TYPE)="CDPLAC" S GLOB=$NA(^XTMP("BQICNPLC")),FIND=$NA(^ORD(101))
 I $G(TYPE)="CDSTAT" S GLOB=$NA(^XTMP("BQICNSTA")),FIND=$NA(^ORD(100.01))
 I $G(TYPE)="CDSNOM" S GLOB=$NA(^XTMP("BQICNSNM")),FIND=$NA(^BSTS(9002318.4,"C",36))
 I $G(TYPE)="CDOFAC" S GLOB=$NA(^XTMP("BQICNOFAC")),FIND=$NA(^DIC(4))
 I $G(TYPE)="CDATTEN" S GLOB=$NA(^XTMP("BQICNATT")),FIND=$NA(^VA(200))
 ;
 K @DATA
 S II=0,SVC=0
 I FIND'["BSTS" S @DATA@(II)="I00010IEN^T00030DESC"_$C(30)
 I FIND["BSTS" S @DATA@(II)="T00030IEN^T00030DESC"_$C(30)
 F  S SVC=$O(@GLOB@(SVC)) Q:SVC=""  D
 . I FIND'["BSTS" D
 .. S DESC=$P(@FIND@(SVC,0),"^",1)
 .. I FIND["ORD(101)" S DESC=$P(DESC,"- ",2)
 . I FIND["BSTS" D
 .. S SN=$O(@FIND@(SVC,"")) I SN="" Q
 .. S DESC=$G(^BSTS(9002318.4,SN,1))
 . S II=II+1,@DATA@(II)=SVC_"^"_DESC_$C(30)
 S II=II+1,@DATA@(II)=$C(31)
 Q
