NAT(DATA) ;EP Nature of Order
 NEW RN,II
 K @DATA
 S II=0,RN=0
 S @DATA@(II)="I00010IEN^T00030DESC"_$C(30)
 F  S RN=$O(^ORD(100.02,RN)) Q:'RN  D
 . I $P(^ORD(100.02,RN,0),"^",4)=1 Q
 . S II=II+1,@DATA@(II)=RN_"^"_$P(^ORD(100.02,RN,0),"^",1)_$C(30)
 S II=II+1,@DATA@(II)=$C(31)
 Q
