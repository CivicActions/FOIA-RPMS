DATE(FNAME,FVAL,VALUE) ;
 I FNAME["FROM"!(FNAME="ORSTARTF")!(FNAME="ORSTOPF")!(FNAME="HRNFD") D
 . I FVAL'="" S VALUE="from date "_FVAL
 I FNAME["THRU"!(FNAME="ORSTARTT")!(FNAME="ORSTOPT")!(FNAME="HRNTD") D
 . I FVAL'="" S VALUE=" thru date "_FVAL
 I FNAME["RANGE" D
 . S PPIEN=$$PP^BQIDCDF(FSOURCE) I PPIEN="" S VALUE="" Q
 . I $G(PPIEN)'="" D RANGE^BQIDCAH1(FVAL,PPIEN,FNAME)
 . I FVAL'["Ever" S VALUE=FVAL_" ("_$$FMTE^BQIUL1(RFROM)_"-"_$$FMTE^BQIUL1(RTHRU)_")"
 . I FVAL["Ever" S VALUE=FVAL
 . i FNAME'="RMDRANGE" S VALUE="for timeframe "_VALUE
 . I FNAME="RMDRANGE" S VALUE=" in "_VALUE
 . K PRANGE,RFROM,RTHRU
 Q
