 S UID=$S($G(ZTSK):"Z"_ZTSK,1:$J)
 S PTREF=$NA(^TMP("BQITAXIPL",UID)) K @PTREF
 S DXCD=$G(DXCD,""),TAX=$G(TAX,""),BDOO=$G(BDOO,"")
 I $G(TAX)'="" D BLD^BQITUTL(TAX,.PTREF)
 I $G(DXCD)'="" D BLDSV^BQITUTL(80,DXCD,.PTREF)
 I TMFRAME'="" S BDATE=$$DATE^BQIUL1(TMFRAME),EDATE=DT
 S IEN="",QFL=0,RESULT=0
 F  S IEN=$O(^AUPNPROB("AC",PTDFN,IEN),-1) Q:IEN=""  D  Q:QFL
 . S TIEN=$P($G(^AUPNPROB(IEN,0)),"^",1) I TIEN="" Q
 . I '$D(@PTREF@(TIEN)) Q
 . ;  Check class - if Family ignore
 . I $P($G(^AUPNPROB(IEN,0)),"^",4)="F" Q
 . S STAT=$P($G(^AUPNPROB(IEN,0)),"^",12)
 . I STAT'="A"&(STAT'="E") Q
 . S VSDTM=$$PROB^BQIUL1(IEN)\1 Q:VSDTM=0
 . I $G(TMFRAME)'="",VSDTM<BDATE Q
 . I BDOO=1 S VSDTM=$$PROBO^BQIUL1(IEN),VSDTM=$$FMTE^BQIUL1(VSDTM)
 . I BDOO'=1 S VSDTM=$$FMTMDY^BQIUL1(VSDTM)
 . S RESULT=1_U_VSDTM_U_TIEN_U_IEN,QFL=1
 ;
 I RESULT=0 Q ""
 I TYPE="O" Q $P(RESULT,"^",2)
 I TYPE="X" D  Q DESC
 . S TIEN=$P(RESULT,"^",3),DESC=$$ICD9^BQIUL3(TIEN,,4)_" ("_$P(^ICD9(TIEN,0),"^",1)_")"
 Q ""
 ;
SHOT(BQDFN,TYPE,NUM) ;EP - Covid Immunizations
 NEW TREF,TIEN,TAX,IEN,CT,RESULT,ITIEN,VISIT,VSDTM,IDATA,STNM,LOC
 NEW SCAT,LCN,SCTN,DATE,UID,BNUM
 S UID=$S($G(ZTSK):"Z"_ZTSK,1:$J)
 S TAX="BQI COVID IZ CVX CODES"
 S TREF=$NA(^TMP("BQITAXSHT",UID)) K @TREF
 D BLD^BQITUTL(TAX,TREF)
 S RREF=9999999.14
 S IEN="",CT=0,RESULT=CT
 F  S IEN=$O(^AUPNVIMM("AC",BQDFN,IEN),-1) Q:'IEN  D
 . S ITIEN=$P($G(^AUPNVIMM(IEN,0)),"^",1) I ITIEN="" Q
 . I $G(TIEN)'="",ITIEN'=TIEN Q
 . I $G(TIEN)="",'$D(@TREF@(ITIEN)) Q
 . S VISIT=$P($G(^AUPNVIMM(IEN,0)),"^",3) I VISIT="" Q
 . I $P($G(^AUPNVSIT(VISIT,0)),"^",11)=1 Q
 . S VSDTM=$P($G(^AUPNVSIT(VISIT,0)),"^",1)\1 I VSDTM=0 Q
 . S RDTM=$P($G(^AUPNVIMM(IEN,12)),"^",1)\1
 . I RDTM'=0 S VSDTM=RDTM
 . S CT=CT+1,RESULT=CT
 . S RESULT(VSDTM,CT)=1_U_VSDTM_U_ITIEN_U_VISIT_U_IEN_U
 ; check for refusal
 I $O(^AUPNPREF("AA",BQDFN,RREF,""))'="" D
 . I $D(TREF) S TIEN="" F  S TIEN=$O(@TREF@(TIEN)) Q:TIEN=""  D
 .. S BDT="",STOP=0
 .. F  S BDT=$O(^AUPNPREF("AA",BQDFN,RREF,TIEN,BDT)) Q:BDT=""  D
 ... S IEN="" F  S IEN=$O(^AUPNPREF("AA",BQDFN,RREF,TIEN,BDT,IEN)) Q:IEN=""  D
 .... S RFDTM=$P(^AUPNPREF(IEN,0),U,3)
 .... I $P(RESULT,U,2)'="",DTM<$P(RESULT,U,2) Q
 .... S CT=CT+1,RESULT=CT
 .... S RESULT(RFDTM,CT)=1_U_RFDTM_U_TIEN_U_U_U_U_"refusal"
 I RESULT=0 Q ""
 I TYPE="T" Q CT
 ;
 K RSDTM,RSDTM1,RSDTM2,RSDTM3
 S RSDTM1=$O(RESULT(""))
 I RSDTM1'="" S RSDTM2=$O(RESULT(RSDTM1))
 I RSDTM2'="" S RSDTM3=$O(RESULT(RSDTM2))
 I NUM=1 S BNUM=$O(RESULT(RSDTM1,"")),RSDTM=RSDTM1
 I NUM=2,$G(RSDTM2)="" Q ""
 I NUM=2,$G(RSDTM2)'="" S BNUM=$O(RESULT(RSDTM2,"")),RSDTM=RSDTM2
 I NUM=3,$G(RSDTM3)="" Q ""
 I NUM=3,$G(RSDTM3)'="" S BNUM=$O(RESULT(RSDTM3,"")),RSDTM=RSDTM3
 ;
 S IDATA=$G(RESULT(RSDTM,BNUM)) I IDATA="" Q ""
 S TIEN=$P(IDATA,"^",3),STNM=$P(^AUTTIMM(TIEN,0),"^",2)
 I $P(IDATA,"^",7)="refusal" S STNM=STNM_" (refusal)"
 S VISIT=$P(IDATA,"^",4) I VISIT'="" S SCTN=$P(^AUPNVSIT(VISIT,0),"^",7),LCN=$P(^AUPNVSIT(VISIT,0),"^",6)
 I VISIT="" S SCTN="",LCN=""
 S LOC="" I LCN'="" S LOC=$P(^DIC(4,LCN,0),"^",1)
 S SCAT="" I SCTN'="" S SCAT=$$STC^BQIUL2(9000010,.07,SCTN)
 S DATE=$$FMTMDY^BQIUL1($P(IDATA,U,2))
 I TYPE="D" Q DATE
 I TYPE="N" Q STNM
 I TYPE="L" Q LOC
 I TYPE="C" Q SCAT
 Q ""
 ;
