 .. S IEN="" F  S IEN=$O(^AUPNVCPT("AA",BQDFN,TIEN,BDT,IEN)) Q:IEN=""  D
 ... S VISIT=$P($G(^AUPNVCPT(IEN,0)),"^",3) I VISIT="" Q
