LCVLB(BQDFN,TMFRAME,TIM) ;EP - Last COVID lab test by patient
 NEW TAX,TREF,TEMP,LIEN,TIEN,BDT,EDT,BDATE,EDATE,VSDTM,CT,RES,BVALUE,OVALUE,QFL,FLAG,CLDTM
 S UID=$S($G(ZTSK):"Z"_ZTSK,1:$J)
 S TREF=$NA(^XTMP("BQITAX",UID))
 F TAX="BQI COVID QUAL ANTIBODY LOINC","BQI COVID QUAN ANTIBODY LOINC","BQI COVID ANTIGEN LOINC" D BLD^BQITUTL(TAX,.TREF)
 F TAX="BQI COVID QUAL ANTIBODY TAX","BQI COVID QUAN ANTIBODY TAX","BQI COVID ANTIGEN TEST TAX" D BLD^BQITUTL(TAX,.TREF,"L")
 F TAX="BQI COVID QUAL TESTING LOINC","BQI COVID QUAN TESTING LOINC" D BLD^BQITUTL(TAX,.TREF)
 F TAX="BQI COVID QUAN TESTING TAX","BQI COVID QUAL TESTING TAX" D BLD^BQITUTL(TAX,.TREF,"L")
 ;
 I $G(TMFRAME)'="" S BDATE=$$DATE^BQIUL1(TMFRAME),EDATE=DT
 I $G(TMFRAME)="" S BDATE="",EDATE=""
 S TEMP=$NA(^TMP("BQITEMP",UID)) K @TEMP
 S LIEN="",QFL=0,RES=0_U_"No Test",CT=0
 S TIEN=""
 F  S TIEN=$O(@TREF@(TIEN)) Q:TIEN=""  D
 . I $G(TMFRAME)'="" S EDT=9999999-BDATE,BDT=(9999999-EDATE)-.001
 . I $G(TMFRAME)="" S EDT=EDATE,BDT=BDATE
 . I $P($G(^LAB(60,TIEN,0)),U,4)="MI" D MIC^BQICAUTL(BQDFN,TIEN,EDT,BDT,.MICRO) Q
 . F  S BDT=$O(^AUPNVLAB("AA",BQDFN,TIEN,BDT)) Q:BDT=""  D
 .. S LIEN=""
 .. F  S LIEN=$O(^AUPNVLAB("AA",BQDFN,TIEN,BDT,LIEN)) Q:LIEN=""  D
 ... S BVALUE=$P(^AUPNVLAB(LIEN,0),U,4)
 ... S VIEN=$P(^AUPNVLAB(LIEN,0),U,3) I VIEN="" Q
 ... S FLAG=$P($G(^AUPNVLAB(LIEN,11)),U,9) I FLAG="" Q
 ... ;I FLAG'="R"&(FLAG'="M") Q
 ... S VSDTM=$P($G(^AUPNVSIT(VIEN,0)),"^",1) I VSDTM="" Q
 ... S CLDTM=$P($G(^AUPNVLAB(LIEN,12)),"^",1)
 ... I CLDTM="" S CLDTM=VSDTM
 ... S VSDTM=CLDTM
 ... ; quit if deleted flag
 ... I $P($G(^AUPNVSIT(VIEN,0)),U,11)=1 Q
 ... I $P($G(^AUPNVLAB(LIEN,11)),U,9)="D" Q
 ... S @TEMP@(VSDTM,VIEN,LIEN)=BVALUE_U_TIEN
 ;
 S VSDTM="",CT=0
 F  S VSDTM=$O(@TEMP@(VSDTM),-1) Q:VSDTM=""  D
 . S VIEN="" F  S VIEN=$O(@TEMP@(VSDTM,VIEN)) Q:VIEN=""  D
 .. S LIEN="" F  S LIEN=$O(@TEMP@(VSDTM,VIEN,LIEN)) Q:LIEN=""  D
 ... S BVALUE=$P(@TEMP@(VSDTM,VIEN,LIEN),U,1),OVALUE=BVALUE
 ... S TIEN=$P(@TEMP@(VSDTM,VIEN,LIEN),U,2)
 ... S CT=CT+1,RES(CT)=1_"^"_$$FMTMDY^BQIUL1(VSDTM\1)_"^"_BVALUE_"^"_$P(^LAB(60,TIEN,0),"^",1)
 I TIM="C" S RES=$G(RES(1))
 I TIM="P" S RES=$G(RES(2))
 I TIM="T" S RES=CT
 I $G(RES)="" S RES=0_"^^^"
 K @TEMP,@TREF
 Q RES
 ;
